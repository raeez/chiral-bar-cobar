def build_structure_tensor(sc_dict, basis):
    """Convert structure constants dict to numpy tensor f[a,b,c].

    f[a,b,c] = coefficient of basis[c] in [basis[a], basis[b]].
    """
    d = len(basis)
    name_to_idx = {name: i for i, name in enumerate(basis)}
    f = np.zeros((d, d, d), dtype=np.float64)
    for (a_name, b_name), outputs in sc_dict.items():
        a_idx = name_to_idx[a_name]
        b_idx = name_to_idx[b_name]
        for c_name, coeff in outputs.items():
            if c_name in name_to_idx:
                f[a_idx, b_idx, name_to_idx[c_name]] = float(coeff)
    return f


def bar_differential_matrix(f_abc, dim_g, n):
    """Build the bar differential d: BМ„вЃї в†’ BМ„вЃївЃ»В№ for a Lie algebra.

    BМ„вЃї has basis indexed by (aв‚Ѓ,...,aв‚™) в€€ {0,...,d-1}^n, dim = d^n.
    BМ„вЃївЃ»В№ has basis indexed by (bв‚Ѓ,...,b_{n-1}) в€€ {0,...,d-1}^{n-1}, dim = d^{n-1}.

    d([aв‚Ѓ|...|aв‚™]) = ОЈ_{i<j} (-1)^{i+j} [aв‚Ѓ|...|Гў_i|...|[aбµў,aв±ј]|...|Гў_j|...|aв‚™]

    where [aбµў,aв±ј] replaces position j (removing position i, putting bracket at j).
    Convention: bracket result goes to position min(i,j), remaining shift.

    Actually, the standard bar differential for an associative algebra is:
      d([aв‚Ѓ|...|aв‚™]) = ОЈбµўв‚Њв‚ЃвЃївЃ»В№ (-1)^i [aв‚Ѓ|...|aбµўВ·aбµўв‚Љв‚Ѓ|...|aв‚™]

    For the COCOMMUTATIVE (Lie) case, it's:
      d([aв‚Ѓ|...|aв‚™]) = ОЈ_{i<j} (-1)^{...} [aв‚Ѓ|...|[aбµў,aв±ј]|...|Гў_j|...|aв‚™]

    Let me use the standard bar differential (adjacent pairs) first and see
    if it gives the right answer for slв‚‚. If not, try the Lie version.

    Returns: scipy sparse matrix or numpy array.
    """
    d = dim_g
    source_dim = d ** n
    target_dim = d ** (n - 1)

    if n <= 1:
        return np.zeros((target_dim, source_dim))

    # Build matrix
    # Use sparse construction for large matrices
    rows = []
    cols = []
    vals = []

    for col_idx in range(source_dim):
        # Decode column index to tuple (aв‚Ѓ,...,aв‚™)
        a = []
        temp = col_idx
        for _ in range(n):
            a.append(temp % d)
            temp //= d
        # a is in little-endian: a[0] is the first generator
        # So a = (aв‚Ђ, aв‚Ѓ, ..., a_{n-1})

        # Standard bar differential: compose adjacent pairs
        for i in range(n - 1):
            sign = (-1) ** (i + 1)  # Standard Koszul sign
            # Compose a[i] and a[i+1] using the bracket
            for c in range(d):
                coeff = f_abc[a[i], a[i+1], c]
                if abs(coeff) < 1e-15:
                    continue
                # Result tuple: a[0],...,a[i-1], c, a[i+2],...,a[n-1]
                b = a[:i] + [c] + a[i+2:]
                row_idx = sum(b[k] * d**k for k in range(n-1))
                rows.append(row_idx)
                cols.append(col_idx)
                vals.append(sign * coeff)

    if len(rows) == 0:
        return np.zeros((target_dim, source_dim))

    # Build dense matrix (for moderate sizes)
    M = np.zeros((target_dim, source_dim))
    for r, c, v in zip(rows, cols, vals):
        M[r, c] += v

    return M


