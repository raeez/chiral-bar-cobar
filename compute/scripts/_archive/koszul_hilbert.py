#!/usr/bin/env python3
r"""Compute bar cohomology = Koszul dual Hilbert series for chiral algebras.

For a chiral algebra A generated by V with quadratic relations R in V^2,
the Koszul dual A! has:
  A!_0 = C, A!_1 = V*, A!_2 = (V*^2)/R^perp, ...

The bar cohomology H^n = dim A!_n.

For KM algebras: V = g (Lie algebra), the bracket [,]: V^2 -> V gives
R = ker([,]) = Sym^2(V) (for semisimple g with [g,g]=g).
R^perp = Lambda^2(V*) (antisymmetric dual tensors).

The CHIRAL bar cohomology uses the OS algebra structure:
  Free_n = V^{tensor n} * OS^{n-1}(C_n)
  Ideal_n = sum over pairs (i,j) of R^perp_{ij} * OS-forms-with-eta_ij

  A!_n = Free_n / Ideal_n
  dim A!_n = dim Free_n - dim Ideal_n
"""

import numpy as np
from math import factorial
from itertools import combinations
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from chiral_bar_cohomology import nbc_basis


def bracket_kernel_sl2():
    """Return R = ker([,]: V tensor V -> V) for sl_2.

    Basis e=0, h=1, f=2. R = Sym^2(V), a 6-dim subspace of V^2 (dim 9).
    Returns: list of 6 vectors in R^9.
    """
    d = 3
    # Structure constants: [e,f]=h, [f,e]=-h, [h,e]=2e, [e,h]=-2e, [h,f]=-2f, [f,h]=2f
    # Bracket map: d x d^2 matrix M where M[c, a*d+b] = f^{ab}_c
    f_abc = np.zeros((d, d, d))
    f_abc[0, 2, 1] = 1   # [e,f] = h
    f_abc[2, 0, 1] = -1  # [f,e] = -h
    f_abc[1, 0, 0] = 2   # [h,e] = 2e
    f_abc[0, 1, 0] = -2  # [e,h] = -2e
    f_abc[1, 2, 2] = -2  # [h,f] = -2f
    f_abc[2, 1, 2] = 2   # [f,h] = 2f

    # Build bracket matrix: 3 x 9
    M = np.zeros((d, d * d))
    for a in range(d):
        for b in range(d):
            for c in range(d):
                M[c, a * d + b] = f_abc[a, b, c]

    # R = kernel of M
    _, s, Vt = np.linalg.svd(M)
    rank = np.sum(s > 1e-10)
    R_basis = Vt[rank:]  # rows are kernel vectors
    print(f"sl_2: bracket rank = {rank}, dim R = {len(R_basis)}")
    return R_basis, d


def bracket_kernel_sl3():
    """Return R = ker([,]: V tensor V -> V) for sl_3."""
    d = 8
    f_abc = np.zeros((d, d, d))
    H1, H2, E1, E2, E3, F1, F2, F3 = range(8)
    CARTAN = np.array([[2, -1], [-1, 2]])

    def sb(a, b, c, v):
        f_abc[a, b, c] += v
        f_abc[b, a, c] -= v

    for i, hi in enumerate([H1, H2]):
        for j, ej in enumerate([E1, E2]):
            if CARTAN[i, j] != 0:
                sb(hi, ej, ej, CARTAN[i, j])
    for i, hi in enumerate([H1, H2]):
        for j, fj in enumerate([F1, F2]):
            if CARTAN[i, j] != 0:
                sb(hi, fj, fj, -CARTAN[i, j])
    sb(E1, F1, H1, 1)
    sb(E2, F2, H2, 1)
    sb(E1, E2, E3, 1)
    sb(F2, F1, F3, 1)
    sb(H1, E3, E3, 1)
    sb(H2, E3, E3, 1)
    sb(H1, F3, F3, -1)
    sb(H2, F3, F3, -1)
    sb(E3, F1, E2, -1)
    sb(E3, F2, E1, 1)
    sb(E3, F3, H1, 1)
    sb(E3, F3, H2, 1)
    sb(E1, F3, F2, -1)
    sb(E2, F3, F1, 1)

    M = np.zeros((d, d * d))
    for a in range(d):
        for b in range(d):
            for c in range(d):
                M[c, a * d + b] = f_abc[a, b, c]

    _, s, Vt = np.linalg.svd(M)
    rank = np.sum(s > 1e-10)
    R_basis = Vt[rank:]
    print(f"sl_3: bracket rank = {rank}, dim R = {len(R_basis)}")
    return R_basis, d


def compute_Rperp(R_basis, d):
    """Compute R^perp subset of V* tensor V* (= annihilator of R in V tensor V).

    R_basis: array of shape (dim_R, d^2), rows are basis vectors of R
    Returns: array of shape (dim_Rperp, d^2)
    """
    # R^perp = left null space of R_basis^T
    # i.e., vectors f such that f . r = 0 for all r in R
    # = kernel of R_basis (acting as a map from (V*xV*) to R*)
    # No: R^perp = {f in V*xV* : <f, r> = 0 for all r in R}
    # = kernel of R_basis^T
    _, s, Vt = np.linalg.svd(R_basis)
    rank = np.sum(s > 1e-10)
    Rperp_basis = Vt[rank:]
    return Rperp_basis


def os_forms_containing_edge(n, edge):
    """Find indices of NBC basis elements of OS^{n-1}(C_n) containing the given edge."""
    basis = nbc_basis(n, n - 1)
    indices = []
    for i, b in enumerate(basis):
        if edge in b:
            indices.append(i)
    return indices


def chiral_ideal_degree_n(Rperp, d, n):
    """Build the ideal subspace of V*^n * OS^{n-1}(C_n) at degree n.

    The ideal is generated by R^perp at each pair (i,j), tensored with
    the OS forms that contain eta_{ij}, and free V* at remaining positions.

    Returns: matrix whose rows span the ideal.
    """
    os_basis = nbc_basis(n, n - 1)
    dim_os = len(os_basis)
    total_dim = d ** n * dim_os  # dim of V*^n * OS^{n-1}

    ideal_vectors = []

    for p in range(1, n + 1):
        for q in range(p + 1, n + 1):
            # Find OS forms containing eta_{pq}
            os_indices = os_forms_containing_edge(n, (p, q))
            if not os_indices:
                continue

            # R^perp acts on positions (p, q) in V*^n
            # Free V* at all other positions
            dim_Rperp = Rperp.shape[0]

            # Build vectors: for each r in R^perp, each choice of
            # free generators at other positions, each OS form
            other_positions = [i for i in range(1, n + 1) if i != p and i != q]

            for r_idx in range(dim_Rperp):
                r = Rperp[r_idx]  # vector in V*^2 (d^2)

                # r encodes r_{ab} where a is at position p, b at position q
                for free_combo in range(d ** len(other_positions)):
                    free_gens = []
                    temp = free_combo
                    for _ in other_positions:
                        free_gens.append(temp % d)
                        temp //= d

                    for os_idx in os_indices:
                        # Build the full vector in V*^n * OS
                        vec = np.zeros(total_dim)

                        for a in range(d):
                            for b in range(d):
                                r_ab = r[a * d + b]
                                if abs(r_ab) < 1e-15:
                                    continue

                                # Build tensor index
                                gens = [0] * n
                                gens[p - 1] = a
                                gens[q - 1] = b
                                for k, pos in enumerate(other_positions):
                                    gens[pos - 1] = free_gens[k]

                                # Convert to linear index (little-endian)
                                t_idx = 0
                                for i in range(n - 1, -1, -1):
                                    t_idx = t_idx * d + gens[i]

                                col = t_idx * dim_os + os_idx
                                vec[col] += r_ab

                        if np.max(np.abs(vec)) > 1e-15:
                            ideal_vectors.append(vec)

    if not ideal_vectors:
        return np.zeros((0, total_dim))

    M = np.array(ideal_vectors)
    return M


def compute_koszul_dual_hilbert(R_basis, d, name, max_deg=5, expected=None):
    """Compute Koszul dual Hilbert series (= bar cohomology)."""
    Rperp = compute_Rperp(R_basis, d)
    print(f"\n{'='*60}")
    print(f"KOSZUL DUAL HILBERT SERIES: {name}")
    print(f"dim V = {d}, dim R = {R_basis.shape[0]}, dim R^perp = {Rperp.shape[0]}")
    print(f"{'='*60}")

    print(f"\nA!_0 = 1")
    print(f"A!_1 = {d}")

    # Degree 2
    dim_free_2 = d ** 2  # OS^1(C_2) = C, dim 1
    dim_ideal_2 = Rperp.shape[0]  # R^perp at single pair (1,2)
    a2 = dim_free_2 - dim_ideal_2
    print(f"A!_2 = {dim_free_2} - {dim_ideal_2} = {a2}")

    for deg in range(3, max_deg + 1):
        os_basis = nbc_basis(deg, deg - 1)
        dim_os = len(os_basis)
        dim_free = d ** deg * dim_os

        if dim_free > 2000000:
            print(f"A!_{deg}: SKIPPED (dim {dim_free} too large)")
            continue

        print(f"\nDegree {deg}: Free = {d}^{deg} * {dim_os} = {dim_free}")

        # Build ideal
        I = chiral_ideal_degree_n(Rperp, d, deg)
        if I.shape[0] > 0:
            rank_I = np.linalg.matrix_rank(I, tol=1e-8)
        else:
            rank_I = 0

        a_n = dim_free - rank_I
        exp_str = ""
        if expected and deg <= len(expected):
            exp_str = f"  (expected: {expected[deg - 1]})"
        print(f"  dim Ideal = {rank_I}")
        print(f"  A!_{deg} = {dim_free} - {rank_I} = {a_n}{exp_str}")


