from compute.lib.utils import partition_number


def prefundamental_clebsch_gordan(
    n: int, depth: int = 40
) -> Dict:
    """Verify V_n ⊗ L⁻ = ⊕_{j=0}^{n} L⁻(hw = n - 2j) at character level.

    Returns dict with verification results and the characters involved.
    """
    Vn = eval_module_Vn(n)
    L_char = prefundamental_character_sl2(depth=depth)

    # Actual tensor product
    actual = tensor_product_characters(Vn, L_char)

    # Predicted: ⊕_{j=0}^{n} L⁻(hw = n - 2j)
    predicted = {}
    for j in range(n + 1):
        hw = n - 2 * j
        shifted = {w + hw: m for w, m in L_char.items()}
        for w, m in shifted.items():
            predicted[w] = predicted.get(w, 0) + m

    # Compare within the reliable depth range
    match = True
    mismatches = []
    for w in set(list(actual.keys()) + list(predicted.keys())):
        if abs(w) <= 2 * depth:
            a = actual.get(w, 0)
            p = predicted.get(w, 0)
            if a != p:
                match = False
                mismatches.append((w, a, p))

    return {
        "n": n,
        "match": match,
        "n_mismatches": len(mismatches),
        "mismatches": mismatches[:5],
        "n_components": n + 1,
        "highest_weights": [n - 2 * j for j in range(n + 1)],
    }


def verify_prefundamental_cg(max_n: int = 8, depth: int = 40) -> Dict[int, bool]:
    """Verify prefundamental Clebsch-Gordan for n = 1, ..., max_n."""
    results = {}
    for n in range(1, max_n + 1):
        data = prefundamental_clebsch_gordan(n, depth=depth)
        results[n] = data["match"]
    return results


def verma_in_k0_span_of_prefundamental(
    lam: int, depth: int = 40
) -> Dict:
    """Show [M(λ)] ∈ K_0-span of {[L⁻(shifted)]} at character level.

    From Fock-Verma: ch(L⁻) = ch(M(0)) × ch(F_multi)
    So [L⁻(0)] = [M(0)] + [K_1] where K_1 is the "excess."

    At character level: M(0) is obtained from L⁻ by subtracting
    the multi-particle sector. Then M(λ) is obtained from M(0)
    via Baxter TQ with V_1.

    The key question: is the excess K_1 itself in K_0-span of {L⁻(shifted)}?
    """
    L_char = prefundamental_character_sl2(depth=depth)
    M0 = sl2_verma_character(0, depth=depth)
    M_lam = sl2_verma_character(lam, depth=depth)

    # K_1 = ch(L⁻) - ch(M(0)): the excess
    K1 = subtract_characters(L_char, M0)

    # K_1 at weight -2m = p(m) - 1 for m ≥ 2, 0 for m < 2
    k1_pattern_match = True
    for m in range(depth):
        w = -2 * m
        k1_val = K1.get(w, 0)
        expected = partition_number(m) - 1 if m >= 0 else 0
        if k1_val != expected:
            k1_pattern_match = False
            break

    # For M(λ): V_λ ⊗ L⁻ has character containing M(λ)'s character
    if lam >= 0:
        V_lam = eval_module_Vn(lam)
        VL = tensor_product_characters(V_lam, L_char)
    else:
        VL = dict(L_char)

    # Check containment: ch(M(λ)) ≤ ch(V_λ ⊗ L⁻) at all weights
    containment = True
    for w in M_lam:
        if VL.get(w, 0) < M_lam[w]:
            containment = False
            break

    return {
        "lam": lam,
        "k1_pattern": k1_pattern_match,
        "containment": containment,
        "interpretation": (
            f"[M({lam})] is {'in' if containment else 'NOT in'} the character-level "
            f"span of V_{lam} ⊗ L⁻. "
            f"K_1 = ch(L⁻) - ch(M(0)) has pattern p(m)-1: {k1_pattern_match}."
        ),
    }


def k0_generation_chain(max_lam: int = 10, depth: int = 40) -> Dict:
    """Verify the K_0 generation chain:
    [M(λ)] ∈ K_0-span of {[L⁻(shifted)]} for all λ = 0, ..., max_lam.

    The chain is:
      [L⁻] = [M(0)] + excess → [M(0)] = [L⁻] - excess
      [V_1][M(0)] = [M(1)] + [M(-1)] → [M(1)] = [V_1][M(0)] - [M(-1)]
      etc. via iterated Baxter TQ.

    At each step, the Verma module [M(λ)] is expressed as a polynomial
    in [V_1], [L⁻], and previously-determined [M(μ)] with |μ| < |λ|.
    """
    results = {}
    for lam in range(max_lam + 1):
        data = verma_in_k0_span_of_prefundamental(lam, depth=depth)
        results[lam] = data["containment"]
    return results
