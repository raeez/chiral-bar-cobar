def decisive_genus2_analysis(c_val: Fraction) -> Dict[str, object]:
    r"""The decisive analysis of multi-generator universality at genus 2.

    The question: does obs_2(W_3) = kappa(W_3) * lambda_2?

    This decomposes into:
      F_2^{full}(W_3) = F_2^{diagonal} + delta_F2^{cross-channel}

    where:
      F_2^{diagonal} = kappa * lambda_2^FP = 7c/6912 * 5/1 = 35c/6912 = 5c * 7/6912
                      = (5c/6) * (7/5760) = 7c/6912 * 5 = 35c/6912
                      Wait: kappa = 5c/6, lambda_2^FP = 7/5760.
                      F_2^{diagonal} = (5c/6)*(7/5760) = 35c/34560 = 7c/6912.

      delta_F2 = (c+204)/(16c).

    For universality at genus 2, we need delta_F2 = 0, which requires
    c + 204 = 0, i.e., c = -204.

    At generic c, delta_F2 != 0, so universality FAILS at genus 2 for W_3
    (in the absence of R-matrix corrections cancelling delta_F2).

    The ratio delta_F2 / F_2^{diagonal} = 432(c+204)/(7c^2):
      - At c = 4 (free boson pair):   432*124/(7*16) = 53568/112 = 478.3
      - At c = 50 (large c):          432*170/(7*2500) = 73440/17500 = 4.20
      - At c = 100:                    432*220/(7*10000) = 95040/70000 = 1.36
      - At c = infinity:              approaches 432/(7c) -> 0

    The cross-channel correction is LARGE relative to the diagonal term
    for small c, indicating that universality fails dramatically.

    CAVEAT (op:multi-generator-universality): This computation uses the
    TREE-LEVEL genus-1 vertex factor kappa_i/24.  The full CohFT
    R-matrix could introduce corrections that modify the genus-1 vertex
    factors in the multi-channel context.  Whether such R-corrections
    exactly cancel delta_F2 at ALL values of c simultaneously is the
    open problem.  This would require a MIRACULOUS cancellation:
    the R-matrix would need to produce exactly -(c+204)/(16c) at
    genus 2, which is a nontrivial functional identity in c.
    """
    kappa = Fraction(5) * c_val / 6
    fp2 = Fraction(7, 5760)
    F2_diag = kappa * fp2  # = 7c/6912

    delta = (c_val + 204) / (16 * c_val)
    F2_full = F2_diag + delta

    # Ratio
    if c_val != 0 and F2_diag != 0:
        ratio = delta / F2_diag
    else:
        ratio = None

    return {
        'c': c_val,
        'kappa': kappa,
        'lambda2_fp': fp2,
        'F2_diagonal': F2_diag,
        'delta_F2': delta,
        'F2_full': F2_full,
        'ratio_delta_over_diag': ratio,
        'universality_holds': delta == 0,
        'universality_c_value': Fraction(-204),  # only c where delta = 0
        'r_matrix_cancellation_needed': delta,
        'notes': (
            f"At c = {c_val}: "
            f"kappa = {kappa}, F2^diag = {F2_diag}, "
            f"delta_F2 = {delta}, F2^full = {F2_full}. "
            f"Universality {'holds' if delta == 0 else 'FAILS'} "
            f"(delta/diag = {ratio})."
        ),
    }


