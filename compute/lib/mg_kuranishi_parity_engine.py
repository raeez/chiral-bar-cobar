@dataclass
class ParityObstructionClassification:
    """Classification of parity obstruction at given genus."""
    genus: int
    algebra_name: str
    num_generators: int
    generator_weights: Tuple[int, ...]
    weight_parity_classes: int  # number of distinct weight parities
    kuranishi_obstructed: bool
    amplitude_correction_nonzero: bool
    min_genus_for_mixed_channels: int
    explanation: str


def classify_parity_obstruction(
    algebra_name: str,
    generator_weights: Tuple[int, ...],
    genus: int,
) -> ParityObstructionClassification:
    r"""Classify the parity obstruction for a given algebra at given genus.

    The key insight: the Kuranishi map ALWAYS vanishes (for dim H^2_cyc = 1),
    but the amplitude correction (cross-channel terms) may be nonzero.

    The minimal genus at which cross-channel corrections can appear is g=2:
      - g=0: tree level, no edges, no channels.
      - g=1: single-edge boundary graphs only (figure-eight at n=0).
             Single edge cannot have mixed channels.
      - g=2: multi-edge boundary graphs exist (banana 2, theta 3, lollipop 2).
             Mixed channels are possible.

    For uniform-weight algebras (all generators same weight):
      - There is only one channel type.
      - Mixed channels are impossible at ANY genus.
      - obs_g = kappa * lambda_g is PROVED at all genera.

    For multi-weight algebras:
      - Mixed channels exist at genus >= 2.
      - The cross-channel correction may be nonzero.
      - Whether it vanishes (after R-matrix corrections) is OPEN.
    """
    n_gen = len(generator_weights)
    weight_parities = set(w % 2 for w in generator_weights)
    n_parity_classes = len(weight_parities)

    # Uniform-weight check
    uniform = (len(set(generator_weights)) == 1)

    # Minimum genus for mixed channels
    if n_gen == 1 or uniform:
        min_genus_mixed = float('inf')  # never
    else:
        min_genus_mixed = 2  # genus-2 boundary graphs have multi-edge structure

    # Amplitude correction analysis
    if uniform:
        amplitude_nonzero = False
    elif genus < 2:
        amplitude_nonzero = False
    else:
        # At genus >= 2, multi-weight algebras generically have nonzero corrections
        amplitude_nonzero = True

    return ParityObstructionClassification(
        genus=genus,
        algebra_name=algebra_name,
        num_generators=n_gen,
        generator_weights=generator_weights,
        weight_parity_classes=n_parity_classes,
        kuranishi_obstructed=False,  # NEVER obstructed when dim H^2_cyc = 1
        amplitude_correction_nonzero=amplitude_nonzero,
        min_genus_for_mixed_channels=min_genus_mixed if isinstance(min_genus_mixed, int) else -1,
        explanation=(
            f"{algebra_name} with generators of weights {generator_weights}: "
            f"{'uniform' if uniform else 'multi'}-weight. "
            f"Kuranishi map vanishes at ALL genera (parity of s^{{-1}}eta). "
            + (
                f"No mixed channels at any genus (uniform weight). "
                f"Tautological identification PROVED."
                if uniform else
                f"Mixed channels appear at genus >= {min_genus_mixed}. "
                f"{'Cross-channel correction is nonzero.' if amplitude_nonzero else 'Cross-channel correction vanishes at this genus.'} "
                f"Tautological identification {'PROVED (genus < 2)' if genus < 2 else 'OPEN (genus >= 2)'}."
            )
        ),
    )


