def genus2_deformation_complex_dimensions(
    algebra_name: str,
    generator_weights: Tuple[int, ...],
    c_val: Optional[Fraction] = None,
) -> Dict[str, object]:
    r"""Compute dimensions of the genus-2 deformation complex.

    For a modular Koszul algebra A with generators of specified weights:

    H^0(Def^{(2,0)}_cyc(A)):
      Infinitesimal automorphisms at genus 2.  For algebras with simple
      Lie symmetry acting by inner derivations, H^0 = 0 in the cyclic
      quotient.

    H^1(Def^{(2,0)}_cyc(A)):
      Infinitesimal deformations at genus 2.  This equals 1 for algebras
      with a single continuous parameter (the level k).  The deformation
      is the level direction d/dk.

    H^2(Def^{(2,0)}_cyc(A)):
      Obstructions.  By algebraic-family-rigidity, dim H^2_cyc = 1 for
      the entire standard Lie-theoretic landscape.

    The GRAPH-SUM SPACE at genus 2 has dimension:
      sum over boundary graphs of (number of channel assignments)
    This is the AMBIENT space in which the graph-sum amplitude lives.
    It is much larger than H^2_cyc = 1.

    For W_3 at genus 2:
      - 5 boundary graphs with a total of 2+4+2+8+4 = 20 channel assignments.
      - Z_2 parity kills some: 3 (theta) + 1 (lollipop) = 4 killed.
      - Surviving: 16 assignments.
      - Of these, 6 are diagonal (per-channel), 10 are surviving mixed.
      - Wait: let us count more carefully.
    """
    n_gen = len(generator_weights)
    n_channels = n_gen  # number of channel types

    # Count graph-sum assignments at genus 2
    # 6 graphs with edge counts 0, 1, 2, 1, 3, 2
    edge_counts = [0, 1, 2, 1, 3, 2]
    graph_names = ["smooth", "fig_eight", "banana", "dumbbell", "theta", "lollipop"]

    total_assignments = sum(n_channels ** e for e in edge_counts)
    diagonal_assignments = sum(n_channels if e > 0 else 1 for e in edge_counts)
    mixed_assignments = total_assignments - diagonal_assignments

    # For W_3: count Z_2-killed assignments
    z2_killed = 0
    z2_surviving_mixed = 0
    if n_gen == 2 and set(generator_weights) == {2, 3}:
        # Banana (2 edges): 2 mixed, both survive (vertex gets even W-count)
        # Theta (3 edges): 6 mixed, 3 killed (TTW-type), 3 survive (TWW-type)
        # Lollipop (2 edges): 2 mixed, 1 killed (TW: C_{TTW}=0), 1 survives (WT)
        z2_killed = 3 + 1  # theta TTW-type + lollipop TW
        z2_surviving_mixed = mixed_assignments - z2_killed

    return {
        'algebra_name': algebra_name,
        'generator_weights': generator_weights,
        'num_generators': n_gen,
        'h0_cyc': 0,
        'h1_cyc': 1,
        'h2_cyc': 1,
        'euler_char': 0,  # 0 - 1 + 1 = 0
        'kuranishi_vanishes': True,
        'graph_sum_space_dim': total_assignments,
        'diagonal_assignments': diagonal_assignments,
        'mixed_assignments': mixed_assignments,
        'z2_killed_assignments': z2_killed,
        'z2_surviving_mixed': z2_surviving_mixed,
        'cross_channel_nonzero': z2_surviving_mixed > 0,
        'notes': (
            f"H^2_cyc = 1 by algebraic-family-rigidity. "
            f"Kuranishi map vanishes by parity (s^{{-1}}eta odd). "
            f"Graph-sum space has {total_assignments} total assignments, "
            f"of which {z2_surviving_mixed} are surviving mixed-channel terms."
        ),
    }


