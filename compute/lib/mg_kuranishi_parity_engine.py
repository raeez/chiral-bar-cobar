@dataclass
class GraphChannelAnalysis:
    """Channel-by-channel analysis of a genus-2 stable graph."""
    graph_name: str
    num_edges: int
    num_channels: int  # 2^{num_edges} total assignments
    num_diagonal: int  # assignments with all edges same type
    num_mixed: int     # assignments with mixed types
    z2_vanishing_count: int  # assignments killed by Z_2 parity
    z2_surviving_mixed: int  # mixed assignments that survive Z_2
    dominant_mixed_type: Optional[str]


def analyze_genus2_graph_channels() -> List[GraphChannelAnalysis]:
    r"""Analyze channel structure of all genus-2 stable graphs.

    For each graph, count:
      - Total channel assignments: 2^{|E|}
      - Diagonal assignments: 2 (all-T and all-W)
      - Mixed assignments: 2^{|E|} - 2
      - Z_2-vanishing: assignments where some vertex has odd W-count
      - Z_2-surviving mixed: the potentially nonzero mixed terms

    The Z_2 parity W -> -W of the W_3 algebra forces:
      C_{i_1,...,i_n} = 0 if the total number of W-indices is odd.

    For VERTEX factors: each vertex independently requires even W-count.
    This is STRONGER than requiring even total W-count globally.
    """
    results = []

    # Graph 0: smooth (0 edges)
    results.append(GraphChannelAnalysis(
        graph_name="smooth", num_edges=0, num_channels=1,
        num_diagonal=1, num_mixed=0, z2_vanishing_count=0,
        z2_surviving_mixed=0, dominant_mixed_type=None,
    ))

    # Graph 1: figure-eight (1 self-loop)
    # Assignments: T, W. Both are diagonal. No mixed.
    results.append(GraphChannelAnalysis(
        graph_name="fig_eight", num_edges=1, num_channels=2,
        num_diagonal=2, num_mixed=0, z2_vanishing_count=0,
        z2_surviving_mixed=0, dominant_mixed_type=None,
    ))

    # Graph 2: banana (2 self-loops at vertex g=0, val=4)
    # Assignments: (T,T), (T,W), (W,T), (W,W). 4 total.
    # Diagonal: (T,T), (W,W). 2.
    # Mixed: (T,W), (W,T). 2.
    # Vertex half-edges: each loop contributes 2 half-edges to the vertex.
    # For (T,W): vertex gets half-edges (T,T,W,W) — 2 W's (even). SURVIVES.
    # For (W,T): vertex gets half-edges (W,W,T,T) — 2 W's (even). SURVIVES.
    results.append(GraphChannelAnalysis(
        graph_name="banana", num_edges=2, num_channels=4,
        num_diagonal=2, num_mixed=2, z2_vanishing_count=0,
        z2_surviving_mixed=2,
        dominant_mixed_type="(T,W) and (W,T): vertex gets (T,T,W,W)",
    ))

    # Graph 3: dumbbell (1 bridge between two g=1 vertices)
    # Assignments: T, W. Both are diagonal from each vertex's perspective:
    #   T: vertex A gets 1 T-half-edge, vertex B gets 1 T-half-edge.
    #       Each vertex has 1 half-edge. genus-1 vertex factor = kappa_T/24.
    #   W: similarly kappa_W/24.
    # No mixed possible (single edge).
    results.append(GraphChannelAnalysis(
        graph_name="dumbbell", num_edges=1, num_channels=2,
        num_diagonal=2, num_mixed=0, z2_vanishing_count=0,
        z2_surviving_mixed=0, dominant_mixed_type=None,
    ))

    # Graph 4: theta (3 bridges between two g=0 trivalent vertices)
    # Assignments: 2^3 = 8. Diagonal: (T,T,T), (W,W,W). 2.
    # Mixed: 6.
    # Each vertex gets 3 half-edges (one from each bridge).
    # Z_2 parity at each vertex: C_{i,j,k} = 0 if odd W-count.
    # (T,T,W): vertex gets (T,T,W) -> C_{TTW} = 0. KILLED.  (3 such assignments)
    # (T,W,W): vertex gets (T,W,W) -> C_{TWW} = c. SURVIVES. (3 such assignments)
    # (W,W,W): vertex gets (W,W,W) -> C_{WWW} = 0. KILLED. (diagonal, already counted)
    # Net surviving mixed: 3 (the (T,W,W)-type).
    results.append(GraphChannelAnalysis(
        graph_name="theta", num_edges=3, num_channels=8,
        num_diagonal=2, num_mixed=6,
        z2_vanishing_count=3,  # the 3 (T,T,W)-type assignments
        z2_surviving_mixed=3,  # the 3 (T,W,W)-type assignments
        dominant_mixed_type="(T,W,W)-type: C_{TWW}^2 * eta^T * (eta^W)^2",
    ))

    # Graph 5: lollipop (1 self-loop at g=0 trivalent vertex + 1 bridge to g=1 vertex)
    # Assignments: (sigma_loop, sigma_bridge). 4 total.
    # Diagonal: (T,T), (W,W). 2.
    # Mixed: (T,W), (W,T). 2.
    #
