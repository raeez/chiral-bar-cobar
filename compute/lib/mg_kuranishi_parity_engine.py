def w3_cross_channel_at_genus2(c_val: Fraction) -> Dict[str, Fraction]:
    r"""Compute the W_3 cross-channel correction at genus 2.

    This computes delta_F2 = F_2^{full} - F_2^{diagonal}
    where F_2^{diagonal} = kappa * lambda_2^FP is the per-channel
    universal prediction and F_2^{full} is the actual multi-channel
    graph sum.

    Per-graph cross-channel contributions:

    Gamma_2 (banana): 2 mixed assignments survive Z_2.
      (T,W): vertex factor = V_{0,4}(T,T,W,W) = 2c (universally).
              propagator = eta^T * eta^W = (2/c)(3/c) = 6/c^2.
              raw = 12/c. Two such, with 1/|Aut|=1/8: 24/(8c) = 3/c.

    Gamma_4 (theta): 3 mixed assignments survive Z_2.
      (T,W,W)-type: each vertex gets (T,W,W) -> C_{TWW} = c.
                     propagators: eta^T * (eta^W)^2 = (2/c)(3/c)^2 = 18/c^3.
                     raw per assignment = 18c^2/c^3 = 18/c.
                     3 such, with 1/|Aut|=1/12: 3*18/(12c) = 9/(2c).

    Gamma_5 (lollipop): 1 mixed assignment survives Z_2.
      (W,T): g=0 vertex (W,W,T) -> C_{WWT} = c.
             g=1 vertex (T) -> kappa_T/24 = c/48.
             propagators: eta^W * eta^T = (3/c)(2/c) = 6/c^2.
             raw = 6c * c/(48 * c^2) = 6/(48) = 1/8.
             With 1/|Aut|=1/2: 1/16.

    Gamma_6 (barbell): 3 mixed assignments survive Z_2.
      2 vertices g=0, 1 bridge + 1 self-loop on each vertex. |Aut|=8.
      (W,T,W): v0=(W,W,T)->C_{WWT}=c, v1=(T,W,W)->C_{TWW}=c.
               props: (3/c)(2/c)(3/c)=18/c^3. raw=18/c. With 1/8: 9/(4c).
      (T,T,W): v0=(T,T,T)->c, v1=(T,W,W)->c.
               props: (2/c)(2/c)(3/c)=12/c^3. raw=12/c. With 1/8: 3/(2c).
      (W,T,T): v0=(W,W,T)->c, v1=(T,T,T)->c.
               props: (3/c)(2/c)(2/c)=12/c^3. raw=12/c. With 1/8: 3/(2c).
      Mixed total: 9/(4c) + 3/(2c) + 3/(2c) = 9/(4c) + 3/c = 21/(4c).

    Total: delta_F2 = 3/c + 9/(2c) + 1/16 + 21/(4c) = (c + 204)/(16c).
    """
    # Per-graph contributions
    delta_banana = Fraction(3) / c_val
    delta_theta = Fraction(9) / (2 * c_val)
    delta_lollipop = Fraction(1, 16)
    delta_barbell = Fraction(21) / (4 * c_val)

    # Total cross-channel correction
    delta_total = delta_banana + delta_theta + delta_lollipop + delta_barbell

    # Verify closed form
    delta_closed = (c_val + 204) / (16 * c_val)

    return {
        'delta_banana': delta_banana,
        'delta_theta': delta_theta,
        'delta_lollipop': delta_lollipop,
        'delta_barbell': delta_barbell,
        'delta_total': delta_total,
        'delta_closed_form': delta_closed,
        'match': delta_total == delta_closed,
        'kappa_times_lambda2': Fraction(5) * c_val / 6 * Fraction(7, 5760),
        'ratio_delta_over_diagonal': (
            delta_total / (Fraction(5) * c_val / 6 * Fraction(7, 5760))
            if c_val != 0 else None
        ),
    }


