"""Finite generation of the σ-invariant shadow ring (A^sh)^σ.

THEOREM (Virasoro): The σ-invariant shadow ring is generated by
three elements:
    Δ^(2) = 13          (modular characteristic, perturbative)
    Δ^(3) = 4           (gravitational cubic, perturbative)
    Δ^(4) = f(c)        (quartic contact conductor, dynamical)

All higher entries are ring products via the master equation:
    Δ^(5) = -(1/10) · {Δ^(3), Δ^(4)}_H^σ
    Δ^(6) = -(1/12) · [{Δ^(3), Δ^(5)}_H^σ + (1/2){Δ^(4), Δ^(4)}_H^σ]

The perturbative subring {Δ^(2), Δ^(3)} is level-independent.
Δ^(4) is the SOLE dynamical generator — the single source of all
level-dependence in the ring. The infinite tower of dynamical
corrections at arities 5, 6, 7, ... is algebraically forced by
this one generator and the ring multiplication.
"""

from __future__ import annotations
from fractions import Fraction
from typing import Dict, Tuple

from sympy import Symbol, Rational, simplify, factor, Poly

c = Symbol('c')


# ===========================================================================
# Shadow tower coefficients (Virasoro, primary line)
# ===========================================================================

def virasoro_shadow_coefficients(max_arity: int = 7) -> Dict[int, object]:
    """S_r where Sh_r = S_r · x^r on the primary line."""
    P = Rational(2) / c
    S = {}
    S[2] = c / 2
    S[3] = Rational(2)
    S[4] = Rational(10) / (c * (5 * c + 22))

    for r in range(5, max_arity + 1):
        o_r = Rational(0)
        for j in range(2, r + 1):
            k = r + 2 - j
            if k < 2 or k > r or k not in S:
                continue
            if j > k:
                continue
            bracket = j * S[j] * P * k * S[k]
            if j == k:
                o_r += Rational(1, 2) * bracket
            else:
                o_r += bracket
        S[r] = factor(-simplify(o_r) / (2 * r))
    return S


# ===========================================================================
# σ-invariant entries: Δ^(r) = S_r(c) + S_r(26-c)
# ===========================================================================

def sigma_invariant_tower(max_arity: int = 7) -> Dict[int, object]:
    """Compute Δ^(r,0) = S_r(c) + S_r(26-c) for Virasoro."""
    S = virasoro_shadow_coefficients(max_arity)
    Delta = {}
    for r, Sr in S.items():
        Sr_dual = Sr.subs(c, 26 - c)
        Delta[r] = factor(simplify(Sr + Sr_dual))
    return Delta


def sigma_invariant_from_products(max_arity: int = 7) -> Dict[int, object]:
    """Compute Δ^(r) from ring products of lower Δ's.

    Uses the master equation: Δ^(r) = -(1/(2r)) Σ products.
    Arities 2, 3, 4 are generators (not products).
    """
    S = virasoro_shadow_coefficients(max_arity)
    P = Rational(2) / c
    Delta_prod = {}

    # Generators
    Delta_prod[2] = factor(simplify(S[2] + S[2].subs(c, 26 - c)))
    Delta_prod[3] = factor(simplify(S[3] + S[3].subs(c, 26 - c)))
    Delta_prod[4] = factor(simplify(S[4] + S[4].subs(c, 26 - c)))

    for r in range(5, max_arity + 1):
        o_sigma = Rational(0)
        for j in range(2, r):
            k = r + 2 - j
            if k < 2 or k >= r or k not in S:
                continue
            if j > k:
                continue
            # σ-invariant product:
            # {Sh_j(c), Sh_k(c)}_H(c) + {Sh_j(c'), Sh_k(c')}_H(c')
            bracket_c = j * S[j] * P * k * S[k]
            P_dual = Rational(2) / (26 - c)
            Sj_dual = S[j].subs(c, 26 - c)
            Sk_dual = S[k].subs(c, 26 - c)
            bracket_c_dual = j * Sj_dual * P_dual * k * Sk_dual
            bracket_sigma = bracket_c + bracket_c_dual

            if j == k:
                o_sigma += Rational(1, 2) * bracket_sigma
            else:
                o_sigma += bracket_sigma

        Delta_prod[r] = factor(simplify(-o_sigma / (2 * r)))

    return Delta_prod


def verify_finite_generation(max_arity: int = 7) -> Dict[int, bool]:
    """Verify Δ^(r) = Δ^(r)_from_products for r = 5, ..., max_arity."""
    direct = sigma_invariant_tower(max_arity)
    products = sigma_invariant_from_products(max_arity)
    results = {}
    for r in range(5, max_arity + 1):
        results[r] = simplify(direct[r] - products[r]) == 0
    return results


# ===========================================================================
# Generator data
# ===========================================================================

def generators() -> Dict[str, object]:
    """The three generators of (A^sh)^σ for Virasoro."""
    return {
        "Delta_2": Rational(13),
        "Delta_3": Rational(4),
        "Delta_4": factor(
            Rational(20) * (5 * c**2 - 130 * c + 1976) /
            (c * (c - 26) * (5 * c - 152) * (5 * c + 22))
        ),
    }


def level_independence_classification(max_arity: int = 7) -> Dict[int, str]:
    """Classify each arity as perturbative or dynamical."""
    Delta = sigma_invariant_tower(max_arity)
    result = {}
    from sympy import diff
    for r, d in Delta.items():
        is_const = simplify(diff(d, c)) == 0
        result[r] = "perturbative" if is_const else "dynamical"
    return result
