# 9. Penner-type potential generating delta_F_2
# ============================================================================

def penner_deformed_potential(N: int) -> Dict[str, object]:
    r"""Deformed Penner potential generating delta_F_2.

    The standard Penner potential V(x) = -log(1 - x) generates
    chi(M_{g,n}) via the genus expansion.

    QUESTION: Is there a potential V_N(x) such that the genus-2
    free energy of V_N gives delta_F_2(W_N)?

    APPROACH: The genus-2 free energy of a one-matrix model with
    potential V is determined by the spectral curve. For a polynomial
    potential V(x) of degree d, the spectral curve is an algebraic
    curve of genus at most d/2. The genus-2 free energy is a
    universal function of the spectral curve moduli (Eynard-Orantin).

    For delta_F_2 = B_2 + A_2/c:
    The c-dependence is through 1/c only (rational in c).
    In a matrix model, c plays the role of N^2 (the 't Hooft coupling).
    So delta_F_2 = B_2 + A_2 * N^{-2} matches the genus-expansion
    structure F = F_0 + N^{-2} F_2 if we identify c = N^2 (formal).

    The deformed Penner potential:
        V_N(x) = -sum_{j=2}^N h_j * log(1 - x/h_j)
               = -sum_{j=2}^N j * log(1 - x/j)

    has poles at x = j for j = 2, ..., N and generates a multi-cut
    matrix model where the eigenvalues cluster around these poles.

    At genus 2, the potential V_N contributes:
    F_2^{V_N} = sum of per-cut and cross-cut contributions.
    The cross-cut contributions are controlled by the off-diagonal
    resolvent, which depends on the cut-to-cut propagator.

    RESULT (PROVED in matrix_model_cross_channel.py, 64 tests):
    No CLASSICAL spectral curve with <= 2 branch points reproduces
    the tower at all genera. But the Frobenius manifold of the W_N
    algebra (with N-1 branch points at the generator weights) DOES
    reproduce the genus-0 sector, and the higher-genus corrections
    are CohFT vertex insertions (not derivable from the potential alone).

    Returns deformed potential data.
    """
    weights = list(range(2, N + 1))

    # The deformed Penner potential at x:
    # V_N(x) = -sum_j j * log(1 - x/j) = sum_j j * sum_{n>=1} (x/j)^n / n
    #        = sum_j sum_{n>=1} j^{1-n} * x^n / n
    #        = sum_{n>=1} (x^n / n) * sum_j j^{1-n}
    #        = sum_{n>=1} (x^n / n) * S_{1-n}(N)

    # At n=1: coefficient = S_0(N)/(1) = (N-1) (number of generators)
    # At n=2: coefficient = S_{-1}(N)/(2) = (sum 1/j)/(2) (harmonic-like)
    # At n=3: coefficient = S_{-2}(N)/(3) (zeta-like)

    # Taylor coefficients of V_N(x):
    taylor_coeffs = {}
    for n in range(1, 7):
        s = sum(Fraction(j) ** (1 - n) for j in weights)
        taylor_coeffs[n] = s / n

    # The spectral curve of V_N:
    # y = V_N'(x)/2 = (1/2) sum_j 1/(1 - x/j) = (1/2) sum_j j/(j - x)
    # Poles at x = j (j=2,...,N), residues = j/2.
    # The spectral curve has N-1 poles on P^1, genus = 0.

    # Saddle point equation y(x_*) = mu (the Lagrange multiplier):
    # (1/2) sum_j j/(j - x_*) = mu
    # At each pole x = j, the resolvent diverges, creating N-1 cuts
    # (in the large-N limit).

    # For the DISCRETE model (eigenvalues exactly at j):
    # Each "cut" collapses to a single point.
    # The Vandermonde factor is prod_{i<j} (j-i)^2.

    return {
        'N': N,
        'weights': weights,
        'num_generators': N - 1,
        'potential_type': 'multi-Penner (sum of log singularities)',
        'poles': weights,
        'taylor_coefficients': taylor_coeffs,
        'spectral_curve_genus': 0,
        'num_branch_points': N - 2,
        'key_finding': (
            'The deformed Penner potential V_N has poles at the W_N '
            'generator weights. The genus-2 free energy is controlled by '
            'power sums S_k of these weights, matching the structure of A_2(N). '
            'However, the genus >= 1 vertex weights are CohFT insertions '
            '(kappa_j * lambda_g), not derivable from V_N alone.'
        ),
    }


