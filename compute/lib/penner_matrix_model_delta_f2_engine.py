@lru_cache(maxsize=256)
def power_sum(k: int, N: int) -> Fraction:
    r"""Newton power sum S_k = p_k = sum_{j=2}^N j^k.

    These are the moments of the discrete measure at {2,...,N}.
    """
    if N < 2:
        return Fraction(0)
    return sum(Fraction(j) ** k for j in range(2, N + 1))


def S1(N: int) -> Fraction:
    """S_1 = sum_{j=2}^N j = N(N+1)/2 - 1."""
    return Fraction(N * (N + 1), 2) - 1


def S2(N: int) -> Fraction:
    """S_2 = sum_{j=2}^N j^2 = N(N+1)(2N+1)/6 - 1."""
    return Fraction(N * (N + 1) * (2 * N + 1), 6) - 1


def S3(N: int) -> Fraction:
    """S_3 = sum_{j=2}^N j^3 = [N(N+1)/2]^2 - 1."""
    return Fraction(N * (N + 1) // 2) ** 2 - 1


def S4(N: int) -> Fraction:
    """S_4 = sum_{j=2}^N j^4."""
    return power_sum(4, N)


