def a2_as_resolvent_moment(N: int) -> Dict[str, object]:
    r"""Express A_2(N) in terms of resolvent moments.

    A_2(N) = (2*S_1^2 + S_2 - 12)/4

    Define the SHIFTED moments: T_k = S_k - h_Vir^k = S_k - 2^k.
    These measure the EXCESS of the W_N generators beyond Virasoro.

    T_1 = S_1 - 2 = sum_{j=3}^N j = (N-2)(N+3)/2
    T_2 = S_2 - 4 = sum_{j=3}^N j^2 = N(N+1)(2N+1)/6 - 5

    Then:
    A_2 = (2*(T_1+2)^2 + (T_2+4) - 12)/4
        = (2*T_1^2 + 8*T_1 + 8 + T_2 + 4 - 12)/4
        = (2*T_1^2 + 8*T_1 + T_2)/4
        = T_1*(2*T_1 + 8)/4 + T_2/4
        = T_1*(T_1 + 4)/2 + T_2/4

    For N=2 (Virasoro only): T_1 = T_2 = 0, so A_2 = 0. Correct.
    For N=3 (W_3): T_1 = 3, T_2 = 5, A_2 = 3*7/2 + 5/4 = 21/2 + 5/4 = 47/4.

    Check: A_2(3) = (2*9 + 5 - 12)/4 = (18+5-12)/4 = 11/4... hmm, wait.
    S_1(3) = 2+3 = 5, S_2(3) = 4+9 = 13.
    A_2(3) = (2*25 + 13 - 12)/4 = (50+13-12)/4 = 51/4.

    And T_1 = 5-2 = 3, T_2 = 13-4 = 9.
    Formula: 3*(3+4)/2 + 9/4 = 21/2 + 9/4 = 42/4 + 9/4 = 51/4. Correct.

    INTERPRETATION: A_2(N) = T_1*(T_1+4)/2 + T_2/4 where:
      T_1 = number of extra generators beyond Virasoro (weighted by conformal weight)
      T_2 = quadratic moment excess beyond Virasoro

    The T_1^2 term comes from PAIRS of distinct higher generators,
    while the T_2 term comes from SINGLE generator self-interactions.

    Returns moment decomposition.
    """
    s1 = S1(N)
    s2 = S2(N)

    t1 = s1 - 2
    t2 = s2 - 4

    a2_from_shifted = t1 * (t1 + 4) / 2 + t2 / 4
    a2_from_power = (2 * s1 ** 2 + s2 - 12) / 4

    return {
        'N': N,
        'S1': s1,
        'S2': s2,
        'T1': t1,
        'T2': t2,
        'A2_from_shifted_moments': a2_from_shifted,
        'A2_from_power_sums': a2_from_power,
        'consistent': a2_from_shifted == a2_from_power,
        'interpretation': (
            'A_2(N) = T_1*(T_1+4)/2 + T_2/4 where T_k = S_k - 2^k '
            'are the EXCESS moments beyond the Virasoro contribution. '
            'The quadratic T_1^2 term counts pairs of extra generators; '
            'the linear T_2 term counts self-interactions.'
        ),
    }


