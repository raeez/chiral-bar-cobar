def vandermonde_squared(eigenvalues: List[int]) -> Fraction:
    r"""Vandermonde determinant squared.

    prod_{i<j} (lambda_j - lambda_i)^2

    For eigenvalues {2, 3, ..., N}:
    prod_{2<=i<j<=N} (j-i)^2 = prod_{k=1}^{N-2} k^{2(N-1-k)}
                               = prod_{k=1}^{N-2} k! * something...

    Actually, for CONSECUTIVE integers from a to a+m-1:
    prod_{i<j} (j-i)^2 = prod_{k=1}^{m-1} k^{2(m-k)} = (G(m+1))^2
    where G(n) = Barnes G-function = prod_{k=0}^{n-2} k!.

    For {2,...,N}: m = N-1 eigenvalues.
    Vandermonde^2 = (G(N))^2 = (prod_{k=0}^{N-2} k!)^2.
    """
    m = len(eigenvalues)
    result = Fraction(1)
    for i in range(m):
        for j in range(i + 1, m):
            result *= Fraction(eigenvalues[j] - eigenvalues[i]) ** 2
    return result


def log_vandermonde_squared(N: int) -> Fraction:
    r"""log(Vandermonde^2) for {2,...,N} eigenvalues.

    For consecutive integers {2,...,N}, the Vandermonde is:
    prod_{2<=i<j<=N} (j-i)^2 = prod_{k=1}^{N-2} k^{2(N-1-k)}.

    log(V^2) = 2 * sum_{k=1}^{N-2} (N-1-k) * log(k).

    Returns exact rational using log of small integers (symbolic).
    """
    # For exact computation, we work with the product directly
    eigenvalues = list(range(2, N + 1))
    return vandermonde_squared(eigenvalues)


def discrete_model_moments(N: int, max_k: int = 6) -> Dict[int, Fraction]:
    r"""Moments of the discrete eigenvalue measure at {2,...,N}.

    mu_k = (1/M) * sum_{j=2}^N j^k = S_k / (N-1)

    where M = N-1 is the number of eigenvalues.

    In the matrix model language, these are the moments of the
    eigenvalue density rho(x) = (1/M) sum delta(x - j).

    Returns {k: mu_k for k = 0, ..., max_k}.
    """
    M = N - 1  # number of eigenvalues
    if M <= 0:
        return {k: Fraction(0) for k in range(max_k + 1)}
    return {k: power_sum(k, N) / M for k in range(max_k + 1)}


def discrete_model_connected_correlators(N: int) -> Dict[str, Fraction]:
    r"""Connected 2-point correlator for the discrete eigenvalue model.

    The connected 2-point function:
    W_2(x, y) = <Tr(1/(x-M)) Tr(1/(y-M))>_c
             = sum_{i,j} 1/((x-lambda_i)(y-lambda_j)) - (1-point)^2

    For the DISCRETE model, the "fluctuations" around the fixed
    eigenvalues produce a connected correlator that encodes the
    resolvent:
    W_2^disc(x, y) = sum_{i != j} 1/((x-lambda_i)^2 (y-lambda_j)^2)
                     * (covariance structure)

    At leading order in 1/N, the discrete model connected correlator
    gives the Bergman kernel B(x,y) = 1/(x-y)^2 of the spectral curve.

    Returns connected correlator data.
    """
    M = N - 1
    eigenvalues = list(range(2, N + 1))

    # Resolvent: W_1(x) = (1/M) sum_j 1/(x - lambda_j)
    # We compute the moments of the resolvent as power sums
    resolvent_moments = {}
    for k in range(1, 7):
        resolvent_moments[k] = Fraction(1, M) * power_sum(-k, N) if k > 0 else Fraction(0)
        # power_sum(-k) doesn't make sense for negative k; use definition
        resolvent_moments[k] = sum(Fraction(1, j ** k) for j in range(2, N + 1)) / M

    # The connected 2-point function at coincident points gives the variance
    # var = (1/M) sum_j lambda_j^2 - ((1/M) sum_j lambda_j)^2
    #     = mu_2 - mu_1^2
    mu1 = S1(N) / M
    mu2 = S2(N) / M
    variance = mu2 - mu1 ** 2

    # The 4th cumulant (relevant to genus-2):
    # kappa_4 = mu_4 - 4*mu_3*mu_1 - 3*mu_2^2 + 12*mu_2*mu_1^2 - 6*mu_1^4
    mu3 = S3(N) / M
    mu4 = S4(N) / M
    kappa_4 = mu4 - 4 * mu3 * mu1 - 3 * mu2 ** 2 + 12 * mu2 * mu1 ** 2 - 6 * mu1 ** 4

    return {
        'N': N,
        'M': M,
        'eigenvalues': eigenvalues,
        'mu_1': mu1,
        'mu_2': mu2,
        'variance': variance,
        'kappa_4': kappa_4,
        'resolvent_moments': resolvent_moments,
    }


