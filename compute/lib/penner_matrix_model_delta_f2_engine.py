def penner_multi_matrix_genus2(N: int, c: Fraction) -> Dict[str, Fraction]:
    r"""Genus-2 free energy of the multi-matrix Penner model.

    The multi-matrix Penner model has N-1 matrices with potentials:
        V_j(x) = -h_j * log(1 - x/h_j) for j = 2, ..., N
    where h_j = j are the conformal weights.

    Each matrix has "size" proportional to kappa_j = c/j (the per-channel
    modular characteristic -- wait, this is wrong).

    Actually, the propagator for channel j is eta^{jj} = j/c (AP27:
    the bar propagator is weight 1, and the metric is kappa_j = c/j,
    so the inverse metric is j/c).

    In the Penner model genus expansion, the genus-g free energy is:
        F_g^Penner = sum over fatgraphs Gamma of (product of vertex weights)
                     * (product of propagators) / |Aut(Gamma)|

    For a single matrix: F_2^Penner = chi(M_2) = -1/240 (Harer-Zagier).

    For the multi-matrix model, we sum over channel assignments at
    each edge, which is EXACTLY the cross-channel graph sum.

    The genus-2 Penner contribution from each graph topology:

    1. FIGURE-EIGHT (1 vertex g=0, 2 self-loops):
       V_{0,4}(i,i,j,j) * prop(i) * prop(j) / |Aut|
       = c * (i/c) * (j/c) / 8 = ij/(8c)
       Mixed sum: (1/(8c)) * sum_{i!=j} ij = (S_1^2 - S_2)/(8c)
       ... but we need to be careful about the vertex factor.

       Actually the W_N vertex factor at genus 0 is:
       V_{0,4}(i,i,j,j) = 2c (by T-exchange factorization through C3)
       For channels (i,i,j,j): the factorization goes through a propagator
       in the middle, giving V_{0,3}(i,i,m) * eta^{mm} * V_{0,3}(m,j,j).

       For the C_{ijk} structure of the W_N algebra:
       C_{ijk} = c if the W-parity condition is met, 0 otherwise.
       For W_N with generators at weights {2,...,N}, the GENERAL
       structure constants C_{ijk} depend on the full W-algebra OPE.

       SIMPLIFICATION: For the purpose of this engine, we use the
       diagonal propagator model where the genus-2 free energy is
       computed by the formula delta_F_2 = B_2(N) + A_2(N)/c,
       which is PROVED in a2_polynomial_combinatorics_engine.py.

    Returns the multi-matrix Penner model prediction.
    """
    # The multi-matrix Penner model at genus 2:
    # F_2^{multi-Penner} = sum_j F_2^{Penner}(h_j) + cross terms
    #
    # For the standard Penner model with eigenvalue weight h_j:
    # F_2(h_j) = h_j^2 * chi(M_2) = h_j^2 * (-1/240)
    #
    # But this is NOT quite right either: the Penner model F_2
    # counts orbifold Euler characteristics, while the shadow tower
    # counts intersection numbers.

    # DIAGONAL contribution: each channel j contributes
    # kappa_j * lambda_2^FP = (c/j) * 7/5760  ... no wait,
    # kappa_j = c/j for channel j (weight j contributes c/j to the curvature).
    # Diagonal: sum_j kappa_j * lambda_2^FP = kappa_total * lambda_2^FP
    # = (5c/6) * 7/5760 for N=3. This is the SCALAR part, not delta_F_2.

    # CROSS part: this IS delta_F_2.
    b2 = delta_F2_B2(N)
    a2 = delta_F2_A2(N)
    delta_f2 = b2 + a2 / c

    # Penner model genus-2:
    penner_f2 = Fraction(-1, 240)  # single-matrix Penner

    # Penner with N-1 matrices at weights h=2,...,N:
    # The "multi-Penner" would give:
    # F_2^{multi} = sum of per-channel Penner + cross-channel Penner
    # The per-channel contribution involves h_j^{4-2g_v} factors from
    # the Penner vertex weights, while the shadow tower uses kappa_j * lambda_g.
    # These are DIFFERENT vertex weight schemes.

    # The KEY comparison: does the ratio delta_F_2 / penner_f2
    # have a clean form in N?

    if penner_f2 != 0:
        ratio_to_penner = delta_f2 / penner_f2
    else:
        ratio_to_penner = None

    return {
        'N': N,
        'c': c,
        'delta_F2': delta_f2,
        'B2': b2,
        'A2': a2,
        'penner_F2_single': penner_f2,
        'lambda_2_FP': lambda_fp(2),
        'ratio_to_penner': ratio_to_penner,
    }


