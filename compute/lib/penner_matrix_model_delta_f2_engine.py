def discrete_resolvent(N: int, x: Fraction) -> Fraction:
    r"""Resolvent of the discrete measure at {2,...,N}.

    W(x) = (1/(N-1)) * sum_{j=2}^N 1/(x - j)

    This is the Stieltjes transform of the discrete measure.
    """
    M = N - 1
    if M <= 0:
        return Fraction(0)
    return sum(Fraction(1, x - j) for j in range(2, N + 1)) / M


def discrete_spectral_curve(N: int) -> Dict[str, object]:
    r"""Spectral curve data for the discrete eigenvalue model.

    For the discrete measure rho(x) = (1/M) sum_j delta(x - h_j),
    the resolvent is:
        W(x) = (1/M) * sum_j 1/(x - h_j)
             = (1/M) * P'(x) / P(x)

    where P(x) = prod_j (x - h_j) = prod_{j=2}^N (x - j).

    The spectral curve is:
        y = W(x) = (1/M) * d/dx [log P(x)]

    Poles at x = 2, 3, ..., N (the eigenvalue positions).
    Residue at x = j: 1/M.

    The genus of the spectral curve: it has N-1 poles on P^1,
    so genus = 0. But the Riemann surface defined by the
    resolvent equation y(x) is just the rational function
    P'(x)/(M * P(x)), which is a meromorphic function on P^1.

    For topological recursion, we need the spectral curve
    (Sigma, x, y) where x and y are functions on Sigma.
    For the discrete model, Sigma = P^1 and the branch points
    are the ZEROS of P'(x) (critical points of x -> y map).

    P'(x) has degree N-2 (one less than P), so N-2 critical points.
    These are the equilibrium points of the discrete eigenvalue system.

    Returns spectral curve data.
    """
    # P(x) = prod_{j=2}^N (x - j) has degree N-1
    # P'(x) has degree N-2
    # Branch points = zeros of P'(x) = critical points of y(x)

    # For small N, compute explicitly
    M = N - 1
    eigenvalues = list(range(2, N + 1))

    # Residues of y = W(x): all equal to 1/M
    residues = {j: Fraction(1, M) for j in eigenvalues}

    # The branch points (zeros of P') are at the critical points
    # of the Coulomb energy sum_{j} log|x - h_j|.
    # By symmetry considerations, for consecutive integers {a, a+1, ..., b},
    # the critical points are approximately at half-integers {a+1/2, ..., b-1/2}.

    # The total number of branch points = N - 2
