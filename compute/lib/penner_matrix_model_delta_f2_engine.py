def delta_F2_A2(N: int) -> Fraction:
    r"""Leading 1/c coefficient A_2(N) = (2*S_1^2 + S_2 - 12)/4.

    Rep 1: power sum formula.
    This is the c^{-1} coefficient of delta_F_2(W_N).
    """
    s1 = S1(N)
    s2 = S2(N)
    return (2 * s1 ** 2 + s2 - 12) / 4


def delta_F2_A2_polynomial(N: int) -> Fraction:
    r"""Leading 1/c coefficient A_2(N) = (N-2)(3N^3+14N^2+22N+33)/24.

    Rep 2: explicit polynomial formula.
    """
    if N <= 2:
        return Fraction(0)
    return Fraction((N - 2) * (3 * N**3 + 14 * N**2 + 22 * N + 33), 24)


def delta_F2_B2(N: int) -> Fraction:
    r"""The c-independent coefficient B_2(N) = (S_1 - 2)/48.

    This is the c^0 coefficient of delta_F_2(W_N).
    """
    s1 = S1(N)
    return (s1 - 2) / 48


def delta_F2_full(N: int, c: Fraction) -> Fraction:
    r"""Full delta_F_2(W_N, c) = B_2(N) + A_2(N)/c.

    Note: for the PHYSICAL W_N algebra at level k,
    c = N(N^2-1)/(N+k). But we treat c as a free parameter here.
    """
    return delta_F2_B2(N) + delta_F2_A2(N) / c


