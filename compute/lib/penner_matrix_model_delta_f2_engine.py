# 5. Genus-2 free energy from power sums: matrix model vs shadow tower
# ============================================================================

def genus2_from_power_sums(N: int) -> Dict[str, Fraction]:
    r"""Genus-2 free energy from the power-sum decomposition.

    In a matrix model with resolvent moments S_k, the genus-2
    free energy receives contributions from three graph topologies:
      1. Figure-eight (2 self-loops): ~ S_1^2 and S_2
      2. Theta (3 bridges): ~ S_2
      3. Sunrise/dumbbell (2 SL + 1 bridge): ~ S_1^2

    The EXACT genus-2 formula from graph enumeration is:
      delta_F_2(W_N) = B_2(N) + A_2(N)/c

    with A_2(N) decomposing as:
      A_2^{FE} = (S_1^2 - S_2)/4     [figure-eight]
      A_2^{Th} = (S_2 - 4)/2         [theta]
      A_2^{SR} = (S_1^2 - 4)/4       [sunrise]
      A_2 = FE + Th + SR = (2*S_1^2 + S_2 - 12)/4

    The QUESTION: does a matrix model with eigenvalue support at
    {2,...,N} reproduce this formula?

    ANSWER: The genus-2 free energy of a CONTINUOUS matrix model
    with spectral density supported at {2,...,N} does NOT directly
    give delta_F_2, because the CohFT vertex weights at genus >= 1
    are NOT derivable from a matrix potential. However, the DISCRETE
    model moments do appear in delta_F_2, suggesting a deformed
    matrix model connection.

    Returns comparison data.
    """
    s1 = S1(N)
    s2 = S2(N)

    # Graph contributions
    fe = (s1 ** 2 - s2) / 4
    th = (s2 - 4) / 2
    sr = (s1 ** 2 - 4) / 4
    a2 = fe + th + sr

    # Also compute via the direct formula
    a2_direct = (2 * s1 ** 2 + s2 - 12) / 4

    # Discrete model cumulants
    M = N - 1
    if M > 0:
        mu1 = s1 / M
        mu2 = s2 / M
        variance = mu2 - mu1 ** 2
    else:
        mu1 = Fraction(0)
        mu2 = Fraction(0)
        variance = Fraction(0)

    # Can we express A_2 in terms of matrix model cumulants?
    # A_2 = (2*S_1^2 + S_2 - 12)/4
    #     = M^2 * (2*mu_1^2 + mu_2/M - 12/M^2) / 4   ... doesn't simplify nicely
    #
    # BUT: A_2 = M^2 * [2*mu_1^2 + (variance + mu_1^2)/M - 12/M^2] / 4
    # ... still not clean.
    #
    # The correct interpretation: A_2 is NOT a standard cumulant of the
    # discrete measure. It is a WEIGHTED sum where the weights come from
    # the CohFT vertex factor (kappa_i * lambda_g), not from the log-gas
    # interaction.

    # However, the GRAPH DECOMPOSITION does match a multi-matrix model:
    # The W_N algebra has N-1 channels with propagators eta^{ii} = i/c.
    # The "discrete matrix model" has N-1 matrices M_2, ..., M_N with
    # propagators <M_i M_j> = delta_{ij} * i/c.
    # The genus-2 graphs of this multi-matrix model produce exactly
    # the cross-channel contributions delta_F_2.

    return {
        'N': N,
        'S1': s1,
        'S2': s2,
        'A2_FE': fe,
        'A2_Th': th,
        'A2_SR': sr,
        'A2_total': a2,
        'A2_direct': a2_direct,
        'A2_consistency': a2 == a2_direct,
        'mu_1': mu1,
        'mu_2': mu2,
        'variance': variance,
        'B2': (s1 - 2) / 48,
    }


