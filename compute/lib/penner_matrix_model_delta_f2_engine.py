def penner_F2_exact(N: int) -> Fraction:
    r"""Penner model genus-2 free energy (exact).

    The Penner model partition function:
      Z_N = integral dM exp(-N Tr[-log(1-M) + M]) = (combinatorial)

    The genus-2 contribution to log Z / N^{-2}:

    From the Harer-Zagier formula:
      chi(M_{g,0}) = B_{2g} / (2g(2g-2))  for g >= 2
    where B_{2g} is the Bernoulli number.

    F_2^Penner = chi(M_2) = B_4 / (4*2) = (-1/30) / 8 = -1/240.

    But this is the ORBIFOLD Euler characteristic. The actual genus-2
    free energy of the Penner model is:
      F_2 = (-1)^2 * |B_4|/(4*2) = 1/240.

    With sign conventions: B_4 = -1/30, so
      F_2^Penner = B_4/(4*2) = -1/30 / 8 = -1/240.

    In the Harer-Zagier convention: chi(M_2) = -1/240.
    In our convention (positive F_g): F_2 = |chi(M_2)| = 1/240.

    However, the PENNER MODEL genus-2 free energy is NOT the same
    as the shadow tower genus-2 free energy. The Penner model
    produces the Euler characteristic chi(M_{g,n}), while the shadow
    tower produces lambda_g^FP = intersection numbers.

    For comparison, lambda_2^FP = 7/5760.

    The two differ by a factor:
      chi(M_2) / lambda_2^FP = (-1/240) / (7/5760) = -24/7.

    This is NOT a simple ratio, confirming that the Penner model
    and the shadow tower are NOT directly equal.
    """
    B4 = Fraction(-1, 30)
    return B4 / 8  # -1/240


def lambda_fp(g: int) -> Fraction:
    r"""Faber-Pandharipande intersection number.

    lambda_g^FP = (2^{2g-1} - 1) / 2^{2g-1} * |B_{2g}| / (2g)!
    """
    B2g = _bernoulli(2 * g)
    return (Fraction(2 ** (2*g - 1) - 1, 2 ** (2*g - 1))
            * abs(B2g) / factorial(2 * g))


@lru_cache(maxsize=64)
def _bernoulli(n: int) -> Fraction:
    """Exact Bernoulli number B_n."""
    if n < 0:
        raise ValueError
    if n == 0:
        return Fraction(1)
    if n == 1:
        return Fraction(-1, 2)
    if n % 2 == 1 and n > 1:
        return Fraction(0)
    result = Fraction(0)
    for k in range(n):
        result += Fraction(comb(n + 1, k)) * _bernoulli(k)
    return -result / (n + 1)


