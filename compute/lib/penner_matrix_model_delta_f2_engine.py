    return {
        'N': N,
        'M': M,
        'eigenvalues': eigenvalues,
        'degree_P': M,
        'degree_P_prime': M - 1,
        'num_branch_points': num_branch_points,
        'residues': residues,
        'genus_spectral_curve': 0,
        'curve_type': 'rational (genus 0)',
    }


