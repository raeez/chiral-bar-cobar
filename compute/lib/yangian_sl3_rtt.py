def adjoint_rep_matrices() -> List[np.ndarray]:
    r"""Adjoint representation matrices for sl_3 (8x8).

    (ad X)_{ab} = f^{a}_{Xb} = structure constant.
    (ad T_c)_{ab} = f^{a}_{cb}

    where [T_c, T_b] = f^{a}_{cb} T_a.

    Returns: list of 8 matrices of shape (8, 8).
    """
    f = sl3_structure_constants()
    mats = []
    for c in range(DIM_G):
        M = np.zeros((DIM_G, DIM_G), dtype=complex)
        for b in range(DIM_G):
            if (c, b) in f:
                for a, coeff in f[(c, b)].items():
                    M[a, b] = float(coeff)
        mats.append(M)
    return mats


def adjoint_killing_form() -> np.ndarray:
    r"""Killing form in the adjoint representation: K_{ab} = tr(ad_a ad_b).

    For sl_3 (simple Lie algebra), the Killing form is proportional
    to the trace form in the adjoint: K_{ab} = 2 h^vee * g_{ab}
    where g_{ab} = tr_{fund}(T_a T_b) and h^vee = 3.

    Returns: 8x8 matrix.
    """
    mats = adjoint_rep_matrices()
    K = np.zeros((DIM_G, DIM_G))
    for a in range(DIM_G):
        for b in range(DIM_G):
            K[a, b] = np.trace(mats[a] @ mats[b]).real
    return K


def adjoint_casimir_tensor() -> np.ndarray:
    r"""Casimir tensor Omega^{adj} in the adjoint representation (64x64).

    Omega^{adj} = sum_a T^a_{adj} \otimes T_a^{adj}

    where T^a_{adj} is the adjoint matrix and T_a^{adj} is the dual basis
    (w.r.t. the trace form in the fundamental, then represented in adjoint).

    Returns: 64x64 matrix.
    """
    adj_mats = adjoint_rep_matrices()
    fund_mats = fund_rep_matrices()
    dual_fund = dual_basis_matrices()

    # Dual basis in adjoint: T_a^{adj} = sum_b (G^{-1})_{ab} T^b_{adj}
    Ginv = inverse_killing_form()
    dual_adj = []
    for a in range(DIM_G):
        M = sum(Ginv[a, b] * adj_mats[b] for b in range(DIM_G))
        dual_adj.append(M)

    d = DIM_G  # 8
    Omega = np.zeros((d * d, d * d), dtype=complex)
    for a in range(DIM_G):
        Omega += np.kron(adj_mats[a], dual_adj[a])

    return Omega.real


def adjoint_R_matrix(u: complex) -> np.ndarray:
    r"""R-matrix in the adjoint representation: R^{adj}(u).

    For the rational Yangian, the adjoint R-matrix is NOT simply u I + P.
    The formula is:

        R^{adj}(u) = 1 - Omega^{adj}/u + higher corrections

    For sl_N in general, the R-matrix on V_lambda x V_mu decomposes
    according to irreducible components of V_lambda x V_mu.

    For sl_3: adj x adj = 1 + 8 + 8 + 10 + 10* + 27
    (1 + 8_s + 8_a + 10 + 10* + 27 is the full decomposition).

    Wait, more precisely for sl_3:
    8 x 8 = 1 + 8 + 8 + 10 + 10* + 27
    (symmetric: 1 + 8 + 27 = 36, antisymmetric: 8 + 10 + 10* = 28,
     but 36 + 28 = 64 = 8^2, correct).

    Actually: 8 otimes 8 has Clebsch-Gordan decomposition:
    adj x adj = 1 + 8 + 8 + 10 + bar{10} + 27

    The R-matrix on each irreducible component acts as a scalar:
    R^{adj}(u)|_{V_lambda} = prod_{(i,j) in lambda-specific} (u + c_{ij}) / (u + c'_{ij})

    For the rational Yang R-matrix evaluated on adj x adj:
    R^{adj}(u) is obtained from the universal R-matrix R = 1 + r/u + O(1/u^2)
    evaluated on adj x adj.  At leading order it equals u * I_{64} + Omega^{adj}.

    We compute it as u * I_{64} + P^{adj} where P^{adj} is the permutation
    on the adjoint tensor product.

    However, for non-fundamental representations, the Yang R-matrix
    R(u) = u I + P is NO LONGER the correct R-matrix.  The correct
    R-matrix for adj x adj involves Casimir eigenvalues on each irreducible
    component.

    For the RATIONAL Yangian at the semiclassical level (1/kappa expansion):
    R^{adj}(u) = 1 + Omega^{adj}/(kappa * u) + O(1/kappa^2)
    and Omega^{adj} has eigenvalues corresponding to C_2 of each irrep
    in adj x adj.

    We compute eigenvalues of Omega^{adj} directly.

    Returns: 64x64 matrix R^{adj}(u) at the leading order u I + P^{adj}.
    """
    d = DIM_G  # 8
    Omega = adjoint_casimir_tensor()
    I64 = np.eye(d * d, dtype=complex)

    # Permutation on adj x adj
    P_adj = np.zeros((d * d, d * d), dtype=complex)
    for i in range(d):
        for j in range(d):
            P_adj[i * d + j, j * d + i] = 1.0

    # The R-matrix at leading semiclassical order
    # For the Yang R-matrix in the fundamental, R(u) = u I + P.
    # For the adjoint, the analogous object is u I + P^{adj},
    # which is the image of the universal R(u) = u + P under adj x adj.
    return u * I64 + P_adj


