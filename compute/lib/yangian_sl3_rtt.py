def rtt_to_drinfeld_map_level1(a: complex = 1.0) -> Dict[str, object]:
    r"""Map from RTT generators t_{ij}^{(1)} to Drinfeld generators.

    The Gauss decomposition of T(u):
        T(u) = F(u) H(u) E(u)
    where F is lower unitriangular, H is diagonal, E is upper unitriangular.

    From this decomposition:
        h_i(u) = H_{ii}(u) / H_{i+1,i+1}(u)    (ratio of diagonal entries)
        e_i(u) = E_{i,i+1}(u)                    (superdiagonal of E)
        f_i(u) = F_{i+1,i}(u)                    (subdiagonal of F)

    At level 1 in the evaluation representation ev_a:
        T^{(1)}_{ij} = a * delta_{ij} (for the trivial evaluation)

    For a nontrivial representation, the RTT-to-Drinfeld map at level 1 is:
        e_1^{(1)} = t_{12}^{(1)}
        e_2^{(1)} = t_{23}^{(1)}
        f_1^{(1)} = t_{21}^{(1)}
        f_2^{(1)} = t_{32}^{(1)}
        h_1^{(1)} = t_{11}^{(1)} - t_{22}^{(1)}
        h_2^{(1)} = t_{22}^{(1)} - t_{33}^{(1)}

    (These are the standard formulas from Molev, Theorem 1.11.2.)

    Returns: dict describing the map.
    """
    return {
        "e1^(1)": "t_{12}^{(1)}",
        "e2^(1)": "t_{23}^{(1)}",
        "f1^(1)": "t_{21}^{(1)}",
        "f2^(1)": "t_{32}^{(1)}",
        "h1^(1)": "t_{11}^{(1)} - t_{22}^{(1)}",
        "h2^(1)": "t_{22}^{(1)} - t_{33}^{(1)}",
        "source": "Molev, Thm 1.11.2",
    }


def rtt_to_drinfeld_map_level2() -> Dict[str, str]:
    r"""Map from RTT generators t_{ij}^{(r)} to Drinfeld generators at level 2.

    At level 2, the map involves products of level-1 generators:
        e_1^{(2)} = t_{12}^{(2)} - t_{12}^{(1)} t_{22}^{(1)} + t_{13}^{(1)} t_{32}^{(1)} - ...
        h_1^{(2)} = t_{11}^{(2)} - t_{22}^{(2)} + (t_{12}^{(1)} t_{21}^{(1)} - t_{21}^{(1)} t_{12}^{(1)})
        ...

    The explicit formulas are obtained by expanding the Gauss decomposition
    T(u) = F(u) H(u) E(u) to second order in u^{-1}.

    From Molev, Section 1.11:
        For the upper-triangular part:
            e_i^{(r)} = t_{i,i+1}^{(r)} - sum_{s=1}^{r-1} t_{i,i}^{(s)} e_i^{(r-s)}  (recursive)
        For the diagonal:
            h_i^{(r)} involves t_{ii}^{(r)} corrected by lower-order terms.

    Returns: dict describing the level-2 map.
    """
    return {
        "e1^(2)": "t_{12}^{(2)} - t_{11}^{(1)} * t_{12}^{(1)}",
        "e2^(2)": "t_{23}^{(2)} - t_{22}^{(1)} * t_{23}^{(1)}",
        "f1^(2)": "t_{21}^{(2)} - t_{21}^{(1)} * t_{11}^{(1)}",
        "f2^(2)": "t_{32}^{(2)} - t_{32}^{(1)} * t_{22}^{(1)}",
        "h1^(2)": "t_{11}^{(2)} - t_{22}^{(2)} - t_{12}^{(1)} * t_{21}^{(1)} + t_{22}^{(1)} * t_{21}^{(1)} (corrected)",
        "h2^(2)": "t_{22}^{(2)} - t_{33}^{(2)} - t_{23}^{(1)} * t_{32}^{(1)} + t_{33}^{(1)} * t_{32}^{(1)} (corrected)",
        "source": "Molev, Section 1.11, recursive Gauss decomposition",
    }


def verify_drinfeld_level0_bracket() -> float:
    r"""Verify that level-0 Drinfeld generators satisfy sl_3 commutation relations.

    [h_i, e_j] = A_{ij} e_j
    [h_i, f_j] = -A_{ij} f_j
    [e_i, f_j] = delta_{ij} h_i

    Returns: max norm of violations.
    """
    gens = drinfeld_generators_level0()
    A = np.array([[2, -1], [-1, 2]])  # Cartan matrix
    max_err = 0.0

    # [h_i, e_j] = A_{ij} e_j
    for i, hi in enumerate(["h1", "h2"]):
        for j, ej in enumerate(["e1", "e2"]):
            comm = gens[hi] @ gens[ej] - gens[ej] @ gens[hi]
            expected = A[i, j] * gens[ej]
            max_err = max(max_err, float(np.linalg.norm(comm - expected)))

    # [h_i, f_j] = -A_{ij} f_j
    for i, hi in enumerate(["h1", "h2"]):
        for j, fj in enumerate(["f1", "f2"]):
            comm = gens[hi] @ gens[fj] - gens[fj] @ gens[hi]
            expected = -A[i, j] * gens[fj]
            max_err = max(max_err, float(np.linalg.norm(comm - expected)))

    # [e_i, f_j] = delta_{ij} h_i
    for i in range(2):
        for j in range(2):
            ei_key = f"e{i+1}"
            fj_key = f"f{j+1}"
            hi_key = f"h{i+1}"
            comm = gens[ei_key] @ gens[fj_key] - gens[fj_key] @ gens[ei_key]
            if i == j:
                expected = gens[hi_key]
            else:
                expected = np.zeros((N, N), dtype=complex)
            max_err = max(max_err, float(np.linalg.norm(comm - expected)))

    return max_err


def verify_drinfeld_serre_relations() -> float:
    r"""Verify Serre relations for level-0 Drinfeld generators.

    For sl_3 with Cartan matrix A = [[2,-1],[-1,2]]:
        (ad e_i)^{1 - A_{ij}} (e_j) = 0 for i != j
        (ad f_i)^{1 - A_{ij}} (f_j) = 0 for i != j

    Since A_{12} = A_{21} = -1, we need 1 - (-1) = 2:
        [e_1, [e_1, e_2]] = 0
        [e_2, [e_2, e_1]] = 0
        [f_1, [f_1, f_2]] = 0
        [f_2, [f_2, f_1]] = 0

    Returns: max norm of violations.
    """
    gens = drinfeld_generators_level0()
    max_err = 0.0

    def ad(X, Y):
        return X @ Y - Y @ X

    # [e_1, [e_1, e_2]] = 0
    max_err = max(max_err, float(np.linalg.norm(ad(gens["e1"], ad(gens["e1"], gens["e2"])))))
    # [e_2, [e_2, e_1]] = 0
    max_err = max(max_err, float(np.linalg.norm(ad(gens["e2"], ad(gens["e2"], gens["e1"])))))
    # [f_1, [f_1, f_2]] = 0
    max_err = max(max_err, float(np.linalg.norm(ad(gens["f1"], ad(gens["f1"], gens["f2"])))))
    # [f_2, [f_2, f_1]] = 0
    max_err = max(max_err, float(np.linalg.norm(ad(gens["f2"], ad(gens["f2"], gens["f1"])))))

    return max_err


