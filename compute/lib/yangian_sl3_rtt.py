def _P_matrix_symbolic() -> Matrix:
    """Permutation matrix P on C^3 otimes C^3 as a 9x9 sympy Matrix."""
    P = zeros(N * N, N * N)
    for i in range(N):
        for j in range(N):
            P[i * N + j, j * N + i] = 1
    return P


def _I9() -> Matrix:
    """9x9 identity matrix."""
    return eye(N * N)


def _kron(A: Matrix, B: Matrix) -> Matrix:
    """Kronecker product of two matrices."""
    ra, ca = A.rows, A.cols
    rb, cb = B.rows, B.cols
    result = zeros(ra * rb, ca * cb)
    for i in range(ra):
        for j in range(ca):
            for k in range(rb):
                for l in range(cb):
                    result[i * rb + k, j * cb + l] = A[i, j] * B[k, l]
    return result


def rtt_relation_order(m: int, n: int) -> str:
    r"""Description of the RTT relation at order u^{-m} v^{-n}.

    The RTT relation R_{12}(u-v) T_1(u) T_2(v) = T_2(v) T_1(u) R_{12}(u-v)
    is expanded in powers of u^{-1} and v^{-1}.  Each order gives a
    constraint on the RTT generators.

    Returns a string description of the constraint.
    """
    descriptions = {
        (0, 0): "Trivial: R(w) = R(w).",
        (1, 0): "[T^(1) x I, R(w)] = 0: T^(1) commutes with R in first factor. "
                 "Forces T^(1) x I to commute with P, so T^(1) is scalar or "
                 "the constraint is automatically satisfied for the Yangian.",
        (0, 1): "[I x T^(1), R(w)] = 0: T^(1) commutes with R in second factor. "
                 "Same constraint as (1,0) by symmetry.",
        (1, 1): "Key quadratic relation: [T^(1)_1, T^(1)_2] = [P, T^(1)_1 - T^(1)_2]. "
                 "This is the defining relation of the Yangian at level 1. "
                 "In components: [t_{ij}^(1), t_{kl}^(1)] = t_{kj}^(1) delta_{il} - t_{il}^(1) delta_{kj}.",
        (2, 0): "T^(2) commutes with R in first factor (same type as (1,0)).",
        (0, 2): "T^(2) commutes with R in second factor.",
        (2, 1): "Mixed relation involving T^(1) and T^(2): cubic constraint.",
        (1, 2): "Mixed relation involving T^(1) and T^(2): cubic constraint.",
        (2, 2): "Quartic relation involving T^(1) and T^(2).",
    }
    return descriptions.get((m, n), f"Order ({m},{n}): higher-order constraint.")


