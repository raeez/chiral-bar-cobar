def rtt_generators_symbolic(max_level: int = 2) -> Dict[int, Matrix]:
    r"""Symbolic RTT generators T^{(r)} for r = 0, ..., max_level.

    T^{(0)} = I_3  (by convention for the Yangian).
    T^{(r)} for r >= 1 are 3x3 matrices of formal symbols t_{ij}^{(r)}.

    Returns:
        Dict mapping level r to a 3x3 sympy Matrix.
    """
    gens = {}
    gens[0] = eye(N)
    for r in range(1, max_level + 1):
        entries = []
        for i in range(N):
            row = []
            for j in range(N):
                row.append(Symbol(f't_{i+1}{j+1}^({r})', commutative=False))
            entries.append(row)
        gens[r] = Matrix(entries)
    return gens


def T_matrix_formal(u: Symbol, max_level: int = 2) -> Matrix:
    r"""Formal generating matrix T(u) = \sum_{r=0}^{max_level} T^{(r)} u^{-r}.

    Returns a 3x3 matrix of formal power series in u^{-1}.
    """
    gens = rtt_generators_symbolic(max_level)
    T = zeros(N, N)
    for r in range(max_level + 1):
        T += gens[r] * u ** (-r)
    return T


