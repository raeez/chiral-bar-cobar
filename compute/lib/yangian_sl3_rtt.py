def casimir_eigenvalue_sl3(lambda1: int, lambda2: int) -> Rational:
    r"""Quadratic Casimir eigenvalue C_2(\lambda) for sl_3 representation.

    For highest weight \lambda = (\lambda_1, \lambda_2) in the omega-basis
    (Dynkin labels), the Casimir eigenvalue with the trace normalization
    (X, Y) = tr_{fund}(X Y) is:

        C_2(\lambda) = (\lambda, \lambda + 2\rho)

    where \rho = (1,1) and the inner product uses A^{-1} = (1/3)[[2,1],[1,2]]
    (inverse Cartan matrix for sl_3).

    Explicitly:
        C_2(\lambda_1, \lambda_2) = (2\lambda_1^2 + 2\lambda_2^2 + 2\lambda_1\lambda_2
                                      + 6\lambda_1 + 6\lambda_2) / 3

    Verification:
        C_2(1,0) = (2+0+0+6+0)/3 = 8/3 (fundamental)
        C_2(0,1) = (0+2+0+0+6)/3 = 8/3 (anti-fundamental)
        C_2(1,1) = (2+2+2+6+6)/3 = 6 (adjoint)
        C_2(2,0) = (8+0+0+12+0)/3 = 20/3 (symmetric square)
        C_2(3,0) = (18+0+0+18+0)/3 = 12
        C_2(0,3) = (0+18+0+0+18)/3 = 12
        C_2(2,2) = (8+8+8+12+12)/3 = 16
        C_2(0,0) = 0 (trivial)

    Returns: Rational Casimir eigenvalue.
    """
    return Rational(
        2 * lambda1 ** 2 + 2 * lambda2 ** 2 + 2 * lambda1 * lambda2
        + 6 * lambda1 + 6 * lambda2,
        3,
    )


def dimension_sl3(lambda1: int, lambda2: int) -> int:
    r"""Dimension of the sl_3 representation with Dynkin labels (lambda1, lambda2).

    dim(\lambda_1, \lambda_2) = (lambda_1 + 1)(lambda_2 + 1)(lambda_1 + lambda_2 + 2) / 2

    Returns: integer dimension.
    """
    return (lambda1 + 1) * (lambda2 + 1) * (lambda1 + lambda2 + 2) // 2


