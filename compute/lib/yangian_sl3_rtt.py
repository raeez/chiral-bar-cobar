def _fund_generators_level1() -> np.ndarray:
    r"""Concrete level-1 RTT generators for Y(sl_3) in fundamental rep.

    For the evaluation representation ev_a: Y(sl_3) -> End(C^3),
    T(u) is represented by:
        T^{ev_a}(u) = (u - a) I + P_{fund}
    i.e. T^{(0)} = I, T^{(1)} = a * I + generators of sl_3.

    For the standard evaluation at a = 0:
        T^{ev_0}(u) = u I (trivial).
    For a general evaluation at a:
        T^{ev_a}(u) = (u - a) I + P → T^{(0)} = I, T^{(1)}_{ij} = a delta_{ij} + E_{ij}.

    We use the EVALUATION REPRESENTATION at a = 0 for simplicity:
    T(u) maps to the L-operator L_0(u) = R_{0,phys}(u) = u I + P|_{0,phys}.
    In the fundamental of sl_3:
        L(u)_{ij,kl} = u delta_{ik} delta_{jl} + delta_{il} delta_{jk}

    The 3x3 matrix entries are: L(u)_{ij} = u delta_{ij} + E_{ji}
    (operator on physical space C^3).
    """
    # L(u)_{ij} as a map on physical C^3:
    # L(u)_{ij} = u delta_{ij} I_{phys} + E_{ji}^{phys}
    # But actually L(u) is an End(C^3)-valued 3x3 matrix.
    # The evaluation homomorphism Y(sl_3) -> End(C^3) sends
    # t_{ij}^{(1)} -> E_{ji} (the elementary matrix).
    T1 = np.zeros((N, N), dtype=complex)
    for i in range(N):
        for j in range(N):
            # t_{ij}^{(1)} -> E_{ji} in the evaluation representation
            T1[i, j] = 0  # placeholder; the actual map is T1[i,j] = E_{ji}
    return T1


def rtt_evaluation_L_operator(u: complex, a: complex = 0.0) -> np.ndarray:
    r"""L-operator for the evaluation representation ev_a of Y(sl_3).

    L_a(u) = R_{aux,phys}(u - a) = (u - a) I_{9} + P

    This is a 9x9 matrix acting on C^3_{aux} \otimes C^3_{phys}.
    The RTT relation for L-operators:
        R_{12}(u-v) L_{1a}(u) L_{2a}(v) = L_{2a}(v) L_{1a}(u) R_{12}(u-v)

    follows from the Yang-Baxter equation R_{12} R_{13} R_{23} = R_{23} R_{13} R_{12}.

    Args:
        u: spectral parameter.
        a: evaluation point.

    Returns:
        9x9 complex matrix.
    """
    return R_matrix_fund_exact_yang(u - a)


def verify_rtt_from_ybe(u: complex, v: complex, a: complex = 0.0) -> float:
    r"""Verify RTT relation for evaluation L-operators via YBE.

    R_{12}(u-v) L_{1}(u) L_{2}(v) = L_{2}(v) L_{1}(u) R_{12}(u-v)

    where L_{1}(u) = R_{13}(u-a) and L_{2}(v) = R_{23}(v-a).
    This is a 27x27 matrix equation (on C^3_{aux1} x C^3_{aux2} x C^3_{phys}).

    The RTT relation IS the YBE with spaces relabeled:
    R_{12}(u-v) R_{13}(u-a) R_{23}(v-a) = R_{23}(v-a) R_{13}(u-a) R_{12}(u-v).

    Returns: Frobenius norm of LHS - RHS (should be ~0).
    """
    from compute.lib.yangian_rmatrix_sl3 import (
        _embed_12,
        _embed_13,
        _embed_23,
    )
    R12 = _embed_12(R_matrix_fund_exact_yang(u - v), N)
    L1 = _embed_13(R_matrix_fund_exact_yang(u - a), N)
    L2 = _embed_23(R_matrix_fund_exact_yang(v - a), N)

    lhs = R12 @ L1 @ L2
    rhs = L2 @ L1 @ R12
    return float(np.linalg.norm(lhs - rhs))


def extract_rtt_relation_11(a: complex = 0.0) -> np.ndarray:
    r"""Extract the (1,1) order RTT relation numerically.

    At order u^{-1} v^{-1}, the RTT relation gives:
        [T_1^{(1)}, T_2^{(1)}] = P (T_1^{(1)} - T_2^{(1)})
                                   - (T_1^{(1)} - T_2^{(1)}) P

    In the evaluation representation ev_a, T^{(1)}_{ij} = a delta_{ij}
    (evaluation shifts). For a = 0 the relation is trivially satisfied.
    For a != 0: T^{(1)} = a I, so [T_1, T_2] = 0 and the RHS also vanishes.

    For a nontrivial check, use two DIFFERENT evaluation points a1, a2
    and verify the RTT relation for L_{1}(u) = R(u-a1) and L_{2}(v) = R(v-a2).

    Returns: the 9x9 matrix [T_1^{(1)}, T_2^{(1)}] - P(T_1^{(1)} - T_2^{(1)}) + (T_1^{(1)} - T_2^{(1)})P
    """
    P = permutation_matrix_3().astype(complex)
    I3 = np.eye(N, dtype=complex)
    # T^{(1)} in evaluation at a: T^{(1)} = a * I_3
    T1_mat = a * I3
    T1_1 = np.kron(T1_mat, I3)  # T^{(1)} x I
    T1_2 = np.kron(I3, T1_mat)  # I x T^{(1)}

    commutator = T1_1 @ T1_2 - T1_2 @ T1_1
    rhs = P @ (T1_1 - T1_2) - (T1_1 - T1_2) @ P

    return commutator - rhs


def verify_rtt_order_by_order(u_val: float = 10.0, v_val: float = 7.0,
                               a_val: float = 0.0, max_order: int = 3) -> Dict[str, float]:
    r"""Verify RTT relation order by order in 1/u and 1/v.

    Expand R(u-v) T_1(u) T_2(v) = T_2(v) T_1(u) R(u-v) numerically.
    Using evaluation L-operators at point a, compute both sides and
    extract coefficients of u^{-m} v^{-n} by numerical differentiation.

    Instead, we verify the full RTT relation at multiple (u,v) and
    check that each order coefficient matches.

    Returns: dict of norms at each order.
    """
    results = {}
    # The RTT relation = YBE with relabeled spaces.
    # Verify at multiple spectral parameter values.
    test_params = [
        (u_val, v_val),
        (u_val + 1, v_val - 1),
        (u_val * 2, v_val * 3),
        (u_val + 0.5j, v_val - 0.3j),
    ]
    for u, v in test_params:
        norm = verify_rtt_from_ybe(u, v, a_val)
        results[f"RTT at u={u}, v={v}, a={a_val}"] = norm
    return results


