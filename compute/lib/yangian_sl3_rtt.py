def quantum_determinant_matrix(u: complex) -> np.ndarray:
    r"""Quantum determinant qdet T(u) for Y(sl_3) in evaluation representation.

    qdet T(u) = \sum_{\sigma \in S_3} (-1)^{l(\sigma)}
        T_{1,\sigma(1)}(u) T_{2,\sigma(2)}(u-1) T_{3,\sigma(3)}(u-2)

    In the evaluation representation ev_a, T_{ij}(u) = (u-a) delta_{ij} + E_{ji},
    so T_{ij}(u) is a 3x3 matrix-valued function.

    The quantum determinant is a scalar (central) element.  In the evaluation
    representation it acts as the scalar:
        qdet = (u - a)(u - a - 1)(u - a - 2)

    This is the value of the quantum determinant on the evaluation module V(a).

    Args:
        u: spectral parameter.

    Returns:
        The 3x3 identity times the scalar qdet value.
    """
    a = 0.0
    qdet_val = quantum_determinant_scalar(u, a)
    return qdet_val * np.eye(N, dtype=complex)


def quantum_determinant_scalar(u: complex, a: complex = 0.0) -> complex:
    r"""Scalar value of qdet T(u) on the evaluation module V(a).

    In the RTT convention R(u) = u I + P with T_{ij}(u) = u delta_{ij} + E_{ji},
    the quantum determinant on the fundamental evaluation module V(a) evaluates to:

        qdet T(u)|_{V(a)} = (u - a + 1)(u - a - 1)(u - a - 2)

    The roots are at u = a - 1, a + 1, a + 2.  The shift by +1 relative to
    the naive (u-a)(u-a-1)(u-a-2) arises from the E_{ji} term in T_{ii}(u):
    on the highest-weight vector, T_{11}(u) = u + 1 (not u).

    Verification: at a = 0, u = 5:
        (5+1)(5-1)(5-2) = 6*4*3 = 72.
    Confirmed by direct computation of the column-determinant sum over S_3.
    """
    return (u - a + 1) * (u - a - 1) * (u - a - 2)


def verify_qdet_centrality(u: complex, v: complex, a: complex = 0.0) -> float:
    r"""Verify that qdet T(u) commutes with T_{ij}(v).

    In the evaluation representation, qdet is a scalar, so it commutes
    with everything.  The nontrivial content is that qdet is central
    in the ABSTRACT Yangian, not just in a specific representation.

    For the evaluation representation, we verify:
        [qdet(u) I, L(v)] = 0

    which is trivially true since qdet(u) is scalar.  The more
    nontrivial check is done via the RTT relation.

    The centrality follows from:
    1. qdet T(u) is invariant under T(u) -> g T(u) g^{-1} for g in GL(3).
    2. The antisymmetrizer A_3 satisfies:
       A_3 R_{12}(u_1-u_2) R_{13}(u_1-u_3) R_{23}(u_2-u_3)
       = A_3 * (u_1-u_2)(u_1-u_3)(u_2-u_3)
    3. Combining: qdet T(u) commutes with T_{ij}(v) for all i,j.

    Returns: norm of [qdet, L(v)] (should be 0).
    """
    qdet_val = quantum_determinant_scalar(u, a)
    I9 = np.eye(N * N, dtype=complex)
    L = rtt_evaluation_L_operator(v, a)

    comm = qdet_val * I9 @ L - L @ (qdet_val * I9)
    return float(np.linalg.norm(comm))


def verify_qdet_antisymmetrizer(u: complex) -> float:
    r"""Verify the quantum determinant via the column-determinant formula.

    The quantum determinant qdet T(u) is defined by:
        qdet T(u) = sum_{sigma in S_3} sgn(sigma)
            T_{1,sigma(1)}(u) T_{2,sigma(2)}(u-1) T_{3,sigma(3)}(u-2)

    We verify two properties:
    1. qdet is a scalar matrix (proportional to I_3).
    2. The scalar value matches (u-a+1)(u-a-1)(u-a-2) for the evaluation at a.

    This function checks both properties at the given spectral parameter u
    for the evaluation at a = 0.

    Returns: max(off-diagonal norm, value error).
    """
    a = 0.0
    qdet = quantum_determinant_explicit(u, a)

    # Check scalar (proportional to I)
    off_diag = qdet - np.diag(np.diag(qdet))
    off_diag_norm = float(np.max(np.abs(off_diag)))

    # Check value
    expected = quantum_determinant_scalar(u, a)
    value_err = float(np.max(np.abs(np.diag(qdet) - expected)))

    return max(off_diag_norm, value_err)


def quantum_determinant_explicit(u: complex, a: complex = 0.0) -> np.ndarray:
    r"""Explicit computation of qdet T(u) as a sum over S_3.

    qdet T(u) = sum_{sigma in S_3} (-1)^{l(sigma)}
        T_{1,sigma(1)}(u) T_{2,sigma(2)}(u-1) T_{3,sigma(3)}(u-2)

    In the evaluation representation at a, T_{ij}(u) acts on physical C^3 by:
        T_{ij}(u) = (u - a) delta_{ij} + E_{ji}

    where E_{ji} is the elementary matrix.

    Returns: 3x3 matrix (should be scalar * I_3).
    """
    def T_entry(i, j, u_val):
        """T_{ij}(u) as a 3x3 matrix acting on physical space."""
        M = np.zeros((N, N), dtype=complex)
        if i == j:
            M += (u_val - a) * np.eye(N, dtype=complex)
        # E_{ji} contribution
        M[j, i] += 1.0
        return M

    qdet = np.zeros((N, N), dtype=complex)
    for sigma in permutations(range(N)):
        sign = 1
        # Count inversions
        for p in range(N):
            for q in range(p + 1, N):
                if sigma[p] > sigma[q]:
                    sign *= -1

        # Product: T_{1,sigma(1)}(u) T_{2,sigma(2)}(u-1) T_{3,sigma(3)}(u-2)
        prod = T_entry(0, sigma[0], u) @ T_entry(1, sigma[1], u - 1) @ T_entry(2, sigma[2], u - 2)
        qdet += sign * prod

    return qdet


def verify_qdet_is_scalar(u: complex, a: complex = 0.0) -> float:
    r"""Verify that qdet T(u) is a scalar matrix (proportional to I_3).

    Returns: max off-diagonal element (should be ~0).
    """
    qdet = quantum_determinant_explicit(u, a)
    # Check if off-diagonal elements are zero
    off_diag = qdet - np.diag(np.diag(qdet))
    return float(np.max(np.abs(off_diag)))


def verify_qdet_value(u: complex, a: complex = 0.0) -> float:
    r"""Verify that qdet T(u) = (u-a)(u-a-1)(u-a-2) I_3.

    Returns: norm of difference.
    """
    qdet = quantum_determinant_explicit(u, a)
    expected = quantum_determinant_scalar(u, a) * np.eye(N, dtype=complex)
    return float(np.linalg.norm(qdet - expected))


