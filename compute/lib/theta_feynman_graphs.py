@dataclass(frozen=True)
class PlantedTree:
    r"""A planted tree (rooted tree with labeled leaves).

    Representation: nested tuples. Leaf i is integer i (0-indexed).
    Internal node is (left, right) for binary trees.
    For general valency, (child_1, child_2, ..., child_k) for k-valent.

    We focus on BINARY planted trees (trivalent vertices) because the
    bar complex is generated by the binary OPE.  Higher-valency vertices
    appear via the transferred A-infinity structure m_k for k >= 3.

    Attributes:
        structure: The tree as nested tuple/int.
        n_leaves: Number of leaves.
        label: Human-readable description.
    """
    structure: Any  # nested tuples of ints
    n_leaves: int
    label: str = ""

    def automorphism_count(self) -> int:
        """Number of automorphisms of the planted tree.

        For planted (rooted, leaf-labeled) binary trees, the automorphism
        group is trivial (|Aut| = 1) because the leaves are labeled.

        For the UNLABELED planted tree shape, automorphisms permute
        isomorphic subtrees.  But since our trees have LABELED LEAVES,
        the only automorphism is the identity.
        """
        return 1

    def internal_vertices(self) -> int:
        """Number of internal (non-leaf) vertices."""
        return _count_internal(self.structure)

    def depth(self) -> int:
        """Maximum depth from root to any leaf."""
        return _tree_depth(self.structure)


def _count_internal(tree) -> int:
    """Count internal vertices in a nested-tuple tree."""
    if isinstance(tree, int):
        return 0
    return 1 + sum(_count_internal(child) for child in tree)


def _tree_depth(tree) -> int:
    """Maximum depth of a nested-tuple tree."""
    if isinstance(tree, int):
        return 0
    return 1 + max(_tree_depth(child) for child in tree)


def _shift_tree(tree, offset: int):
    """Shift all leaf labels by offset."""
    if isinstance(tree, int):
        return tree + offset
    return tuple(_shift_tree(child, offset) for child in tree)


