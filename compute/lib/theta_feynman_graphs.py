def _count_leaves(tree_struct) -> int:
    """Count leaves in a nested-tuple tree."""
    if isinstance(tree_struct, int):
        return 1
    return sum(_count_leaves(child) for child in tree_struct)


def _tree_to_string(tree_struct) -> str:
    """Convert nested-tuple tree to readable string."""
    if isinstance(tree_struct, int):
        return str(tree_struct)
    parts = [_tree_to_string(child) for child in tree_struct]
    return f"({', '.join(parts)})"


