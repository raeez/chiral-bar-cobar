    def mc_residual(self, r: int) -> FR:
        """MC equation residual at arity r.

        The MC equation on the primary line:
            sum_{j+k=r+2, j,k>=2} j*k * S_j * S_k = 0  (for r >= 2)

        At r=2: 4*S_2^2 = 4*kappa^2 (nonzero -- this is the curvature m_0)
        At r=3: 2*3*S_2*S_3 + 3*2*S_3*S_2 = 12*kappa*alpha
        At r=4: 2*4*S_2*S_4 + 4*2*S_4*S_2 + 3*3*S_3*S_3
              = 16*kappa*S_4 + 9*alpha^2

        For r >= 5: the MC equation is SATISFIED by the generating function
        construction.  The residual should be zero.

        Actually, the MC equation from the generating function is:
            H(t)^2 = t^4 * Q_L(t)
        where H(t) = sum_r r*S_r*t^r.

        Expanding H(t)^2 = (sum r*S_r*t^r)^2 = sum_n (sum_{j+k=n} j*k*S_j*S_k) t^n

        Setting this equal to t^4 * Q_L(t) = q_0*t^4 + q_1*t^5 + q_2*t^6:

        Coefficient of t^n for n >= 7: sum_{j+k=n, j,k>=2} j*k*S_j*S_k = 0
        (since t^4 * Q_L has no t^n for n >= 7).

        So the MC equation sum_{j+k=n} j*k*S_j*S_k = 0 holds for n >= 7.
        Setting r = n - 2: sum_{j+k=r+2} j*k*S_j*S_k = 0 for r >= 5.

        For n = 4,5,6 (r = 2,3,4): these equalities encode the seed data.
        """
        if r < 5:
            return ZERO  # Seed equations (not residuals)

        total = ZERO
        for j in range(2, r + 1):
            k = r + 2 - j
            if k < 2:
                continue
            s_j = self.S(j)
            s_k = self.S(k)
            total += j * k * s_j * s_k

        return total

    def verify_mc(self, max_r: int = None) -> Dict[int, FR]:
        """Verify MC equation at all arities. Returns {r: residual}."""
        if max_r is None:
            max_r = self.max_arity
        return {r: self.mc_residual(r) for r in range(5, max_r + 1)}

