def planted_binary_trees(n: int) -> List[PlantedTree]:
    """Enumerate all planted binary trees with n labeled leaves.

    These are rooted, planar binary trees with leaves labeled 0, ..., n-1.
    Count = Catalan(n-1) * n! / n! ... actually for LABELED leaves:

    The number of labeled rooted binary trees with n leaves is
    (2n-3)!! = 1 * 3 * 5 * ... * (2n-3) for n >= 2.

    But for PLANTED PLANAR binary trees with labeled leaves in a FIXED ORDER
    (left-to-right = 0, 1, ..., n-1), the count is Catalan(n-1).

    We enumerate ALL planar trees (different parenthesizations),
    corresponding to different graph topologies.

    Returns list of PlantedTree objects.
    """
    if n < 1:
        return []
    structures = _gen_binary_trees(n)
    result = []
    for i, s in enumerate(structures):
        result.append(PlantedTree(
            structure=s,
            n_leaves=n,
            label=f"T_{n}_{i}",
        ))
    return result


def _gen_binary_trees(n: int) -> List:
    """Generate all planar binary trees with n leaves (0-indexed).

    Leaves are in left-to-right order 0, 1, ..., n-1.
    Count = Catalan(n-1).
    """
    if n == 1:
        return [0]
    results = []
    for k in range(1, n):
        # Left subtree has leaves 0, ..., k-1
        # Right subtree has leaves k, ..., n-1
        for lt in _gen_binary_trees(k):
            for rt in _gen_binary_trees(n - k):
                shifted_rt = _shift_tree(rt, k)
                results.append((lt, shifted_rt))
    return results


def catalan(n: int) -> int:
    """Catalan number C_n = binom(2n, n) / (n+1)."""
    if n < 0:
        return 0
    return comb(2 * n, n) // (n + 1)


def planted_trees_count(n: int) -> int:
    """Number of planar binary trees with n labeled leaves = Catalan(n-1)."""
    return catalan(n - 1)


