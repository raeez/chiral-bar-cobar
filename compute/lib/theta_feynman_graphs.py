def _frac(x) -> Fraction:
    """Coerce to Fraction."""
    if isinstance(x, Fraction):
        return x
    if isinstance(x, (int,)):
        return Fraction(x)
    if isinstance(x, float):
        return Fraction(x).limit_denominator(10 ** 15)
    return Fraction(x)


