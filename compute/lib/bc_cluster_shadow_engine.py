# 10.  Koszul duality as cluster transformation
# ============================================================================

def koszul_dual_shadow_tower(max_r: int = 15) -> Dict[int, Any]:
    r"""Shadow tower of Vir_{26-c} (the Koszul dual of Vir_c).

    S_r^!(c) = S_r(26 - c).
    """
    tower = virasoro_shadow_tower(max_r)
    dual = {}
    for r, expr in tower.items():
        dual[r] = cancel(expr.subs(c, 26 - c))
    return dual


def koszul_duality_mutation_test(c_val: float, max_r: int = 10) -> Dict[str, Any]:
    r"""Test whether cluster mutations map S_r(c) to S_r(26-c).

    Evaluates both towers numerically and checks the relationship.

    The key question: is Koszul duality c -> 26-c realised as a sequence
    of cluster mutations on the shadow exchange data?

    MATHEMATICAL RESULT: This is NOT a simple mutation sequence, because
    Koszul duality changes ALL shadow coefficients simultaneously (not
    one at a time as mutations do).  However, the COMPLEMENTARITY formula
    S_r(c) + S_r(26-c) has a specific structure (Theorem C) which
    constrains the relationship.

    Returns comparison data.
    """
    tower = virasoro_shadow_numerical(c_val, max_r)
    dual_tower = virasoro_shadow_numerical(26 - c_val, max_r)

    comparisons = {}
    for r in range(2, max_r + 1):
        s_r = tower[r]
        s_r_dual = dual_tower[r]
        complement_sum = s_r + s_r_dual

        comparisons[r] = {
            'S_r': s_r,
            'S_r_dual': s_r_dual,
            'sum': complement_sum,
            'ratio': s_r_dual / s_r if abs(s_r) > 1e-30 else None,
        }

    # Check complementarity at arity 2: kappa + kappa' = c/2 + (26-c)/2 = 13
    kappa_sum = tower[2] + dual_tower[2]

    return {
        'c': c_val,
        'kappa_sum': kappa_sum,
        'kappa_sum_expected': 13.0,
        'kappa_complementarity': abs(kappa_sum - 13.0) < 1e-10,
        'comparisons': comparisons,
        'is_simple_mutation': False,  # See docstring
    }


