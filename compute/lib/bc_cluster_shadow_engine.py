def exchange_relation_check(c_val: float, r: int) -> Dict[str, Any]:
    r"""Verify the exchange relation at arity r for Virasoro at central charge c_val.

    The MC equation at arity r is the FULL sum set to zero:

        sum_{j+k=r+2, 2<=j<=k} eps(j,k) * j * k * S_j * S_k = 0

    This includes the j=2, k=r term which provides the linear flow
    (Hessian operator): 2*r*S_2*S_r = r*c*S_r.  The remaining terms
    (j >= 3) are the nonlinear obstruction.  The MC equation says these
    cancel exactly.

    Equivalently, the recursion formula defining S_r for r >= 5:
        S_r = -(1/(r*c)) * sum_{3<=j<=k, j+k=r+2} eps(j,k)*j*k*S_j*S_k

    We verify by checking the full sum vanishes.

    Returns dict with full_sum and deviation.
    """
    tower = virasoro_shadow_numerical(c_val, max(r, 15))

    # Full MC sum: sum_{j+k=r+2, 2<=j<=k} eps(j,k) * j*k * S_j * S_k
    full_sum = 0.0
    for j in range(2, r + 1):
        kk = r + 2 - j
        if kk < 2 or j > kk:
            continue
        if j not in tower or kk not in tower:
            continue
        contrib = j * kk * tower[j] * tower[kk]
        if j == kk:
            contrib /= 2.0
        full_sum += contrib

    # Also compute the nonlinear part (j >= 3) separately
    nonlinear = 0.0
    for j in range(3, r + 1):
        kk = r + 2 - j
        if kk < 2 or j > kk:
            continue
        if j not in tower or kk not in tower:
            continue
        contrib = j * kk * tower[j] * tower[kk]
        if j == kk:
            contrib /= 2.0
        nonlinear += contrib

    linear_term = 2 * r * tower[2] * tower[r]  # = r*c*S_r

    return {
        'r': r,
        'c': c_val,
        'full_sum': full_sum,
        'linear_term': linear_term,
        'nonlinear_part': nonlinear,
        'deviation': abs(full_sum),
        'consistent': abs(full_sum) < 1e-10 * max(abs(linear_term), 1e-30),
    }


