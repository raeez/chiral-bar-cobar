# 5.  Laurent phenomenon
# ============================================================================

def shadow_as_rational_function(max_r: int = 15) -> Dict[int, Dict[str, Any]]:
    r"""Express S_r as a rational function of kappa = S_2 = c/2 and check Laurent property.

    For the single-line Virasoro tower:
        S_r(c) = rational function of c
        Substituting c = 2*kappa: S_r(kappa) = rational in kappa

    Laurent means: S_r is a polynomial in kappa and kappa^{-1}
    (equivalently, the denominator is a power of kappa = c/2,
    possibly times polynomial factors).

    Returns dict {r: {expression, num_degree, den_degree, is_laurent_in_kappa, ...}}
    """
    tower = virasoro_shadow_tower(max_r)
    kappa = Symbol('kappa')
    results = {}

    for r, expr in tower.items():
        # Substitute c = 2*kappa
        expr_k = cancel(expr.subs(c, 2 * kappa))

        # Get numerator and denominator
        num = numer(expr_k)
        den = denom(expr_k)

        # Check if denominator is a power of kappa times constants
        # (i.e., Laurent in kappa)
        den_expanded = expand(den)
        num_expanded = expand(num)

        # For Virasoro: S_r has poles in c only at c=0 and 5c+22=0.
        # In kappa: poles at kappa=0 and 10*kappa+22=0 (i.e., kappa=-11/5).
        # Laurent in kappa means den = kappa^a for some a.
        # This fails at r >= 4 because of the (5c+22) factor.
        # So S_r is NOT Laurent in kappa alone for r >= 4.

        # Check if Laurent in BOTH kappa and (5c+22) = (10*kappa+22)
        # This is a more refined check.

        try:
            den_poly = Poly(den_expanded, kappa)
            num_poly = Poly(num_expanded, kappa)
            den_deg = den_poly.total_degree()
            num_deg = num_poly.total_degree()
        except Exception:
            den_deg = -1
            num_deg = -1

        # Check: is den a monomial in kappa?
        # (i.e., kappa^a * constant)
        is_monomial_den = False
        try:
            p = Poly(den_expanded, kappa)
            # A monomial has exactly one term
            is_monomial_den = len(p.as_dict()) == 1
        except Exception:
            pass

        results[r] = {
            'expression': expr_k,
            'num_degree': num_deg,
            'den_degree': den_deg,
            'is_laurent_in_kappa': is_monomial_den,
            'has_extra_poles': not is_monomial_den and r >= 4,
        }

    return results


def shadow_laurent_in_initial_data(max_r: int = 10) -> Dict[int, Dict[str, Any]]:
    r"""Check if S_r is Laurent in the initial cluster (S_2, S_3).

    For Virasoro, the initial data is S_2 = c/2, S_3 = 2.
    Substitute c = 2*S_2, then S_4 = 10/(2*S_2*(5*2*S_2 + 22))
                                    = 10/(2*S_2*(10*S_2 + 22))
                                    = 5/(S_2*(10*S_2 + 22))

    Since S_3 = 2 is a constant for Virasoro, the Laurent phenomenon
    reduces to S_r being Laurent in S_2.

    For general algebras with variable S_3, we'd need both S_2 and S_3.
    """
    s2 = Symbol('S2', positive=True)
    tower_c = virasoro_shadow_tower(max_r)
    results = {}

    for r, expr in tower_c.items():
        expr_s2 = cancel(expr.subs(c, 2 * s2))
        num_e = numer(expr_s2)
        den_e = denom(expr_s2)

        try:
            den_p = Poly(expand(den_e), s2)
            is_monomial = len(den_p.as_dict()) == 1
            den_deg = den_p.total_degree()
        except Exception:
            is_monomial = False
            den_deg = -1

        results[r] = {
            'expression_in_S2': expr_s2,
            'is_laurent_in_S2': is_monomial,
            'den_degree_S2': den_deg,
        }

    return results


