def p_adic_valuation(n: int, p: int) -> int:
    """Compute the p-adic valuation v_p(n) = max power of p dividing n.

    For n = 0 returns a large sentinel value.
    """
    if n == 0:
        return 999  # sentinel for zero
    if abs(n) == 1:
        return 0
    v = 0
    n = abs(n)
    while n % p == 0:
        n //= p
        v += 1
    return v


def rational_p_adic_valuation(q: Fraction, p: int) -> int:
    """p-adic valuation of a rational number: v_p(a/b) = v_p(a) - v_p(b)."""
    if q == 0:
        return 999
    return p_adic_valuation(q.numerator, p) - p_adic_valuation(q.denominator, p)


def tropical_shadow_vector(c_val: int, p: int, max_r: int = 10) -> List[int]:
    r"""Tropical shadow spectrum: trop(S_r) = -v_p(S_r) for each arity.

    The tropicalisation of the cluster variety is the set of integer
    vectors obtained by applying -v_p to the cluster variables.

    Parameters:
        c_val: integer central charge value
        p: prime for p-adic valuation
        max_r: maximum arity

    Returns list of -v_p(S_r) for r = 2, ..., max_r.
    """
    tower = virasoro_shadow_tower(max_r)
    result = []
    for r in range(2, max_r + 1):
        val = tower[r].subs(c, c_val)
        # Convert to Fraction for exact p-adic valuation
        val_frac = Fraction(val)
        if val_frac == 0:
            result.append(0)  # convention: trop(0) = -infinity, but we use 0
        else:
            result.append(-rational_p_adic_valuation(val_frac, p))
    return result


def tropical_shadow_spectrum(p: int, c_range: range = range(1, 26),
                              max_r: int = 10) -> Dict[int, List[int]]:
    r"""Tropical shadow spectrum for Virasoro at c = 1..25, prime p.

    Returns dict {c: trop_vector} where trop_vector[i] = -v_p(S_{i+2}).
    """
    spectrum = {}
    for c_val in c_range:
        spectrum[c_val] = tropical_shadow_vector(c_val, p, max_r)
    return spectrum


