# 12.  Cluster character (Caldero-Chapoton map)
# ============================================================================

def caldero_chapoton_rank1(dim_ext: int) -> int:
    r"""Caldero-Chapoton map for rank-1 cluster category.

    For the cluster category of A_1, there is one indecomposable object
    (the simple representation S) with CC(S) = x_1 + 1/x_1.

    For rank-1, the CC map gives the classical cluster variable.
    """
    # CC(M) = x^{g(M)} * sum_{e} chi(Gr_e(M)) * y^e
    # For M = S (simple): dim S = 1, Gr_0(S) = {0} (one point), Gr_1(S) = {S} (one point)
    # CC(S) = x^{-1} * (1 + y) = x^{-1} + x^{-1}*y
    # With principal coefficients: CC(S) = (1 + y_1)/x_1
    return 1 + dim_ext  # Euler characteristic of Grassmannian


def cluster_character_rank2(n: int) -> Dict[str, Any]:
    r"""Cluster character for rank-2 cluster category of type A_2.

    The indecomposable objects of the cluster category C_{A_2} are:
    - S_1, S_2 (the two simples)
    - P_1 = S_1 -> S_2 (projective, = injective envelope of S_2)

    The CC map gives:
    - CC(S_1) = (1 + y_1) / x_1
    - CC(S_2) = (1 + y_2) / x_2
    - CC(P_1) = (x_1 + y_1*x_2 + y_1*y_2) / (x_1 * x_2)

    For the shadow tower analogy:
    - x_1 = S_2 (kappa), x_2 = S_3 (cubic)
    - The cluster variable CC(P_1) should correspond to S_4

    Returns the cluster character data.
    """
    y1, y2, x1, x2 = symbols('y1 y2 x1 x2', positive=True)

    cc_s1 = (1 + y1) / x1
    cc_s2 = (1 + y2) / x2
    cc_p1 = (x1 + y1 * x2 + y1 * y2) / (x1 * x2) if n >= 3 else None

    # For the shadow interpretation at depth n:
    # The indecomposable objects of C_{Q(A)} index the cluster variables
    # S_2, S_3, ..., S_{n+1}

    return {
        'type': f'A_{n}' if n <= 3 else f'infinite (n={n})',
        'n_indecomposables': n + (n - 1) if n <= 3 else None,
        'CC_S1': cc_s1,
        'CC_S2': cc_s2,
        'CC_P1_exists': n >= 3,
    }


