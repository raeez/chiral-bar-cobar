# 9.  Shadow quiver
# ============================================================================

def shadow_quiver(max_arity: int = 8) -> Dict[str, Any]:
    r"""Construct the shadow quiver Q(A) from the exchange matrix.

    Nodes = arities {2, 3, ..., max_arity}.
    Arrows: B_{ij} > 0 gives an arrow i -> j with multiplicity |B_{ij}|.

    For the Virasoro MC recursion:
    - Arities i, j are connected if they couple in some recursion equation.
    - The coupling i + j = r + 2 means arities i and j participate in
      determining arity r = i + j - 2.

    Returns dict with nodes, arrows, and quiver classification.
    """
    B = shadow_exchange_matrix_exact(max_arity)
    nodes = list(range(2, max_arity + 1))

    arrows = []
    for i in nodes:
        for j in nodes:
            if i < j and B.get((i, j), Rational(0)) > 0:
                arrows.append({
                    'from': i,
                    'to': j,
                    'weight': int(B[(i, j)]),
                })

    # Classification: check if mutation-equivalent to a Dynkin diagram
    # For small quivers this is decidable
    n_nodes = len(nodes)
    n_arrows = len(arrows)

    # Simple classification based on structure
    quiver_type = 'unknown'
    if n_nodes == 0:
        quiver_type = 'empty'
    elif n_nodes == 1:
        quiver_type = 'A_1 (trivial, class G)'
    elif n_nodes == 2:
        if n_arrows == 1:
            w = arrows[0]['weight'] if arrows else 0
            if w <= 1:
                quiver_type = 'A_2 (finite, class L)'
            elif w == 2:
                quiver_type = 'B_2 (finite, class C-like)'
            elif w == 3:
                quiver_type = 'G_2 (finite)'
            else:
                quiver_type = f'infinite type (weight {w})'
        elif n_arrows == 0:
            quiver_type = 'A_1 x A_1 (disconnected)'
    elif n_nodes >= 3:
        # For the shadow quiver with standard MC coupling,
        # the weights are i*j which grow fast -> infinite type
        max_weight = max((a['weight'] for a in arrows), default=0)
        if max_weight <= 1:
            quiver_type = f'possibly finite (max weight 1, {n_nodes} nodes)'
        else:
            quiver_type = f'infinite type (max weight {max_weight}, {n_nodes} nodes, class M)'

    return {
        'nodes': nodes,
        'arrows': arrows,
        'n_nodes': n_nodes,
        'n_arrows': n_arrows,
        'quiver_type': quiver_type,
        'exchange_matrix_sample': {(i, j): B.get((i, j), Rational(0))
                                    for i in nodes[:4] for j in nodes[:4] if i < j},
    }


