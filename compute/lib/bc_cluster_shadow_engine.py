def virasoro_shadow_tower(max_r: int = 15) -> Dict[int, Any]:
    r"""Virasoro shadow coefficients S_r(c) via the MC master equation.

    The recursion:
        S_r = -(1/(r*c)) * sum_{j+k=r+2, 2<=j<=k} eps(j,k) * j*k * S_j * S_k

    where eps(j,k) = 1 if j < k, 1/2 if j = k.

    Returns dict mapping arity r -> S_r(c) as a cancelled sympy expression.

    Reference: ds_shadow_higher_arity.py, shadow_tower_atlas.py.
    """
    tower: Dict[int, Any] = {}
    tower[2] = c / 2
    tower[3] = Rational(2)
    tower[4] = Rational(10) / (c * (5 * c + 22))

    for r in range(5, max_r + 1):
        total = Rational(0)
        for j in range(2, r + 1):
            kk = r + 2 - j
            if kk < 2 or kk > r or j > kk:
                continue
            if j not in tower or kk not in tower:
                continue
            contrib = j * kk * tower[j] * tower[kk]
            if j == kk:
                contrib = contrib / 2
            total += contrib
        tower[r] = cancel(-total / (r * c))

    return tower


def virasoro_shadow_numerical(c_val: float, max_r: int = 15) -> Dict[int, float]:
    """Evaluate the Virasoro shadow tower at a specific numerical c value.

    Uses exact rational arithmetic internally, converts to float at the end.
    """
    c_rat = Rational(c_val) if isinstance(c_val, int) else Rational(c_val).limit_denominator(10**12)
    tower_sym = virasoro_shadow_tower(max_r)
    result = {}
    for r, expr in tower_sym.items():
        result[r] = float(expr.subs(c, c_rat))
    return result


def affine_sl2_shadow_tower(max_r: int = 15) -> Dict[int, Any]:
    r"""Affine sl_2 shadow tower (class L, depth 3).

    S_2 = kappa = 3(k+2)/4, S_3 = alpha (nonzero for generic k),
    S_r = 0 for r >= 4 (vanishing quartic, Delta = 0).

    For the T-line (Sugawara Virasoro at c = 3k/(k+2)):
    this is the Virasoro tower evaluated at the DS central charge.
    But the INTRINSIC sl_2 tower terminates at depth 3.
    """
    k_sym = Symbol('k')
    tower: Dict[int, Any] = {}
    tower[2] = 3 * (k_sym + 2) / 4
    tower[3] = Rational(1)  # normalised cubic
    for r in range(4, max_r + 1):
        tower[r] = Rational(0)
    return tower


def heisenberg_shadow_tower(max_r: int = 15) -> Dict[int, Any]:
    r"""Heisenberg shadow tower (class G, depth 2).

    S_2 = kappa (the level), S_r = 0 for r >= 3.
    """
    kappa_sym = Symbol('kappa')
    tower: Dict[int, Any] = {}
    tower[2] = kappa_sym
    for r in range(3, max_r + 1):
        tower[r] = Rational(0)
    return tower


