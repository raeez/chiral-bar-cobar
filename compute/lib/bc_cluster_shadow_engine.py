# 7.  Cluster modular group (rank-2 analysis)
# ============================================================================

def rank2_cluster_variable_orbit(b: int, max_mutations: int = 100) -> Dict[str, Any]:
    r"""Compute the orbit of rank-2 cluster variables under mutations.

    For a rank-2 cluster algebra with exchange matrix B = [[0, b], [-b, 0]],
    the cluster variables are generated by the exchange relations:
        x_k * x_k' = x_i^{|B_{ki}|} + x_j^{|B_{kj}|}

    The orbit of cluster variables is FINITE iff b ∈ {0, 1, 2, 3}
    (corresponding to types A_1xA_1, A_2, B_2, G_2).
    For b >= 4, the orbit is INFINITE.

    For rank-2, the mutation at node 0 replaces x_0 by:
        x_0' = (x_1^b + 1) / x_0  (if B_{01} = b > 0)

    Starting from (x_0, x_1) and alternating mutations:
        mu_0: x_0 -> (x_1^b + 1)/x_0
        mu_1: x_1 -> (x_0^b + 1)/x_1

    Parameters:
        b: the exchange parameter (positive integer)
        max_mutations: cutoff for infinite orbits

    Returns dict with orbit data.
    """
    # Track cluster variables as symbolic rational expressions
    # For efficiency, just track the sequence of exchanges and check for repeats
    # Use the recursive formula to generate cluster variables as functions of
    # the initial pair (a, b) -> sequence

    # For rank 2 with integer b, the finiteness is determined by b:
    # b = 0: trivial (A_1 x A_1), 2 variables per node = 4 total
    # b = 1: A_2, 5 cluster variables
    # b = 2: B_2, 6 cluster variables
    # b = 3: G_2, 8 cluster variables
    # b >= 4: infinite

    if b <= 0:
        return {
            'is_finite': True,
            'orbit_size': 4,
            'cluster_type': 'A_1 x A_1',
            'b': b,
        }

    # Generate cluster variable sequence using the recursive relation
    # x_{n+1} = (x_n^b + 1) / x_{n-1} (alternating which variable mutates)
    # Start with x_0 = a, x_1 = c (symbolic), track as rational functions

    # For finiteness classification, it suffices to use the known result:
    is_fin = b <= 3
    if b == 1:
        ctype, n_vars = 'A_2', 5
    elif b == 2:
        ctype, n_vars = 'B_2', 6
    elif b == 3:
        ctype, n_vars = 'G_2', 8
    else:
        ctype, n_vars = f'infinite (b={b})', None

    # For infinite type, verify numerically by checking growth of variable sequence
    if not is_fin:
        # The cluster variables grow exponentially; check sequence diverges
        # Use numerical initial values
        x = [2.0, 3.0]  # arbitrary initial cluster
        for step in range(max_mutations):
            if step % 2 == 0:
                x_new = (x[-1] ** b + 1.0) / x[-2]
            else:
                x_new = (x[-2] ** b + 1.0) / x[-1]
            x.append(x_new)
            # If variables grow beyond threshold, orbit is infinite
            if abs(x_new) > 1e15:
                return {
                    'is_finite': False,
                    'orbit_size': None,
                    'cluster_type': ctype,
                    'b': b,
                    'growth_detected_at_step': step,
                    'n_variables_computed': len(x),
                }

        # Check if sequence returned to initial values
        for i in range(2, len(x) - 1):
            if (abs(x[i] - x[0]) < 1e-10 and abs(x[i + 1] - x[1]) < 1e-10):
                return {
                    'is_finite': True,
                    'orbit_size': i,
                    'cluster_type': ctype,
                    'b': b,
                }

        return {
            'is_finite': False,
            'orbit_size': None,
            'cluster_type': ctype,
            'b': b,
            'n_variables_computed': len(x),
        }

    return {
        'is_finite': True,
        'orbit_size': n_vars,
        'cluster_type': ctype,
        'b': b,
    }


