# 14.  Complementarity sum structure
# ============================================================================

def complementarity_sum_tower(max_r: int = 10) -> Dict[int, Any]:
    r"""Compute S_r(c) + S_r(26-c) for each arity (Theorem C shadow complement).

    The complementarity theorem (Theorem C) constrains these sums.

    At arity 2: kappa(c) + kappa(26-c) = c/2 + (26-c)/2 = 13.
    At arity 3: S_3(c) + S_3(26-c) = 2 + 2 = 4 (both constant).
    At higher arities: rational functions of c.
    """
    tower = virasoro_shadow_tower(max_r)
    results = {}

    for r in range(2, max_r + 1):
        s_r = tower[r]
        s_r_dual = cancel(s_r.subs(c, 26 - c))
        complement = cancel(s_r + s_r_dual)
        results[r] = {
            'S_r': s_r,
            'S_r_dual': s_r_dual,
            'complement_sum': complement,
            'complement_factored': factor(complement),
        }

    return results


