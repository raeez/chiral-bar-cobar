r"""Cluster algebra mutations from shadow obstruction tower coefficients.

Mathematical foundation
-----------------------
The shadow obstruction tower has a recursive structure: each S_{r+1} is
determined by S_2, ..., S_r via the MC equation.  This recursion has the
same shape as cluster exchange relations: new variables produced from old
by polynomial relations.

The MC recursion at arity r (from virasoro_shadow_tower.py,
ds_shadow_higher_arity.py, shadow_tower_atlas.py) is:

    S_r = -(1/(r*c)) * sum_{j+k=r+2, j,k>=2} eps(j,k) * j*k * S_j * S_k

where eps(j,k) = 1 if j < k, 1/2 if j = k, and the sum runs over
decompositions j + k = r + 2 with j, k >= 2.  The j = 2, k = r term
provides the "linear flow" (the nabla_H operator); all other terms
are the nonlinear obstruction.

CLUSTER INTERPRETATION:

1. SHADOW EXCHANGE MATRIX B_{ij}: The coefficient of S_i * S_j in the
   arity-(i+j-2) MC recursion equation, after extracting the linear flow.
   This is an integer-valued skew-symmetrisable matrix when suitably
   normalised.

2. MUTATION SEQUENCE: Starting from initial cluster x = (S_2, S_3, S_4, ...),
   the MC recursion defines exchange relations.  We test whether mutating
   at node k reproduces the dual shadow S_r(26-c) (Koszul duality).

3. LAURENT PHENOMENON: Each S_r is expressed as a rational function of the
   initial data (kappa = S_2, alpha = S_3).  We test whether this is
   Laurent (polynomial in kappa, kappa^{-1}, alpha, alpha^{-1}).

4. TROPICAL SHADOW: The tropicalisation trop(S_r) = -v_p(S_r) for p-adic
   valuations v_p gives integer vectors forming a tropical variety.

5. CLUSTER MODULAR GROUP: The group generated by mutations acts on shadow
   data.  For rank-2 (S_2, S_3 seed), the orbit structure encodes the
   Coxeter group type.

6. F-POLYNOMIALS AND g-VECTORS: Standard cluster algebra invariants
   extracted from the Laurent expansion of shadow variables.

7. SHADOW QUIVER: The directed graph encoding B_{ij}, with nodes = arities
   and arrows from the sign pattern of B.

8. CLUSTER CHARACTER (Caldero-Chapoton): For the cluster category of Q(A),
   the CC map should produce S_r from indecomposable objects.

Verification paths (>= 3 per claim)
------------------------------------
Path 1: Laurent phenomenon — S_r is Laurent in initial variables
Path 2: F-polynomial positivity — all coefficients positive
Path 3: Mutation involution — mu_k^2 = id
Path 4: Exchange relation consistency — both sides agree
Path 5: Quiver mutation — B-matrix transforms correctly under mu_k
Path 6: Koszul duality — mutation at c maps S_r(c) toward S_r(26-c)
Path 7: Depth class determines quiver type — G/L/C/M classification
Path 8: Numerical cross-check — evaluate at specific c values

Conventions
-----------
- Shadow coefficients S_r follow the monograph convention:
  S_2 = kappa = c/2 for Virasoro
  S_3 = alpha = 2 for Virasoro
  S_4 = Q0 = 10/(c(5c+22)) for Virasoro
- All exact arithmetic via sympy Rational where possible, mpmath for
  numerical evaluations.
- Cohomological grading (|d| = +1).

References
----------
    Fomin-Zelevinsky, "Cluster algebras I: Foundations", JAMS 2002.
    Gross-Hacking-Keel-Kontsevich, "Canonical bases for cluster algebras",
        JAMS 2018.
    thm:obstruction-recursion (higher_genus_modular_koszul.tex)
    thm:shadow-archetype-classification (higher_genus_modular_koszul.tex)
    thm:single-line-dichotomy (higher_genus_modular_koszul.tex)
    cor:virasoro-quintic-shadow-explicit (w_algebras.tex)
    virasoro_shadow_tower.py, quintic_shadow_engine.py, shadow_tower_atlas.py
"""

