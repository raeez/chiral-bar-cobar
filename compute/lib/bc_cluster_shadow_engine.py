# 8.  F-polynomials and g-vectors
# ============================================================================

def compute_f_polynomials(max_r: int = 15) -> Dict[int, Dict[str, Any]]:
    r"""Compute F-polynomials for shadow cluster variables.

    The F-polynomial F_r(y_1, y_2, ...) is defined via the separation formula:
        S_r = (F_r(y-hat) / prod x_i^{g_r^{(i)}})  (Fomin-Zelevinsky)

    For the shadow tower, we use the Laurent expansion of S_r in terms
    of the initial cluster variables x_1 = S_2, x_2 = S_3.

    For Virasoro: S_2 = c/2, S_3 = 2 (constant).
    In terms of u = c: S_r(u) is a rational function.

    The F-polynomial is the numerator of S_r expressed in the cluster
    variables, with the coefficient vector g_r being the exponent of
    the initial cluster monomial.

    Since S_3 = 2 is constant for Virasoro, the F-polynomial is
    effectively univariate.

    Returns dict {r: {F_poly, g_vector, positive_coefficients}}.
    """
    tower = virasoro_shadow_tower(max_r)
    y = Symbol('y', positive=True)
    results = {}

    for r, expr in tower.items():
        # Express in terms of c
        expr_simplified = cancel(expr)
        num_expr = numer(expr_simplified)
        den_expr = denom(expr_simplified)

        # The g-vector records the power of c in the denominator
        try:
            den_poly = Poly(expand(den_expr), c)
            # Find the minimal power of c in denominator
            den_terms = den_poly.as_dict()
            if den_terms:
                # g-vector component = degree of c in denominator
                g_c = max(k[0] for k in den_terms.keys())
            else:
                g_c = 0
        except Exception:
            g_c = 0

        # F-polynomial: multiply S_r by c^{g_c} to clear the c-poles
        # then the result should have positive coefficients
        F_candidate = cancel(expr * c ** g_c)
        F_num = numer(F_candidate)

        # Check positivity of coefficients
        try:
            F_poly = Poly(expand(F_num), c)
            coeffs = [float(v) for v in F_poly.all_coeffs()]
            all_positive = all(v >= -1e-15 for v in coeffs)
            all_nonneg = all(v > -1e-15 for v in coeffs)
        except Exception:
            coeffs = []
            all_positive = False
            all_nonneg = False

        results[r] = {
            'g_vector': (g_c,),
            'F_numerator': F_num,
            'F_coefficients': coeffs,
            'positive_coefficients': all_positive,
            'nonneg_coefficients': all_nonneg,
        }

    return results


def g_vectors(max_r: int = 15) -> Dict[int, Tuple[int, ...]]:
    r"""Extract g-vectors for shadow cluster variables.

    The g-vector g_r = (g_r^{(1)}, g_r^{(2)}) records the exponent of
    the initial cluster monomial in the Laurent expansion of S_r.

    For the Virasoro single-line tower with a single variable c:
    g_r = (power of c in the denominator of S_r,).
    """
    tower = virasoro_shadow_tower(max_r)
    vectors = {}

    for r, expr in tower.items():
        expr_c = cancel(expr)
        den_e = expand(denom(expr_c))
        try:
            p = Poly(den_e, c)
            # Get the total c-degree of denominator
            g_c = p.total_degree()
        except Exception:
            g_c = 0
        vectors[r] = (g_c,)

    return vectors


