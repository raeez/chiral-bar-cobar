def mc_recursion_coefficient(i: int, j: int, r: int) -> Rational:
    r"""Coefficient of S_i * S_j in the MC equation at arity r.

    The MC equation at arity r reads:
        r * c * S_r + sum_{j+k=r+2, 2<=j<=k} eps(j,k) * j*k * S_j * S_k = 0

    So the coefficient of S_i * S_j (with i < j and i + j = r + 2) is:
        B^raw_{ij} = i * j

    With the symmetry factor: 1 for i < j, 1/2 for i = j.
    The exchange matrix is extracted from these after removing the linear
    (Hessian flow) term.
    """
    if i + j != r + 2:
        return Rational(0)
    if i < 2 or j < 2:
        return Rational(0)
    if i > j:
        i, j = j, i
    coeff = Rational(i * j)
    if i == j:
        coeff = coeff / 2
    return coeff


def shadow_exchange_matrix(max_arity: int = 10) -> np.ndarray:
    r"""Construct the shadow exchange matrix B_{ij} for arities 2..max_arity.

    B_{ij} encodes the coupling between shadow variables S_i and S_j
    in the MC recursion.  Specifically, B_{ij} is the coefficient of
    S_i * S_j in the recursion for S_{i+j-2} (after normalisation).

    The matrix is antisymmetrised: B_{ij} = -B_{ji} for the cluster
    interpretation (skew-symmetrisable exchange matrix).

    The raw MC recursion gives:
        S_r involves S_i * S_j with i + j = r + 2.
        Coefficient of S_i * S_j (i < j) is i * j / (r * c).

    For the exchange matrix, we extract the RELATIVE coupling between
    arities, normalised by the diagonal (Hessian flow) term.

    The linear term (j=2 contribution) is extracted as the "frozen"
    variable; the remaining quadratic terms define B.

    Returns:
        numpy array of shape (n, n) where n = max_arity - 1
        (rows/cols indexed by arities 2, 3, ..., max_arity).
    """
    n = max_arity - 1  # arities 2..max_arity
    B = np.zeros((n, n), dtype=float)

    for r in range(4, 2 * max_arity + 1):
        # S_r involves products S_i * S_j with i + j = r + 2
        for i in range(2, max_arity + 1):
            j = r + 2 - i
            if j < 2 or j > max_arity or i == j:
                continue
            # Index into matrix
            ii = i - 2
            jj = j - 2
            # Coefficient is proportional to i*j
            # Sign convention: S_r = -(1/(rc)) * sum ...
            # i < j contributes with + sign in the B-matrix
            if i < j:
                B[ii, jj] += float(i * j)
                B[jj, ii] -= float(i * j)

    return B


def shadow_exchange_matrix_exact(max_arity: int = 10) -> Dict[Tuple[int, int], Rational]:
    r"""Exact (rational) shadow exchange matrix B_{ij}.

    Returns dict mapping (i, j) -> B_{ij} for arities 2 <= i, j <= max_arity.
    Skew-symmetric: B_{ij} = -B_{ji}.
    """
    B: Dict[Tuple[int, int], Rational] = {}
    for i in range(2, max_arity + 1):
        for j in range(2, max_arity + 1):
            if i == j:
                B[(i, j)] = Rational(0)
            elif i < j:
                # i and j couple in the recursion for arity r = i + j - 2
                B[(i, j)] = Rational(i * j)
                B[(j, i)] = Rational(-i * j)
            # j < i already handled by skew symmetry
    return B


