# 3.  Cluster mutations
# ============================================================================

def cluster_mutation(B: np.ndarray, k: int) -> np.ndarray:
    r"""Perform cluster mutation mu_k on exchange matrix B.

    Standard Fomin-Zelevinsky mutation at node k (0-indexed):
        B'_{ij} = -B_{ij}                    if i = k or j = k
        B'_{ij} = B_{ij} + sgn(B_{ik}) * max(B_{ik}*B_{kj}, 0)  otherwise

    where sgn(x) = +1 if x > 0, -1 if x < 0, 0 if x = 0,
    and the formula uses:
        B'_{ij} = B_{ij} + (|B_{ik}| * B_{kj} + B_{ik} * |B_{kj}|) / 2

    Returns new B' matrix.
    """
    n = B.shape[0]
    Bp = np.copy(B)

    for i in range(n):
        for j in range(n):
            if i == k or j == k:
                Bp[i, j] = -B[i, j]
            else:
                Bp[i, j] = B[i, j] + (abs(B[i, k]) * B[k, j] +
                                        B[i, k] * abs(B[k, j])) / 2.0

    return Bp


def mutation_sequence(B: np.ndarray, seq: List[int]) -> np.ndarray:
    """Apply a sequence of mutations to B.

    seq is a list of 0-indexed node indices.
    """
    result = np.copy(B)
    for k in seq:
        result = cluster_mutation(result, k)
    return result


def mutation_involution_check(B: np.ndarray, k: int) -> Tuple[bool, float]:
    r"""Check that mu_k^2 = id: mutating twice at k recovers original B.

    Returns (is_involution, max_deviation).
    """
    Bp = cluster_mutation(B, k)
    Bpp = cluster_mutation(Bp, k)
    dev = np.max(np.abs(Bpp - B))
    return bool(dev < 1e-12), float(dev)


