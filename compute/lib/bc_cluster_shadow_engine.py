# 13.  Master comparison: all verification paths
# ============================================================================

def full_cluster_shadow_analysis(c_val: float = 10.0, max_r: int = 12,
                                  max_arity_quiver: int = 8) -> Dict[str, Any]:
    r"""Run all cluster shadow analyses at a given central charge.

    Executes all verification paths and returns a comprehensive report.
    """
    # 1. Shadow tower
    tower = virasoro_shadow_numerical(c_val, max_r)

    # 2. Exchange matrix
    B = shadow_exchange_matrix(max_arity_quiver)

    # 3. Mutation involution check
    involution_checks = {}
    n = B.shape[0]
    for k in range(min(n, 6)):
        ok, dev = mutation_involution_check(B, k)
        involution_checks[k] = {'involution': ok, 'deviation': dev}

    # 4. Exchange relation consistency
    exchange_checks = {}
    for r in range(5, min(max_r + 1, 13)):
        exchange_checks[r] = exchange_relation_check(c_val, r)

    # 5. Laurent phenomenon
    laurent = shadow_as_rational_function(min(max_r, 10))

    # 6. Koszul duality
    koszul = koszul_duality_mutation_test(c_val, min(max_r, 10))

    # 7. Quiver
    quiver = shadow_quiver(max_arity_quiver)

    # 8. Depth class
    depth = depth_class_from_quiver('virasoro')

    # 9. F-polynomials
    f_polys = compute_f_polynomials(min(max_r, 10))

    # 10. g-vectors
    gvecs = g_vectors(min(max_r, 10))

    return {
        'c': c_val,
        'tower': tower,
        'exchange_matrix_shape': B.shape,
        'involution_checks': involution_checks,
        'exchange_relation_checks': exchange_checks,
        'laurent_data': {r: d['is_laurent_in_kappa'] for r, d in laurent.items()},
        'koszul_duality': {
            'kappa_complementarity': koszul['kappa_complementarity'],
            'is_simple_mutation': koszul['is_simple_mutation'],
        },
        'quiver_type': quiver['quiver_type'],
        'depth_class': depth,
        'f_polynomial_positivity': {r: d['positive_coefficients'] for r, d in f_polys.items()},
        'g_vectors': gvecs,
    }


