# 15. Summary: physical S-duality versus companion diagnosis
# =========================================================================

def diagnose_s_duality_companion_relation() -> Dict[str, Any]:
    r"""Comprehensive diagnosis of the S-duality versus companion question.

    FINDING: physical S-duality and the FF/Verdier companion are different
    operations at finite generic coupling.

    STRUCTURE:
    - S-duality: boundary slot changes and Psi -> 1/Psi.
    - FF/Verdier companion: boundary slot fixed and k -> -k - 2h^v.
    - Free-field chiral Koszul duality: beta-gamma <-> bc at the boundary
      limit; here it agrees with the physical S-dual target.

    For affine gl_N:
      Neumann(Psi) level: k = Psi - N
      physical S-dual level: Dirichlet(1/Psi), k^S = Psi - N
      FF-dual level: k^FF = -k - 2N = -(Psi-N) - 2N = -Psi - N

      k^S = k^FF iff Psi = 0, the singular boundary of this finite-coupling
      affine model.

    ANSWER: NO, generically. But the DISCREPANCY is systematic and
    controlled by the affine kappa formula.

    For Neumann:
      kappa(A^FF) - kappa(A^S)
        = -((N^2 + 2N - 1)/N) * Psi.

    For Dirichlet:
      kappa(A^FF) - kappa(A^S)
        = -((N^2 + 2N - 1)/N) * (1/Psi).

    The Gaiotto-Rapcak S_3 triality is generated by physical S-duality
    sigma and the tau move Psi -> 1 - Psi.  The FF/Verdier same-index
    involution is recorded separately.
    """
    diagnosis = {
        'main_finding': 'physical S-duality != FF/Verdier companion (generically)',
        'coincidence_locus': 'no finite nonzero affine Neumann/Dirichlet coupling',
        'structure': 'S_3 triality = <S-duality sigma, tau>; FF/Verdier is separate',
    }

    # Verify for N = 2, 3 at several Psi values
    examples = []
    for N in [1, 2, 3]:
        for psi_num, psi_den in [(3, 1), (2, 1), (5, 2)]:
            psi = Fraction(psi_num, psi_den)
            try:
                comp = s_duality_companion_comparison(N, psi, 'neumann')
                examples.append(comp)
            except (ValueError, ZeroDivisionError):
                pass

    diagnosis['examples'] = examples

    # Count how many have the recorded companion equal to the physical S-dual.
    n_match = sum(1 for e in examples if e.get('companion_equals_s_duality'))
    n_total = len(examples)
    diagnosis['match_count'] = n_match
    diagnosis['total_count'] = n_total
    diagnosis['match_fraction'] = f"{n_match}/{n_total}"

    return diagnosis


