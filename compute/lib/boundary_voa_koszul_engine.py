# 12. Physical S-duality versus duality companion comparison
# =========================================================================

def s_duality_companion_comparison(N: int, psi: Num,
                                   boundary_type: str = 'neumann') -> Dict[str, Any]:
    r"""Compare physical S-dual and recorded duality companion.

    For Neumann: A = gl_N at level k = Psi - N.
      S-dual:    Dirichlet at coupling 1/Psi, level k^S = Psi - N.
      FF/Verdier companion:
                 gl_N at level k^FF = -k - 2N = -Psi - N.

    For Dirichlet: A = gl_N at level k = 1/Psi - N.
      S-dual:    Neumann at coupling 1/Psi, level k^S = 1/Psi - N.
      FF/Verdier companion:
                 gl_N at level k^FF = -1/Psi - N.

    For affine gl_N: kappa(k) = (N^2-1)(k+N)/(2N) + k.
    The physical S-dual keeps the affine level attached to the boundary
    condition, while the FF/Verdier companion reflects the shifted level.
    """
    psi_f = _to_frac(psi)

    if boundary_type == 'neumann':
        data = neumann_boundary_voa(N, psi_f)
    elif boundary_type == 'dirichlet':
        data = dirichlet_boundary_voa(N, psi_f)
    elif boundary_type == 'nahm_principal':
        data = nahm_principal_boundary_voa(N, psi_f)
    else:
        raise ValueError(f"Unknown boundary type: {boundary_type}")

    result = {
        'name': data.name,
        'boundary_type': boundary_type,
        'N': N,
        'Psi': psi_f,
        'kappa': data.kappa,
        'kappa_companion': data.dual_kappa,
        'kappa_s_dual': data.s_dual_kappa,
        'companion_equals_s_duality': data.companion_equals_s_duality,
        'companion_kind': data.companion_map.kind if data.companion_map else None,
        'koszul_kind': data.koszul_map.kind if data.koszul_map else None,
        'complementarity_sum': data.complementarity_sum,
        'central_charge': data.central_charge,
        'companion_central_charge': data.dual_central_charge,
        's_dual_central_charge': data.s_dual_central_charge,
        'claim_status': "computed finite boundary VOA diagnostic",
        'scope_firewall': finite_boundary_diagnostic_scope(data),
        'kernel_constants': kernel_constants(),
    }

    # Compute the discrepancy
    if data.s_dual_kappa is not None:
        result['kappa_discrepancy'] = data.dual_kappa - data.s_dual_kappa
    else:
        result['kappa_discrepancy'] = None

    return result


