Num = Union[int, Fraction, Rational]


def _to_frac(x) -> Fraction:
    """Coerce to Fraction for exact arithmetic."""
    if isinstance(x, Fraction):
        return x
    if isinstance(x, Rational):
        return Fraction(int(x.p), int(x.q))
    if isinstance(x, int):
        return Fraction(x)
    return Fraction(x)


