# Landscape summary
# ---------------------------------------------------------------------------

def full_shadow_landscape(level: Any = Fraction(1)) -> List[Dict]:
    """Compute the shadow landscape for all standard families at given level.

    Returns a list of dicts with family name, kappa, depth class, admissibility.
    """
    results = []

    # Free fields
    for h in [1, 2, 3]:
        flca = FreeLieConformal(weight=h, level=level)
        results.append({
            'family': flca.name,
            'kappa': flca.kappa(),
            'depth_class': flca.shadow_depth_class(),
            'admissible': True,
        })

    # Current algebras
    for (lt, rk, name) in [
        ('A', 1, 'sl_2'), ('A', 2, 'sl_3'),
        ('B', 2, 'so_5'), ('C', 2, 'sp_4'), ('G', 2, 'G_2'),
    ]:
        ca = CurrentAlgebra(lt, rk, level)
        results.append({
            'family': f'Cur({name})',
            'kappa': ca.kappa(),
            'depth_class': ca.shadow_depth_class(),
            'admissible': True,
        })

    # Virasoro
    for c_val in [Fraction(1), Fraction(1, 2), Fraction(26)]:
        vir = VirasoroLCA(c_val)
        results.append({
            'family': f'Vir(c={c_val})',
            'kappa': vir.kappa(),
            'depth_class': vir.shadow_depth_class(),
            'admissible': True,
        })

    return results
