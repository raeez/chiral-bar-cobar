def ds_central_charge(lie_type: str, rank: int, level: Any) -> Any:
    """Central charge of W_N = DS(V_k(sl_N)).

    c(W_N, k) = (N-1) * [1 - N*(N+1)*(k + N - 1)^2 / (k + N)]

    For type A only.  N = rank + 1.
    """
    if lie_type != 'A':
        raise ValueError(f"DS central charge only implemented for type A, got {lie_type}")
    n = rank + 1
    k = level
    if isinstance(k, (int, Fraction)):
        k = Fraction(k)
        hv = Fraction(n)
        if k + hv == 0:
            raise ValueError(f"Critical level k = -{n}")
        return Fraction(n - 1) * (
            Fraction(1) - Fraction(n * (n + 1)) * (k + n - 1) ** 2 / (k + n)
        )
    return (n - 1) * (1 - n * (n + 1) * (k + n - 1) ** 2 / (k + n))


def anomaly_ratio_wN(N: int) -> Fraction:
    """Anomaly ratio rho(sl_N) = H_N - 1 = sum_{i=1}^{N-1} 1/(i+1).

    rho(sl_2) = 1/2  [Virasoro]
    rho(sl_3) = 5/6  [W_3]
    rho(sl_4) = 13/12 [W_4]
    """
    return sum(Fraction(1, i + 1) for i in range(1, N))


def ds_kappa(lie_type: str, rank: int, level: Any) -> Any:
    """kappa of W-algebra = rho * c_W.

    kappa(W_N, k) = rho(sl_N) * c(W_N, k)
    """
    if lie_type != 'A':
        raise ValueError(f"DS kappa only for type A")
    n = rank + 1
    rho = anomaly_ratio_wN(n)
    c_w = ds_central_charge(lie_type, rank, level)
    return rho * c_w


@dataclass
class DSReduction:
    """DS reduction pipeline: Cur(g) -> W(g, f).

    For principal f: Cur(sl_N) -> W_N (principal W-algebra).
    """
    source_type: str
    source_rank: int
    source_level: Any
    nilpotent_type: str = 'principal'

    @property
    def source_N(self) -> int:
        if self.source_type == 'A':
            return self.source_rank + 1
        return self.source_rank

    def source_kappa(self) -> Any:
        """kappa of the source affine algebra."""
        ca = CurrentAlgebra(self.source_type, self.source_rank, self.source_level)
        return ca.kappa()

    def target_central_charge(self) -> Any:
        """Central charge of the target W-algebra."""
        return ds_central_charge(self.source_type, self.source_rank, self.source_level)

    def target_kappa(self) -> Any:
        """kappa of the target W-algebra."""
        return ds_kappa(self.source_type, self.source_rank, self.source_level)

    def kappa_consistent(self) -> bool:
        """Verify kappa(W_N) = rho * c(W_N).

        The DS shadow functor at arity 2 maps kappa(affine) to kappa(W).
        """
        n = self.source_N
        rho = anomaly_ratio_wN(n)
        c_w = self.target_central_charge()
        kappa_via_rho = rho * c_w
        kappa_direct = self.target_kappa()
        try:
            return simplify(kappa_via_rho - kappa_direct) == 0
        except Exception:
            return kappa_via_rho == kappa_direct

    def target_shadow_tower(self, max_arity: int = 4) -> Dict[int, Any]:
        """Shadow obstruction tower of the W-algebra after DS."""
        tower = {2: self.target_kappa()}
        tower[3] = S(1)  # cubic from W generators
        if self.source_N == 2:
            # W_2 = Virasoro
            c_w = self.target_central_charge()
            vir = VirasoroLCA(c_w)
            tower[4] = vir.quartic_contact()
        else:
            tower[4] = S(1)  # quartic for W_N, N >= 3 (class M)
        return tower

    def shadow_commutes(self, arity: int = 2) -> bool:
        """Check: does the shadow functor commute with DS at given arity?

        At arity 2: kappa(DS(Cur(g))) should equal DS^sh(kappa(Cur(g))).
        This reduces to kappa(W_N, k) = rho(sl_N) * c(W_N, k).
        """
        return self.kappa_consistent()


def ds_reduction(lie_type: str, rank: int, level: Any) -> DSReduction:
    """Construct a DS reduction from Cur(g) at level k."""
    return DSReduction(source_type=lie_type, source_rank=rank, source_level=level)


def ds_sl2_to_virasoro(level: Any) -> DSReduction:
    """DS reduction: sl_2-hat_k -> Vir_{c(k)}."""
    return ds_reduction('A', 1, level)


def ds_sl3_to_w3(level: Any) -> DSReduction:
    """DS reduction: sl_3-hat_k -> W_3(c(k))."""
    return ds_reduction('A', 2, level)


