@dataclass
class VirasoroLCA:
    """Virasoro 'Lie conformal algebra' data.

    The Virasoro algebra with generator L and lambda-bracket:
      [L_lambda L] = (d + 2*lambda)*L + (c/12)*lambda^3

    This is NOT a Lie conformal algebra in the strict sense because the
    lambda^3 term corresponds to a quartic pole (L_{(3)} L = c/2), which
    goes beyond the Lie conformal structure.  Nishinaka's construction
    obtains Virasoro via Sugawara from affine sl_2.

    For shadow obstruction tower computation: kappa = c/2, and higher shadows are
    determined by the T-T OPE.
    """
    central_charge: Any

    @property
    def name(self) -> str:
        return 'virasoro'

    def kappa(self) -> Any:
        """kappa(Vir, c) = c/2."""
        c = self.central_charge
        if isinstance(c, (int, Fraction)):
            return Fraction(c) / Fraction(2)
        return c / 2

    def cubic_shadow(self) -> Any:
        """Cubic shadow from [T, T] OPE: nonzero (2*T from z^{-2} pole)."""
        return S(1)

    def quartic_contact(self) -> Any:
        """Quartic contact invariant Q^contact_Vir = 10/[c*(5c + 22)].

        Poles at c=0 and c=-22/5 (Lee-Yang edge).
        """
        c = self.central_charge
        if isinstance(c, (int, Fraction)):
            if c == 0 or 5 * c + 22 == 0:
                return oo
            return Fraction(10) / (c * (5 * c + 22))
        return Rational(10) / (c * (5 * c + 22))

    def S3(self) -> Any:
        """Third shadow coefficient S_3 for Virasoro.

        From the T-T OPE at arity 3.  The cubic shadow is proportional to
        the structure constant of [T, T] at the simple pole: T_{(0)} T = dT.
        The shadow coefficient S_3 is normalized so that the cubic
        contribution to the shadow generating function is S_3 * t^3.

        For Virasoro: S_3 = 2 (from the coefficient 2 in the T-T OPE
        at the double pole: T_{(1)} T = 2T).

        More precisely, from the shadow obstruction tower recursion in
        higher_genus_modular_koszul.tex, the cubic shadow coefficient
        is alpha = 2/c = 2/(2*kappa) = 1/kappa for Virasoro.
        The shadow metric Q_L(t) = (2*kappa + 3*alpha*t)^2 + 2*Delta*t^2.

        S_3 = alpha in the shadow metric convention.
        """
        c = self.central_charge
        if isinstance(c, (int, Fraction)):
            if c == 0:
                return oo
            return Fraction(2, 1) / c
        return Rational(2) / c

    def S4(self) -> Any:
        """Fourth shadow coefficient S_4 for Virasoro.

        S_4 = Q^contact = 10/[c*(5c+22)].
        """
        return self.quartic_contact()

    def shadow_depth_class(self) -> str:
        """Virasoro is class M (mixed, r_max = infinity)."""
        return 'M'

    def shadow_tower(self, max_arity: int = 5) -> Dict[int, Any]:
        """Shadow obstruction tower for Virasoro."""
        tower = {
            2: self.kappa(),
            3: self.cubic_shadow(),
            4: self.quartic_contact(),
        }
        if max_arity >= 5:
            tower[5] = S(1)  # quintic is nonzero for class M
        return tower


def virasoro_lca(central_charge: Any = c_sym) -> VirasoroLCA:
    """Construct Virasoro data at given central charge."""
    return VirasoroLCA(central_charge=central_charge)


