# 8. Cyclic admissibility checks
# ---------------------------------------------------------------------------

def check_cyclic_admissibility(family: str, **kwargs) -> Dict[str, Any]:
    """Check cyclic admissibility for standard families.

    Cyclic admissibility (def:cyclically-admissible) requires:
      (i)   Conformal grading: L = bigoplus_{Delta >= 0} L_Delta
      (ii)  Bounded pole orders in OPE
      (iii) Invariant pairing: cyclic non-degenerate bilinear form

    Expected results:
      Cur(g) for simple g: YES (all three conditions hold)
      Virasoro: YES (inherits from sl_2 via Sugawara)
      W_N for N >= 3: YES (inherits from sl_N via DS)
      Free field: YES (trivially)
      Bershadsky-Polyakov: YES (it is W(sl_3, f_min), a W-algebra)
    """
    result = {
        'family': family,
        'conformal_grading': True,
        'bounded_poles': True,
        'invariant_pairing': True,
        'is_admissible': True,
        'notes': '',
    }

    if family.startswith('Cur(') or family.startswith('affine'):
        result['notes'] = 'Affine KM: all conditions from Killing form + conformal grading'
    elif family == 'virasoro' or family == 'Vir':
        result['notes'] = 'Virasoro: inherits from sl_2 via Sugawara'
    elif family.startswith('W_') or family.startswith('W('):
        N = kwargs.get('N', 3)
        result['notes'] = f'W_{N}: inherits from sl_{N} via DS'
    elif family == 'free_field' or family == 'Heisenberg':
        result['notes'] = 'Free field: trivially admissible (abelian)'
    elif family == 'betagamma' or family == 'BetaGamma':
        result['notes'] = 'Betagamma: admissible with symplectic pairing'
    elif family == 'Bershadsky-Polyakov' or family == 'BP':
        result['notes'] = 'BP = W(sl_3, f_min): admissible as a W-algebra'
    elif family == 'free_fermion':
        result['notes'] = 'Free fermion: admissible (abelian, Clifford pairing)'
    elif family == 'lattice':
        result['notes'] = 'Lattice VOA: admissible from lattice bilinear form'
    else:
        result['notes'] = 'Unknown family: admissibility not verified'
        result['is_admissible'] = None

    return result


# ---------------------------------------------------------------------------
# Master computation: full shadow from Lie conformal input
# ---------------------------------------------------------------------------

@dataclass
class FactorizationEnvelopeShadow:
    """Complete shadow obstruction tower computation from Lie conformal input.

    Packages the full pipeline result:
      - Input Lie conformal data (family, type)
      - Shadow obstruction tower {arity: value}
      - Depth classification (G/L/C/M)
      - Cyclic admissibility
      - DS compatibility (if applicable)
      - Modular envelope approximation (if requested)
    """
    family: str
    kappa: Any
    central_charge: Any
    shadow_tower: Dict[int, Any]
    depth_class: str
    is_admissible: bool
    ds_source: Optional[str] = None
    ds_compatible: Optional[bool] = None
    modular_contributions: Optional[Dict[int, Any]] = None

    def summary(self) -> str:
        lines = [
            f"{'=' * 60}",
            f"  Factorization Envelope Shadow: {self.family}",
            f"{'=' * 60}",
            f"  kappa:          {self.kappa}",
            f"  central charge: {self.central_charge}",
            f"  depth class:    {self.depth_class}",
            f"  admissible:     {self.is_admissible}",
            "  shadow obstruction tower:",
        ]
        for arity, val in sorted(self.shadow_tower.items()):
            lines.append(f"    arity {arity}: {val}")
        if self.ds_source:
            lines.append(f"  DS source: {self.ds_source}")
            lines.append(f"  DS compatible: {self.ds_compatible}")
        if self.modular_contributions:
            lines.append("  modular contributions:")
            for g, fg in sorted(self.modular_contributions.items()):
                lines.append(f"    genus {g}: {fg}")
        return "\n".join(lines)


def compute_shadow_from_current(ca: CurrentAlgebra, max_genus: int = 0) -> FactorizationEnvelopeShadow:
    """Compute the full shadow from a current algebra Cur(g)."""
    adm = check_cyclic_admissibility(ca.name)
    mod = None
    if max_genus > 0:
        mod_approx = modular_envelope_approximation(ca, max_genus)
        mod = mod_approx.genus_contributions

    return FactorizationEnvelopeShadow(
        family=ca.name,
        kappa=ca.kappa(),
        central_charge=ca.central_charge(),
        shadow_tower=ca.shadow_tower(),
        depth_class=ca.shadow_depth_class(),
        is_admissible=adm['is_admissible'],
        modular_contributions=mod,
    )


def compute_shadow_from_virasoro(c_val: Any) -> FactorizationEnvelopeShadow:
    """Compute the full shadow for Virasoro at central charge c."""
    vir = VirasoroLCA(c_val)
    adm = check_cyclic_admissibility('virasoro')
    return FactorizationEnvelopeShadow(
        family='virasoro',
        kappa=vir.kappa(),
        central_charge=c_val,
        shadow_tower=vir.shadow_tower(),
        depth_class=vir.shadow_depth_class(),
        is_admissible=adm['is_admissible'],
        ds_source='Cur(sl_2)',
        ds_compatible=True,
    )


def compute_shadow_from_free(weight: int, level: Any = S(1)) -> FactorizationEnvelopeShadow:
    """Compute the full shadow for a free Lie conformal algebra."""
    flca = FreeLieConformal(weight=weight, level=level)
    adm = check_cyclic_admissibility('free_field')
    return FactorizationEnvelopeShadow(
        family=flca.name,
        kappa=flca.kappa(),
        central_charge=flca.central_charge(),
        shadow_tower=flca.shadow_tower(),
        depth_class=flca.shadow_depth_class(),
        is_admissible=adm['is_admissible'],
    )


def compute_shadow_via_ds(lie_type: str, rank: int, level: Any) -> FactorizationEnvelopeShadow:
    """Compute W-algebra shadow via DS reduction from affine."""
    ds = DSReduction(lie_type, rank, level)
    n = rank + 1 if lie_type == 'A' else rank
    adm = check_cyclic_admissibility(f'W_{n}', N=n)
    return FactorizationEnvelopeShadow(
        family=f'W({lie_type}_{n})',
        kappa=ds.target_kappa(),
        central_charge=ds.target_central_charge(),
        shadow_tower=ds.target_shadow_tower(),
        depth_class='M',
        is_admissible=adm['is_admissible'],
        ds_source=f'Cur({lie_type}_{n})',
        ds_compatible=ds.kappa_consistent(),
    )


