# 7. Modular factorization envelope (genus >= 1)
# ---------------------------------------------------------------------------

@dataclass
class ModularEnvelopeApproximation:
    """First approximation to the modular factorization envelope U^mod_X(L).

    U^mod goes beyond Nishinaka's construction by including genus g >= 1 data.

    At genus 0: U^mod_0 = standard envelope = V_k(g)
    At genus 1: includes F_1 = kappa/24 (from lambda_1)
    At genus 2: includes F_2 from the planted-forest formula

    This is the BEGINNING of the modular programme; the full modular
    factorization envelope is a target (conj:platonic-adjunction).

    Attributes:
        source: the Lie conformal algebra data
        kappa: the modular characteristic
        genus_contributions: {genus: F_g value}
    """
    source_name: str
    kappa: Any
    genus_contributions: Dict[int, Any]

    def F_g(self, g: int) -> Any:
        """Genus-g contribution F_g.

        F_1 = kappa / 24  (from lambda_1 on M-bar_1)
        F_2 = kappa^2 * 7/1440  (from A-hat genus 2)
        F_3 = kappa^3 * 31/60480  (from A-hat genus 3)
        """
        if g in self.genus_contributions:
            return self.genus_contributions[g]
        return S(0)


def modular_envelope_approximation(
    ca: CurrentAlgebra,
    max_genus: int = 3,
) -> ModularEnvelopeApproximation:
    """Build the first few levels of U^mod for an affine algebra.

    F_g = kappa * Bernoulli_coefficient_g at the scalar level.

    The A-hat generating function:
      A-hat(ix) = (x/2) / sin(x/2)
      A-hat(ix) - 1 = x^2/24 + 7*x^4/5760 + 31*x^6/967680 + ...

    So F_g (at leading scalar order) = kappa * coeff of x^{2g} in A-hat(ix) - 1.

    F_1 = kappa * 1/24
    F_2 = kappa * 7/5760 = kappa * 7/5760
    F_3 = kappa * 31/967680

    Wait, the convention (AP22): sum F_g hbar^{2g} = kappa * (A-hat(i*hbar) - 1).
    So F_g = kappa * a_{2g} where A-hat(ix) - 1 = sum_{g>=1} a_{2g} * x^{2g}.

    A-hat(ix) = (x/2) / sin(x/2) = sum B_n^+ * x^{2n} / (2n)!
    where B_n^+ are related to Bernoulli numbers.

    Explicitly:
      A-hat(ix) = 1 + x^2/24 + 7*x^4/5760 + 31*x^6/967680 + ...

    So:
      a_2 = 1/24, a_4 = 7/5760, a_6 = 31/967680.
    """
    kap = ca.kappa()

    genus_data = {}
    if max_genus >= 0:
        genus_data[0] = S(1)  # genus 0: the envelope itself

    if max_genus >= 1:
        # F_1 = kappa / 24
        if isinstance(kap, (int, Fraction)):
            genus_data[1] = Fraction(kap) / Fraction(24)
        else:
            genus_data[1] = kap / 24

    if max_genus >= 2:
        # F_2 = kappa * 7/5760
        if isinstance(kap, (int, Fraction)):
            genus_data[2] = Fraction(kap) * Fraction(7, 5760)
        else:
            genus_data[2] = kap * Rational(7, 5760)

    if max_genus >= 3:
        # F_3 = kappa * 31/967680
        if isinstance(kap, (int, Fraction)):
            genus_data[3] = Fraction(kap) * Fraction(31, 967680)
        else:
            genus_data[3] = kap * Rational(31, 967680)

    return ModularEnvelopeApproximation(
        source_name=ca.name,
        kappa=kap,
        genus_contributions=genus_data,
    )


