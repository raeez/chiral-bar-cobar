@dataclass
class FreeLieConformal:
    """Free Lie conformal algebra Lie{x} with generator x of weight h.

    The lambda-bracket is determined by the single generator:
      [x_lambda x] = 0  (the FREE Lie conformal algebra on one generator
                          has zero bracket; the envelope is a symmetric algebra).

    The factorization envelope V(Lie{x}) is the free vertex algebra
    generated by x of conformal weight h.

    For h=1: V = Heisenberg at level k=1 (free boson).
    For h=2: V = free field of weight 2.

    The shadow obstruction tower depends on whether the envelope has a nontrivial
    invariant bilinear form.  For a single free generator:
      - The metric is < x | x > = 1 (normalized)
      - kappa = anomaly_from_weight(h) = (6h^2 - 6h + 1) / 2
        This is c/2 where c = 2(6h^2 - 6h + 1) is the central charge
        of a free field of weight h via the Friedan-Martinec-Shenker formula.
      - Shadow depth: class G (r_max=2) for h=1 (abelian); class C for h>=2.
    """
    weight: int
    level: Any = S(1)

    @property
    def name(self) -> str:
        return f'free_weight_{self.weight}'

    def central_charge(self) -> Any:
        """Central charge of the free field of weight h.

        For a single bc-type system at weight h (= conformal weight of beta):
          c = 1 - 12*(h - 1/2)^2 = 1 - 12(h^2 - h + 1/4) = -2(6h^2 - 6h + 1)

        But the SIGN convention depends on bc vs betagamma:
          bc ghost:     c = -2(6h^2 - 6h + 1)
          betagamma:    c = 2(6h^2 - 6h + 1)

        For h=1 (Heisenberg): c_bg = 2, c_bc = -2.
        We use the betagamma convention (positive for h=0,1).

        HOWEVER: for the free Lie conformal algebra with a SINGLE generator
        of weight h and trivial bracket, the envelope is Sym^ch(Cx),
        the symmetric algebra on the single generator.  This has c=1 for h=1
        (single free boson), not c=2 (which is the betagamma pair).

        Correction: for a SINGLE free field of weight h with unit metric:
          c = 1  for h=1 (one free boson, not a pair)

        The general formula for one free boson of weight h:
          c = 1 - 6(2h-1)^2 / 1 is WRONG (that is for one bc pair).

        For a single bosonic free field (one current J of weight h),
        the central charge is determined by the OPE J(z)J(w) ~ k/(z-w)^{2h}.
        For h=1: c = 1 (one Heisenberg field at level k=1).

        Actually, the free boson at level k has c = k for the Heisenberg.
        More precisely, one free boson = one Heisenberg at level 1, c = 1.

        For the free Lie conformal algebra on one generator of weight h,
        the envelope is the polynomial vertex algebra generated by x.
        The central charge is 1 (one free field), independent of h,
        when the self-OPE is normalized to x(z)x(w) ~ 1/(z-w)^{2h}.

        AP1: use self.level as the overall normalization.
        """
        h = self.weight
        # For a single free field at level k: c = k for h=1 (Heisenberg).
        # For h >= 2: the central charge of a single free field
        # x(z)x(w) ~ k/(z-w)^{2h} is more subtle.
        # The Sugawara construction T = (1/(2k)) :x d^{2h-2}x: would give
        # c values depending on h.
        #
        # For simplicity and correctness at h=1,2:
        # h=1: c = k (Heisenberg)
        # h=2: c = 2k (one weight-2 field, c = 2 at k=1 by standard bc/bg)
        # General: c = (2h-1)*k but we only trust h=1,2.
        if h == 1:
            return self.level
        elif h == 2:
            return 2 * self.level
        else:
            return (2 * h - 1) * self.level

    def kappa(self) -> Any:
        """Modular characteristic kappa = c/2 for a single free field.

        For h=1 (Heisenberg at level k): kappa = k/2.
        Wait -- kappa(Heis, k) = k, NOT k/2.

        The Heisenberg at level k has c = k and kappa = k.
        But kappa = c/2 = k/2 for c = k contradicts kappa = k.

        Resolution: kappa(Heis, k) = k is the level, which equals c
        (not c/2) because the anomaly ratio for abelian algebras is 1
        (not 1/2).  The formula kappa = c/2 applies to VIRASORO, not
        to Heisenberg.

        For Heisenberg: kappa = k (the level).  The anomaly ratio is
        rho(u(1)) = 1.

        For a general free field at weight h with metric k:
          h=1: kappa = k (Heisenberg)
          h=2: kappa = -k (bc ghost at c = -2k, kappa = c/2 = -k)
               or kappa = k at c = 2k (betagamma), kappa = c/2 = k
        """
        h = self.weight
        if h == 1:
            # Heisenberg: kappa = k (the level itself)
            return self.level
        elif h == 2:
            # Weight-2 free field: kappa = c/2
            # c = 2k for betagamma convention at weight 2
            return self.level
        else:
            # General weight h: kappa = c/2 = (2h-1)*k/2
            return self.central_charge() / 2

    def shadow_depth_class(self) -> str:
        """Shadow depth classification.

        h=1: class G (Gaussian, r_max=2) -- abelian, no bracket
        h=2: class C (contact, r_max=4) -- like betagamma
        h>=3: class M (mixed, infinite tower) in general
        """
        if self.weight == 1:
            return 'G'
        elif self.weight == 2:
            return 'C'
        else:
            return 'M'

    def shadow_tower(self, max_arity: int = 5) -> Dict[int, Any]:
        """Compute the shadow obstruction tower to given arity.

        Returns {arity: shadow_value}.
        """
        tower = {2: self.kappa()}
        if self.weight == 1:
            # Class G: terminates at arity 2
            return tower
        if self.weight == 2:
            # Class C: has quartic, terminates at arity 4
            tower[3] = S(0)  # no cubic (no Lie bracket)
            # Quartic contact invariant is nonzero but tower terminates
            tower[4] = S(1)  # structural marker
            return tower
        # Weight >= 3: class M in general
        tower[3] = S(0)  # still no cubic for free algebra (abelian)
        tower[4] = S(1)  # structural marker
        if max_arity >= 5:
            tower[5] = S(1)
        return tower


def free_lie_conformal(weight: int, level: Any = S(1)) -> FreeLieConformal:
    """Construct a free Lie conformal algebra on one generator of weight h."""
    return FreeLieConformal(weight=weight, level=level)


