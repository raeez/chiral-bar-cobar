@dataclass
class VicedoConstruction:
    """Vicedo (2025) non-chiral factorization algebra from a Lie algebra.

    The non-chiral direction adds a topological component.  For shadow
    tower comparison: the CHIRAL shadow component should agree with the
    Nishinaka construction.

    For g = sl_2 at level k: the chiral component gives kappa = 3(k+2)/4,
    which matches the affine shadow.  The topological component is trivial
    at the shadow level.
    """
    lie_type: str
    rank: int
    level: Any

    def chiral_kappa(self) -> Any:
        """Chiral component kappa -- matches affine kappa."""
        ca = CurrentAlgebra(self.lie_type, self.rank, self.level)
        return ca.kappa()

    def topological_kappa(self) -> Any:
        """Topological component kappa: zero (topological = no modular anomaly)."""
        return S(0)

    def total_kappa(self) -> Any:
        """Total kappa = chiral + topological = chiral kappa."""
        return self.chiral_kappa() + self.topological_kappa()

    def chiral_agrees_with_nishinaka(self) -> bool:
        """The chiral component should agree with the Nishinaka construction."""
        ca = CurrentAlgebra(self.lie_type, self.rank, self.level)
        try:
            return simplify(self.chiral_kappa() - ca.kappa()) == 0
        except Exception:
            return self.chiral_kappa() == ca.kappa()


def vicedo_construction(lie_type: str, rank: int, level: Any) -> VicedoConstruction:
    """Construct Vicedo's non-chiral factorization algebra."""
    return VicedoConstruction(lie_type=lie_type, rank=rank, level=level)


