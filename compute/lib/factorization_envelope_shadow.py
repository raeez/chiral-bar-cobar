@dataclass
class CurrentAlgebra:
    """Current Lie conformal algebra Cur(g) = g tensor C[d].

    The lambda-bracket is:
      [a_lambda b] = [a, b] + k*(a, b)*lambda

    where [a,b] is the Lie bracket and (a,b) is the Killing form
    normalized so that long roots have length 2.

    The factorization envelope V(Cur(g)) = V_k(g), the universal
    affine vertex algebra at level k.

    Attributes:
        lie_type: type of simple Lie algebra ('A', 'B', 'C', 'D', 'E', 'F', 'G')
        rank: rank of the Lie algebra
        level: affine level k
    """
    lie_type: str
    rank: int
    level: Any

    @property
    def name(self) -> str:
        return f'Cur({self.lie_type}_{self.rank})'

    @property
    def N(self) -> int:
        """For type A: sl_N where N = rank + 1."""
        if self.lie_type == 'A':
            return self.rank + 1
        return self.rank

    def dim(self) -> int:
        """Dimension of the Lie algebra g."""
        if self.lie_type == 'A':
            n = self.rank + 1
            return n * n - 1
        elif self.lie_type == 'B':
            n = self.rank
            return n * (2 * n + 1)
        elif self.lie_type == 'C':
            n = self.rank
            return n * (2 * n + 1)
        elif self.lie_type == 'D':
            n = self.rank
            return n * (2 * n - 1)
        elif self.lie_type == 'G' and self.rank == 2:
            return 14
        elif self.lie_type == 'F' and self.rank == 4:
            return 52
        elif self.lie_type == 'E' and self.rank == 6:
            return 78
        elif self.lie_type == 'E' and self.rank == 7:
            return 133
        elif self.lie_type == 'E' and self.rank == 8:
            return 248
        else:
            raise ValueError(f"Unknown Lie type {self.lie_type}_{self.rank}")

    def dual_coxeter(self) -> int:
        """Dual Coxeter number h^v."""
        _DUAL_COXETER = {
            ('A', 1): 2, ('A', 2): 3, ('A', 3): 4, ('A', 4): 5,
            ('A', 5): 6, ('A', 6): 7, ('A', 7): 8,
            ('B', 2): 3, ('B', 3): 5, ('B', 4): 7,
            ('C', 2): 3, ('C', 3): 4, ('C', 4): 5,
            ('D', 3): 4, ('D', 4): 6, ('D', 5): 8,
            ('G', 2): 4,
            ('F', 4): 9,
            ('E', 6): 12, ('E', 7): 18, ('E', 8): 30,
        }
        key = (self.lie_type, self.rank)
        if key in _DUAL_COXETER:
            return _DUAL_COXETER[key]
        # Type A general: h^v = N = rank + 1
        if self.lie_type == 'A':
            return self.rank + 1
        # Type B general: h^v = 2*rank - 1
        if self.lie_type == 'B':
            return 2 * self.rank - 1
        # Type C general: h^v = rank + 1
        if self.lie_type == 'C':
            return self.rank + 1
        # Type D general: h^v = 2*rank - 2
        if self.lie_type == 'D':
            return 2 * self.rank - 2
        raise ValueError(f"No dual Coxeter number for {self.lie_type}_{self.rank}")

    def central_charge(self) -> Any:
        """Sugawara central charge c = k * dim(g) / (k + h^v)."""
        k = self.level
        d = self.dim()
        hv = self.dual_coxeter()
        if isinstance(k, (int, Fraction)):
            if k + hv == 0:
                raise ValueError(f"Critical level k = -{hv}")
            return Fraction(d) * Fraction(k) / (Fraction(k) + Fraction(hv))
        # Symbolic
        return d * k / (k + hv)

    def kappa(self) -> Any:
        """Modular characteristic kappa(g, k) = dim(g) * (k + h^v) / (2 * h^v).

        This is the universal formula for affine Kac-Moody algebras.
        AP1: recomputed from first principles for each family.
        """
        k = self.level
        d = self.dim()
        hv = self.dual_coxeter()
        if isinstance(k, (int, Fraction)):
            return Fraction(d) * (Fraction(k) + Fraction(hv)) / (2 * Fraction(hv))
        return Rational(d) * (k + hv) / (2 * hv)

    def shadow_depth_class(self) -> str:
        """All affine KM algebras are class L (Lie/tree, r_max=3)."""
        return 'L'

    def cubic_shadow(self) -> Any:
        """Cubic shadow: 1 (nonzero from Lie bracket, class L)."""
        return S(1)

    def quartic_shadow(self) -> Any:
        """Quartic shadow: 0 (Jacobi kills quartic for affine KM)."""
        return S(0)

    def shadow_tower(self, max_arity: int = 5) -> Dict[int, Any]:
        """Shadow obstruction tower for Cur(g): terminates at arity 3."""
        return {
            2: self.kappa(),
            3: self.cubic_shadow(),
        }

    def anomaly_ratio(self) -> Any:
        """Anomaly ratio rho = kappa / c.

        For affine sl_N: rho = dim(g) * (k + h^v) / (2 * h^v * c)
                             = dim(g) * (k + h^v) / (2 * h^v) / [k * dim(g) / (k + h^v)]
                             = (k + h^v)^2 / (2 * h^v * k)

        This is NOT family-independent for affine KM. The level-independent
        anomaly ratio (rho in CLAUDE.md) is defined for W-algebras, where
        kappa = rho * c with rho = H_N - 1.  For affine algebras, the
        ratio kappa/c = (k + h^v) / (2k) depends on k.
        """
        k = self.level
        hv = self.dual_coxeter()
        c = self.central_charge()
        if c == 0:
            return oo
        return self.kappa() / c


def current_algebra(lie_type: str, rank: int, level: Any = k_sym) -> CurrentAlgebra:
    """Construct Cur(g) for a simple Lie algebra g."""
    return CurrentAlgebra(lie_type=lie_type, rank=rank, level=level)


