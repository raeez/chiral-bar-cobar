def theorem_summary_wp() -> str:
    """Summary of W(p) Koszulness results."""
    lines = []
    for p in [2, 3]:
        v = koszulness_verdict(p, 20)
        lines.append(f"W({p}): {v['verdict']} ({v['confidence']})")
        lines.append(f"  {v['evidence']}")
    return '\n'.join(lines)
