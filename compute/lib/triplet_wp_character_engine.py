r"""Triplet W(p) Koszulness engine via character comparison.

Tests whether the triplet VOA W(p) is freely strongly generated
(and hence chirally Koszul) by comparing the PBW character with
independently computed vacuum character data.

MATHEMATICAL FRAMEWORK
======================

The triplet VOA W(p) (p >= 2) has:
    c(p) = 1 - 6(p-1)^2/p
    Strong generators (Adamovic-Milas 2008):
      T: conformal weight 2, multiplicity 1
      W^+, W^0, W^-: conformal weight 2p-1, multiplicity 3

The PBW character (assuming FREE strong generation) is:
    ch_PBW(q) = prod_{n >= 2} 1/(1-q^n) * [prod_{n >= 2p-1} 1/(1-q^n)]^3

KOSZULNESS CRITERION: If W(p) is freely strongly generated (no null
vectors among the normally ordered products of generators), then W(p)
is chirally Koszul by prop:pbw-universality.

FREE STRONG GENERATION TEST: Compare PBW character with the actual
W(p) vacuum character. Agreement at all weights implies free strong
generation.  PBW > actual at some weight implies a null vector there.

THE ACTUAL CHARACTER: two independent computation methods.

METHOD A (lattice VOA extraction):
    V_L for L = sqrt(2p)Z has character theta_L(q)/eta(q).
    V_L decomposes under W(p) as a direct sum of W(p)-modules.
    The vacuum module M_{1,1} is the zero-charge sector.
    Its character is the zero-momentum projection:
        ch_{M_{1,1}} = (1/eta(q)) * [terms with q-exponent integer]

METHOD B (Fock space dimension count):
    The Fock space F_0 at momentum 0 has character 1/eta(q).
    W(p) = ker(Q_-) on the full Fock space of a free boson.
    The vacuum module has dim_h determined by the structure of
    null vectors (which create relations among normally ordered products).
    If there are NO null vectors, then dim_h = PBW_h.

    The FIRST possible null vector is at weight 2*(2p-1) = 4p-2
    (the bar-relevant weight), where two W-fields collide and could
    produce a relation. By computing the character through this weight
    and comparing with PBW, we can settle the question.

    For W(2): first possible null at weight 4*2-2 = 6.
    For W(3): first possible null at weight 4*3-2 = 10.

METHOD C (lattice theta expansion, verified):
    For the rank-1 lattice L = sqrt(2p)Z, the lattice VOA V_L has
    character ch_{V_L}(q) = Theta_L(q)/eta(q) where Theta_L = sum_n q^{pn^2}.

    The zero-charge sector F_0 (momentum = 0) has character 1/eta(q).
    The FULL W(p) vacuum module includes F_0 AND corrections from the
    screening structure.

    For FREE strong generation: dim(W(p)_h) = dim(F_0,h) at weights
    below the first null vector, and equals the PBW character (which
    equals the number of normally ordered monomials of generators).

HONESTY NOTE: The PBW character for 4 generators (1@wt2, 3@wt(2p-1))
does NOT equal the full Fock space character 1/eta(q). It equals the
character of the sub-VOA generated by T and W^a, which is SMALLER than
the Fock space (the Fock space has generators at ALL weights >= 1).
The comparison: PBW <= Fock (PBW counts only products of T, W^a;
Fock counts products of ALL modes a_{-n}). Agreement PBW = actual means
the W-fields W^a and their descendants account for all states that T
alone misses.

VERIFICATION PATHS (3+):
    Path 1: PBW character from partition function
    Path 2: Lattice theta / eta (independent series computation)
    Path 3: Virasoro sub-character as lower bound
    Path 4: Known values from literature (Gaberdiel-Kausch, Wood, FGST)
    Path 5: Zhu algebra dimension (Adamovic-Milas: dim A(W(p)) = 2p)

References:
    Kausch (1991): triplet construction
    Gaberdiel-Kausch (1996): logarithmic CFT
    Adamovic-Milas (2008): W(p) structure, strong generation, Zhu algebra
    FGST (2006): modular data
    Nagatomo-Tsuchiya (2009): character formulas
    Wood (2014): triplet character tables
    Manuscript: prop:pbw-universality, thm:kac-shapovalov-koszulness
"""

from __future__ import annotations

from fractions import Fraction
from typing import Dict, List, Optional, Tuple
