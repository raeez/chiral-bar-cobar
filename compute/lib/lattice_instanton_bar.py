def enumerate_roots(G: np.ndarray, search_bound: int = 0) -> List[np.ndarray]:
    """Enumerate all roots α with ⟨α,α⟩ = 2 for an even root lattice.

    For ADE root lattices, roots are the lattice vectors of norm 2.
    We use the inverse Cartan matrix to convert from the simple root
    basis (where G = Cartan matrix) to the weight basis, and then
    enumerate systematically.

    For small rank (≤ 6), brute-force search suffices.
    For rank 7-8 (E₇, E₈), we use the positive root construction
    via the Weyl group orbit of simple roots.

    Returns list of integer arrays (root vectors in the simple root basis).
    """
    n = G.shape[0]

    # For small rank, brute force works
    if n <= 6:
        if search_bound <= 0:
            search_bound = 4
        roots = []
        for coords in itertools.product(range(-search_bound, search_bound + 1), repeat=n):
            v = np.array(coords, dtype=int)
            if np.any(v != 0):
                norm_sq = int(v @ G @ v)
                if norm_sq == 2:
                    roots.append(v)
        return roots

    # For larger rank, use the positive root enumeration algorithm.
    # Positive roots are non-negative integer linear combinations of
    # simple roots with norm 2.  We enumerate them level by level
    # (level = sum of coefficients).
    roots = []

    # Simple roots: e_i (standard basis vectors)
    for i in range(n):
        e = np.zeros(n, dtype=int)
        e[i] = 1
        roots.append(e.copy())

    # BFS: from known positive roots, try adding simple roots
    # A positive root α has ⟨α,α⟩ = 2.  If ⟨α, α_i⟩ < 0 for simple
    # root α_i, then α + α_i is also a root (by the reflection formula).
    # We can also check: if α + α_i has norm 2, it's a root.
    seen = {tuple(e.tolist()) for e in roots}
    queue = list(roots)
    while queue:
        alpha = queue.pop(0)
        for i in range(n):
            e_i = np.zeros(n, dtype=int)
            e_i[i] = 1
            candidate = alpha + e_i
            key = tuple(candidate.tolist())
            if key not in seen:
                norm_sq = int(candidate @ G @ candidate)
                if norm_sq == 2:
                    seen.add(key)
                    roots.append(candidate.copy())
                    queue.append(candidate)

    # Add negative roots
    pos_roots = list(roots)  # copy before extending
    for alpha in pos_roots:
        neg = -alpha
        key = tuple(neg.tolist())
        if key not in seen:
            seen.add(key)
            roots.append(neg)

    return roots


def root_inner_product_census(roots: List[np.ndarray], G: np.ndarray) -> Dict[int, int]:
    """For each root α, count neighbors β with ⟨α,β⟩ = k for each k.

    Returns dict mapping inner product value -> count of pairs (α,β)
    with α fixed (should be the same for all α by Weyl group transitivity
    in simply-laced types).
    """
    if not roots:
        return {}
    alpha = roots[0]
    census = {}
    for beta in roots:
        ip = int(alpha @ G @ beta)
        census[ip] = census.get(ip, 0) + 1
    return census


def simple_pole_pairs(roots: List[np.ndarray], G: np.ndarray) -> List[Tuple[np.ndarray, np.ndarray]]:
    """Pairs (α, β) of roots with ⟨α,β⟩ = -1 (simple pole in OPE).

    These are exactly the pairs where the bar differential is nonzero
    on pure lattice vertex operator elements.

    Reference: thm:lattice:bar-structure(ii).
    """
    pairs = []
    for i, alpha in enumerate(roots):
        for j, beta in enumerate(roots):
            if i != j:
                ip = int(alpha @ G @ beta)
                if ip == -1:
                    pairs.append((alpha, beta))
    return pairs


def opposite_root_pairs(roots: List[np.ndarray], G: np.ndarray) -> List[Tuple[np.ndarray, np.ndarray]]:
    """Pairs (α, β) with ⟨α,β⟩ = -2, i.e., β = -α (opposite roots).

    The bar differential on these extracts the SUBLEADING simple pole:
    V_α(z)V_{-α}(w) ~ ε(α,-α)/(z-w)² + α·j(w)/(z-w) + ...
    d_B = α·j + k·|0⟩

    Reference: comp:E8-bar-deg2, type III with ⟨α,β⟩ = -2.
    """
    pairs = []
    for alpha in roots:
        for beta in roots:
            ip = int(alpha @ G @ beta)
            if ip == -2:
                pairs.append((alpha, beta))
    return pairs


