def instanton_bar_differential_structure(
    G: np.ndarray,
    roots: Optional[List[np.ndarray]] = None,
) -> Dict[str, Any]:
    """Analyze the bar differential structure in instanton sectors.

    For each root α, classify the bar differential d_B([e^α | e^β]):
      ⟨α,β⟩ = -1: d_B = ε(α,β)·e^{α+β}  (instanton → instanton)
      ⟨α,β⟩ = -2: d_B = J_α + k|0⟩       (instanton → perturbative)
      ⟨α,β⟩ ≥ 0:  d_B = 0                 (trivial)

    The key structural result: the instanton-to-perturbative transitions
    (⟨α,β⟩ = -2, i.e., opposite roots) source the CURVATURE of the bar
    complex.  The instanton-to-instanton transitions (⟨α,β⟩ = -1) form
    chains that encode the root system combinatorics.
    """
    if roots is None:
        roots = enumerate_roots(G)

    n_roots = len(roots)
    rank = G.shape[0]

    # Classify all root-root pairs
    n_trivial = 0       # ⟨α,β⟩ ≥ 0
    n_fusion = 0        # ⟨α,β⟩ = -1 (instanton fusion)
    n_annihilation = 0  # ⟨α,β⟩ = -2 (instanton-antiinstanton)

    fusion_chains = {}  # Track fusion: (α, β) → α+β
    annihilation_targets = {}  # Track: α → J_α

    for alpha in roots:
        for beta in roots:
            ip = int(alpha @ G @ beta)
            if ip >= 0:
                n_trivial += 1
            elif ip == -1:
                n_fusion += 1
                gamma = tuple((alpha + beta).tolist())
                if gamma not in fusion_chains:
                    fusion_chains[gamma] = []
                fusion_chains[gamma].append((tuple(alpha.tolist()), tuple(beta.tolist())))
            elif ip == -2:
                n_annihilation += 1
                annihilation_targets[tuple(alpha.tolist())] = True

    # Check: n_annihilation should equal n_roots (each root has exactly
    # one opposite, and ⟨α,-α⟩ = -2)
    assert n_annihilation == n_roots, (
        f"Expected {n_roots} annihilation pairs, got {n_annihilation}"
    )

    # Fusion target analysis: where do the fusion products land?
    fusion_targets_are_roots = 0
    fusion_targets_nonroot = 0
    for gamma, pairs in fusion_chains.items():
        gamma_arr = np.array(gamma, dtype=int)
        gamma_norm = int(gamma_arr @ G @ gamma_arr)
        if gamma_norm == 2:
            fusion_targets_are_roots += len(pairs)
        else:
            fusion_targets_nonroot += len(pairs)

    return {
        'n_roots': n_roots,
        'total_root_root_pairs': n_roots ** 2,
        'n_trivial': n_trivial,
        'n_fusion': n_fusion,
        'n_annihilation': n_annihilation,
        'n_distinct_fusion_targets': len(fusion_chains),
        'fusion_targets_are_roots': fusion_targets_are_roots,
        'fusion_targets_nonroot': fusion_targets_nonroot,
        'fraction_active': (n_fusion + n_annihilation) / (n_roots ** 2) if n_roots > 0 else 0,
    }


def instanton_homology_estimate(
    G: np.ndarray,
    roots: Optional[List[np.ndarray]] = None,
) -> Dict[str, Any]:
    """Estimate the bar homology contributed by instanton sectors.

    In each sector γ = α + β with γ ≠ 0:
    - If γ is a root (|γ|² = 2): the sector has both bar-degree-1
      generators [e^γ] and bar-degree-2 generators [e^α | e^β].
      The differential maps B^2_γ → B^1_γ.  The homology detects
      the kernel/cokernel of this map.
    - If γ is not a root but γ ∈ Λ with |γ|² = 4: the sector has
      bar-degree-1 generators [e^γ] (lattice vectors of norm 4) and
      bar-degree-2 generators from fusion.

    The instanton homology classes are the topological invariants that
    distinguish lattices with the same rank.
    """
    if roots is None:
        roots = enumerate_roots(G)

    rank = G.shape[0]
    root_set = {tuple(r.tolist()) for r in roots}

    # For each root sector γ (with |γ|² = 2):
    # Count dim B^1_γ (= 1 if γ is a root, from [e^γ])
    # Count dim B^2_γ (= number of pairs (α,β) with α+β = γ and ⟨α,β⟩ = -1)
    root_sector_data = []
    for root in roots:
        gamma = tuple(root.tolist())
        # How many pairs (α, β) with α+β = root and ⟨α,β⟩ = -1?
        n_sources = 0
        for alpha in roots:
            beta = root - alpha
            beta_tuple = tuple(beta.tolist())
            if beta_tuple in root_set:
                ip = int(alpha @ G @ beta)
                if ip == -1:
                    n_sources += 1

        root_sector_data.append({
            'gamma': gamma,
            'dim_B1': 1,  # [e^γ]
            'dim_B2_active': n_sources,  # pairs with nonzero d_B
            # If n_sources > 0: homology = ker(d) / im(d)
            # d: B^2_γ → B^1_γ is a linear map from n_sources-dim to 1-dim
            # So: ker(d) has dim n_sources - 1 (if d is surjective)
            # and coker(d) = 0 (if d is surjective)
            # Generically d is surjective, so H^1_γ = 0, H^2_γ has dim n_sources - 1
            'H1_estimate': 0 if n_sources > 0 else 1,
            'H2_estimate': max(0, n_sources - 1) if n_sources > 0 else 0,
        })

    total_H1 = sum(s['H1_estimate'] for s in root_sector_data)
    total_H2 = sum(s['H2_estimate'] for s in root_sector_data)

    return {
        'n_root_sectors': len(root_sector_data),
        'total_H1_instanton': total_H1,
        'total_H2_instanton': total_H2,
        'sample_sectors': root_sector_data[:5] if root_sector_data else [],
    }


# =========================================================================
# 5. Lattice comparison: same rank, different root systems
# =========================================================================

def compare_lattice_instantons(
    G1: np.ndarray,
    G2: np.ndarray,
    label1: str = 'Λ₁',
    label2: str = 'Λ₂',
    max_norm_sq: int = 10,
) -> Dict[str, Any]:
    """Compare instanton structure of two lattices.

    For lattices of the same rank, the perturbative (Heisenberg) sector
    is identical: same κ = rank.  The difference is entirely in the
    instanton sectors.

    Key comparisons:
    1. Shell counts (theta function coefficients)
    2. Root system data (number of roots, adjacency structure)
    3. Bar differential activity
    """
    rank1, rank2 = G1.shape[0], G2.shape[0]

    # Theta function coefficients
    theta1 = instanton_counting_function(G1, max_norm_sq)
    theta2 = instanton_counting_function(G2, max_norm_sq)

    # Root enumeration
    roots1 = enumerate_roots(G1)
    roots2 = enumerate_roots(G2)

    # Inner product census
    census1 = root_inner_product_census(roots1, G1) if roots1 else {}
    census2 = root_inner_product_census(roots2, G2) if roots2 else {}

    # Bar differential structure
    bar1 = instanton_bar_differential_structure(G1, roots1)
    bar2 = instanton_bar_differential_structure(G2, roots2)

    # Detect differences
    theta_match = (theta1 == theta2)
    root_count_match = (len(roots1) == len(roots2))
    census_match = (census1 == census2)

    return {
        'rank': (rank1, rank2),
        'same_rank': rank1 == rank2,
        'same_kappa': rank1 == rank2,  # κ = rank for lattice VOAs
        label1: {
            'n_roots': len(roots1),
            'theta_coefficients': theta1,
            'inner_product_census': census1,
            'n_fusion_pairs': bar1['n_fusion'],
            'determinant': lattice_determinant(G1),
        },
        label2: {
            'n_roots': len(roots2),
            'theta_coefficients': theta2,
            'inner_product_census': census2,
            'n_fusion_pairs': bar2['n_fusion'],
            'determinant': lattice_determinant(G2),
        },
        'theta_match': theta_match,
        'root_count_match': root_count_match,
        'census_match': census_match,
        'instanton_distinguishes': not theta_match,
    }


