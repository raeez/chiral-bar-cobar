def instanton_shadow_bridge(G: np.ndarray, max_norm_sq: int = 20, search_bound: int = 5) -> Dict[str, Any]:
    """Analyze the relationship between instanton and shadow data.

    KEY FINDING: The shadow tower of V_Λ terminates at arity 2 (Gaussian
    archetype, cor:lattice-postnikov-termination).  The scalar shadow
    sees only κ = rank(Λ).  The lattice structure Λ is invisible to
    the shadow tower.

    However, the FULL MC element Θ_{V_Λ} (not just its scalar projection)
    contains the lattice data.  The genus-g sewing amplitude factors:
      Z_g(V_Λ; Ω) = Z_g(H_r; Ω) · Θ_Λ(Ω)

    The Heisenberg factor is controlled by κ (the shadow).
    The theta factor is the instanton contribution.

    The bridge formula:
      Θ_Λ(τ) = 1 + Σ_{α∈Λ, α≠0} q^{⟨α,α⟩/2}
             = 1 + Σ_n N_n · q^{n/2}

    where N_n = #{α ∈ Λ : ⟨α,α⟩ = n} is the shell count.

    This is NOT a projection of the shadow tower Θ_A^{≤r}; it is a
    SEPARATE invariant of the bar complex, living in the instanton sectors.
    The shadow tower (scalar, arity-r projections of the MC element)
    captures the perturbative data.  The theta function captures the
    non-perturbative data.  Together they give the full partition function.

    The relationship is:
      Z_1(V_Λ) = exp(-F^pert_1) · Θ_Λ
    where F^pert_1 = κ/24 = rank/24 (the shadow's genus-1 free energy).
    So: Θ_Λ = Z_1 / Z_1(H_rank) = Z_1 · η(τ)^{2·rank}
    i.e., the instanton factor equals the full partition function DIVIDED
    by the perturbative (Heisenberg) factor.
    """
    rank = G.shape[0]
    inst = instanton_counting_function(G, max_norm_sq, search_bound)

    # Shadow data (perturbative)
    kappa = rank  # κ(V_Λ) = rank(Λ) by thm:lattice:curvature-braiding-orthogonal
    F1_shadow = Rational(kappa, 24)  # F_1 = κ/24

    # Instanton data (non-perturbative)
    total_instantons = sum(inst.values())

    # Shell structure
    shells = sorted(inst.items())

    # Growth rate of instanton counting
    # By Smith-Minkowski-Siegel mass formula, N_n ~ C · n^{rank/2 - 1}
    # for large n (polynomial in n for fixed rank).
    growth_data = []
    for norm_sq, count in shells:
        if norm_sq > 0:
            growth_data.append({
                'norm_sq': norm_sq,
                'count': count,
                'weight': norm_sq / 2,
                'log_count': math.log(count) if count > 0 else 0,
            })

    return {
        'rank': rank,
        'kappa': kappa,
        'F1_perturbative': str(F1_shadow),
        'shadow_depth': 2,  # Gaussian archetype
        'shadow_class': 'G',
        'instanton_shell_counts': dict(shells),
        'total_instantons_up_to_norm': total_instantons,
        'max_norm_sq_searched': max_norm_sq,
        'perturbative_sees': f'κ = {rank} only',
        'instanton_sees': f'full lattice Λ via Θ_Λ (shell counts N_n)',
        'bridge_formula': 'Θ_Λ(τ) = Z_1(V_Λ)/Z_1(H_rank) = Z_1 · η^{2r}',
        'growth_data': growth_data[:10],
    }


