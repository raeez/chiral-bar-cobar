def gram_matrix(typ: str, rank: int) -> np.ndarray:
    """Gram matrix (Cartan matrix) for an ADE root lattice.

    Returns integer symmetric matrix G with G_{ii} = 2 (even lattice).

    Reference: ex:lattice:root-lattices in lattice_foundations.tex.
    """
    G = np.zeros((rank, rank), dtype=int)
    for i in range(rank):
        G[i, i] = 2

    if typ == 'A':
        for i in range(rank - 1):
            G[i, i + 1] = -1
            G[i + 1, i] = -1
    elif typ == 'D':
        if rank < 3:
            raise ValueError(f"D_n requires n >= 3, got n={rank}")
        for i in range(rank - 2):
            G[i, i + 1] = -1
            G[i + 1, i] = -1
        # D_n branching: last node connects to (n-3)
        G[rank - 1, rank - 3] = -1
        G[rank - 3, rank - 1] = -1
    elif typ == 'E':
        if rank not in (6, 7, 8):
            raise ValueError(f"E_n requires n in {{6,7,8}}, got n={rank}")
        # Spine: 0-1-2-3-4-...(rank-2)
        for i in range(rank - 2):
            G[i, i + 1] = -1
            G[i + 1, i] = -1
        # Branch: node (rank-1) connects to node 2
        G[rank - 1, 2] = -1
        G[2, rank - 1] = -1
    else:
        raise ValueError(f"Unknown root system type: {typ}")

    return G


def lattice_determinant(G: np.ndarray) -> int:
    """Determinant of the Gram matrix = order of discriminant group."""
    return abs(int(round(np.linalg.det(G))))


def is_unimodular(G: np.ndarray) -> bool:
    """A lattice is unimodular iff det(Gram) = 1."""
    return lattice_determinant(G) == 1


