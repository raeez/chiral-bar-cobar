def bar_sector_decomposition(
    roots: List[np.ndarray],
    G: np.ndarray,
) -> Dict[str, Any]:
    """Decompose the degree-2 bar complex into sectors by total lattice degree.

    B^2(V_Λ) = ⊕_{γ ∈ Λ} B^2_γ(V_Λ)
    The differential preserves γ = α + β.

    Returns:
        perturbative: data about the γ = 0 (Cartan/Heisenberg) sector
        instanton_sectors: data about γ ≠ 0 sectors
        total_bar2_dim: total dimension of bar degree 2 (root×root part)
    """
    rank = G.shape[0]
    n_roots = len(roots)

    # Classify pairs by total lattice degree γ = α + β
    sectors = {}  # γ (as tuple) -> list of (α, β) pairs
    for alpha in roots:
        for beta in roots:
            gamma = tuple((alpha + beta).tolist())
            if gamma not in sectors:
                sectors[gamma] = []
            sectors[gamma].append((alpha, beta))

    # Perturbative sector: γ = 0 (opposite roots only)
    zero = tuple([0] * rank)
    perturbative_pairs = sectors.get(zero, [])

    # Instanton sectors: γ ≠ 0
    instanton_sectors = {}
    for gamma, pairs in sectors.items():
        if gamma != zero:
            # Check which pairs have nonzero bar differential
            active_pairs = []
            for alpha, beta in pairs:
                ip = int(alpha @ G @ beta)
                if ip == -1:
                    active_pairs.append((alpha, beta, 'simple_pole'))
                elif ip == -2:
                    active_pairs.append((alpha, beta, 'double_pole_with_subleading'))
            instanton_sectors[gamma] = {
                'total_pairs': len(pairs),
                'active_pairs': active_pairs,
                'n_active': len(active_pairs),
                'gamma_norm_sq': sum(g * G[i, j] * g2
                                     for i, g in enumerate(gamma)
                                     for j, g2 in enumerate(gamma))
                if all(isinstance(x, int) for x in gamma)
                else int(np.array(gamma) @ G @ np.array(gamma)),
            }

    # Count distinct nonzero sectors
    nonzero_sectors = {g for g in sectors if g != zero}

    return {
        'rank': rank,
        'n_roots': n_roots,
        'total_bar2_root_root': n_roots ** 2,
        'perturbative': {
            'sector': zero,
            'n_pairs': len(perturbative_pairs),  # = n_roots (opposite pairs)
            'curvature': f'κ = {rank}',
            'bar_diff_type': 'double_pole_subleading (J_α + k|0⟩)',
        },
        'n_instanton_sectors': len(nonzero_sectors),
        'instanton_sectors': instanton_sectors,
    }


