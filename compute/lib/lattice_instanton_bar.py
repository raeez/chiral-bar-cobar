def lattice_shells(G: np.ndarray, max_norm_sq: int = 20, search_bound: int = 5) -> Dict[int, List[np.ndarray]]:
    """Enumerate lattice vectors organized by norm squared ⟨α,α⟩.

    Returns dict: norm_sq -> list of lattice vectors with that norm.
    The "instanton number" at shell n is the number of vectors with
    |α|²/2 = n/2, i.e., ⟨α,α⟩ = n.

    For even lattices, all norms are even, so shells are at n = 2, 4, 6, ...

    For high rank (> 6), uses BFS from simple roots to avoid exponential
    brute-force search.
    """
    rank = G.shape[0]

    if rank > 6:
        # BFS approach: start from simple roots and extend.
        # This finds all lattice vectors reachable by adding/subtracting
        # simple roots, with norm ≤ max_norm_sq.
        shells = {}
        seen = set()
        # Start with simple roots and their negatives
        starters = []
        for i in range(rank):
            e = np.zeros(rank, dtype=int)
            e[i] = 1
            starters.append(e)
            starters.append(-e)
        queue = list(starters)
        for v in queue:
            key = tuple(v.tolist())
            if key not in seen:
                seen.add(key)
                ns = int(v @ G @ v)
                if 0 < ns <= max_norm_sq:
                    if ns not in shells:
                        shells[ns] = []
                    shells[ns].append(v.copy())

        # BFS expansion
        frontier = list(starters)
        max_iters = 500000  # safety bound
        iters = 0
        while frontier and iters < max_iters:
            alpha = frontier.pop(0)
            iters += 1
            for i in range(rank):
                e_i = np.zeros(rank, dtype=int)
                e_i[i] = 1
                for sign in [1, -1]:
                    candidate = alpha + sign * e_i
                    key = tuple(candidate.tolist())
                    if key not in seen:
                        ns = int(candidate @ G @ candidate)
                        if 0 < ns <= max_norm_sq:
                            seen.add(key)
                            if ns not in shells:
                                shells[ns] = []
                            shells[ns].append(candidate.copy())
                            frontier.append(candidate)
                        elif ns > max_norm_sq:
                            seen.add(key)  # Don't revisit, but don't expand
        return shells

    shells = {}
    for coords in itertools.product(range(-search_bound, search_bound + 1), repeat=rank):
        v = np.array(coords, dtype=int)
        if np.any(v != 0):
            norm_sq = int(v @ G @ v)
            if 0 < norm_sq <= max_norm_sq:
                if norm_sq not in shells:
                    shells[norm_sq] = []
                shells[norm_sq].append(v)
    return shells


def instanton_counting_function(G: np.ndarray, max_norm_sq: int = 20, search_bound: int = 5) -> Dict[int, int]:
    """The instanton partition function Z^inst_Λ(q) = Θ_Λ(q) - 1.

    Returns dict: norm_sq -> number of lattice vectors at that shell.
    Equivalently: the coefficient of q^{n/2} in Θ_Λ(q) - 1.

    Z^inst_Λ(q) = Σ_{α ∈ Λ, α≠0} q^{⟨α,α⟩/2}
                 = Σ_{n=2,4,6,...} N_n · q^{n/2}

    where N_n = #{α ∈ Λ : ⟨α,α⟩ = n}.
    """
    shells = lattice_shells(G, max_norm_sq, search_bound)
    return {n: len(vecs) for n, vecs in sorted(shells.items())}


def theta_coefficients(G: np.ndarray, max_norm_sq: int = 20, search_bound: int = 5) -> Dict[int, int]:
    """Theta function coefficients: Θ_Λ(q) = 1 + Σ N_n q^{n/2}.

    Same as instanton_counting_function but with the q^0 = 1 term.
    """
    inst = instanton_counting_function(G, max_norm_sq, search_bound)
    result = {0: 1}  # The zero vector contributes 1
    result.update(inst)
    return result


def e8_theta_check(max_norm_sq: int = 10) -> Dict[str, Any]:
    """Verify E₈ theta function = E₄(τ) = 1 + 240q + 2160q² + ...

    The Eisenstein series E₄(τ) = 1 + 240 Σ_{n≥1} σ₃(n) q^n
    where σ₃(n) = Σ_{d|n} d³.

    For the E₈ lattice: Θ_{E₈}(τ) = E₄(τ), so the shell counts must be
    N_2 = 240 (roots), N_4 = 2160, N_6 = 6720, ...

    Ground truth: 240 = σ₃(1)·240 = 240; 2160 = σ₃(2)·240 = 9·240 = 2160;
    6720 = σ₃(3)·240 = 28·240 = 6720 (wait: σ₃(3) = 1+27 = 28, 28·240 = 6720).
    """
    G = gram_matrix('E', 8)
    inst = instanton_counting_function(G, max_norm_sq, search_bound=4)

    # E₄ coefficients: 240·σ₃(n) for the coefficient of q^n
    def sigma3(n):
        return sum(d ** 3 for d in range(1, n + 1) if n % d == 0)

    expected = {}
    for n_half in range(1, max_norm_sq // 2 + 1):
        # q^{n_half} corresponds to norm_sq = 2*n_half
        expected[2 * n_half] = 240 * sigma3(n_half)

    results = {
        'computed': inst,
        'expected_E4': expected,
        'match': True,
    }
    for ns, count in expected.items():
        if ns in inst:
            if inst[ns] != count:
                results['match'] = False
                results[f'mismatch_at_{ns}'] = f'computed={inst.get(ns, 0)}, expected={count}'
        else:
            if count != 0:
                results['match'] = False
                results[f'missing_{ns}'] = f'expected={count}'

    return results


