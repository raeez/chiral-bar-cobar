def legendre_symbol(a: int, p: int) -> int:
    """Compute the Legendre symbol (a/p) for odd prime p.

    (a/p) = a^{(p-1)/2} mod p.
    Returns 0 if p | a, 1 if a is QR, -1 if a is QNR.
    """
    if p == 2:
        raise ValueError("Legendre symbol not defined for p=2")
    a = a % p
    if a == 0:
        return 0
    result = pow(a, (p - 1) // 2, p)
    return result if result <= 1 else result - p


def hilbert_symbol_p(a: int, b: int, p: int) -> int:
    r"""Compute the Hilbert symbol (a, b)_p for integers a, b and prime p.

    The Hilbert symbol (a, b)_p = 1 if ax^2 + by^2 = z^2 has a nontrivial
    p-adic solution, and -1 otherwise.

    For p odd, write a = p^alpha * u, b = p^beta * v with gcd(u,p) = gcd(v,p) = 1:
    (a,b)_p = (-1)^{alpha*beta*eps(p)} * (u/p)^beta * (v/p)^alpha
    where eps(p) = (p-1)/2 and (./p) is the Legendre symbol.

    For p = 2:
    (a,b)_2 = (-1)^{eps(u)*eps(v) + alpha*omega(v) + beta*omega(u)}
    where eps(u) = (u-1)/2 mod 2, omega(u) = (u^2-1)/8 mod 2.
    """
    if a == 0 or b == 0:
        return 0

    # Factor out powers of p
    alpha = 0
    u = abs(a)
    while u % p == 0:
        u //= p
        alpha += 1
    if a < 0:
        u = -u

    beta = 0
    v = abs(b)
    while v % p == 0:
        v //= p
        beta += 1
    if b < 0:
        v = -v

    if p == 2:
        # For p = 2, use the explicit formula
        # Normalize u, v to be odd (they should be since we factored out 2)
        u_mod8 = u % 8
        v_mod8 = v % 8
        eps_u = ((u_mod8 - 1) // 2) % 2
        eps_v = ((v_mod8 - 1) // 2) % 2
        omega_u = ((u_mod8 * u_mod8 - 1) // 8) % 2
        omega_v = ((v_mod8 * v_mod8 - 1) // 8) % 2
        exponent = eps_u * eps_v + alpha * omega_v + beta * omega_u
        return (-1) ** (exponent % 2)
    else:
        # For p odd
        eps_p = (p - 1) // 2
        # u, v should be coprime to p (we factored out p-powers)
        u_mod = u % p
        v_mod = v % p
        if u_mod == 0 or v_mod == 0:
            # This shouldn't happen after factoring, but safety check
            return 0

        leg_u = legendre_symbol(u_mod, p)
        leg_v = legendre_symbol(v_mod, p)

        sign1 = (-1) ** ((alpha * beta * eps_p) % 2)
        sign2 = leg_u ** beta if leg_u != 0 else 0
        sign3 = leg_v ** alpha if leg_v != 0 else 0

        if sign2 == 0 or sign3 == 0:
            return 0
        return sign1 * sign2 * sign3


def hilbert_symbol_rational(a: Fraction, b: Fraction, p: int) -> int:
    """Hilbert symbol for rational numbers at prime p.

    Write a = p^alpha * (m/n) with gcd(mn, p) = 1, similarly for b.
    Then compute using the integer Hilbert symbol of numerators/denominators.
    """
    # Convert to integer problem: a = num_a/den_a, b = num_b/den_b
    # (a, b)_p = (num_a * den_b, num_b * den_a)_p * (den_a, den_b)_p^{-1}
    # More precisely: (a, b)_p = (num_a, num_b)_p * (num_a, den_b)_p^{-1}
    #                            * (den_a, num_b)_p^{-1} * (den_a, den_b)_p
    # Using bilinearity.
    na, da = int(a.numerator), int(a.denominator)
    nb, db = int(b.numerator), int(b.denominator)

    # By bilinearity of Hilbert symbol:
    # (a/c, b/d)_p = (a,b)_p * (a,d)_p^{-1} * (c,b)_p^{-1} * (c,d)_p
    # Since Hilbert symbol is +/-1, inverse = itself.
    h_ab = hilbert_symbol_p(na, nb, p)
    h_ad = hilbert_symbol_p(na, db, p)
    h_cb = hilbert_symbol_p(da, nb, p)
    h_cd = hilbert_symbol_p(da, db, p)

    return h_ab * h_ad * h_cb * h_cd


def hilbert_symbol_float(a: float, b: float, p: int,
                         denom_bound: int = 10000) -> int:
    """Hilbert symbol for float approximations, via rational approximation.

    Approximate a, b as rationals with bounded denominator, then compute.
    """
    fa = Fraction(a).limit_denominator(denom_bound)
    fb = Fraction(b).limit_denominator(denom_bound)
    if fa == 0 or fb == 0:
        return 0
    return hilbert_symbol_rational(fa, fb, p)


