def c_from_zeta_zero(n: int) -> float:
    """Map zeta zero rho_n to a central charge value.

    Convention: c(rho_n) = rho_n (the imaginary part of the n-th zero).
    This is an ANSATZ connecting zeta zeros to the modular parameter space.
    The mapping is not canonical; we use the simplest identification.
    """
    return zeta_zero(n)


def norm_residue_at_zero(n: int, primes: Optional[List[int]] = None) -> Dict[str, Any]:
    """Compute norm residue data at the n-th zeta zero.

    For rho_n, compute:
    - c(rho_n) and kappa(c(rho_n)) = c(rho_n)/2
    - Delta(c(rho_n)) = critical discriminant
    - Hilbert symbols (kappa, Delta)_p for various p
    - Tame symbols at various p
    """
    if primes is None:
        primes = [2, 3, 5, 7, 11]

    rho = zeta_zero(n)
    c = c_from_zeta_zero(n)
    kap = kappa_virasoro(c)
    delta = shadow_discriminant_virasoro(c)
    s4 = shadow_S4_virasoro(c)

    # Rational approximations for Hilbert symbol computation
    kap_frac = Fraction(kap).limit_denominator(100000)
    delta_frac = Fraction(delta).limit_denominator(100000)
    s4_frac = Fraction(s4).limit_denominator(100000)

    hilbert_kappa_delta = {}
    hilbert_kappa_s4 = {}
    tame_kappa_delta = {}

    for p in primes:
        try:
            hilbert_kappa_delta[p] = hilbert_symbol_rational(kap_frac, delta_frac, p)
        except (ValueError, ZeroDivisionError):
            hilbert_kappa_delta[p] = None

        try:
            hilbert_kappa_s4[p] = hilbert_symbol_rational(kap_frac, s4_frac, p)
        except (ValueError, ZeroDivisionError):
            hilbert_kappa_s4[p] = None

        if p <= 7:
            try:
                tame_kappa_delta[p] = tame_symbol(kap_frac, delta_frac, p)
            except (ValueError, ZeroDivisionError, OverflowError):
                tame_kappa_delta[p] = None

    return {
        'zero_index': n,
        'rho_n': rho,
        'c': c,
        'kappa': kap,
        'kappa_frac': kap_frac,
        'S_4': s4,
        'Delta': delta,
        'delta_frac': delta_frac,
        'hilbert_kappa_delta': hilbert_kappa_delta,
        'hilbert_kappa_s4': hilbert_kappa_s4,
        'tame_kappa_delta': tame_kappa_delta,
    }


def norm_residue_sign_changes(n_zeros: int = 20,
                               primes: Optional[List[int]] = None) -> Dict[str, Any]:
    """Test whether Hilbert symbols detect zero-crossing (sign changes at rho_n).

    For consecutive zeros rho_n, rho_{n+1}, compute the Hilbert symbol
    (kappa(c(rho_n)), Delta(c(rho_n)))_p and check for sign changes.

    A sign change at p would indicate that the local norm residue
    structure changes as we cross a zeta zero.
    """
    if primes is None:
        primes = [2, 3, 5, 7, 11]

    data = []
    for i in range(1, min(n_zeros + 1, len(RIEMANN_ZETA_ZEROS) + 1)):
        data.append(norm_residue_at_zero(i, primes))

    sign_changes: Dict[int, List[int]] = {p: [] for p in primes}
    for i in range(len(data) - 1):
        for p in primes:
            h1 = data[i]['hilbert_kappa_delta'].get(p)
            h2 = data[i + 1]['hilbert_kappa_delta'].get(p)
            if h1 is not None and h2 is not None and h1 != h2:
                sign_changes[p].append(i + 1)

    return {
        'n_zeros': len(data),
        'primes': primes,
        'data': data,
        'sign_changes': sign_changes,
        'any_sign_change': any(len(v) > 0 for v in sign_changes.values()),
    }


