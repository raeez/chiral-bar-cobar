# Section 13: Cross-family consistency checks
# =====================================================================

def cross_family_kappa_additivity_check() -> Dict[str, Any]:
    """Verify kappa additivity for independent sums.

    For A = A_1 oplus A_2 with vanishing mixed OPE:
    kappa(A) = kappa(A_1) + kappa(A_2).

    Test: Heisenberg(k1) oplus Heisenberg(k2) has kappa = k1 + k2.
    Test: sl_2(k) as Heisenberg(level) plus correction.
    """
    results = {}

    # Test 1: Heisenberg additivity
    for k1, k2 in [(1, 2), (3, 5), (7, 11)]:
        kap1 = kappa_heisenberg(k1)
        kap2 = kappa_heisenberg(k2)
        kap_sum = kap1 + kap2
        results[f'Heis({k1})+Heis({k2})'] = {
            'kappa_1': kap1,
            'kappa_2': kap2,
            'kappa_sum': kap_sum,
            'expected': k1 + k2,
            'match': kap_sum == k1 + k2,
        }

    # Test 2: Virasoro complementarity sum
    for c in [Fraction(1), Fraction(10), Fraction(13), Fraction(25)]:
        kap = kappa_virasoro(c)
        kap_dual = kappa_virasoro_dual(c)
        s = kap + kap_dual
        results[f'Vir({c})+Vir({26 - c})'] = {
            'kappa': kap,
            'kappa_dual': kap_dual,
            'sum': s,
            'expected': Fraction(13),
            'match': s == Fraction(13),  # AP24
        }

    # Test 3: Affine sl_N complementarity (kappa + kappa' = 0)
    for N in [2, 3, 4]:
        for k in [Fraction(1), Fraction(3)]:
            kap = kappa_affine_slN(N, k)
            kap_dual = kappa_affine_slN_dual(N, k)
            s = kap + kap_dual
            results[f'sl_{N}(k={k})'] = {
                'kappa': kap,
                'kappa_dual': kap_dual,
                'sum': s,
                'expected': Fraction(0),
                'match': s == Fraction(0),  # AP24
            }

    return results
