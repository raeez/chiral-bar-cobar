# Section 10: Hilbert reciprocity verification
# =====================================================================

def hilbert_reciprocity_check(a: Fraction, b: Fraction,
                               primes: Optional[List[int]] = None) -> Dict[str, Any]:
    """Verify Hilbert reciprocity: prod_{v} (a, b)_v = 1.

    The product runs over ALL places v of Q, including v = infinity.
    (a, b)_infty = 1 if a > 0 or b > 0, -1 if a < 0 and b < 0.

    For finite primes, only finitely many (a,b)_p != 1.
    """
    if primes is None:
        # Include all primes dividing numerators/denominators of a, b, and 2
        primes_set = {2}
        for x in [a, b]:
            n = abs(int(x.numerator))
            d = abs(int(x.denominator))
            for val in [n, d]:
                if val <= 1:
                    continue
                # Trial division for small factors
                temp = val
                for p_try in range(2, min(val + 1, 1000)):
                    if p_try * p_try > temp:
                        if temp > 1:
                            primes_set.add(temp)
                        break
                    while temp % p_try == 0:
                        primes_set.add(p_try)
                        temp //= p_try
        primes = sorted(primes_set)

    # Archimedean place
    h_inf = 1
    if float(a) < 0 and float(b) < 0:
        h_inf = -1

    finite_symbols = {}
    product = h_inf
    for p in primes:
        h = hilbert_symbol_rational(a, b, p)
        finite_symbols[p] = h
        product *= h

    return {
        'a': a,
        'b': b,
        'h_infinity': h_inf,
        'finite_symbols': finite_symbols,
        'product': product,
        'reciprocity_holds': (product == 1),
    }


# =====================================================================
# Section 11: Multi-path verification framework
# =====================================================================

def verify_symbol_multipath(a: Fraction, b: Fraction, p: int) -> Dict[str, Any]:
    """Multi-path verification of the Hilbert symbol (a, b)_p.

    Path 1: Direct computation via Legendre symbols
    Path 2: Steinberg identity check (if applicable)
    Path 3: Tame symbol at p
    Path 4: Hilbert reciprocity (product formula)
    Path 5: Numerical evaluation via norm equation
    """
    results: Dict[str, Any] = {
        'a': a, 'b': b, 'p': p,
    }

    # Path 1: Direct Hilbert symbol
    try:
        h_direct = hilbert_symbol_rational(a, b, p)
        results['path1_direct'] = h_direct
    except Exception as e:
        results['path1_direct'] = f'error: {e}'

    # Path 2: If b = 1-a, Steinberg says {a, 1-a} = 0 in K^M_2,
    # but this does NOT mean (a, 1-a)_p = 1 for all p.
    # The Steinberg relation is about the GLOBAL class, not local symbols.
    is_steinberg_pair = (a + b == Fraction(1))
    results['path2_steinberg_pair'] = is_steinberg_pair

    # Path 3: Tame symbol at p
    try:
        t = tame_symbol(a, b, p)
        results['path3_tame'] = t
    except Exception as e:
        results['path3_tame'] = f'error: {e}'

    # Path 4: Hilbert reciprocity
    try:
        rec = hilbert_reciprocity_check(a, b)
        results['path4_reciprocity'] = rec['reciprocity_holds']
    except Exception as e:
        results['path4_reciprocity'] = f'error: {e}'

    # Path 5: Numerical check - does ax^2 + by^2 = 1 have small integer solution?
    found_solution = False
    if p <= 100:
        af, bf = float(a), float(b)
        for x in range(p):
            for y in range(p):
                if x == 0 and y == 0:
                    continue
                val = (int(a.numerator) * x * x * int(a.denominator)
                       + int(b.numerator) * y * y * int(b.denominator))
                # Check mod p
                # Actually: ax^2 + by^2 = z^2 mod p
                for z in range(p):
                    lhs = (int(a.numerator) * pow(int(a.denominator), p - 2, p) * x * x
                           + int(b.numerator) * pow(int(b.denominator), p - 2, p) * y * y) % p
                    rhs = (z * z) % p
                    if lhs == rhs and (x != 0 or y != 0 or z != 0):
                        found_solution = True
                        break
                if found_solution:
                    break
            if found_solution:
                break
    results['path5_numerical_solution'] = found_solution

    # Consistency check
    if isinstance(results.get('path1_direct'), int):
        if results['path1_direct'] == 1:
            results['consistent'] = found_solution or p > 100
        else:
            results['consistent'] = not found_solution or p > 100
    else:
        results['consistent'] = None

    return results


# =====================================================================
# Section 12: Full shadow Milnor K-theory report
# =====================================================================

def full_shadow_ktheory_report(family: str = 'Virasoro',
                                c: Optional[float] = None,
                                N: Optional[int] = None,
                                k: Optional[Union[int, float, Fraction]] = None,
                                n_zeros: int = 10) -> Dict[str, Any]:
    """Generate a comprehensive Milnor K-theory report for a shadow field.

    Includes:
    - K^M_0, K^M_1, K^M_2 generators and relations
    - Steinberg locus analysis
    - Complementarity symbols
    - Hilbert symbols at small primes
    - Tame symbols
    - Norm residue at zeta zeros (for Virasoro)
    """
    report: Dict[str, Any] = {'family': family}

    if family == 'Heisenberg':
        if k is None:
            k = Fraction(1)
        kg = shadow_kgroup_heisenberg(k)
        report['kgroup'] = {
            'K0': kg.km0(),
            'K1_generators': kg.km1_generators(),
            'K2_symbols': [str(s) for s in kg.km2_symbols()],
            'K2_steinberg': kg.km2_steinberg_check(),
        }
        csym = kg.complementarity_symbol()
        if csym:
            report['complementarity'] = {
                'symbol': str(csym),
                'is_zero': csym.entries[0] + csym.entries[1] == 0,
            }

    elif family == 'Virasoro':
        if c is None:
            c = 26.0  # Anomaly cancellation point
        kg = shadow_kgroup_virasoro(c)
        report['kgroup'] = {
            'K0': kg.km0(),
            'K1_generators': kg.km1_generators(),
            'K2_symbols': [str(s) for s in kg.km2_symbols()],
            'K2_steinberg': kg.km2_steinberg_check(),
        }
        report['complementarity'] = complementarity_km2_virasoro(c)
        report['steinberg_locus'] = steinberg_locus_check(kappa_virasoro(c))

        # Norm residue at zeta zeros
        zero_data = []
        for i in range(1, min(n_zeros + 1, len(RIEMANN_ZETA_ZEROS) + 1)):
            zero_data.append(norm_residue_at_zero(i))
        report['norm_residue_at_zeros'] = zero_data

    elif family.startswith('Affine'):
        if N is None:
            N = 2
        if k is None:
            k = Fraction(1)
        kg = shadow_kgroup_affine_slN(N, k)
        report['kgroup'] = {
            'K0': kg.km0(),
            'K1_generators': kg.km1_generators(),
            'K2_symbols': [str(s) for s in kg.km2_symbols()],
            'K2_steinberg': kg.km2_steinberg_check(),
        }
        report['complementarity'] = complementarity_km2_affine(N, k)

    return report


