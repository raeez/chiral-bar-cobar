# Section 9: Complementarity symbol analysis
# =====================================================================

def complementarity_km2_virasoro(c: Union[float, Fraction]) -> Dict[str, Any]:
    """Analyze the complementarity symbol {kappa, kappa'} for Virasoro.

    kappa = c/2, kappa' = (26-c)/2. Sum = 13 (AP24).
    Symbol {kappa, kappa'} = {c/2, (26-c)/2} in K^M_2.

    At c = 13 (self-dual): kappa = kappa' = 13/2.
    {13/2, 13/2} = {13/2, -13/2} + {13/2, -1}  (by bilinearity)
                  = 0 + {13/2, -1}  (since {a, -a} = 0)
    So the self-dual complementarity symbol reduces to {13/2, -1}.
    """
    if isinstance(c, Fraction):
        kap = c / Fraction(2)
        kap_dual = (Fraction(26) - c) / Fraction(2)
    else:
        c_frac = Fraction(c).limit_denominator(100000)
        kap = c_frac / Fraction(2)
        kap_dual = (Fraction(26) - c_frac) / Fraction(2)

    result: Dict[str, Any] = {
        'c': c,
        'kappa': kap,
        'kappa_dual': kap_dual,
        'sum': kap + kap_dual,
        'symbol': milnor_symbol(kap, kap_dual),
    }

    # Check special structure
    is_self_dual = (kap == kap_dual)
    result['is_self_dual'] = is_self_dual

    # Hilbert symbols at small primes
    if kap != 0 and kap_dual != 0:
        hilbert_data = {}
        for p in [2, 3, 5, 7, 11, 13]:
            try:
                h = hilbert_symbol_rational(kap, kap_dual, p)
                hilbert_data[p] = h
            except (ValueError, ZeroDivisionError):
                hilbert_data[p] = None
        result['hilbert_symbols'] = hilbert_data

    return result


def complementarity_km2_affine(N: int, k: Union[int, Fraction]) -> Dict[str, Any]:
    """Analyze the complementarity symbol {kappa, kappa'} for affine sl_N.

    kappa + kappa' = 0 for KM (AP24).
    So {kappa, kappa'} = {kappa, -kappa} = 0 in K^M_2.
    """
    if isinstance(k, int):
        k = Fraction(k)
    kap = kappa_affine_slN(N, k)
    kap_dual = kappa_affine_slN_dual(N, k)

    return {
        'N': N,
        'k': k,
        'kappa': kap,
        'kappa_dual': kap_dual,
        'sum': kap + kap_dual,
        'sum_is_zero': (kap + kap_dual == 0),
        'symbol_is_zero': True,  # {a, -a} = 0 always
    }


