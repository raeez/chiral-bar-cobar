def mod_p_km2(a: Fraction, b: Fraction, p: int) -> int:
    """Image of {a, b} in K^M_2(Q)/p.

    By Bloch-Kato (now a theorem, Voevodsky-Rost):
    K^M_2(Q)/p ~ H^2(Q, mu_p^{otimes 2}).

    For Q, the mod-p K^M_2 is computed via the Hilbert symbol:
    the image of {a,b} in K^M_2(Q)/p is detected by the collection
    of Hilbert symbols (a,b)_q for all primes q (including q = infty).

    We return the Hilbert symbol product over a finite set of primes
    (which determines the class by Hilbert reciprocity).
    """
    return hilbert_symbol_rational(a, b, p)


def galois_h2_mod_p(a: Fraction, b: Fraction, p: int,
                     test_primes: Optional[List[int]] = None) -> Dict[str, Any]:
    """Compute the mod-p Galois cohomology class via Bloch-Kato.

    K^M_2(Q)/p ~ H^2_Gal(Q, Z/p(2)).

    Returns the Hilbert symbol at each test prime, plus the product
    (which should be 1 by Hilbert reciprocity for the full set).
    """
    if test_primes is None:
        test_primes = [2, 3, 5, 7, 11, 13]

    symbols = {}
    product = 1
    for q in test_primes:
        h = hilbert_symbol_rational(a, b, q)
        symbols[q] = h
        product *= h

    return {
        'symbol_a': a,
        'symbol_b': b,
        'prime': p,
        'hilbert_symbols': symbols,
        'product': product,
        'bloch_kato_class': mod_p_km2(a, b, p),
    }


