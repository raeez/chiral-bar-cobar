def p_valuation(n: int, p: int) -> int:
    """p-adic valuation v_p(n) for nonzero integer n."""
    if n == 0:
        return float('inf')
    n = abs(n)
    v = 0
    while n % p == 0:
        n //= p
        v += 1
    return v


def p_valuation_rational(r: Fraction, p: int) -> int:
    """p-adic valuation of a rational number."""
    return p_valuation(int(r.numerator), p) - p_valuation(int(r.denominator), p)


def tame_symbol(a: Fraction, b: Fraction, p: int) -> Fraction:
    r"""Tame symbol partial_p: K^M_2(Q) -> K^M_1(F_p) = F_p^*.

    partial_p({a, b}) = (-1)^{v(a)*v(b)} * a^{v(b)} / b^{v(a)}  mod p

    where v = v_p is the p-adic valuation.

    Returns the result as a rational number (the representative in Z
    coprime to p, taken mod p and lifted to {1, ..., p-1}).
    """
    va = p_valuation_rational(a, p)
    vb = p_valuation_rational(b, p)

    # Compute (-1)^{va*vb}
    sign = (-1) ** (va * vb)

    # Compute a^{vb} * b^{-va} as a rational
    # a^{vb}: if vb >= 0, raise to power; if vb < 0, invert first
    if vb >= 0:
        num_part = a ** vb
    else:
        num_part = (Fraction(1) / a) ** (-vb)

    if va >= 0:
        den_part = b ** va
    else:
        den_part = (Fraction(1) / b) ** (-va)

    result = Fraction(sign) * num_part / den_part

    # Reduce mod p: extract the p-adic unit part
    result_num = int(result.numerator)
    result_den = int(result.denominator)

    # Remove p-factors (should cancel but be safe)
    while result_num % p == 0:
        result_num //= p
    while result_den % p == 0:
        result_den //= p

    # Compute mod p
    result_mod = (result_num * pow(result_den, p - 2, p)) % p
    if result_mod == 0:
        result_mod = p  # shouldn't happen for tame symbol of units
    return Fraction(result_mod)


