class ShadowKGroup:
    """Milnor K-group data for the shadow field of a chiral algebra.

    For each family, we track:
    - K^M_0 = Z
    - K^M_1 = F_A^* (units of the shadow field)
    - K^M_2 = symbols {a, b} / (bilinear + Steinberg)
    - K^M_3, K^M_4 = higher symbols

    Generators of K^M_1 are the nonzero shadow parameters.
    Generators of K^M_2 are symbols of pairs of shadow parameters.
    """

    def __init__(self, family: str, params: Dict[str, Union[float, Fraction]]):
        self.family = family
        self.params = params

    def km0(self) -> str:
        """K^M_0 = Z for any field."""
        return "Z"

    def km1_generators(self) -> List[str]:
        """Generators of K^M_1 = F_A^* (the nonzero shadow parameters)."""
        return [name for name, val in self.params.items()
                if val != 0 and val != Fraction(0)]

    def km2_symbols(self) -> List[MilnorSymbol]:
        """Representative symbols in K^M_2.

        For each pair of nonzero shadow parameters, form {a, b}.
        """
        gens = [(name, val) for name, val in self.params.items()
                if val != 0 and val != Fraction(0)]
        symbols = []
        for i in range(len(gens)):
            for j in range(i + 1, len(gens)):
                symbols.append(milnor_symbol(gens[i][1], gens[j][1]))
        return symbols

    def km2_steinberg_check(self) -> List[Dict[str, Any]]:
        """Check Steinberg relations for pairs of shadow parameters.

        For each nonzero parameter a, form {a, 1-a} and record its status.
        """
        results = []
        for name, val in self.params.items():
            if val == 0 or val == Fraction(0):
                continue
            sym = steinberg_symbol(val)
            results.append({
                'parameter': name,
                'value': val,
                'symbol': sym,
                'is_steinberg': sym.is_zero_by_steinberg(),
                'one_minus_val': sym.entries[1],
            })
        return results

    def complementarity_symbol(self) -> Optional[MilnorSymbol]:
        """The complementarity symbol {kappa, kappa'} in K^M_2.

        For KM: kappa + kappa' = 0, so {kappa, kappa'} = {kappa, -kappa} = 0.
        For Virasoro: kappa + kappa' = 13. Symbol {kappa, 13 - kappa}.
        AP24: these are DIFFERENT.
        """
        if 'kappa' in self.params and 'kappa_dual' in self.params:
            k = self.params['kappa']
            kd = self.params['kappa_dual']
            if k != 0 and kd != 0:
                return milnor_symbol(k, kd)
        return None


def shadow_kgroup_heisenberg(k: Union[int, float, Fraction]) -> ShadowKGroup:
    """Shadow K-group data for the Heisenberg VOA at level k.

    F_A = Q(k). Shadow parameters: kappa = k.
    Shadow tower terminates at arity 2 (class G): S_r = 0 for r >= 3.
    """
    kap = kappa_heisenberg(k)
    if isinstance(k, (int, float)):
        k = Fraction(k) if isinstance(k, int) else k
    return ShadowKGroup('Heisenberg', {
        'kappa': kap,
        'kappa_dual': -kap if isinstance(kap, Fraction) else -kap,
        # Heisenberg: kappa + kappa' = 0 (AP24)
    })


def shadow_kgroup_virasoro(c: Union[float, Fraction]) -> ShadowKGroup:
    """Shadow K-group data for the Virasoro algebra at central charge c.

    F_A = Q(c). Shadow parameters: kappa = c/2, S_4 = -12/(5c+22),
    Delta = -48c/(5c+22), Q^contact = 10/[c(5c+22)].
    AP24: kappa + kappa' = 13 (NOT 0).
    AP39: kappa = c/2 is specific to Virasoro.
    """
    kap = kappa_virasoro(c)
    kap_dual = kappa_virasoro_dual(c)
    s4 = shadow_S4_virasoro(c)
    delta = shadow_discriminant_virasoro(c)
    params: Dict[str, Any] = {
        'kappa': kap,
        'kappa_dual': kap_dual,
        'S_4': s4,
        'Delta': delta,
    }
    # Q^contact only defined for c != 0
    if (isinstance(c, Fraction) and c != 0) or (not isinstance(c, Fraction) and abs(c) > 1e-15):
        params['Q_contact'] = Qcontact_virasoro(c)
    return ShadowKGroup('Virasoro', params)


def shadow_kgroup_affine_slN(N: int, k: Union[float, Fraction]) -> ShadowKGroup:
    """Shadow K-group data for affine sl_N at level k.

    F_A = Q(k). kappa = (N^2-1)(k+N)/(2N).
    AP24: kappa + kappa' = 0 for affine KM (FF involution).
    """
    kap = kappa_affine_slN(N, k)
    kap_dual = kappa_affine_slN_dual(N, k)
    return ShadowKGroup(f'Affine sl_{N}', {
        'kappa': kap,
        'kappa_dual': kap_dual,
    })


