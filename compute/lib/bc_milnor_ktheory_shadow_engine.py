def kappa_heisenberg(k: Union[int, float, Fraction]) -> Union[float, Fraction]:
    """kappa(H_k) = k for the rank-1 Heisenberg VOA."""
    return k


def kappa_virasoro(c: Union[float, Fraction]) -> Union[float, Fraction]:
    """kappa(Vir_c) = c/2. AP48: this is specific to Virasoro."""
    if isinstance(c, Fraction):
        return c / Fraction(2)
    return c / 2.0


def kappa_virasoro_dual(c: Union[float, Fraction]) -> Union[float, Fraction]:
    """kappa(Vir_{26-c}) = (26-c)/2. Koszul dual of Virasoro.

    AP24: kappa + kappa' = 13 for Virasoro (NOT 0).
    """
    if isinstance(c, Fraction):
        return (Fraction(26) - c) / Fraction(2)
    return (26.0 - c) / 2.0


def kappa_affine_slN(N: int, k: Union[float, Fraction]) -> Union[float, Fraction]:
    """kappa(V_k(sl_N)) = dim(sl_N) * (k + N) / (2N).

    dim(sl_N) = N^2 - 1. Dual Coxeter number h^v = N.
    AP1: this formula is specific to sl_N. Do not use for other types.
    """
    dim_g = N * N - 1
    if isinstance(k, Fraction):
        return Fraction(dim_g) * (k + Fraction(N)) / (Fraction(2) * Fraction(N))
    return dim_g * (k + N) / (2.0 * N)


def kappa_affine_slN_dual(N: int, k: Union[float, Fraction]) -> Union[float, Fraction]:
    """Koszul dual kappa for sl_N: k -> -k - 2h^v = -k - 2N.

    AP24: kappa + kappa' = 0 for affine KM (Feigin-Frenkel involution).
    AP33: this is the FF involution, NOT the same as Koszul duality.
    """
    return kappa_affine_slN(N, -k - 2 * N if isinstance(k, int) else
                            (Fraction(-1) * k - Fraction(2 * N) if isinstance(k, Fraction)
                             else -k - 2.0 * N))


def shadow_S4_virasoro(c: Union[float, Fraction]) -> Union[float, Fraction]:
    """The arity-4 shadow coefficient S_4 for Virasoro.

    S_4 = -12/(5c + 22). This is the quartic shadow invariant.
    """
    if isinstance(c, Fraction):
        return Fraction(-12) / (Fraction(5) * c + Fraction(22))
    return -12.0 / (5.0 * c + 22.0)


def shadow_discriminant_virasoro(c: Union[float, Fraction]) -> Union[float, Fraction]:
    """Critical discriminant Delta = 8*kappa*S_4 for Virasoro.

    Delta = 8*(c/2)*(-12/(5c+22)) = -48c/(5c+22).

    Delta = 0 iff c = 0 (class G boundary).
    Delta != 0 for generic c: class M (infinite shadow depth).
    """
    kap = kappa_virasoro(c)
    s4 = shadow_S4_virasoro(c)
    if isinstance(c, Fraction):
        return Fraction(8) * kap * s4
    return 8.0 * kap * s4


def Qcontact_virasoro(c: Union[float, Fraction]) -> Union[float, Fraction]:
    """Q^contact_Vir = 10 / [c * (5c + 22)].

    The quartic contact invariant for Virasoro.
    """
    if isinstance(c, Fraction):
        return Fraction(10) / (c * (Fraction(5) * c + Fraction(22)))
    return 10.0 / (c * (5.0 * c + 22.0))


