# Section 7: Steinberg locus analysis
# =====================================================================

def steinberg_locus_check(kappa_val: Union[float, Fraction]) -> Dict[str, Any]:
    """Analyze the Steinberg locus for a given kappa value.

    The Steinberg relation {a, 1-a} = 0 in K^M_2 holds for ALL a != 0, 1.
    This is a THEOREM (axiom of K^M_2), not something that can "fail."

    The mathematical content is:
    1. Whether kappa = 0 or 1 (where the symbol is undefined)
    2. The Hilbert symbol (kappa, 1-kappa)_p at various primes p
       (the local obstruction to representing the norm form)
    3. The tame symbols at various primes
    """
    is_zero = (abs(float(kappa_val)) < 1e-15)
    is_one = (abs(float(kappa_val) - 1.0) < 1e-15)

    result: Dict[str, Any] = {
        'kappa': kappa_val,
        'is_excluded': is_zero or is_one,
        'steinberg_holds': not is_zero and not is_one,
        # Steinberg {a, 1-a} = 0 holds in K^M_2 for a != 0, 1.
        # This is an axiom, always true.
    }

    if not result['is_excluded']:
        kf = (kappa_val if isinstance(kappa_val, Fraction)
              else Fraction(kappa_val).limit_denominator(100000))
        one_minus_kf = Fraction(1) - kf

        if one_minus_kf != 0:
            # Hilbert symbols of {kappa, 1-kappa} at small primes
            hilbert_data = {}
            for p in [2, 3, 5, 7, 11]:
                try:
                    h = hilbert_symbol_rational(kf, one_minus_kf, p)
                    hilbert_data[p] = h
                except (ValueError, ZeroDivisionError):
                    hilbert_data[p] = None
            result['hilbert_symbols'] = hilbert_data

            # Tame symbols at small primes
            tame_data = {}
            for p in [2, 3, 5, 7]:
                try:
                    t = tame_symbol(kf, one_minus_kf, p)
                    tame_data[p] = t
                except (ValueError, ZeroDivisionError, OverflowError):
                    tame_data[p] = None
            result['tame_symbols'] = tame_data

    return result


def steinberg_locus_virasoro(c_values: Optional[List[float]] = None) -> List[Dict[str, Any]]:
    """Map out the Steinberg locus for Virasoro shadow field.

    For Virasoro: kappa = c/2. The Steinberg symbol {c/2, 1 - c/2} = 0
    in K^M_2 for c != 0, 2 (i.e., kappa != 0, 1).

    At c = 0: kappa = 0 (excluded, class G boundary).
    At c = 2: kappa = 1 (excluded point).
    At c = 13: kappa = 13/2, self-dual point.
    At c = 26: kappa = 13, anomaly cancellation point.
    """
    if c_values is None:
        c_values = [0.5, 1.0, 2.0, 4.0, 6.0, 10.0, 13.0, 22.0, 26.0, 52.0]

    results = []
    for c in c_values:
        kap = kappa_virasoro(c)
        data = steinberg_locus_check(kap)
        data['c'] = c
        data['family'] = 'Virasoro'
        results.append(data)
    return results


