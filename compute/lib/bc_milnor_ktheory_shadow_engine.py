class MilnorSymbol:
    """A symbol {a_1, ..., a_n} in K^M_n(F).

    Represented as a list of nonzero field elements.
    The Steinberg relation {a, 1-a} = 0 is a relation in K^M_2.
    Bilinearity: {ab, c} = {a,c} + {b,c} and {a, bc} = {a,b} + {a,c}.
    Antisymmetry: {a, b} = -{b, a} in K^M_2.
    """

    def __init__(self, entries: List[Union[float, Fraction, complex]]):
        self.entries = list(entries)
        self.degree = len(entries)

    def __repr__(self) -> str:
        parts = ", ".join(str(e) for e in self.entries)
        return f"{{{parts}}}"

    def is_steinberg(self, tol: float = 1e-12) -> bool:
        """Check if this symbol satisfies a Steinberg relation.

        A degree-2 symbol {a, b} is Steinberg if a + b = 1 (i.e. b = 1-a).
        More generally, a symbol contains a Steinberg pair if some consecutive
        pair (a_i, a_{i+1}) satisfies a_i + a_{i+1} = 1.

        In K^M_2: {a, 1-a} = 0 for ALL a != 0, 1.
        This is an axiom of Milnor K-theory, not something to verify.
        The content is whether we can REDUCE a given symbol to Steinberg form.
        """
        if self.degree < 2:
            return False
        for i in range(self.degree - 1):
            a, b = self.entries[i], self.entries[i + 1]
            if isinstance(a, complex) or isinstance(b, complex):
                if abs(a + b - 1) < tol:
                    return True
            elif isinstance(a, Fraction) and isinstance(b, Fraction):
                if a + b == Fraction(1):
                    return True
            else:
                if abs(float(a) + float(b) - 1.0) < tol:
                    return True
        return False

    def is_zero_by_steinberg(self, tol: float = 1e-12) -> bool:
        """Check if this degree-2 symbol is zero in K^M_2 by Steinberg.

        {a, 1-a} = 0 for a != 0, 1.
        Also {a, -a} = 0 (from {a, 1-a} with a -> -a and bilinearity).
        """
        if self.degree != 2:
            return False
        a, b = self.entries
        # Check {a, 1-a} form
        if isinstance(a, Fraction) and isinstance(b, Fraction):
            if a + b == Fraction(1) and a != 0 and a != 1:
                return True
            # Check {a, -a} form
            if a + b == Fraction(0) and a != 0:
                return True
        else:
            af, bf = float(a), float(b)
            if abs(af + bf - 1.0) < tol and abs(af) > tol and abs(af - 1.0) > tol:
                return True
            if abs(af + bf) < tol and abs(af) > tol:
                return True
        return False


def milnor_symbol(*args) -> MilnorSymbol:
    """Create a Milnor K-theory symbol {a_1, ..., a_n}."""
    return MilnorSymbol(list(args))


def steinberg_symbol(a: Union[float, Fraction]) -> MilnorSymbol:
    """Create the canonical Steinberg symbol {a, 1-a} in K^M_2."""
    if isinstance(a, Fraction):
        return MilnorSymbol([a, Fraction(1) - a])
    return MilnorSymbol([a, 1.0 - a])


