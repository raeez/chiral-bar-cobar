def multichannel_genus2_cross_channel(
    kappas: Dict[str, Fraction],
    metric: Dict[Tuple[str, str], Fraction],
) -> Dict[str, object]:
    """Analyze cross-channel contributions at genus 2.

    For a multi-generator algebra with channels {s_1, ..., s_r},
    the genus-2 graph sum involves:

    DIAGONAL TERMS (channel s with itself):
    - Theta_ss: both vertices in channel s
    - Sunset_ss: vertex in channel s, both loops in channel s
    - Fig8_ss: genus-1 vertex in channel s, self-loop in channel s

    CROSS-CHANNEL TERMS (channels s != t):
    - Theta_st: one vertex in channel s, other in channel t
    - Sunset_st: vertex has mixed legs (some in s, some in t)
    - Fig8: no cross-channel contribution (single vertex)

    The propagator between channels s, t is eta^{-1}_{st} where
    eta_{st} = metric(s, t) is the Zamolodchikov metric.

    For ORTHOGONAL channels (eta_{st} = 0 for s != t):
    - Cross-channel terms vanish for theta and sunset
    - F_2 = sum_s F_2^{(s)} = sum_s kappa_s * lambda_2^FP
    - Universality holds trivially (additive in channels)

    For NON-ORTHOGONAL channels (eta_{st} != 0 for some s != t):
    - Cross-channel terms may contribute
    - Universality is not immediate

    Key question: Are the channels orthogonal for standard landscape algebras?
    For W_3: eta_{TW} = 0 (T and W are quasi-primary of different spin,
    so the 2-point function <T|W> = 0 by spin selection).
    For general W_N: all generators have distinct spin, so eta_{ij} = 0
    for i != j.

    CONCLUSION: For W-algebras (standard landscape), channels are orthogonal.
    Cross-channel terms vanish at genus 2.  Universality at genus 2 then
    follows from single-channel universality for each spin sector.

    THE REMAINING SUBTLETY: Even with orthogonal channels, the genus-2
    amplitude for channel s depends on kappa_s AND the shadow data of
    channel s (alpha_s, S4_s, ...).  The question is whether the total
    F_2 = sum_s F_2^{(s)} where each F_2^{(s)} = kappa_s * lambda_2^FP.
    This is the SINGLE-CHANNEL universality question applied to each sector.

    Parameters:
        kappas: {channel_name: kappa_value}
        metric: {(s, t): eta_{st}} (Zamolodchikov metric)
    """
    channels = sorted(kappas.keys())
    r = len(channels)
    kappa_total = sum(kappas.values())

    # Check orthogonality
    is_orthogonal = True
    for i, s in enumerate(channels):
        for j, t in enumerate(channels):
            if i < j:
                if metric.get((s, t), Fraction(0)) != 0:
                    is_orthogonal = False
                    break
        if not is_orthogonal:
            break

    # For orthogonal channels: F_2 = sum_s kappa_s * lambda_2^FP = kappa_total * lambda_2^FP
    F_2_if_orthogonal = kappa_total * _lambda_fp_exact(2)

    return {
        "num_channels": r,
        "channels": channels,
        "kappas": kappas,
        "kappa_total": kappa_total,
        "is_orthogonal": is_orthogonal,
        "F_2_conjectural": F_2_if_orthogonal,
        "lambda_2": _lambda_fp_exact(2),
        "cross_channel_analysis": {
            "theta_cross": "vanishes if orthogonal",
            "sunset_cross": "vanishes if orthogonal",
            "fig8_cross": "always diagonal (single vertex)",
        },
        "universality_for_orthogonal": (
            "If channels are orthogonal, F_2 decomposes as "
            "sum_s F_2^(s) where each F_2^(s) is a single-channel amplitude. "
            "Single-channel universality (proved for uniform weight) then gives "
            "F_2 = kappa_total * lambda_2^FP."
        ),
        "w_algebra_channels_orthogonal": (
            "For W_N algebras, the generators T = W_2, W_3, ..., W_N have "
            "distinct spin. The Zamolodchikov metric is diagonal: "
            "eta_{ij} = 0 for i != j (spin selection rule). "
            "So cross-channel terms vanish."
        ),
    }


