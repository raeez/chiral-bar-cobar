def free_fcom_dimension_genus2(dim_g: int, kappa_nonzero: bool = True
                               ) -> Dict[str, object]:
    """Compute the dimension of Free_FCom(B^{(0,*)})(2,0).

    The free FCom-algebra generated by the genus-0 bar data B^{(0,n)}
    at genus 2, arity 0 is:

        Free_FCom(B^{(0,*)})(2,0) = bigoplus_Gamma bigotimes_v B^{(0, n_v)}

    summed over stable graphs Gamma of genus 2 with 0 legs, where
    each vertex v has genus g_v and valence n_v, and we use ONLY
    genus-0 vertex data B^{(0, n_v)}.

    For the FULL FCom image (including genus-1 vertices generated from
    genus-0 data), we also include the figure-eight contribution.

    Parameters:
        dim_g: dimension of the Lie algebra (= number of generators)
        kappa_nonzero: whether kappa != 0 (the uncurved case is degenerate)

    Returns:
        Dimensions and composition analysis.
    """
    d = dim_g

    # Theta graph: two genus-0 vertices of valence 3, connected by 3 edges.
    # Source space: B^{(0,3)} tensor B^{(0,3)} = g^3 tensor g^3.
    # After contracting 3 edges (pairing 3 legs from each vertex):
    # Target: scalar (g^0 = k), one-dimensional.
    # The contraction map: sum over basis elements, triple Killing contraction.
    # Image dimension: at most 1 (target is scalar).
    theta_source_dim = d**3 * d**3
    theta_target_dim = 1  # (2,0) is scalar
    # The image is 1-dimensional if the triple contraction is nonzero.
    # For sl_2 (d=3): the Killing form is nondegenerate, so the contraction
    # produces a nonzero scalar.

    # Sunset graph: one genus-0 vertex of valence 4, with 2 self-loops.
    # Source space: B^{(0,4)} = g^4.
    # After contracting 2 self-loops (each pairs 2 of the 4 legs):
    # Target: scalar.
    # Image: 1-dimensional if the double self-contraction is nonzero.
    sunset_source_dim = d**4
    sunset_target_dim = 1

    # Figure-eight: one genus-1 vertex of valence 2, with 1 self-loop.
    # The genus-1, valence-2 vertex data is obtained FROM genus-0 data:
    #   B^{(1,2)} is obtained from B^{(0,4)} via self-loop contraction
    #   (contracting 2 of the 4 legs of a genus-0 4-valent vertex).
    # Then the figure-eight contracts the remaining 2 legs.
    # Equivalently: this is a double self-contraction of B^{(0,4)},
    # but with the contractions in a DIFFERENT order than the sunset.
    #
    # Key distinction: sunset contracts (leg1, leg2) and (leg3, leg4) as
    # two independent self-loops.  Figure-eight contracts (leg1, leg2) as a
    # self-loop to get genus 1, then contracts (leg3, leg4) as another self-loop.
    # These are the SAME operation (both are double self-contraction of g^4).
    # The difference is topological (graph structure), not algebraic.
    #
    # CORRECTION: The figure-eight has vertex genus 1, valence 2, self-loop.
    # The vertex data B^{(1,2)} is NOT simply a double contraction of B^{(0,4)}.
    # B^{(1,2)} = FCom composition at the genus-1, arity-2 graph, which involves:
    #   genus-1 curvature (= kappa) and the genus-0 propagator.
    # The figure-eight contribution = kappa * (metric contraction at valence 2).
    figure_eight_dim = 1 if kappa_nonzero else 0

    # Smooth genus-2: NO genus-0 data. B^{(2,0)} is the smooth genus-2
    # amplitude, which is NOT in the image of Free_FCom(B^{(0,*)}).
    smooth_dim = 1  # = lambda_2 (Hodge class)

    # Total FCom image dimension at (2,0)
    fcom_image_dim = 3 if kappa_nonzero else 2
    # (theta + sunset + figure_eight)

    # Total B^{(2,0)} dimension
    total_dim = 4  # All four graph contributions

    return {
        "dim_g": d,
        "theta": {
            "source_dim": theta_source_dim,
            "target_dim": theta_target_dim,
            "graph_type": "genus_0_only",
            "description": "Triple contraction of B^(0,3) x B^(0,3)",
        },
        "sunset": {
            "source_dim": sunset_source_dim,
            "target_dim": sunset_target_dim,
            "graph_type": "genus_0_only",
            "description": "Double self-contraction of B^(0,4)",
        },
        "figure_eight": {
            "contribution_dim": figure_eight_dim,
            "graph_type": "genus_0_via_genus_1",
            "description": "Self-contraction of B^(1,2), itself from B^(0,4)",
        },
        "smooth": {
            "contribution_dim": smooth_dim,
            "graph_type": "intrinsic",
            "description": "Smooth genus-2 amplitude = lambda_2",
        },
        "fcom_image_dim": fcom_image_dim,
        "total_dim": total_dim,
        "cokernel_dim": total_dim - fcom_image_dim,
        "is_free": fcom_image_dim == total_dim,
    }


