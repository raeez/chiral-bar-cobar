"""Orlik-Solomon algebra and residue maps for configuration spaces.

The OS algebra OS^*(Conf_n(C)) is the cohomology ring of the
configuration space of n labeled points in C. It is generated by
1-forms eta_{ij} = dlog(z_i - z_j) subject to the OS ideal:
for each circuit (simple cycle) C in K_n, the boundary dC = 0.

Key facts:
- Poincare polynomial: prod_{i=0}^{n-1} (1 + i*t)
- dim OS^k(n) = |s(n, n-k)| (unsigned Stirling numbers of 1st kind)
- dim OS^{n-1}(n) = (n-1)!  (top degree, relevant for bar complex)

The residue map Res_{D_{ij}}: OS^k(n) -> OS^{k-1}(n-1) extracts
the coefficient of eta_{ij} and merges points i,j.

CONVENTIONS:
- Points labeled 1,...,n (1-indexed)
- eta_{ij} = eta_{ji} (dlog(z_i - z_j) = dlog(z_j - z_i))
- Pairs stored as (i,j) with i < j
- Cohomological grading
"""

from __future__ import annotations

import numpy as np
from itertools import combinations, permutations
from typing import Dict, FrozenSet, List, Optional, Tuple
from functools import lru_cache
from math import factorial


Pair = Tuple[int, int]  # (i, j) with i < j


def make_pair(i: int, j: int) -> Pair:
    return (min(i, j), max(i, j))


def _all_pairs(n: int) -> List[Pair]:
    """All edges of K_n in lex order."""
    return [(i, j) for i in range(1, n + 1) for j in range(i + 1, n + 1)]


def _all_monomials(n: int, k: int) -> List[Tuple[Pair, ...]]:
    """All k-fold wedge products of eta_{ij}, sorted lex."""
    return [tuple(combo) for combo in combinations(_all_pairs(n), k)]


def _perm_sign(perm: list) -> int:
    """Sign of a permutation given as a list [perm[0], perm[1], ...]."""
    n = len(perm)
    visited = [False] * n
    sign = 1
    for i in range(n):
        if visited[i]:
            continue
        j = i
        cycle_len = 0
        while not visited[j]:
            visited[j] = True
            j = perm[j]
            cycle_len += 1
        if cycle_len % 2 == 0:
            sign *= -1
    return sign


def wedge_sign(pairs_list: List[Pair], target_order: List[Pair]) -> int:
    """Sign of permutation taking pairs_list to target_order."""
    idx = {p: i for i, p in enumerate(target_order)}
    perm = [idx[p] for p in pairs_list]
    return _perm_sign(perm)


# ---------------------------------------------------------------------------
# Circuit enumeration and OS ideal
# ---------------------------------------------------------------------------

def _find_all_circuits(n: int) -> List[List[Pair]]:
    """Find all circuits (simple cycles) in K_n.

    A circuit is a minimal dependent set in the cycle matroid of K_n,
    i.e., a simple cycle. For K_n, circuits are all simple cycles of
    length 3, 4, ..., n.
    """
    circuits = []
    vertices = list(range(1, n + 1))

    # Enumerate simple cycles of each length
    for length in range(3, n + 1):
        # Choose which vertices participate
        for vertex_subset in combinations(vertices, length):
            # Enumerate distinct cycles on these vertices
            # Fix the smallest vertex, enumerate permutations of the rest
            # A cycle (v0, v1, ..., v_{l-1}) with v0 = min, and we
            # identify (v0,...,v_{l-1}) with its reverse.
            v0 = vertex_subset[0]
            rest = list(vertex_subset[1:])
            seen = set()
            for perm in permutations(rest):
                # The cycle is v0 -> perm[0] -> perm[1] -> ... -> perm[-1] -> v0
                # Canonical form: normalize by choosing smaller of forward/reverse
                cycle_edges = []
                path = [v0] + list(perm)
                for idx in range(length):
                    a = path[idx]
                    b = path[(idx + 1) % length]
                    cycle_edges.append(make_pair(a, b))
                edge_set = frozenset(cycle_edges)
                if edge_set not in seen:
                    seen.add(edge_set)
                    circuits.append(sorted(cycle_edges))

    return circuits


def _circuit_boundary(circuit_edges: List[Pair], all_mons: List[Tuple[Pair, ...]],
                       mon_idx: Dict[Tuple[Pair, ...], int]) -> np.ndarray:
    """Compute boundary of a circuit as a vector in the monomial basis.

    For a circuit C = {e_1, ..., e_l} (edges sorted), the OS relation is:
    partial(e_C) = sum_{k=0}^{l-1} (-1)^k e_1 ^ ... ^ hat{e_k} ^ ... ^ e_l = 0

    This gives a relation in degree l-1.
    """
    l = len(circuit_edges)
    k = l - 1  # degree of the relation
    num_mons = len(all_mons)
    row = np.zeros(num_mons, dtype=float)

    sorted_edges = sorted(circuit_edges)

    for omit in range(l):
        # Omit the omit-th edge (in sorted order)
        remaining = [sorted_edges[i] for i in range(l) if i != omit]
        sorted_remaining = tuple(sorted(remaining))

        # Sign: (-1)^omit from the boundary formula
        sign = (-1) ** omit

        # Additional sign from sorting the remaining edges
        # (they should already be sorted since sorted_edges is sorted
        # and we removed one element)
        # Actually, remaining is already in sorted order since
        # sorted_edges is sorted and we remove one element.

        if sorted_remaining in mon_idx:
            row[mon_idx[sorted_remaining]] += sign

    return row


@lru_cache(maxsize=64)
def _os_relation_data(n: int, k: int) -> Tuple[Tuple, np.ndarray, int]:
    """Compute OS relation matrix for degree k forms on n points.

    Returns (monomials_tuple, null_space_basis, dim) where:
    - monomials_tuple: the k-fold monomials (as tuple for caching)
    - null_space_basis: NOT USED (kept for compat)
    - dim: dimension of OS^k(n)

    The actual basis is computed by _os_basis_data.
    """
    mons = _all_monomials(n, k)
    num_mons = len(mons)

    if num_mons == 0:
        return ((), np.zeros((0, 0)), 0)

    mon_idx = {m: i for i, m in enumerate(mons)}

    # Collect all relations in degree k from circuits of all sizes
    relations = []

    circuits = _find_all_circuits(n)
    for circuit in circuits:
        l = len(circuit)
        if l - 1 != k:
            # This circuit contributes to degree l-1, not k
            # But we also need "circuit boundary × complement" for larger circuits
            # contributing to degree k.
            # Actually NO: we need boundary relations from circuits of size k+1,
            # AND products of smaller-circuit relations with complement forms.
            continue

        # Direct boundary relation
        row = _circuit_boundary(circuit, mons, mon_idx)
        if np.any(np.abs(row) > 1e-10):
            relations.append(row)

    # Also: for circuits of size m+1 < k+1, multiply their degree-m
    # boundary relations by (k-m)-fold complement monomials.
    # This generates the full OS ideal in degree k.
    for circuit in circuits:
        m = len(circuit) - 1  # degree of the boundary relation
        if m >= k or m < 1:
            continue

        complement_degree = k - m

        # Get the boundary of this circuit as a relation in degree m
        m_mons = _all_monomials(n, m)
        m_mon_idx = {mm: i for i, mm in enumerate(m_mons)}
        boundary_vec = _circuit_boundary(circuit, m_mons, m_mon_idx)

        # Multiply by each complement_degree-fold monomial
        circuit_edges = set(circuit)
        complement_pairs = [p for p in _all_pairs(n)]  # all pairs (including circuit edges OK)

        for complement in combinations(complement_pairs, complement_degree):
            complement_set = set(complement)

            # For each nonzero term in the boundary, wedge with complement
            row = np.zeros(num_mons, dtype=float)

            for m_idx, coeff in enumerate(boundary_vec):
                if abs(coeff) < 1e-10:
                    continue

                boundary_mon = m_mons[m_idx]
                boundary_set = set(boundary_mon)

                # Check for overlap with complement
                if boundary_set & complement_set:
                    continue  # wedge product is zero

                # Combine and sort
                combined = list(boundary_mon) + list(complement)
                sorted_combined = tuple(sorted(combined))

                # Sign from sorting
                sign = wedge_sign(combined, list(sorted_combined))

                if sorted_combined in mon_idx:
                    row[mon_idx[sorted_combined]] += coeff * sign

            if np.any(np.abs(row) > 1e-10):
                relations.append(row)

    if not relations:
        return (tuple(mons), np.eye(num_mons), num_mons)

    rel_matrix = np.array(relations, dtype=float)

    # Compute rank via SVD
    if rel_matrix.shape[0] > 0:
        # NOTE: numpy SVD gives approximate rank via floating-point threshold.
        # For the small integer OS relation matrices this is reliable;
        # for exact computation, sympy.Matrix.rank() could be used instead.
        U, S, Vt = np.linalg.svd(rel_matrix, full_matrices=False)
        rank = np.sum(S > 1e-8)
    else:
        rank = 0

    dim = num_mons - rank

    return (tuple(mons), rel_matrix, dim)


@lru_cache(maxsize=64)
def _os_basis_data(n: int, k: int) -> Tuple[List[Tuple[Pair, ...]], np.ndarray]:
    """Compute basis for OS^k(n) as rows of a matrix in monomial coords.

    Returns (monomials, basis_matrix) where basis_matrix has shape
    (dim, num_monomials) and each row is a basis vector.
    """
    mons_tuple, rel_matrix, dim = _os_relation_data(n, k)
    mons = list(mons_tuple)
    num_mons = len(mons)

    if num_mons == 0:
        return [], np.zeros((0, 0))

    if dim == num_mons:
        return mons, np.eye(num_mons)

    if dim == 0:
        return mons, np.zeros((0, num_mons))

    # Find a basis for the quotient space = null space of the relation matrix
    # projected to independent coordinates.
    # More precisely: we want basis vectors for the column space complement
    # of rel_matrix.

    # Use SVD of rel_matrix to find the null space
    U, S, Vt = np.linalg.svd(rel_matrix, full_matrices=True)
    rank = np.sum(S > 1e-8)

    # The null space of rel_matrix is spanned by rows of Vt[rank:]
    # Wait, Vt has shape (min(m,n), n) for full_matrices=False.
    # With full_matrices=True, Vt has shape (n, n).
    # Actually for np.linalg.svd with full_matrices=True:
    # rel_matrix = U @ diag(S) @ Vt
    # U: (m, m), S: (min(m,n),), Vt: (n, n)
    # Null space = rows of Vt[rank:]

    # But rel_matrix might be tall (many relations, fewer monomials)
    # or wide. SVD handles both.

    # Re-compute with proper shapes
    m, nn = rel_matrix.shape
    U, S, Vt = np.linalg.svd(rel_matrix, full_matrices=True)
    rank = np.sum(S > 1e-8)

    null_space = Vt[rank:]  # (nn - rank) × nn

    # Clean up small values
    null_space[np.abs(null_space) < 1e-10] = 0

    return mons, null_space


def os_dimension(n: int, k: int) -> int:
    """Dimension of OS^k(Conf_n(C))."""
    if k < 0 or k >= n:
        return 0 if k != 0 else 1
    if k == 0:
        return 1
    _, _, dim = _os_relation_data(n, k)
    return dim


def os_basis(n: int, k: int) -> Tuple[List[Tuple[Pair, ...]], np.ndarray]:
    """Basis for OS^k(Conf_n) as (monomials, basis_matrix).

    basis_matrix: (dim × num_monomials) array, each row is a basis element
    expressed as coordinates in the monomial basis.
    """
    return _os_basis_data(n, k)


# ---------------------------------------------------------------------------
# Residue maps
# ---------------------------------------------------------------------------

def residue_map(n: int, k: int, i: int, j: int) -> np.ndarray:
    """Compute the residue map Res_{D_{ij}}: OS^k(n) -> OS^{k-1}(n-1).

    When points i and j collide, extract coefficient of eta_{ij}
    and relabel remaining points to {1, ..., n-1}.

    The merged point takes label min(i,j). Points > max(i,j) decrement by 1.

    Returns matrix M of shape (dim_target, dim_source) such that
    M @ v gives the residue image in target basis coordinates.
    """
    if k < 1:
        return np.zeros((1, 1))

    # Get source and target bases
    src_mons, src_basis = os_basis(n, k)
    tgt_mons, tgt_basis = os_basis(n - 1, k - 1)

    num_src_mons = len(src_mons)
    num_tgt_mons = len(tgt_mons)
    dim_src = src_basis.shape[0] if src_basis.size > 0 else 0
    dim_tgt = tgt_basis.shape[0] if tgt_basis.size > 0 else 0

    if dim_src == 0 or dim_tgt == 0:
        return np.zeros((max(dim_tgt, 1), max(dim_src, 1)))

    # Build residue on monomial level
    p_ij = make_pair(i, j)
    i_min, i_max = min(i, j), max(i, j)

    def relabel(pt: int) -> int:
        if pt == i_max:
            return i_min
        elif pt > i_max:
            return pt - 1
        else:
            return pt

    tgt_mon_idx = {m: idx for idx, m in enumerate(tgt_mons)}

    # res_on_mons: num_tgt_mons × num_src_mons
    res_on_mons = np.zeros((num_tgt_mons, num_src_mons))

    for s_idx, s_mon in enumerate(src_mons):
        if p_ij not in s_mon:
            continue

        pos = list(s_mon).index(p_ij)
        sign = (-1) ** pos

        remaining = [p for p in s_mon if p != p_ij]

        relabeled = []
        valid = True
        for (a, b) in remaining:
            ra, rb = relabel(a), relabel(b)
            if ra == rb:
                valid = False
                break
            relabeled.append(make_pair(ra, rb))

        if not valid:
            continue

        sorted_relabeled = tuple(sorted(relabeled))

        if len(relabeled) > 0:
            reorder_sign = wedge_sign(relabeled, list(sorted_relabeled))
        else:
            reorder_sign = 1

        total_sign = sign * reorder_sign

        if sorted_relabeled in tgt_mon_idx:
            t_idx = tgt_mon_idx[sorted_relabeled]
            res_on_mons[t_idx, s_idx] += total_sign

    # Convert from monomial coords to basis coords
    # Source basis vectors: src_basis (dim_src × num_src_mons)
    # Target basis vectors: tgt_basis (dim_tgt × num_tgt_mons)
    #
    # For source basis vector v (row of src_basis), its residue image
    # in monomial coords is res_on_mons @ v.
    # We need to express this in target basis coords.

    # image_in_mons = res_on_mons @ src_basis.T  (num_tgt_mons × dim_src)
    image_in_mons = res_on_mons @ src_basis.T

    # Express in target basis: solve tgt_basis.T @ c = image_in_mons
    # Using least squares since tgt_basis rows span the target space
    # tgt_basis: dim_tgt × num_tgt_mons
    # We need c such that c @ tgt_basis ≈ image_in_mons.T
    # i.e., image_in_mons ≈ tgt_basis.T @ c.T

    # Normal equations: (tgt_basis @ tgt_basis.T) @ result = tgt_basis @ image_in_mons
    gram = tgt_basis @ tgt_basis.T  # dim_tgt × dim_tgt
    rhs = tgt_basis @ image_in_mons  # dim_tgt × dim_src

    try:
        result = np.linalg.solve(gram, rhs)
    except np.linalg.LinAlgError:
        result = np.linalg.lstsq(gram, rhs, rcond=None)[0]

    # Clean up
    result[np.abs(result) < 1e-10] = 0

    return result


def top_degree_residue(n: int, i: int, j: int) -> np.ndarray:
    """Residue map on top-degree forms: OS^{n-1}(n) -> OS^{n-2}(n-1)."""
    return residue_map(n, n - 1, i, j)


# ---------------------------------------------------------------------------
# Verification
# ---------------------------------------------------------------------------

def verify_os_dimensions(max_n: int = 6) -> Dict[str, bool]:
    """Verify OS algebra dimensions against known formulas.

    Poincare polynomial: prod_{i=0}^{n-1}(1 + i*t)
    """
    results = {}

    # Expected Poincare polynomials
    def expected_poincare(n):
        """Return list of coefficients of prod(1+i*t, i=0..n-1)."""
        poly = [1]
        for i in range(n):
            new_poly = [0] * (len(poly) + 1)
            for j, c in enumerate(poly):
                new_poly[j] += c
                new_poly[j + 1] += i * c
            poly = new_poly
        return poly

    for n in range(2, max_n + 1):
        expected = expected_poincare(n)
        for k in range(n):
            dim = os_dimension(n, k)
            exp_dim = expected[k] if k < len(expected) else 0
            results[f"dim OS^{k}({n}) = {exp_dim}"] = (dim == exp_dim)

    # Top degree = (n-1)!
    for n in range(2, max_n + 1):
        dim = os_dimension(n, n - 1)
        results[f"dim OS^{n-1}({n}) = {factorial(n-1)}"] = (dim == factorial(n - 1))

    return results


def verify_residue_maps(max_n: int = 4) -> Dict[str, bool]:
    """Verify residue maps satisfy expected properties."""
    results = {}

    # Res_{12}: OS^1(2) -> OS^0(1) should be 1x1 identity-like
    R = residue_map(2, 1, 1, 2)
    results["Res_12: OS^1(2)->OS^0(1) nonzero"] = np.abs(R).max() > 0.5

    # Sum of residues on OS^2(3): should map to OS^1(2) = 1-dim
    for i, j in [(1, 2), (1, 3), (2, 3)]:
        R = residue_map(3, 2, i, j)
        results[f"Res_{i}{j}: OS^2(3)->OS^1(2) shape"] = R.shape == (
            os_dimension(2, 1), os_dimension(3, 2))

    return results


