def sl2_quantum_cg_coefficients(j1: int, j2: int, k: int) -> List[Tuple[int, int]]:
    r"""Fusion channels from quantum group CG decomposition at root of unity.

    At q = exp(i*pi/(k+2)) (root of unity), the tensor product of
    U_q(sl_2) representations decomposes as:

        V_{j1} tensor V_{j2} = bigoplus_m N_{j1,j2}^m V_m

    with truncated CG rule: m in {|j1-j2|, |j1-j2|+2, ..., min(j1+j2, 2k-j1-j2)}.

    Returns list of (m, N_{j1,j2}^m) pairs.
    """
    channels = []
    m_min = abs(j1 - j2)
    m_max = min(j1 + j2, 2 * k - j1 - j2)
    for m in range(m_min, m_max + 1, 2):
        channels.append((m, 1))
    return channels


def verify_quantum_group_verlinde(k: int, tol: float = 1e-8) -> Dict[str, Any]:
    r"""Cross-verify: quantum group CG rule vs Verlinde formula (Path 3).

    Both should give the same fusion coefficients.
    """
    N_verlinde = sl2_fusion_coefficients(k)
    size = k + 1
    mismatches = []

    for j1 in range(size):
        for j2 in range(size):
            qg_channels = sl2_quantum_cg_coefficients(j1, j2, k)
            qg_dict = dict(qg_channels)
            for m in range(size):
                verlinde_val = round(N_verlinde[j1, j2, m])
                qg_val = qg_dict.get(m, 0)
                if verlinde_val != qg_val:
                    mismatches.append((j1, j2, m, verlinde_val, qg_val))

    return {
        "all_match": len(mismatches) == 0,
        "mismatches": mismatches,
        "total_checked": size ** 3,
    }


