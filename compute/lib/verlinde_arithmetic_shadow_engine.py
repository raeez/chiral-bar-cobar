def verlinde_large_k_growth(j_fixed: int, l_fixed: int, k_values: List[int]) -> List[Dict[str, float]]:
    r"""Growth of fusion coefficients N_{j,l}^m as k varies.

    For large k with fixed j, l:
        N_{j,l}^m -> delta_{m in CG range}

    The fusion rules approach the classical Clebsch-Gordan decomposition
    for SU(2), with the truncation boundary at m = 2k-j-l receding to infinity.
    """
    results = []
    for k in k_values:
        if j_fixed > k or l_fixed > k:
            continue
        N = sl2_fusion_coefficients(k)
        nonzero_channels = []
        for m in range(k + 1):
            val = round(N[j_fixed, l_fixed, m])
            if val > 0:
                nonzero_channels.append((m, val))
        results.append({
            "k": k,
            "nonzero_channels": nonzero_channels,
            "num_channels": len(nonzero_channels),
        })
    return results


def shadow_large_k_growth(k_values: List[int]) -> List[Dict[str, float]]:
    r"""Growth of shadow invariants as k varies.

    kappa(sl_2_k) = 3(k+2)/4 -> 3k/4 for large k.
    The shadow tower for affine KM is class L and terminates.
    """
    results = []
    for k in k_values:
        kap = sl2_kappa(k)
        c = 3.0 * k / (k + 2)
        results.append({
            "k": k,
            "kappa": kap,
            "kappa_asymptotic": 0.75 * k,
            "kappa_ratio": kap / (0.75 * k) if k > 0 else float('inf'),
            "central_charge": c,
            "c_asymptotic": 3.0,
        })
    return results


def verlinde_vs_shadow_correlation(max_k: int = 20) -> Dict[str, Any]:
    r"""Correlation between fusion coefficients and shadow data.

    The fusion coefficients N_{ij}^m are integers in {0, 1} for sl_2.
    The shadow kappa is a rational number growing linearly in k.
    These are fundamentally different: one is combinatorial (integer),
    the other is analytic (rational/real).

    The Verlinde dimension Z_g grows polynomially:
        Z_g ~ (k+2)^{(2g-2)(N^2-1)/2} for large k (sl_N)

    The shadow F_g grows linearly:
        F_g = kappa * lambda_g ~ k for large k

    So the Verlinde dimension grows MUCH faster than the shadow.
    """
    k_values = list(range(1, max_k + 1))
    verlinde_g2 = []
    shadow_g2 = []

    for k in k_values:
        # Verlinde at genus 2
        S = sl2_S_matrix(k)
        Z2 = sum(S[0, j] ** (-2) for j in range(k + 1) if abs(S[0, j]) > 1e-15)
        verlinde_g2.append(round(Z2))

        # Shadow at genus 2: F_2 = kappa * lambda_2 = kappa * 7/5760
        kap = sl2_kappa(k)
        F2 = kap * 7.0 / 5760.0
        shadow_g2.append(F2)

    # Growth rate: log(Z_2)/log(k) for large k
    if max_k >= 10:
        growth_verlinde = math.log(verlinde_g2[-1]) / math.log(k_values[-1])
        growth_shadow = math.log(shadow_g2[-1]) / math.log(k_values[-1])
    else:
        growth_verlinde = None
        growth_shadow = None

    return {
        "k_values": k_values,
        "verlinde_genus2": verlinde_g2,
        "shadow_genus2": shadow_g2,
        "growth_exponent_verlinde": growth_verlinde,
        "growth_exponent_shadow": growth_shadow,
    }


