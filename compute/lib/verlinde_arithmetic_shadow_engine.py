def kappa_vs_sum_d_squared(k: int) -> Dict[str, float]:
    r"""Compare kappa with sum of squared quantum dimensions.

    kappa = 3(k+2)/4
    sum d_j^2 = D^2 = (k+2)/(2 sin^2(pi/(k+2)))
    ratio = kappa / D^2 = (3/2) sin^2(pi/(k+2))

    At large k: ratio ~ (3/2)(pi/(k+2))^2 -> 0.
    At k=1: ratio = (3/2) sin^2(pi/3) = (3/2)(3/4) = 9/8.
    """
    kap = sl2_kappa(k)
    D2 = sl2_total_quantum_dim_sq(k)
    ratio = kap / D2
    n = k + 2
    sin_val = math.sin(math.pi / n)
    formula_ratio = 1.5 * sin_val ** 2

    return {
        "k": k,
        "kappa": kap,
        "D_squared": D2,
        "ratio": ratio,
        "formula_ratio": formula_ratio,
        "match": abs(ratio - formula_ratio) < 1e-12,
    }


def kappa_not_rational_function_of_D2() -> Dict[str, Any]:
    r"""Demonstrate that kappa is NOT a rational function of D^2.

    If kappa = f(D^2) for some rational function f, then f would
    have to map D^2(k) -> kappa(k) for all k.  We show this is
    impossible by checking that the map k -> (D^2(k), kappa(k))
    does not lie on any algebraic curve of low degree.

    Evidence: the ratio kappa/D^2 is NOT constant.
    """
    ratios = []
    for k in range(1, 21):
        kap = sl2_kappa(k)
        D2 = sl2_total_quantum_dim_sq(k)
        ratios.append(kap / D2)

    # Check if ratios are constant
    is_constant = max(ratios) - min(ratios) < 1e-10
    # Check if ratios are monotone
    is_monotone = all(ratios[i] >= ratios[i + 1] for i in range(len(ratios) - 1))

    return {
        "ratios": ratios,
        "is_constant": is_constant,
        "is_monotone_decreasing": is_monotone,
        "min_ratio": min(ratios),
        "max_ratio": max(ratios),
        "conclusion": "kappa is NOT a rational function of D^2",
    }


