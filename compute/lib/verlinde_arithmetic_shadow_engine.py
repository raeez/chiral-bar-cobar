def sl2_verlinde_genus_g(k: int, g: int) -> float:
    r"""Verlinde dimension at genus g for sl_2 at level k.

    Z_g = sum_{j=0}^k S_{0j}^{2-2g}
    """
    S = sl2_S_matrix(k)
    total = 0.0
    for j in range(k + 1):
        s0j = S[0, j]
        if abs(s0j) > 1e-15:
            total += s0j ** (2 - 2 * g)
    return total


def sl2_verlinde_genus_g_exact(k: int, g: int) -> int:
    """Rounded to exact integer."""
    return round(sl2_verlinde_genus_g(k, g))


def faber_pandharipande_lambda(g: int) -> float:
    r"""Faber-Pandharipande number lambda_g^{FP}.

    This is the coefficient of x^{2g} in the A-hat generating function
    evaluated at imaginary argument:

        (x/2)/sin(x/2) = 1 + sum_{g>=1} lambda_g * x^{2g}

    Equivalently, from the Bernoulli number formula:
        lambda_g = sum_{j=0}^{g} [product formula involving B_{2j}]

    The first few values (AP22, independently verified by series expansion):
        lambda_1 = 1/24
        lambda_2 = 7/5760
        lambda_3 = 31/967680
        lambda_4 = 127/154828800
        lambda_5 = 73/3503554560
    """
    # Precomputed exact values (verified via sympy series expansion
    # of (x/2)/sin(x/2) to order x^14)
    _LAMBDA_FP = {
        1: 1.0 / 24,
        2: 7.0 / 5760,
        3: 31.0 / 967680,
        4: 127.0 / 154828800,
        5: 73.0 / 3503554560,
        6: 1414477.0 / 2678117105664000,
    }
    if g in _LAMBDA_FP:
        return _LAMBDA_FP[g]

    # For higher g, compute via series expansion
    # (x/2)/sin(x/2) power series coefficient extraction
    # Using the recurrence from Bernoulli numbers:
    # The coefficient of x^{2g} in (x/2)/sin(x/2) can be computed as
    # a_{2g} = sum_{j=0}^g (-1)^j * (2^{2j} - 2) * B_{2j} / (2j)! * a_{2(g-j)}
    # with a_0 = 1, but this is circular.
    # For g <= 6 we use the precomputed table.
    # For g > 6, compute numerically via the Bernoulli-number expression.
    from sympy import symbols, series, sin as sym_sin
    x = symbols('x')
    f = (x / 2) / sym_sin(x / 2)
    s = series(f, x, 0, 2 * g + 2)
    coeff = s.coeff(x, 2 * g)
    return float(coeff)


def sl2_shadow_F_g(k: int, g: int) -> float:
    r"""Shadow partition function F_g = kappa * lambda_g^{FP}.

    lambda_g is the Faber-Pandharipande number: the coefficient of x^{2g}
    in (x/2)/sin(x/2) = Ahat(ix), the A-hat genus at imaginary argument.

    lambda_1 = 1/24, lambda_2 = 7/5760, lambda_3 = 31/967680.

    NOTE: The formula |B_{2g}|/(4g*(2g-2)!) is ONLY correct for g=1.
    For g >= 2, one must use the actual A-hat series coefficients.
    """
    kap = sl2_kappa(k)
    lam = faber_pandharipande_lambda(g)
    return kap * lam


def verlinde_shadow_genus_comparison(k: int, max_g: int = 5) -> List[Dict[str, Any]]:
    r"""Compare Verlinde dimensions with shadow F_g for genus g=1,...,max_g.

    The Verlinde dimension Z_g counts ALL channels.
    The shadow F_g is the scalar projection.
    The ratio Z_g/F_g measures how much of the partition function
    is captured by the scalar shadow alone.
    """
    results = []
    for g in range(1, max_g + 1):
        Zg = sl2_verlinde_genus_g_exact(k, g)
        Fg = sl2_shadow_F_g(k, g)
        results.append({
            "g": g,
            "verlinde_dim": Zg,
            "shadow_F_g": Fg,
            "ratio_Z_over_F": Zg / Fg if Fg > 1e-15 else float('inf'),
        })
    return results


