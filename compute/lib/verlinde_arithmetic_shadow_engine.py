def sl2_number_field_degree(k: int) -> int:
    r"""Degree of the number field generated by S-matrix entries.

    The S-matrix for sl_2 at level k has entries involving
    sin(pi*m/(k+2)) for m = 1, ..., k+1.

    These generate the field Q(cos(2*pi/(k+2))) = Q(zeta_{k+2})^+,
    the maximal real subfield of the (k+2)-th cyclotomic field.

    Degree = phi(k+2)/2 where phi is Euler's totient function,
    EXCEPT when k+2 = 1 or 2 (where the field is Q itself).
    """
    n = k + 2
    if n <= 2:
        return 1
    return _euler_totient(n) // 2


def _euler_totient(n: int) -> int:
    """Euler's totient function phi(n)."""
    result = n
    p = 2
    temp = n
    while p * p <= temp:
        if temp % p == 0:
            while temp % p == 0:
                temp //= p
            result -= result // p
        p += 1
    if temp > 1:
        result -= result // temp
    return result


def sl2_prime_level_field(p: int) -> Dict[str, Any]:
    r"""Number field data at prime-related level k = p - 2.

    At this level, k+2 = p (prime), so:
        - Field = Q(cos(2*pi/p)) = Q(zeta_p)^+
        - Degree = (p-1)/2
        - S-matrix entries involve sin(pi*m/p)

    The Galois group Gal(Q(zeta_p)^+/Q) = (Z/pZ)^* / {+/-1}
    is cyclic of order (p-1)/2.
    """
    if p < 3:
        raise ValueError(f"Prime p must be >= 3, got {p}")
    k = p - 2
    deg = (p - 1) // 2
    n = k + 2  # = p

    # The primitive 2cos(2*pi/p) generates Q(zeta_p)^+
    # Its minimal polynomial is the p-th minimal polynomial of 2*cos(2*pi*m/p)
    # which is the Chebyshev-related polynomial of degree (p-1)/2.

    # Galois group generators: the map cos(2*pi/p) -> cos(2*pi*g/p)
    # where g is a primitive root mod p.
    galois_order = deg

    # Sample S-matrix entries at this level
    s_entries = {}
    for j in range(k + 1):
        val = math.sqrt(2.0 / p) * math.sin(math.pi * (j + 1) / p)
        s_entries[j] = val

    return {
        "prime": p,
        "level": k,
        "field_degree": deg,
        "galois_group_order": galois_order,
        "galois_group_cyclic": True,  # (Z/pZ)^*/{+-1} is cyclic
        "s_matrix_first_row": s_entries,
    }


def quantum_dim_minimal_poly_degree(j: int, k: int) -> int:
    r"""Degree of minimal polynomial of d_j = sin((j+1)*pi/(k+2))/sin(pi/(k+2)).

    The quantum dimension d_j is an algebraic number.  Its minimal
    polynomial degree divides phi(k+2)/2.

    For j = 0: d_0 = 1, degree 1.
    For j = k: d_k = 1 (charge conjugation symmetry), degree 1.
    For j = 1 at prime k+2 = p: d_1 = 2*cos(pi/p), minimal poly degree (p-1)/2.
    """
    n = k + 2
    if j == 0 or j == k:
        return 1

    # d_j = sin((j+1)*pi/n) / sin(pi/n) = U_j(cos(pi/n))
    # where U_j is the j-th Chebyshev polynomial of the second kind.
    # The minimal polynomial degree divides phi(n)/2.

    # For a rough estimate: we use the fact that d_j lies in Q(cos(2*pi/n))
    # which has degree phi(n)/2 over Q.
    # The exact degree depends on j and n.

    # Simple cases
    if n <= 2:
        return 1

    field_deg = _euler_totient(n) // 2
    if field_deg == 0:
        field_deg = 1

    # For the fundamental rep j=1: d_1 = 2*cos(pi/n) for n >= 3.
    # Its minimal polynomial is the n-th cyclotomic-related polynomial.
    # Degree = phi(2n)/2 if n is odd, phi(n)/2 if n is even.
    # But d_1 = 2*cos(pi/n) generates the same field as cos(2*pi/n)
    # (since cos(2*pi/n) = 2*cos^2(pi/n) - 1), so degree divides phi(n)/2.

    # We return the field degree as an upper bound; the exact degree
    # requires computing the minimal polynomial.
    return field_deg


def quantum_dim_approx_minimal_poly(j: int, k: int) -> Dict[str, Any]:
    r"""Compute approximate minimal polynomial of d_j.

    Uses numerical methods to find the minimal polynomial by
    computing powers of d_j and finding integer relations.

    Returns dict with degree, approximate coefficients, and verification.
    """
    n = k + 2
    d_j = sl2_quantum_dimension(j, k)

    if abs(d_j - round(d_j)) < 1e-10:
        # d_j is (approximately) an integer
        int_val = round(d_j)
        return {
            "j": j, "k": k, "d_j": d_j,
            "degree": 1,
            "approx_coeffs": [-int_val, 1],  # x - int_val
            "is_integer": True,
        }

    # Try to find minimal polynomial up to degree 20
    max_deg = min(20, _euler_totient(n) // 2 + 2)
    powers = [d_j ** i for i in range(max_deg + 1)]

    # Use numpy to find integer linear relation
    for deg in range(2, max_deg + 1):
        # Build matrix: find integer coefficients c_0,...,c_deg
        # such that c_0 + c_1*d + ... + c_deg*d^deg = 0
        A = np.array(powers[:deg + 1]).reshape(1, -1)
        # Try LLL-like approach: scale up and look for small integer relations
        # Simple approach: build Vandermonde and check for near-integer nullspace
        scale = 1e8
        row = np.array(powers[:deg + 1]) * scale
        # Extend to (deg+1) x (deg+2) with identity block
        M = np.zeros((deg + 1, deg + 2))
        for i in range(deg + 1):
            M[i, i] = 1
        M[:, deg + 1] = row

        # Use QR to find the near-nullspace
        Q, R = np.linalg.qr(M.T)
        # The last column of Q is the candidate
        candidate = Q[:, -1]

        # Normalize so the leading coefficient is positive integer
        if abs(candidate[deg]) > 1e-15:
            candidate = candidate / candidate[deg]
            # Check if coefficients are near-integers
            rounded = np.round(candidate[:deg + 1])
            if np.max(np.abs(candidate[:deg + 1] - rounded)) < 0.01:
                # Verify
                val = sum(int(rounded[i]) * d_j ** i for i in range(deg + 1))
                if abs(val) < 1e-6:
                    coeffs = [int(rounded[i]) for i in range(deg + 1)]
                    return {
                        "j": j, "k": k, "d_j": d_j,
                        "degree": deg,
                        "approx_coeffs": coeffs,
                        "is_integer": False,
                        "residual": abs(val),
                    }

    return {
        "j": j, "k": k, "d_j": d_j,
        "degree": -1,  # Could not determine
        "approx_coeffs": None,
        "is_integer": False,
    }


def shadow_number_field_degree(k: int) -> int:
    r"""Degree of number field generated by shadow coefficients for sl_2_k.

    The shadow coefficients S_r(sl_2_k) are:
        kappa = 3(k+2)/4 (rational)
        S_3 = 0 (affine KM is class L, tower terminates at arity 3)
        S_r = 0 for r >= 4

    Since all shadow coefficients are RATIONAL, the shadow number
    field is Q itself, degree 1 over Q.
    """
    return 1  # Always Q for affine KM (class L, finite tower)


def arithmetic_gap(k: int) -> Dict[str, Any]:
    r"""The arithmetic gap between shadow and Verlinde number fields.

    Shadow data lives in Q (degree 1).
    Verlinde data lives in Q(cos(2*pi/(k+2))) (degree phi(k+2)/2).

    The gap measures how much richer the Verlinde arithmetic is
    compared to the shadow arithmetic.
    """
    shadow_deg = shadow_number_field_degree(k)
    verlinde_deg = sl2_number_field_degree(k)

    return {
        "k": k,
        "shadow_field_degree": shadow_deg,
        "verlinde_field_degree": verlinde_deg,
        "arithmetic_gap": verlinde_deg - shadow_deg,
        "gap_ratio": verlinde_deg / max(shadow_deg, 1),
    }


