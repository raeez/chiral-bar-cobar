def sl2_quantum_dimension(j: int, k: int) -> float:
    r"""Quantum dimension of V_j for sl_2 at level k.

    d_j = sin((j+1)*pi/(k+2)) / sin(pi/(k+2))

    Properties:
        d_0 = 1, d_j = d_{k-j}, d_j > 0 for 0 <= j <= k.
    """
    n = k + 2
    return math.sin((j + 1) * math.pi / n) / math.sin(math.pi / n)


def sl2_quantum_dimensions_all(k: int) -> np.ndarray:
    """All quantum dimensions d_0, ..., d_k."""
    return np.array([sl2_quantum_dimension(j, k) for j in range(k + 1)])


def sl2_total_quantum_dim_sq(k: int) -> float:
    r"""Total quantum dimension squared D^2 = sum_j d_j^2 = 1/S_{00}^2.

    Closed form: D^2 = (k+2) / (2 sin^2(pi/(k+2)))
    """
    n = k + 2
    return n / (2.0 * math.sin(math.pi / n) ** 2)


def sl2_total_quantum_dim_sq_from_sum(k: int) -> float:
    """D^2 computed by direct summation of d_j^2."""
    dims = sl2_quantum_dimensions_all(k)
    return float(np.sum(dims ** 2))


def sl2_kappa(k: int) -> float:
    r"""Modular characteristic kappa for sl_2 at level k.

    kappa(sl_2, k) = dim(sl_2) * (k + h^v) / (2 * h^v) = 3(k+2)/4

    where dim(sl_2) = 3 and h^v = 2.
    """
    return 3.0 * (k + 2) / 4.0


def sl2_kappa_exact(k: int) -> Rational:
    """Exact kappa as Rational."""
    return Rational(3, 4) * (k + 2)


def kappa_from_quantum_dimensions(k: int) -> Dict[str, Any]:
    r"""Investigate if kappa is expressible from fusion/quantum-dimension data.

    We compute:
        D^2 = sum d_j^2 = (k+2)/(2 sin^2(pi/(k+2)))
        kappa = 3(k+2)/4
        ratio = kappa / D^2 = (3/2) sin^2(pi/(k+2))

    The ratio is NOT constant -- it depends on k through sin^2.
    For large k: ratio ~ 3 pi^2 / (2(k+2)^2) -> 0.

    So kappa is NOT a simple rational function of D^2.
    But kappa IS expressible from the S-matrix:
        kappa = (3/4) * S_{00}^{-2} * 2 * sin^2(pi/(k+2)) * (k+2)/... (circular)

    The direct connection: kappa = dim(g)*(k+h^v)/(2*h^v) uses the LEVEL
    and the LIE ALGEBRA DATA, not the fusion ring alone.
    """
    D2 = sl2_total_quantum_dim_sq(k)
    kap = sl2_kappa(k)
    ratio = kap / D2
    n = k + 2
    ratio_formula = 1.5 * math.sin(math.pi / n) ** 2
    large_k_approx = 1.5 * (math.pi / n) ** 2

    return {
        "k": k,
        "kappa": kap,
        "D_squared": D2,
        "ratio_kappa_over_D2": ratio,
        "ratio_formula": ratio_formula,
        "ratio_matches_formula": abs(ratio - ratio_formula) < 1e-12,
        "large_k_approx": large_k_approx,
    }


