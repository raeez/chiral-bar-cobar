def is_prime(n: int) -> bool:
    """Simple primality test."""
    if n < 2:
        return False
    if n < 4:
        return True
    if n % 2 == 0 or n % 3 == 0:
        return False
    i = 5
    while i * i <= n:
        if n % i == 0 or n % (i + 2) == 0:
            return False
        i += 6
    return True


def prime_level_analysis(p: int) -> Dict[str, Any]:
    r"""Complete arithmetic analysis at prime level k = p-2.

    At level k = p-2, we have k+2 = p (prime), so:
    - S-matrix entries involve sin(pi*m/p) for m=1,...,p-1
    - These generate Q(cos(2*pi/p)) = Q(zeta_p)^+
    - Field degree = (p-1)/2
    - Galois group = (Z/pZ)^*/{+/-1}, cyclic of order (p-1)/2
    """
    if not is_prime(p):
        raise ValueError(f"{p} is not prime")
    if p < 3:
        raise ValueError(f"Prime must be >= 3, got {p}")

    k = p - 2
    n = p  # k + 2

    # Number field data
    field_deg = (p - 1) // 2

    # Quantum dimensions
    dims = sl2_quantum_dimensions_all(k)

    # kappa
    kap = sl2_kappa(k)

    # Verlinde rank
    v_rank = sl2_verlinde_rank(k)

    # Specific trigonometric values
    cos_2pi_p = math.cos(2 * math.pi / p)
    sin_pi_p = math.sin(math.pi / p)

    # D^2
    D2 = sl2_total_quantum_dim_sq(k)

    # Fusion data
    N = sl2_fusion_coefficients(k)
    # Check integrality
    max_dev = 0.0
    for i in range(k + 1):
        for j in range(k + 1):
            for m in range(k + 1):
                dev = abs(N[i, j, m] - round(N[i, j, m]))
                max_dev = max(max_dev, dev)

    return {
        "prime": p,
        "level": k,
        "field_degree": field_deg,
        "verlinde_rank": v_rank,
        "kappa": kap,
        "D_squared": D2,
        "quantum_dims": dims.tolist(),
        "cos_2pi_p": cos_2pi_p,
        "sin_pi_p": sin_pi_p,
        "fusion_integrality_max_dev": max_dev,
        "shadow_field_degree": 1,
        "arithmetic_gap": field_deg - 1,
    }


