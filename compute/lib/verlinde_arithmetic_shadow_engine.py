def sl2_S_entry(j: int, l: int, k: int) -> float:
    r"""Single S-matrix entry for sl_2 at level k.

    S_{jl} = sqrt(2/(k+2)) * sin(pi*(j+1)*(l+1)/(k+2))
    """
    n = k + 2
    return math.sqrt(2.0 / n) * math.sin(math.pi * (j + 1) * (l + 1) / n)


def sl2_S_matrix(k: int) -> np.ndarray:
    r"""Full S-matrix for sl_2 at level k.

    Returns (k+1) x (k+1) numpy array.
    """
    n = k + 2
    size = k + 1
    pf = math.sqrt(2.0 / n)
    S = np.zeros((size, size))
    for j in range(size):
        for l in range(size):
            S[j, l] = pf * math.sin(math.pi * (j + 1) * (l + 1) / n)
    return S


def sl2_fusion_coefficients(k: int) -> np.ndarray:
    r"""All fusion coefficients N_{ij}^m for sl_2 at level k.

    Verlinde formula:
        N_{ij}^m = sum_l S_{il} S_{jl} S_{ml} / S_{0l}

    For sl_2 the S-matrix is real symmetric with S^2 = I,
    so conj(S_{ml}) = S_{ml}.

    Returns (k+1)^3 array.
    """
    S = sl2_S_matrix(k)
    size = k + 1
    N = np.zeros((size, size, size))
    for i in range(size):
        for j in range(size):
            for m in range(size):
                total = 0.0
                for l in range(size):
                    if abs(S[0, l]) > 1e-15:
                        total += S[i, l] * S[j, l] * S[m, l] / S[0, l]
                N[i, j, m] = total
    return N


def sl2_fusion_exact(i: int, j: int, m: int, k: int) -> int:
    r"""Exact fusion coefficient via truncated Clebsch-Gordan rule.

    N_{ij}^m = 1 if |i-j| <= m <= min(i+j, 2k-i-j) and i+j+m even,
    N_{ij}^m = 0 otherwise.

    This is the known closed form for sl_2 (unique to rank 1).
    """
    if (abs(i - j) <= m <= min(i + j, 2 * k - i - j)
            and (i + j + m) % 2 == 0):
        return 1
    return 0


def verify_verlinde_integrality(k: int, tol: float = 1e-8) -> Dict[str, Any]:
    r"""Verify that all fusion coefficients are non-negative integers.

    Returns dict with:
        all_integer: bool
        all_nonneg: bool
        max_deviation: float (max distance from nearest integer)
        total_checked: int
    """
    N = sl2_fusion_coefficients(k)
    size = k + 1
    max_dev = 0.0
    all_int = True
    all_nn = True
    total = 0

    for i in range(size):
        for j in range(size):
            for m in range(size):
                val = N[i, j, m]
                rounded = round(val)
                dev = abs(val - rounded)
                max_dev = max(max_dev, dev)
                if dev > tol:
                    all_int = False
                if rounded < 0:
                    all_nn = False
                total += 1

    return {
        "all_integer": all_int,
        "all_nonneg": all_nn,
        "max_deviation": max_dev,
        "total_checked": total,
    }


def verify_verlinde_vs_cg(k: int, tol: float = 1e-8) -> Dict[str, Any]:
    r"""Cross-verify Verlinde formula against Clebsch-Gordan rule.

    Path 1: Verlinde formula (S-matrix sum)
    Path 2: Truncated CG rule (closed form)

    These must agree for sl_2 at all levels.
    """
    N = sl2_fusion_coefficients(k)
    size = k + 1
    mismatches = []

    for i in range(size):
        for j in range(size):
            for m in range(size):
                verlinde_val = round(N[i, j, m])
                cg_val = sl2_fusion_exact(i, j, m, k)
                if verlinde_val != cg_val:
                    mismatches.append((i, j, m, verlinde_val, cg_val))

    return {
        "all_match": len(mismatches) == 0,
        "mismatches": mismatches,
        "total_checked": size ** 3,
    }


