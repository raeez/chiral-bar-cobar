def sl2_fusion_ring_characters(k: int) -> np.ndarray:
    r"""Characters of the Verlinde ring for sl_2 at level k.

    chi_l(V_j) = S_{jl} / S_{0l}

    These are the eigenvalues of the multiplication operator M_{V_j}
    acting on R_k.  There are k+1 characters, one for each l.

    Returns (k+1) x (k+1) array where result[j, l] = chi_l(V_j).
    """
    S = sl2_S_matrix(k)
    size = k + 1
    chi = np.zeros((size, size))
    for j in range(size):
        for l in range(size):
            if abs(S[0, l]) > 1e-15:
                chi[j, l] = S[j, l] / S[0, l]
    return chi


def verify_character_multiplicativity(k: int, tol: float = 1e-8) -> bool:
    r"""Verify chi_l(V_i * V_j) = chi_l(V_i) * chi_l(V_j).

    Characters are ring homomorphisms R_k -> C.
    """
    chi = sl2_fusion_ring_characters(k)
    N = sl2_fusion_coefficients(k)
    size = k + 1

    for l in range(size):
        for i in range(size):
            for j in range(size):
                # chi_l(V_i * V_j) = sum_m N_{ij}^m chi_l(V_m)
                lhs = sum(round(N[i, j, m]) * chi[m, l] for m in range(size))
                # chi_l(V_i) * chi_l(V_j)
                rhs = chi[i, l] * chi[j, l]
                if abs(lhs - rhs) > tol:
                    return False
    return True


def sl2_fusion_ring_is_semisimple(k: int) -> Dict[str, Any]:
    r"""Check semisimplicity of the Verlinde ring.

    The Verlinde ring R_k is semisimple (as Q-algebra) iff the character
    table is a square matrix of full rank.  For sl_2 this is always true
    because the S-matrix is unitary.

    Semisimplicity means R_k tensor Q = product of fields.
    Since all characters chi_l take values in R (for sl_2), we have
    R_k tensor Q = Q^{k+1} (product of k+1 copies of Q or number fields).
    """
    chi = sl2_fusion_ring_characters(k)
    size = k + 1
    rank = np.linalg.matrix_rank(chi, tol=1e-10)

    return {
        "k": k,
        "ring_dimension": size,
        "character_matrix_rank": rank,
        "is_semisimple": rank == size,
    }


def sl2_multiplication_eigenvalues(j: int, k: int) -> np.ndarray:
    r"""Eigenvalues of multiplication by V_j in the Verlinde ring.

    The multiplication operator M_{V_j} acts on R_k by
    M_{V_j}(V_i) = sum_m N_{ij}^m V_m.

    Its eigenvalues are chi_l(V_j) = S_{jl}/S_{0l} for l = 0,...,k.
    """
    chi = sl2_fusion_ring_characters(k)
    return chi[j, :]


