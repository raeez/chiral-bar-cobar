def sl3_integrable_weights(k: int) -> List[Tuple[int, int]]:
    r"""Integrable highest weights for sl_3 at level k.

    Dynkin labels (a1, a2) with a1, a2 >= 0 and a1 + a2 <= k.
    """
    weights = []
    for a1 in range(k + 1):
        for a2 in range(k + 1 - a1):
            weights.append((a1, a2))
    return weights


def sl3_S_matrix(k: int) -> np.ndarray:
    r"""Modular S-matrix for sl_3 at level k (Weyl-Kac-Peterson formula).

    S_{lambda,mu} = C * sum_{w in S_3} sgn(w) *
        exp(-2*pi*i * <w(lambda+rho), mu+rho>_0 / (k+3))

    where <,>_0 is the inner product on the traceless hyperplane.
    """
    N = 3
    n = k + N

    weights = sl3_integrable_weights(k)
    num = len(weights)

    def shifted_eps(a1, a2):
        return np.array([a1 + a2 + 2, a2 + 1, 0], dtype=float)

    def inner_traceless(v, w):
        v_p = v - np.mean(v)
        w_p = w - np.mean(w)
        return np.dot(v_p, w_p)

    perms = [(0, 1, 2), (1, 0, 2), (0, 2, 1), (2, 0, 1), (1, 2, 0), (2, 1, 0)]
    signs = [1, -1, -1, 1, 1, -1]

    S_raw = np.zeros((num, num), dtype=complex)
    for i, (a1_i, a2_i) in enumerate(weights):
        p = shifted_eps(a1_i, a2_i)
        for j_idx, (a1_j, a2_j) in enumerate(weights):
            q = shifted_eps(a1_j, a2_j)
            val = 0.0j
            for perm, sgn in zip(perms, signs):
                wp = p[list(perm)]
                ip = inner_traceless(wp, q)
                val += sgn * np.exp(-2.0j * math.pi * ip / n)
            S_raw[i, j_idx] = val

    # Normalize
    SSd = S_raw @ np.conj(S_raw.T)
    c_norm = math.sqrt(np.diag(SSd).real[0])
    S_mat = S_raw / c_norm

    # Phase: S_{00} real positive
    phase = S_mat[0, 0] / abs(S_mat[0, 0])
    S_mat = S_mat / phase

    return S_mat


def sl3_quantum_dimensions(k: int) -> np.ndarray:
    r"""Quantum dimensions for sl_3 at level k.

    d_lambda = S_{0,lambda}/S_{0,0}

    Uses the q-Weyl dimension formula:
        d_{(a1,a2)} = prod_{alpha>0} sin(pi<lambda+rho,alpha>/(k+3))
                     / sin(pi<rho,alpha>/(k+3))
    """
    n = k + 3
    weights = sl3_integrable_weights(k)
    dims = np.zeros(len(weights))

    def shifted_eps(a1, a2):
        return np.array([a1 + a2 + 2, a2 + 1, 0], dtype=float)

    rho = shifted_eps(0, 0)  # [2, 1, 0]
    pos_root_pairs = [(0, 1), (0, 2), (1, 2)]

    for idx, (a1, a2) in enumerate(weights):
        lam_rho = shifted_eps(a1, a2)
        d = 1.0
        for (i, j) in pos_root_pairs:
            num = math.sin(math.pi * (lam_rho[i] - lam_rho[j]) / n)
            den = math.sin(math.pi * (rho[i] - rho[j]) / n)
            d *= num / den
        dims[idx] = d

    return dims


def sl3_fusion_coefficients(k: int) -> np.ndarray:
    r"""Fusion coefficients for sl_3 at level k via Verlinde formula."""
    S = sl3_S_matrix(k)
    num = S.shape[0]
    N = np.zeros((num, num, num))

    for i in range(num):
        for j in range(num):
            for m in range(num):
                total = 0.0j
                for l in range(num):
                    s0l = S[0, l]
                    if abs(s0l) > 1e-15:
                        total += S[i, l] * S[j, l] * np.conj(S[m, l]) / s0l
                N[i, j, m] = total.real
    return N


def sl3_verlinde_integrality(k: int, tol: float = 1e-6) -> Dict[str, Any]:
    r"""Check integrality of sl_3 fusion coefficients."""
    N = sl3_fusion_coefficients(k)
    num = N.shape[0]
    max_dev = 0.0
    all_int = True
    all_nn = True

    for i in range(num):
        for j in range(num):
            for m in range(num):
                val = N[i, j, m]
                rd = round(val)
                dev = abs(val - rd)
                max_dev = max(max_dev, dev)
                if dev > tol:
                    all_int = False
                if rd < 0:
                    all_nn = False

    return {
        "all_integer": all_int,
        "all_nonneg": all_nn,
        "max_deviation": max_dev,
        "num_weights": num,
    }


