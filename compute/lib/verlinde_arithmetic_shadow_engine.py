r"""Verlinde formula arithmetic and shadow connections.

Arithmetic structure of the Verlinde formula for affine Lie algebras
and its interplay with the shadow obstruction tower.

MATHEMATICAL FRAMEWORK
======================

The Verlinde formula for sl_2 at level k:

    N_{ij}^m = sum_l S_{il} S_{jl} S_{ml}^* / S_{0l}

where S_{jl} = sqrt(2/(k+2)) sin(pi(j+1)(l+1)/(k+2)), produces
NON-NEGATIVE INTEGER fusion coefficients.  This integrality is a
deep arithmetic fact: the Verlinde formula computes dimensions of
spaces of conformal blocks (Beauville-Laszlo, Tsuchiya-Ueno-Yamada).

ARITHMETIC CONTENT:

1. NUMBER FIELDS FROM S-MATRIX:
   The S-matrix entries for sl_2 at level k involve sin(pi*m/(k+2)).
   At prime levels k = p-2, the splitting field is the maximal real
   subfield Q(cos(2pi/p)) = Q(zeta_p)^+ of degree (p-1)/2 over Q.

2. QUANTUM DIMENSION ARITHMETIC:
   d_j = sin((j+1)pi/(k+2)) / sin(pi/(k+2)) are algebraic numbers
   with minimal polynomials of degree <= (k+1)/2.  The Galois group
   acts by d_j -> d_{sigma(j)} where sigma permutes the representations.

3. SHADOW-VERLINDE BRIDGE:
   kappa(sl_2_k) = 3(k+2)/4 is the shadow's leading invariant.
   The total quantum dimension D^2 = (k+2)/(2 sin^2(pi/(k+2))).
   The ratio kappa/D^2 = (3/2) sin^2(pi/(k+2)) is a transcendental
   function of k that approaches 3pi^2/(2(k+2)^2) for large k.

4. ARITHMETIC DEPTH:
   The arithmetic depth d_arith measures the complexity of the
   number field generated by the shadow data.  For affine sl_2,
   the shadow coefficients are RATIONAL (d_arith = 0), while the
   Verlinde/S-matrix data generates Q(zeta_{k+2})^+.  The gap
   between d_arith(shadow) = 0 and the Verlinde field degree
   is the "arithmetic gap" of the Verlinde formula.

5. FUSION RING STRUCTURE:
   R_k = Z[V_0,...,V_k] is commutative, associative, semisimple.
   Characters: chi_l(V_j) = S_{jl}/S_{0l} = d_j evaluated at
   the l-th root of unity.  The fusion ring is isomorphic to
   Z[x]/(U_{k+1}(x/2)) where U_n is the Chebyshev polynomial
   of the second kind.

Manuscript references:
    thm:modular-characteristic (higher_genus_modular_koszul.tex)
    thm:shadow-cohft (higher_genus_modular_koszul.tex)
    sec:concordance-koszulness-programme (concordance.tex)

Conventions:
    - Representations labeled j = 0, 1, ..., k (= twice the spin) for sl_2
    - S_{00} > 0 (positive normalization)
    - kappa(sl_2, k) = dim(sl_2)*(k+h^v)/(2*h^v) = 3(k+2)/4 (AP1, AP39)
    - The bar propagator is weight 1 (AP27), not weight h
"""

