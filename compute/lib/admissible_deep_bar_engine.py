@dataclass
class ExtDiscrepancyData:
    """Bar-Ext vs ordinary-Ext comparison data."""
    p: int
    q: int
    k: Fraction
    # Bar-Ext (from bar complex)
    bar_ext_dims: Dict[int, int]  # degree -> dim
    # Ordinary Ext (from module category)
    ordinary_ext_semisimple: bool
    ordinary_ext1_nonzero_pairs: int
    ordinary_ext2_nonzero_pairs: int
    # Discrepancy
    discrepancy_at_degree: Dict[int, str]  # degree -> description
    # Overall
    ext_agree_at_high_degree: bool


def ext_discrepancy_sl2(p: int, q: int) -> ExtDiscrepancyData:
    """Compute the bar-Ext vs ordinary-Ext discrepancy for L_k(sl_2).

    BAR-EXT:
    H^1(B(L_k)) = sl_2 (dim 3) for all Koszul L_k.
    H^n(B(L_k)) = 0 for n >= 2 when L_k is Koszul.

    ORDINARY-EXT (in the module category):
    At integrable levels: semisimple category, all Ext vanish.
    At non-integrable admissible:
    - Module category is non-semisimple (logarithmic)
    - Ext^1 != 0 for some pairs (staggered modules)
    - Ext^2: typically nonzero for some pairs

    The KEY DISCREPANCY: bar-Ext is an invariant of the ALGEBRA,
    while ordinary-Ext depends on the MODULE PAIR.
    bar-Ext^1 = sl_2 (3-dim, always) measures the generating space.
    Ext^1_{mod}(L_i, L_j) depends on the specific (i,j) pair.

    These are fundamentally DIFFERENT invariants that happen to have
    similar names. The bar-Ext controls the Koszul dual structure;
    the ordinary Ext controls the derived category of modules.
    """
    k = Fraction(p, q) - 2
    h_null = (p - 1) * q

    # Bar-Ext
    bar_ext = {1: 3}  # H^1 = sl_2, always
    for d in range(2, 4):
        bar_ext[d] = 0  # Koszul (for sl_2 with h_null >= 3)

    # Ordinary-Ext
    is_integrable = (q == 1)
    n_modules = (p - 1) * q

    if is_integrable:
        ext1_pairs = 0
        ext2_pairs = 0
        discrepancy = {
            1: 'bar-Ext^1 = 3 (generating space), Ext^1_{mod} = 0 (semisimple)',
            2: 'AGREE: both zero',
        }
    else:
        # For non-integrable admissible levels, the module category has
        # non-trivial extensions. Count heuristically from adjacency in
        # the Kac table.
        weights = admissible_weights_sl2(admissible_level_sl2(p, q))
        ext1_pairs = 0
        for i, wi in enumerate(weights):
            for j, wj in enumerate(weights):
                if i >= j:
                    continue
                if abs(wi.r - wj.r) + abs(wi.s - wj.s) == 1:
                    ext1_pairs += 1

        # Ext^2: heuristic â€” typically fewer nonzero pairs
        ext2_pairs = max(0, ext1_pairs // 3)

        discrepancy = {
            1: (f'bar-Ext^1 = 3 (generating space) != '
                f'Ext^1_{{mod}} = variable ({ext1_pairs} nonzero pairs)'),
            2: (f'bar-Ext^2 = 0 (Koszul) vs '
                f'Ext^2_{{mod}} = variable (~{ext2_pairs} nonzero pairs)'),
        }

    return ExtDiscrepancyData(
        p=p, q=q, k=k,
        bar_ext_dims=bar_ext,
        ordinary_ext_semisimple=is_integrable,
        ordinary_ext1_nonzero_pairs=ext1_pairs,
        ordinary_ext2_nonzero_pairs=ext2_pairs,
        discrepancy_at_degree=discrepancy,
        ext_agree_at_high_degree=(bar_ext.get(2, 0) == 0 and ext2_pairs == 0)
                                 if is_integrable else False,
    )


