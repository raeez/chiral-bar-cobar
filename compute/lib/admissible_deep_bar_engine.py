# 4. Shadow metric at admissible levels
# =========================================================================

@dataclass(frozen=True)
class ShadowMetricData:
    """Shadow metric data for an admissible-level algebra."""
    kappa: Fraction
    c: Fraction
    alpha: Fraction       # cubic shadow coefficient
    S4: Fraction          # quartic shadow S_4
    delta: Fraction       # critical discriminant Delta = 8*kappa*S_4
    Q_coefficients: Tuple[Fraction, Fraction, Fraction]  # Q(t) = a + b*t + c*t^2
    is_perfect_square: bool
    shadow_class: str     # G, L, C, M
    r_max: int            # shadow depth


def shadow_metric_admissible_sl2(p: int, q: int) -> ShadowMetricData:
    """Compute the shadow metric Q_L(t) for L_k(sl_2) at admissible level.

    For affine KM algebras, the shadow obstruction tower has:
    - kappa = 3p/(4q) (modular characteristic)
    - alpha = cubic shadow coefficient (from Lie bracket)
    - S_4 = quartic shadow coefficient (0 by Jacobi for KM)
    - Delta = 8*kappa*S_4 = 0 (Jacobi identity)

    The shadow metric is:
    Q_L(t) = (2*kappa + 3*alpha*t)^2 + 2*Delta*t^2
           = (2*kappa + 3*alpha*t)^2   (since Delta = 0)

    This is a PERFECT SQUARE, confirming class L (shadow depth 3).

    NOTE: For NEGATIVE central charge (c < 0), kappa = 3p/(4q) is still
    POSITIVE (since p,q > 0). The shadow metric is still a perfect square.
    The sign of c does NOT affect the shadow depth class.

    The Koszul dual has level k' = -p/q - 2 with kappa' = 3(-p/q)/(4)
    which is NEGATIVE. But the shadow metric uses |kappa|, so the
    complementarity structure is maintained.
    """
    kappa = Fraction(3 * p, 4 * q)
    k = Fraction(p, q) - 2
    c = 3 * k / (k + 2)

    # For KM algebras, the cubic coefficient alpha comes from the
    # Lie bracket structure constant. For sl_2:
    # [e,f] = h gives alpha proportional to the Killing form normalization.
    # The exact value: alpha = -kappa/3 for sl_2 (from the shadow
    # obstruction tower at arity 3).
    # With this, Q_L(t) = (2*kappa - kappa*t)^2 = kappa^2 * (2-t)^2.
    alpha = -kappa / 3

    S4 = Fraction(0)  # quartic vanishes by Jacobi
    delta = 8 * kappa * S4  # = 0

    # Q(t) = (2*kappa + 3*alpha*t)^2 + 2*delta*t^2
    #       = (2*kappa - kappa*t)^2
    #       = kappa^2 * (2-t)^2
    # Coefficients: Q(t) = 4*kappa^2 - 4*kappa^2*t + kappa^2*t^2
    a = 4 * kappa**2
    b = -4 * kappa**2
    c_coeff = kappa**2

    is_sq = (delta == 0)  # perfect square iff discriminant = 0

    return ShadowMetricData(
        kappa=kappa, c=c, alpha=alpha, S4=S4, delta=delta,
        Q_coefficients=(a, b, c_coeff),
        is_perfect_square=is_sq,
        shadow_class='L',
        r_max=3,
    )


