    kappa_sl3 = Fraction(8) * (k_sl3 + 3) / 6

    # W_3 central charge via the DS reduction formula
    c_w3 = Fraction(2) * (1 - 12 * Fraction((p - q)**2, p * q))
    kappa_w3 = c_w3 * Fraction(5, 6)  # AP1/AP9: kappa(W_3) = c*(H_3-1) = 5c/6

    return WAlgebraAdmissibleData(
        p=p, q=q,
        k_sl3=k_sl3, c_sl3=c_sl3, kappa_sl3=kappa_sl3,
        c_w3=c_w3, kappa_w3=kappa_w3,
        shadow_class_sl3='L',
        shadow_class_w3='M',
        ds_changes_class=True,
    )


