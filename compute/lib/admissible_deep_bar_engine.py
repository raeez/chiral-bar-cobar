@dataclass(frozen=True)
class KoszulDualData:
    """Koszul dual level data."""
    k: Fraction               # original level
    k_dual: Fraction          # dual level
    kappa: Fraction           # kappa at original
    kappa_dual: Fraction      # kappa at dual
    kappa_sum: Fraction       # kappa + kappa_dual
    dual_is_admissible: bool  # whether k_dual is admissible
    dual_p: Optional[int]     # p parameter of dual (if admissible)
    dual_q: Optional[int]     # q parameter of dual (if admissible)
    c: Fraction               # central charge at k
    c_dual: Fraction          # central charge at k_dual
    c_sum: Fraction           # c + c_dual


def koszul_dual_level_sl2(p: int, q: int) -> KoszulDualData:
    """Analyze the Koszul dual level for sl_2 at admissible k = p/q - 2.

    The Feigin-Frenkel involution for sl_2: k <-> -k - 2h^v = -k - 4.
    So k' = -(p/q - 2) - 4 = -p/q - 2.

    For k' to be admissible: k' = p'/q' - 2 requires p'/q' = k' + 2 = -p/q.
    Since p'/q' must be a POSITIVE rational (p' >= 2, q' >= 1),
    and -p/q < 0, the dual level is NOT admissible.

    kappa(k) = 3(k+2)/4 = 3p/(4q)
    kappa(k') = 3(k'+2)/4 = 3(-p/q)/4 = -3p/(4q) = -kappa(k)

    So kappa + kappa' = 0 for sl_2 (the KM anti-symmetry AP24 holds).

    Central charge sum: c(k) + c(k') = 3k/(k+2) + 3k'/(k'+2)
    = 3(p/q-2)/(p/q) + 3(-p/q-2)/(-p/q)
    = 3(p-2q)/p + 3(-p-2q)/(-p)
    = 3(p-2q)/p + 3(p+2q)/p
    = 3(p-2q+p+2q)/p = 6p/p = 6.

    So c + c' = 6 = 2*dim(sl_2)/1. This is the complementarity sum.
    (For general g: c + c' = 2*dim(g)/h^v * (something).)

    CRITICAL (AP24): For KM algebras, kappa + kappa' = 0 HOLDS.
    The anti-symmetry is exact, not approximate.
    """
    k = Fraction(p, q) - 2
    k_dual = -k - 4  # = -p/q - 2
    kappa = Fraction(3 * p, 4 * q)
    kappa_dual = Fraction(3) * (k_dual + 2) / 4  # = 3*(-p/q)/4 = -3p/(4q)

    c_val = 3 * k / (k + 2)
    c_dual = 3 * k_dual / (k_dual + 2)

    # Check if dual is admissible
    # k' + 2 = -p/q. For admissible: k'+2 = p'/q' > 0.
    # But -p/q < 0, so dual is NOT admissible.
    dual_admissible = False
    dual_p = None
    dual_q = None

    # However, at the critical level k' = -2 (k = 2):
    # k' = -2 - 4 + 2 = ... Let me recompute.
    # k = p/q - 2. k' = -k - 4 = -(p/q - 2) - 4 = -p/q + 2 - 4 = -p/q - 2.
    # k' + 2 = -p/q < 0. So dual is never admissible for p,q > 0.

    return KoszulDualData(
        k=k, k_dual=k_dual,
        kappa=kappa, kappa_dual=kappa_dual,
        kappa_sum=kappa + kappa_dual,
        dual_is_admissible=dual_admissible,
        dual_p=dual_p, dual_q=dual_q,
        c=c_val, c_dual=c_dual,
        c_sum=c_val + c_dual,
    )


