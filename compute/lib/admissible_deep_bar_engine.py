def enumerate_admissible_sl2(
    max_k: float = 4.0,
    min_k: float = -1.99,
    max_q: int = 8,
) -> List[Tuple[int, int, Fraction]]:
    """Enumerate all admissible levels k = p/q - 2 in [min_k, max_k].

    Returns list of (p, q, k) tuples sorted by k.
    """
    results = []
    for q_val in range(1, max_q + 1):
        for p_val in range(2, int((max_k + 2) * q_val) + 3):
            if gcd(p_val, q_val) != 1:
                continue
            k = Fraction(p_val, q_val) - 2
            if min_k <= float(k) <= max_k:
                results.append((p_val, q_val, k))
    results.sort(key=lambda x: float(x[2]))
    return results


