def negative_c_shadow_sl2(p: int, q: int) -> NegativeCData:
    """Analyze shadow data at negative central charge for sl_2.

    For sl_2 at admissible k = p/q - 2:
    c = 3(p-2q)/p. Negative when p < 2q, i.e., k < 0.

    IMPORTANT: Even at negative c, kappa = 3p/(4q) is ALWAYS POSITIVE
    (since p,q > 0). The shadow obstruction tower is controlled by
    kappa, not by c. The shadow metric Q_L(t) = kappa^2 * (2-t)^2
    has the same structure regardless of the sign of c.

    The sign of c affects:
    1. Unitarity: c < 0 implies non-unitary.
    2. The Koszul dual: c' = 6 - c > 6 when c < 0.
    3. The modular characteristic of the VIRASORO realization
       (which is c/2, potentially negative).

    The sign of c does NOT affect:
    1. kappa(hat{sl}_2, k) = 3p/(4q) > 0 (always).
    2. Shadow depth class (always L for KM).
    3. Koszulness of V_k (always true).
    """
    k = Fraction(p, q) - 2
    c = 3 * k / (k + 2)  # = 3(p-2q)/p
    kappa = Fraction(3 * p, 4 * q)

    # Koszul dual central charge
    k_dual = -k - 4
    c_dual = 3 * k_dual / (k_dual + 2)
    comp_sum = c + c_dual

    # Shadow metric Q(t) = kappa^2 * (2-t)^2
    Q_zero = 4 * kappa**2
    # Q(t) = 0 iff t = 2. Since Q is a perfect square, single root at t=2.
    Q_real_roots = True  # always has root at t = 2

    # Growth rate for shadow obstruction tower
    # For class L (terminates at arity 3), there's no asymptotic growth
    rho = None

    return NegativeCData(
        p=p, q=q, k=k, c=c,
        c_is_negative=(c < 0),
        kappa=kappa,
        kappa_is_positive=(kappa > 0),
        shadow_class='L',
        c_dual=c_dual,
        c_dual_is_positive=(c_dual > 0),
        complementarity_sum=comp_sum,
        Q_at_zero=Q_zero,
        Q_has_real_roots=Q_real_roots,
        growth_rate=rho,
    )


