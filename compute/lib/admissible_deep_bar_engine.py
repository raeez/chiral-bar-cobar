def colored_partition_count(h: int, n_colors: int) -> int:
    """Number of partitions of h into parts of n_colors colors.

    This is the coefficient of q^h in prod_{n>=1} (1-q^n)^{-n_colors}.
    For sl_2 (3 generators of weight 1): n_colors = 3.
    For sl_3 (8 generators of weight 1): n_colors = 8.

    Computed iteratively via the product formula.
    """
    if h < 0:
        return 0
    if h == 0:
        return 1
    coeffs = [0] * (h + 1)
    coeffs[0] = 1
    for n in range(1, h + 1):
        for _ in range(n_colors):
            for j in range(n, h + 1):
                coeffs[j] += coeffs[j - n]
    return coeffs[h]


def vacuum_verma_dims(max_weight: int, rank: int = 3) -> List[int]:
    """Dimension of weight-h space in vacuum Verma module.

    For hat{sl}_2: rank = dim(sl_2) = 3, generators all of weight 1.
    dim V_k[h] = p_{rank}(h) (rank-colored partition of h).

    For hat{sl}_3: rank = dim(sl_3) = 8.
    """
    coeffs = [0] * (max_weight + 1)
    coeffs[0] = 1
    for n in range(1, max_weight + 1):
        for _ in range(rank):
            for h in range(n, max_weight + 1):
                coeffs[h] += coeffs[h - n]
    return coeffs


def bar_chain_dims_arity(dim_g: int, max_arity: int) -> Dict[int, int]:
    """Dimensions of bar chain groups at each arity.

    For the PBW-reduced bar complex (CE complex of g):
    B^d = Lambda^d(g), dim = C(dim_g, d).

    The bar complex for V_k(g) is quasi-isomorphic to the CE complex
    of g by the PBW spectral sequence collapse.
    """
    result = {}
    for d in range(max_arity + 1):
        result[d] = comb(dim_g, d) if d <= dim_g else 0
    return result


