def master_deep_verification(max_q: int = 5, max_k: float = 3.0) -> Dict:
    """Run the deep bar analysis across all admissible sl_2 levels.

    Returns comprehensive verification data.
    """
    levels = enumerate_admissible_sl2(max_k=max_k, max_q=max_q)
    results = []
    all_koszul = True
    all_kappa_sum_zero = True
    all_c_sum_six = True
    all_paths_agree = True

    for p_val, q_val, k in levels:
        analysis = deep_bar_analysis_sl2(p_val, q_val)
        results.append(analysis)

        if not analysis.is_koszul:
            all_koszul = False
        if analysis.koszul_dual.kappa_sum != 0:
            all_kappa_sum_zero = False
        if analysis.koszul_dual.c_sum != 6:
            all_c_sum_six = False
        if not analysis.koszulness_verification.all_paths_agree:
            all_paths_agree = False

    return {
        'n_levels': len(results),
        'all_koszul': all_koszul,
        'all_kappa_sum_zero': all_kappa_sum_zero,
        'all_c_sum_six': all_c_sum_six,
        'all_paths_agree': all_paths_agree,
        'analyses': results,
        'negative_c_count': sum(1 for a in results if a.negative_c.c_is_negative),
        'positive_c_count': sum(1 for a in results if not a.negative_c.c_is_negative),
    }
