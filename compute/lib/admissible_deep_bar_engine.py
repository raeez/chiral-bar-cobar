@dataclass
class WeightSpaceData:
    """Data for a weight space in V_k or L_k."""
    weight: int
    verma_dim: int
    null_dim: int
    quotient_dim: int  # = verma_dim - null_dim (cumulative)


def quotient_dims_shapovalov(p: int, q: int, max_weight: int) -> List[WeightSpaceData]:
    """Compute L_k = V_k / N_k dimensions weight by weight via Shapovalov.

    Uses the Shapovalov determinant to find null space dimensions at
    each weight, then subtracts from V_k dimensions.

    Below h_null = (p-1)*q, L_k = V_k (no nulls).
    At h >= h_null, null vectors and their descendants reduce dimensions.

    WARNING: expensive for max_weight > 6 (state count grows as p_3(h)).
    """
    verma = vacuum_verma_dims(max_weight, rank=3)
    h_null = (p - 1) * q
    k_val = float(Fraction(p, q) - 2)

    results = []
    for h in range(max_weight + 1):
        if h < h_null:
            results.append(WeightSpaceData(
                weight=h, verma_dim=verma[h],
                null_dim=0, quotient_dim=verma[h]
            ))
        else:
            # Compute Shapovalov null dimension
            n_states, rank, null_dim = shapovalov_null_dimension(k_val, h)
            results.append(WeightSpaceData(
                weight=h, verma_dim=verma[h],
                null_dim=null_dim, quotient_dim=rank
            ))
    return results


