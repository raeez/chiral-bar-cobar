@dataclass
class DeepBarAnalysisSl3:
    """Deep bar analysis for sl_3 at admissible level."""
    p: int
    q: int
    k: Fraction
    c: Fraction
    kappa: Fraction
    h_null_estimate: int
    max_bar_arity: int     # = dim(sl_3) = 8
    bar_relevant_null: bool
    is_universal_koszul: bool
    is_quotient_koszul: Optional[bool]
    # W_3 data from DS reduction
    w3_data: WAlgebraAdmissibleData
    # Koszul dual
    kappa_dual: Fraction
    kappa_sum: Fraction
    # CE complex
    ce_dims: Dict[int, int]
    summary: str


def deep_bar_analysis_sl3(p: int, q: int) -> DeepBarAnalysisSl3:
    """Deep bar analysis for sl_3 at admissible level k = p/q - 3."""
    if gcd(p, q) != 1:
        raise ValueError(f"Need gcd(p,q) = 1")
    if p < 3:
        raise ValueError(f"Need p >= 3 for sl_3 (h^v = 3)")

    k = Fraction(p, q) - 3
    c = Fraction(8) * k / (k + 3)
    kappa = Fraction(8) * (k + 3) / 6
    h_null = (p - 1) * q  # estimate for first null

    max_arity = 8  # dim(sl_3)
    bar_relevant = (h_null <= max_arity)

    # CE complex dimensions
    ce_dims = {d: comb(8, d) for d in range(9)}

    # Koszulness
    is_univ_koszul = True
    if h_null > max_arity:
        is_quot_koszul = True
    else:
        is_quot_koszul = None  # needs detailed analysis

    # W_3 data
    w3 = w3_at_admissible_sl3(p, q)

    # Koszul dual
    k_dual = -k - 6  # = -(p/q - 3) - 6 = -p/q - 3
    kappa_dual = Fraction(8) * (k_dual + 3) / 6
    kappa_sum = kappa + kappa_dual

    summary = (
        f"L_k(sl_3) at k = {k} (p={p}, q={q}): c = {c}, kappa = {kappa}. "
        f"h_null(est) = {h_null}. max CE arity = {max_arity}. "
        f"{'Null above bar range: Koszul' if not bar_relevant else 'Null in bar range: analysis needed'}. "
        f"W_3 central charge: {w3.c_w3}. DS changes shadow class: L -> M."
    )

    return DeepBarAnalysisSl3(
        p=p, q=q, k=k, c=c, kappa=kappa,
        h_null_estimate=h_null,
        max_bar_arity=max_arity,
        bar_relevant_null=bar_relevant,
        is_universal_koszul=is_univ_koszul,
        is_quotient_koszul=is_quot_koszul,
        w3_data=w3,
        kappa_dual=kappa_dual,
        kappa_sum=kappa_sum,
        ce_dims=ce_dims,
        summary=summary,
    )


