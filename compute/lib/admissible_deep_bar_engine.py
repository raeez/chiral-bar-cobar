# 6. Fusion rules at admissible levels
# =========================================================================

def fusion_rules_integrable_sl2(p: int) -> np.ndarray:
    """Fusion rules for integrable sl_2 at level k = p - 2.

    Integrable modules: L_j for j = 0, 1, ..., k = p-2.
    Fusion: N_{ij}^m from the Verlinde formula.

    For SU(2)_k: N_{ij}^m = 1 if |i-j| <= m <= min(i+j, 2k-i-j)
    and i+j+m even, else 0.
    """
    k = p - 2
    n = p - 1  # number of integrable modules (j = 0, ..., k)
    N = np.zeros((n, n, n), dtype=int)

    for i in range(n):
        for j in range(n):
            for m in range(n):
                # SU(2)_k fusion rule
                if abs(i - j) <= m <= min(i + j, 2 * k - i - j):
                    if (i + j + m) % 2 == 0:
                        N[i, j, m] = 1

    return N


def fusion_ring_structure(p: int, q: int) -> Dict:
    """Compute the fusion ring structure at admissible levels.

    For integrable levels (q=1): standard Verlinde formula.
    For non-integrable admissible: extended Verlinde with modular S-matrix.

    The Verlinde formula: N_{ij}^m = sum_l S_{il} S_{jl} S_{ml}^* / S_{0l}
    """
    n_modules = (p - 1) * q
    S = admissible_modular_s_matrix_sl2(admissible_level_sl2(p, q))
    n = S.shape[0]

    # Compute fusion coefficients via Verlinde
    N = np.zeros((n, n, n), dtype=complex)
    for i in range(n):
        for j in range(n):
            for m in range(n):
                val = 0.0
                for l in range(n):
                    if abs(S[0, l]) > 1e-14:
                        val += S[i, l] * S[j, l] * np.conj(S[m, l]) / S[0, l]
                N[i, j, m] = val

    # Check integrality
    N_int = np.round(N.real).astype(int)
    integrality_error = np.max(np.abs(N.real - N_int))
    imaginary_error = np.max(np.abs(N.imag))

    return {
        'p': p, 'q': q,
        'n_modules': n_modules,
        'fusion_tensor': N_int,
        'integrality_error': float(integrality_error),
        'imaginary_error': float(imaginary_error),
        'is_integral': integrality_error < 1e-6,
        'vacuum_trivial': all(N_int[0, j, j] == 1 for j in range(n))
                          and all(N_int[0, j, m] == 0
                                  for j in range(n) for m in range(n) if m != j),
    }


