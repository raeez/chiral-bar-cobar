"""Genus-3 Killing correction for sl_2 Kac-Moody (task B9).

Verifies that the genus-3 (and genus-4, 5) homotopy transfer correction
epsilon_g vanishes for sl_2 KM, using BOTH the strict dg Lie argument
AND the full L-infinity structure on the minimal model.

=== Background ===

The universal MC class Theta = sum_g theta_g satisfies the MC equation
on Def_cyc(A) hat-tensor G_mod. At genus g the equation reads:

    l_1(theta_g) + (1/2) sum_{g1+g2=g} l_2(theta_{g1}, theta_{g2})
                 + (1/6) sum_{g1+g2+g3=g} l_3(theta_{g1}, theta_{g2}, theta_{g3})
                 + ... = 0

The HTT correction epsilon_g measures the failure of theta_g to be a
cocycle. For KM algebras, the genus-2 computation (genus2_htt.py) showed
epsilon_2 = 0 because:
  (i)  l_2(theta_1, theta_1) = 0 by Jacobi,
  (ii) l_n = 0 for n >= 3 on the original dg Lie complex (strict).

=== This Module: Two Independent Vanishing Arguments ===

**Argument 1 (strict dg Lie)**:
For KM algebras, the cyclic deformation complex Def_cyc(g_kappa) is a
STRICT dg Lie algebra: l_1 = d (CE differential), l_2 = [-,-]_NR
(Nijenhuis-Richardson bracket), and l_n = 0 for n >= 3. Therefore:
    epsilon_g = -(1/6) h * l_3(theta_1, theta_1, theta_1) = 0
since l_3 = 0.

**Argument 2 (degree obstruction on minimal model)**:
Even using the FULL transferred L-infinity structure on the minimal
model H*(Def_cyc) — which has a nontrivial l_3 (the Killing 3-cocycle
phi(a,b,c) = kap([a,b],c)) — the correction still vanishes for DEGREE
REASONS.

The cyclic deformation complex of sl_2:
    Def^0 = g              (dim 3)   — infinitesimal automorphisms
    Def^1 = Lambda^2(g*)   (dim 3)   — deformations (via Killing iso)
    Def^2 = Lambda^3(g*)   (dim 1)   — obstructions

The minimal model H = H*(Def_cyc) for semisimple g:
    H^0 = 0 (Whitehead: H^1(g) = 0)
    H^1 = 0 (Whitehead: H^2(g) = 0)
    H^2 = C (generated by [phi] = Killing 3-cocycle class)

So H is concentrated in degree 2 (one-dimensional).

The theta_1 class lives in H^2: theta_1 = kappa * [phi] tensor lambda_1.

The transferred l_3 has degree 2-3 = -1 (L-infinity convention: l_n has
degree 2-n). Applied to three inputs of degree 2:

    l_3(theta_1, theta_1, theta_1) has degree 2+2+2+(-1) = 5.

But H^5 = 0 (the complex ends at degree 2), so l_3(theta_1, theta_1, theta_1) = 0
for degree reasons, regardless of the specific form of the transferred l_3.

This degree vanishing is STRONGER than the strict dg Lie argument: it
applies to ANY L-infinity structure on the minimal model, not just the
one coming from the CE complex.

=== Antisymmetry Argument ===

There is a third independent argument. The transferred l_3 on the minimal
model is an antisymmetric multilinear map. When applied to three copies
of the SAME element theta_1:

    l_3(theta_1, theta_1, theta_1) = sgn(sigma) * l_3(theta_1, theta_1, theta_1)

for any permutation sigma of the three inputs (by graded antisymmetry).
Since theta_1 has degree 2 (even), swapping any two inputs gives:
    l_3(theta_1, theta_1, theta_1) = (-1)^{2*2+1} * l_3(theta_1, theta_1, theta_1)
                                    = -l_3(theta_1, theta_1, theta_1)
    => l_3(theta_1, theta_1, theta_1) = 0.

Wait — the Koszul sign for swapping two degree-2 elements in an L-infinity
operation of degree -1 involves (-1)^{|a||b|} = (-1)^4 = +1 for the element
swap, plus the antisymmetry sign of l_3 itself. The net sign for a transposition
of two degree-2 inputs is (-1)^{2*2} * (-1) = -1. So:
    l_3(theta_1, theta_1, theta_1) = -l_3(theta_1, theta_1, theta_1) = 0.

This argument works for ANY l_3 and any even-degree theta_1.

CONVENTIONS:
- Cohomological grading: |d| = +1
- L-infinity: l_n has degree 2-n
- sl_2 basis: e=0, h=1, f=2
- Killing form: (e,f) = (f,e) = 1, (h,h) = 2
"""

