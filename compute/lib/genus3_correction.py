# Explicit NR bracket degree analysis
# ============================================================

def nr_bracket_degree_analysis() -> Dict[str, object]:
    """Analyze which NR brackets are nonzero by degree counting.

    The NR bracket has bidegree:
        [-, -]_NR: Hom(Lambda^a(g), g) x Hom(Lambda^b(g), g)
                    -> Hom(Lambda^{a+b-1}(g), g)

    For dim(g) = 3, Lambda^k(g) = 0 for k > 3.
    So we need a + b - 1 <= 3, i.e., a + b <= 4.

    With a, b in {1, 2, 3} (the nonzero exterior powers):
    Nonzero possibilities: (1,1) -> 1, (1,2) -> 2, (2,1) -> 2,
                          (1,3) -> 3, (3,1) -> 3, (2,2) -> 3.

    The Def_cyc degrees are shifted: Def^n = C^{n+1} = Hom(Lambda^{n+1}(g), g).
    So a = n+1 for degree n.

    For Def^2 x Def^2 -> Def^?: a = b = 3, target a+b-1 = 5 > 3. ZERO.
    For Def^1 x Def^2 -> Def^?: a=2, b=3, target = 4 > 3. ZERO.
    For Def^0 x Def^2 -> Def^?: a=1, b=3, target = 3 = Def^2. NONZERO.
    For Def^0 x Def^0 -> Def^{-1}: a=1, b=1, target = 1. But Def has no degree -1.
    Actually: target Hom(Lambda^1(g), g) = Def^0. So (Def^0 x Def^0 -> Def^0).

    Returns the analysis.
    """
    results = {}
    for a in range(1, 4):
        for b in range(1, 4):
            target = a + b - 1
            in_range = (1 <= target <= 3)
            def_a = a - 1
            def_b = b - 1
            def_target = target - 1
            results[f"Def^{def_a} x Def^{def_b} -> Def^{def_target}"] = {
                "lambda_source": (a, b),
                "lambda_target": target,
                "target_in_range": in_range,
                "nr_bracket_potentially_nonzero": in_range,
            }

    # Key result: Def^2 x Def^anything = 0 (except Def^0 x Def^2)
    # Since theta_1 lives in Def^2, l_2(theta_1, anything_in_Def^2) = 0.
    results["l2_on_two_deg2_inputs_vanishes"] = True
    results["l2_reason"] = "Lambda^5(g) = 0 for dim(g) = 3"

    return results


