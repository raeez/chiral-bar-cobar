@lru_cache(maxsize=128)
def bernoulli_number(n: int) -> Fraction:
    r"""Bernoulli number B_n. Convention: B_1 = -1/2."""
    if n < 0:
        raise ValueError(f"n must be >= 0, got {n}")
    if n == 0:
        return F(1)
    if n == 1:
        return F(-1, 2)
    if n % 2 == 1 and n >= 3:
        return F(0)
    a = [F(1, k + 1) for k in range(n + 1)]
    for j in range(n):
        for k in range(n - j):
            a[k] = F(k + 1) * (a[k] - a[k + 1])
    return a[0]


@lru_cache(maxsize=64)
def lambda_fp(g: int) -> Fraction:
    r"""Faber-Pandharipande intersection number lambda_g^{FP}.

    lambda_g^{FP} = (2^{2g-1} - 1) / 2^{2g-1} * |B_{2g}| / (2g)!

    This is the coefficient in the A-hat generating function:
      (t/2)/sin(t/2) - 1 = sum_{g>=1} lambda_g^{FP} * t^{2g}

    Verified values:
      g=1: 1/24
      g=2: 7/5760
      g=3: 31/967680
      g=4: 127/154828800
      g=5: 73/3503554560
      g=6: 1414477/2678117105664000
      g=7: 8191/612141052723200
    """
    if g < 1:
        raise ValueError(f"Genus must be >= 1, got {g}")
    B_2g = bernoulli_number(2 * g)
    power = 2 ** (2 * g - 1)
    return F(power - 1, power) * abs(B_2g) / F(math.factorial(2 * g))


@lru_cache(maxsize=64)
def lambda_fp_via_ahat(g: int) -> Fraction:
    r"""Compute lambda_g^{FP} via A-hat generating function (independent path).

    Ahat(it) = (t/2)/sin(t/2) = sum_{k>=0} (2^{2k}-2)|B_{2k}|/(2k)! * t^{2k}
    (with (2^0-2)|B_0|/0! = -1 at k=0 by convention; the constant term is 1).

    The coefficient of t^{2g} for g >= 1 is lambda_g^{FP}.
    """
    B_2g = bernoulli_number(2 * g)
    power = 2 ** (2 * g - 1)
    return F(power - 1, power) * abs(B_2g) / F(math.factorial(2 * g))


def lambda_fp_via_raw_bernoulli(g: int) -> Fraction:
    r"""Compute lambda_g^{FP} via raw Bernoulli number computation (third path).

    Decompose into: prefactor * |B_{2g}| / (2g)!
    where prefactor = (2^{2g-1} - 1) / 2^{2g-1}.

    Verify the Bernoulli number independently before combining.
    """
    n = 2 * g
    # Compute B_{2g} from scratch using Akiyama-Tanigawa algorithm
    a = [F(1, k + 1) for k in range(n + 1)]
    for j in range(n):
        for k in range(n - j):
            a[k] = F(k + 1) * (a[k] - a[k + 1])
    B_2g = a[0]

    prefactor = F(2 ** (2 * g - 1) - 1, 2 ** (2 * g - 1))
    factorial_2g = F(math.factorial(n))
    return prefactor * abs(B_2g) / factorial_2g


