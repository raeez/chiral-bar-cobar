# Section 7: Asymptotic analysis of lambda_g
# ============================================================================

def lambda_g_asymptotics(g: int) -> Dict[str, Any]:
    r"""Asymptotic analysis of lambda_g^FP.

    For large g:
      |B_{2g}| ~ 2 * (2g)! / (2*pi)^{2g}  (leading term of the asymptotic expansion)
      (2^{2g-1}-1)/2^{2g-1} -> 1

    So lambda_g ~ 2 / (2*pi)^{2g} = 2 / (4*pi^2)^g.

    The ratio to the asymptotic:
      lambda_g / (2 / (2*pi)^{2g}) -> 1 as g -> infinity.
    """
    exact = lambda_fp(g)
    exact_float = float(exact)

    asymptotic = 2.0 / (2.0 * math.pi) ** (2 * g)
    ratio_to_asymptotic = exact_float / asymptotic if asymptotic > 0 else float('inf')

    # Correction factor from zeta function: |B_{2g}| = 2*(2g)!/(2*pi)^{2g} * zeta(2g)
    # and zeta(2g) -> 1 as g -> infinity.
    # More precisely: |B_{2g}| = 2*(2g)!/(2*pi)^{2g} * (1 + 1/2^{2g} + 1/3^{2g} + ...)
    zeta_correction = 1.0 + 1.0 / 2.0 ** (2 * g) + 1.0 / 3.0 ** (2 * g)
    prefactor = (2.0 ** (2 * g - 1) - 1) / 2.0 ** (2 * g - 1)

    return {
        'g': g,
        'lambda_g_exact': exact,
        'lambda_g_float': exact_float,
        'asymptotic': asymptotic,
        'ratio_to_asymptotic': ratio_to_asymptotic,
        'prefactor': prefactor,
        'zeta_correction': zeta_correction,
        'corrected_asymptotic': asymptotic * prefactor * zeta_correction,
    }


