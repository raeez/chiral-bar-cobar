def verify_lambda_g_generating_function(max_g: int = 14) -> Dict[str, Any]:
    r"""Verify the generating function identity:

      sum_{g>=1} lambda_g * t^{2g} = (t/2)/sin(t/2) - 1

    at several values of t. For small t, few terms suffice; near the
    radius 2*pi, many terms are needed. We adapt the number of terms
    and tolerance accordingly.
    """
    # Test at small t where max_g terms give high accuracy
    test_points_small = [0.5, 1.0, 1.5, 2.0]
    results = {}

    for t in test_points_small:
        partial = sum(float(lambda_fp(g)) * t ** (2 * g)
                      for g in range(1, max_g + 1))
        closed = shadow_pf_closed_form(t)
        diff = abs(partial - closed)
        results[t] = {
            'partial_sum': partial,
            'closed_form': closed,
            'difference': diff,
            'match': diff < 1e-12,
        }

    # At t=3.0 and t=5.0, use more terms for convergence
    for t, n_terms in [(3.0, 30), (5.0, 60)]:
        partial = sum(float(lambda_fp(g)) * t ** (2 * g)
                      for g in range(1, n_terms + 1))
        closed = shadow_pf_closed_form(t)
        diff = abs(partial - closed)
        # Tolerance proportional to (t/(2*pi))^{2*n_terms} (truncation error)
        tol = 1e-6 if t <= 3.0 else 1e-3
        results[t] = {
            'partial_sum': partial,
            'closed_form': closed,
            'difference': diff,
            'match': diff < tol,
        }

    return {
        'test_points': results,
        'max_g': max_g,
        'all_match': all(r['match'] for r in results.values()),
        'identity': 'sum_{g>=1} lambda_g * t^{2g} = (t/2)/sin(t/2) - 1',
    }


def verify_bernoulli_consistency(max_g: int = 10) -> Dict[str, bool]:
    r"""Cross-verify Bernoulli numbers via multiple identities.

    Identity 1: sum_{k=0}^n C(n+1,k) B_k = 0 for n >= 1.
    Identity 2: B_{2g} = (-1)^{g+1} * 2 * (2g)! / (2*pi)^{2g} * zeta(2g).
    Identity 3: B_{2g} = (-1)^{g+1} * 2 * (2g)! * sum_{n>=1} 1/n^{2g} / (2*pi)^{2g}.
    """
    results = {}

    # Identity 1: recurrence
    for n in range(1, 2 * max_g + 1):
        s = sum(
            F(math.comb(n + 1, k)) * bernoulli_number(k)
            for k in range(n + 1)
        )
        results[f'recurrence_n={n}'] = (s == F(0))

    # Identity 2: numerical check via zeta (approximate)
    # Use more terms for small g where convergence is slower
    for g in range(1, max_g + 1):
        B_exact = float(bernoulli_number(2 * g))
        N_terms = max(100000, 10 ** (2 * g))  # More terms for slowly converging zeta(2)
        N_terms = min(N_terms, 500000)
        zeta_2g = sum(1.0 / n ** (2 * g) for n in range(1, N_terms + 1))
        B_approx = ((-1) ** (g + 1) * 2 * math.factorial(2 * g)
                     / (2 * math.pi) ** (2 * g) * zeta_2g)
        # Tolerance scales with convergence rate: zeta(2) converges as 1/N,
        # zeta(4) as 1/N^3, etc. Allow 1e-4 for g=1, tighter for g >= 2.
        tol = 1e-4 if g == 1 else 1e-6
        results[f'zeta_g={g}'] = abs(B_exact - B_approx) / abs(B_exact) < tol

    return results


