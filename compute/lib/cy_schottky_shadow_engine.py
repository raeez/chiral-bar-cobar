@dataclass
class ShadowAmplitude:
    """Shadow amplitude F_g = kappa * lambda_g^FP at genus g.

    This is a rational number: the tautological intersection number
    on M-bar_g. It does not depend on moduli.
    """
    genus: int
    kappa: Fraction

    @property
    def lambda_g(self) -> Fraction:
        return lambda_fp(self.genus)

    @property
    def F_g(self) -> Fraction:
        return self.kappa * self.lambda_g

    @property
    def F_g_float(self) -> float:
        return float(self.F_g)


def shadow_amplitude(g: int, kappa: Fraction = F(1)) -> ShadowAmplitude:
    """Construct the shadow amplitude F_g = kappa * lambda_g^FP."""
    return ShadowAmplitude(genus=g, kappa=kappa)


def compute_F_g_three_paths(g: int, kappa: Fraction = F(1)) -> Dict[str, Any]:
    r"""Compute F_g via three independent paths.

    Path 1: Direct formula F_g = kappa * lambda_g^FP.
    Path 2: A-hat generating function coefficient.
    Path 3: Raw Bernoulli computation.

    Returns dict with values from each path and agreement check.
    """
    path1 = kappa * lambda_fp(g)
    path2 = kappa * lambda_fp_via_ahat(g)
    path3 = kappa * lambda_fp_via_raw_bernoulli(g)

    return {
        'g': g,
        'kappa': kappa,
        'F_g': path1,
        'F_g_float': float(path1),
        'path_direct': path1,
        'path_ahat': path2,
        'path_raw_bernoulli': path3,
        'all_agree': (path1 == path2 == path3),
        'lambda_g': lambda_fp(g),
    }


def shadow_tower_table(max_g: int = 7, kappa: Fraction = F(1)) -> Dict[int, Dict[str, Any]]:
    """Complete shadow tower table for g = 1, ..., max_g."""
    return {g: compute_F_g_three_paths(g, kappa) for g in range(1, max_g + 1)}


