def theta_char_count(g: int) -> Dict[str, int]:
    """Count even and odd theta characteristics at genus g.

    Total = 2^{2g}.
    Even = 2^{g-1}(2^g + 1).
    Odd = 2^{g-1}(2^g - 1).

    These are the characteristics m in (Z/2Z)^{2g} classified by
    the parity of the associated theta function theta[m](z, Omega).
    """
    total = 2 ** (2 * g)
    even = 2 ** (g - 1) * (2 ** g + 1)
    odd = 2 ** (g - 1) * (2 ** g - 1)
    return {
        'genus': g,
        'total': total,
        'even': even,
        'odd': odd,
        'check': even + odd == total,
    }


def theta_char_table(max_g: int = 8) -> Dict[int, Dict[str, int]]:
    """Theta characteristic counts for g = 1, ..., max_g."""
    return {g: theta_char_count(g) for g in range(1, max_g + 1)}


