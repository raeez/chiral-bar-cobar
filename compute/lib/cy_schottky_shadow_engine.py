def dim_M_g(g: int) -> int:
    """Dimension of M_g = moduli of genus-g curves."""
    if g == 0:
        return 0  # M_0 is a point (with automorphisms)
    if g == 1:
        return 1  # M_1 = H/SL(2,Z)
    return 3 * g - 3


def dim_A_g(g: int) -> int:
    """Dimension of A_g = moduli of g-dimensional ppavs.

    A_g = Sp(2g,Z) \\ H_g where H_g is the Siegel upper half-space.
    dim_C H_g = g(g+1)/2.
    """
    return g * (g + 1) // 2


def schottky_codimension(g: int) -> int:
    r"""Codimension of J_g = j(M_g) in A_g.

    codim = dim A_g - dim M_g
          = g(g+1)/2 - (3g-3)     for g >= 2
          = (g^2 - 5g + 6)/2
          = (g-2)(g-3)/2

    Values:
      g=1: 0 (isomorphism)
      g=2: 0 (birational)
      g=3: 0 (injective, open dense)
      g=4: 1 (FIRST genuine Schottky)
      g=5: 3
      g=6: 6
      g=10: 28 (first genus where codim > dim_M_g)
    """
    if g <= 1:
        return 0
    return dim_A_g(g) - dim_M_g(g)


def schottky_codimension_formula(g: int) -> int:
    """Closed-form formula: codim = (g-2)(g-3)/2 for g >= 2."""
    if g <= 1:
        return 0
    return (g - 2) * (g - 3) // 2


def schottky_table(max_g: int = 20) -> Dict[int, Dict[str, Any]]:
    r"""Complete Schottky problem data for g = 1, ..., max_g.

    Computes dimensions, codimensions, Torelli type, and the
    codim/dim_M ratio to identify when the Jacobian locus becomes
    negligible.
    """
    results = {}
    for g in range(1, max_g + 1):
        d_M = dim_M_g(g)
        d_A = dim_A_g(g)
        codim = schottky_codimension(g)
        codim_formula = schottky_codimension_formula(g)

        # Torelli type classification
        if g == 1:
            torelli_type = "isomorphism"
        elif g == 2:
            torelli_type = "birational"
        elif g == 3:
            torelli_type = "injective, codim 0 (open dense)"
        else:
            torelli_type = f"injective, codim {codim} (Schottky)"

        codim_over_dim_M = F(codim, d_M) if d_M > 0 else F(0)

        results[g] = {
            'genus': g,
            'dim_M_g': d_M,
            'dim_A_g': d_A,
            'codim': codim,
            'codim_formula': codim_formula,
            'formula_matches': codim == codim_formula,
            'torelli_type': torelli_type,
            'has_schottky': codim > 0,
            'codim_over_dim_M': codim_over_dim_M,
            'codim_exceeds_dim': codim > d_M,
        }
    return results


def schottky_crossover_genus() -> int:
    """Find the smallest genus g where codim(J_g, A_g) > dim(M_g).

    At this genus, the Jacobian locus is "negligible" in the sense
    that its codimension exceeds its own dimension.

    Answer: g = 10.
      dim M_10 = 27, dim A_10 = 55, codim = 28 > 27.
    """
    for g in range(2, 100):
        if schottky_codimension(g) > dim_M_g(g):
            return g
    return -1  # Should never reach here


