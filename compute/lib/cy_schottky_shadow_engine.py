def schottky_constraint_on_amplitudes(g: int) -> Dict[str, Any]:
    r"""How the Schottky problem constrains genus-g amplitudes.

    For g <= 3: no Schottky constraint (Torelli is surjective onto
    the closure of J_g in A_g).

    For g = 4: one constraint (J = 0 is a codimension-1 condition).
    The genus-4 partition function Z_4(Omega) is restricted to the
    hypersurface {J = 0} in A_4.

    For g >= 5: multiple constraints. The number of independent
    Schottky-type relations grows with (g-2)(g-3)/2.

    The shadow amplitude F_g is INSULATED from all Schottky constraints:
    it is a tautological intersection number on M-bar_g, computed
    purely from the Hodge bundle. The Schottky constraints act on the
    analytic partition function Z_g(Omega), not on the topological
    skeleton.
    """
    codim = schottky_codimension(g)

    result = {
        'genus': g,
        'schottky_codimension': codim,
        'has_constraint': codim > 0,
        'num_independent_constraints': codim,
        'shadow_insulated': True,
        'reason_insulated': (
            'F_g is a tautological intersection number on M-bar_g, '
            'computed from Hodge classes. It does not depend on the '
            'embedding M_g -> A_g and is insensitive to Schottky.'
        ),
    }

    if g == 4:
        result['schottky_form'] = schottky_igusa_form()
        result['constraint_type'] = 'Single equation: J(Omega) = 0'
    elif g >= 5:
        result['constraint_type'] = (
            f'{codim} independent equations cutting out J_{g} in A_{g}'
        )

    return result


