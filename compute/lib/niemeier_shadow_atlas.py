def _orthogonal_roots_in_component(family: str, n: int) -> int:
    """Number of roots orthogonal to a given root within a single simple component.

    A_n: (n-1)(n-2) for n >= 3, 0 for n=1,2.
    D_n: 2n^2 - 10n + 14 for n >= 4, 2 for n=3 (D_3 = A_3).
    E_6: 30,  E_7: 72,  E_8: 126.
    """
    if family == 'A':
        if n <= 2:
            return 0
        return (n - 1) * (n - 2)
    elif family == 'D':
        if n == 3:
            return 2
        if n == 4:
            return 6
        return 2 * n * n - 10 * n + 14
    elif family == 'E':
        return {6: 30, 7: 72, 8: 126}[n]
    else:
        raise ValueError(f"Unknown family: {family}")


def orthogonal_roots_per_root(label: str) -> Optional[int]:
    """Number of roots orthogonal to a root in the first component.

    Returns None for Leech (no roots).
    """
    if label == 'Leech':
        return None
    data = NIEMEIER_REGISTRY[label]
    if not data['components']:
        return None
    f0, n0 = data['components'][0]
    orth_within = _orthogonal_roots_in_component(f0, n0)
    other_roots = sum(root_count(f, n) for f, n in data['components'][1:])
    return orth_within + other_roots


def genus2_diag11_rep(label: str) -> int:
    r"""Genus-2 representation number at T = diag(1,1).

    r_2(Lambda, diag(1,1)) = sum_{alpha root} #{beta root : <alpha,beta>=0}.
    """
    if label == 'Leech':
        return 0
    data = NIEMEIER_REGISTRY[label]
    components = data['components']
    N_total = data['num_roots']
    if not components:
        return 0
    total = 0
    sum_sq = 0
    for f, n in components:
        nr = root_count(f, n)
        ow = _orthogonal_roots_in_component(f, n)
        total += nr * ow
        sum_sq += nr * nr
    total += N_total * N_total - sum_sq
    return total


