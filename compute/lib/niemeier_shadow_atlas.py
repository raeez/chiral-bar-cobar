def root_count(family: str, n: int) -> int:
    """Number of roots in a simple root system of type family_n.

    A_n: n(n+1) roots.
    D_n: 2n(n-1) roots.
    E_6: 72,  E_7: 126,  E_8: 240.
    """
    if family == 'A':
        return n * (n + 1)
    elif family == 'D':
        if n < 3:
            raise ValueError(f"D_n requires n >= 3, got {n}")
        return 2 * n * (n - 1)
    elif family == 'E':
        return {6: 72, 7: 126, 8: 240}[n]
    else:
        raise ValueError(f"Unknown family: {family}")


def coxeter_number(family: str, n: int) -> int:
    """Coxeter number h of a simple root system.

    A_n: h = n + 1.
    D_n: h = 2(n - 1).
    E_6: 12,  E_7: 18,  E_8: 30.
    """
    if family == 'A':
        return n + 1
    elif family == 'D':
        return 2 * (n - 1)
    elif family == 'E':
        return {6: 12, 7: 18, 8: 30}[n]
    else:
        raise ValueError(f"Unknown family: {family}")


def dual_coxeter_number(family: str, n: int) -> int:
    """Dual Coxeter number h^vee.  For simply-laced types, h^vee = h."""
    return coxeter_number(family, n)


def root_lattice_det(family: str, n: int) -> int:
    """Determinant of the Cartan matrix (= dual lattice index).

    A_n: n + 1.   D_n: 4.   E_6: 3.   E_7: 2.   E_8: 1.
    """
    if family == 'A':
        return n + 1
    elif family == 'D':
        return 4
    elif family == 'E':
        return {6: 3, 7: 2, 8: 1}[n]
    else:
        raise ValueError(f"Unknown family: {family}")


