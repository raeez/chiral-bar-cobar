def verify_all_shadow_identical() -> bool:
    """Verify shadow obstruction tower is identical for all 24."""
    ref = shadow_data(ALL_NIEMEIER_LABELS[0])
    for label in ALL_NIEMEIER_LABELS[1:]:
        sd = shadow_data(label)
        for key in ['kappa', 'cubic_shadow', 'quartic_contact', 'S3', 'S4',
                     'critical_discriminant', 'shadow_depth', 'shadow_class',
                     'shadow_metric', 'alpha', 'central_charge']:
            if sd[key] != ref[key]:
                return False
    return True


def verify_theta_integrality(max_n: int = 10) -> bool:
    """Verify all theta coefficients are non-negative integers."""
    for label in ALL_NIEMEIER_LABELS:
        for n in range(max_n + 1):
            r = theta_coefficient(label, n)
            if not isinstance(r, int) or r < 0:
                return False
    return True


def verify_root_count_at_n1() -> bool:
    """Verify r(1) = |R| for all Niemeier lattices."""
    for label in ALL_NIEMEIER_LABELS:
        expected = NIEMEIER_REGISTRY[label]['num_roots']
        actual = theta_coefficient(label, 1)
        if actual != expected:
            return False
    return True


def verify_complementarity_sum_zero() -> bool:
    """Verify kappa + kappa' = 0 for all."""
    for label in ALL_NIEMEIER_LABELS:
        comp = complementarity(label)
        if comp['kappa_sum'] != 0:
            return False
    return True


def coxeter_analysis() -> Dict[str, Any]:
    """Coxeter number analysis across all 24 Niemeier lattices."""
    analysis = {}
    for label in ALL_NIEMEIER_LABELS:
        data = NIEMEIER_REGISTRY[label]
        h_vals = data['coxeter_numbers']
        if h_vals:
            all_same = all(h == h_vals[0] for h in h_vals)
            analysis[label] = {
                'coxeter_numbers': h_vals,
                'uniform_h': all_same,
                'h': h_vals[0] if all_same else None,
            }
        else:
            analysis[label] = {
                'coxeter_numbers': [],
                'uniform_h': True,
                'h': None,
            }
    by_h: Dict[int, List[str]] = {}
    for label, info in analysis.items():
        if isinstance(info, dict) and 'h' in info and info['h'] is not None:
            h = info['h']
            if h not in by_h:
                by_h[h] = []
            by_h[h].append(label)
    analysis['by_coxeter_number'] = by_h
    return analysis


