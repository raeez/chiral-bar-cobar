def genus_expansion(max_g: int = 5) -> Dict[int, Rational]:
    """Genus expansion F_g for ANY Niemeier lattice VOA.

    F_g = 24 * lambda_g^FP.  Identical for all 24 lattices.
    """
    return {g: Rational(24) * faber_pandharipande(g)
            for g in range(1, max_g + 1)}


def F_g(g: int) -> Rational:
    """Single genus-g amplitude for any Niemeier lattice."""
    return Rational(24) * faber_pandharipande(g)


