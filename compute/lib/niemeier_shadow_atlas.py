@lru_cache(maxsize=500)
def _sigma_k(n: int, k: int) -> int:
    """Divisor sum sigma_k(n) = sum_{d|n} d^k."""
    if n <= 0:
        return 0
    return sum(d ** k for d in range(1, n + 1) if n % d == 0)


@lru_cache(maxsize=500)
def _ramanujan_tau(n: int) -> int:
    r"""Ramanujan tau function."""
    if n < 1:
        return 0
    N = n
    coeffs = [0] * (N + 1)
    coeffs[0] = 1
    for m in range(1, N + 1):
        for _ in range(24):
            for i in range(N, m - 1, -1):
                coeffs[i] -= coeffs[i - m]
    return coeffs[n - 1] if n - 1 <= N else 0


def c_delta(label: str) -> Fraction:
    r"""Coefficient c_Delta in Theta_Lambda = E_{12} + c_Delta * Delta.

    c_Delta = N_roots - 65520/691 = (691*N_roots - 65520)/691.
    """
    if label not in NIEMEIER_REGISTRY:
        raise ValueError(f"Unknown Niemeier lattice: {label}")
    N = NIEMEIER_REGISTRY[label]['num_roots']
    return Fraction(691 * N - 65520, 691)


def theta_coefficient(label: str, n: int) -> int:
    r"""Coefficient of q^n in the genus-1 theta series of a Niemeier lattice.

    For n >= 1:
      r(n) = (65520*sigma_11(n) + (691*N_roots - 65520)*tau(n)) / 691
    """
    if n < 0:
        return 0
    if n == 0:
        return 1
    N = NIEMEIER_REGISTRY[label]['num_roots']
    sig11 = _sigma_k(n, 11)
    tau_n = _ramanujan_tau(n)
    numer = 65520 * sig11 + (691 * N - 65520) * tau_n
    assert numer % 691 == 0, (
        f"Non-integral theta coefficient for {label} at n={n}"
    )
    result = numer // 691
    assert result >= 0, (
        f"Negative theta coefficient for {label} at n={n}: {result}"
    )
    return result


@lru_cache(maxsize=100)
def theta_series(label: str, max_n: int = 10) -> Tuple[int, ...]:
    """First max_n+1 coefficients of the theta series."""
    return tuple(theta_coefficient(label, n) for n in range(max_n + 1))


