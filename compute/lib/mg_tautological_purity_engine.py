def w3_cross_channel_analytic(c: Fraction) -> Dict[str, Fraction]:
    r"""Analytic formula for the W_3 genus-2 cross-channel correction.

    From rem:w3-genus2-cross-channel and direct computation:

    F_2^{full}(W_3) = (7c/6912) + (c + 204)/(16c)

    where:
      7c/6912 = kappa * lambda_2^FP = (5c/6) * (7/5760)
      (c + 204)/(16c) = delta_F2 (cross-channel correction)

    The cross-channel correction comes from four sources:
      banana mixed: 3/c
      theta mixed: 9/(2c)
      lollipop mixed: 1/16
      barbell mixed: 21/(4c)

    Total: 3/c + 9/(2c) + 1/16 + 21/(4c)
         = 48/(16c) + 72/(16c) + c/(16c) + 84/(16c)
         = (c + 204)/(16c)

    Verification at specific values:
      c=2: delta = (2+204)/(32) = 206/32 = 103/16
      c=10: delta = 214/160 = 107/80
      c=50: delta = 254/800 = 127/400
    """
    if c == 0:
        raise ValueError("c=0 is singular for W_3")

    kappa = Fraction(5) * c / 6
    F2_scalar = kappa * lambda_fp(2)

    # Analytic cross-channel components
    banana_mixed = Fraction(3) / c
    theta_mixed = Fraction(9) / (2 * c)
    lollipop_mixed = Fraction(1, 16)
    barbell_mixed = Fraction(21) / (4 * c)

    delta_F2 = banana_mixed + theta_mixed + lollipop_mixed + barbell_mixed
    # Simplify: 3/c + 9/(2c) + 1/16 + 21/(4c)
    #         = 48/(16c) + 72/(16c) + c/(16c) + 84/(16c) = (c + 204)/(16c)
    delta_check = (c + 204) / (16 * c)

    return {
        'kappa': kappa,
        'F2_scalar': F2_scalar,
        'banana_mixed': banana_mixed,
        'theta_mixed': theta_mixed,
        'lollipop_mixed': lollipop_mixed,
        'delta_F2': delta_F2,
        'delta_F2_closed_form': delta_check,
        'delta_agrees': delta_F2 == delta_check,
        'F2_full': F2_scalar + delta_F2,
        'delta_ratio': delta_F2 / F2_scalar if F2_scalar != 0 else None,
    }


def w3_purity_obstruction_class(c: Fraction) -> Dict[str, Any]:
    r"""The tautological purity obstruction for W_3 at genus 2.

    MATHEMATICAL CONTENT:

    The cross-channel correction delta_F2 = (c+204)/(16c) is an INTEGRATED
    quantity (a number). The underlying CLASS-LEVEL obstruction lives in
    H^*(M-bar_2, Q) and has components on different boundary strata.

    Decomposition of the obstruction class:
      The banana mixed term (3/c) lives on Delta_irr^{(2)}
        (two nonseparating nodes).
      The theta mixed term (9/(2c)) lives on Delta_0^{(3)}
        (compact-type three-component curves).
      The lollipop mixed term (1/16) lives on Delta_irr cap Delta_1
        (mixed stratum).

    KEY FINDING: the lollipop term 1/16 is INDEPENDENT OF c.
    This means the obstruction persists in the large-c limit (c -> infinity).
    The semi-classical limit does NOT restore purity.

    However: the RATIO delta_F2/F2_scalar = 432(c+204)/(7c^2) DOES
    vanish as c -> infinity (the correction becomes relatively small
    compared to the scalar prediction). So in a RELATIVE sense, purity
    is restored semi-classically, even though the ABSOLUTE correction persists.

    This has a physical interpretation: the cross-channel correction
    is a QUANTUM effect (it comes from loop diagrams with mixed channel
    assignments), and it is suppressed in the classical limit c -> infinity.
    But it is nonvanishing at any finite c.

    CONCLUSION: op:multi-generator-universality is NEGATIVE at genus 2
    for the INTEGRATED free energy. The formula F_2 = kappa * lambda_2^FP
    fails for W_3 (and generically for any multi-weight algebra with
    nonvanishing cross-channel structure constants).
    """
    analytic = w3_cross_channel_analytic(c)

    return {
        'c': c,
        'analytic': analytic,
        'obstruction_components': {
            'banana_stratum': analytic['banana_mixed'],
            'theta_stratum': analytic['theta_mixed'],
            'lollipop_stratum': analytic['lollipop_mixed'],
        },
        'c_independent_component': Fraction(1, 16),
        'large_c_behavior': "delta_F2 ~ 1/16 + 15/(2c) -> 1/16 as c -> inf",
        'relative_correction': analytic['delta_ratio'],
        'conclusion': (
            "The bar construction does NOT produce pure lambda_2 for W_3 at genus 2. "
            f"The cross-channel correction delta_F2 = (c+204)/(16c) = {analytic['delta_F2']} "
            "is nonvanishing for all c != 0. "
            "op:multi-generator-universality is NEGATIVE at the integrated level."
        ),
    }


