# The six stable graphs of (g=2, n=0) and their positions in the
# stratification of M-bar_2:
#
# Graph         Stratum              Codim   Tautological class generated
# -----         -------              -----   ----------------------------
# smooth        M_2 (interior)       0       lambda_2 (Hodge class)
# fig-eight     Delta_irr            1       lambda_1 (from genus-1 vertex)
# banana        Delta_irr^{(2)}      2       1 (from genus-0 vertex)
# dumbbell      Delta_1              1       lambda_1 x lambda_1
# theta         Delta_0^{(3)}        2       1 x 1 (from genus-0 vertices)
# lollipop      Delta_irr cap Delta_1 2      lambda_1 (from genus-1 vertex)
#
# The KEY observation: the smooth graph gives lambda_2, while the
# boundary graphs give LOWER Hodge classes or boundary classes.
# The FULL amplitude is a specific linear combination.
# For single-channel: this combination is ALWAYS proportional to lambda_2
# (by Heisenberg calibration + algebraic-family rigidity).
# For multi-channel: the boundary coefficients depend on per-channel data
# and need not produce a lambda_2-proportional combination.

@dataclass
class Genus2GraphAmplitude:
    """Amplitude of a single genus-2 stable graph with channel tracking."""
    graph_name: str
    aut_order: int
    # Per-channel decomposition
    diagonal_amplitude: Fraction = Fraction(0)  # all-same-channel
    mixed_amplitude: Fraction = Fraction(0)      # mixed channels
    per_channel_detail: Dict[str, Fraction] = field(default_factory=dict)

    @property
    def total(self) -> Fraction:
        return self.diagonal_amplitude + self.mixed_amplitude


def _compute_fig_eight(alg: MultiWeightAlgebra) -> Genus2GraphAmplitude:
    """Fig-eight graph: 1 genus-1 vertex with 1 self-loop.

    Amplitude = (1/2) * sum_i eta^{ii} * V_{1,2}(e_i, e_i)
    V_{1,2}(e_i, e_i) = kappa_i / 24 (per-channel genus-1 universality)
    eta^{ii} = 1/kappa_i

    A_fig_eight = (1/2) * sum_i (1/kappa_i) * (kappa_i/24) = (1/2) * r/24

    where r = number of channels. NO mixed terms (single self-loop).
    """
    total = Fraction(0)
    detail = {}
    for ch in alg.channels:
        amp = alg.propagator(ch) * alg.kappa[ch] / 24 / 2
        total += amp
        detail[ch] = amp

    return Genus2GraphAmplitude(
        graph_name='fig_eight',
        aut_order=2,
        diagonal_amplitude=total,
        mixed_amplitude=Fraction(0),
        per_channel_detail=detail,
    )


def _compute_banana(alg: MultiWeightAlgebra) -> Genus2GraphAmplitude:
    """Banana graph: 1 genus-0 vertex with 2 self-loops (4 half-edges).

    |Aut| = 8 (2 ways to pair each self-loop x 2 loop orientations).

    Amplitude = (1/8) * sum_{i,j channels for loop1, loop2}
                eta^{ii} * eta^{jj} * V_{0,4}(i,i,j,j)

    V_{0,4}(i,i,j,j) = sum_m eta^{mm} C_{iim} C_{jjm}

    For W_3: V_{0,4} = 2c for all (i,j) pairs (T-channel universality).
    """
    from itertools import product as cartprod
    diagonal = Fraction(0)
    mixed = Fraction(0)
    detail = {}
    aut = 8

    for i, j in cartprod(alg.channels, repeat=2):
        prop = alg.propagator(i) * alg.propagator(j)
        vertex = alg.V04(i, i, j, j)
        amp = prop * vertex / aut
        key = f"{i},{j}"
        detail[key] = amp
        if i == j:
            diagonal += amp
        else:
            mixed += amp

    return Genus2GraphAmplitude(
        graph_name='banana',
        aut_order=aut,
        diagonal_amplitude=diagonal,
        mixed_amplitude=mixed,
        per_channel_detail=detail,
    )


def _compute_dumbbell(alg: MultiWeightAlgebra) -> Genus2GraphAmplitude:
    """Dumbbell: 2 genus-1 vertices connected by 1 bridge.

    |Aut| = 2 (swap the two vertices).

    Amplitude = (1/2) * sum_i eta^{ii} * V_{1,1}(e_i) * V_{1,1}(e_i)
    V_{1,1}(e_i) = kappa_i / 24

    A_dumbbell = (1/2) * sum_i (1/kappa_i) * (kappa_i/24)^2
               = (1/2) * sum_i kappa_i / 576
    """
    diagonal = Fraction(0)
    detail = {}
    aut = 2

    for ch in alg.channels:
        amp = alg.propagator(ch) * (alg.kappa[ch] / 24) ** 2 / aut
        diagonal += amp
        detail[ch] = amp

    return Genus2GraphAmplitude(
        graph_name='dumbbell',
        aut_order=aut,
        diagonal_amplitude=diagonal,
        mixed_amplitude=Fraction(0),
        per_channel_detail=detail,
    )


def _compute_theta(alg: MultiWeightAlgebra) -> Genus2GraphAmplitude:
    """Theta graph: 2 genus-0 trivalent vertices, 3 bridges.

    |Aut| = 12 (S_3 on the 3 edges x Z_2 swapping vertices).

    Amplitude = (1/12) * sum_{i,j,k} eta^{ii} eta^{jj} eta^{kk}
                * C3(i,j,k) * C3(i,j,k)

    For W_3 with Z_2 symmetry: C3(i,j,k) = 0 if odd W-count.
    Nonvanishing assignments: (T,T,T), (T,W,W) and permutations.
    """
    from itertools import product as cartprod
    diagonal = Fraction(0)
    mixed = Fraction(0)
    detail = {}
    aut = 12

    for i, j, k in cartprod(alg.channels, repeat=3):
        prop = alg.propagator(i) * alg.propagator(j) * alg.propagator(k)
        c3_val = alg.C3_func(i, j, k)
        amp = prop * c3_val ** 2 / aut
        key = f"{i},{j},{k}"
        detail[key] = amp
        if len(set([i, j, k])) == 1:
            diagonal += amp
        else:
            mixed += amp

    return Genus2GraphAmplitude(
        graph_name='theta',
        aut_order=aut,
        diagonal_amplitude=diagonal,
        mixed_amplitude=mixed,
        per_channel_detail=detail,
    )


def _compute_lollipop(alg: MultiWeightAlgebra) -> Genus2GraphAmplitude:
    """Lollipop: genus-0 trivalent vertex + genus-1 univalent vertex,
    connected by 1 bridge + 1 self-loop on the genus-0 vertex.

    |Aut| = 2 (self-loop orientation).

    The genus-0 vertex has 3 half-edges: 2 from the self-loop (same channel i)
    and 1 from the bridge (channel j). So the vertex factor is C3(i,i,j).
    The genus-1 vertex has 1 half-edge from the bridge (channel j).
    Vertex factor: kappa_j / 24.

    Amplitude = (1/2) * sum_{i,j} eta^{ii} * eta^{jj} * C3(i,i,j) * (kappa_j/24)
    """
    from itertools import product as cartprod
    diagonal = Fraction(0)
    mixed = Fraction(0)
    detail = {}
    aut = 2

    for i, j in cartprod(alg.channels, repeat=2):
        prop = alg.propagator(i) * alg.propagator(j)
        c3_val = alg.C3_func(i, i, j)
        vertex_g1 = alg.kappa[j] / 24
        amp = prop * c3_val * vertex_g1 / aut
        key = f"loop={i},bridge={j}"
        detail[key] = amp
        if i == j:
            diagonal += amp
        else:
            mixed += amp

    return Genus2GraphAmplitude(
        graph_name='lollipop',
        aut_order=aut,
        diagonal_amplitude=diagonal,
        mixed_amplitude=mixed,
        per_channel_detail=detail,
    )


def _compute_barbell(alg: MultiWeightAlgebra) -> Genus2GraphAmplitude:
    """Barbell: 2 genus-0 trivalent vertices, each with a self-loop,
    connected by 1 bridge.

    |Aut| = 8 (2 self-loop flips x 2 vertex swap when genera equal).

    The genus-0 vertex v0 has 3 half-edges: 2 from its self-loop (channel i)
    and 1 from the bridge (channel k). Vertex factor: C3(i, i, k).
    The genus-0 vertex v1 has 3 half-edges: 2 from its self-loop (channel j)
    and 1 from the bridge (channel k). Vertex factor: C3(j, j, k).

    Amplitude = (1/8) * sum_{i,j,k} eta^{ii} * eta^{jj} * eta^{kk}
                * C3(i,i,k) * C3(j,j,k)
    """
    from itertools import product as cartprod
    diagonal = Fraction(0)
    mixed = Fraction(0)
    detail = {}
    aut = 8

    for i, j, k in cartprod(alg.channels, repeat=3):
        c3_v0 = alg.C3_func(i, i, k)
        c3_v1 = alg.C3_func(j, j, k)
        if c3_v0 == Fraction(0) or c3_v1 == Fraction(0):
            continue
        prop = alg.propagator(i) * alg.propagator(j) * alg.propagator(k)
        amp = prop * c3_v0 * c3_v1 / aut
        key = f"loop0={i},loop1={j},bridge={k}"
        detail[key] = amp
        if i == j == k:
            diagonal += amp
        else:
            mixed += amp

    return Genus2GraphAmplitude(
        graph_name='barbell',
        aut_order=aut,
        diagonal_amplitude=diagonal,
        mixed_amplitude=mixed,
        per_channel_detail=detail,
    )


