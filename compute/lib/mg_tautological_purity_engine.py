@dataclass
class TautologicalPurityResult:
    """Complete tautological purity analysis at genus 2.

    The central question: is obs_2(A) proportional to lambda_2?

    ANSWER STRUCTURE:
    - For single-channel algebras: YES (by calibration, lambda_2 is forced).
    - For multi-channel algebras: the diagonal part is proportional to lambda_2,
      but the mixed part introduces boundary corrections.

    The mixed/cross-channel correction:
        delta_F2 = F2_mixed / (kappa * lambda_2^FP)
    measures the failure of tautological purity at the integrated level.
    """
    algebra_name: str
    kappa_total: Fraction
    num_channels: int

    # Per-graph amplitudes
    graphs: Dict[str, Genus2GraphAmplitude]

    # Decomposition
    F2_scalar: Fraction       # kappa * lambda_2^FP (the universal prediction)
    F2_diagonal: Fraction     # sum of all-same-channel amplitudes
    F2_mixed: Fraction        # sum of mixed-channel amplitudes
    F2_total: Fraction        # F2_diagonal + F2_mixed

    # The key diagnostic: cross-channel correction
    delta_F2: Fraction        # = F2_mixed
    delta_ratio: Optional[Fraction]   # delta_F2 / F2_scalar

    # Tautological purity verdict
    is_pure_lambda2: bool     # True iff delta_F2 = 0
    purity_obstruction: str   # human-readable diagnosis

    def summary(self) -> str:
        lines = [
            f"=== Tautological Purity Analysis: {self.algebra_name} ===",
            f"Number of channels: {self.num_channels}",
            f"kappa_total = {self.kappa_total}",
            f"",
            f"Scalar prediction: F2 = kappa * lambda_2^FP = {self.F2_scalar}",
            f"Diagonal amplitude: {self.F2_diagonal}",
            f"Mixed amplitude: {self.F2_mixed}",
            f"Total boundary amplitude: {self.F2_total}",
            f"",
            f"Cross-channel correction: delta_F2 = {self.delta_F2}",
        ]
        if self.delta_ratio is not None:
            lines.append(f"Relative correction: delta_F2/F2_scalar = {self.delta_ratio}")
        lines.append(f"")
        lines.append(f"TAUTOLOGICAL PURITY: {'YES' if self.is_pure_lambda2 else 'NO'}")
        lines.append(f"Diagnosis: {self.purity_obstruction}")
        lines.append(f"")
        lines.append("Per-graph decomposition:")
        for name in ['fig_eight', 'banana', 'dumbbell', 'theta', 'lollipop', 'barbell']:
            g = self.graphs.get(name)
            if g:
                lines.append(f"  {name:12s}: diag={g.diagonal_amplitude}  "
                             f"mixed={g.mixed_amplitude}  total={g.total}")
        return "\n".join(lines)


def analyze_tautological_purity(alg: MultiWeightAlgebra) -> TautologicalPurityResult:
    """Full tautological purity analysis at genus 2 for a multi-weight algebra.

    Computes:
    1. Per-graph amplitudes with diagonal/mixed decomposition
    2. Total cross-channel correction
    3. Tautological purity verdict

    The MATHEMATICAL CONTENT:
    - For single-channel (uniform-weight) algebras: the diagonal amplitude
      equals kappa * lambda_2^FP, and there is no mixed part. Purity holds.
    - For multi-channel algebras: the mixed amplitude is generically nonzero,
      meaning the integrated free energy F_2 != kappa * lambda_2^FP.

    The integrated correction delta_F2 comes from four sources:
    (a) The banana graph: mixed channel assignments on the two self-loops
    (b) The theta graph: mixed channel assignments on the three bridges
    (c) The lollipop: mixed loop/bridge channel assignments
    (d) The barbell: mixed self-loop/bridge assignments on two trivalent vertices

    The fig-eight and dumbbell have no mixed terms (single edge each, or
    the vertex factor kills cross-channel by the diagonal metric).
    """
    # Compute per-graph amplitudes
    fig_eight = _compute_fig_eight(alg)
    banana = _compute_banana(alg)
    dumbbell = _compute_dumbbell(alg)
    theta = _compute_theta(alg)
    lollipop = _compute_lollipop(alg)
    barbell = _compute_barbell(alg)

    graphs = {
        'fig_eight': fig_eight,
        'banana': banana,
        'dumbbell': dumbbell,
        'theta': theta,
        'lollipop': lollipop,
        'barbell': barbell,
    }

    # Total diagonal and mixed amplitudes
    F2_diagonal = sum(g.diagonal_amplitude for g in graphs.values())
    F2_mixed = sum(g.mixed_amplitude for g in graphs.values())
    F2_total = F2_diagonal + F2_mixed

    # Scalar prediction
    fp2 = lambda_fp(2)
    F2_scalar = alg.kappa_total * fp2

    # Cross-channel correction
    delta_F2 = F2_mixed
    delta_ratio = None
    if F2_scalar != 0:
        delta_ratio = delta_F2 / F2_scalar

    # Purity verdict
    is_pure = (delta_F2 == 0)
    if is_pure:
        obstruction = "No cross-channel correction: obs_2 is lambda_2-pure."
    elif alg.kappa_total == 0:
        obstruction = "kappa = 0: degenerate case."
    else:
        obstruction = (
            f"Cross-channel correction delta_F2 = {delta_F2} != 0. "
            f"The genus-2 amplitude has non-lambda_2 boundary components. "
            f"This is EVIDENCE (not proof) that obs_2 is not proportional to lambda_2."
        )

    return TautologicalPurityResult(
        algebra_name=alg.name,
        kappa_total=alg.kappa_total,
        num_channels=len(alg.channels),
        graphs=graphs,
        F2_scalar=F2_scalar,
        F2_diagonal=F2_diagonal,
        F2_mixed=F2_mixed,
        F2_total=F2_total,
        delta_F2=delta_F2,
        delta_ratio=delta_ratio,
        is_pure_lambda2=is_pure,
        purity_obstruction=obstruction,
    )


