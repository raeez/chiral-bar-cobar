def vertex_purity_analysis(alg: MultiWeightAlgebra) -> Dict[str, Any]:
    r"""Analyze whether vertex contributions for the multi-weight algebra
    introduce non-lambda classes at genus 2.

    KEY INSIGHT (from AP27): all EDGES use E_1 (weight 1). The higher
    Hodge bundles E_h do NOT appear. But VERTICES carry cross-channel
    OPE data that is NOT controlled by kappa alone.

    The vertex factors at genus 0 are structure constants C_{ijk} from
    the genus-0 CohFT. These are NUMBERS (not classes on M-bar_2).
    However, they WEIGHT the contribution of each boundary stratum
    differently for different channel assignments.

    The question: when these differently-weighted boundary contributions
    are summed, do they assemble into a class proportional to lambda_2?

    ANSWER: NO, generically. The assembly into lambda_2 requires
    specific RELATIONS among the structure constants that hold for
    single-channel algebras (where all structure constants are powers
    of kappa) but fail for multi-channel algebras.

    Proof sketch:
    For single-channel: all C3 = kappa^{3/2} * (universal coefficient).
    The graph sum factors as kappa * (universal intersection number).
    Since the Heisenberg calibration gives lambda_2, the class is lambda_2.

    For multi-channel: C3(T,T,T) = c, C3(T,W,W) = c, but the
    propagators are eta^{TT} = 2/c, eta^{WW} = 3/c (DIFFERENT ratios).
    The mixed-channel amplitudes have weights that depend on the
    RATIO eta^{WW}/eta^{TT} = 3/2, which is NOT unity.
    This introduces a "twist" in the boundary assembly that prevents
    the sum from being proportional to lambda_2.
    """
    # Check if all propagator ratios are unity (necessary for purity)
    if len(alg.channels) <= 1:
        return {
            'num_channels': len(alg.channels),
            'propagator_ratio_unity': True,
            'vertex_purity': True,
            'conclusion': "Single-channel: vertices trivially pure.",
        }

    # Compute propagator ratios
    ch0 = alg.channels[0]
    prop0 = alg.propagator(ch0)
    ratios = {}
    all_unity = True
    for ch in alg.channels[1:]:
        ratio = alg.propagator(ch) / prop0
        ratios[f"eta^{{{ch}}}/eta^{{{ch0}}}"] = ratio
        if ratio != 1:
            all_unity = False

    # Compute the "twist factor" for each boundary graph
    # The twist factor measures how much the mixed-channel assignment
    # deviates from the universal (single-channel) assignment.
    banana_twist = Fraction(0)
    theta_twist = Fraction(0)
    from itertools import product as cartprod

    # Banana: sum_{i!=j} (eta^{ii} * eta^{jj}) / (eta^{i_0 i_0})^2
    if len(alg.channels) >= 2:
        prop_sq_sum = sum(alg.propagator(ch)**2 for ch in alg.channels)
        prop_cross = Fraction(0)
        for i, j in cartprod(alg.channels, repeat=2):
            if i != j:
                # V_{0,4}(i,i,j,j) * eta^{ii} * eta^{jj}
                prop_cross += alg.propagator(i) * alg.propagator(j) * alg.V04(i, i, j, j)
        banana_twist = prop_cross

    # Theta: sum over mixed (i,j,k) with C3(i,j,k)!=0
    theta_data = {}
    for i, j, k in cartprod(alg.channels, repeat=3):
        c3 = alg.C3_func(i, j, k)
        if c3 != 0 and len(set([i, j, k])) > 1:
            key = tuple(sorted([i, j, k]))
            amp = alg.propagator(i) * alg.propagator(j) * alg.propagator(k) * c3**2
            theta_data[key] = theta_data.get(key, Fraction(0)) + amp

    return {
        'num_channels': len(alg.channels),
        'propagator_ratios': ratios,
        'propagator_ratio_unity': all_unity,
        'banana_cross_factor': banana_twist,
        'theta_cross_terms': theta_data,
        'vertex_purity': all_unity and banana_twist == 0,
        'conclusion': (
            "Vertices are tautologically pure."
            if all_unity
            else "Propagator ratios != 1: vertices introduce non-lambda_2 boundary weights."
        ),
    }


