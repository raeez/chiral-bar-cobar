# Section 8: Per-stratum tautological class analysis
# ============================================================================

def per_stratum_analysis(alg: MultiWeightAlgebra) -> Dict[str, Any]:
    """Decompose the genus-2 amplitude per boundary stratum with
    class-level information.

    The genus-2 bar amplitude, viewed as a class in H^*(M-bar_2),
    decomposes as:
        obs_2 = (interior contribution) + (boundary contributions)

    Interior: proportional to lambda_2 (controlled by smooth graph).
    Boundary: lives on Delta_irr, Delta_1, and their intersections.

    For single-channel algebras:
        The boundary contributions assemble (via Mumford relations) into
        a class proportional to lambda_2. This is the content of the
        Heisenberg calibration + algebraic-family rigidity.

    For multi-channel algebras:
        The boundary contributions have DIFFERENT weights on different
        boundary strata, because the per-channel vertex factors are
        NOT proportional to each other. This breaks the Mumford relation
        assembly and introduces non-lambda_2 components.

    THE DECISIVE TEST:
    For single-channel at genus 2, the boundary graph amplitudes satisfy:
        A_fig_eight / A_dumbbell = (specific ratio from Mumford relations)
        A_banana / A_theta = (specific ratio from Mumford relations)
    These ratios are FIXED by the tautological ring structure.

    For multi-channel, the mixed-channel terms change these ratios,
    breaking the tautological assembly.
    """
    result = analyze_tautological_purity(alg)

    # Single-channel check: verify all ratios match
    n_ch = len(alg.channels)

    # Per-channel amplitude contributions
    per_channel_F2 = {}
    for ch in alg.channels:
        # The contribution from channel ch alone
        kappa_ch = alg.kappa[ch]
        F2_ch = kappa_ch * lambda_fp(2)
        per_channel_F2[ch] = F2_ch

    # Stratum-level decomposition
    # Each boundary graph lives on a specific stratum:
    #   fig-eight -> Delta_irr (nonseparating node)
    #   banana -> Delta_irr^{(2)} (two nonseparating nodes)
    #   dumbbell -> Delta_1 (separating node)
    #   theta -> Delta_0^{(3)} (stratum of compact type, 3 components)
    #   lollipop -> Delta_irr cap Delta_1 (mixed boundary)

    strata = {
        'Delta_irr': {
            'fig_eight': result.graphs['fig_eight'],
            'class': 'H^2 contribution via nonseparating degeneration',
        },
        'Delta_1': {
            'dumbbell': result.graphs['dumbbell'],
            'class': 'H^2 contribution via separating degeneration',
        },
        'Delta_irr_sq': {
            'banana': result.graphs['banana'],
            'class': 'H^4 contribution via double nonseparating degeneration',
        },
        'Delta_0_cube': {
            'theta': result.graphs['theta'],
            'class': 'H^4 contribution via triple bridge (compact type)',
        },
        'Delta_irr_Delta_1': {
            'lollipop': result.graphs['lollipop'],
            'class': 'H^4 contribution via mixed degeneration',
        },
    }

    # THE KEY RATIO TEST:
    # For a single-channel algebra with kappa:
    #   fig-eight = kappa/(2*24) = kappa/48
    #   dumbbell = kappa^2/(2*576) = kappa^2/1152 -> per-channel sum = sum kappa_i^2/1152
    #   banana = 2c/(8*eta^{ii}*eta^{jj}) -> depends on channel structure
    #
    # The ratio fig-eight/dumbbell for single-channel:
    #   (kappa/48) / (kappa^2/1152) = 1152/(48*kappa) = 24/kappa
    #
    # For multi-channel, the fig-eight has sum_i 1/48 (independent of kappa_i!)
    # and the dumbbell has sum_i kappa_i/1152.
    # The ratio: (r/48) / (sum kappa_i / 1152) = 24r / sum kappa_i
    # For single-channel (r=1): 24/kappa. MATCHES.
    # For W_3 (r=2): 48 / (5c/6) = 288/(5c). DIFFERENT from 24/kappa.

    fig_total = result.graphs['fig_eight'].total
    dumbbell_total = result.graphs['dumbbell'].total
    if dumbbell_total != 0:
        ratio_fig_dumbbell = fig_total / dumbbell_total
    else:
        ratio_fig_dumbbell = None

    # For lambda_2-purity, this ratio must equal 24/kappa_total
    kappa = alg.kappa_total
    if kappa != 0:
        expected_ratio = Fraction(24) / kappa
    else:
        expected_ratio = None

    ratio_matches = (ratio_fig_dumbbell is not None
                     and expected_ratio is not None
                     and ratio_fig_dumbbell == expected_ratio)

    return {
        'result': result,
        'strata': strata,
        'per_channel_F2': per_channel_F2,
        'ratio_fig_dumbbell': ratio_fig_dumbbell,
        'expected_ratio_for_purity': expected_ratio,
        'ratio_matches': ratio_matches,
        'purity_verdict': (
            "PURE: all stratum ratios match lambda_2 assembly"
            if result.is_pure_lambda2
            else "IMPURE: cross-channel terms break tautological assembly"
        ),
    }


