@dataclass
class StratumDecomposition:
    """Decomposition of a genus-2 graph amplitude across strata of M-bar_2.

    The boundary of M-bar_2 has two irreducible components:
      Delta_irr: closure of irreducible nodal curves (nonseparating node)
      Delta_1: closure of unions C_1 cup C_2 with g(C_1) = g(C_2) = 1

    The deeper strata (codimension >= 2) include:
      Delta_00: three-component curves (the theta graph stratum)
      Delta_{irr,irr}: two nonseparating nodes (the banana graph stratum)
      Delta_{1,irr}: one separating + one nonseparating node
    """
    # Contributions by graph type (each is a rational function of c)
    smooth: Fraction = Fraction(0)       # from the smooth (g=2, val=0) vertex
    fig_eight: Fraction = Fraction(0)    # 1-loop correction (g=1 vertex + self-loop)
    banana: Fraction = Fraction(0)       # 2-loop (g=0, val=4, 2 self-loops)
    dumbbell: Fraction = Fraction(0)     # 2 genus-1 vertices connected
    theta: Fraction = Fraction(0)        # 2 genus-0 trivalent vertices, 3 bridges
    lollipop: Fraction = Fraction(0)     # genus-0 trivalent + genus-1, self-loop + bridge

    @property
    def total(self) -> Fraction:
        return (self.smooth + self.fig_eight + self.banana
                + self.dumbbell + self.theta + self.lollipop)

    @property
    def boundary_total(self) -> Fraction:
        """Sum of all boundary graph contributions (everything except smooth)."""
        return (self.fig_eight + self.banana + self.dumbbell
                + self.theta + self.lollipop)


