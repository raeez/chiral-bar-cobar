# Section 9: The clutching test
# ============================================================================

def clutching_test(alg: MultiWeightAlgebra) -> Dict[str, Any]:
    r"""The clutching pullback test for tautological purity.

    If obs_2 = kappa * lambda_2 (tautologically pure), then the
    clutching map gl*: H*(M-bar_2) -> H*(M-bar_{1,1}) x H*(M-bar_{1,1})
    gives:
        gl*(obs_2) = gl*(kappa * lambda_2) = kappa * gl*(lambda_2)

    The clutching pullback of lambda_2 is:
        gl*(lambda_2) = lambda_1 tensor lambda_1
    (because gl*(E) = E_1 oplus E_2, so gl*(c_2(E)) = c_1(E_1)*c_1(E_2)
    = lambda_1 tensor lambda_1).

    So: gl*(obs_2) = kappa * (lambda_1 tensor lambda_1)

    For the graph sum: the clutching map restricts to the DUMBBELL graph
    (the separating degeneration). The dumbbell amplitude IS the
    clutching pullback of obs_2 restricted to Delta_1.

    Test: A_dumbbell = (1/2) * sum_i (kappa_i/24)^2 / kappa_i
                     = (1/2) * sum_i kappa_i / 576

    For purity, this should equal:
        kappa * (lambda_1^FP)^2 = kappa / 576

    So: sum_i kappa_i / 1152 = kappa / 1152.
    This is ALWAYS true because sum_i kappa_i = kappa.

    The dumbbell test is TRIVIALLY satisfied. The clutching pullback
    does not detect the failure of purity because it only sees the
    SEPARATING degeneration, where the per-channel contributions
    automatically assemble correctly.

    The failure of purity comes from the NONSEPARATING degeneration
    (Delta_irr) and the deeper strata (banana, theta, lollipop).
    The nonseparating clutching map gl_ns*: H*(M-bar_2) -> H*(M-bar_{1,3})
    maps lambda_2 to a specific class involving lambda_1 and psi-classes
    on M-bar_{1,3}. This is where the cross-channel correction lives.
    """
    # Separating clutching test
    dumbbell_amp = Fraction(0)
    for ch in alg.channels:
        dumbbell_amp += alg.kappa[ch] / 1152

    expected_sep = alg.kappa_total / 1152

    sep_passes = (dumbbell_amp == expected_sep)

    # Nonseparating clutching test
    # The fig-eight graph is the nonseparating degeneration of M-bar_2.
    # gl_ns*: M-bar_2 -> Delta_irr
    # maps the genus-2 curve to the genus-1 curve with self-node.
    #
    # For purity: the fig-eight amplitude should equal kappa * I(lambda_2, Delta_irr)
    # where I is the intersection number.
    #
    # The fig-eight amplitude: sum_i 1/48 = r/48
    # For purity: this should be proportional to kappa.
    # r/48 = kappa * C for some constant C independent of the algebra.
    # For single-channel (r=1, kappa arbitrary): C = 1/(48*kappa).
    #   Wait, fig-eight = 1/48 for single-channel.
    #   For purity: obs_2 = kappa * lambda_2.
    #   The fig-eight gives the restriction of obs_2 to Delta_irr.
    #   For Heisenberg at level k: fig-eight = 1/48, kappa = k.
    #   So the ratio fig-eight/kappa = 1/(48k) depends on k.
    #   This means the fig-eight amplitude is 1/48, NOT proportional to kappa.
    #   But wait: the FULL obs_2 = kappa * lambda_2 has restriction to Delta_irr
    #   equal to kappa * lambda_2|_{Delta_irr}, which is a class on Delta_irr.
    #
    # Actually for the integrated version:
    #   F_2 = sum of all graph amplitudes (including smooth).
    #   The fig-eight gives one contribution. The smooth vertex gives another.
    #   Purity means the TOTAL F_2 = kappa * lambda_2^FP.
    #   The per-graph decomposition need not individually be proportional to kappa.

    fig_eight_amp = Fraction(len(alg.channels), 48)  # r/48

    # The KEY nonseparating test: the MIXED-channel terms in the
    # banana, theta, lollipop, barbell graphs are the obstruction.
    # If these are all zero, purity holds (the boundary assembly is correct).
    # If any is nonzero, purity is broken at the integrated level.

    banana = _compute_banana(alg)
    theta = _compute_theta(alg)
    lollipop = _compute_lollipop(alg)
    barbell = _compute_barbell(alg)

    nonsep_mixed = (banana.mixed_amplitude + theta.mixed_amplitude
                    + lollipop.mixed_amplitude + barbell.mixed_amplitude)

    return {
        'separating_clutching': {
            'dumbbell_amplitude': dumbbell_amp,
            'expected': expected_sep,
            'passes': sep_passes,
        },
        'nonseparating_test': {
            'fig_eight_amplitude': fig_eight_amp,
            'banana_mixed': banana.mixed_amplitude,
            'theta_mixed': theta.mixed_amplitude,
            'lollipop_mixed': lollipop.mixed_amplitude,
            'barbell_mixed': barbell.mixed_amplitude,
            'total_mixed': nonsep_mixed,
            'passes': nonsep_mixed == 0,
        },
        'verdict': (
            "Tautological purity HOLDS at genus 2"
            if nonsep_mixed == 0
            else f"Tautological purity FAILS: nonsep mixed = {nonsep_mixed}"
        ),
    }


