# Section 5: Multi-channel genus-2 graph sum with CLASS-LEVEL tracking
# ============================================================================

@dataclass
class MultiWeightAlgebra:
    """Data for a multi-weight chiral algebra.

    Channels: list of generator labels.
    kappa: per-channel modular characteristic.
    C3: sphere 3-point function C_{ijk}.
    """
    name: str
    channels: List[str]
    kappa: Dict[str, Fraction]
    C3_func: Any  # callable (i, j, k) -> Fraction

    @property
    def kappa_total(self) -> Fraction:
        return sum(self.kappa.values())

    def propagator(self, ch: str) -> Fraction:
        k = self.kappa[ch]
        if k == 0:
            raise ValueError(f"Channel {ch} has kappa=0")
        return Fraction(1) / k

    def V04(self, i1: str, i2: str, j1: str, j2: str) -> Fraction:
        """Genus-0 4-point vertex = sum_m eta^{mm} C_{i1 i2 m} C_{m j1 j2}."""
        total = Fraction(0)
        for m in self.channels:
            km = self.kappa[m]
            if km == 0:
                continue
            total += self.C3_func(i1, i2, m) * (Fraction(1) / km) * self.C3_func(m, j1, j2)
        return total


def make_w3(c: Fraction) -> MultiWeightAlgebra:
    """W_3 algebra at central charge c."""
    def C3(i: str, j: str, k: str) -> Fraction:
        w_count = sum(1 for x in (i, j, k) if x == 'W')
        if w_count % 2 == 1:
            return Fraction(0)
        labels = sorted([i, j, k])
        if labels == ['T', 'T', 'T']:
            return c
        elif labels == ['T', 'W', 'W']:
            return c
        return Fraction(0)

    return MultiWeightAlgebra(
        name=f"W_3(c={c})",
        channels=['T', 'W'],
        kappa={'T': c / 2, 'W': c / 3},
        C3_func=C3,
    )


def make_heisenberg(k: Fraction) -> MultiWeightAlgebra:
    """Heisenberg algebra at level k (single channel, uniform weight)."""
    def C3(i: str, j: str, kk: str) -> Fraction:
        return k  # C_{JJJ} = k for Heisenberg

    return MultiWeightAlgebra(
        name=f"H_{k}",
        channels=['J'],
        kappa={'J': k},
        C3_func=C3,
    )


def make_virasoro(c: Fraction) -> MultiWeightAlgebra:
    """Virasoro algebra at central charge c (single channel)."""
    def C3(i: str, j: str, k: str) -> Fraction:
        return c  # C_{TTT} = c

    return MultiWeightAlgebra(
        name=f"Vir_{c}",
        channels=['T'],
        kappa={'T': c / 2},
        C3_func=C3,
    )


