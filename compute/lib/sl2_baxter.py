"""Baxter TQ relation for Y(sl_2) — pilot computation for MC3 path.

The Baxter TQ relation is the three-term identity in K_0(O_Y):

    [V_1(a)] * [M(lambda, b)] = [M(lambda+1, ...)] + [M(lambda-1, ...)]

where V_1(a) is the 2-dimensional evaluation module (pullback of the
standard sl_2 rep via ev_a), and M(lambda, b) is a Verma-like module
of highest weight lambda.

This module verifies:
  1. The K_0 identity at the level of characters (formal weight multiplicities).
  2. The short exact sequence 0 -> M(lam-1) -> V_1 tensor M(lam) -> M(lam+1) -> 0
     at the level of graded dimensions.
  3. The Yang R-matrix computation for V_1(a) tensor V_1(b).
  4. Transfer matrix eigenvalues and the TQ functional equation.

Mathematical setup:
  - Y(sl_2) in the Drinfeld presentation: generators e, f, h (level 0)
    and E, F, H (level 1).
  - [h, e] = 2e, [h, f] = -2f, [e, f] = h  (level 0 = sl_2)
  - [h, E] = 2E, [h, F] = -2F, [H, e] = 2E, [H, f] = -2F  (mixed)
  - [E, F] = H + h^2/2  (level 1 commutation)
  - Evaluation homomorphism ev_a: E -> a*e, F -> a*f, H -> a*h
  - V_1(a) = pullback of standard 2-dim sl_2 rep via ev_a
  - Verma module M(lambda): infinite-dimensional, weight spaces
    lambda, lambda-2, lambda-4, ... each of dimension 1.

CONVENTIONS:
  - Cohomological grading (consistent with the monograph).
  - sl_2 basis: e, h, f with standard structure constants.
  - Weights are integers (or half-integers for spin reps).
  - K_0 classes are formal sums of weight-space dimensions.

References:
  - yangians.tex, sec:yangian-rep-bar, conj:baxter-exact-triangles
  - Hernandez-Jimbo, Baxter's relations and spectra of quantum integrable models
  - Zhang, Shifted Yangians and TQ relations
"""

from __future__ import annotations

from typing import Dict, List, Optional, Tuple

import numpy as np
from sympy import Matrix, Rational, Symbol, eye, simplify, symbols, oo


# ---------------------------------------------------------------------------
# Weight data types
