def shift_operator_action(lam: int, depth: int = 50) -> Dict[str, FormalCharacter]:
    """Show that [V_1] acts as a shift operator on the K_0 lattice.

    In K_0(O_Y), the class [V_1] acts on the lattice of standard
    module classes {[M(n)] : n in Z} by:

        [V_1] * [M(n)] = [M(n+1)] + [M(n-1)]

    This means [V_1] is the "adjacency matrix" of the Z-lattice,
    i.e., the discrete Laplacian / Chebyshev recurrence operator.

    Formally: if we set X_n = [M(n)], then [V_1] acts as the
    operator X_n -> X_{n+1} + X_{n-1}, which is the shift
    (x + x^{-1}) in the group ring Z[Z].

    Returns dict demonstrating the shift action for several values of lam.
    """
    results = {}
    for n in range(max(0, lam - 2), lam + 3):
        V1 = eval_module_V1()
        Mn = sl2_verma_character(n, depth=depth)
        tensor = tensor_product_characters(V1, Mn)
        results[f"V1 tensor M({n})"] = tensor
        results[f"M({n+1}) + M({n-1})"] = sum_characters(
            sl2_verma_character(n + 1, depth=depth),
            sl2_verma_character(n - 1, depth=depth),
        )
    return results


def verify_shift_operator(max_lam: int = 10, depth: int = 30) -> bool:
    """Verify the shift operator property for lam = 0, 1, ..., max_lam."""
    return all(verify_baxter_tq_k0(lam, depth=depth)
               for lam in range(max_lam + 1))


