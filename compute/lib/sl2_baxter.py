# A formal character is a dict: weight -> multiplicity.
# Weights are integers (for integral highest weight modules).
FormalCharacter = Dict[int, int]


def formal_character_equal(chi1: FormalCharacter, chi2: FormalCharacter,
                           max_depth: int = 50) -> bool:
    """Compare two formal characters up to a finite depth.

    For infinite-dimensional modules (Verma), we truncate at max_depth
    weight levels below the highest weight.
    """
    all_weights = set(chi1.keys()) | set(chi2.keys())
    for w in all_weights:
        if chi1.get(w, 0) != chi2.get(w, 0):
            return False
    return True


# ---------------------------------------------------------------------------
# sl_2 representations
# ---------------------------------------------------------------------------

def sl2_fd_character(dim: int) -> FormalCharacter:
    """Formal character of the (dim)-dimensional irreducible sl_2 representation.

    V_n has dimension n+1 and weights n, n-2, ..., -n.
    So the dim-dimensional rep has highest weight dim-1.

    Args:
        dim: dimension of the representation (dim >= 1).

    Returns:
        dict: weight -> multiplicity (all multiplicities are 1).
    """
    if dim < 1:
        raise ValueError(f"Dimension must be >= 1, got {dim}")
    hw = dim - 1  # highest weight
    return {hw - 2 * k: 1 for k in range(dim)}


def sl2_verma_character(lam: int, depth: int = 50) -> FormalCharacter:
    """Formal character of the Verma module M(lambda) for sl_2.

    M(lambda) has weight spaces: lambda, lambda-2, lambda-4, ...
    Each weight space is 1-dimensional (generated by f^k applied to
    the highest weight vector).

    Args:
        lam: highest weight (integer).
        depth: number of weight levels to compute.

    Returns:
        dict: weight -> multiplicity.
    """
    return {lam - 2 * k: 1 for k in range(depth)}


def tensor_product_characters(chi1: FormalCharacter,
                              chi2: FormalCharacter) -> FormalCharacter:
    """Compute the character of the tensor product of two representations.

    (chi1 tensor chi2)(mu) = sum_{mu1 + mu2 = mu} chi1(mu1) * chi2(mu2).
    """
    result: FormalCharacter = {}
    for w1, m1 in chi1.items():
        for w2, m2 in chi2.items():
            w = w1 + w2
            result[w] = result.get(w, 0) + m1 * m2
    return result


def sum_characters(*chars: FormalCharacter) -> FormalCharacter:
    """Compute the direct sum of formal characters."""
    result: FormalCharacter = {}
    for chi in chars:
        for w, m in chi.items():
            result[w] = result.get(w, 0) + m
    return result


def subtract_characters(chi1: FormalCharacter,
                        chi2: FormalCharacter) -> FormalCharacter:
    """Subtract chi2 from chi1 (for exact sequence verification)."""
    result = dict(chi1)
    for w, m in chi2.items():
        result[w] = result.get(w, 0) - m
    # Clean up zeros
    return {w: m for w, m in result.items() if m != 0}


# ---------------------------------------------------------------------------
# Evaluation modules for Y(sl_2)
# ---------------------------------------------------------------------------

def eval_module_V1() -> FormalCharacter:
    """Character of the 2-dimensional evaluation module V_1(a).

    V_1(a) = pullback of the standard 2-dim sl_2 rep via ev_a.
    Weights: +1, -1 (each multiplicity 1).
    The evaluation parameter a does not affect the character.
    """
    return sl2_fd_character(2)


def eval_module_Vn(n: int) -> FormalCharacter:
    """Character of the (n+1)-dimensional evaluation module V_n(a).

    V_n(a) = pullback of the (n+1)-dim irreducible sl_2 rep via ev_a.
    """
    return sl2_fd_character(n + 1)


# ---------------------------------------------------------------------------
# Baxter TQ relation in K_0
# ---------------------------------------------------------------------------

def baxter_tq_k0(lam: int, depth: int = 50) -> Tuple[FormalCharacter,
                                                       FormalCharacter]:
    """Verify the Baxter TQ relation in K_0(O_Y) for Y(sl_2).

    The identity is:
        [V_1] * [M(lam)] = [M(lam+1)] + [M(lam-1)]

    in K_0, where * is the tensor product.

    Returns:
        (lhs_character, rhs_character) — both truncated to depth levels.
    """
    V1 = eval_module_V1()
    M_lam = sl2_verma_character(lam, depth=depth)

    # LHS: V_1 tensor M(lam)
    lhs = tensor_product_characters(V1, M_lam)

    # RHS: M(lam+1) + M(lam-1)
    M_plus = sl2_verma_character(lam + 1, depth=depth)
    M_minus = sl2_verma_character(lam - 1, depth=depth)
    rhs = sum_characters(M_plus, M_minus)

    return lhs, rhs


def verify_baxter_tq_k0(lam: int, depth: int = 50) -> bool:
    """Check whether the Baxter TQ identity holds at K_0 level.

    Returns True if [V_1][M(lam)] = [M(lam+1)] + [M(lam-1)]
    as formal characters, truncated at the given depth.
    """
    lhs, rhs = baxter_tq_k0(lam, depth=depth)
    return formal_character_equal(lhs, rhs)


def baxter_tq_higher_spin(n: int, lam: int,
                           depth: int = 50) -> Tuple[FormalCharacter,
                                                      FormalCharacter]:
    """Higher-spin Baxter TQ relation in K_0.

    [V_n] * [M(lam)] = sum_{j=0}^{n} [M(lam + n - 2j)]
                      = [M(lam+n)] + [M(lam+n-2)] + ... + [M(lam-n)]

    This is the Clebsch-Gordan decomposition at the K_0 level.

    Args:
        n: spin label (V_n is (n+1)-dimensional).
        lam: highest weight of the Verma module.
        depth: truncation depth.

    Returns:
        (lhs, rhs) characters.
    """
    Vn = eval_module_Vn(n)
    M_lam = sl2_verma_character(lam, depth=depth)

    lhs = tensor_product_characters(Vn, M_lam)

    # RHS: sum of shifted Verma modules
    summands = [sl2_verma_character(lam + n - 2 * j, depth=depth)
                for j in range(n + 1)]
    rhs = sum_characters(*summands)

    return lhs, rhs


def verify_baxter_tq_higher_spin(n: int, lam: int,
                                  depth: int = 50) -> bool:
    """Verify the higher-spin TQ relation."""
    lhs, rhs = baxter_tq_higher_spin(n, lam, depth=depth)
    return formal_character_equal(lhs, rhs)


# ---------------------------------------------------------------------------
# Short exact sequence: 0 -> M(lam-1) -> V_1 tensor M(lam) -> M(lam+1) -> 0
# ---------------------------------------------------------------------------

def ses_dimension_check(lam: int, depth: int = 50) -> Dict[int, Tuple[int, int, int]]:
    """Verify the short exact sequence at the level of weight-space dimensions.

    For each weight mu, we check:
        dim (V_1 tensor M(lam))_mu = dim M(lam+1)_mu + dim M(lam-1)_mu

    This is equivalent to the K_0 identity but presented weight-by-weight.

    Returns:
        dict: weight -> (dim_tensor, dim_M_plus, dim_M_minus)
    """
    V1 = eval_module_V1()
    M_lam = sl2_verma_character(lam, depth=depth)
    M_plus = sl2_verma_character(lam + 1, depth=depth)
    M_minus = sl2_verma_character(lam - 1, depth=depth)

    tensor = tensor_product_characters(V1, M_lam)

    all_weights = sorted(set(tensor.keys()) | set(M_plus.keys()) | set(M_minus.keys()),
                         reverse=True)

    result = {}
    for w in all_weights:
        d_tensor = tensor.get(w, 0)
        d_plus = M_plus.get(w, 0)
        d_minus = M_minus.get(w, 0)
        result[w] = (d_tensor, d_plus, d_minus)

    return result


def verify_ses_dimensions(lam: int, depth: int = 50) -> bool:
    """Verify that the SES holds at the level of dimensions for every weight."""
    data = ses_dimension_check(lam, depth=depth)
    return all(d_t == d_p + d_m for d_t, d_p, d_m in data.values())


