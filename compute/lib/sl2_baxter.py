# Chebyshev structure in K_0
# ---------------------------------------------------------------------------

def chebyshev_recurrence(n: int, x_char: FormalCharacter,
                         depth: int = 50) -> FormalCharacter:
    """Compute U_n(x/2) in K_0 via the Chebyshev recurrence.

    The Chebyshev polynomials of the second kind satisfy:
        U_0 = 1, U_1 = x, U_{n+1} = x * U_n - U_{n-1}

    In our context, x = [V_1] and the multiplication is tensor product.
    U_n([V_1]/2) = [V_n] (the (n+1)-dimensional evaluation module).

    We verify: [V_n] is the n-th Chebyshev polynomial of [V_1].

    Args:
        n: degree of Chebyshev polynomial.
        x_char: character to use as x (should be V_1 character).
        depth: truncation depth.
    """
    if n == 0:
        return {0: 1}  # trivial 1-dim rep
    if n == 1:
        return dict(x_char)

    U_prev = {0: 1}
    U_curr = dict(x_char)
    for _ in range(2, n + 1):
        U_next = subtract_characters(
            tensor_product_characters(x_char, U_curr),
            U_prev,
        )
        U_prev = U_curr
        U_curr = U_next
    return U_curr


def verify_chebyshev_structure(max_n: int = 6) -> bool:
    """Verify that [V_n] = U_n([V_1]) in K_0 (Chebyshev recurrence).

    This is the categorical shadow of the Clebsch-Gordan fusion rules:
        V_1 tensor V_n = V_{n+1} + V_{n-1}

    which is the same as the Chebyshev recurrence.
    """
    V1 = eval_module_V1()
    for n in range(max_n + 1):
        Vn_direct = eval_module_Vn(n)
        Vn_chebyshev = chebyshev_recurrence(n, V1)
        if not formal_character_equal(Vn_direct, Vn_chebyshev):
            return False
    return True


# ---------------------------------------------------------------------------
# Iterated TQ: building the K_0 lattice from V_1
# ---------------------------------------------------------------------------

def build_k0_lattice(max_lam: int = 5,
                     depth: int = 30) -> Dict[int, FormalCharacter]:
    """Build the K_0 lattice of standard module classes using TQ iterations.

    Starting from M(0) = trivial module and V_1, recursively build
    M(n) classes using the TQ relation:

        [M(n+1)] = [V_1] * [M(n)] - [M(n-1)]

    This is exactly the Chebyshev recurrence, confirming that the
    standard modules are Chebyshev-generated from V_1 and the trivial module.
    """
    V1 = eval_module_V1()
    lattice: Dict[int, FormalCharacter] = {}

    # Seed: M(0) is trivial, M(1) is V_1 itself... but actually M(lam) are Verma modules.
    # The TQ relation says [V_1]*[M(n)] = [M(n+1)] + [M(n-1)].
    # So [M(n+1)] = [V_1]*[M(n)] - [M(n-1)].
    # Seed: M(0) = Verma of hw 0, M(1) = Verma of hw 1.

    lattice[0] = sl2_verma_character(0, depth=depth)
    lattice[1] = sl2_verma_character(1, depth=depth)

    for n in range(1, max_lam):
        tensor = tensor_product_characters(V1, lattice[n])
        lattice[n + 1] = subtract_characters(tensor, lattice[n - 1])

    return lattice


def verify_k0_lattice(max_lam: int = 5, depth: int = 30) -> bool:
    """Verify that the K_0 lattice built by TQ iteration matches
    direct Verma module characters."""
    lattice = build_k0_lattice(max_lam, depth=depth)
    for n in range(max_lam + 1):
        direct = sl2_verma_character(n, depth=depth)
        if not formal_character_equal(lattice[n], direct):
            return False
    return True


