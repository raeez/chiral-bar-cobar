# Universal vs simple: dimensional comparison
# ============================================================

def universal_bar_dims(max_weight: int = 12, max_n: int = 6) -> Dict[Tuple[int, int], int]:
    """Bar complex dimensions for the UNIVERSAL Virasoro V_c.

    These are c-INDEPENDENT: they depend only on the structure of
    the vacuum module, which is the same for all generic c.

    Returns {(bar_degree, weight): dimension}.
    """
    result = {}
    for n in range(1, max_n + 1):
        for h in range(2 * n, max_weight + 1):
            d = _universal_chain_dim(n, h)
            if d > 0:
                result[(n, h)] = d
    return result


def _universal_chain_dim(n: int, h: int) -> int:
    """Chain dimension for universal Virasoro (= virasoro_bar.bar_chain_dim)."""
    if n <= 0 or h < 2 * n:
        return 0
    form_dim = factorial(n - 1)
    tensor_dim = _universal_tensor_dim(n, h)
    return tensor_dim * form_dim


def _universal_tensor_dim(n: int, h: int) -> int:
    """Tensor product dimension for universal Virasoro."""
    if n == 1:
        return partitions_geq2(h)
    total = 0
    for a in range(2, h - 2 * (n - 1) + 1):
        d_a = partitions_geq2(a)
        if d_a == 0:
            continue
        rest = _universal_tensor_dim(n - 1, h - a)
        total += d_a * rest
    return total


def simple_bar_dims(p: int, q: int, max_weight: int = 12,
                    max_n: int = 6) -> Dict[Tuple[int, int], int]:
    """Bar complex dimensions for the SIMPLE Virasoro L(c_{p,q}, 0).

    These DIFFER from universal at weight >= (p-1)(q-1) where null
    vectors reduce the vacuum module dimension.

    Returns {(bar_degree, weight): dimension}.
    """
    c_val = float(minimal_model_c(p, q))
    bar = VirasoroBarComplex(c_val, max_weight, simple=True, p=p, q=q)
    result = {}
    for n in range(1, max_n + 1):
        for h in range(2 * n, max_weight + 1):
            d = bar.chain_dim(n, h)
            if d > 0:
                result[(n, h)] = d
    return result


def compare_universal_vs_simple(p: int, q: int,
                                max_weight: int = 12,
                                max_n: int = 6) -> Dict[str, object]:
    """Compare bar complex dimensions: universal V_c vs simple L(c_{p,q}, 0).

    Returns:
    - c_val: central charge
    - null_weight: weight of first singular vector
    - universal: {(n,h): dim} for universal
    - simple: {(n,h): dim} for simple
    - differences: list of (n, h, dim_univ, dim_simple) where they differ
    - first_difference_weight: lowest weight where dimensions differ
    """
    c_val = minimal_model_c(p, q)
    null_w = vacuum_singular_weight(p, q)
    univ = universal_bar_dims(max_weight, max_n)
    simp = simple_bar_dims(p, q, max_weight, max_n)

    diffs = []
    all_keys = set(univ.keys()) | set(simp.keys())
    for key in sorted(all_keys):
        d_u = univ.get(key, 0)
        d_s = simp.get(key, 0)
        if d_u != d_s:
            diffs.append((key[0], key[1], d_u, d_s))

    first_diff_weight = min((d[1] for d in diffs), default=None)

    return {
        'c_val': c_val,
        'null_weight': null_w,
        'universal': univ,
        'simple': simp,
        'differences': diffs,
        'first_difference_weight': first_diff_weight,
    }


# ============================================================
# Bar cohomology via Euler characteristics
# ============================================================

def bar_euler_chars(max_weight: int = 12, universal: bool = True,
                    p: int = None, q: int = None) -> Dict[int, int]:
    """Euler characteristics chi(h) = sum_{n>=1} (-1)^n dim B^n_h.

    For a Koszul algebra, the Euler characteristic equals the
    alternating sum of Koszul dual dimensions (with appropriate signs).

    Returns {weight: euler_char}.
    """
    result = {}
    for h in range(2, max_weight + 1):
        chi = 0
        for n in range(1, h // 2 + 1):
            if universal:
                d = _universal_chain_dim(n, h)
            else:
                c_val = float(minimal_model_c(p, q))
                bar = VirasoroBarComplex(c_val, h, simple=True, p=p, q=q)
                d = bar.chain_dim(n, h)
            chi += (-1) ** n * d
        result[h] = chi
    return result


# ============================================================
# Zhu-based lower bounds on bar cohomology
# ============================================================

def zhu_ext_lower_bound(max_n: int = 8, simple: bool = False,
                        p: int = None, q: int = None) -> Dict[int, int]:
    """Lower bound on bar cohomology from Zhu's algebra Ext.

    For the universal Virasoro (A = C[x]):
      Ext^0 = C, Ext^1 = C, Ext^n = 0 for n >= 2.
    This says H^0(B) >= 1, H^1(B) >= 1.

    For the simple minimal model (A = semisimple):
      Ext^0 = C, Ext^n = 0 for n >= 1.
    The semisimplicity of Zhu's algebra means the WEIGHT-0 part
    of bar cohomology is concentrated in degree 0.

    These bounds are WEAK: the actual bar cohomology uses the
    full OPE structure, not just Zhu's product.

    Returns {n: lower_bound_dim}.
    """
    if simple and p is not None and q is not None:
        c_val = minimal_model_c(p, q)
    else:
        c_val = Symbol('c')
    zhu = ZhuAlgebra(c_val, simple=simple, p=p, q=q)
    return zhu.ext_dims(max_n)


# ============================================================
# The key theorem: c-independence of UNIVERSAL bar cohomology
# ============================================================

def verify_c_independence_chain_level(max_weight: int = 10,
                                      max_n: int = 5) -> Dict[str, object]:
    """Verify that bar CHAIN dimensions are c-independent for universal V_c.

    The chain dimensions dim B^n_h = (tensor product dim) * (n-1)!
    depend only on dim(V_+)_h = p_{>=2}(h), which is c-INDEPENDENT.

    This is because the vacuum module of the UNIVERSAL Virasoro V_c
    has the same graded dimension for all c: it is always the Verma
    module M(c,0), whose character is prod_{n>=2} 1/(1-q^n).

    The bar COHOMOLOGY dimensions could still be c-dependent through
    the DIFFERENTIAL, which involves OPE coefficients that depend on c.
    But for the universal Virasoro, these are polynomial/rational in c,
    and the cohomology dimensions are semicontinuous.

    Returns verification results.
    """
    results = {}
    results['chain_dims_c_independent'] = True  # tautologically true

    # The chain complex dimensions are functions of p_{>=2}(h) only
    for n in range(1, max_n + 1):
        for h in range(2 * n, max_weight + 1):
            d = _universal_chain_dim(n, h)
            results[f'B^{n}_{h}'] = d

    # For COHOMOLOGY c-independence: at weight 4 (bar degree 2 -> 1),
    # the differential D: B^2_4 -> B^1_4 has a SINGLE entry c/2
    # (from T_{(3)}T). Its rank is 0 when c=0, 1 when c != 0.
    # So dim ker D changes at c=0!
    #
    # Wait: D: B^2_4 -> B^1_4 has dim(B^2_4) = 1, dim(B^1_4) = 2.
    # B^2_4 = [T|T] otimes eta (one basis element).
    # B^1_4 = {L_{-2}^2|0>, L_{-4}|0>} (two basis elements).
    #
    # D([T|T] otimes eta) = T_{(3)}T * |0> + T_{(1)}T + T_{(0)}T
    # But wait, the WEIGHT must be preserved. Weight of [T|T] is 4.
    # D maps to weight 4 in bar degree 1. Bar degree 0 gets the
    # vacuum piece T_{(3)}T = (c/2)|0> at weight 0, not weight 4.
    #
    # So D: B^2_4 -> B^1_4 extracts:
    # T_{(1)}T = 2T (weight 2, NOT weight 4)
    # T_{(0)}T = dT (weight 3, NOT weight 4)
    #
    # These go to weight 2 and weight 3 respectively, NOT to weight 4!
    # The bar differential D: B^2_h -> B^1_h does NOT preserve weight;
    # it reduces weight by (n+1) for the n-th product.
    #
    # CORRECTION: In the chiral bar complex, the differential DOES
    # preserve the TOTAL weight h. The bar element [T|T] has total
    # weight 4. D([T|T]) lands in B^1 with various weights:
    # - T_{(3)}T = (c/2)|0> at weight 0 -> B^0_0 (bar degree 0)
    # - T_{(1)}T = 2T at weight 2 -> B^1_2
    # - T_{(0)}T = dT at weight 3 -> B^1_3
    #
    # So D: B^2_4 -> B^0_0 + B^1_2 + B^1_3.
    # The differential does NOT preserve weight! It maps to LOWER weights.
    #
    # Actually, re-reading virasoro_bar.py more carefully:
    # The bar differential extracts ALL singular OPE products.
    # The n-th product T_{(n)}T has output weight 2+2-n-1 = 3-n.
    # n=3: weight 0, n=1: weight 2, n=0: weight 3.
    #
    # So the bar differential maps B^2_4 to
    #   B^0_0 (vacuum, from n=3) + B^1_2 (from n=1) + B^1_3 (from n=0).
    #
    # This means bar cohomology at a fixed total weight h is NOT
    # well-defined! The differential mixes weights.
    #
    # BUT: the bar complex has a natural FILTRATION by total weight,
    # and the associated graded is the PBW spectral sequence.
    # The bar degree n, total weight h piece B^n_h has
    # D mapping to B^{n-1}_{h'} with h' < h (because OPE lowers weight).
    #
    # Wait, that's wrong too. Let me think again.
    #
    # The chiral bar complex B(A) has:
    # - Bar degree: number of tensor factors
    # - Internal weight: sum of conformal weights of the factors
    # - The differential D uses d log(z_i - z_j) forms, which are
    #   weight 0 in the conformal sense. The OPE extraction at
    #   the n-th product extracts a term of weight h1+h2-n-1 from
    #   two fields of weights h1, h2. Total weight of the output is
    #   h1+h2-n-1 + sum(other weights) = (total weight) - n - 1.
    #
    # So D LOWERS the total weight by n+1 for the n-th product term!
    # This means the bar complex is NOT weight-graded; it is
    # weight-FILTERED.
    #
    # For Koszul algebras, the PBW spectral sequence associated to
    # this filtration degenerates at E_2. The E_1 page at fixed
    # weight h computes the bar cohomology of the associated graded
    # (which is a commutative/free algebra).
    #
    # The FULL bar cohomology is the abutment of this spectral sequence.
    # For Koszul algebras, E_2 = E_infinity, so bar cohomology is
    # computed by the CE differential on the associated graded.
    #
    # KEY INSIGHT: The BAR COHOMOLOGY dimensions for V_c at GENERIC c
    # are given by the E_2 page of the PBW spectral sequence.
    # Since V_c is chirally Koszul (PBW degeneration at E_2),
    # H^n(B(V_c)) is the CE cohomology of the negative-mode Lie algebra.
    # This is INDEPENDENT of c because:
    # (a) The Lie algebra structure on negative modes is c-independent
    #     (the central extension vanishes on negative modes).
    # (b) The PBW spectral sequence E_2 = E_infinity for all generic c.

    results['cohomology_c_independent_reason'] = (
        "PBW spectral sequence degenerates at E_2 for generic c. "
        "CE differential on gr(V_c) is c-independent because the "
        "central extension vanishes on negative loop modes."
    )

    # At specific c (minimal model): the SIMPLE quotient L(c,0) has
    # different chain dimensions, hence potentially different cohomology.
    results['simple_quotient_differs'] = True
    results['simple_differs_at'] = "weight >= (p-1)(q-1)"

    return results


