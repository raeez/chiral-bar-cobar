@lru_cache(maxsize=512)
def partitions_geq2(h: int) -> int:
    """Number of partitions of h into parts >= 2."""
    if h < 0:
        return 0
    if h == 0:
        return 1
    if h == 1:
        return 0
    dp = [0] * (h + 1)
    dp[0] = 1
    for part in range(2, h + 1):
        for j in range(part, h + 1):
            dp[j] += dp[j - part]
    return dp[h]


@lru_cache(maxsize=512)
def num_partitions(n: int) -> int:
    """Number of unrestricted partitions of n."""
    if n < 0:
        return 0
    if n == 0:
        return 1
    dp = [0] * (n + 1)
    dp[0] = 1
    for part in range(1, n + 1):
        for j in range(part, n + 1):
            dp[j] += dp[j - part]
    return dp[n]


def partitions_into_parts_geq2(n: int) -> List[Tuple[int, ...]]:
    """Enumerate all partitions of n into parts >= 2, as sorted tuples."""
    if n < 0:
        return []
    if n == 0:
        return [()]

    result = []

    def _recurse(remaining, min_part, current):
        if remaining == 0:
            result.append(tuple(current))
            return
        for p in range(min_part, remaining + 1):
            if p < 2:
                continue
            _recurse(remaining - p, p, current + [p])

    _recurse(n, 2, [])
    return result


