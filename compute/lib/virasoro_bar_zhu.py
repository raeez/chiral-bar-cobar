# Virasoro vacuum module: state labeling
# ============================================================

class VirasoroVacuumBasis:
    """Basis for the Virasoro vacuum module augmentation ideal V_+.

    States are labeled by partitions of weight h into parts >= 2.
    State lambda = (n_1, ..., n_k) with n_1 <= ... <= n_k, sum = h,
    corresponds to L_{-n_k} ... L_{-n_1} |0>.

    Parameters
    ----------
    max_weight : int
        Maximum conformal weight to include.
    null_weight : int or None
        If set, exclude states at weight >= null_weight that are
        descendants of the singular vector. For the UNIVERSAL Virasoro,
        null_weight = None (no exclusions). For the SIMPLE quotient
        L(c,0) at c_{p,q}, set null_weight = (p-1)(q-1).

        NOTE: This is a SIMPLIFIED model. The actual null submodule
        has a complex structure. Here we model the DIMENSIONAL effect:
        at and above the null weight, the module is smaller.
    """

    def __init__(self, max_weight: int, null_weight: Optional[int] = None,
                 null_dims: Optional[Dict[int, int]] = None):
        self.max_weight = max_weight
        self.null_weight = null_weight
        self._null_dims = null_dims  # {weight: dim of null subspace}
        self._basis: Dict[int, List[Tuple[int, ...]]] = {}
        self._dim: Dict[int, int] = {}
        self._build()

    def _build(self):
        for h in range(2, self.max_weight + 1):
            states = partitions_into_parts_geq2(h)
            if self.null_weight is not None and h >= self.null_weight:
                if self._null_dims is not None and h in self._null_dims:
                    # Remove exactly null_dims[h] states
                    n_remove = self._null_dims[h]
                    states = states[:max(0, len(states) - n_remove)]
                # If no explicit null_dims, keep all (conservative)
            self._basis[h] = states
            self._dim[h] = len(states)

    def dim(self, h: int) -> int:
        if h < 2:
            return 0
        return self._dim.get(h, 0)

    def basis(self, h: int) -> List[Tuple[int, ...]]:
        if h < 2:
            return []
        return self._basis.get(h, [])

    def total_dim(self, max_h: Optional[int] = None) -> int:
        mh = max_h if max_h is not None else self.max_weight
        return sum(self.dim(h) for h in range(2, mh + 1))


