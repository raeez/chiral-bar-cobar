def minimal_model_c(p: int, q: int):
    """Central charge for the (p,q) minimal model.

    c_{p,q} = 1 - 6(p-q)^2/(pq), coprime p > q >= 2.
    """
    return Rational(1) - Rational(6) * Rational((p - q) ** 2, p * q)


def vacuum_singular_weight(p: int, q: int) -> int:
    """Weight of the first singular vector in M(c_{p,q}, 0).

    The singular vector in the vacuum Verma module is at weight
    (p-1)(q-1) for the minimal model c_{p,q}.
    """
    return (p - 1) * (q - 1)


# Kac determinant for the vacuum module M(c, 0) at weight n.
# det_n(c, h=0) = prod_{1 <= rs <= n} h_{r,s}(c)^{p(n-rs)}
# where h_{r,s}(c) depends on c through the parametrization.

def kac_h_rs(r: int, s: int, c_val):
    """Conformal weight h_{r,s} as function of c.

    Using the parametrization c = 1 - 6(p-q)^2/(pq) with t = p/q:
    h_{r,s} = ((rt - s)^2 - (t-1)^2) / (4t)

    But for NUMERICAL c, we use the direct formula:
    c = 1 - 6/t - 6t + 12  ... no, the correct formula is:

    h_{r,s} = ((m+1)r - ms)^2 - 1) / (4m(m+1))
    where c = 1 - 6/(m(m+1)).

    For generic c, solve for m from c = 1 - 6/(m(m+1)):
    m(m+1) = 6/(1-c), m = (-1 + sqrt(1 + 24/(1-c)))/2.
    """
    if isinstance(c_val, (int, float, Rational)):
        c_num = float(c_val)
        if abs(c_num - 1) < 1e-12:
            # c = 1: degenerate
            return 0
        m_param = (-1 + (1 + 24 / (1 - c_num)) ** 0.5) / 2
        h = ((m_param + 1) * r - m_param * s) ** 2 - 1
        h /= (4 * m_param * (m_param + 1))
        return h
    # Symbolic
    return None


def vacuum_null_dimensions(p: int, q: int, max_weight: int) -> Dict[int, int]:
    """Dimensions of null subspaces in M(c_{p,q}, 0) by weight.

    The singular vector at weight w_0 = (p-1)(q-1) generates a
    submodule. At weight w >= w_0, the null subspace has dimension
    p(w - w_0) (partitions of the excess weight).

    This is because the null submodule is isomorphic (as a vector space)
    to a Verma module starting at weight w_0.

    NOTE: This is the FIRST-ORDER approximation. The actual null
    submodule structure can be more complex (embedded singular vectors),
    but for weights not too far above w_0, this is correct.
    """
    w0 = vacuum_singular_weight(p, q)
    result = {}
    for w in range(w0, max_weight + 1):
        excess = w - w0
        # The null module at weight w has dimension equal to the number
        # of ways to apply L_{-n} (n >= 1) to the singular vector to
        # reach weight w. But the singular vector is at weight w0, so
        # descendants at weight w have L-modes summing to w - w0.
        # However, in the VACUUM module, modes >= 2 only contribute.
        # Actually, the singular vector generates a Verma submodule,
        # so descendants use ALL L_{-n} with n >= 1 (including L_{-1}).
        # dim = p(w - w0) (unrestricted partitions).
        #
        # But wait: L_{-1} applied to the singular vector might also
        # be zero or proportional to other states. For the vacuum
        # representation, L_{-1} acts nontrivially on states at h > 0.
        # The singular vector v at weight w0 > 0 has L_{-1}v generally
        # nonzero.
        #
        # For a first-order model: null_dim(w) = p(w - w0).
        null_dim = num_partitions(excess)
        # But cannot exceed total dim of vacuum module at weight w
        total_dim = partitions_geq2(w)
        result[w] = min(null_dim, total_dim)
    return result


