# Direct bar cohomology computation: differential matrices
# ============================================================

class VirasoroBarDifferential:
    """Explicit bar differential matrices for Virasoro at specific c.

    Computes the differential D: B^n_h -> B^{n-1}_h by extracting
    OPE collision data from each pair of adjacent tensor factors.

    The key formula: for [a_1 | ... | a_n] otimes omega,
    D extracts all singular OPE products between adjacent a_i, a_{i+1}
    and sums over configuration space face maps.

    For WEIGHT-GRADED computation: the differential preserves weight,
    so we can compute the differential matrix at each weight h separately.

    We work with a MONOMIAL BASIS for V_+: at weight h, the basis is
    {L_{-lambda} |0>} indexed by partitions lambda of h into parts >= 2.

    The OPE provides the structure constants:
    (L_{-lambda}|0>) _{(n)} (L_{-mu}|0>) = sum_nu C^nu_{lambda,mu;n} (L_{-nu}|0>)

    For the simplest case (bar degree 2 -> 1): we need these structure
    constants. For higher bar degrees, the computation is more complex.

    THIS MODULE FOCUSES ON LOW-WEIGHT BAR COHOMOLOGY where the
    computation is tractable.
    """

    def __init__(self, c_val: float, max_weight: int = 10,
                 simple: bool = False, p: int = None, q: int = None):
        self.c_val = c_val
        self.max_weight = max_weight
        self.bar = VirasoroBarComplex(
            c_val, max_weight, simple=simple, p=p, q=q
        )
        self.simple = simple
        self.p = p
        self.q = q
        # Cache: basis states at each weight
        self._basis_cache: Dict[int, List[Tuple[int, ...]]] = {}
        for h in range(2, max_weight + 1):
            self._basis_cache[h] = self.bar.vacuum.basis(h)

    def _state_index(self, h: int, state: Tuple[int, ...]) -> int:
        """Index of state in the weight-h basis."""
        basis = self._basis_cache.get(h, [])
        try:
            return basis.index(state)
        except ValueError:
            return -1

    def _virasoro_ope_matrix(self, h1: int, h2: int, n: int) -> np.ndarray:
        """Matrix of the n-th product a_{(n)} b for a in V_{h1}, b in V_{h2}.

        Result lives in V_{h1+h2-n-1} (the output weight).

        For the GENERATOR T = L_{-2}|0> (weight 2):
          T_{(3)} T = (c/2)|0>          -> weight 0 (vacuum)
          T_{(1)} T = 2T               -> weight 2
          T_{(0)} T = L_{-3}|0> = dT   -> weight 3

        For descendants: need to compute L_{-lambda}|0> _{(n)} L_{-mu}|0>
        using the Virasoro algebra commutation relations.

        We implement this for LOW WEIGHTS only where it is tractable.
        """
        # This is the core computational challenge. For a full implementation
        # we would need the complete OPE algebra of the Virasoro VOA.
        # Here we implement what is needed for bar cohomology at low weight.

        output_weight = h1 + h2 - n - 1
        if output_weight < 0:
            return np.array([]).reshape(0, 0)

        basis1 = self._basis_cache.get(h1, [])
        basis2 = self._basis_cache.get(h2, [])
        if output_weight == 0:
            # Output is vacuum: scalar coefficients
            dim1 = len(basis1)
            dim2 = len(basis2)
            mat = np.zeros((1, dim1 * dim2))
            # Only T_{(3)} T = c/2 contributes at weight (2,2)
            if h1 == 2 and h2 == 2 and n == 3:
                mat[0, 0] = self.c_val / 2
            return mat

        basis_out = self._basis_cache.get(output_weight, [])
        if not basis1 or not basis2 or not basis_out:
            return np.zeros((len(basis_out) if basis_out else 0,
                             len(basis1) * len(basis2) if basis1 and basis2 else 0))

        dim1 = len(basis1)
        dim2 = len(basis2)
        dim_out = len(basis_out)
        mat = np.zeros((dim_out, dim1 * dim2))

        # Implement known OPE products
        # T = (2,) at weight 2
        # dT = (3,) at weight 3
        # L_{-2}^2|0> = (2,2) at weight 4
        # L_{-3}L_{-2}|0> is NOT (2,3); the canonical form is (2,3) or (3,2).
        # Partitions are sorted: (2,2), (2,3), etc.

        # T_{(1)} T = 2T: input (2,)x(2,), output (2,), n=1
        if h1 == 2 and h2 == 2 and n == 1 and output_weight == 2:
            i1 = 0  # (2,) in basis1
            i2 = 0  # (2,) in basis2
            i_out = 0  # (2,) in basis_out
            mat[i_out, i1 * dim2 + i2] = 2.0

        # T_{(0)} T = dT = L_{-3}|0>: input (2,)x(2,), output (3,), n=0
        if h1 == 2 and h2 == 2 and n == 0 and output_weight == 3:
            i1 = 0
            i2 = 0
            i_out = self._state_index(3, (3,))
            if i_out >= 0:
                mat[i_out, i1 * dim2 + i2] = 1.0

        # T_{(1)}(dT) = 3dT: input (2,)x(3,), output (3,), n=1
        if h1 == 2 and h2 == 3 and n == 1 and output_weight == 3:
            i1 = self._state_index(2, (2,))
            i2 = self._state_index(3, (3,))
            i_out = self._state_index(3, (3,))
            if i1 >= 0 and i2 >= 0 and i_out >= 0:
                mat[i_out, i1 * dim2 + i2] = 3.0

        # T_{(0)}(dT) = d^2T = L_{-4}|0> + ...: input (2,)x(3,), output (4,), n=0
        # L_{-1}(L_{-3}|0>) = [L_{-1}, L_{-3}]|0> = 2 L_{-4}|0>
        # But T_{(0)}(dT) = translation = L_{-1}(dT) = 2 L_{-4}|0>
        # Wait: a_{(0)}b is the RESIDUE of Y(a,z)b at z=0 (the simple pole).
        # For T_{(0)}(dT): this is L_{-1}(L_{-3}|0>) but including the
        # regular OPE terms. Actually T_{(0)}b = L_{-1}b for all b.
        # NO! That's wrong. The n-th product a_{(n)} is the mode a_n.
        # For a = omega = L_{-2}|0>: omega_n = L_{n-1}.
        # So omega_{(0)} = L_{-1} (the translation operator).
        # omega_{(0)}(dT) = L_{-1}(L_{-3}|0>) = (L_{-1}L_{-3} - L_{-3}L_{-1})|0>
        #                                       + L_{-3}L_{-1}|0>
        #                 = [L_{-1}, L_{-3}]|0> + 0
        #                 = 2 L_{-4}|0>
        if h1 == 2 and h2 == 3 and n == 0 and output_weight == 4:
            i1 = self._state_index(2, (2,))
            i2 = self._state_index(3, (3,))
            i_out = self._state_index(4, (4,))
            if i1 >= 0 and i2 >= 0 and i_out >= 0:
                mat[i_out, i1 * dim2 + i2] = 2.0

        # (dT)_{(1)} T = 3dT: input (3,)x(2,), output (3,), n=1
        if h1 == 3 and h2 == 2 and n == 1 and output_weight == 3:
            i1 = self._state_index(3, (3,))
            i2 = self._state_index(2, (2,))
            i_out = self._state_index(3, (3,))
            if i1 >= 0 and i2 >= 0 and i_out >= 0:
                mat[i_out, i1 * dim2 + i2] = 3.0

        # (dT)_{(0)} T = 2d^2T: input (3,)x(2,), output (4,), n=0
        if h1 == 3 and h2 == 2 and n == 0 and output_weight == 4:
            i1 = self._state_index(3, (3,))
            i2 = self._state_index(2, (2,))
            i_out = self._state_index(4, (4,))
            if i1 >= 0 and i2 >= 0 and i_out >= 0:
                mat[i_out, i1 * dim2 + i2] = 2.0

        # T_{(3)}(dT) = 0: no contribution (verified by translation covariance)
        # (dT)_{(3)} T: (3,)x(2,), n=3, output weight = 3+2-3-1 = 1. No basis.

        # ---- Weight 4 operations ----
        # (2,2) = L_{-2}^2|0>, (4,) = L_{-4}|0>

        # T_{(1)}(L_{-2}^2|0>) = L_0(L_{-2}^2|0>) = 4 L_{-2}^2|0>
        if h1 == 2 and h2 == 4 and n == 1 and output_weight == 4:
            i1 = self._state_index(2, (2,))
            # L_{-2}^2|0> = (2,2), L_{-4}|0> = (4,)
            for state2 in self._basis_cache.get(4, []):
                i2 = self._state_index(4, state2)
                if state2 == (2, 2):
                    # L_0(L_{-2}^2|0>) = ([-2]+[-2]) L_{-2}^2|0> = 4 * L_{-2}^2|0>
                    i_out = self._state_index(4, (2, 2))
                    if i_out >= 0:
                        mat[i_out, i1 * len(self._basis_cache.get(4, [])) + i2] = 4.0
                elif state2 == (4,):
                    # L_0(L_{-4}|0>) = 4 L_{-4}|0>
                    i_out = self._state_index(4, (4,))
                    if i_out >= 0:
                        mat[i_out, i1 * len(self._basis_cache.get(4, [])) + i2] = 4.0

        # T_{(3)}(L_{-2}^2|0>) = L_2(L_{-2}^2|0>)
        # = [L_2, L_{-2}] L_{-2}|0> + L_{-2} [L_2, L_{-2}]|0>
        # = (4L_0 + c/2) L_{-2}|0> + L_{-2}(c/2)|0>
        # = 4*2*L_{-2}|0> + (c/2)L_{-2}|0> + (c/2)L_{-2}|0>
        # = (8 + c) L_{-2}|0>
        # Output weight = 2+4-3-1 = 2
        if h1 == 2 and h2 == 4 and n == 3 and output_weight == 2:
            i1 = self._state_index(2, (2,))
            for state2 in self._basis_cache.get(4, []):
                i2 = self._state_index(4, state2)
                if state2 == (2, 2):
                    # L_2(L_{-2}^2|0>) = (8+c) T
                    i_out = self._state_index(2, (2,))
                    if i_out >= 0 and i2 >= 0:
                        mat[i_out, i1 * len(self._basis_cache.get(4, [])) + i2] = 8 + self.c_val
                elif state2 == (4,):
                    # L_2(L_{-4}|0>) = [L_2, L_{-4}]|0> = 6 L_{-2}|0> = 6T
                    i_out = self._state_index(2, (2,))
                    if i_out >= 0 and i2 >= 0:
                        mat[i_out, i1 * len(self._basis_cache.get(4, [])) + i2] = 6.0

        return mat


