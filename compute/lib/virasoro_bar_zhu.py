# Main comparison: dimensions at each bar degree
# ============================================================

def total_bar_dim_by_degree(max_n: int = 6, max_weight: int = 12,
                             universal: bool = True,
                             p: int = None, q: int = None) -> Dict[int, int]:
    """Total dimension of bar degree n through given max_weight.

    Returns {n: sum_h dim B^n_h} for n = 1, ..., max_n.
    """
    result = {}
    for n in range(1, max_n + 1):
        total = 0
        for h in range(2 * n, max_weight + 1):
            if universal:
                total += _universal_chain_dim(n, h)
            else:
                c_val = float(minimal_model_c(p, q))
                bar = VirasoroBarComplex(c_val, max_weight, simple=True, p=p, q=q)
                total += bar.chain_dim(n, h)
        result[n] = total
    return result


