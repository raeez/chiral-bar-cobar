class VirasoroBarComplex:
    """Bar complex of the Virasoro VOA at a specific central charge.

    The bar complex B^n_h consists of elements
        a_1 otimes ... otimes a_n otimes omega
    where a_i in V_+, sum(wt(a_i)) = h, omega in Omega^{n-1}(Conf_n).

    The differential D: B^n_h -> B^{n-1}_h extracts OPE collisions.

    For the UNIVERSAL Virasoro: V_+ has basis given by partitions of h
    into parts >= 2. The OPE coefficients involve c as a parameter.

    For the SIMPLE quotient: V_+ at weight >= null_weight has fewer
    states (null descendants removed).

    Parameters
    ----------
    c_val : number or Symbol
        Central charge value. Use Symbol('c') for generic.
    max_weight : int
        Maximum conformal weight.
    simple : bool
        If True and c_val corresponds to a minimal model, use the
        simple quotient L(c,0) instead of the universal V_c.
    p, q : int or None
        Minimal model parameters. Required if simple=True.
    """

    def __init__(self, c_val, max_weight: int = 10,
                 simple: bool = False, p: int = None, q: int = None):
        self.c_val = c_val
        self.max_weight = max_weight
        self.simple = simple
        self.p = p
        self.q = q

        null_weight = None
        null_dims = None
        if simple and p is not None and q is not None:
            null_weight = vacuum_singular_weight(p, q)
            null_dims = vacuum_null_dimensions(p, q, max_weight)

        self.vacuum = VirasoroVacuumBasis(
            max_weight, null_weight=null_weight, null_dims=null_dims
        )

    def chain_dim(self, n: int, h: int) -> int:
        """Dimension of B^n_h.

        B^n_h = (V_+^{otimes n})_h  otimes  Omega^{n-1}(Conf_n).
        dim = (tensor product dim at weight h) * (n-1)!
        """
        if n <= 0 or h < 2 * n:
            return 0
        form_dim = factorial(n - 1)
        tensor_dim = self._tensor_dim(n, h)
        return tensor_dim * form_dim

    def _tensor_dim(self, n: int, h: int) -> int:
        """Dimension of (V_+^{otimes n})_h: weight-h part of n-fold tensor."""
        if n == 1:
            return self.vacuum.dim(h)
        total = 0
        for a in range(2, h - 2 * (n - 1) + 1):
            d_a = self.vacuum.dim(a)
            if d_a == 0:
                continue
            rest = self._tensor_dim(n - 1, h - a)
            total += d_a * rest
        return total

    def euler_char(self, h: int, max_n: int = None) -> int:
        """Euler characteristic at weight h: sum (-1)^n dim B^n_h."""
        if max_n is None:
            max_n = h // 2
        return sum((-1) ** n * self.chain_dim(n, h)
                   for n in range(1, max_n + 1))


