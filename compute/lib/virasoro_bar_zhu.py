def virasoro_ope_coefficients(c_val=None):
    """Singular OPE T(z)T(w) structure constants.

    T_{(n)} T for n = 0, 1, 2, 3:
      n=3: c/2 * |0>  (quartic pole)
      n=2: 0           (no cubic pole for Virasoro)
      n=1: 2T          (double pole, conformal weight)
      n=0: dT = L_{-3}|0>  (simple pole, translation)

    Returns dict {pole_order: (output_label, coefficient)}.
    """
    if c_val is None:
        c = Symbol('c')
    else:
        c = c_val
    return {
        3: ('vac', c / 2),
        1: ('T', 2),
        0: ('dT', 1),
    }


