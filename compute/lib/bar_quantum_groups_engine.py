@dataclass
class UqSl2PBW:
    r"""PBW-ordered monomials of U_q(sl_2): F^a K^b E^c (or E^c K^b F^a).

    U_q(sl_2) has PBW basis {F^a K^b E^c : a >= 0, b in Z, c >= 0} (generic q).
    We work with a truncated sub-basis where a, c are bounded and b ranges over
    a finite window, which suffices for computing bar cohomology in a bounded
    range of bar degrees.

    The augmentation ideal bar{U_q} = ker(eps) has basis
      - all monomials with a > 0 or c > 0 (E and F are in the kernel)
      - K^b - 1 for b != 0 (since eps(K^b) = 1)

    For the primitive-filtration/Hochschild computation we use a canonical
    generating subspace:
      V = span{E, F, K - 1, K^{-1} - 1}
    (four generators).  The ordered bar B^{ord,n} = V^{otimes n} is a
    finite-dimensional approximation of the true augmentation-ideal bar
    complex.
    """
    q: complex
    a_max: int
    c_max: int
    b_range: Tuple[int, int]  # (b_min, b_max)

    def generating_dimension(self) -> int:
        r"""dim V = 4 (generators E, F, K-1, K^{-1}-1)."""
        return 4


def uq_sl2_generating_space(q: complex) -> Dict[str, Any]:
    r"""Canonical four-dimensional generating space V of bar{U_q(sl_2)}.

    The four generators are E, F, K - 1, K^{-1} - 1.
    All are in the augmentation ideal (eps vanishes on all four).

    Returns a dict with:
      - names: ['E', 'F', 'K-1', 'Kinv-1']
      - dim: 4
      - q: the quantum parameter
      - degrees: [0, 0, 0, 0] (all in the ungraded 'generator' component)
    """
    return {
        "names": ["E", "F", "K-1", "Kinv-1"],
        "dim": 4,
        "q": q,
        "degrees": [0, 0, 0, 0],  # all primitive-like at q = 1
    }


