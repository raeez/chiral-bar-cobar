# 3. Hochschild cochain complex and cohomology dimensions
# =========================================================================

def hochschild_chain_dim(gen_dim: int, degree: int) -> int:
    r"""Dimension of the Hochschild chain group at degree n.

    For a finite-dim augmentation ideal of dimension d:
        dim C_n = d^n
    The normalized Hochschild bar complex.
    """
    if degree < 0:
        return 0
    return gen_dim ** degree


def classical_sl2_cohomology_dimensions() -> Dict[int, int]:
    r"""Classical Chevalley-Eilenberg cohomology H^*(sl_2, k).

    H^0 = k (dim 1)
    H^1 = 0 (sl_2 is perfect, [sl_2, sl_2] = sl_2)
    H^2 = 0 (sl_2 has no central extensions)
    H^3 = k (the fundamental class, generated by the Cartan 3-form)

    For U(sl_2), the Hochschild cohomology with trivial coefficients is
    H^*(U(sl_2), k) = H^*_{Lie}(sl_2, k).

    Reference: Cartan-Eilenberg, "Homological Algebra" Ch. XIII.
    """
    return {0: 1, 1: 0, 2: 0, 3: 1, 4: 0, 5: 0}


def q_deformed_sl2_cohomology_generic(q: complex) -> Dict[int, int]:
    r"""Bar cohomology of U_q(sl_2) at generic q.

    By flat deformation / rigidity of Lie algebra cohomology for semisimple
    algebras, at generic q the cohomology of U_q(sl_2) agrees with that
    of U(sl_2):
        H^0(U_q(sl_2), k) = k  (dim 1)
        H^1(U_q(sl_2), k) = 0
        H^2(U_q(sl_2), k) = 0
        H^3(U_q(sl_2), k) = k  (dim 1, the quantum fundamental class)

    This reflects the rigidity theorem: H^2(sl_2, sl_2) = 0 for semisimple
    Lie algebras means the deformation is unique, so the associated
    cohomology rings agree.

    At q = 1 exactly, q_number(n, 1) = n formally, and [E,F] reduces to
    the classical bracket.  For generic q different from roots of unity,
    the argument is via flatness of the PBW filtration.
    """
    # At generic q, rigidity gives the same answer as U(sl_2)
    return classical_sl2_cohomology_dimensions()


# =========================================================================
# 4. Small quantum group u_q(sl_2) at roots of unity
# =========================================================================

def small_uq_sl2_dimension(p: int) -> int:
    r"""Dimension of the small quantum group u_q(sl_2) at q^{2p} = 1.

    Setting q = exp(pi*i/p), the small quantum group has PBW basis
    {F^a K^b E^c : 0 <= a, b, c < p}, giving dim = p^3.

    Reference: Lusztig, "Finite dimensional Hopf algebras arising from
    quantized universal enveloping algebras", JAMS 3 (1990), 257-296.
    """
    if p < 2:
        raise ValueError(f"Need p >= 2, got {p}")
    return p ** 3


def small_uq_sl2_cohomology_dimensions(p: int) -> Dict[int, int]:
    r"""Cohomology H^*(u_q(sl_2), k) at p-th root of unity.

    Ginzburg-Kumar (Duke 1993) proved:
        H^*(u_q(g), k) = C[N_g]
    where N_g is the nilpotent cone of g.  For sl_2, the nilpotent cone
    N_{sl_2} is a 2-dimensional affine variety (in fact, isomorphic to
    the quadric xy = z^2, the minimal nilpotent orbit closure).

    The cohomology H^*(u_q(sl_2), k) is a polynomial
    ring in one generator of cohomological degree 2 (corresponding to the
    Euler vector field on the nilpotent cone).  Specifically:

        H^{2n}(u_q(sl_2), k) = C   for all n >= 0 (dim 1)
        H^{2n+1}(u_q(sl_2), k) = 0 for all n >= 0

    The generator x in H^2 corresponds to the generator of the polynomial
    ring C[x] = H^*(u_q(sl_2), k).

    This is independent of p (the root of unity order) for p >= 3.

    Reference: Ginzburg-Kumar 1993, Cor. 2.5; Bendel-Nakano-Parshall-Pillen,
    "Support varieties for Frobenius kernels and quantum groups", Tr. Amer.
    Math. Soc. 358 (2006) for the generalization.
    """
    if p < 2:
        raise ValueError(f"Need p >= 2, got {p}")
    return {n: (1 if n % 2 == 0 else 0) for n in range(8)}


def small_uq_sl2_hilbert_series(p: int, max_degree: int = 10) -> List[int]:
    r"""Hilbert series coefficients of H^*(u_q(sl_2), k).

    Returns [a_0, a_1, ..., a_max] where a_n = dim H^n.
    For sl_2: a_{2n} = 1, a_{2n+1} = 0.
    """
    return [1 if n % 2 == 0 else 0 for n in range(max_degree + 1)]


def nilpotent_cone_dimension_sl2() -> int:
    r"""Dimension of the nilpotent cone N_{sl_2}.

    For sl_N, dim N = N^2 - N = N(N-1).
    For sl_2: dim N = 2 * 1 = 2.

    This is the Krull dimension of C[N_{sl_2}], which equals the
    transcendence degree.  The cohomology ring H^*(u_q(sl_2), k)
    has this as its polynomial-ring Krull dimension... but with
    the caveat that we grade by cohomological degree, where the
    classical coordinate functions have degree 2.
    """
    return 2


# =========================================================================
# 5. Drinfeld double D(U_q(sl_2)) and Koszul duality
# =========================================================================

def drinfeld_double_dimension(bar_dim_input: int) -> int:
    r"""Dimension of the Drinfeld double D(B) for B of dimension d.

    For B a finite-dimensional Hopf algebra: dim D(B) = (dim B)^2.
    For the small quantum Borel u_q(b_+) of dimension p^2, the double
    has dimension p^4.
    """
    return bar_dim_input ** 2


def small_uq_borel_dimension(p: int) -> int:
    r"""Dimension of u_q(b_+) at p-th root of unity.

    Generated by E, K with E^p = 0, K^p = 1: PBW basis E^a K^b for
    0 <= a, b < p, giving dim = p^2.

    The Drinfeld double D(u_q(b_+)) has dimension p^4.
    By Drinfeld's theorem + Lusztig's normalization, D(u_q(b_+))
    is the small quantum group u_q(sl_2) TIMES an extra Cartan copy,
    giving a twisted form of u_q(sl_2) tensor C[K_2^{+/-}].
    """
    return p ** 2


def drinfeld_double_koszul_pairing(p: int) -> Dict[str, int]:
    r"""Koszul-duality style pairing for the Drinfeld double D(u_q(b_+)).

    At q = exp(2pi*i/p), the Borel subalgebra u_q(b_+) is Koszul dual
    (in the sense of super-Koszul duality between algebras and coalgebras)
    to u_q(b_-).  The double is the universal home for this pairing:
        D(u_q(b_+)) = u_q(b_+) bowtie u_q(b_-)^{cop}

    The dimensions:
        dim u_q(b_+) = p^2
        dim u_q(b_-) = p^2
        dim D(u_q(b_+)) = p^4

    The Koszul pairing matrix is nondegenerate between E^a K^b in u_q(b_+)
    and F^a K^b in u_q(b_-), with matrix entries given by q-factorials.
    """
    return {
        "dim_borel_plus": small_uq_borel_dimension(p),
        "dim_borel_minus": small_uq_borel_dimension(p),
        "dim_double": small_uq_borel_dimension(p) ** 2,
        "p": p,
    }


def verify_drinfeld_double_selfduality(p: int) -> Dict[str, Any]:
    r"""Verify dimension-level Koszul self-duality for D(u_q(b_+)).

    Check: dim D(u_q(b_+)) = dim u_q(b_+) * dim u_q(b_-) = p^4.
    This is the classical result that the double has the product dimension.
    """
    d_bp = small_uq_borel_dimension(p)
    d_bm = small_uq_borel_dimension(p)
    d_D = drinfeld_double_dimension(d_bp)
    expected = d_bp * d_bm
    return {
        "dim_borel_plus": d_bp,
        "dim_borel_minus": d_bm,
        "dim_double": d_D,
        "dim_product": expected,
        "matches": d_D == expected,
        "p_to_4": p ** 4 == d_D,
    }


