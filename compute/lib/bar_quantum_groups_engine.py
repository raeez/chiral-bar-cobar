def verify_classical_limit_agreement() -> Dict[str, Any]:
    r"""Verify that U_q(sl_2) at q = 1 recovers U(sl_2) cohomology.

    Path 1: Direct CE cohomology of sl_2.
    Path 2: q -> 1 limit of U_q(sl_2) bar cohomology.
    Both should give H^0 = 1, H^1 = 0, H^2 = 0, H^3 = 1.
    """
    classical = classical_sl2_cohomology_dimensions()
    q_deformed = q_deformed_sl2_cohomology_generic(cmath.exp(1j * 0.01))
    agree = all(
        classical.get(n, 0) == q_deformed.get(n, 0)
        for n in range(4)
    )
    return {
        "classical": classical,
        "q_deformed": q_deformed,
        "agree": agree,
    }


