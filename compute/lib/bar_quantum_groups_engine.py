# 2. Ordered bar complex of U_q(sl_2) on the generating subspace
# =========================================================================

@dataclass
class OrderedBarSpace:
    r"""Ordered bar complex data B^{ord,n}(A) = V^{otimes n}.

    For a four-dimensional generating space V, dim B^{ord,n} = 4^n.

    We work with this COMBINATORIAL approximation of the true bar complex,
    which is sufficient for:
    - Dimension counts at low degrees
    - Verification of the q = 1 classical limit
    - Comparison with CE cohomology of U(sl_2)
    - Detection of the universal R-matrix as a (1,1) cohomology class

    The true (infinite-dimensional) bar complex on the full augmentation
    ideal has cohomology computed by the Koszul resolution of quadratic
    algebras (when applicable).
    """
    q: complex
    gen_dim: int  # dim V (usually 4 for U_q(sl_2))
    names: List[str]

    def basis_size(self, degree: int) -> int:
        """dim B^{ord,degree} = gen_dim^degree."""
        return self.gen_dim ** degree

    def enumerate_basis(self, degree: int) -> List[Tuple[int, ...]]:
        """Enumerate ordered tuples (i_1, ..., i_degree) with i_k in range(gen_dim)."""
        if degree == 0:
            return [()]
        return [tuple(idx) for idx in iproduct(range(self.gen_dim), repeat=degree)]


def ordered_bar_uqsl2(q: complex) -> OrderedBarSpace:
    """Build the ordered bar approximation for U_q(sl_2)."""
    data = uq_sl2_generating_space(q)
    return OrderedBarSpace(q=q, gen_dim=data["dim"], names=data["names"])


