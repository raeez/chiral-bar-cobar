r"""Bar cohomology of quantum groups U_q(g) as non-cocommutative Hopf algebras.

MATHEMATICAL FRAMEWORK
======================

The previous quantum_group_bar_engine.py computed U_q(g) FROM the bar complex
of the affine vertex algebra V_k(g).  This engine takes the dual perspective:
U_q(g) is itself a Hopf algebra (non-cocommutative for generic q), and we
compute the bar cohomology of U_q(g) as an algebra/coalgebra in its own right.

The PRIMITIVE structure is the ORDERED bar complex B^{ord}(U_q(g)):

    B^{ord,n}(U_q) := (s^{-1} bar{U_q})^{otimes n}

where bar{U_q} = U_q / C.1 is the augmentation ideal and s^{-1} is the
desuspension (|s^{-1}v| = |v| - 1, AP45).  The ORDERED bar is required
because U_q(g) is non-cocommutative: the antisymmetrization map
pi: B^{ord} -> B^{un} used in the CE framework loses precisely the
non-cocommutative information (the R-matrix).

Because B^{ord} is the primitive structure, this engine works entirely
with ordered tensors.  The "exterior" version is recovered a posteriori
via antisymmetrization (only meaningful at q = 1).

THE SEVEN TASKS
===============

1. B^{ord}(U_q(sl_2)) at generic q: compute cohomology dimensions for the
   four-generator algebra with generators E, F, K, K^{-1}.  The quadratic
   relations KE = q^2 EK, KF = q^{-2} FK, [E,F] = (K-K^{-1})/(q-q^{-1})
   give a quadratic algebra in the PBW sense; the bar cohomology is the
   Koszul dual quadratic cohomology.

2. HH*(U_q(sl_2)) Hochschild cohomology.  The Hochschild cochain complex
   C^n(U_q, U_q) = Hom(U_q^{otimes n}, U_q) with the standard differential.
   Relationship to bar cohomology: HH^n(U_q, k) = Tor_n^{U_q}(k, k) where
   k is the trivial module via the counit, and this equals H^n(B^{bar}(U_q))
   where B^{bar} is the classical (non-cohomological) bar complex.

3. Small quantum group u_q(sl_2) at q root of unity.  Setting q = exp(2pi*i/p),
   the small quantum group u_q(sl_2) is the Hopf subalgebra generated by
   E, F, K subject to E^p = F^p = 0, K^p = 1 (dimension p^3).  At p = 3
   this has dimension 27, at p = 4 dimension 64, etc.  The cohomology
   H*(u_q(sl_2), k) was computed by Ginzburg-Kumar (Duke 1993) and equals
   (as a graded ring) the coordinate ring of the nilpotent cone:
       H*(u_q(g), k) = C[N]
   For sl_2: H*(u_q(sl_2), k) = C[x], a polynomial ring in one generator
   of degree 2 (the nilpotent cone of sl_2 is 2-dimensional).

4. Drinfeld double D(U_q(sl_2)).  By Drinfeld's theorem, D(U_q(b_+)) is
   isomorphic to U_q(sl_2) + (twist by Cartan), where b_+ is the Borel
   subalgebra.  This gives a self-duality statement: U_q(b_+) and
   U_q(b_-) are Koszul dual within the double.  We test this at the level
   of bar cohomology dimensions.

5. Universal R-matrix R in degree (1,1) of the ordered bar.  The R-matrix
       R = q^{H tensor H / 4} sum_n c_n E^n tensor F^n
   lives naturally in U_q(b_+) tensor U_q(b_-) subset U_q tensor U_q.
   Its bar-degree (1,1) is precisely the first non-trivial class.  We
   verify that [R] represents a nonzero element in the ordered bar H^{1,1}.

6. q-deformation of Chevalley-Eilenberg cohomology.  At q = 1 we have
   H*(U(sl_2), k) = H*_{Lie}(sl_2, k), the Lie algebra cohomology.  At
   q root of unity we have H*(u_q(sl_2), k) = C[nilpotent cone].  At
   generic q: H*(U_q, k) agrees with the classical U(sl_2) answer (by
   flat deformation; the cohomology is rigid).

7. Classical limit q -> 1.  We verify that at q = 1 + epsilon, the
   ordered bar dimensions agree with those of U(sl_2).  This is the
   PBW degeneration / flat deformation principle.

CONVENTIONS
===========

- Cohomological grading: |d| = +1.
- Desuspension: |s^{-1}v| = |v| - 1 (AP45).
- q-numbers: [n]_q = (q^n - q^{-n})/(q - q^{-1}).
- Drinfeld-Jimbo: K E K^{-1} = q^2 E, K F K^{-1} = q^{-2} F, [E,F] = (K-K^{-1})/(q-q^{-1}).
- Hopf structure: Delta(E) = E tensor 1 + K tensor E, Delta(F) = F tensor K^{-1} + 1 tensor F,
  Delta(K) = K tensor K.
- Counit: eps(E) = eps(F) = 0, eps(K) = 1.
- Antipode: S(E) = -K^{-1}E, S(F) = -FK, S(K) = K^{-1}.
- Trivial module k: E, F act as 0, K acts as 1 (via the counit).

AP COMPLIANCE
=============

- AP25: B produces a coalgebra; Omega(B(A)) ~= A; D_Ran(B(A)) ~= B(A^!).  All three are distinct.
- AP33: Koszul dual of U_q is distinct from q -> 1/q and from negative-level substitution.
- AP45: desuspension LOWERS cohomological degree by 1.
- AP50: A^!_infty and A^! are different objects.

References
==========
- Ginzburg-Kumar, "Cohomology of quantum groups at roots of unity", Duke Math. J. 69 (1993), 179-198
- Drinfeld, "Quantum groups", ICM 1986
- Kassel, "Quantum Groups", GTM 155 (1995)
- Chari-Pressley, "A Guide to Quantum Groups" (1994)
- Arkhipov-Gaitsgory, "Another realization of the category O" (2003)
- Bezrukavnikov-Finkelberg-Mirkovic, "Equivariant homology and K-theory of affine Grassmannians and Toda lattices"
- concordance.tex: thm:mc2-bar-intrinsic, AP25, AP33, AP45, AP50
- quantum_group_bar_engine.py (companion: U_q FROM bar)
- quantum_group_root_of_unity_engine.py (companion: root-of-unity truncation)
"""

