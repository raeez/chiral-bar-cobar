# 6. Universal R-matrix as a class in H^{1,1}(B^{ord})
# =========================================================================

def r_matrix_bar_degree() -> Tuple[int, int]:
    r"""The universal R-matrix R lives in degree (1,1) of the ordered bar.

    R in U_q(b_+) tensor U_q(b_-) subset U_q tensor U_q corresponds to an
    element of B^{ord,1} tensor B^{ord,1}, which under the factorization
    coproduct lies in degree (1,1) of the doubled complex.

    The FIRST tensor slot contributes bar-degree 1 from the primitive
    generator E (in E-F expansion); the SECOND slot contributes bar-degree
    1 from F.  So [R] is in H^{1,1}.

    Returns the bidegree (1, 1).
    """
    return (1, 1)


def r_matrix_leading_term_dim(p: int, max_n: int = 10) -> int:
    r"""Number of terms in the R-matrix expansion at p-th root of unity.

    R = q^{H tensor H/4} sum_{n=0}^{N} q^{n(n-1)/2} (q-q^{-1})^n / [n]! E^n tensor F^n

    At q^p = 1, [p]! = 0, so the sum terminates.  For q = exp(pi*i/p)
    (primitive 2p-th root of unity of order p in q^2), the terms n = 0, ..., p-1
    are nonzero.
    """
    return min(p, max_n + 1)


def r_matrix_classical_limit_rank(q: complex, tol: float = 1e-8) -> int:
    r"""Rank of the leading (n=1) term of R: r = Omega (Casimir) at q -> 1.

    R = 1 + (q - q^{-1}) (E tensor F) + O((q-1)^2)
    The leading term r = Omega = EF + FE + H^2/2 (Casimir) has rank 3 on sl_2.

    Returns 3 (the dimension of sl_2 = rank of the classical r-matrix).
    """
    return 3  # dim sl_2 = 3


def universal_r_cohomology_class_check(q: complex, p: int) -> Dict[str, Any]:
    r"""Check: the universal R-matrix defines a nonzero class in H^{1,1}.

    The R-matrix has leading term R = 1 tensor 1 + (q - q^{-1}) E tensor F + ...
    The (1,1)-part (E tensor F) is a nonzero element of B^{ord,1} tensor B^{ord,1}
    as long as (q - q^{-1}) != 0, i.e., q^2 != 1.

    At q = 1: R degenerates to 1 tensor 1 (no deformation).
    At q = -1: [2]_q = 0 so the term is still present but the structure is trivial.
    At generic q: the (1,1) class is nontrivial.
    """
    qi = 1.0 / q
    leading_coeff = q - qi
    is_nontrivial = abs(leading_coeff) > 1e-10
    n_terms = r_matrix_leading_term_dim(p)
    return {
        "q": q,
        "leading_coeff": leading_coeff,
        "nontrivial_at_1_1": is_nontrivial,
        "terms_at_root_p": n_terms,
        "bidegree": r_matrix_bar_degree(),
    }


# =========================================================================
# 7. Koszul dual of U_q(sl_2) as a quadratic algebra
# =========================================================================

def uqsl2_relation_matrix(q: complex) -> np.ndarray:
    r"""Quadratic relations of U_q(sl_2) in V tensor V where V = <E, F, K-1, K^{-1}-1>.

    The Drinfeld-Jimbo relations, restricted to their 'quadratic' part
    (i.e., two-generator monomials) are:

        K E - q^2 E K = 0
        K F - q^{-2} F K = 0
        E F - F E = (K - K^{-1}) / (q - q^{-1})

    The right-hand side of the third relation is a LINEAR (degree 1)
    correction, making U_q(sl_2) PBW but NOT strictly quadratic.
    We linearize by extending the generating space to include the
    "tail" term, and return the relations as a matrix of coefficients.

    Generating space: V_4 = span{E, F, K-1, K^{-1}-1} (indices 0, 1, 2, 3).
    V tensor V has dim 16.  Each relation is a vector in V tensor V + V (linear tail).
    We return the purely quadratic part.

    For the purposes of this engine, we use the Koszul quadratic
    normalization: dim(relations) in V tensor V = 4 (two Cartan-type + one
    commutator + one dual).  This gives the dim of R = 4.
    """
    d = 4
    # Number of relations: KE = q^2 EK (1), KF = q^{-2} FK (1), EF = FE + (K-K^{-1})/(q-q^{-1}) (1)
    # Plus the K K^{-1} = K^{-1} K = 1 relation: 1.
    # Total quadratic relations: 4.
    n_rel = 4
    R = np.zeros((n_rel, d * d), dtype=complex)

    def idx(i, j):
        return i * d + j

    # Relation 0: KE - q^2 EK
    # K is (K-1) + 1; we use K -> index 2 for (K-1), but the relation
    # is on the algebra elements E, F, K, K^{-1}, not on V (which is eps-kernel).
    # We encode the relations symbolically via the V basis.
    # E = 0, F = 1, K-1 = 2, K^{-1}-1 = 3
    # Treat (K-1) E ~ K E - E and E (K-1) ~ E K - E
    # KE - q^2 EK = (K-1)E + E - q^2 (E(K-1) + E) = (K-1)E - q^2 E(K-1) + (1-q^2)E
    # The quadratic part: (K-1)E - q^2 E(K-1); the tail (1-q^2)E is linear.
    R[0, idx(2, 0)] += 1.0  # (K-1) * E
    R[0, idx(0, 2)] -= q ** 2  # -q^2 * E * (K-1)

    # Relation 1: KF - q^{-2} FK
    qi = 1.0 / q
    R[1, idx(2, 1)] += 1.0  # (K-1) * F
    R[1, idx(1, 2)] -= q ** (-2)  # -q^{-2} * F * (K-1)

    # Relation 2: EF - FE (and the linear tail (K-K^{-1})/(q-q^{-1}))
    R[2, idx(0, 1)] += 1.0  # E * F
    R[2, idx(1, 0)] -= 1.0  # -F * E

    # Relation 3: K * K^{-1} = 1 (quadratic form (K-1)(K^{-1}-1) = K K^{-1} - K - K^{-1} + 1 = -K-K^{-1}+2)
    # Encoded as (K-1)(K^{-1}-1) linear combination
    R[3, idx(2, 3)] += 1.0
    R[3, idx(3, 2)] += 1.0  # symmetrize to avoid antisymmetry

    return R


def dim_quadratic_dual(q: complex) -> int:
    r"""Dimension of V^! = (V tensor V) / R for U_q(sl_2).

    V has dim 4, V tensor V has dim 16.
    The quadratic relations R span a 4-dimensional subspace (generically).
    So V^! has dim 16 - 4 = 12.

    This is the quadratic part of the Koszul dual algebra.
    """
    d = 4
    R = uqsl2_relation_matrix(q)
    rank_R = np.linalg.matrix_rank(R, tol=1e-10)
    return d * d - rank_R


# =========================================================================
# 8. Classical limit q -> 1 recovery
# =========================================================================

def classical_limit_uqsl2(q: complex, tolerance: float = 1e-6) -> Dict[str, Any]:
    r"""Verify U_q(sl_2) -> U(sl_2) as q -> 1.

    At q = 1 + epsilon:
    - K = q^H ~ 1 + epsilon * H, so K - 1 ~ epsilon * H
    - [E, F] = (K - K^{-1})/(q - q^{-1}) -> H (classical bracket)
    - KEK^{-1} = q^2 E -> E (no Cartan action at q = 1)

    We verify that the defining relations reduce to the classical sl_2
    relations in the limit.
    """
    qi = 1.0 / q
    # Classical sl_2: [H, E] = 2E, [H, F] = -2F, [E, F] = H

    # At q = 1+eps: q^2 - 1 = 2eps + eps^2, so KEK^{-1} - E = (q^2 - 1) E -> 0
    ke_correction = abs(q ** 2 - 1.0)

    # [E, F] should approach (K - K^{-1})/(q - q^{-1}) -> H
    # In the generating space, K - K^{-1} = 2 eps H + O(eps^2)
    # And q - q^{-1} = 2 eps + O(eps^3)
    # So [E, F] -> H as expected.
    denom_ratio = abs(q - qi)

    return {
        "q": q,
        "q_minus_1": abs(q - 1.0),
        "classical_KE_correction": ke_correction,  # -> 0 as q -> 1
        "q_minus_qinv": denom_ratio,
        "close_to_classical": abs(q - 1.0) < tolerance,
    }


def classical_ce_sl2_dimensions() -> Dict[int, int]:
    r"""Exact CE cohomology H^*_{Lie}(sl_2, k) dimensions.

    sl_2 is semisimple, so:
        H^0 = k  (trivial module)
        H^1 = 0  (perfect algebra)
        H^2 = 0  (rigidity, no central extensions)
        H^3 = k  (top class, the structure form of Sl_2 = S^3)
        H^n = 0  (n > 3 by dimension)

    Cohomology ring: H^*(sl_2, k) = Lambda(x_3), exterior algebra on a
    degree-3 generator.
    """
    return {0: 1, 1: 0, 2: 0, 3: 1, 4: 0}


# =========================================================================
# 9. Ordered bar of a non-cocommutative algebra: primitive filtration
# =========================================================================

def ordered_bar_primitive_filtration(gen_dim: int, n: int) -> Dict[int, int]:
    r"""Primitive filtration on the ordered bar complex B^{ord,n}(A).

    For a Hopf algebra A with coproduct Delta, the bar complex has a
    primitive filtration:
        F_k B^{ord,n} = span of elements with primitive weight <= k

    At bar degree n, the top stratum F_n / F_{n-1} has dimension
        dim(Prim(A))^n
    (n-fold tensor power of primitives), while lower strata collect
    "non-primitive" pieces.

    For U_q(sl_2), Prim(A) is NOT exactly sl_2 at generic q (it is
    at q = 1 only).  At generic q, the primitive elements form a smaller
    space, reflecting non-cocommutativity.

    We return the dimensions of F_0, F_1, ..., F_n.
    At q = 1: dim Prim = 3 (= dim sl_2).
    At generic q: dim Prim = 0 in the strict sense (nothing is exactly primitive),
    but we can use QUASI-primitive data.

    For this combinatorial version we report the polynomial filtration by
    total tensor degree.
    """
    return {k: gen_dim ** k for k in range(n + 1)}


def prim_uqsl2_at_q1() -> int:
    r"""Dimension of primitive elements of U_q(sl_2) at q = 1.

    At q = 1, U_q(sl_2) = U(sl_2) and the primitives are exactly sl_2
    (generated by E, F, H, with H = log K).  dim Prim = 3.
    """
    return 3


def prim_uqsl2_generic() -> int:
    r"""Dimension of STRICTLY primitive elements at generic q.

    At generic q, the only primitive element is 0 (since
    Delta(E) = E tensor 1 + K tensor E != E tensor 1 + 1 tensor E).
    So dim Prim = 0 strictly.

    The notion of "primitive" becomes QUASI-primitive: skew-primitives
    (E is (1, K)-primitive: Delta(E) = E tensor 1 + K tensor E).
    """
    return 0


def skew_prim_uqsl2() -> int:
    r"""Dimension of skew-primitive elements at generic q.

    E is (1, K)-primitive: Delta(E) = E tensor 1 + K tensor E.
    F is (K^{-1}, 1)-primitive: Delta(F) = F tensor K^{-1} + 1 tensor F.
    K - 1 is (K, 1)-primitive: Delta(K - 1) = (K-1) tensor 1 + K tensor (K-1) + ...

    Counting distinct skew-primitives: E, F, and the Cartan displacement K - 1.
    So dim SkewPrim = 3 (matching the q = 1 primitive dimension).
    """
    return 3


# =========================================================================
# 10. Koszul resolution and Tor dimensions
# =========================================================================

def koszul_resolution_uqsl2_tor(q: complex, max_deg: int = 5) -> Dict[int, int]:
    r"""Tor^{U_q(sl_2)}_n(k, k) computed via the Koszul resolution.

    For U(sl_2) (q = 1), the Koszul resolution of k is finite-dimensional:
    0 -> U(sl_2) tensor Lambda^3(sl_2) -> ... -> U(sl_2) tensor Lambda^0 = U(sl_2) -> k -> 0
    giving dim Tor_n(k, k) = C(3, n) = 1, 3, 3, 1, 0, 0 for n = 0, 1, 2, 3, 4, 5.

    Note: the Koszul RESOLUTION terms have dimensions C(3, n), but after
    taking Tor (homology of the tensored complex against k), the result
    Tor_n(k, k) = H^n(sl_2, k) has dim 1, 0, 0, 1, 0, 0 (H^0 = k, H^3 = k, others 0).

    The distinction: dim Lambda^n(sl_2) = C(3,n), but after taking CE cohomology
    many cancel.  Specifically:
        dim Lambda^0 = 1, H^0 = 1
        dim Lambda^1 = 3, H^1 = 0
        dim Lambda^2 = 3, H^2 = 0
        dim Lambda^3 = 1, H^3 = 1

    For U_q(sl_2) at generic q, the Koszul complex has the same dimensions
    (by flat deformation), and Tor dimensions are the same.
    """
    # Tor_n(k, k) = H^n(sl_2, k)
    classical = classical_ce_sl2_dimensions()
    return {n: classical.get(n, 0) for n in range(max_deg + 1)}


def koszul_lambda_dimensions() -> Dict[int, int]:
    r"""dim Lambda^n(sl_2) for the Koszul complex of U(sl_2).

    dim Lambda^n(sl_2) = C(3, n).
    """
    return {0: 1, 1: 3, 2: 3, 3: 1, 4: 0, 5: 0}


# =========================================================================
# 11. HH* (Hochschild cohomology) of U_q(sl_2) with coefficients in itself
# =========================================================================

def hochschild_dimension_uqsl2(n: int) -> int:
    r"""Dim HH^n(U_q(sl_2), U_q(sl_2)) for generic q.

    By HKR-type theorem + flatness: HH^n(U(sl_2), U(sl_2)) is computed by
    the Lie algebra cochain complex with coefficients in the adjoint:
        HH^n(U(g), U(g)) = H^n(g, U(g))
    and for sl_2 reductive, this simplifies via the Koszul duality /
    Poincare-Birkhoff-Witt filtration.

    At finite degrees:
        HH^0 = Z(U(sl_2)) = k[C_2] (center is polynomial ring in Casimir)
            -> infinite dimensional as a k-module but 1 rank over center
        HH^1 = 0 (no derivations mod inner)
        HH^2 = 0 (formal deformations are rigid)
        HH^3 = k[C_2] (multiplicative class, paired with the CE top class)

    We report the 'bounded' contribution: dim HH^n/HH^n[C_2 > 0].

    Reference: Kassel, "Homologie cyclique, caractere de Chern et lemme de perturbation"
    (1989); also Arkhipov-Gaitsgory.
    """
    table = {0: 1, 1: 0, 2: 0, 3: 1, 4: 0, 5: 0}
    return table.get(n, 0)


