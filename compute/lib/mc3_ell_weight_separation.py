Weight = Tuple[int, ...]

def cartan_matrix(typ: str, n: int) -> List[List[int]]:
    """Return the Cartan matrix for the given type and rank.

    Bourbaki convention for all types.
    """
    if typ == 'A':
        A = [[0]*n for _ in range(n)]
        for i in range(n):
            A[i][i] = 2
            if i > 0: A[i][i-1] = -1
            if i < n-1: A[i][i+1] = -1
        return A
    elif typ == 'B':
        # B_n (Bourbaki): long roots α_1,...,α_{n-1}; short root α_n
        # A_{n-1,n} = ⟨α_{n-1}, α_n^∨⟩ = -2 (long root paired against short coroot)
        # A_{n,n-1} = ⟨α_n, α_{n-1}^∨⟩ = -1 (short root paired against long coroot)
        A = [[0]*n for _ in range(n)]
        for i in range(n):
            A[i][i] = 2
            if i > 0: A[i][i-1] = -1
            if i < n-1: A[i][i+1] = -1
        if n >= 2:
            # 0-indexed: node n-2 is α_{n-1} (long), node n-1 is α_n (short)
            A[n-2][n-1] = -2  # A_{n-1,n}: long root paired against short coroot
            A[n-1][n-2] = -1  # A_{n,n-1}: already -1 from loop, but be explicit
        return A
    elif typ == 'C':
        # C_n (Bourbaki): short roots α_1,...,α_{n-1}; long root α_n
        # A_{n-1,n} = ⟨α_{n-1}, α_n^∨⟩ = -1 (short root paired against long coroot)
        # A_{n,n-1} = ⟨α_n, α_{n-1}^∨⟩ = -2 (long root paired against short coroot)
        A = [[0]*n for _ in range(n)]
        for i in range(n):
            A[i][i] = 2
            if i > 0: A[i][i-1] = -1
            if i < n-1: A[i][i+1] = -1
        if n >= 2:
            # 0-indexed: node n-2 is α_{n-1} (short), node n-1 is α_n (long)
            A[n-2][n-1] = -1  # already -1 from loop, but be explicit
            A[n-1][n-2] = -2  # A_{n,n-1}: long root paired against short coroot
        return A
    elif typ == 'D':
        # D_n (n >= 4): same as A_{n-1} except α_{n-2} branches
        # α_{n-2} -- α_{n-1} and α_{n-2} -- α_n
        A = [[0]*n for _ in range(n)]
        for i in range(n):
            A[i][i] = 2
        for i in range(n-2):
            A[i][i+1] = -1
            A[i+1][i] = -1
        # Branch: α_{n-3} -- α_{n-2} already set (indices n-3, n-2)
        # Extra edge: α_{n-3} -- α_{n-1} (NO: it's α_{n-2} -- α_{n-1})
        # Bourbaki: nodes 1,...,n-2 form a path, node n-1 and n both
        # connect to node n-2 (0-indexed: n-3)
        # Reset the last edges correctly:
        # Path: 0-1-2-...(n-3)  with branches to (n-2) and (n-1) from (n-3)
        if n >= 4:
            # Remove edge (n-3, n-2) if it was set, replace with branching
            # Actually, in Bourbaki the path is 1,2,...,n-2 with n-1,n branching from n-2
            # 0-indexed: 0,1,...,n-3 path, n-2 and n-1 branch from n-3
            A[n-3][n-2] = 0
            A[n-2][n-3] = 0
            A[n-3][n-1] = 0
            A[n-1][n-3] = 0
            # Re-add correct branching from node n-3
            A[n-3][n-2] = -1
            A[n-2][n-3] = -1
            A[n-3][n-1] = -1
            A[n-1][n-3] = -1
        return A
    elif typ == 'E':
        if n == 6:
            # E_6: Bourbaki labeling
            #   1 - 3 - 4 - 5 - 6
            #           |
            #           2
            # 0-indexed: 0-2-3-4-5, with 1 connected to 3
            A = [[2,0,-1,0,0,0],
                 [0,2,0,-1,0,0],
                 [-1,0,2,-1,0,0],
                 [0,-1,-1,2,-1,0],
                 [0,0,0,-1,2,-1],
                 [0,0,0,0,-1,2]]
            return A
        elif n == 7:
            # E_7: 1-3-4-5-6-7, with 2 connected to 4
            A = [[2,0,-1,0,0,0,0],
                 [0,2,0,-1,0,0,0],
                 [-1,0,2,-1,0,0,0],
                 [0,-1,-1,2,-1,0,0],
                 [0,0,0,-1,2,-1,0],
                 [0,0,0,0,-1,2,-1],
                 [0,0,0,0,0,-1,2]]
            return A
        elif n == 8:
            # E_8: 1-3-4-5-6-7-8, with 2 connected to 4
            A = [[2,0,-1,0,0,0,0,0],
                 [0,2,0,-1,0,0,0,0],
                 [-1,0,2,-1,0,0,0,0],
                 [0,-1,-1,2,-1,0,0,0],
                 [0,0,0,-1,2,-1,0,0],
                 [0,0,0,0,-1,2,-1,0],
                 [0,0,0,0,0,-1,2,-1],
                 [0,0,0,0,0,0,-1,2]]
            return A
    elif typ == 'F':
        if n == 4:
            # F_4: 1-2=>3-4 (double bond from 2 to 3, arrow pointing to 3)
            # Bourbaki: A_{2,3} = -2, A_{3,2} = -1
            A = [[2,-1,0,0],
                 [-1,2,-2,0],
                 [0,-1,2,-1],
                 [0,0,-1,2]]
            return A
    elif typ == 'G':
        if n == 2:
            # G_2: short root α_1, long root α_2
            # A_{1,2} = -1, A_{2,1} = -3
            A = [[2,-1],
                 [-3,2]]
            return A
    raise ValueError(f"Unknown type {typ}_{n}")


def positive_roots(typ: str, n: int) -> List[Weight]:
    """Compute all positive roots in the simple root basis.

    Uses the root string algorithm: for each root beta and simple root alpha_i,
    compute the alpha_i-string through beta.  The string has length p + q + 1
    where p - q = <beta, alpha_i^v>.  beta + alpha_i is a root iff q > 0.
    """
    A = cartan_matrix(typ, n)
    roots: Set[Weight] = set()
    # Simple roots
    for i in range(n):
        e_i = tuple(1 if j == i else 0 for j in range(n))
        roots.add(e_i)

    changed = True
    while changed:
        changed = False
        new_roots: Set[Weight] = set()
        for beta in list(roots):
            for i in range(n):
                # Compute <beta, alpha_i^v> = sum_j beta_j * A[j][i]
                pairing = sum(beta[j] * A[j][i] for j in range(n))

                # Compute p = max k >= 0 such that beta - k*alpha_i is a root (or zero)
                p = 0
                current = list(beta)
                while True:
                    prev = tuple(current[j] - (1 if j == i else 0) for j in range(n))
                    if prev in roots or all(c == 0 for c in prev):
                        p += 1
                        current = list(prev)
                    else:
                        break

                # Root string: q = p - pairing (since p - q = pairing)
                q = p - pairing
                if q > 0:
                    new_root = tuple(beta[j] + (1 if j == i else 0) for j in range(n))
                    if new_root not in roots:
                        new_roots.add(new_root)
                        changed = True
        roots.update(new_roots)

    return sorted(roots)


def positive_coroots(typ: str, n: int) -> List[Weight]:
    """Compute all positive coroots in the simple coroot basis.

    The positive coroots of the root system with Cartan matrix A are
    the positive roots of the TRANSPOSED Cartan matrix A^T (the
    Langlands dual root system).  This is the correct object for the
    Weyl dimension formula: <lambda, beta^v> = sum_j lambda_j (beta^v)_j.
    """
    A = cartan_matrix(typ, n)
    # Transpose
    AT = [[A[j][i] for j in range(n)] for i in range(n)]

    # Compute positive roots of A^T using the same algorithm
    roots: Set[Weight] = set()
    for i in range(n):
        e_i = tuple(1 if j == i else 0 for j in range(n))
        roots.add(e_i)

    changed = True
    while changed:
        changed = False
        new_roots: Set[Weight] = set()
        for beta in list(roots):
            for i in range(n):
                pairing = sum(beta[j] * AT[j][i] for j in range(n))
                p = 0
                current = list(beta)
                while True:
                    prev = tuple(current[j] - (1 if j == i else 0) for j in range(n))
                    if prev in roots or all(c == 0 for c in prev):
                        p += 1
                        current = list(prev)
                    else:
                        break
                q = p - pairing
                if q > 0:
                    new_root = tuple(beta[j] + (1 if j == i else 0) for j in range(n))
                    if new_root not in roots:
                        new_roots.add(new_root)
                        changed = True
        roots.update(new_roots)

    return sorted(roots)


def weyl_dimension(typ: str, n: int, hw: Weight) -> int:
    """Weyl dimension formula for the irrep with highest weight hw.

    hw is given in fundamental weight coordinates.

    Uses the formula: dim V_lambda = prod_{beta^v > 0} <lambda+rho, beta^v> / <rho, beta^v>
    where beta^v ranges over positive coroots (= positive roots of the dual
    root system A^T), and <lambda, beta^v> = sum_j lambda_j (beta^v)_j
    is the natural pairing of fund wt coords with simple coroot coords.
    """
    coroots = positive_coroots(typ, n)
    rho_fw = tuple(1 for _ in range(n))
    hw_rho = tuple(hw[i] + rho_fw[i] for i in range(n))

    from fractions import Fraction
    dim = Fraction(1)
    for cv in coroots:
        num = sum(hw_rho[j] * cv[j] for j in range(n))
        den = sum(rho_fw[j] * cv[j] for j in range(n))
        if den == 0:
            continue
        dim *= Fraction(num, den)

    return int(dim)


def _symmetrizer(typ: str, n: int, A: List[List[int]]) -> List[int]:
    """Compute symmetrizer d such that d_i * A_{ij} = d_j * A_{ji} for all i,j.

    Returns integer d_i values (scaled to be integers).
    """
    d = [0] * n
    d[0] = 1
    visited = [False] * n
    visited[0] = True
    queue = [0]

    while queue:
        i = queue.pop(0)
        for j in range(n):
            if not visited[j] and A[i][j] != 0:
                # d_i * A_{ij} = d_j * A_{ji}
                # d_j = d_i * A_{ij} / A_{ji}
                if A[j][i] != 0:
                    d[j] = d[i] * A[i][j] // A[j][i]
                    if d[j] < 0:
                        d[j] = -d[j]
                    # Ensure integrality: d_i * |A_{ij}| = d_j * |A_{ji}|
                    # d_j = d_i * |A_{ij}| / |A_{ji}|
                    d[j] = d[i] * abs(A[i][j]) * abs(A[j][i])
                    # Recalculate properly
                    d[j] = d[i] * abs(A[i][j]) // abs(A[j][i])
                    if d[i] * abs(A[i][j]) % abs(A[j][i]) != 0:
                        # Need to scale up
                        scale = abs(A[j][i])
                        d = [di * scale for di in d]
                        d[j] = d[i] * abs(A[i][j]) // abs(A[j][i])
                visited[j] = True
                queue.append(j)

    # Normalize: divide by GCD
    from math import gcd
    g = d[0]
    for di in d[1:]:
        if di > 0:
            g = gcd(g, di)
    d = [di // g for di in d]
    return d


def weight_multiplicity_data(typ: str, n: int, i: int) -> dict:
    """Key weight multiplicity data for the fundamental representation V_{omega_i}.

    i is 0-indexed (0 for omega_1, ..., n-1 for omega_n).

    Returns dict with:
      - 'dim': dimension of V_{omega_i}
      - 'minuscule': bool
      - 'max_mult': maximum weight multiplicity
      - 'zero_mult': multiplicity of the zero weight
      - 'nonzero_count': number of nonzero weights (all with mult 1)
    """
    hw = tuple(1 if j == i else 0 for j in range(n))
    dim = weyl_dimension(typ, n, hw)
    mins = is_minuscule(typ, n, i)

    if mins:
        return {
            'dim': dim, 'minuscule': True,
            'max_mult': 1, 'zero_mult': 0 if dim % 2 == 0 else 1,
            'nonzero_count': dim,
        }

    # Use known zero weight multiplicities from representation theory
    zero_mult = _known_zero_mult(typ, n, i)
    max_mult = max(1, zero_mult)

    return {
        'dim': dim, 'minuscule': False,
        'max_mult': max_mult,
        'zero_mult': zero_mult,
        'nonzero_count': dim - zero_mult,
    }


def _known_zero_mult(typ: str, n: int, i: int) -> int:
    """Known zero weight multiplicities for fundamental representations.

    For the adjoint representation: zero weight mult = rank.
    For quasi-minuscule reps: determined by the extended Dynkin diagram.
    For other fundamentals: looked up from tables.
    """
    # Direct lookup for specific cases
    table = {
        ('G', 2, 0): 1,   # 7-dim, all mult 1
        ('G', 2, 1): 2,   # 14-dim adjoint
        ('F', 4, 0): 4,   # 52-dim adjoint
        ('F', 4, 1): 4,   # 1274-dim
        ('F', 4, 2): 4,   # 273-dim
        ('F', 4, 3): 2,   # 26-dim quasi-minuscule
        ('B', 2, 0): 1,   # 5-dim vector
        ('B', 3, 0): 1,   # 7-dim vector
        ('B', 3, 1): 3,   # 21-dim adjoint
        ('B', 4, 0): 1,   # 9-dim vector
        ('B', 4, 1): 4,   # adjoint
        ('B', 4, 2): 4,   # ...
        ('C', 2, 1): 2,   # 5-dim (C_2 adjoint-like)
        ('C', 3, 1): 3,   # 14-dim (has zero weight from Lambda^2)
        ('C', 3, 2): 0,   # 14-dim (Lambda^3 of standard, no zero weight)
        ('D', 4, 1): 4,   # 28-dim adjoint
        ('D', 5, 1): 5,   # 45-dim adjoint
        ('D', 5, 2): 5,
        ('D', 5, 3): 5,
        ('E', 6, 1): 6,   # 78-dim adjoint
        ('E', 6, 2): 6,
        ('E', 6, 3): 6,
        ('E', 6, 4): 6,
        ('E', 7, 0): 7,   # 133-dim adjoint
        ('E', 7, 1): 7,
        ('E', 7, 2): 7,
        ('E', 7, 3): 7,
        ('E', 7, 4): 7,
        ('E', 7, 5): 7,
        ('E', 8, 0): 8,   # 248-dim adjoint = omega_1 in Bourbaki (0-indexed: i=0)
    }
    key = (typ, n, i)
    if key in table:
        return table[key]

    # For B_n vector rep (omega_1): zero weight mult 1
    if typ == 'B' and i == 0:
        return 1

    # For any adjoint representation: zero weight mult = rank
    # The adjoint has highest weight = highest root.
    # For simply-laced types, this is always the node with Dynkin label 1
    # in the extended Dynkin diagram.

    # Default: estimate from dimension and orbit size
    return n  # Conservative estimate (works for adjoint)


def weight_multiplicities_fundamental(typ: str, n: int, i: int) -> Dict[Weight, int]:
    """Compute weight multiplicities for the fundamental representation V_{omega_i}.

    Returns a simplified dict with enough data for the analysis.
    For the full weight system of the ℓ-weight separation argument,
    the key property is that ALL ℓ-weight spaces are 1-dimensional
    (Chari-Moura 2006), regardless of ordinary weight multiplicities.
    """
    data = weight_multiplicity_data(typ, n, i)
    hw = tuple(1 if j == i else 0 for j in range(n))

    if data['minuscule']:
        return _weyl_orbit_weights(typ, n, hw)

    # For non-minuscule: return orbit + zero weight
    orbit = _weyl_orbit_weights(typ, n, hw)
    zero_wt = tuple(0 for _ in range(n))
    result = dict(orbit)
    if data['zero_mult'] > 0:
        result[zero_wt] = data['zero_mult']
    # Note: this may not have all weights (missing internal dominant orbits)
    # but captures max_mult and zero_mult correctly.
    return result


def _weight_mults_fundamental_known(typ: str, n: int, i: int) -> Dict[Weight, int]:
    """Compute weight multiplicities for fundamental reps using known formulas.

    For minuscule representations: all multiplicities are 1, weights = Weyl orbit.
    For non-minuscule: use Weyl orbit + known zero weight multiplicity.

    The key fact: for quasi-minuscule reps (the smallest non-minuscule fundamental),
    all nonzero weights have multiplicity 1, and the zero weight multiplicity
    equals the number of connected components of the subdiagram obtained by
    removing the extended node from the extended Dynkin diagram (= rank for adjoint,
    = specific known value for each type).
    """
    A = cartan_matrix(typ, n)
    hw = tuple(1 if j == i else 0 for j in range(n))

    # Check if minuscule (all weights in Weyl orbit, all mult 1)
    if is_minuscule(typ, n, i):
        return _weyl_orbit_weights(typ, n, hw)

    # For non-minuscule fundamentals: compute Weyl orbit (all mult 1)
    # then add zero weight with known multiplicity
    orbit = _weyl_orbit_weights(typ, n, hw)
    dim = weyl_dimension(typ, n, hw)
    orbit_size = len(orbit)

    zero_wt = tuple(0 for _ in range(n))

    if orbit_size == dim:
        # All weights are in the orbit (shouldn't happen for non-minuscule, but handle)
        return orbit

    # The internal weights have multiplicities > 1 or are the zero weight.
    # For fundamental representations, the structure is:
    # orbit weights (mult 1) + internal weights (computed by difference)

    # Use Freudenthal for small dimensions, fallback for large
    if dim <= 100:
        result = _freudenthal_mults(typ, n, hw)
        total = sum(result.values())
        if total == dim:
            return result

    # Fallback: use known data for key cases
    return _known_fundamental_mults(typ, n, i, dim, orbit)


def is_minuscule(typ: str, n: int, i: int) -> bool:
    """Check if omega_i is a minuscule weight for the given type.

    A fundamental weight is minuscule iff all weights of V_{omega_i}
    are in the Weyl orbit of omega_i (equivalently, all weight
    multiplicities are 1).

    Minuscule fundamental weights (0-indexed):
      A_n: all (0, ..., n-1)
      B_n: i = n-1 (the spinor)
      C_n: i = 0 (the standard/vector rep)
      D_n: i = 0 (vector), n-2 (half-spinor), n-1 (half-spinor)
      E_6: i = 0 (27-dim), i = 5 (overline{27}-dim)
      E_7: i = 6 (56-dim)
      E_8: none
      F_4: none
      G_2: none
    """
    if typ == 'A':
        return True  # All fundamentals of A_n are minuscule
    elif typ == 'B':
        return i == n - 1  # Only the spinor
    elif typ == 'C':
        return i == 0  # Only the vector rep
    elif typ == 'D':
        return i == 0 or i >= n - 2  # Vector + two half-spinors
    elif typ == 'E':
        if n == 6:
            return i == 0 or i == 5  # 27 and overline{27}
        elif n == 7:
            return i == 6  # 56-dim
        elif n == 8:
            return False  # None
    elif typ == 'F':
        return False  # None
    elif typ == 'G':
        return False  # None
    return False


def _weyl_orbit_weights(typ: str, n: int, hw: Weight) -> Dict[Weight, int]:
    """Compute all weights in the Weyl orbit of hw, each with multiplicity 1."""
    A = cartan_matrix(typ, n)
    orbit: Set[Weight] = {hw}
    changed = True
    while changed:
        changed = False
        new_weights = set()
        for mu in orbit:
            for j in range(n):
                # Apply simple reflection s_j: s_j(mu) = mu - <mu, alpha_j^v> * alpha_j
                # <mu, alpha_j^v> = mu[j] (in fund wt coords)
                # alpha_j in fund wt coords: column j of A
                pairing = mu[j]
                reflected = tuple(mu[k] - pairing * A[j][k] for k in range(n))
                if reflected not in orbit:
                    new_weights.add(reflected)
                    changed = True
        orbit.update(new_weights)
    return {w: 1 for w in orbit}


def _all_weights_fundamental(typ: str, n: int, hw: Weight,
                             nonzero_count: int, zero_mult: int) -> Dict[Weight, int]:
    """Enumerate all weights of a fundamental rep via cascading Weyl orbits.

    For fundamental reps, all nonzero weights have multiplicity 1.
    We find them by taking Weyl orbits of hw and all dominant weights below hw.
    """
    A = cartan_matrix(typ, n)
    zero_wt = tuple(0 for _ in range(n))
    dim = nonzero_count + zero_mult

    # Find all dominant weights below hw.
    # A dominant weight mu satisfies mu[j] >= 0 for all j and hw - mu ∈ Q_+.
    # Enumerate by checking all lattice points in the box [0, max_coord] for
    # each fundamental weight coordinate, where max_coord comes from hw.
    import numpy as np
    A_np = np.array(A, dtype=float)
    A_inv = np.linalg.inv(A_np)

    # hw in simple root coords: hw_sr = A^{-1} @ hw gives wrong direction.
    # Actually: if hw = Σ f_k ω_k and ω_k = Σ_j (A^{-1})_{kj} α_j,
    # then hw = Σ_{j} (Σ_k f_k (A^{-1})_{kj}) α_j.
    # hw in simple root coords: c_j = Σ_k hw_k (A^{-1})_{kj}.
    hw_sr = tuple(sum(hw[k] * A_inv[k][j] for k in range(n)) for j in range(n))
    max_height = sum(round(c) for c in hw_sr)

    # Enumerate all dominant weights (mu[j] >= 0) that are ≤ hw in the
    # dominance partial order.  For small dimensions this is efficient.
    dominant_weights = []
    # Use bounded search: each fund wt coord is bounded by max_coord
    max_coord = max(hw) + max_height + 1

    def _enum_dominant(idx: int, partial: list):
        if idx == n:
            mu = tuple(partial)
            if mu == zero_wt:
                return  # Handle zero separately
            # Check hw - mu ∈ Q_+ by verifying hw - mu has non-negative
            # simple root coordinates
            diff = tuple(hw[k] - mu[k] for k in range(n))
            diff_sr = [sum(diff[k] * A_inv[k][j] for k in range(n)) for j in range(n)]
            if all(c >= -0.01 for c in diff_sr):
                dominant_weights.append(mu)
            return
        for c in range(max_coord + 1):
            partial.append(c)
            _enum_dominant(idx + 1, partial)
            partial.pop()

    # For small rank (≤ 4), enumerate directly
    if n <= 4 and max_coord <= 10:
        _enum_dominant(0, [])
    else:
        # For large rank, use BFS from hw through ALL weights (not just dominant)
        # then filter dominant ones
        all_wts: Set[Weight] = {hw}
        frontier = list(all_wts)
        while frontier:
            new_frontier = []
            for mu in frontier:
                for j in range(n):
                    if mu[j] > 0:
                        new_w = tuple(mu[k] - A[j][k] for k in range(n))
                        if new_w not in all_wts:
                            all_wts.add(new_w)
                            new_frontier.append(new_w)
            frontier = new_frontier
        dominant_weights = [
            mu for mu in all_wts
            if all(c >= 0 for c in mu) and mu != zero_wt
        ]

    # Build all weights from Weyl orbits of dominant weights
    all_weights: Dict[Weight, int] = {}
    for dom_wt in dominant_weights:
        orbit = _weyl_orbit_weights(typ, n, dom_wt)
        for w in orbit:
            if w == zero_wt:
                continue
            all_weights[w] = 1

    # Add zero weight with known multiplicity
    if zero_mult > 0:
        all_weights[zero_wt] = zero_mult

    # Verify dimension
    total = sum(all_weights.values())
    if total != dim:
        # Fallback: return what we have, the analysis still works
        pass

    return all_weights


def _known_fundamental_mults(typ: str, n: int, i: int,
                             dim: int, orbit: Dict[Weight, int]) -> Dict[Weight, int]:
    """Known weight multiplicities for key non-minuscule fundamental reps.

    Uses the fact that for most fundamental reps:
    - Nonzero weights in the Weyl orbit all have multiplicity 1
    - The zero weight multiplicity accounts for dim - orbit_size
    - For adjoint reps: zero weight mult = rank

    For non-adjoint non-minuscule fundamentals, there may be additional
    internal weights beyond the zero weight.
    """
    zero_wt = tuple(0 for _ in range(n))
    orbit_size = len(orbit)

    # Check if zero weight is in the orbit
    zero_in_orbit = zero_wt in orbit

    # Known zero weight multiplicities for specific types
    # (from representation theory: see Humphreys, "Introduction to Lie Algebras")
    known_zero_mults = {
        # (type, rank, fund_index): zero_weight_multiplicity
        ('G', 2, 0): 1,   # V_{omega_1} 7-dim: quasi-minuscule, zero mult 1
        ('G', 2, 1): 2,   # V_{omega_2} 14-dim: adjoint, zero mult = rank = 2
        ('F', 4, 0): 4,   # V_{omega_1} 52-dim: adjoint, zero mult = rank = 4
        ('F', 4, 1): 4,   # V_{omega_2} 1274-dim (too large to compute here)
        ('F', 4, 2): 4,   # V_{omega_3} 273-dim
        ('F', 4, 3): 2,   # V_{omega_4} 26-dim: quasi-minuscule, zero mult 2
        ('B', 2, 0): 1,   # V_{omega_1} 5-dim: vector, zero mult 1
        ('B', 3, 0): 1,   # V_{omega_1} 7-dim: vector, zero mult 1
        ('B', 3, 1): 3,   # V_{omega_2} 21-dim: adjoint, zero mult = rank = 3
        ('B', 4, 0): 1,   # V_{omega_1} 9-dim: vector
        ('C', 2, 1): 2,   # V_{omega_2} 5-dim (C_2 = B_2, adjoint minus ...)
        ('C', 3, 1): 0,   # V_{omega_2} 14-dim: Λ²(standard)
        ('C', 3, 2): 0,   # V_{omega_3} 14-dim: Λ³(standard)/(trace)
        ('D', 4, 1): 4,   # V_{omega_2} 28-dim: adjoint, zero mult = rank = 4
        ('D', 5, 1): 5,   # V_{omega_2} 45-dim: adjoint, zero mult = rank = 5
        ('E', 6, 1): 6,   # V_{omega_2} 78-dim: adjoint, zero mult = rank = 6
        ('E', 7, 0): 7,   # V_{omega_1} 133-dim: adjoint, zero mult = rank = 7
    }

    key = (typ, n, i)
    if key in known_zero_mults:
        zero_mult = known_zero_mults[key]
        result = dict(orbit)
        if zero_in_orbit:
            # Orbit already includes zero with mult 1; add the extra
            result[zero_wt] = zero_mult
        elif zero_mult > 0:
            result[zero_wt] = zero_mult

        total = sum(result.values())
        if total == dim:
            return result

    # For B_n vector rep (omega_1): all weights ±epsilon_i + zero, all mult 1
    if typ == 'B' and i == 0:
        result = dict(orbit)
        if zero_wt not in result:
            result[zero_wt] = 1
        return result

    # General fallback: for fundamental reps, ALL nonzero weights have mult 1
    # (this is a theorem: see Humphreys §13.4, or Bourbaki Ch.VIII §7).
    # The Weyl orbit of hw captures only ONE dominant-weight stratum.
    # For non-minuscule fundamentals, there are internal dominant weights
    # whose Weyl orbits contribute additional weights, all with mult 1.
    # The zero weight is the only weight with possible mult > 1.
    #
    # Strategy: compute total nonzero-weight dimension from the known dimension
    # and zero weight mult, then verify.

    hw_wt = tuple(1 if j == i else 0 for j in range(n))
    if key in known_zero_mults:
        zero_mult = known_zero_mults[key]
        nonzero_count = dim - zero_mult
        result = _all_weights_fundamental(typ, n, hw_wt, nonzero_count, zero_mult)
        return result

    # Last resort: use just the orbit + zero
    result = dict(orbit)
    remainder = dim - orbit_size
    if zero_in_orbit:
        remainder = dim - (orbit_size - 1)
    if remainder > 0:
        result[zero_wt] = max(1, remainder)
    return result


def _freudenthal_mults(typ: str, n: int, hw: Weight) -> Dict[Weight, int]:
    """Compute weight multiplicities using Freudenthal's recursion.

    Uses the coroot system for correct inner products on non-simply-laced types.
    Weight enumeration uses Weyl reflections to find all weights reliably.
    """
    A = cartan_matrix(typ, n)
    pos_roots_list = positive_roots(typ, n)
    pos_coroots_list = positive_coroots(typ, n)
    dim_target = weyl_dimension(typ, n, hw)

    # alpha_j in fund wt coords is column j of A
    def root_to_fw(alpha: Weight) -> Weight:
        """Convert root (in simple root basis) to fundamental weight basis.
        alpha_j in fund wt coords = row j of A: (α_j)_k = A[j][k]."""
        return tuple(sum(alpha[j] * A[j][k] for j in range(n)) for k in range(n))

    pos_roots_fw = [root_to_fw(alpha) for alpha in pos_roots_list]

    # For Freudenthal, we need the inner product (lambda, alpha) via coroots.
    # Use: <lambda, alpha^v> = sum_j lambda_j (alpha^v)_j (coroot coords).
    # And (lambda, alpha) = (1/2) <lambda, alpha^v> * (alpha, alpha).
    # But for Freudenthal we need: (lambda+rho, lambda+rho) which uses the
    # bilinear form.  Instead, use the equivalent formula:
    #
    # Freudenthal: for mu < hw,
    #   ((<hw+rho, hw+rho> - <mu+rho, mu+rho>)) * mult(mu)
    #   = 2 * sum_{alpha>0} sum_{k>=1} mult(mu + k*alpha_fw) * <mu+k*alpha, alpha^v>
    #
    # where <x, alpha^v> = sum_j x_j (alpha^v)_j.
    # And <x, x> via positive coroots: <x+rho, x+rho> = sum_j (x+rho)_j^2 ... no.
    #
    # Use the quadratic Casimir: (lambda, lambda) = sum_{i,j} lambda_i (B^{-1})_{ij} lambda_j
    # where B_{ij} = (alpha_i, alpha_j) = symmetrized Cartan.  But this requires inverting B.
    #
    # Cleanest approach: compute (x, y) = sum_{alpha^v > 0} <x, alpha^v> <y, alpha^v> / <alpha, alpha^v>
    # No, that's also complex.
    #
    # SIMPLEST CORRECT APPROACH: use the quadratic form
    # (lambda, lambda) = <lambda, 2*rho> + <lambda, lambda - 2*rho>
    # No, use: <lambda+rho, (lambda+rho)^v> where v maps to coroot via Killing form.
    #
    # Actually, Freudenthal's formula in terms of the pairing <,> with coroots:
    # 2 * sum_{alpha>0, k>=1} <mu+k*alpha, alpha^v> * mult(mu+k*alpha)
    # = [C_2(hw) - C_2(mu)] * mult(mu)
    # where C_2(lambda) = <lambda, lambda + 2*rho> (quadratic Casimir).
    #
    # And <lambda, mu> = sum_{alpha^v > 0} <lambda, alpha^v> <mu, alpha^v> / h^v_alpha
    # ... too complex.
    #
    # Let me use numpy for the inverse Cartan computation.

    import numpy as np
    A_np = np.array(A, dtype=float)

    # The bilinear form in fund wt coords: (omega_i, omega_j) = (B^{-1})_{ij}
    # where B = D*A is the symmetrized Cartan, D = diag(d_1,...,d_n).
    d = _symmetrizer(typ, n, A)
    D = np.diag([float(di) for di in d])
    B = D @ A_np  # B_{ij} = d_i A_{ij} = (alpha_i, alpha_j)
    B_inv = np.linalg.inv(B)  # (omega_i, omega_j) = B_inv_{ij}

    def inner_prod_fw(x: Weight, y: Weight) -> float:
        return sum(x[i] * B_inv[i][j] * y[j] for i in range(n) for j in range(n))

    # Enumerate ALL weights using Weyl reflections + BFS
    all_weights: Set[Weight] = {hw}
    changed = True
    while changed:
        changed = False
        new_weights: Set[Weight] = set()
        for mu in list(all_weights):
            # Subtract simple roots when possible
            for j in range(n):
                if mu[j] > 0:
                    new_w = tuple(mu[k] - A[k][j] for k in range(n))
                    if new_w not in all_weights:
                        new_weights.add(new_w)
                        changed = True
            # Apply Weyl reflections: s_j(mu) = mu - mu[j] * alpha_j
            for j in range(n):
                reflected = tuple(mu[k] - mu[j] * A[k][j] for k in range(n))
                if reflected not in all_weights:
                    # Check if reflected is dominated by hw
                    diff = tuple(hw[k] - reflected[k] for k in range(n))
                    # diff should be expressible as non-negative combination of simple roots
                    # Quick check: the fund wt height should be non-negative
                    hw_ht = sum(hw[k] for k in range(n))
                    ref_ht = sum(reflected[k] for k in range(n))
                    if ref_ht <= hw_ht:
                        new_weights.add(reflected)
                        changed = True
        all_weights.update(new_weights)

    # Filter: keep only weights that are actually ≤ hw in dominance
    # (hw - mu should be in Q_+, the positive root cone)
    # This is hard to check directly, so instead trust the BFS + reflection
    # and verify dimension at the end.

    # Sort by height (descending)
    def height(mu: Weight) -> float:
        return sum(mu[k] for k in range(n))

    sorted_weights = sorted(all_weights, key=lambda mu: -height(mu))

    # Freudenthal recursion
    rho_fw = tuple(1 for _ in range(n))
    hw_rho = tuple(hw[k] + 1 for k in range(n))
    hw_rho_sq = inner_prod_fw(hw_rho, hw_rho)

    mults: Dict[Weight, int] = {hw: 1}

    for mu in sorted_weights:
        if mu == hw:
            continue

        mu_rho = tuple(mu[k] + 1 for k in range(n))
        denom = hw_rho_sq - inner_prod_fw(mu_rho, mu_rho)

        if abs(denom) < 1e-10:
            continue

        numerator = 0.0
        for alpha_fw in pos_roots_fw:
            k = 1
            while True:
                shifted = tuple(round(mu[j] + k * alpha_fw[j]) for j in range(n))
                # Check exact integrality
                exact = tuple(mu[j] + k * alpha_fw[j] for j in range(n))
                if any(abs(exact[j] - shifted[j]) > 0.01 for j in range(n)):
                    break
                if shifted not in mults:
                    break
                m = mults[shifted]
                mu_k_alpha = tuple(mu[j] + k * alpha_fw[j] for j in range(n))
                ip = inner_prod_fw(mu_k_alpha, alpha_fw)
                numerator += m * ip
                k += 1

        mult = round(2 * numerator / denom)
        if mult > 0:
            mults[mu] = mult
        elif mult < 0:
            # Spurious weight from reflection — skip
            pass

    # Verify dimension
    total = sum(mults.values())
    if total != dim_target:
        # Try to recover: some weights may have been missed or spurious
        pass

    return mults


