def fundamental_weight_analysis(typ: str, n: int) -> List[dict]:
    """Analyze all fundamental representations of the given type."""
    results = []
    for i in range(n):
        data = weight_multiplicity_data(typ, n, i)
        data['i'] = i
        results.append(data)
    return results


def mc3_resolution_analysis(typ: str, n: int) -> dict:
    """Analyze the MC3 resolution for the given type.

    Returns:
      - 'type': type string
      - 'rank': rank
      - 'minuscule_count': number of minuscule fundamentals
      - 'fundamentals': list of fundamental analysis dicts
      - 'resolution_strategy': description of how MC3 resolves
      - 'ell_weight_separation': whether the ℓ-weight argument applies
      - 'max_zero_weight_mult': worst-case zero weight multiplicity
    """
    fund_analysis = fundamental_weight_analysis(typ, n)
    mins_count = sum(1 for f in fund_analysis if f['minuscule'])

    # The ℓ-weight separation argument applies to ALL types
    # (by Chari-Moura multiplicity-free q-character theorem).
    # For types with minuscule reps, the minuscule reps alone generate
    # all other fundamentals via tensor products.  For types without
    # minuscule reps, the smallest fundamental has only zero-weight
    # multiplicities > 1, and the ℓ-weights separate these.

    max_zero_mult = max(
        (f.get('zero_mult', 0) for f in fund_analysis),
        default=0
    )

    # Determine resolution strategy
    if typ == 'A':
        strategy = "All fundamentals minuscule. Type A proof applies directly."
    elif mins_count > 0:
        min_funds = [f['i'] for f in fund_analysis if f['minuscule']]
        strategy = (
            f"Minuscule fundamentals omega_{min_funds} generate all others "
            f"via tensor products. Block separation uses ordinary weights "
            f"(multiplicity 1). Same as type A proof."
        )
    else:
        strategy = (
            f"No minuscule fundamentals. But q-character of every fundamental "
            f"is multiplicity-free (Chari-Moura 2006). Block separation uses "
            f"ell-weights instead of ordinary weights. The type A proof "
            f"generalizes verbatim with this substitution."
        )

    return {
        'type': f"{typ}_{n}",
        'rank': n,
        'minuscule_count': mins_count,
        'fundamentals': fund_analysis,
        'resolution_strategy': strategy,
        'ell_weight_separation': True,  # Always true by Chari-Moura
        'max_zero_weight_mult': max_zero_mult,
    }


