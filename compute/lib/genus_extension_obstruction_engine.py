def is_energy_bounded(family: str, **kwargs) -> Optional[bool]:
    r"""Energy boundedness check.

    A VOA V is energy-bounded if for every v in V, Y(v,z) satisfies
    polynomial energy bounds with respect to L_0.

    Energy-bounded families:
      - All unitary VOAs (automatic by unitarity)
      - Affine KM at non-negative integer level (unitary)
      - Virasoro with c >= 1 and h >= 0, or minimal models (unitary ones)
      - Lattice VOAs from positive-definite lattices (unitary)
      - Heisenberg (unitary for k > 0)

    Non-unitary but energy-bounded:
      - Some non-unitary minimal models (case-by-case)

    Not energy-bounded:
      - Certain VOAs at negative level
      - Non-standard extensions
    """
    unitary = kwargs.get("unitary", None)
    level = kwargs.get("level", None)

    if unitary is True:
        return True

    if family == "Heisenberg":
        # Unitary for k > 0 (positive-definite inner product)
        if level is not None:
            return bool(Rational(level) > 0)
        return True  # Default assumption: positive level

    if family in ("sl2", "sl3", "affine_KM"):
        if level is not None and isinstance(level, int) and level >= 0:
            return True  # Integrable: unitary
        if level is not None and bool(Rational(level) < 0):
            return False  # Negative level: not unitary, not energy-bounded in general
        return True  # Generic positive level

    if family == "Virasoro":
        c_val = kwargs.get("c", None)
        if c_val is not None:
            c_rat = Rational(c_val)
            if bool(c_rat >= 1):
                return True  # c >= 1 with h >= 0 is unitary
            if bool(c_rat >= 0):
                return True  # Minimal models are energy-bounded
        return True  # Generic assumption

    if family == "betagamma":
        # The bc/betagamma system has c = 2 but is NOT unitary
        # (beta has weight 0, indefinite inner product).
        # Energy boundedness: TRUE (polynomial growth of vertex operators)
        return True

    if family == "lattice":
        return True  # Lattice VOAs are unitary

    if family == "minimal_model":
        return True  # Unitary minimal models are energy-bounded

    return None


