def is_rational(family: str, **kwargs) -> Optional[bool]:
    r"""Rationality check for standard families.

    Rational = every admissible module is completely reducible.

    Rational standard families:
      - L_k(g) at positive integer level k (integrable reps)
      - Minimal models M(p,q)
      - Lattice VOAs V_Lambda (Lambda even, positive-definite)

    Non-rational but C_2-cofinite:
      - Universal affine V_k(g) at generic k (uncountably many simples)
      - Virasoro Vir_c at generic c
      - W^k(g) at generic level
      - Triplet algebras W(p) [Adamovic-Milas]

    The distinction matters: non-rational C_2-cofinite theories have
    LOGARITHMIC conformal blocks (non-semisimple monodromy), but still
    extend to all genera by Gui-Zhang [2024-2025].
    """
    level = kwargs.get("level", None)
    c_val = kwargs.get("c", None)

    if family == "lattice":
        return True

    if family == "minimal_model":
        return True

    if family in ("sl2", "sl3", "affine_KM"):
        if level is not None and isinstance(level, int) and level > 0:
            return True  # Integrable level
        return False  # Generic level: not rational

    if family == "Virasoro":
        # Rational iff c is a minimal model value c_{p,q}
        if c_val is not None:
            return _is_minimal_model_c(c_val)
        return False  # Generic c: not rational

    if family in ("Heisenberg", "betagamma"):
        # Heisenberg is not rational (uncountably many Fock modules)
        return False

    if family.startswith("W_"):
        if level is not None and isinstance(level, int) and level > 0:
            return True  # Integrable
        return False

    return None


def _is_minimal_model_c(c_val) -> bool:
    """Check if c is a minimal model central charge c_{p,q} = 1 - 6(p-q)^2/(pq).

    Only checks small p,q for exact rational match.
    """
    c_rat = Rational(c_val) if not isinstance(c_val, type(Rational(0))) else c_val
    for p in range(2, 20):
        for q in range(p + 1, 20):
            from math import gcd
            if gcd(p, q) != 1:
                continue
            c_pq = 1 - Rational(6 * (p - q) ** 2, p * q)
            if c_rat == c_pq:
                return True
    return False


def verlinde_dim(family: str, genus: int, **kwargs) -> Optional[int]:
    r"""Dimension of conformal block space at genus g for rational theories.

    For sl_2 at level k, genus g:
        dim V_g = ((k+2)/2)^{g-1} * sum_{j=0}^{k} sin(pi*(j+1)/(k+2))^{2-2g}

    This is ALWAYS a positive integer (Beauville's theorem / Faltings).
    """
    if family == "sl2":
        level = kwargs.get("level", 1)
        return _verlinde_sl2(level, genus)
    return None


def _verlinde_sl2(k: int, g: int) -> int:
    """Verlinde formula for sl_2 at level k, genus g.

    Uses exact computation via the S-matrix.
    """
    if g == 0:
        return 1
    if g == 1:
        return k + 1  # Number of integrable representations

    # General formula: sum_{j=0}^{k} (S_{0j}/S_{00})^{2-2g}
    # S_{0j} = sqrt(2/(k+2)) * sin(pi*(j+1)/(k+2))
    # S_{0j}/S_{00} = sin(pi*(j+1)/(k+2)) / sin(pi/(k+2))
    # dim = sum_{j=0}^{k} (sin(pi*(j+1)/(k+2)) / sin(pi/(k+2)))^{2-2g}
    # This is always an integer for g >= 0.

    # For small k, compute directly
    from sympy import sin, pi as PI, nsimplify
    total = Rational(0)
    for j in range(k + 1):
        ratio = sin(PI * (j + 1) / (k + 2)) / sin(PI / (k + 2))
        # ratio^{2-2g} -- for g >= 2 this is ratio^{negative}
        power = 2 - 2 * g
        term = ratio ** power
        total += term
    # The result should be a positive integer
    result = simplify(total)
    try:
        return int(result)
    except (TypeError, ValueError):
        # Numerical fallback
        val = complex(result).real
        return int(round(val))


