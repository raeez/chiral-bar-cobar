_SHADOW_CLASS_TABLE = {
    "Heisenberg": ("G", 2),       # Gaussian, terminates at arity 2
    "sl2": ("L", 3),              # Lie/tree, terminates at arity 3
    "sl3": ("L", 3),              # Lie/tree
    "affine_KM": ("L", 3),       # Lie/tree for all affine KM
    "g2": ("L", 3),
    "b2": ("L", 3),
    "betagamma": ("C", 4),        # Contact/quartic, terminates at arity 4
    "Virasoro": ("M", None),      # Mixed, infinite tower (r_max = infinity)
    "W_3": ("M", None),           # Mixed, infinite tower
    "w3": ("M", None),
    "W_algebra": ("M", None),     # Mixed for non-quadratic W-algebras
    "lattice": ("G", 2),          # Lattice VOAs = Heisenberg + lattice part; class G
    "minimal_model": ("M", None), # Virasoro quotient: class M
}


def shadow_class(family: str) -> Tuple[Optional[str], Optional[int]]:
    """Return (shadow_class, shadow_depth) for a given family.

    Shadow classes:
      G (Gaussian):      r_max = 2.  Tower terminates at quadratic.
      L (Lie/tree):      r_max = 3.  Tower terminates at cubic.
      C (contact/quartic): r_max = 4. Tower terminates at quartic.
      M (mixed):         r_max = infinity. Infinite tower.

    Shadow depth does NOT characterize Koszulness (AP14).
    All standard families are Koszul; depth classifies complexity.
    """
    if family in _SHADOW_CLASS_TABLE:
        return _SHADOW_CLASS_TABLE[family]
    return (None, None)


def shadow_discriminant(family: str, **kwargs):
    r"""Critical discriminant Delta = 8*kappa*S_4 classifying shadow depth.

    Delta = 0 <==> tower terminates (classes G, L).
    Delta != 0 <==> tower is infinite (class M).
    Class C escapes via stratum separation.

    For standard families:
      Heisenberg: S_4 = 0, so Delta = 0. Class G.
      Affine KM: S_4 = 0 (quadratic OPE), so Delta = 0. Class L.
      betagamma: S_4 != 0 but stratum separation kills it. Class C.
      Virasoro: S_4 = -10/(c(5c+22)), Delta = -80/(c(5c+22)) * kappa. Class M.
      W_3: S_4 != 0, Delta != 0. Class M.
    """
    kappa_val = _get_kappa(family, **kwargs)
    if kappa_val is None:
        return None

    c_val = kwargs.get("c", None)

    if family == "Heisenberg":
        return Rational(0)  # S_4 = 0

    if family in ("sl2", "sl3", "affine_KM", "g2", "b2"):
        return Rational(0)  # Quadratic OPE: S_4 = 0

    if family == "betagamma":
        # Nonzero S_4 but stratum separation => effective Delta = 0
        return Rational(0)

    if family == "Virasoro":
        if c_val is not None:
            c = Rational(c_val)
            if c == 0 or (5 * c + 22) == 0:
                return None  # Singular
            S4 = Rational(-10) / (c * (5 * c + 22))
            return 8 * kappa_val * S4
        return None

    return None


