def genus_g_obstruction_class(family: str, genus: int, **kwargs) -> ObstructionClass:
    r"""Compute the genus-g obstruction class for a given family.

    The genus-g obstruction is:
        obs_g(A) = kappa(A) * lambda_g^{FP}

    where lambda_g^{FP} = (2^{2g-1}-1)/2^{2g-1} * |B_{2g}|/(2g)!.

    This is the SCALAR projection of the shadow obstruction tower at genus g.
    It is nonzero whenever kappa(A) != 0, but it does NOT obstruct genus
    extension --- it is a CURVATURE, not an obstruction to existence.

    The actual obstruction to genus extension is cohomological:
    the curvature is exact in the relevant complex (killed by the
    period correction), so the bar complex extends to all genera
    with D^2 = 0 after the correction F_g = kappa * lambda_g.
    """
    kappa_val = _get_kappa(family, **kwargs)
    if kappa_val is None:
        return ObstructionClass(
            name=f"obs_{genus}",
            layer=4,
            description="kappa unknown",
            vanishes=None,
            genus=genus,
        )

    if genus < 1:
        return ObstructionClass(
            name="obs_0",
            layer=4,
            description="No obstruction at genus 0 (ground field)",
            vanishes=True,
            value=Rational(0),
            genus=0,
        )

    obs_val = F_g(kappa_val, genus)
    vanishes = (simplify(obs_val) == 0)

    return ObstructionClass(
        name=f"obs_{genus}",
        layer=4,
        description=f"F_{genus} = kappa * lambda_{genus}^FP = {obs_val}",
        vanishes=vanishes,
        value=obs_val,
        genus=genus,
    )


def _get_kappa(family: str, **kwargs):
    """Retrieve kappa for a given family. Returns sympy expression or None."""
    level = kwargs.get("level", None)
    c_val = kwargs.get("c", None)
    k = kwargs.get("k", level)

    if family == "Heisenberg":
        return kappa_heisenberg(k)
    if family == "Virasoro":
        return kappa_virasoro(c_val)
    if family in ("sl2", "affine_KM_sl2"):
        return kappa_sl2(k)
    if family in ("sl3", "affine_KM_sl3"):
        return kappa_sl3(k)
    if family == "g2":
        return kappa_g2(k)
    if family in ("b2", "so5"):
        return kappa_b2(k)
    if family in ("W_3", "w3"):
        return kappa_w3(c_val)
    if family == "betagamma":
        return Rational(1)  # kappa(betagamma) = 1, c = 2
    if family == "lattice":
        rank = kwargs.get("rank", 1)
        # For lattice VOA of rank r: kappa = rank (Heisenberg subalgebra)
        return Rational(rank)
    return None


def curvature_is_exact(family: str, **kwargs) -> bool:
    r"""Check whether the curvature (obstruction class) is exact.

    For ALL standard modular Koszul families, the curvature obs_g = kappa * lambda_g
    is exact in H^2(Def_cyc^mod(A)).  This means the genus extension exists
    even though the curvature is nonzero.

    The exactness is a consequence of:
      (1) PBW concentration (MC1): the bar spectral sequence collapses
      (2) Algebraic family rigidity (thm:algebraic-family-rigidity):
          the tangent space to the deformation complex is 1-dimensional
          in the level direction
      (3) The period correction absorbs the curvature

    Result: D^2 = 0 AFTER the period correction, at ALL genera.
    """
    standard_families = {
        "Heisenberg", "affine_KM", "Virasoro", "W_algebra",
        "betagamma", "lattice", "minimal_model",
        "sl2", "sl3", "g2", "b2", "so5", "W_3", "w3",
    }
    if family in standard_families:
        return True
    return False  # Unknown families: cannot guarantee exactness


