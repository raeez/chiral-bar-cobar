def c2_quotient_dim_heisenberg(rank: int = 1) -> int:
    r"""Dimension of H_k / C_2(H_k) for Heisenberg of rank r.

    The Heisenberg VOA H_k of rank r is generated by r fields a^i of weight 1.
    C_2(H_k) contains a^i_{(-2)}|0> and all products of depth >= 2.
    V/C_2(V) = C[a^1, ..., a^r] (polynomial ring in r variables, but
    the quotient is the weight-0 and weight-1 piece only in the filtered sense).

    For the rank-1 Heisenberg: V/C_2(V) = C[a] where a = a_{(-1)}|0>.
    dim V/C_2(V) = infinity as a graded space, BUT V/C_2(V) is finitely
    generated as a commutative algebra (generated by a single element a).

    C_2-cofiniteness for Heisenberg: TRUE (V/C_2(V) = C[a^1,...,a^r],
    finitely generated).

    We return the number of generators of V/C_2(V) as an algebra.
    """
    return rank


def c2_quotient_generators_affine_km(type_: str, rank: int) -> int:
    r"""Number of generators of V_k(g)/C_2(V_k(g)) as a commutative algebra.

    For the universal affine VOA V_k(g):
        V/C_2(V) = Sym(g) (symmetric algebra on g)
    which is finitely generated with dim(g) generators.
    C_2-cofinite: TRUE.

    For the SIMPLE quotient L_k(g) at integrable level:
        V/C_2(V) is a finite-dimensional quotient of Sym(g).
    C_2-cofinite: TRUE (finite-dimensional quotient of f.g. algebra).
    """
    # dim(g) for simple Lie algebra of given type and rank
    dim_table = {
        ("A", 1): 3,   # sl_2
        ("A", 2): 8,   # sl_3
        ("A", 3): 15,  # sl_4
        ("A", 4): 24,  # sl_5
        ("B", 2): 10,  # so_5
        ("B", 3): 21,  # so_7
        ("C", 2): 10,  # sp_4
        ("C", 3): 21,  # sp_6
        ("D", 4): 28,  # so_8
        ("G", 2): 14,  # G_2
        ("F", 4): 52,  # F_4
        ("E", 6): 78,  # E_6
        ("E", 7): 133, # E_7
        ("E", 8): 248, # E_8
    }
    key = (type_, rank)
    if key in dim_table:
        return dim_table[key]
    # General formula for type A: dim(sl_{n+1}) = (n+1)^2 - 1
    if type_ == "A":
        return (rank + 1) ** 2 - 1
    raise ValueError(f"Unknown Lie type ({type_}, {rank})")


def c2_cofinite_virasoro(c=None) -> bool:
    r"""C_2-cofiniteness for Virasoro at central charge c.

    The universal Virasoro VOA Vir_c is C_2-cofinite for ALL c:
    V/C_2(V) = C[T] (polynomial ring in one variable T = L_{-2}|0>).
    This is 1-generated.

    The simple quotient L(c,0) is also C_2-cofinite:
      - At minimal model values c = c_{p,q}: finite-dimensional V/C_2(V).
      - At generic c: V/C_2(V) = C[T], same as universal.
    """
    return True


def c2_cofinite_w_algebra(type_: str, rank: int) -> bool:
    r"""C_2-cofiniteness for the universal W-algebra W^k(g).

    By Feigin-Frenkel: W^k(g) is freely generated for all k.
    V/C_2(V) = C[W_1, ..., W_r] (polynomial ring in rank generators).
    C_2-cofinite: TRUE (finitely generated).

    For SIMPLE quotients at admissible levels: also C_2-cofinite
    (Arakawa's theorem for principal W-algebras at admissible levels).
    """
    return True


def c2_cofinite_betagamma() -> bool:
    r"""C_2-cofiniteness for the beta-gamma system.

    The bc (or beta-gamma) system has generators beta (weight 0) and
    gamma (weight 1).  V/C_2(V) = C[beta, gamma].
    C_2-cofinite: TRUE (2 generators).

    NOTE: beta has weight 0, which means the positive-energy condition
    is borderline.  The weight-0 piece is 2-dimensional: {|0>, beta_{(0)}|0>}.
    """
    return True


def c2_cofinite_lattice(rank: int) -> bool:
    r"""C_2-cofiniteness for lattice VOAs V_Lambda.

    For any positive-definite even lattice Lambda of rank r:
    V_Lambda is C_2-cofinite.  In fact V_Lambda is rational.
    V/C_2(V) = C[h^1, ..., h^r] (from the Heisenberg subalgebra)
    times a finite group algebra piece from the lattice.
    """
    return True


def is_c2_cofinite(family: str, **kwargs) -> bool:
    """Master C_2-cofiniteness checker.

    All standard families (Heisenberg, affine KM, Virasoro, W_N,
    beta-gamma, lattice VOAs) are C_2-cofinite.

    Known non-C_2-cofinite examples:
      - Free boson Fock modules (not VOAs themselves)
      - Certain non-finitely-generated vertex algebras
      - Heisenberg VOA extended by non-standard modules
    """
    standard_families = {
        "Heisenberg", "affine_KM", "Virasoro", "W_algebra",
        "betagamma", "lattice", "minimal_model",
        "sl2", "sl3", "sl4", "g2", "f4", "e6", "e7", "e8",
    }
    if family in standard_families:
        return True
    if family.startswith("W_"):
        return True
    return None  # Unknown


