def verify_kappa_shadow_consistency(family: str, **kwargs) -> Dict[str, object]:
    r"""Cross-verify kappa, shadow class, and genus extension.

    Three independent verifications:
    1. kappa from the defining formula
    2. Shadow class from the OPE structure
    3. Genus extension from the four-layer analysis

    Consistency checks:
      - kappa = 0 iff bar complex is uncurved at genus 0
      - Class G/L has Delta = 0; class M has Delta != 0
      - All C_2-cofinite standard families extend to all genera
    """
    kappa_val = _get_kappa(family, **kwargs)
    sc, sd = shadow_class(family)
    profile = genus_extension_profile(family, **kwargs)
    disc = shadow_discriminant(family, **kwargs)

    results = {
        "family": family,
        "kappa": kappa_val,
        "shadow_class": sc,
        "shadow_depth": sd,
        "discriminant": disc,
        "c2_cofinite": profile.c2_cofinite,
        "extends": profile.extends_to_all_genera,
    }

    # Check 1: kappa = 0 iff uncurved
    if kappa_val is not None:
        kappa_zero = (simplify(kappa_val) == 0)
        results["kappa_zero"] = kappa_zero
        # When kappa = 0, F_g = 0 for all g (no genus-g obstruction)
        if kappa_zero:
            results["all_F_g_zero"] = True
        else:
            results["F_1"] = F_g(kappa_val, 1)
            results["F_2"] = F_g(kappa_val, 2)

    # Check 2: discriminant consistency
    if disc is not None and sc is not None:
        disc_zero = (simplify(disc) == 0)
        if sc in ("G", "L"):
            results["disc_consistent"] = disc_zero
        elif sc == "M":
            results["disc_consistent"] = not disc_zero
        elif sc == "C":
            # Class C has nonzero S_4 but stratum separation
            results["disc_consistent"] = True  # By design

    # Check 3: all standard families extend
    results["standard_extends"] = profile.extends_to_all_genera

    return results


def full_landscape_verification(max_genus: int = 3) -> List[Dict]:
    r"""Verify genus extension for the full standard landscape.

    Returns a list of verification results for every standard family
    at canonical parameter values.
    """
    families = [
        ("Heisenberg", {"k": 1}),
        ("Heisenberg", {"k": 2}),
        ("Heisenberg", {"k": 24}),
        ("sl2", {"level": 1}),
        ("sl2", {"level": 2}),
        ("sl2", {"level": 10}),
        ("sl3", {"level": 1}),
        ("sl3", {"level": 3}),
        ("Virasoro", {"c": 1}),
        ("Virasoro", {"c": Rational(1, 2)}),   # Ising
        ("Virasoro", {"c": Rational(7, 10)}),   # Tricritical Ising
        ("Virasoro", {"c": 26}),
        ("betagamma", {}),
        ("W_3", {"c": 2}),
        ("lattice", {"rank": 1}),
        ("lattice", {"rank": 8}),
        ("lattice", {"rank": 24}),
    ]

    results = []
    for fam, params in families:
        r = verify_kappa_shadow_consistency(fam, **params)
        # Add genus-g obstruction values
        for g in range(1, max_genus + 1):
            obs = genus_g_obstruction_class(fam, g, **params)
            r[f"obs_{g}"] = obs.value
        results.append(r)

    return results


