def ope_growth_order(family: str, **kwargs) -> Optional[str]:
    r"""Growth rate of OPE coefficients for a given family.

    The HS-sewing condition requires polynomial OPE growth:
        ||a_{(n)}b|| <= C * (1 + |n|)^N * ||a|| * ||b||
    for some N (the OPE growth order).

    Standard families all have polynomial OPE growth:
      - Heisenberg: N = 1 (linear growth, a_{(1)}a = k)
      - Affine KM: N = 1 (J^a_{(1)}J^b = k*kappa^{ab})
      - Virasoro: N = 3 (T_{(3)}T = c/2, highest pole order 4 -> growth ~ n^3)
      - W_N: N = 2N-1 (W_{(2N-1)}W = c_N, highest pole 2N -> growth ~ n^{2N-1})
      - betagamma: N = 1
    """
    if family == "Heisenberg":
        return "polynomial_1"
    if family in ("sl2", "sl3", "affine_KM"):
        return "polynomial_1"
    if family == "Virasoro":
        return "polynomial_3"
    if family == "betagamma":
        return "polynomial_1"
    if family == "lattice":
        return "polynomial_1"
    if family == "minimal_model":
        return "polynomial_3"
    if family.startswith("W_"):
        N = kwargs.get("N", 3)
        return f"polynomial_{2*N - 1}"
    return None


def sector_growth_type(family: str, **kwargs) -> Optional[str]:
    r"""Growth type of graded dimensions dim V_n as n -> infinity.

    The HS-sewing condition requires subexponential sector growth:
        dim V_n = O(exp(C * n^alpha))  with alpha < 1.

    For standard families, the asymptotic is controlled by the
    Hardy-Ramanujan/Rademacher expansion:
      - Heisenberg rank r: dim V_n ~ exp(2*pi*sqrt(r*n/6)) / (polynomial)
        which is subexponential (alpha = 1/2).
      - Affine KM: similar asymptotics from Kac character formula.
      - Virasoro: dim V_n ~ exp(pi*sqrt(2cn/3)) (Cardy formula)
      - W_N: subexponential by same mechanism.

    Exponential growth (alpha = 1) would VIOLATE HS-sewing.
    No standard family has exponential growth.
    """
    if family in ("Heisenberg", "affine_KM", "sl2", "sl3",
                   "Virasoro", "betagamma", "lattice", "minimal_model"):
        return "subexponential_half"  # alpha = 1/2

    if family.startswith("W_"):
        return "subexponential_half"

    return None


def hs_sewing_holds(family: str, **kwargs) -> Optional[bool]:
    r"""Check whether the HS-sewing condition holds.

    By thm:general-hs-sewing: polynomial OPE growth + subexponential
    sector growth ==> HS-sewing convergence at all genera.

    This holds for the ENTIRE standard landscape.
    """
    ope = ope_growth_order(family, **kwargs)
    sector = sector_growth_type(family, **kwargs)

    if ope is not None and ope.startswith("polynomial"):
        if sector is not None and sector.startswith("subexponential"):
            return True

    return None


