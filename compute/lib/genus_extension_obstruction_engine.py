def genus_extension_profile(family: str, **kwargs) -> GenusExtensionData:
    """Compute the full genus-extension obstruction profile for a family.

    This is the master function that assembles all four obstruction layers
    plus the shadow tower data into a single profile.
    """
    level = kwargs.get("level", None)
    c_val = kwargs.get("c", None)
    k = kwargs.get("k", level)
    rank = kwargs.get("rank", 1)

    # Layer 1: C_2-cofiniteness
    c2 = is_c2_cofinite(family, **kwargs)

    # Layer 2: Rationality
    rat = is_rational(family, **kwargs)

    # Layer 3: Energy boundedness
    eb = is_energy_bounded(family, **kwargs)

    # Layer 4: Sewing convergence
    hs = hs_sewing_holds(family, **kwargs)

    # Kappa
    kappa_val = _get_kappa(family, **kwargs)

    # Shadow data
    sc, sd = shadow_class(family)

    # Central charge
    cc = _get_central_charge(family, **kwargs)

    # Genus extension: holds if C_2-cofinite + sewing convergence
    extends = None
    if c2 is True and hs is True:
        extends = True
    elif c2 is True:
        # C_2-cofinite alone guarantees finite-dim conformal blocks,
        # but full analytic extension also needs sewing convergence.
        # For standard families with polynomial OPE, this is automatic.
        extends = True

    return GenusExtensionData(
        name=_make_name(family, **kwargs),
        family=family,
        c2_cofinite=c2 if c2 is not None else False,
        c2_quotient_dim=None,
        associated_variety_dim=_associated_variety_dim(family),
        rational=rat,
        num_simple_modules=_num_simple_modules(family, **kwargs),
        energy_bounded=eb,
        unitary=_is_unitary(family, **kwargs),
        hs_sewing=hs,
        ope_growth_polynomial=(ope_growth_order(family, **kwargs) is not None),
        sector_growth_subexponential=(
            sector_growth_type(family, **kwargs) is not None
        ),
        extends_to_all_genera=extends,
        kappa=kappa_val,
        shadow_depth=sd,
        shadow_class=sc,
        central_charge=cc,
    )


def _make_name(family: str, **kwargs) -> str:
    """Human-readable name."""
    level = kwargs.get("level", kwargs.get("k", None))
    c_val = kwargs.get("c", None)
    rank = kwargs.get("rank", None)

    if family == "Heisenberg":
        if level is not None:
            return f"H_{level}"
        return "H_k"
    if family == "Virasoro":
        if c_val is not None:
            return f"Vir_{c_val}"
        return "Vir_c"
    if family in ("sl2", "sl3"):
        if level is not None:
            return f"{family}_level_{level}"
        return f"V_k({family})"
    if family == "betagamma":
        return "betagamma"
    if family == "lattice":
        if rank is not None:
            return f"V_Lambda (rank {rank})"
        return "V_Lambda"
    if family.startswith("W_"):
        if c_val is not None:
            return f"{family}_c={c_val}"
        return family
    return family


def _get_central_charge(family: str, **kwargs):
    """Central charge for a family."""
    c_val = kwargs.get("c", None)
    level = kwargs.get("level", kwargs.get("k", None))

    if c_val is not None:
        return Rational(c_val)

    if family == "Heisenberg":
        if level is not None:
            return Rational(level)  # c = k for rank 1
        return None
    if family == "betagamma":
        return Rational(2)
    if family == "sl2" and level is not None:
        k = Rational(level)
        h_dual = 2
        dim_g = 3
        return dim_g * k / (k + h_dual)  # Sugawara formula
    if family == "sl3" and level is not None:
        k = Rational(level)
        h_dual = 3
        dim_g = 8
        return dim_g * k / (k + h_dual)
    return None


def _associated_variety_dim(family: str) -> Optional[int]:
    """Dimension of the associated variety X_V = Specm(V/C_2(V)).

    For a C_2-cofinite VOA, X_V is a finite-type Poisson variety.
    dim X_V controls the "size" of the algebra.

    For rational VOAs: X_V = point (dim 0).
    For non-rational C_2-cofinite: dim X_V > 0.
    For lisse (dim X_V = 0): the theory is "quasi-lisse."
    """
    if family in ("minimal_model", "lattice"):
        return 0  # Rational => lisse
    if family == "Heisenberg":
        return 1  # C[a] has dim 1
    if family == "Virasoro":
        return 1  # C[T] has dim 1
    if family in ("sl2",):
        return 3  # Sym(sl_2) = C^3
    if family in ("sl3",):
        return 8  # Sym(sl_3)
    if family == "betagamma":
        return 2  # C[beta, gamma]
    return None


def _num_simple_modules(family: str, **kwargs) -> Optional[int]:
    """Number of simple modules (for rational theories)."""
    if family == "minimal_model":
        p = kwargs.get("p", 3)
        q = kwargs.get("q", 4)
        return (p - 1) * (q - 1) // 2
    if family in ("sl2",):
        level = kwargs.get("level", kwargs.get("k", None))
        if level is not None and isinstance(level, int) and level > 0:
            return level + 1  # k+1 integrable reps
    if family == "lattice":
        # |Lambda*/Lambda| simple modules
        return kwargs.get("discriminant", None)
    if family == "Heisenberg":
        return None  # Uncountably many Fock modules
    return None


def _is_unitary(family: str, **kwargs) -> Optional[bool]:
    """Unitarity check."""
    level = kwargs.get("level", kwargs.get("k", None))
    c_val = kwargs.get("c", None)

    if family == "Heisenberg":
        if level is not None:
            return bool(Rational(level) > 0)
        return True

    if family in ("sl2", "sl3", "affine_KM"):
        if level is not None:
            return bool(Rational(level) >= 0)
        return None

    if family == "Virasoro":
        if c_val is not None:
            c = Rational(c_val)
            if bool(c >= 1):
                return True
            # Unitary minimal models: c = 1 - 6/((m+2)(m+3)) for m >= 1
            return None
        return None

    if family == "betagamma":
        return False  # Indefinite inner product

    if family == "lattice":
        return True

    if family == "minimal_model":
        p = kwargs.get("p", None)
        q = kwargs.get("q", None)
        if p is not None and q is not None:
            return (q == p + 1)  # Unitary series: M(m+2, m+3)
        return None

    return None


