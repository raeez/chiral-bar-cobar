def _embed_12(M: np.ndarray, d: int) -> np.ndarray:
    """Embed operator on spaces 1,2 into spaces 1,2,3: M tensor I_d."""
    return np.kron(M, np.eye(d, dtype=complex))


def _embed_23(M: np.ndarray, d: int) -> np.ndarray:
    """Embed operator on spaces 2,3 into spaces 1,2,3: I_d tensor M."""
    return np.kron(np.eye(d, dtype=complex), M)


def verify_ybe_trig(q: complex, tol: float = 1e-10) -> Dict[str, Any]:
    r"""Verify the braid-form Yang-Baxter equation for the trigonometric R-matrix.

    R_{12} R_{23} R_{12} = R_{23} R_{12} R_{23}

    on V_{1/2}^{tensor 3} (8x8 matrices).
    This holds for ALL q, including roots of unity.
    """
    d = 2
    R = trig_r_matrix_fund(q)
    R12 = _embed_12(R, d)
    R23 = _embed_23(R, d)

    lhs = R12 @ R23 @ R12
    rhs = R23 @ R12 @ R23

    diff_norm = la.norm(lhs - rhs)
    scale = max(la.norm(lhs), la.norm(rhs), 1.0)

    return {
        'ybe_holds': diff_norm / scale < tol,
        'residual_norm': diff_norm,
        'relative_residual': diff_norm / scale,
        'q': q,
        'q_is_root_of_unity': _is_root_of_unity(q),
    }


def _is_root_of_unity(q: complex, max_order: int = 100) -> Optional[int]:
    """Check if q is a root of unity and return its order, or None."""
    for n in range(1, max_order + 1):
        if abs(q ** n - 1.0) < 1e-10:
            return n
    return None


