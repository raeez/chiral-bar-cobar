def quantum_integer(n: int, q: complex) -> complex:
    r"""Quantum integer [n]_q = (q^n - q^{-n}) / (q - q^{-1}).

    At q = 1: [n]_q -> n (L'Hopital).
    At q = root of unity q^p = 1: [p]_q = 0.
    """
    qi = 1.0 / q
    denom = q - qi
    if abs(denom) < 1e-14:
        return complex(n)
    return (q ** n - qi ** n) / denom


def quantum_factorial(n: int, q: complex) -> complex:
    r"""Quantum factorial [n]_q! = [1]_q [2]_q ... [n]_q."""
    result = complex(1.0)
    for i in range(1, n + 1):
        result *= quantum_integer(i, q)
    return result


def quantum_binomial(n: int, m: int, q: complex) -> complex:
    r"""Quantum binomial coefficient [n choose m]_q = [n]_q! / ([m]_q! [n-m]_q!).

    Well-defined at roots of unity when the denominator is nonzero.
    """
    if m < 0 or m > n:
        return complex(0.0)
    num = quantum_factorial(n, q)
    den = quantum_factorial(m, q) * quantum_factorial(n - m, q)
    if abs(den) < 1e-30:
        # At roots of unity, use the limit / polynomial formula
        return complex(0.0)
    return num / den


def quantum_dimension(j: float, q: complex) -> complex:
    r"""Quantum dimension dim_q(V_j) = [2j+1]_q.

    At generic q: positive real for small enough j.
    At q = e^{2*pi*i/(k+2)}: vanishes when 2j+1 = k+2, i.e. j = (k+1)/2.
    """
    n = int(round(2 * j + 1))
    return quantum_integer(n, q)


def q_from_level(k: int) -> complex:
    r"""Compute q = exp(2*pi*i / (k+2)) for sl_2 at WZW level k.

    The order of q is k+2 (= k + h^v for sl_2).
    """
    return np.exp(2j * math.pi / (k + 2))


