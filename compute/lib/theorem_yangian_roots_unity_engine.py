def permutation_operator(d: int) -> np.ndarray:
    r"""Permutation (swap) operator P on C^d tensor C^d."""
    P = np.zeros((d * d, d * d), dtype=complex)
    for i in range(d):
        for j in range(d):
            P[j * d + i, i * d + j] = 1.0
    return P


def trig_r_matrix_fund(q: complex) -> np.ndarray:
    r"""Trigonometric R-matrix for U_q(sl_2) on V_{1/2} tensor V_{1/2}.

    Jimbo convention (braid form):
      check{R} = q sum_{i} e_{ii} tensor e_{ii}
                + sum_{i != j} e_{ii} tensor e_{jj}
                + (q - q^{-1}) sum_{i < j} e_{ij} tensor e_{ji}

    In the basis |1,1>, |1,2>, |2,1>, |2,2> (= |++>, |+->, |-+>, |-->):

      R = [[q,    0,          0,    0],
           [0,    q - q^{-1}, 1,    0],
           [0,    1,          0,    0],
           [0,    0,          0,    q]]

    This satisfies the BRAID form of YBE:
      R_{12} R_{23} R_{12} = R_{23} R_{12} R_{23}
    (acting on V^{tensor 3}).

    Eigenvalues on V_{1/2} tensor V_{1/2} = V_1 + V_0:
      - On V_1 (symmetric, dim 3): eigenvalue q
      - On V_0 (antisymmetric, dim 1): eigenvalue -q^{-1}
    """
    qi = 1.0 / q
    lam = q - qi  # = q - q^{-1}

    R = np.zeros((4, 4), dtype=complex)
    # Diagonal: q on |11> and |22>; 0 on |12> and |21>
    R[0, 0] = q       # |11> -> |11>
    R[3, 3] = q       # |22> -> |22>
    # Off-diagonal block: |12>, |21>
    R[1, 1] = lam     # |12> -> |12> with weight q - q^{-1}
    R[1, 2] = 1.0     # |21> -> |12>
    R[2, 1] = 1.0     # |12> -> |21>
    R[2, 2] = 0.0     # |21> -> |21> with weight 0

    return R


def trig_r_matrix_fund_inverse(q: complex) -> np.ndarray:
    r"""Inverse of the trigonometric R-matrix.

    R^{-1} = R - (q - q^{-1}) I
    (since R satisfies R^2 = (q - q^{-1}) R + I, the Hecke relation).

    Equivalently: R^{-1} has eigenvalue q^{-1} on V_1 and -q on V_0.
    """
    qi = 1.0 / q
    lam = q - qi
    return trig_r_matrix_fund(q) - lam * np.eye(4, dtype=complex)


