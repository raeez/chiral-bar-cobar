def verify_hecke_relation(q: complex, tol: float = 1e-10) -> Dict[str, Any]:
    r"""Verify the Hecke algebra relation for the trigonometric R-matrix.

    R^2 = (q - q^{-1}) R + I

    Equivalently: (R - q I)(R + q^{-1} I) = 0.
    The minimal polynomial factors as (R - q)(R + q^{-1}) = 0,
    giving eigenvalues q (on V_1, dim 3) and -q^{-1} (on V_0, dim 1).
    """
    R = trig_r_matrix_fund(q)
    qi = 1.0 / q
    lam = q - qi
    I4 = np.eye(4, dtype=complex)

    # R^2 - (q - q^{-1}) R - I should vanish
    hecke_residual = R @ R - lam * R - I4

    # Factored form
    factor1 = R - q * I4
    factor2 = R + qi * I4
    product = factor1 @ factor2

    return {
        'hecke_holds': la.norm(hecke_residual) < tol,
        'factored_holds': la.norm(product) < tol,
        'residual_norm': la.norm(hecke_residual),
        'eigenvalue_sym': q,
        'eigenvalue_anti': -qi,
    }


