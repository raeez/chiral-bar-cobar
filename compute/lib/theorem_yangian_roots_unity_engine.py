def quantum_dimensions_at_level(k: int) -> Dict[str, Any]:
    r"""Compute quantum dimensions for all admissible representations at level k.

    Admissible: j = 0, 1/2, 1, ..., k/2.

    CONVENTION: The physical quantum dimension is computed with q_half = e^{pi*i/(k+2)}:
      dim_q(V_j) = [2j+1]_{q_half} = sin((2j+1)*pi/(k+2)) / sin(pi/(k+2))
    This is always REAL and POSITIVE for admissible j.

    Our engine's q_from_level returns q = e^{2*pi*i/(k+2)} (= q_half^2).
    The formula [n]_q = (q^n - q^{-n})/(q - q^{-1}) with THIS q gives
      sin(2*n*pi/(k+2))/sin(2*pi/(k+2)),
    which differs from the Weyl formula by the substitution pi -> 2*pi.
    We cross-check BOTH and report the Weyl formula as the authoritative dimension.

    Key identities:
      dim_q(V_0) = 1 (always)
      dim_q(V_{1/2}) = sin(2*pi/(k+2)) / sin(pi/(k+2)) = 2*cos(pi/(k+2))
      dim_q(V_{(k+1)/2}) = 0  (truncation point)

    The total quantum dimension squared: D^2 = sum_j dim_q(V_j)^2 = (k+2) / (2 sin^2(pi/(k+2)))
    """
    q = q_from_level(k)
    q_half = np.exp(1j * math.pi / (k + 2))
    spins = [i / 2.0 for i in range(k + 1)]
    dims = {}
    dim_sq_sum = 0.0

    for j in spins:
        # Path 1: [2j+1]_{q_half} via algebraic formula
        d_alg = quantum_dimension(j, q_half)
        # Path 2: Weyl character formula (trigonometric)
        n = int(round(2 * j + 1))
        sin_num = math.sin(math.pi * n / (k + 2))
        sin_den = math.sin(math.pi / (k + 2))
        d_weyl = sin_num / sin_den if abs(sin_den) > 1e-14 else float(n)
        dims[j] = {
            'q_dim': d_alg,
            'weyl_dim': d_weyl,
            'match': abs(d_alg.real - d_weyl) < 1e-8,
            'positive': d_weyl > -1e-10,
        }
        dim_sq_sum += d_weyl ** 2

    # Truncation: dim_q at j = (k+1)/2 should vanish
    j_trunc = (k + 1) / 2.0
    d_trunc = quantum_dimension(j_trunc, q_half)

    # Total quantum dimension squared
    D_sq_expected = (k + 2) / (2 * math.sin(math.pi / (k + 2)) ** 2)

    return {
        'level': k,
        'q': q,
        'spins': spins,
        'dimensions': dims,
        'truncation_dim': d_trunc,
        'truncation_vanishes': abs(d_trunc) < 1e-8,
        'total_dim_sq': dim_sq_sum,
        'total_dim_sq_expected': D_sq_expected,
        'total_dim_sq_match': abs(dim_sq_sum - D_sq_expected) < 1e-6,
        'all_weyl_match': all(d['match'] for d in dims.values()),
    }


