def verlinde_fusion_coefficient(j1: float, j2: float, J: float, k: int) -> int:
    r"""Verlinde fusion coefficient N_{j1, j2}^J at level k.

    For sl_2 at level k:
      N_{j1, j2}^J = 1 if:
        (a) |j1 - j2| <= J <= j1 + j2  (triangle inequality)
        (b) J <= k - j1 - j2            (Verlinde truncation)
        (c) j1 + j2 + J is an integer   (integrality)
        (d) 0 <= J <= k/2               (admissibility)
      N_{j1, j2}^J = 0 otherwise.
    """
    # All spins must be admissible
    j_max = k / 2.0
    if j1 < -1e-10 or j1 > j_max + 1e-10:
        return 0
    if j2 < -1e-10 or j2 > j_max + 1e-10:
        return 0
    if J < -1e-10 or J > j_max + 1e-10:
        return 0

    # Integrality
    if abs((j1 + j2 + J) - round(j1 + j2 + J)) > 1e-10:
        return 0

    # Triangle inequality
    if J < abs(j1 - j2) - 1e-10:
        return 0
    if J > j1 + j2 + 1e-10:
        return 0

    # Verlinde truncation
    if J > k - j1 - j2 + 1e-10:
        return 0

    return 1


def verlinde_fusion_rules(k: int) -> Dict[str, Any]:
    r"""Compute full Verlinde fusion rules at level k for sl_2.

    Returns the fusion matrix N_{j1,j2}^J for all admissible j1, j2, J.
    """
    j_max = k / 2.0
    spins = [i / 2.0 for i in range(k + 1)]  # 0, 1/2, 1, ..., k/2
    n_irreps = len(spins)

    # Fusion tensor N[j1][j2] = list of (J, N_coeff)
    fusion = {}
    for j1 in spins:
        for j2 in spins:
            channels = []
            for J in spins:
                coeff = verlinde_fusion_coefficient(j1, j2, J, k)
                if coeff > 0:
                    channels.append(J)
            fusion[(j1, j2)] = channels

    return {
        'level': k,
        'spins': spins,
        'num_irreps': n_irreps,
        'fusion': fusion,
        'j_max': j_max,
    }


def verify_verlinde_fusion(k: int) -> Dict[str, Any]:
    r"""Verify Verlinde fusion rules against the modular S-matrix.

    The Verlinde formula: N_{j1 j2}^J = sum_m S_{j1,m} S_{j2,m} S^*_{J,m} / S_{0,m}

    For sl_2 at level k:
      S_{j,j'} = sqrt(2/(k+2)) * sin(pi * (2j+1)(2j'+1) / (k+2))
    """
    q = q_from_level(k)
    j_max = k / 2.0
    spins = [i / 2.0 for i in range(k + 1)]
    n = len(spins)

    # Build the modular S-matrix
    S = np.zeros((n, n), dtype=complex)
    prefactor = math.sqrt(2.0 / (k + 2))
    for i, j1 in enumerate(spins):
        for j_idx, j2 in enumerate(spins):
            S[i, j_idx] = prefactor * math.sin(
                math.pi * (2 * j1 + 1) * (2 * j2 + 1) / (k + 2)
            )

    # Verify unitarity: S S^dagger = I
    SS_dag = S @ S.conj().T
    unitarity_residual = la.norm(SS_dag - np.eye(n, dtype=complex))

    # Verify symmetry: S = S^T
    symmetry_residual = la.norm(S - S.T)

    # Compute Verlinde formula: N_{ij}^k = sum_m S_{im} S_{jm} S*_{km} / S_{0m}
    N_verlinde = np.zeros((n, n, n), dtype=float)
    for i in range(n):
        for j_idx in range(n):
            for k_idx in range(n):
                val = 0.0
                for m in range(n):
                    if abs(S[0, m]) > 1e-14:
                        val += (S[i, m] * S[j_idx, m] * S[k_idx, m].conj()
                                / S[0, m]).real
                N_verlinde[i, j_idx, k_idx] = val

    # Compare with combinatorial fusion rules
    max_discrepancy = 0.0
    all_match = True
    for i, j1 in enumerate(spins):
        for j_idx, j2 in enumerate(spins):
            for k_idx, J in enumerate(spins):
                comb = verlinde_fusion_coefficient(j1, j2, J, k)
                verl = N_verlinde[i, j_idx, k_idx]
                disc = abs(verl - comb)
                if disc > max_discrepancy:
                    max_discrepancy = disc
                if disc > 0.1:  # generous tolerance for rounding
                    all_match = False

    return {
        'level': k,
        'S_matrix': S,
        'S_unitary': unitarity_residual < 1e-10,
        'S_symmetric': symmetry_residual < 1e-10,
        'verlinde_matches_combinatorial': all_match,
        'max_discrepancy': max_discrepancy,
        'num_irreps': n,
    }


