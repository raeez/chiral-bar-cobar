def verify_cartan_properties(ade_type, rank):
    """Verify standard properties of the Cartan matrix.

    1. C_{ii} = 2
    2. C_{ij} <= 0 for i != j
    3. det(C) > 0 (positive definite for finite type)
    4. C is symmetric (ADE are simply-laced)
    """
    C = cartan_matrix(ade_type, rank)
    results = {}

    # Diagonal entries = 2
    results['diagonal_2'] = all(C[i, i] == 2 for i in range(rank))

    # Off-diagonal <= 0
    results['off_diag_nonpositive'] = all(
        C[i, j] <= 0 for i in range(rank) for j in range(rank) if i != j)

    # Positive definite (all eigenvalues > 0)
    eigenvalues = np.linalg.eigvalsh(C.astype(float))
    results['positive_definite'] = all(ev > 1e-10 for ev in eigenvalues)
    results['min_eigenvalue'] = float(min(eigenvalues))

    # Symmetric
    results['symmetric'] = np.allclose(C, C.T)

    # Determinant
    results['determinant'] = int(round(np.linalg.det(C.astype(float))))

    return results


def verify_affine_cartan_properties(ade_type, rank):
    """Verify properties of the extended Cartan matrix.

    1. Positive semidefinite (corank 1)
    2. Has a null vector (the marks)
    3. All marks positive
    """
    C_aff = extended_cartan_matrix(ade_type, rank)
    marks = affine_marks(ade_type, rank)
    results = {}

    # Null vector check
    residual = C_aff @ marks
    results['null_vector'] = np.allclose(residual, 0, atol=1e-10)

    # All marks positive
    results['marks_positive'] = all(m > 0 for m in marks)

    # Corank 1
    eigenvalues = np.linalg.eigvalsh(C_aff.astype(float))
    n_zero = sum(1 for ev in eigenvalues if abs(ev) < 1e-10)
    results['corank_1'] = (n_zero == 1)

    # Positive semidefinite
    results['positive_semidefinite'] = all(ev >= -1e-10 for ev in eigenvalues)

    return results


def verify_mckay_dim(ade_type, rank):
    """Verify |Gamma| = sum a_i^2 (McKay correspondence dimension formula)."""
