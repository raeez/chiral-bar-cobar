def mckay_adjacency(ade_type, rank):
    """Return the adjacency matrix of the McKay quiver (extended Dynkin diagram).

    The McKay quiver for Gamma < SL(2) of ADE type has:
    - Vertices: irreducible representations rho_0 (trivial), rho_1, ..., rho_r
    - Edges: rho_i -- rho_j if rho_j appears in rho_nat tensor rho_i

    For ADE type this is exactly the EXTENDED Dynkin diagram (undirected).
    We return the adjacency matrix of the UNDIRECTED graph.

    Nodes indexed 0,...,r where 0 is the extending node (trivial rep).
    """
    C_aff = extended_cartan_matrix(ade_type, rank)
    # Adjacency = -(C_aff - 2I)/1 for simply-laced
    n = rank + 1
    adj = np.zeros((n, n), dtype=int)
    for i in range(n):
        for j in range(n):
            if i != j and C_aff[i, j] < 0:
                adj[i, j] = -C_aff[i, j]
    return adj


