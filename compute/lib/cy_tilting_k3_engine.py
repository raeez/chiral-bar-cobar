def intersection_matrix(ade_type, rank):
    """Return the intersection matrix E_i . E_j for exceptional divisors.

    E_i . E_j = -C_{ij} where C is the Cartan matrix.
    All self-intersections E_i^2 = -2.
    """
    return -cartan_matrix(ade_type, rank)


def euler_form_on_resolution(ade_type, rank):
    """Compute the Euler form chi(O(E_i), O(E_j)) on the minimal resolution.

    On the resolution of C^2/Gamma (non-compact surface):
        chi(O(D), O(D')) = sum_k (-1)^k dim Ext^k(O(D), O(D'))

    For line bundles on a smooth surface:
        chi(O(D), O(D')) = chi(O(D'-D))

    On the resolution (affine surface, H^2(O(D)) = 0 for effective D):
        chi(O(D'-D)) = 1 + (D'-D).(D'-D+K)/2
    but K = 0 (resolution of Kleinian singularity is crepant => K = pi^*(K_{C^2/Gamma}) + 0).
    Actually the resolution has K = 0 (trivial canonical) since Gamma < SL(2)
    and the resolution is crepant.

    For the AFFINE surface (non-compact), chi(O) = 1, and for divisors
    supported on the exceptional locus:
        chi(O(D)) = 1 + D.D/2     (by RR with K=0, chi(O)=1, and correction)

    Wait -- this needs care. On the non-compact resolution, H^0(O) = C,
    H^k(O) = 0 for k > 0 (affine variety), so chi(O) = 1.

    For a divisor D = sum n_i E_i supported on the exceptional locus:
        H^0(O(D)) depends on positivity
        H^2(O(D)) = 0 (affine surface)

    The Euler form between tilting summands:
        chi(O(E_i), O(E_j)) = chi(O(E_j - E_i))

    By Riemann-Roch on a smooth surface with K = 0:
        chi(O(D)) = chi(O_S) + D.D/2 = 1 + D.D/2   (Noether's formula part)

    But this is for COMPACT surfaces. For the non-compact resolution, we use
    the fact that the Euler form equals:
        chi(O(E_i), O(E_j)) = delta_{ij}     for i,j >= 1

    This is WRONG. Let me reconsider.

    The correct computation: on the minimal resolution of C^2/Gamma,
    the bounded derived category D^b(Coh(S_tilde)) is equivalent to
    D^b(mod-Pi(Q)) where Pi(Q) is the preprojective algebra.

    The Euler form in the preprojective algebra is:
        chi(S_i, S_j) = delta_{ij} - a_{ij}

    where a_{ij} is the number of arrows from i to j in the McKay quiver Q.

    For the McKay quiver of Z/n (type A_{n-1}):
    arrows: i -> i+1 and i+1 -> i for each i (cyclic quiver, n nodes)
    So the adjacency matrix has a_{i,i+1} = 1, a_{i+1,i} = 1 (mod n).

    The Euler form is:
        chi(S_i, S_j) = 2*delta_{ij} - a_{ij} - a_{ji}   (for symmetric quiver)

    Wait, I need to be more careful. For a quiver Q with vertex set {0,...,r}
    and arrow set, the Euler form of the PATH ALGEBRA kQ is:
        chi(S_i, S_j) = delta_{ij} - #{arrows from i to j}

    For the PREPROJECTIVE ALGEBRA Pi(Q) (which has arrows and their duals),
    the Ext algebra is different.

    Let me use the GEOMETRIC computation directly.

    For the tilting bundle T = O (+) O(E_1) (+) ... (+) O(E_r):
        Hom(O(E_i), O(E_j)) = H^0(O(E_j - E_i))
        Ext^1(O(E_i), O(E_j)) = H^1(O(E_j - E_i))
        Ext^2 = 0 (affine surface)

    With D_0 = 0 (for the O summand) and D_i = E_i for i >= 1.

    For the RESOLUTION of C^2/(Z/n) (type A_{n-1}), the exceptional
    divisors form a chain E_1,...,E_{n-1} with:
        E_i.E_j = -2 if i=j, 1 if |i-j|=1, 0 otherwise

    H^0(O(E_j - E_i)):
        If i = j: H^0(O) = C (1-dimensional)
        If j = i+1: H^0(O(E_{i+1} - E_i)). The divisor E_{i+1}-E_i has
            (E_{i+1}-E_i)^2 = -2 - 2 + 2 = -2, so this is negative on each
            component... Actually for a non-compact surface, H^0 of a line bundle
            associated to a divisor with negative intersection with each component
            is just C (the constants) if the divisor is linearly trivial away from
            the exceptional locus.

    This is getting complicated for a general treatment. Let me use the
    REPRESENTATION-THEORETIC approach instead.

    For the McKay quiver Q_Gamma (extended Dynkin diagram of type ade_type):
    The tilting bundle T gives End(T) = Pi_0(Q_Gamma), the preprojective algebra
    of the DOUBLE quiver, modulo the preprojective relation.

    The dimension of e_i * Pi_0 * e_j can be computed from the representation
    theory of the finite group Gamma.

    For type A_{n-1} (Gamma = Z/n):
        The irreps are rho_0 = trivial, rho_1, ..., rho_{n-1} (all 1-dimensional).
        The McKay quiver is the cyclic quiver with n nodes.
        The preprojective algebra dimension:
            dim(e_i Pi_0 e_j) = min(i,j,n-i,n-j,...) -- this requires care.

    Let me just compute End(T) dimensions directly from representation theory.

    Returns: (n+1) x (n+1) matrix where entry (i,j) = chi(T_i, T_j)
    with T_0 = O, T_i = O(E_i).
    """
    # Use the Euler form chi(S_i, S_j) = delta_{ij} - #{arrows i->j in Q}
    # for the PATH ALGEBRA of the McKay quiver.
    # The McKay quiver for ADE is the EXTENDED Dynkin diagram with arrows
    # i -> j whenever i,j are connected, and DOUBLE arrows (both directions).
    # Actually, the McKay quiver has a_{ij} arrows from i to j where
    # rho_nat tensor rho_j = sum_i a_{ij} rho_i (rho_nat = natural 2-dim rep).

    # For the Euler form of the PATH ALGEBRA:
    #   chi(S_i, S_j) = delta_{ij} - #{arrows i -> j}
    # For type A_{n-1}: cyclic quiver with single arrows i -> i+1 (mod n).
    # So chi(S_i, S_j) = delta_{ij} - delta_{j, i+1 mod n}.

    # But we want the Euler form on the RESOLUTION, which for the tilting
    # bundle T with End(T) = preprojective algebra gives:
    #   chi(T_i, T_j) = delta_{ij} - #{edges between i,j in the underlying graph}
    # because Ext^1 counts arrows in BOTH directions, Ext^2 = 0.
