def mukai_vector(rk, c1, c2, surface='K3'):
    """Compute the Mukai vector v(E) = (rk, c_1, s) where s = rk + c1^2/2 - c2.

    For a K3 surface: td(S) = (1, 0, 2), so
        v(E) = ch(E) * sqrt(td(S)) = (rk, c_1, rk + c_1^2/2 - c_2)

    The "s" component is the holomorphic Euler characteristic chi(E) when
    c_1 is represented as an integer (intersection with H for Picard rank 1).

    Parameters
    ----------
    rk : int
        Rank of the sheaf.
    c1 : int
        First Chern class (as integer, = degree with respect to H).
    c2 : int or Fraction
        Second Chern class.
    surface : str
        'K3' for K3 surface (td = (1,0,2)).

    Returns
    -------
    tuple (r, c, s) = Mukai vector components.
    """
    if surface == 'K3':
        # v(E) = ch(E) * sqrt(td(S))
        # ch(E) = (rk, c1, rk + c1^2/2 - c2)  (for line bundles rk=1, c2=0)
        # sqrt(td(S)) = (1, 0, 1) since td(S) = 1 + 2*pt for K3, sqrt = 1 + pt
        # So v(E) = (rk, c1, ch_2(E) + rk) = (rk, c1, rk + c1^2/2 - c2 + rk)
        # Wait: ch(E) = (rk, c1, c1^2/2 - c2). Then
        # v(E) = ch(E) * sqrt(td(S)) = ch(E) * (1 + pt)
        #       = (rk, c1, c1^2/(2) - c2 + rk)

        # Let me be precise. The Mukai vector is:
        # v(E) = ch(E) . sqrt(td(S))
        # For K3: c_1(S) = 0, c_2(S) = 24, so td(S) = 1 + 0 + 2[pt] where [pt] = c_2/12 = 2.
        # Actually td(K3) = 1 + c_2/12 = 1 + 24/12 = 1 + 2 = ... no, td = 1 + c1/2 + (c1^2+c2)/12
        # For K3: c1(TS) = 0, c2(TS) = 24. td = 1 + 0 + 24/12 = 1 + 2*pt.
        # sqrt(td) = 1 + pt (since (1+pt)^2 = 1 + 2*pt in cohomology, pt^2 = 0).
        # ch(E) = (rk, c1(E), ch_2(E)) where ch_2 = c1^2/2 - c2.
        # v(E) = ch(E) * (1 + pt) = (rk, c1, ch_2 + rk) = (rk, c1, c1^2/2 - c2 + rk)

        s = Fraction(c1**2, 2) - c2 + rk
        return (rk, c1, s)
    raise ValueError(f"Unknown surface type: {surface}")


def mukai_pairing(v, w):
    """Compute the Mukai pairing <v, w>.

    Convention: <(r, c, s), (r', c', s')> = c*c' - r*s' - r'*s

    This is the NEGATIVE of the Euler form: <v(E), v(F)> = -chi(E, F).

    Parameters
    ----------
    v, w : tuples (r, c, s)

    Returns
    -------
    int or Fraction
    """
    r1, c1, s1 = v
    r2, c2, s2 = w
    return c1 * c2 - r1 * s2 - r2 * s1


def euler_form_k3(v, w):
    """Compute the Euler form chi(E, F) = -<v(E), v(F)> on K3.

    chi(E, F) = sum_k (-1)^k dim Ext^k(E, F)

    On K3 (CY2): Serre duality gives Ext^k(E, F) ~ Ext^{2-k}(F, E)^*
    so chi(E, F) = chi(F, E) (symmetric!).

    Parameters
    ----------
    v, w : Mukai vectors (r, c, s)

    Returns
    -------
    int or Fraction
    """
    return -mukai_pairing(v, w)


def mukai_self_pairing(v):
    """Compute <v, v> = c^2 - 2*r*s."""
    r, c, s = v
    return c**2 - 2*r*s


def ext_groups_k3(v, w):
    """Estimate Ext group dimensions on K3.

    For sheaves E, F on K3 with Mukai vectors v(E), v(F):
        chi(E, F) = -<v, w> = r_1*s_2 + r_2*s_1 - c_1*c_2

    Serre duality: Ext^k(E, F) ~ Ext^{2-k}(F, E)^*
    So: ext^0 - ext^1 + ext^2 = chi
    and ext^2(E,F) = ext^0(F,E) = hom(F,E)

    For STABLE sheaves with different Mukai vectors and appropriate stability:
        hom(E, F) and hom(F, E) are often 0, giving ext^1 = -chi.

    Returns dict with 'chi', 'hom_lower', 'ext1_lower', 'ext2_lower'.
    """
    chi = euler_form_k3(v, w)
    chi_rev = euler_form_k3(w, v)
    # chi = chi_rev on K3 (symmetric Euler form)
    return {'chi': chi, 'chi_reverse': chi_rev}


