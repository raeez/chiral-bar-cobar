def cartan_matrix(ade_type, rank):
    """Return the Cartan matrix C for the given root system.

    Convention: C_{ii} = 2, C_{ij} = -#{edges between i,j} for i != j.
    Nodes indexed 1,...,rank internally, returned as rank x rank matrix.

    Parameters
    ----------
    ade_type : str
        One of 'A', 'D', 'E'.
    rank : int
        Rank of the root system.

    Returns
    -------
    np.ndarray of shape (rank, rank)
    """
    if ade_type == 'A':
        C = 2 * np.eye(rank, dtype=int)
        for i in range(rank - 1):
            C[i, i+1] = -1
            C[i+1, i] = -1
        return C
    elif ade_type == 'D':
        if rank < 4:
            raise ValueError("D_n requires n >= 4")
        C = 2 * np.eye(rank, dtype=int)
        # Chain: 1-2-...(n-2), then (n-2) branches to (n-1) and (n)
        for i in range(rank - 2):
            if i < rank - 3:
                C[i, i+1] = -1
                C[i+1, i] = -1
            # Last chain edge: (n-3) -- (n-2)
        # D_n branching: node (rank-3) connects to (rank-2) and (rank-1)
        # Actually let me re-index carefully.
        # Standard D_n: nodes 0,...,n-1. Chain 0-1-...(n-3), branch (n-3)-(n-2) and (n-3)-(n-1)
        C = 2 * np.eye(rank, dtype=int)
        for i in range(rank - 3):
            C[i, i+1] = -1
            C[i+1, i] = -1
        # Branch node at index rank-3
        C[rank-3, rank-2] = -1
        C[rank-2, rank-3] = -1
        C[rank-3, rank-1] = -1
        C[rank-1, rank-3] = -1
        return C
    elif ade_type == 'E':
        if rank not in (6, 7, 8):
            raise ValueError("E_n requires n in {6, 7, 8}")
        C = 2 * np.eye(rank, dtype=int)
        # E_n: chain 0-1-2-...(n-2) with branch at node 2 connecting to extra node
        # Standard: nodes 1,...,n. Chain: 1-3-4-5-...-n, branch: 3-2
        # We use 0-indexed: chain 0-2-3-4-...(n-1), branch: 2-1
        edges = []
        # Main chain: 0-2, 2-3, 3-4, ..., (n-2)-(n-1)
        edges.append((0, 2))
        for i in range(2, rank - 1):
            edges.append((i, i+1))
        # Branch: 2-1
        edges.append((2, 1))
        for (i, j) in edges:
            C[i, j] = -1
            C[j, i] = -1
        return C
    else:
        raise ValueError(f"Unknown ADE type: {ade_type}")


def extended_cartan_matrix(ade_type, rank):
    """Return the extended (affine) Cartan matrix.

    The extending node is indexed 0; the finite nodes are 1,...,rank.
    Returns a (rank+1) x (rank+1) matrix.

    The null vector delta satisfies C_aff * delta = 0, with delta_i = a_i
    (the marks/labels of the affine Dynkin diagram).
    """
    C_fin = cartan_matrix(ade_type, rank)
    n = rank + 1
    C_aff = 2 * np.eye(n, dtype=int)
    # Fill in finite part at indices 1,...,rank
    C_aff[1:, 1:] = C_fin

    # The extending node 0 connects to specific finite nodes depending on type.
    # The highest root theta = sum a_i alpha_i; the extending node connects to
    # those simple roots alpha_j with <alpha_j, theta> != 0.
    if ade_type == 'A':
        # Affine A_n: cycle. Node 0 connects to node 1 and node rank.
        C_aff[0, 1] = -1
        C_aff[1, 0] = -1
        C_aff[0, rank] = -1
        C_aff[rank, 0] = -1
    elif ade_type == 'D':
        # Affine D_n: node 0 connects to node 1 (the first node of the chain).
        # Standard affine D_n has the extending node attached to node 1.
        C_aff[0, 1] = -1
        C_aff[1, 0] = -1
    elif ade_type == 'E':
        if rank == 6:
            # Affine E_6: extending node connects to node rank (the end of the long arm).
            C_aff[0, rank] = -1
            C_aff[rank, 0] = -1
        elif rank == 7:
            # Affine E_7: extending node connects to node 1 (end of short arm).
            # Actually: for E_7, the extending node connects to the end of the
            # longest chain. Let me use the standard convention.
            # With our E_n indexing: chain is 0-2-3-4-...(n-1), branch 2-1.
            # For E_7 (rank 7), the extending node of affine E_7 connects to
            # the END of the long arm, which is node rank = 7 (index rank in
            # 1-indexed = 7, or index rank-1 in 0-indexed finite = 6).
            # In our extended matrix: finite node 7 = index 7.
            C_aff[0, rank] = -1
            C_aff[rank, 0] = -1
        elif rank == 8:
            # Affine E_8: extending node connects to the end of the short arm.
            # In our indexing: finite node 1 (the branch tip) maps to index 1+0=1
            # Actually for E_8 the extending node connects to node 8 (end of long arm).
            # Let me reconsider. The Coxeter labels for E_8 affine:
            # the extending node has label 1 and connects to the end of the
            # longest arm.
            C_aff[0, rank] = -1
            C_aff[rank, 0] = -1

    return C_aff


def affine_marks(ade_type, rank):
    """Return the marks (Kac labels) a_0, a_1, ..., a_r for the affine Dynkin diagram.

    These satisfy sum_i a_i * alpha_i = 0 (null root) with a_0 = 1.
    |Gamma| = sum_{i=0}^{r} a_i^2  for ADE (not true in general, but for
    the McKay correspondence |Gamma| = sum a_i for the EXTENDED diagram marks
    -- actually |Gamma| = sum a_i^2 for the regular representation dimension).

    Actually: for finite subgroup Gamma < SL(2) of type ADE with rank r:
        |Gamma| = sum_{i=0}^{r} a_i^2     (FALSE)
        |Gamma| = sum_{i=0}^{r} a_i       (FALSE in general)

    Correct: |Gamma| = sum_{i=0}^r dim(rho_i)^2 where rho_i are the irreps.
    And dim(rho_i) = a_i (the marks of the affine diagram).
    So |Gamma| = sum a_i^2.

    Wait -- let me be precise. For Z/n: |Gamma| = n, irreps are all 1-dim,
    so sum dim^2 = n * 1 = n. Marks of affine A_{n-1}: all equal to 1.
    So sum a_i^2 = n. Check.

    For binary dihedral D*_{4n} (|Gamma| = 4n, type D_{n+2}):
    marks of affine D_{n+2}: a_0 = 1, a_1 = 1, a_2 = 2, a_3 = 2, ..., a_{n} = 2, a_{n+1} = 1, a_{n+2} = 1
    sum a_i^2 = 1+1+4*(n-1)+1+1 = 4n-4+4 = 4n. Check for n=2: D_4, |Gamma|=8.
    affine D_4 marks: 1,1,2,1,1. sum = 1+1+4+1+1 = 8. Check.
    """
    if ade_type == 'A':
        # Affine A_n: all marks = 1, n+1 nodes total for rank n
        return np.ones(rank + 1, dtype=int)
    elif ade_type == 'D':
        # Affine D_n, n >= 4: marks are 1,1,2,2,...,2,1,1
        # Nodes: 0 (extending), 1,...,n (finite Dynkin)
        # With our convention: finite D_n has chain 0'-(chain)-branch
        # Affine marks for D_n (total n+1 nodes):
        #   extending node: 1
        #   finite node 1: 1 (chain start)
        #   finite nodes 2,...,n-2: 2 (interior of chain up to branch)
        #   Wait, I need to be more careful with our indexing.
        # Standard affine D_n marks: a_0=1, a_1=1, a_2=2,...,a_{n-2}=2, a_{n-1}=1, a_{n}=1
        marks = np.ones(rank + 1, dtype=int)
        # Interior nodes get mark 2
        for i in range(2, rank - 1):
            marks[i] = 2
        return marks
    elif ade_type == 'E':
        if rank == 6:
            # Affine E_6 marks: 1,1,2,3,2,1,2 (with standard ordering)
            # Our ordering: ext(0), then finite nodes 1,...,6
            # Finite E_6 in our indexing: chain 0'-2-3-4-5, branch 2-1
            # So finite marks (1-indexed): node1=1, node2=2, node3=3, node4=2, node5=1, node6=2
            # Wait, let me just list them correctly.
            # Standard E_6 marks: alpha_1,...,alpha_6 with Bourbaki numbering
            # 1-2-3-4-5 with branch 3-6, marks: 1,2,3,2,1,2
            # Affine E_6: extending node has mark 1.
            # In our code: finite E_6 indexing is 0'-2-3-4-5, branch 2-1
            # map: our 0' = Bourbaki 1, our 1 = Bourbaki 6, our 2 = Bourbaki 3,
            #       our 3 = Bourbaki 4, our 4 = Bourbaki 5, our 5 = Bourbaki 2
            # Hmm, this is getting confusing. Let me just hardcode the correct marks
            # for our specific node ordering.
            # Our finite E_6: edges (0,2),(2,3),(3,4),(4,5),(2,1)
            # This is: 0-2-3-4-5 chain, with 1 branching off 2.
            # The marks must satisfy C * marks = 0 for the affine version.
            # Let me compute them from the null vector of the affine Cartan matrix.
            return _compute_marks_from_null(ade_type, rank)
        elif rank == 7:
            return _compute_marks_from_null(ade_type, rank)
        elif rank == 8:
            return _compute_marks_from_null(ade_type, rank)
    raise ValueError(f"Unknown type {ade_type}_{rank}")


def _compute_marks_from_null(ade_type, rank):
    """Compute affine marks as the null vector of the extended Cartan matrix."""
    C_aff = extended_cartan_matrix(ade_type, rank)
    # Find null vector: C_aff * v = 0, with v_0 = 1 and all entries positive integers.
    # Use numpy: find kernel of C_aff.
    # Since C_aff is singular of corank 1 for affine type:
    U, S, Vt = np.linalg.svd(C_aff.astype(float))
    # The null vector is the last row of Vt (corresponding to smallest singular value)
    null_vec = Vt[-1]
    # Normalize so that the minimum positive entry is 1
    null_vec = null_vec / np.min(np.abs(null_vec[null_vec != 0]))
    # Ensure all positive
    if np.sum(null_vec) < 0:
        null_vec = -null_vec
    # Round to integers
    marks = np.round(null_vec).astype(int)
    return marks


def group_order_from_marks(ade_type, rank):
    """Compute |Gamma| = sum_{i=0}^{r} a_i^2 for ADE subgroup Gamma < SL(2)."""
    marks = affine_marks(ade_type, rank)
    return int(np.sum(marks**2))


def ade_group_order(ade_type, rank):
    """Return the order of the finite subgroup Gamma < SL(2,C) of given ADE type.

    A_n: Gamma = Z/(n+1), |Gamma| = n+1
    D_n: Gamma = binary dihedral of order 4(n-2)
    E_6: Gamma = binary tetrahedral, |Gamma| = 24
    E_7: Gamma = binary octahedral, |Gamma| = 48
    E_8: Gamma = binary icosahedral, |Gamma| = 120
    """
    if ade_type == 'A':
        return rank + 1
    elif ade_type == 'D':
        return 4 * (rank - 2)
    elif ade_type == 'E':
        return {6: 24, 7: 48, 8: 120}[rank]
    raise ValueError(f"Unknown type {ade_type}_{rank}")


