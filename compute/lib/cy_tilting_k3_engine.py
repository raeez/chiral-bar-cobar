def mckay_quiver_arrows(ade_type, rank):
    """Return the arrows of the McKay quiver with a standard orientation.

    For type A_{n-1}: oriented cycle 0 -> 1 -> 2 -> ... -> n-1 -> 0
    Plus reverse arrows for the double quiver (preprojective).

    Returns: list of (source, target) pairs for the DOUBLE quiver.
    """
    adj = mckay_adjacency(ade_type, rank)
    n = rank + 1
    arrows = []
    for i in range(n):
        for j in range(i+1, n):
            if adj[i, j] > 0:
                # Add both directions for the double quiver
                for _ in range(adj[i, j]):
                    arrows.append((i, j))
                    arrows.append((j, i))
    return arrows


def preprojective_relation_count(ade_type, rank):
    """Count the number of preprojective relations.

    The preprojective algebra Pi(Q) = kQ_double / (sum [a, a*])
    where Q_double is the double quiver and a* is the dual arrow.

    The number of relations = number of vertices (one relation per vertex).
    """
    return rank + 1  # One relation per vertex of the extended Dynkin diagram


