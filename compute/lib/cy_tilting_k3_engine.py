def euler_form_matrix_resolution(ade_type, rank):
    """Compute the full Euler form matrix chi(R_i, R_j) on the resolution.

    For the tautological bundles R_0, ..., R_r, the Euler form is:
        chi(R_i, R_j) = delta_{ij}
    (preserved by the McKay correspondence / BKR equivalence from the
    equivariant Euler form on C^2).

    This is the identity matrix.
    """
    n = rank + 1
    return np.eye(n, dtype=int)


