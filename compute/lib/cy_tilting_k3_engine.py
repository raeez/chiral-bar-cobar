def is_spherical(v, dim=2):
    """Check if a Mukai vector corresponds to a spherical object on a CY dim-fold.

    E is spherical if Ext*(E, E) ~ H*(S^dim) as graded vector spaces.
    For CY2 (K3): Ext*(E, E) ~ H*(S^2) = C + 0 + C, so chi(E, E) = 2.
    Equivalently: <v, v> = -chi(E, E) = -2.

    For CY3: Ext*(E, E) ~ H*(S^3) = C + 0 + 0 + C, so chi(E, E) = 0.
    And <v, v> = 0. (But need additional data to check sphericality.)
    """
    if dim == 2:
        return mukai_self_pairing(v) == -2
    elif dim == 3:
        return mukai_self_pairing(v) == 0
    return False


def spherical_twist_on_mukai(v_E, v_F):
    """Compute the Mukai vector of T_E(F) where T_E is the spherical twist.

    T_E(F) = Cone(RHom(E, F) tensor E -> F)
    On K-theory/Mukai vectors:
        v(T_E(F)) = v(F) + <v(E), v(F)> * v(E)

    (The sign convention: T_E acts on the Mukai lattice as the REFLECTION
    in the hyperplane perpendicular to v(E):
        T_E: v -> v + <v(E), v> * v(E)
    This uses the fact that <v(E), v(E)> = -2 for a spherical object on K3.)

    Parameters
    ----------
    v_E : Mukai vector of the spherical object E
    v_F : Mukai vector of the object F to twist

    Returns
    -------
    Mukai vector of T_E(F)
    """
    pairing = mukai_pairing(v_E, v_F)
    r_E, c_E, s_E = v_E
    r_F, c_F, s_F = v_F
    return (r_F + pairing * r_E,
            c_F + pairing * c_E,
            s_F + pairing * s_E)


def spherical_twist_squared_shift(v_E, v_F):
    """Verify T_E^2(F) = F[-2] on Mukai vectors.

    T_E^2 = [-2] means T_E^2(F) has the same Mukai vector as F[-2].
    On K3: the shift [-2] acts as identity on K-theory (since shifts by even
    numbers preserve the K-theory class: [F[n]] = (-1)^n [F]).
    So [-2] acts as +1 on K-theory.
    Thus T_E^2 should act as identity on Mukai vectors.

    Returns
    -------
    Mukai vector of T_E^2(F), which should equal v_F.
    """
    v_TE_F = spherical_twist_on_mukai(v_E, v_F)
    v_TE2_F = spherical_twist_on_mukai(v_E, v_TE_F)
    return v_TE2_F


def rhom_structure_sheaf_to_line_bundle(degree, surface='K3'):
    """Compute RHom(O_S, O_S(H)) for K3 with H^2 = degree.

    By Kodaira vanishing on K3 (omega_S = O_S, so O_S(H) tensor omega = O_S(H)):
        H^0(O_S(H)) = chi(O_S(H)) = 2 + H^2/2 = 2 + degree/2  (by RR on K3)
        H^1(O_S(H)) = 0  (Kodaira vanishing, H ample)
        H^2(O_S(H)) = H^0(O_S(-H))^* = 0  (H ample => -H has no sections)

    So RHom(O_S, O_S(H)) = C^{2 + degree/2} concentrated in degree 0.

    Parameters
    ----------
    degree : int
        H^2, must be even for K3 (H^2 = 2d for a polarized K3 of degree 2d).

    Returns
    -------
    dict with ext dimensions.
    """
    if degree % 2 != 0:
        raise ValueError("H^2 must be even for K3")
    chi = 2 + degree // 2
    return {'ext0': chi, 'ext1': 0, 'ext2': 0, 'chi': chi}


def spherical_twist_cone_data(degree):
    """Compute the data of the cone T_{O_S}(O_S(H)).

    T_{O_S}(O_S(H)) = Cone(O_S^{chi(O(H))} -> O_S(H))

    where chi(O(H)) = 2 + H^2/2.

    On Mukai vectors:
    v(O_S) = (1, 0, 1)  (structure sheaf of K3)
    v(O_S(H)) = (1, H, 1 + H^2/2)  (line bundle, c2=0)

    <v(O_S), v(O_S(H))> = 0*H - 1*(1+H^2/2) - 1*1 = -(2+H^2/2)

    T_{O_S}(O_S(H)) has Mukai vector:
    v(O_S(H)) + <v(O_S), v(O_S(H))> * v(O_S)
    = (1, H, 1+H^2/2) + (-(2+H^2/2)) * (1, 0, 1)
    = (1 - 2 - H^2/2, H, 1 + H^2/2 - 2 - H^2/2)
    = (-1 - H^2/2, H, -1)

    For quartic K3 (H^2 = 4):
    = (-3, H, -1) with <(-3,H,-1), (-3,H,-1)> = H^2 - 2*(-3)*(-1) = 4 - 6 = -2.
    So T_{O_S}(O_S(1)) is again spherical. Good.
    """
    d = degree
    v_O = (1, 0, 1)
    v_OH = (1, 1, 1 + d // 2)  # c1 = 1 (class of H), c2 = 0
    v_twist = spherical_twist_on_mukai(v_O, v_OH)
    return {
        'v_O': v_O,
        'v_OH': v_OH,
        'v_twist': v_twist,
        'is_spherical': is_spherical(v_twist, dim=2),
        'rhom_rank': 2 + d // 2
    }


