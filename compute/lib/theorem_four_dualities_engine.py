# ============================================================================
# Type alias
# ============================================================================

Num = Union[int, Fraction]


def _frac(x) -> Fraction:
    """Coerce to exact Fraction."""
    if isinstance(x, Fraction):
        return x
    return Fraction(x)


# ============================================================================
# Core kappa formulas (recomputed from first principles, AP1)
# ============================================================================

def kappa_sl(N: int, k: Num) -> Fraction:
    r"""kappa(sl_N, k) = (N^2 - 1)(k + N) / (2N).

    AP1, AP39: this is NOT c/2 for N > 2.
    """
    k_f, N_f = _frac(k), Fraction(N)
    if N <= 1:
        return Fraction(0)
    if k_f + N_f == 0:
        raise ValueError(f"Critical level k = -{N}")
    return (N_f ** 2 - 1) * (k_f + N_f) / (2 * N_f)


def kappa_gl(N: int, k: Num) -> Fraction:
    r"""kappa(gl_N, k) = kappa(sl_N, k) + k.

    The u(1) Heisenberg factor contributes kappa = k.
    """
    k_f = _frac(k)
    return kappa_sl(N, k_f) + k_f


def c_sl(N: int, k: Num) -> Fraction:
    """Sugawara central charge c(sl_N, k) = k(N^2-1)/(k+N)."""
    k_f, N_f = _frac(k), Fraction(N)
    if N <= 1:
        return Fraction(0)
    if k_f + N_f == 0:
        raise ValueError(f"Critical level k = -{N}")
    return k_f * (N_f ** 2 - 1) / (k_f + N_f)


def c_wn(N: int, k: Num) -> Fraction:
    """Fateev-Lukyanov: c(W_N, k) = (N-1) - N(N^2-1)(k+N-1)^2/(k+N)."""
    k_f, N_f = _frac(k), Fraction(N)
    kN = k_f + N_f
    if kN == 0:
        raise ValueError(f"Critical level k = -{N}")
    return Fraction(N - 1) - N_f * (N_f ** 2 - 1) / kN


def kappa_wn(N: int, c: Num) -> Fraction:
    r"""kappa(W_N, c) = (H_N - 1) * c.

    H_N = 1 + 1/2 + ... + 1/N is the Nth harmonic number.
    """
    H_N = sum(Fraction(1, j) for j in range(1, N + 1))
    return (H_N - 1) * _frac(c)


def kappa_virasoro(c: Num) -> Fraction:
    """kappa(Vir_c) = c/2."""
    return _frac(c) / 2


# ============================================================================
# Duality operations on levels and couplings
# ============================================================================

def ff_dual_level(k: Num, h_dual: int) -> Fraction:
    """Feigin-Frenkel dual level: k' = -k - 2h^v.

    For sl_N: h^v = N, so k' = -k - 2N.
    """
    return -_frac(k) - 2 * h_dual


def ff_dual_psi(psi: Num) -> Fraction:
    """FF duality on coupling: Psi -> -Psi."""
    return -_frac(psi)


def s_dual_psi(psi: Num) -> Fraction:
    """S-duality on coupling: Psi -> 1/Psi."""
    psi_f = _frac(psi)
    if psi_f == 0:
        raise ValueError("Psi = 0: S-dual undefined")
    return Fraction(1) / psi_f


def s_dual_level(k: Num, N: int) -> Fraction:
    """S-dual level: k -> k^S = 1/(k+N) - N.

    From Psi = k + N -> 1/Psi, so k^S = 1/(k+N) - N.
    """
    k_f = _frac(k)
    psi = k_f + N
    if psi == 0:
        raise ValueError("Psi = 0: S-dual undefined")
    return Fraction(1) / psi - N


def triality_23_psi(psi: Num) -> Fraction:
    """(23) transposition of S_3 corner triality: Psi -> 1 - Psi."""
    return Fraction(1) - _frac(psi)


def triality_13_psi(psi: Num) -> Fraction:
    """(13) transposition of S_3 corner triality: Psi -> Psi/(Psi - 1)."""
    psi_f = _frac(psi)
    if psi_f == 1:
        raise ValueError("Psi = 1: (13) transposition undefined")
    return psi_f / (psi_f - 1)


