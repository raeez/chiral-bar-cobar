r"""Theorem: the four dualities are generically distinct.

THEOREM (thm:four-dualities-generic-distinctness)
==================================================

The four dualities acting on boundary VOAs of 4d Chern-Simons theory --

    (1) Chiral Koszul duality (Feigin-Frenkel: Psi -> -Psi)
    (2) S-duality (Langlands: Psi -> 1/Psi)
    (3) 3d mirror symmetry (Coulomb <-> Higgs branch exchange)
    (4) Categorical Koszul duality (BLPW: category O Koszul duality)

-- are generically distinct operations.  No two coincide except at
isolated (often empty over R) loci in the coupling/parameter space.

PROOF ARCHITECTURE
==================

Four independent proofs, each self-contained:

PROOF 1 (Explicit kappa computation):
    For sl_N at level k, the Koszul dual level k' = -k - 2N and the
    S-dual level k^S = 1/(k+N) - N are different rational functions of k.
    The discrepancy kappa(A^S) - kappa(A^!) is a rational function of Psi
    that vanishes only at Psi^2 = -1 (no real solutions).

PROOF 2 (Fixed-point analysis):
    The four operations have different fixed-point sets:
    - FF/Koszul: Psi = 0 (critical level, degenerate)
    - S-duality: Psi = +/- 1 (self-dual coupling)
    - S_3 triality (23): Psi = 1/2
    - Mirror: no universal fixed-point in coupling space
    Different fixed loci => different operations.

PROOF 3 (Action on shadow invariants):
    Under the four dualities, the modular characteristic kappa transforms as:
    - Koszul: kappa -> -kappa (for KM); kappa -> rho*K - kappa (for W-type)
    - S-duality: kappa -> kappa^S (a rational function, NOT -kappa)
    - Mirror: kappa_C -> kappa_H (different algebra, different invariant)
    - Categorical KD: agrees with chiral KD on evaluation core (class G/L),
      differs at higher shadow depth (class C/M)
    The four images of a generic kappa are all distinct.

PROOF 4 (Group-theoretic):
    The four operations generate a group.  For gl_N:
    - S and FF generate the infinite dihedral group D_inf on real Psi
      (since S^2 = FF^2 = id but (S.FF)^n != id for any n over R)
    - The S_3 triality of corner VOAs is a SEPARATE S_3 acting by
      Mobius transformations on Psi
    - FF (Psi -> -Psi) is NOT a Mobius transformation preserving {0,1,inf},
      so it lies OUTSIDE the S_3
    - The full group generated by S, FF, and (23) is S_3 x Z_2 (or larger),
      which is not cyclic.
    Four non-commuting generators of a non-cyclic group cannot all be equal.

CONVENTIONS
===========
    AP1:  kappa formulas recomputed per family, never copied
    AP24: kappa + kappa' = 0 for KM/free; = rho*K for W-algebras
    AP33: H_k^! = Sym^ch(V*) != H_{-k}
    AP39: kappa != c/2 for general VOA
    AP42: correct@sophisticated, false@naive

MULTI-PATH VERIFICATION:
    Path 1: Direct kappa computation at specific (N, k) values
    Path 2: Closed-form discriminant formula
    Path 3: Fixed-point enumeration
    Path 4: Group structure computation
    Path 5: Shadow invariant transformation laws
    Path 6: Cross-engine consistency with mirror_koszul_comparison_engine
    Path 7: Limiting cases (N=1, k->0, k->inf)
    Path 8: Numerical evaluation over parameter grid

References:
    Feigin-Frenkel: arXiv:hep-th/9210032 (FF duality)
    Costello-Gaiotto: arXiv:1812.09257 (S-duality and boundary VOAs)
    BLPW: arXiv:1208.3863 (categorical Koszul duality)
    Webster: arXiv:1611.06541 (Koszul duality between Higgs and Coulomb)
    Gaiotto-Rapcak: arXiv:1703.00982 (corner VOA triality)
    Manuscript: Theorems A, C, D; thm:four-dualities-generic-distinctness
"""

