@dataclass(frozen=True)
class Proof1Result:
    """Result of Proof 1: explicit kappa comparison.

    For gl_N at coupling Psi:
        kappa_original = kappa(gl_N, Psi - N)
        kappa_koszul   = kappa(gl_N, -Psi - N)     [FF dual]
        kappa_s_dual   = kappa(gl_N, 1/Psi - N)    [S-dual]

    The discriminant Delta = kappa^S - kappa^KD measures the
    failure of S-duality to coincide with Koszul duality.
    """
    N: int
    psi: Fraction
    kappa_original: Fraction
    kappa_koszul: Fraction
    kappa_s_dual: Fraction
    discriminant: Fraction           # kappa^S - kappa^KD
    all_three_distinct: bool         # kappa, kappa^KD, kappa^S pairwise distinct
    kd_ne_s: bool                    # kappa^KD != kappa^S


def proof1_explicit_kappa(N: int, psi: Num) -> Proof1Result:
    r"""Proof 1: compute kappa under KD and S-duality, verify distinctness.

    For gl_N at coupling Psi (level k = Psi - N):
        kappa(gl_N, k) = (N^2 - 1)(k + N)/(2N) + k

    Koszul dual: k' = -k - 2N = -Psi - N
        kappa^KD = (N^2-1)(-Psi)/(2N) + (-Psi - N)

    S-dual: k^S = 1/Psi - N
        kappa^S = (N^2-1)(1/Psi)/(2N) + (1/Psi - N)

    Discriminant:
        Delta = kappa^S - kappa^KD
              = [(N^2+2N-1)/(2N)] * (Psi + 1/Psi)
    """
    psi_f = _frac(psi)
    k = psi_f - N

    kap = kappa_gl(N, k)
    kap_kd = kappa_gl(N, ff_dual_level(k, N))
    kap_s = kappa_gl(N, s_dual_level(k, N))

    disc = kap_s - kap_kd

    return Proof1Result(
        N=N,
        psi=psi_f,
        kappa_original=kap,
        kappa_koszul=kap_kd,
        kappa_s_dual=kap_s,
        discriminant=disc,
        all_three_distinct=(kap != kap_kd and kap != kap_s and kap_kd != kap_s),
        kd_ne_s=(kap_kd != kap_s),
    )


def proof1_discriminant_closed_form(N: int, psi: Num) -> Fraction:
    r"""Closed-form discriminant: Delta = [(N^2+2N-1)/(2N)] * (Psi + 1/Psi).

    Derivation:
        kappa(gl_N, k) = (N^2-1)(k+N)/(2N) + k

        Let alpha = (N^2-1)/(2N), so kappa(gl_N, k) = alpha*(k+N) + k
                                                     = (alpha+1)*k + alpha*N
                                                     = (alpha+1)*(Psi-N) + alpha*N
                                                     = (alpha+1)*Psi - N

        kappa^KD = (alpha+1)*(-Psi) - N = -(alpha+1)*Psi - N
        kappa^S  = (alpha+1)*(1/Psi) - N = ... wait, this simplification loses
                   the rational dependence on Psi via the level.

    Let us compute directly:
        kappa(gl_N, k) = (N^2-1)(k+N)/(2N) + k

        For k = Psi - N:
            kappa = (N^2-1)*Psi/(2N) + Psi - N

        For k^KD = -Psi - N:
            kappa^KD = (N^2-1)*(-Psi)/(2N) + (-Psi - N)
                     = -(N^2-1)*Psi/(2N) - Psi - N

        For k^S = 1/Psi - N:
            kappa^S = (N^2-1)*(1/Psi)/(2N) + (1/Psi - N)
                    = (N^2-1)/(2N*Psi) + 1/Psi - N

        Delta = kappa^S - kappa^KD
              = (N^2-1)/(2N*Psi) + 1/Psi - N + (N^2-1)*Psi/(2N) + Psi + N
              = (N^2-1)/(2N) * (Psi + 1/Psi) + (Psi + 1/Psi)
              = [(N^2-1)/(2N) + 1] * (Psi + 1/Psi)
              = [(N^2 + 2N - 1)/(2N)] * (Psi + 1/Psi)

    For Psi real and nonzero: Psi + 1/Psi >= 2 (if Psi > 0) or <= -2 (if Psi < 0).
    The coefficient (N^2+2N-1)/(2N) > 0 for all N >= 1.
    So Delta != 0 for all real Psi != 0.

    Delta = 0 iff Psi + 1/Psi = 0 iff Psi^2 = -1 (no real solutions).
    """
    psi_f = _frac(psi)
    N_f = Fraction(N)
    coeff = (N_f ** 2 + 2 * N_f - 1) / (2 * N_f)
    return coeff * (psi_f + Fraction(1) / psi_f)


def proof1_discriminant_never_zero_real(N: int, psi_values: List[Num]) -> bool:
    """Verify the discriminant is nonzero at all given real Psi values."""
    for psi in psi_values:
        disc = proof1_discriminant_closed_form(N, psi)
        if disc == 0:
            return False
    return True


def proof1_discriminant_coefficient(N: int) -> Fraction:
    """The coefficient (N^2 + 2N - 1)/(2N) in the discriminant formula."""
    N_f = Fraction(N)
    return (N_f ** 2 + 2 * N_f - 1) / (2 * N_f)


def proof1_am_gm_lower_bound(N: int) -> Fraction:
    """Lower bound on |Delta| for real Psi > 0: Delta >= 2 * coeff.

    By AM-GM: Psi + 1/Psi >= 2 for Psi > 0.
    """
    return 2 * proof1_discriminant_coefficient(N)


def proof1_wn_discriminant(N: int, psi: Num) -> Fraction:
    r"""Discriminant for W_N algebras: kappa^S(W_N) - kappa^KD(W_N).

    For W_N at level k = Psi - N:
        c = c_wn(N, k)
        kappa = kappa_wn(N, c) = (H_N - 1) * c

    Koszul dual: k' = -k - 2N, c' = c_wn(N, k'), kappa' = (H_N-1)*c'
    S-dual: k^S = 1/Psi - N, c^S = c_wn(N, k^S), kappa^S = (H_N-1)*c^S

    Discriminant = kappa^S - kappa^KD = (H_N - 1) * (c^S - c^KD)
    """
    psi_f = _frac(psi)
    k = psi_f - N
    k_kd = ff_dual_level(k, N)
    psi_s = Fraction(1) / psi_f
    k_s = psi_s - N

    c_kd = c_wn(N, k_kd)
    c_s = c_wn(N, k_s)

    kap_kd = kappa_wn(N, c_kd)
    kap_s = kappa_wn(N, c_s)

    return kap_s - kap_kd


