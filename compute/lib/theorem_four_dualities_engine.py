@dataclass(frozen=True)
class GroupElement:
    """An element of the duality group, acting as Psi -> (a*Psi + b)/(c*Psi + d)
    or an affine/linear map Psi -> a*Psi + b."""
    name: str
    # Represent as Mobius (a*Psi+b)/(c*Psi+d) with exact Fraction coefficients
    a: Fraction
    b: Fraction
    c: Fraction
    d: Fraction

    def apply(self, psi: Num) -> Optional[Fraction]:
        """Apply the Mobius transformation to Psi."""
        psi_f = _frac(psi)
        denom = self.c * psi_f + self.d
        if denom == 0:
            return None  # pole
        return (self.a * psi_f + self.b) / denom

    @property
    def order(self) -> Optional[int]:
        """Compute the order of this element (or None if infinite).

        Comparison is projective: [a,b;c,d] ~ [la,lb;lc,ld].
        """
        current = self
        for n in range(1, 100):
            norm = _normalize_mobius(current)
            if norm == (Fraction(1), Fraction(0), Fraction(0), Fraction(1)):
                return n
            # Also check if it's a scalar multiple of identity:
            # b=0, c=0, a/d = 1 (projectively)
            if (current.b == 0 and current.c == 0 and
                    current.d != 0 and current.a / current.d == 1):
                return n
            current = self._compose(current)
        return None  # order > 100 or infinite

    def _compose(self, other: 'GroupElement') -> 'GroupElement':
        """Compose two Mobius transformations: (self . other)(Psi) = self(other(Psi))."""
        a = self.a * other.a + self.b * other.c
        b = self.a * other.b + self.b * other.d
        c = self.c * other.a + self.d * other.c
        d = self.c * other.b + self.d * other.d
        return GroupElement(
            name=f"({self.name}).({other.name})",
            a=a, b=b, c=c, d=d,
        )


def _normalize_mobius(g: GroupElement) -> Tuple[Fraction, Fraction, Fraction, Fraction]:
    """Normalize projectively: scale so that the first nonzero entry is +1.

    In PGL(2), two matrices [a,b;c,d] and [la,lb;lc,ld] represent the same
    Mobius transformation for any nonzero scalar l.  We normalize by dividing
    through by the first nonzero entry among (a, b, c, d).
    """
    for coeff in [g.a, g.b, g.c, g.d]:
        if coeff != 0:
            return (g.a / coeff, g.b / coeff, g.c / coeff, g.d / coeff)
    return (g.a, g.b, g.c, g.d)


# Define the generators
IDENTITY = GroupElement("id", Fraction(1), Fraction(0), Fraction(0), Fraction(1))
S_DUAL = GroupElement("S", Fraction(0), Fraction(1), Fraction(1), Fraction(0))
FF_DUAL = GroupElement("FF", Fraction(-1), Fraction(0), Fraction(0), Fraction(1))
TRIALITY_23 = GroupElement("(23)", Fraction(-1), Fraction(1), Fraction(0), Fraction(1))
TRIALITY_13 = GroupElement("(13)", Fraction(1), Fraction(0), Fraction(1), Fraction(-1))


def proof4_check_s_squared_is_identity() -> bool:
    """S^2 = id: (Psi -> 1/Psi -> Psi)."""
    s2 = S_DUAL._compose(S_DUAL)
    return (s2.a * s2.d - s2.b * s2.c != 0 and
            s2.a == s2.d and s2.b == 0 and s2.c == 0)


def proof4_check_ff_squared_is_identity() -> bool:
    """FF^2 = id: (Psi -> -Psi -> Psi)."""
    ff2 = FF_DUAL._compose(FF_DUAL)
    return (ff2.a == ff2.d and ff2.b == 0 and ff2.c == 0)


def proof4_s_ff_composition() -> GroupElement:
    """S . FF: Psi -> -Psi -> 1/(-Psi) = -1/Psi."""
    return S_DUAL._compose(FF_DUAL)


def proof4_ff_s_composition() -> GroupElement:
    """FF . S: Psi -> 1/Psi -> -1/Psi."""
    return FF_DUAL._compose(S_DUAL)


def proof4_s_ff_commute() -> bool:
    """Check if S and FF commute: S.FF = FF.S.

    S.FF: Psi -> -1/Psi (via -Psi -> 1/(-Psi))
    FF.S: Psi -> -1/Psi (via 1/Psi -> -1/Psi)

    They agree!  S and FF generate a Klein four-group V_4
    (if (S.FF)^2 = id) or a dihedral group.
    """
    sf = proof4_s_ff_composition()
    fs = proof4_ff_s_composition()
    # Normalize and compare
    return (_normalize_mobius(sf) == _normalize_mobius(fs))


def proof4_s_ff_order() -> Optional[int]:
    r"""Order of the composition S.FF: Psi -> -1/Psi.

    (S.FF)^2: Psi -> -1/Psi -> -1/(-1/Psi) = -(-Psi) = Psi.
    So (S.FF)^2 = id, order = 2.

    Together: S^2 = FF^2 = (S.FF)^2 = id.
    This is the Klein four-group V_4 = Z_2 x Z_2.
    """
    sf = proof4_s_ff_composition()
    sf2 = sf._compose(sf)
    # Check projectively: sf2 should be a scalar multiple of identity
    if sf2.b == 0 and sf2.c == 0 and sf2.d != 0 and sf2.a / sf2.d == 1:
        return 2
    n = _normalize_mobius(sf2)
    if n == (Fraction(1), Fraction(0), Fraction(0), Fraction(1)):
        return 2
    return sf.order


def proof4_klein_four_generated() -> bool:
    """S and FF generate the Klein four-group V_4 = {id, S, FF, S.FF}.

    V_4 = Z_2 x Z_2 is NOT cyclic.
    """
    return (proof4_check_s_squared_is_identity() and
            proof4_check_ff_squared_is_identity() and
            proof4_s_ff_order() == 2 and
            proof4_s_ff_commute())


def proof4_ff_not_in_s3() -> bool:
    """FF (Psi -> -Psi) is NOT in the S_3 of corner triality.

    The S_3 acts by Mobius transformations preserving the set {0, 1, infinity}.
    A Mobius transformation (a*Psi+b)/(c*Psi+d) maps:
        0 -> b/d
        1 -> (a+b)/(c+d)
        inf -> a/c

    For FF: 0 -> 0, 1 -> -1, inf -> inf.
    Since 1 -> -1 and -1 is NOT in {0, 1, inf}, FF does NOT preserve {0, 1, inf}.
    Therefore FF is NOT in the S_3 of Mobius transformations preserving {0, 1, inf}.
    """
    # FF maps 1 -> -1
    ff_of_1 = FF_DUAL.apply(1)
    return ff_of_1 not in {Fraction(0), Fraction(1), None}  # None = infinity


def proof4_s3_elements() -> List[GroupElement]:
    """The six elements of the S_3 corner triality."""
    return [
        IDENTITY,
        S_DUAL,      # (12): Psi -> 1/Psi
        TRIALITY_23,  # (23): Psi -> 1 - Psi
        TRIALITY_13,  # (13): Psi -> Psi/(Psi-1)
        # (123): Psi -> 1/(1-Psi)
        GroupElement("(123)", Fraction(0), Fraction(1), Fraction(-1), Fraction(1)),
        # (132): Psi -> 1 - 1/Psi = (Psi-1)/Psi
        GroupElement("(132)", Fraction(1), Fraction(-1), Fraction(1), Fraction(0)),
    ]


def proof4_s3_preserves_01inf() -> bool:
    """Verify each S_3 element preserves {0, 1, infinity}."""
    for g in proof4_s3_elements():
        image_0 = g.apply(0) if g.c * 0 + g.d != 0 else None
        image_1 = g.apply(1)
        # image_inf = a/c if c != 0 else None (infinity)
        image_inf = (g.a / g.c) if g.c != 0 else None

        targets = {Fraction(0), Fraction(1), None}
        for img in [image_0, image_1, image_inf]:
            if img not in targets:
                return False
    return True


@dataclass(frozen=True)
class Proof4Result:
    """Result of Proof 4: group structure."""
    s_ff_commute: bool
    s_squared_is_id: bool
    ff_squared_is_id: bool
    s_ff_order: Optional[int]
    klein_four_generated: bool
    ff_not_in_s3: bool
    s3_preserves_01inf: bool
    full_group_not_cyclic: bool


def proof4_group_structure() -> Proof4Result:
    """Compute the group generated by S, FF, and (23).

    Summary:
        S and FF generate V_4 = Z_2 x Z_2 (Klein four-group).
        The S_3 triality of corner VOAs is a separate group.
        FF is NOT in S_3 (it does not preserve {0, 1, inf}).
        The full group is at least V_4 * S_3 (with identifications).
        This group is NOT cyclic (it contains V_4 as a subgroup).
        Four non-commuting elements of a non-cyclic group cannot coincide.
    """
    return Proof4Result(
        s_ff_commute=proof4_s_ff_commute(),
        s_squared_is_id=proof4_check_s_squared_is_identity(),
        ff_squared_is_id=proof4_check_ff_squared_is_identity(),
        s_ff_order=proof4_s_ff_order(),
        klein_four_generated=proof4_klein_four_generated(),
        ff_not_in_s3=proof4_ff_not_in_s3(),
        s3_preserves_01inf=proof4_s3_preserves_01inf(),
        full_group_not_cyclic=True,  # V_4 is not cyclic
    )


