def landscape_distinctness_survey(
    N_max: int = 4,
    psi_values: Optional[List[Num]] = None,
) -> Dict[str, Any]:
    """Survey distinctness across a grid of (N, Psi) values.

    Returns the fraction of (N, Psi) pairs where all four dualities
    produce distinct kappa values.
    """
    if psi_values is None:
        psi_values = [Fraction(1, 3), Fraction(1, 2), Fraction(2),
                      Fraction(3), Fraction(5, 2), Fraction(7, 3)]

    total = 0
    all_distinct = 0
    kd_ne_s = 0
    results = []

    for N in range(1, N_max + 1):
        for psi in psi_values:
            try:
                p1 = proof1_explicit_kappa(N, psi)
                total += 1
                if p1.kd_ne_s:
                    kd_ne_s += 1
                if p1.all_three_distinct:
                    all_distinct += 1
                results.append({
                    'N': N, 'psi': psi,
                    'kd_ne_s': p1.kd_ne_s,
                    'all_distinct': p1.all_three_distinct,
                    'discriminant': p1.discriminant,
                })
            except (ValueError, ZeroDivisionError):
                pass

    return {
        'total_tested': total,
        'kd_ne_s_count': kd_ne_s,
        'all_distinct_count': all_distinct,
        'kd_ne_s_fraction': Fraction(kd_ne_s, total) if total > 0 else None,
        'all_distinct_fraction': Fraction(all_distinct, total) if total > 0 else None,
        'results': results,
    }


