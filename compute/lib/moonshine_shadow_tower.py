def mckay_thompson_coefficients(conjugacy_class: str,
                                 max_n: int = 5) -> Tuple[int, ...]:
    """Coefficients of T_g(tau) for a given conjugacy class g.

    Returns (c(0), c(1), ..., c(max_n)) where T_g = q^{-1} + sum c(n) q^n.
    """
    if conjugacy_class not in MCKAY_THOMPSON_DATA:
        raise ValueError(
            f"Unknown conjugacy class: {conjugacy_class}. "
            f"Known: {list(MCKAY_THOMPSON_DATA.keys())}"
        )
    data = MCKAY_THOMPSON_DATA[conjugacy_class]['coefficients']
    result = []
    for n in range(max_n + 1):
        if n in data:
            result.append(data[n])
        else:
            result.append(None)
    return tuple(result)


def mckay_thompson_shadow_kappa(conjugacy_class: str) -> Optional[Rational]:
    """Equivariant modular characteristic kappa_g.

    For the identity 1A: kappa_1A = kappa(V^natural) = 12.

    For a general element g of order N, the McKay-Thompson series
    T_g(tau) has level N.  The equivariant kappa is related to the
    Rademacher sum:
      kappa_g = (1/24) * c_g(0) (if the Hauptmodul has c_g(0) = 0,
      then the equivariant genus-1 amplitude vanishes).

    NOTE: c_g(0) = 0 for ALL conjugacy classes (this is a consequence
    of the Hauptmodul property: the unique Hauptmodul for Gamma_g
    normalized to have q^{-1} leading term has vanishing constant term).
    Therefore kappa_g is NOT simply c_g(0)/24.

    CORRECT APPROACH: kappa_g is defined via the equivariant bar complex.
    At genus 1, the g-twisted partition function Tr(g * q^{L_0 - c/24})
    gives T_g(tau).  The equivariant modular characteristic is:
      kappa_g = Tr(g|curvature class) = Tr(g on the kappa-eigenspace)
    For the identity: kappa_1A = Tr(1|V) * kappa = kappa = 12.
    For a general g: kappa_g = Tr(g acting on the bar-complex genus-1 class).

    Without the explicit bar complex of V^natural, we can only compute
    kappa_g for the Virasoro sector:
      kappa_g^{Vir} = (c/2) * chi_g(1) where chi_g(1) = 1 for all g.
    So kappa_g^{Vir} = 12 for all g.

    The actual kappa_g for V^natural includes corrections from the
    weight-2 primaries: kappa_g = 12 + (correction from Griess algebra).
    These corrections are the equivariant version of the S_3 contribution.
    """
    if conjugacy_class == '1A':
        return Rational(12)
    # For other classes: the Virasoro contribution is always 12
    # (the Virasoro algebra is a singlet under M, so Tr(g|Vir) = 1 for all g).
    # The Griess algebra correction is frontier.
    return Rational(12)  # Virasoro level only


def mckay_thompson_equivariant_F1(conjugacy_class: str) -> Optional[Rational]:
    """Equivariant genus-1 amplitude F_1^g.

    F_1^g = kappa_g / 24.

    At the Virasoro level, kappa_g = 12 for all g, so F_1^g = 1/2.

    The equivariant corrections from the Griess algebra would modify this.
    """
    kappa_g = mckay_thompson_shadow_kappa(conjugacy_class)
    if kappa_g is None:
        return None
    return kappa_g / 24


