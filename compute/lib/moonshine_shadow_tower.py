def _virasoro_shadow_coefficient(r: int) -> Rational:
    r"""Virasoro shadow coefficient S_r at c = 24.

    These are the shadow tower coefficients for the Virasoro subalgebra
    of V^natural (i.e., treating V^natural as if generated by T alone).

    S_2 = c/2 = 12
    S_3 = 2  (universal for Virasoro)
    S_4 = 10/[c(5c+22)] = 10/(24*142) = 5/1704
    S_r for r >= 5: recursion from the master equation.
    """
    if r < 2:
        return Rational(0)
    if r == 2:
        return Rational(12)  # c/2 = 24/2
    if r == 3:
        return Rational(2)   # universal Virasoro
    if r == 4:
        return Rational(5, 1704)  # 10/(24*142)

    # Recursion for r >= 5 (shadow tower ODE at c = 24)
    # 2r * S_r + sum_{j<k, j+k=r+2, j,k>=3} 2jk*S_j*S_k/c
    #          + [if r+2 even: m^2*S_m^2/c, m=(r+2)/2] = 0
    c = Rational(24)
    obstruction = Rational(0)
    for j in range(3, (r + 2) // 2 + 1):
        k = r + 2 - j
        if k < 3:
            continue
        Sj = _virasoro_shadow_coefficient(j)
        Sk = _virasoro_shadow_coefficient(k)
        if j < k:
            obstruction += 2 * j * k * Sj * Sk / c
        else:  # j == k
            obstruction += j * k * Sj * Sk / c

    return Rational(-obstruction, 2 * r)


@lru_cache(maxsize=64)
def virasoro_shadow_tower(max_r: int = 10) -> Dict[int, Rational]:
    """Virasoro shadow tower at c = 24: {r: S_r(c=24)} for r = 2..max_r.

    This is the LOWER BOUND on the V^natural shadow tower,
    computed from the Virasoro subsector alone.
    """
    return {r: _virasoro_shadow_coefficient(r) for r in range(2, max_r + 1)}


