def verify_decomposition_totals() -> bool:
    """Verify that irrep dimensions sum to the J-function coefficients.

    dim V_0 = 1 = J[-1] (from the q^{-1} term)
    dim V_n = J[n] for n >= 1 (since J = q^{-1} + sum_{n>=1} c(n) q^n
    and dim V_n = c(n) for the n-th graded piece).

    CORRECTION: The graded piece V_n of V^natural has n >= 0 with
    V_0 = vacuum (1-dimensional), and the grading convention is
    Z(tau) = q^{-1} + sum_{n>=0} dim(V_{n+1}) q^n.
    Actually: Z = sum_{n>=0} dim(V_n) q^{n-1} = q^{-1} * sum dim(V_n) q^n.
    So dim(V_0) = 1 (from q^{-1}), dim(V_1) = 0 (from c(0) = 0),
    dim(V_2) = 196884 (from c(1) = 196884).

    BUT: the standard convention for V^natural is:
      V^natural = bigoplus_{n >= -1} V_n with V_{-1} = vacuum.
    So V_{-1} = 1 (q^{-1}), V_0 = 0, V_1 = 196884, V_2 = 21493760.

    The decomposition table uses the convention where the "weight-n"
    space V_n has dim = J[n] for n >= 1, dim(V_0) = 1 from the vacuum.
    Actually: the standard grading has V^natural = bigoplus V_n with
    L_0 eigenvalue n >= 0.  The partition function is
      Z(tau) = Tr q^{L_0 - c/24} = q^{-1} sum dim(V_n) q^n
    So the q^{-1} coefficient gives dim(V_0) = 1.
    The q^0 coefficient gives dim(V_1) = 0.
    The q^1 coefficient gives dim(V_2) = 196884.

    Convention here: MONSTER_DECOMPOSITIONS[n] describes V_{n+1}, the
    space at L_0 = n+1, whose dimension is J[n] for n >= 1, and
    V_0 (the vacuum) at J[-1] = 1.

    SIMPLIFICATION: Just verify the sum of irrep dims equals the total.
    """
    for n, data in MONSTER_DECOMPOSITIONS.items():
        total = sum(dim for dim, _ in data['irreps'])
        if total != data['total']:
            return False
    return True


