def ds_chirhoch1_hook_type(N: int, partition: Tuple[int, ...]) -> Dict:
    r"""ChirHoch^1 compatibility for non-principal DS: V_k(sl_N) -> W_k(sl_N, f_lambda).

    For a hook-type partition lambda of N:
      The W-algebra W_k(sl_N, f_lambda) has specific ChirHoch^1.
      The DS functor must map dim(sl_N) outer derivations to
      the correct ChirHoch^1 of the target.

    For the PRINCIPAL nilpotent (partition = (N)):
      ChirHoch^1(W_N) = 0 (all killed, as above).

    For the SUBREGULAR nilpotent (partition = (N-1, 1)):
      The Levi subalgebra is gl_1, and one outer derivation survives
      as an outer derivation of the subregular W-algebra.
      ChirHoch^1(W_k(sl_N, f_{(N-1,1)})) expected to be nonzero.

    For the MINIMAL nilpotent (partition = (2, 1, ..., 1)):
      The Levi is gl_{N-1}, and many derivations survive.

    This function computes the expected ChirHoch^1 dimension based on
    the Levi decomposition g = l + n_+ + n_-.

    The key formula: dim ChirHoch^1(W(g, f)) = dim(l) for the Levi
    subalgebra l = centralizer of the sl_2 triple.  This is because:
      - n_+ derivations are BRST-exact (dim n_+ killed)
      - n_- derivations become screening (dim n_- absorbed)
      - Levi derivations survive as outer derivations of W(g, f)
        (but only the CENTER z(l) contributes to ChirHoch^1,
        since the semisimple part [l,l] derivations become inner).

    More precisely: ChirHoch^1(W(g, f)) = z(l), the center of the Levi.
    For principal f: l = 0, z(l) = 0, ChirHoch^1 = 0. Consistent.
    For subregular (N-1,1): l = gl_1, z(l) = C, dim = 1.
    For minimal (2,1,...,1): l = gl_{N-1}, z(l) = C, dim = 1.
    For (p, q) with p+q=N: l = gl_1, z(l) = C, dim = 1 (for hook).

    NOTE: The formula ChirHoch^1(W(g,f)) = z(l) is at generic level.
    At special levels (critical, admissible), additional structure may appear.
    """
    if sum(partition) != N:
        raise ValueError(f"Partition {partition} does not sum to N={N}")

    # Compute Levi data from partition
    # For partition (p1, p2, ..., pr) of N in type A:
    # The Levi subalgebra is gl_{p1} x gl_{p2} x ... x gl_{pr} (minus overall trace)
    # Its center z(l) has dimension = (number of parts) - 1
    num_parts = len(partition)
    dim_center_levi = num_parts - 1

    # For principal (N): 1 part, z(l) dim = 0
    # For (N-1,1): 2 parts, z(l) dim = 1
    # For (p,q): 2 parts, z(l) dim = 1
    # For (1,1,...,1): N parts, z(l) dim = N-1 (but this is the zero nilpotent)

    # Dimension of n_+ and n_- from the partition
    # For partition lambda, dim(n_+) = (N^2-1 - dim_levi)/2
    # dim_levi = sum p_i^2 - 1
    dim_levi = sum(p ** 2 for p in partition) - 1
    dim_n_plus = (N * N - 1 - dim_levi) // 2
    dim_n_minus = dim_n_plus

    # Verify: dim_g = dim_levi + 2*dim_n_plus
    assert N * N - 1 == dim_levi + 2 * dim_n_plus, (
        f"Dimension mismatch for partition {partition} of {N}"
    )

    return {
        "N": N,
        "partition": partition,
        "dim_g": N * N - 1,
        "dim_levi": dim_levi,
        "dim_center_levi": dim_center_levi,
        "dim_n_plus": dim_n_plus,
        "dim_n_minus": dim_n_minus,
        "chirhoch1_source": N * N - 1,
        "chirhoch1_target_expected": dim_center_levi,
        "killed_by_brst": dim_n_plus,
        "absorbed_inner_semisimple": dim_levi - dim_center_levi,
        "absorbed_screening": dim_n_minus,
        "surviving_outer": dim_center_levi,
        "total_accounted": dim_n_plus + (dim_levi - dim_center_levi) + dim_n_minus + dim_center_levi,
        "consistent": (dim_n_plus + (dim_levi - dim_center_levi) + dim_n_minus + dim_center_levi) == N * N - 1,
    }
