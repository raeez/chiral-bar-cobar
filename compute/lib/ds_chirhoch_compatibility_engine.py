def ds_chirhoch1_dimension_accounting(N: int) -> Dict[str, int]:
    r"""Verify that DS annihilates all dim(sl_N) = N^2-1 outer derivations.

    Returns a dict with:
      dim_source: dim ChirHoch^1(V_k(sl_N)) = N^2 - 1
      dim_target: dim ChirHoch^1(W_N) = 0
      killed_by_brst: N(N-1)/2  (positive roots, BRST-exact)
      absorbed_sugawara: N-1  (Cartan, inner via Sugawara)
      absorbed_screening: N(N-1)/2  (negative roots, inner via screening)
      total_accounted: N^2 - 1
      consistent: True iff total_accounted == dim_source

    Checks:
      N=2: 3 = 1 + 1 + 1.  Source 3, target 0.
      N=3: 8 = 3 + 2 + 3.  Source 8, target 0.
      N=4: 15 = 6 + 3 + 6. Source 15, target 0.
    """
    data = sl_n_data(N)
    dim_source = data["dim"]
    killed = data["pos_roots"]
    sugawara = data["rank"]
    screening = data["neg_roots"]
    total = killed + sugawara + screening

    return {
        "N": N,
        "dim_source": dim_source,
        "dim_target": 0,
        "killed_by_brst": killed,
        "absorbed_sugawara": sugawara,
        "absorbed_screening": screening,
        "total_accounted": total,
        "consistent": total == dim_source,
        "deficit": dim_source - total,
    }


