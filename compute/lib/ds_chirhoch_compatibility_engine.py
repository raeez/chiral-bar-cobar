# sl_N data
def sl_n_data(N: int) -> Dict[str, int]:
    r"""Root space decomposition data for sl_N.

    dim(sl_N) = N^2 - 1
    rank = N - 1
    number of positive roots = N(N-1)/2
    number of negative roots = N(N-1)/2

    Checks:
      N=2: dim=3, rank=1, pos_roots=1, neg_roots=1.  3 = 1 + 1 + 1.
      N=3: dim=8, rank=2, pos_roots=3, neg_roots=3.  8 = 3 + 2 + 3.
    """
    if not isinstance(N, int) or N < 2:
        raise ValueError(f"N must be integer >= 2, got {N!r}")
    dim = N * N - 1
    rank = N - 1
    pos_roots = N * (N - 1) // 2
    neg_roots = pos_roots
    # Sanity: dim = pos + rank + neg
    assert dim == pos_roots + rank + neg_roots, (
        f"sl_{N}: {dim} != {pos_roots} + {rank} + {neg_roots}"
    )
    return {
        "N": N,
        "dim": dim,
        "rank": rank,
        "pos_roots": pos_roots,
        "neg_roots": neg_roots,
    }


