def admissible_character_leading_coeffs(p: int, q: int, n_terms: int = 5) -> Dict:
    """Leading coefficients of the vacuum character of L_k(sl_2).

    ch(L_k; tau) = q^{-c/24} * sum_{n>=0} a_n q^n

    For the universal algebra V_k:
        ch(V_k; tau) = q^{-c/24} * prod_{n>=1} (1-q^n)^{-3}
                     = q^{-c/24} * sum p_3(n) q^n

    For the simple quotient L_k:
        ch(L_k; tau) = ch(V_k; tau) - (corrections from null ideal)

    The corrections start at weight h_null = (p-1)*q.
    Below h_null, the characters agree: a_n(L_k) = p_3(n) = a_n(V_k).
    """
    c = 3 * (Fraction(p, q) - 2) / (Fraction(p, q))
    h_null = (p - 1) * q

    # Compute p_3(n) for n = 0, ..., n_terms-1
    verma_dims = vacuum_verma_dims_sl2(max(n_terms, h_null + 5))

    coeffs = {}
    for n in range(min(n_terms, h_null)):
        coeffs[n] = verma_dims[n]

    # At and above h_null, we would need the Shapovalov data
    # This is expensive, so we mark as needing numerical computation
    for n in range(h_null, n_terms):
        coeffs[n] = None  # need Shapovalov computation

    return {
        'c': c,
        'c_over_24': c / 24,
        'h_null': h_null,
        'leading_coeffs': coeffs,
        'agree_with_universal_below_null': True,
    }


