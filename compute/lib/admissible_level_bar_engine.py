# Virasoro minimal model identification
# =========================================================================

def minimal_model_identification(p: int, q: int) -> Dict:
    """Identify the Virasoro minimal model M(p,q) associated to L_k(sl_2).

    The DS (Drinfeld-Sokolov) reduction of L_k(sl_2) at k = p/q - 2
    gives the Virasoro minimal model M(p,q) (when it exists).

    M(p,q) has central charge c = 1 - 6(p-q)^2/(pq).
    Primary fields labeled by (r,s) with 1 <= r <= p-1, 1 <= s <= q-1
    (for the UNITARY minimal models with p = q+1, these are all positive).

    The number of primary fields: (p-1)(q-1)/2.
    """
    c_min = Fraction(1) - 6 * Fraction((p - q)**2, p * q)
    kappa_vir = c_min / 2

    # Number of primary fields
    if q >= 2:
        n_primaries = (p - 1) * (q - 1) // 2
    else:
        n_primaries = 0  # degenerate case

    # Conformal weights of primary fields
    primaries = []
    if q >= 2:
        for r in range(1, p):
            for s in range(1, q):
                h_rs = (Fraction(r * q - s * p, 1)**2 - Fraction(p - q, 1)**2) / (4 * p * q)
                if (r, s) not in [(p - r, q - s) for r2, s2 in [(r, s)]] or r * q < s * p:
                    # Kac table identification: (r,s) ~ (p-r, q-s)
                    primaries.append({
                        'r': r, 's': s,
                        'h': h_rs,
                        'label': f'phi_({r},{s})',
                    })

    return {
        'p': p, 'q': q,
        'c_minimal_model': c_min,
        'kappa_virasoro': kappa_vir,
        'n_primaries': n_primaries,
        'primaries': primaries[:10],  # first 10
        'ds_source': f'L_{{p/q-2}}(sl_2) = L_{{{Fraction(p,q)-2}}}(sl_2)',
        'is_unitary': (p == q + 1),
    }


# =========================================================================
# Bar-Ext vs ordinary Ext: structural comparison
# =========================================================================

def bar_ext_vs_ordinary_ext(p: int, q: int) -> Dict:
    """Compare bar-derived Ext with ordinary (module-category) Ext.

    For V_k (universal): bar-Ext = CE cohomology of g = sl_2.
        bar-Ext^0 = C (trivial, for reduced: 0)
        bar-Ext^1 = sl_2 (3-dim)
        bar-Ext^n = 0 for n >= 2

    For L_k (simple quotient): bar-Ext is the cohomology of B(L_k).
        If Koszul: bar-Ext^1 = sl_2, bar-Ext^n = 0 for n >= 2 (same as V_k).
        If NOT Koszul: bar-Ext^n != 0 for some n >= 2.

    Ordinary Ext (in the module category of L_k):
        At integrable levels: semisimple, so Ext^i = 0 for i >= 1.
        At admissible non-integrable: non-semisimple, Ext^1 != 0
        for some module pairs.

    KEY DISTINCTION: bar-Ext and ordinary Ext are DIFFERENT invariants.
    bar-Ext measures the bar cohomology of the ALGEBRA.
    ordinary Ext measures the homological algebra of the MODULE CATEGORY.

    They are related by the Koszul duality functor: when L_k is Koszul,
    the bar-derived category is equivalent to modules over the Koszul dual,
    and Ext groups can be compared via the Koszul functor.

    When L_k is NOT Koszul, the bar construction still exists but the
    equivalence breaks down — bar-Ext gives a WEAKER invariant than
    the full derived category of L_k-modules.
    """
    analysis = admissible_bar_analysis_sl2(p, q)
    level = admissible_level_sl2(p, q)
    weights = admissible_weights_sl2(level)

    result = {
        'p': p, 'q': q,
        'k': str(Fraction(p, q) - 2),
        'bar_ext_1': 3,  # dim sl_2, always
        'bar_ext_2_universal': 0,  # for V_k
        'bar_ext_2_quotient': 0 if analysis.is_quotient_koszul else None,
        'ordinary_ext_semisimple': (q == 1),
        'n_admissible_modules': len(weights),
        'module_category_type': 'semisimple MTC' if q == 1 else 'non-semisimple braided',
    }

    if q >= 2:
        # Compute some Ext^1 pairs heuristically
        ext1_pairs = []
        for i, wi in enumerate(weights):
            for j, wj in enumerate(weights):
                if i >= j:
                    continue
                # Heuristic: adjacent Kac table entries have Ext^1 != 0
                if abs(wi.r - wj.r) + abs(wi.s - wj.s) == 1:
                    ext1_pairs.append((wi.label, wj.label))
        result['ordinary_ext1_pairs'] = ext1_pairs
        result['ordinary_ext1_count'] = len(ext1_pairs)
    else:
        result['ordinary_ext1_pairs'] = []
        result['ordinary_ext1_count'] = 0

    return result


