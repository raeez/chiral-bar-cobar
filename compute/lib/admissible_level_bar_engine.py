# sl_3 admissible levels
# =========================================================================

def admissible_bar_analysis_sl3(p: int, q: int) -> Dict:
    """Bar complex analysis for L_k(sl_3) at admissible level k = p/q - 3.

    sl_3 has h^v = 3, dim = 8.
    Admissible: k = p/q - 3, p >= 3, q >= 1, gcd(p,q) = 1.

    First null vector: more complex than sl_2 (multiple root types).
    For the principal embedding, the first null at:
        h_null depends on the minimal positive integer solution of
        m*(k+3) - <rho, alpha^v> = j for positive real roots.

    KEY FACT: For sl_3 with 8 generators all of weight 1, the CE complex
    reaches arity 8. So null vectors at weight < 8 ARE bar-relevant.
    This makes the sl_3 case much more delicate than sl_2.

    Shadow depth: L (Lie, r_max=3) for affine sl_3.
    """
    k = Fraction(p, q) - 3
    c = 8 * k / (k + 3)  # c = dim(g)*k/(k+h^v) for sl_3
    kappa = Fraction(8) * (k + 3) / (2 * 3)  # dim(g)*(k+h^v)/(2h^v)

    # For sl_3, the first null vector weight depends on the root system.
    # Positive real roots of type alpha_1, alpha_2, alpha_1+alpha_2.
    # With h^v = 3 and k+3 = p/q:
    # Type II beta_{m,-,alpha_i}: m*(k+3) - <rho, alpha_i^v> = j
    # For long roots: <rho, alpha^v> = 1. So j = m*p/q - 1.
    # Smallest m = q: j = p-1, grade = (p-1)*q.
    h_null_est = (p - 1) * q  # estimate (same formula as sl_2 for each root)

    # CE complex for sl_3:
    # dim Lambda^d(sl_3) = C(8, d)
    # Max arity = 8 (dim sl_3 = 8)
    ce_dims = {d: comb(8, d) for d in range(9)}

    return {
        'type': 'sl3',
        'p': p, 'q': q,
        'k': k, 'c': c,
        'kappa': kappa,
        'h_null_estimate': h_null_est,
        'max_bar_arity': 8,
        'ce_dims': ce_dims,
        'shadow_depth_class': 'L',
        'bar_relevant_null': h_null_est <= 8,
        'is_universal_koszul': True,
        'is_quotient_koszul': True if h_null_est > 8 else None,
        'mechanism': (
            f"h_null_est = {h_null_est}. "
            f"Bar-relevant if h_null <= 8 (max CE arity for sl_3). "
            f"{'ABOVE bar range: Koszul.' if h_null_est > 8 else 'WITHIN bar range: detailed analysis needed.'}"
        ),
    }


