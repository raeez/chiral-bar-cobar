# Comprehensive classification table
# =========================================================================

def classification_table_sl2(max_q: int = 5, max_k: float = 4.0) -> List[Dict]:
    """Build the comprehensive classification table for admissible sl_2 levels.

    For each admissible level, computes:
    - Level, central charge, kappa
    - Null vector data
    - Koszulness verdict (V_k and L_k)
    - Shadow depth
    - Associated variety
    - Bar-Ext comparison
    """
    results = []
    for q_val in range(1, max_q + 1):
        for p_val in range(2, int(max_k * q_val) + 5):
            if gcd(p_val, q_val) != 1:
                continue
            k = Fraction(p_val, q_val) - 2
            if float(k) < -2 or float(k) > max_k:
                continue

            analysis = admissible_bar_analysis_sl2(p_val, q_val)
            results.append({
                'p': p_val,
                'q': q_val,
                'k': str(k),
                'k_float': float(k),
                'c': str(analysis.c),
                'kappa_km': str(analysis.kappa_km),
                'h_null': analysis.h_null,
                'n_modules': analysis.n_admissible_modules,
                'V_k_koszul': analysis.is_universal_koszul,
                'L_k_koszul': analysis.is_quotient_koszul,
                'shadow_class': analysis.shadow_depth_class,
                'assoc_var_dim': analysis.associated_variety_dim,
                'mechanism': analysis.koszul_mechanism[:80] + '...',
            })

    results.sort(key=lambda x: x['k_float'])
    return results


def print_classification_table(max_q: int = 4, max_k: float = 3.0):
    """Print the classification table."""
    table = classification_table_sl2(max_q, max_k)
    header = (
        f"{'k':>8s}  {'p':>3s}  {'q':>3s}  {'c':>10s}  {'kappa':>8s}  "
        f"{'h_null':>6s}  {'V_k':>4s}  {'L_k':>4s}  {'class':>5s}  {'X':>3s}"
    )
    print(header)
    print("-" * 80)
    for row in table:
        vk = 'YES' if row['V_k_koszul'] else 'NO'
        lk = 'YES' if row['L_k_koszul'] is True else ('NO' if row['L_k_koszul'] is False else '?')
        print(
            f"  {row['k']:>6s}  {row['p']:3d}  {row['q']:3d}  {row['c']:>10s}  "
            f"{row['kappa_km']:>8s}  {row['h_null']:6d}  {vk:>4s}  {lk:>4s}  "
            f"{row['shadow_class']:>5s}  {row['assoc_var_dim']:3d}"
        )


