# =========================================================================

def shadow_depth_admissible_sl2(p: int, q: int) -> Dict:
    """Shadow depth classification for L_k(sl_2) at admissible level.

    The shadow depth of an affine KM algebra depends on the Lie bracket
    structure, not on the level. Affine sl_2 is always class L
    (Lie/tree, r_max = 3): the cubic shadow is the Lie bracket,
    the quartic vanishes by Jacobi, tower terminates at arity 3.

    The simple quotient L_k preserves the shadow depth class because:
    1. The OPE structure constants are the same (quotient only removes
       states, doesn't change the OPE of remaining ones)
    2. The shadow obstruction tower is determined by the OPE structure
       constants, not by the module structure
    3. The Lie bracket [e,f] = h, [h,e] = 2e, [h,f] = -2f is
       level-independent

    HOWEVER: the modular characteristic kappa = 3p/(4q) DOES depend on
    the level. Different admissible levels give different kappa,
    affecting the genus tower F_g = kappa * lambda_g.
    """
    k = Fraction(p, q) - 2
    kappa = kappa_path1_sugawara(p, q)

    return {
        'algebra': f'L_k(sl_2), k = {k}',
        'shadow_depth_class': 'L',
        'r_max': 3,
        'cubic_shadow': 'Lie bracket [,] on sl_2',
        'quartic_shadow': '0 (Jacobi identity)',
        'terminates': True,
        'termination_arity': 3,
        'kappa': kappa,
        'genus_1_obstruction': kappa / 24,
    }


