def bar_complex_universal_sl2(max_bar_degree: int = 4) -> Dict[int, int]:
    """Bar cohomology dimensions for V_k(sl_2) (universal, always Koszul).

    By PBW + Koszulness (prop:pbw-universality):
        H^1(B(V_k(sl_2))) = sl_2 (3-dimensional)
        H^n(B(V_k(sl_2))) = 0 for n != 1

    The Koszul dual is the symmetric algebra Sym(sl_2[-1]).
    """
    result = {}
    for d in range(1, max_bar_degree + 1):
        if d == 1:
            result[d] = 3  # dim(sl_2) = 3
        else:
            result[d] = 0  # Koszul: concentrated in degree 1
    return result


def _bar_chain_dim_arity_d(dim_gen: int, d: int) -> int:
    """Dimension of bar chain group at arity d for dim_gen generators.

    B^d = (s^{-1} A_bar)^{otimes d} with Arnold relations.
    For weight-1 generators (KM case), bar arity d involves d generators
    each of weight 1, total weight d.

    The Arnold quotient at d points on P^1: dim = d! / d = (d-1)!
    But this is the OS algebra dimension. The bar chain group is:
        dim = dim_gen^d * (d-1)! / d! = dim_gen^d / d (overcounting fix)

    Actually for the chiral bar complex on P^1:
    B^d has basis indexed by d-tuples of generators modulo Arnold relations.
    The dimension of the d-th bar chain group (for weight-1 generators) is:
        C(dim_gen + d - 1, d) * correction_factor

    For the CE complex (Koszul bar for Lie):
        dim = C(dim_gen, d) (exterior powers)

    CORRECT for chiral bar of KM algebra (= CE complex by PBW):
        dim B^d = C(dim_gen, d) for the PBW-reduced bar complex.

    For the FULL bar chain group (before quotienting by Arnold):
        dim = dim_gen^d (all d-tuples)

    The PBW spectral sequence collapses at E_2, and the E_1 page
    is the CE complex Lambda^d(g).
    """
    if d < 0 or d > dim_gen:
        return 0
    return comb(dim_gen, d)


