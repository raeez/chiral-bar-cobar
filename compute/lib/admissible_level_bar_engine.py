def vacuum_verma_dims_sl2(max_weight: int) -> List[int]:
    """Dimension of the weight-h space in the vacuum Verma module M_k(Lambda_0).

    The vacuum Verma has PBW basis:
        {e_{-n1} ... h_{-m1} ... f_{-l1} ... |0> : n_i, m_j, l_k >= 1}

    At weight h, the dimension is p_3(h) = number of partitions of h
    into 3 colors (one for each generator e, h, f).

    Generating function: prod_{n>=1} 1/(1-q^n)^3 = sum p_3(h) q^h.
    """
    dims = [0] * (max_weight + 1)
    dims[0] = 1  # vacuum
    # Use the recurrence for colored partitions
    for n in range(1, max_weight + 1):
        for h in range(max_weight, n - 1, -1):
            dims[h] += 3 * dims[h - n]  # 3 colors
            # Correction: 3-colored partitions need the full product formula
    # Actually, let's compute directly via the product formula
    dims_correct = [0] * (max_weight + 1)
    dims_correct[0] = 1
    for n in range(1, max_weight + 1):
        # Each mode e_{-n}, h_{-n}, f_{-n} can be applied any number of times
        # This is like 3 copies of ordinary partitions
        new_dims = dims_correct[:]
        for color in range(3):
            temp = [0] * (max_weight + 1)
            temp[0] = 1
            for m in range(1, max_weight + 1):
                # Add partition of weight n with multiplicity
                pass
        # Simpler: use the explicit product formula numerically
    # Use numpy for the product formula
    coeffs = np.ones(max_weight + 1, dtype=np.int64)
    for n in range(1, max_weight + 1):
        # Multiply by 1/(1-q^n)^3 = (1 + q^n + q^{2n} + ...)^3
        new_coeffs = np.zeros(max_weight + 1, dtype=np.int64)
        for h in range(max_weight + 1):
            for m in range(0, (max_weight - h) // n + 1 if n > 0 else 1):
                # Coefficient of q^{h + m*n} from (1-q^n)^{-1}
                # But we need (1-q^n)^{-3}
                pass
        # Better approach: iterative multiplication
    coeffs = [0] * (max_weight + 1)
    coeffs[0] = 1
    for n in range(1, max_weight + 1):
        # Multiply by 1/(1-q^n)^3
        # First, multiply by 1/(1-q^n) three times
        for _ in range(3):
            for h in range(n, max_weight + 1):
                coeffs[h] += coeffs[h - n]
    return coeffs


def simple_quotient_dims_sl2(p: int, q: int, max_weight: int) -> List[int]:
    """Dimension of the weight-h space in L_k(sl_2) at admissible k = p/q - 2.

    L_k = V_k / N_k where N_k is generated by the first null vector at
    weight h_null = (p-1)*q.

    Below weight h_null, L_k and V_k agree: dim L_k[h] = dim V_k[h].
    At and above h_null, the null ideal subtracts dimensions.

    For a ROUGH estimate (ignoring the detailed null vector structure):
    We compute numerically using the Shapovalov determinant to find
    the null space dimension at each weight.
    """
    verma_dims = vacuum_verma_dims_sl2(max_weight)
    h_null = (p - 1) * q
    k_val = float(Fraction(p, q) - 2)

    quotient_dims = list(verma_dims)
    for h in range(h_null, max_weight + 1):
        n_states, rank, null_dim = shapovalov_null_dimension(k_val, h)
        quotient_dims[h] = rank  # = n_states - null_dim

    return quotient_dims


