@dataclass
class NullVectorBarEffect:
    """Effect of a null vector on the bar complex.

    When we quotient V_k by the ideal generated by a null vector v_null
    at weight h_null:
    1. v_null and all its descendants are removed from V_k
    2. The bar complex B(L_k) = B(V_k/N_k) is NOT simply B(V_k)/B(N_k)
       in general (the bar construction is not right exact)
    3. Instead, the long exact sequence of bar complexes
       0 -> B(N_k) -> B(V_k) -> B(L_k) -> ... gives connecting maps
       that can create new cohomology

    The KEY QUESTION: does the quotient create H^n(B(L_k)) for n > 1?
    If yes, L_k is NOT Koszul.
    """
    h_null: int
    null_dim_at_h_null: int
    bar_relevant: bool  # h_null >= 2 (in bar-relevant range)
    creates_new_cycles: Optional[bool]
    mechanism: str


def null_vector_bar_effect_sl2(p: int, q: int) -> NullVectorBarEffect:
    """Analyze the effect of the first null vector on the bar complex.

    The first null vector lies at weight h_null = (p-1)*q.
    It is bar-relevant iff h_null >= 2.

    For sl_2 (weight-1 generators), bar arity d has weight d.
    The null vector at weight h_null first enters the bar complex
    at arity h_null.

    STRUCTURAL ANALYSIS:
    For the UNIVERSAL algebra V_k, bar cohomology is concentrated
    in degree 1 (CE cohomology of g = sl_2):
        H^0 = 0, H^1 = g, H^2 = 0, H^3 = 0

    The null vector v at weight h_null is an element of V_k[h_null].
    In the bar complex, it enters at bar arity >= h_null (because
    each generator has weight 1).

    Since the CE complex only reaches arity 3 (dim g = 3), and
    h_null >= 2 for all non-trivial admissible levels, the null
    vector can affect the bar complex in two ways:

    (A) If h_null = 2: the null vector is at the SAME weight as
        bar arity 2. It removes a relation from the bar complex,
        potentially creating new H^2.

    (B) If h_null >= 3: the null vector is above bar arity 3
        (max arity for sl_2). In this case, the effect is indirect:
        the null vector removes descendants that could participate
        in higher-weight bar chains. For weight-1 generators,
        bar arity d has weight d, so the null at h >= 4 cannot
        directly affect bar arities 1-3.

    CONCLUSION for sl_2: When h_null >= 4, the null vector is ABOVE
    the bar-relevant range for the CE complex (max arity 3, weight 3).
    The bar cohomology at arities 1-3 is unaffected, and higher arities
    are zero by CE vanishing. So L_k is KOSZUL when h_null >= 4.

    When h_null = 2 or 3, the analysis is more delicate.
    """
    nv = first_null_vector_weight_sl2(p, q)
    h_null = nv.h_null
    k_val = float(Fraction(p, q) - 2)

    # Compute null dimension at h_null
    _, _, null_dim = shapovalov_null_dimension(k_val, min(h_null, 6))

    bar_relevant = (h_null >= 2)

    if h_null >= 4:
        creates_new_cycles = False
        mechanism = (
            f"h_null = {h_null} >= 4: the null vector lies ABOVE the "
            f"maximal bar arity 3 (dim sl_2 = 3). Bar cohomology at "
            f"arities 1-3 is unaffected. Higher arities vanish by CE. "
            f"L_k(sl_2) is Koszul."
        )
    elif h_null == 3:
        creates_new_cycles = None  # needs detailed analysis
        mechanism = (
            f"h_null = 3: null at weight 3 = max bar arity. "
            f"The null vector could potentially create new H^3 by "
            f"removing an element from the kernel of d: B^3 -> B^2. "
            f"Detailed Shapovalov analysis needed."
        )
    elif h_null == 2:
        creates_new_cycles = None
        mechanism = (
            f"h_null = 2: null at weight 2, directly in bar arity 2. "
            f"Could create new H^2 by modifying the CE differential. "
            f"This is the most critical case."
        )
    else:
        creates_new_cycles = False
        mechanism = (
            f"h_null = {h_null} < 2: below bar-relevant range."
        )

    return NullVectorBarEffect(
        h_null=h_null,
        null_dim_at_h_null=null_dim,
        bar_relevant=bar_relevant,
        creates_new_cycles=creates_new_cycles,
        mechanism=mechanism,
    )


