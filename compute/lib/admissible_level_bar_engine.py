def ce_differential_sl2(k_val: float) -> Dict:
    """Compute the CE differential for sl_2 at level k.

    The CE complex Lambda^*(sl_2) has:
        Lambda^0 = C (scalar)
        Lambda^1 = sl_2 (3-dimensional, basis e, h, f)
        Lambda^2 = Lambda^2(sl_2) (3-dimensional, basis e^h, e^f, h^f)
        Lambda^3 = Lambda^3(sl_2) (1-dimensional, basis e^h^f)

    The differential d: Lambda^{d+1} -> Lambda^d is:
        d(x1^...^xd) = sum_{i<j} (-1)^{i+j+1} [xi,xj] ^ x1^...^xd

    For sl_2 with [e,f]=h, [h,e]=2e, [h,f]=-2f:

    d^2: Lambda^2 -> Lambda^1:
        d(e^h) = [e,h] = -2e
        d(e^f) = [e,f] = h
        d(h^f) = [h,f] = -2f

    d^3: Lambda^3 -> Lambda^2:
        d(e^h^f) = [e,h]^f - [e,f]^h + [h,f]^e
                 = -2e^f - h^h + (-2f)^e
                 = -2(e^f) - 0 + 2(e^f) = 0

    Wait: d^3 should give something nonzero. Let me recompute.
    d(e^h^f) = sum_{i<j} (-1)^{i+j+1} [x_i, x_j] ^ (remove x_i, x_j)
    with x_1=e, x_2=h, x_3=f:
        (i,j) = (1,2): (-1)^{1+2+1} [e,h] ^ f = (-1)^4 * (-2e) ^ f = -2(e^f)
        (i,j) = (1,3): (-1)^{1+3+1} [e,f] ^ h = (-1)^5 * h ^ h = 0
                        Wait, [e,f] = h, and h^h = 0 (wedge product), but
                        we need [e,f] ^ (remaining) = h ^ h = 0.

    Hmm, that's not right. Let me be more careful.
    d: Lambda^3 -> Lambda^2
    d(e ^ h ^ f) = [e,h] ^ f - [e,f] ^ h + [h,f] ^ e
                  = (-2e) ^ f  -  h ^ h  +  (-2f) ^ e
                  = -2(e^f) - 0 + (-2)(f^e)
                  = -2(e^f) + 2(e^f)
                  = 0

    So d^3 = 0 on the 1-dim space Lambda^3.

    The CE cohomology of sl_2 (semisimple):
        H^0 = C (trivial rep, but for CE of the current algebra this is 0)
        H^1 = 0 (Whitehead's first lemma)
        H^2 = 0 (Whitehead's second lemma)
        H^3 = C (fundamental class, Poincare duality)

    Wait, this is CE cohomology with TRIVIAL coefficients.
    For the bar complex, we need CE with ADJOINT coefficients:
        H^n(g, g) for the bar of U(g).

    For semisimple g: H^1(g, g) = Der(g)/Inn(g) = 0 (all derivations inner)
                      H^2(g, g) = 0 (Whitehead)

    For the BAR of the CHIRAL algebra hat{g}_k:
    The PBW spectral sequence collapse gives:
        H^n(B(hat{g}_k)) = H^n(CE(g, Sym(g[-1])))

    This reduces to the classical Lie algebra cohomology of g with
    coefficients in Sym(g[-1]). For sl_2 semisimple:
        H^1 = g (the generating space: 3-dimensional)
        H^n = 0 for n >= 2 (Koszulness: PBW spectral sequence collapses)

    KEY POINT: The bar cohomology of the UNIVERSAL algebra is always
    concentrated in degree 1 (this is prop:pbw-universality).
    The simple quotient may differ because the PBW filtration is modified.
    """
    # d^1: Lambda^1 -> Lambda^0 = 0 (no degree-0 terms for reduced CE)
    # d^2: Lambda^2 -> Lambda^1
    # Basis of Lambda^1: {e, h, f} (indices 0, 1, 2)
    # Basis of Lambda^2: {e^h, e^f, h^f} (indices (0,1), (0,2), (1,2))

    # d(e^h) = [e,h] = -[h,e] = -2e
    # d(e^f) = [e,f] = h
    # d(h^f) = [h,f] = -2f

    # Matrix d^2 (3x3): rows = Lambda^1 basis, cols = Lambda^2 basis
    d2 = np.array([
        [-2, 0, 0],    # e coefficient of d(e^h), d(e^f), d(h^f)
        [0, 1, 0],     # h coefficient
        [0, 0, -2],    # f coefficient
    ], dtype=float)

    # d^3: Lambda^3 -> Lambda^2
    # d(e^h^f) = [e,h]^f - [e,f]^h + [h,f]^e
    #          = (-2e)^f - h^h + (-2f)^e
    #          = -2(e^f) - 0 + 2(e^f) = 0
    d3 = np.array([[0], [0], [0]], dtype=float)  # 3x1

    # Now compute ranks and cohomology
    rank_d2 = np.linalg.matrix_rank(d2, tol=1e-10)
    rank_d3 = np.linalg.matrix_rank(d3, tol=1e-10)

    # H^1 = ker(d1) / im(d2). Since d1=0, ker(d1) = Lambda^1 (dim 3).
    # im(d2) = rank(d2) = 3 (d2 is invertible when k != 0)
    # Wait, d2 doesn't depend on k! The CE differential uses the
    # Lie bracket, which is the simple-pole coefficient.
    # For sl_2: [e,f] = h, [h,e] = 2e, [h,f] = -2f.
    # These DON'T depend on k (k appears only in double poles).

    # The CE complex is:
    # 0 -> Lambda^3 --d3--> Lambda^2 --d2--> Lambda^1 --d1--> Lambda^0 -> 0
    # where d1 = 0 for the REDUCED CE complex.

    # H^1(CE) = Lambda^1 / im(d2) = 3 - rank(d2)
    # But this is for trivial-coefficient CE.

    # For the BAR complex of the universal algebra V_k(sl_2):
    # H^1(B(V_k)) = sl_2 (dim 3), H^n = 0 for n != 1.

    return {
        'd2_matrix': d2,
        'd3_matrix': d3,
        'rank_d2': rank_d2,
        'rank_d3': rank_d3,
        'dims': {0: 1, 1: 3, 2: 3, 3: 1},
        'h1': 3 - rank_d2 + rank_d3,  # formal; need proper computation
        'h2': 3 - rank_d2 - rank_d3,
        'h3': 1 - rank_d3,
    }


# =========================================================================
# Admissible bar complex: Koszulness analysis
# =========================================================================

@dataclass(frozen=True)
class AdmissibleBarAnalysis:
    """Complete analysis of the bar complex at an admissible level."""
    p: int
    q: int
    k: Fraction
    c: Fraction
    kappa_km: Fraction
    kappa_vir: Fraction  # kappa of the DS-reduced Virasoro model
    h_null: int
    n_admissible_modules: int
    shadow_depth_class: str  # G, L, C, or M
    is_universal_koszul: bool  # V_k is always Koszul
    is_quotient_koszul: Optional[bool]  # L_k Koszulness
    koszul_mechanism: str
    associated_variety_dim: int
    bar_ext_vs_ordinary_ext: str  # description of the comparison
    null_effect: NullVectorBarEffect


def admissible_bar_analysis_sl2(p: int, q: int) -> AdmissibleBarAnalysis:
    """Comprehensive bar complex analysis for L_k(sl_2) at admissible level.

    This is the main entry point. It computes:
    1. Level, central charge, kappa (three paths)
    2. Null vector data and effect on bar complex
    3. Shadow depth classification
    4. Koszulness verdict
    5. Associated variety data
    6. Bar-Ext vs ordinary Ext comparison
    """
    if gcd(p, q) != 1:
        raise ValueError(f"Need gcd(p,q) = 1, got gcd({p},{q}) = {gcd(p, q)}")
    if p < 2:
        raise ValueError(f"Need p >= 2, got {p}")

    k = Fraction(p, q) - 2
    c = 3 * k / (k + 2)
    kappa_km = kappa_path1_sugawara(p, q)
    kappa_vir = kappa_path3_ds_reduction(p, q)
    h_null = (p - 1) * q
    n_adm = (p - 1) * q

    # Shadow depth for the affine sl_2 algebra:
    # Affine KM algebras have shadow depth class L (Lie/tree, r_max=3)
    # because the cubic shadow is the Lie bracket and the quartic
    # vanishes by the Jacobi identity.
    # The SIMPLE QUOTIENT L_k preserves this classification.
    shadow_depth = 'L'

    # Null vector effect
    null_eff = null_vector_bar_effect_sl2(p, q)

    # Koszulness analysis
    # V_k is ALWAYS Koszul
    is_universal_koszul = True

    # For the simple quotient L_k:
    # Case 1: Integrable levels (q=1) — well-understood, Koszul
    # Case 2: h_null >= 4 — null above bar-relevant range, Koszul
    # Case 3: h_null = 2 or 3 — needs detailed analysis

    if q == 1:
        # Integrable level
        is_quotient_koszul = True
        mechanism = (
            f"Integrable level k = {p-2}: L_k is the WZW model with "
            f"finitely many simple modules. The bar complex is standard "
            f"CE (same as V_k at arities 1-3). Koszul."
        )
    elif h_null >= 4:
        # Null vector above bar-relevant range
        is_quotient_koszul = True
        mechanism = (
            f"h_null = {h_null} >= 4 > dim(sl_2) = 3 = max bar arity. "
            f"The null vector cannot affect bar cohomology at arities 1-3 "
            f"(weight-1 generators, arity d has weight d). Bar concentration "
            f"in degree 1 persists. L_k is Koszul."
        )
    elif h_null == 3:
        # Null at weight 3 = max bar arity
        # For sl_2 at k = -4/3 (p=2, q=3): h_null = 3
        # The null vector lies in Lambda^3(sl_2) (1-dimensional).
        # The CE differential d: Lambda^3 -> Lambda^2 is already 0
        # (computed above). Quotienting by a null at weight 3
        # removes the generator of Lambda^3, but since d = 0 on Lambda^3,
        # this removes a class from H^3 — making H^3 smaller, not larger.
        # So Koszulness is PRESERVED (or even improved).
        is_quotient_koszul = True
        mechanism = (
            f"h_null = 3 = max bar arity. The null at weight 3 lies in "
            f"Lambda^3(sl_2) (1-dim). Since d^3 = 0, the null vector "
            f"is a CE 3-cocycle. Quotienting removes it from H^3, which "
            f"was already 0 for the bar of V_k (Koszulness). The quotient "
            f"cannot CREATE new cohomology at lower degrees. L_k is Koszul."
        )
    elif h_null == 2:
        # Null at weight 2 = bar arity 2
        # For sl_2 at k = 0 (p=2, q=1 — integrable, handled above) or
        # hypothetical boundary cases.
        # At weight 2, the null is in Lambda^2(sl_2) (3-dimensional).
        # The CE differential d: Lambda^2 -> Lambda^1 has rank 3 (full rank,
        # since sl_2 is semisimple). A null vector at weight 2 removes
        # an element from Lambda^2, reducing the domain of d^2.
        # If the removed element was in im(d^3) or contributed to ker(d^2)/im(d^3),
        # this could change H^2. But im(d^3) = 0 and ker(d^2) is computed
        # from the rank of d^2.
        # With one element removed from the 3-dim Lambda^2, the new d^2
        # acts on a 2-dim space. Its rank is at most 2.
        # New H^1 = 3 - rank(new d^2) >= 3 - 2 = 1.
        # But H^2 = dim(new Lambda^2) - rank(new d^2) = 2 - rank(new d^2).
        # If rank = 2: H^2 = 0 (still Koszul).
        # If rank < 2: H^2 > 0 (FAILURE).
        # This depends on WHICH null vector is removed.
        is_quotient_koszul = None  # detailed analysis needed
        mechanism = (
            f"h_null = 2: null at weight 2 = bar arity 2. Detailed "
            f"analysis of which null vector is removed from Lambda^2(sl_2) "
            f"is needed to determine if Koszulness survives."
        )
    else:
        is_quotient_koszul = True
        mechanism = f"h_null = {h_null}: trivial or below bar-relevant range."

    # Associated variety
    # Arakawa's theorem: for non-critical admissible L_k(sl_2),
    # the associated variety X_{L_k} = Spec(R_{L_k}) where R_{L_k} is
    # the Zhu C_2 algebra quotient.
    # For admissible sl_2: X_{L_k} = {0} (reduced = point) by C_2 cofiniteness.
    # dim X = 0 for all admissible levels.
    # This is a CONSEQUENCE of rationality (Arakawa 2017).
    assoc_var_dim = 0

    # Bar-Ext vs ordinary Ext comparison
    if q == 1:
        ext_comparison = (
            "Integrable level: ordinary module category is semisimple (MTC). "
            "Ext^i_{ord} = 0 for i >= 1. "
            "Bar-Ext = CE cohomology = 0 for i >= 2 (Koszul). "
            "AGREE: both vanish at i >= 2."
        )
    elif is_quotient_koszul is True:
        ext_comparison = (
            f"Admissible level k = {k}: ordinary module category is "
            f"non-semisimple (logarithmic extensions exist). "
            f"Ext^1_{{ord}} != 0 (non-split extensions between admissible modules). "
            f"Bar-Ext = 0 for i >= 2 (Koszul despite non-semisimplicity). "
            f"The bar-Ext and ordinary Ext DISAGREE at degree 1: "
            f"bar-Ext^1 = sl_2 (3-dim, independent of modules), while "
            f"Ext^1_{{ord}}(L_i, L_j) depends on the specific module pair."
        )
    else:
        ext_comparison = (
            f"Admissible level k = {k}: status OPEN. "
            f"Bar-Ext may disagree with ordinary Ext at degree >= 2."
        )

    return AdmissibleBarAnalysis(
        p=p, q=q, k=k, c=c,
        kappa_km=kappa_km,
        kappa_vir=kappa_vir,
        h_null=h_null,
        n_admissible_modules=n_adm,
        shadow_depth_class=shadow_depth,
        is_universal_koszul=is_universal_koszul,
        is_quotient_koszul=is_quotient_koszul,
        koszul_mechanism=mechanism,
        associated_variety_dim=assoc_var_dim,
        bar_ext_vs_ordinary_ext=ext_comparison,
        null_effect=null_eff,
    )


