def irrep_dimension(lam: Tuple[int, ...]) -> int:
    """Dimension of the S_n irrep indexed by partition lam.

    Equals the character value at the identity class (1^n), which is the
    number of standard Young tableaux of shape lam (Frame-Robinson-Thrall
    hook-length formula).
    """
    n = sum(lam)
    if n == 0:
        return 1
    table = CHARACTER_TABLES.get(n)
    if table is not None:
        id_class = tuple([1] * n)
        return table[(lam, id_class)]
    # Fall back to hook-length formula
    return _hook_length_dimension(lam)


def _hook_length_dimension(lam: Tuple[int, ...]) -> int:
    """Frame-Robinson-Thrall hook length formula."""
    if not lam:
        return 1
    n = sum(lam)
    rows = len(lam)
    # Build Young diagram as list of column heights
    col_heights = [sum(1 for r in lam if r >= c + 1)
                   for c in range(lam[0])]
    hook_product = 1
    for i in range(rows):
        for j in range(lam[i]):
            arm = lam[i] - j - 1
            leg = col_heights[j] - i - 1
            hook = arm + leg + 1
            hook_product *= hook
    return factorial(n) // hook_product


# ============================================================
# Schur functor dimensions via SSYT counting
# ============================================================

def schur_functor_dim(lam: Tuple[int, ...], d: int) -> int:
    """dim S^lam(V) where dim V = d.

    Equals the number of semi-standard Young tableaux of shape lam with
    entries in {1, ..., d}. Uses the Weyl dimension formula for GL_d:

        dim S^lam(V) = prod_{i<j} (lam_i - lam_j + j - i) / (j - i)

    where lam is padded with zeros to length d.
    """
    if len(lam) > d:
        return 0
    # Pad lam to length d
    padded = list(lam) + [0] * (d - len(lam))
    from fractions import Fraction as F
    num = F(1)
    den = F(1)
    for i in range(d):
        for j in range(i + 1, d):
            num *= (padded[i] - padded[j] + (j - i))
            den *= (j - i)
    result = num / den
    assert result.denominator == 1, f"Non-integer Schur dim for {lam}, d={d}"
    return int(result)


