# The main engine
# ============================================================

class SnBarIrrepEngine:
    """S_n action on the bar complex and irreducible decomposition.

    Supports two algebra families:

    1. sl_2 "regular" generator space: V_sl2 = span{e, f, h}, dim 3.
       This is the weight-1 generator space of affine sl_2 at conformal
       weight 1. S_n acts without Koszul signs (desuspended degree 0).
       Schur-Weyl duality applies: V^{tensor n} = oplus_{lam} S^lam(V) (x) V_lam.

    2. Virasoro weight-2 generator: V_vir = span{T}, dim 1.
       A single generator T with desuspended degree 1. The tensor power
       T^{tensor n} is one-dimensional at each n. S_n acts by SIGN on this
       line (because each transposition of two degree-1 elements picks up
       (-1)^{1*1} = -1).

    For ARITY-4 Virasoro with multiple modes (L_{-2}, L_{-3}, L_{-4}, ...),
    we have a non-trivial generator space and the tensor decomposition
    becomes interesting. We support this via the "virasoro_modes" algebra
    type which enumerates Vir_- generators up to a given weight.
    """

    def __init__(self, algebra_type: str = 'sl2', max_weight: int = 6):
        self.algebra_type = algebra_type
        self.max_weight = max_weight

        if algebra_type == 'sl2':
            # V_sl2 = span{e, f, h}, all weight 1, desuspended degree 0
            self.generators = [('e', 1, 0), ('f', 1, 0), ('h', 1, 0)]
            self.dim_v = 3
            self.generator_weight = 1
            self.desuspended_degree = 0
            self._weight_of_basis_vector = [1, 1, 1]
        elif algebra_type == 'sl3':
            # V_sl3 has dim 8
            self.generators = [(f'e{i}', 1, 0) for i in range(8)]
            self.dim_v = 8
            self.generator_weight = 1
            self.desuspended_degree = 0
            self._weight_of_basis_vector = [1] * 8
        elif algebra_type == 'virasoro_single':
            # Single generator T of weight 2, desuspended degree 1
            self.generators = [('T', 2, 1)]
            self.dim_v = 1
            self.generator_weight = 2
            self.desuspended_degree = 1
            self._weight_of_basis_vector = [2]
        elif algebra_type == 'virasoro_modes':
            # Multiple Virasoro modes: L_{-2}, L_{-3}, ..., L_{-max_weight}
            # Each L_{-n} has weight n and desuspended degree 1
            self.generators = [(f'L_-{n}', n, 1)
                               for n in range(2, max_weight + 1)]
            self.dim_v = len(self.generators)
            self.generator_weight = None  # mixed
            self.desuspended_degree = 1
            self._weight_of_basis_vector = [g[1] for g in self.generators]
        else:
            raise ValueError(f"Unknown algebra: {algebra_type}")

        self._basis_cache: Dict[Tuple[int, int], List[Tuple[int, ...]]] = {}
        self._action_cache: Dict[Tuple[int, int, Tuple[int, ...]], np.ndarray] = {}

    # ----------------------------------------------------------
    # Basis of V^{tensor n} at fixed total weight
    # ----------------------------------------------------------

    def tensor_basis(self, n: int, weight: Optional[int] = None
                     ) -> List[Tuple[int, ...]]:
        """Basis of V^{tensor n} (or its weight-h subspace).

        Returns list of n-tuples (i_1, ..., i_n) of basis indices 0,..,d-1.
        Repetitions are ALLOWED (full tensor power).

        If weight is None, returns all n-tuples (dimension = d^n).
        Otherwise, filters by sum of basis vector weights = weight.
        """
        key = (n, weight if weight is not None else -1)
        if key in self._basis_cache:
            return self._basis_cache[key]

        out: List[Tuple[int, ...]] = []
        self._enum_tensor(n, weight, [], out)
        self._basis_cache[key] = out
        return out

    def _enum_tensor(self, remaining: int, remaining_weight: Optional[int],
                     current: List[int], out: List[Tuple[int, ...]]):
        if remaining == 0:
            if remaining_weight is None or remaining_weight == 0:
                out.append(tuple(current))
            return
        if remaining_weight is not None and remaining_weight < remaining:
            return  # not enough weight budget
        for i in range(self.dim_v):
            w = self._weight_of_basis_vector[i]
            if remaining_weight is not None and w > remaining_weight:
                continue
            current.append(i)
            self._enum_tensor(
                remaining - 1,
                None if remaining_weight is None else remaining_weight - w,
                current, out)
            current.pop()

    def tensor_dim(self, n: int, weight: Optional[int] = None) -> int:
        return len(self.tensor_basis(n, weight))

    # ----------------------------------------------------------
    # S_n action matrix (with Koszul signs)
    # ----------------------------------------------------------

    def koszul_sign_of_perm(self, sigma: Tuple[int, ...],
                            tuple_degs: Sequence[int]) -> int:
        """Koszul sign of sigma acting on a tuple with given desuspended
        degrees tuple_degs. Returns +1 or -1.

        Formula: for each inversion (i, j) (i < j, sigma(i) > sigma(j)),
        multiply by (-1)^{deg_i * deg_j} where the degrees are of the
        ELEMENTS AT POSITIONS being swapped.
        """
        n = len(sigma)
        sign = 1
        for i in range(n):
            for j in range(i + 1, n):
                if sigma[i] > sigma[j]:
                    # Inversion
                    if (tuple_degs[i] * tuple_degs[j]) % 2 == 1:
                        sign *= -1
        return sign

    def apply_perm(self, sigma: Tuple[int, ...], basis_vec: Tuple[int, ...]
                   ) -> Tuple[int, Tuple[int, ...]]:
        """Apply permutation sigma to basis vector.

        Convention: sigma acts by rearranging POSITIONS, specifically
        (sigma . v)_i = v_{sigma^{-1}(i)}.

        Returns (koszul_sign, new_tuple).
        """
        n = len(sigma)
        # new[i] = old[sigma^{-1}(i)]
        inv = [0] * n
        for i in range(n):
            inv[sigma[i]] = i
        new_tuple = tuple(basis_vec[inv[i]] for i in range(n))
        # Koszul sign: depends on degrees of elements in basis_vec at ORIGINAL
        # positions, computed via number of inversions weighted by deg products.
        tuple_degs = [self.generators[basis_vec[k]][2] for k in range(n)]
        sign = self.koszul_sign_of_perm(sigma, tuple_degs)
        return sign, new_tuple

    def sn_action_matrix(self, n: int, sigma: Tuple[int, ...],
                         weight: Optional[int] = None) -> np.ndarray:
        """Matrix of sigma in S_n acting on V^{tensor n} (weight-filtered).

        Basis: self.tensor_basis(n, weight). Matrix is (d_basis, d_basis).
        """
        cache_key = (n, weight if weight is not None else -1, sigma)
        if cache_key in self._action_cache:
            return self._action_cache[cache_key]

        basis = self.tensor_basis(n, weight)
        size = len(basis)
        basis_idx = {b: i for i, b in enumerate(basis)}
        mat = _frac_array((size, size))
        for col, b in enumerate(basis):
            sign, new_b = self.apply_perm(sigma, b)
            row = basis_idx.get(new_b)
            if row is not None:
                mat[row, col] += Fraction(sign)
        self._action_cache[cache_key] = mat
        return mat

    # ----------------------------------------------------------
    # Character computation
    # ----------------------------------------------------------

    def character(self, n: int, weight: Optional[int] = None
                  ) -> Dict[Tuple[int, ...], Fraction]:
        """Character of the S_n representation on V^{tensor n} (weight-h part).

        Returns dict {cycle_type: trace}.
        """
        if n == 0:
            return {(): Fraction(1)}
        if n > 5:
            raise ValueError("character only implemented for n <= 5")

        basis = self.tensor_basis(n, weight)
        if not basis:
            # Return zero character for every class
            result: Dict[Tuple[int, ...], Fraction] = {}
            for ct in set(cycle_type(p) for p in permutations(range(n))):
                result[ct] = Fraction(0)
            return result

        # Group permutations by cycle type, compute trace once per class
        result: Dict[Tuple[int, ...], Fraction] = {}
        seen_types: Dict[Tuple[int, ...], Tuple[int, ...]] = {}
        for sigma in permutations(range(n)):
            ct = cycle_type(sigma)
            if ct in seen_types:
                continue
            seen_types[ct] = sigma
            mat = self.sn_action_matrix(n, sigma, weight)
            tr = Fraction(0)
            for i in range(len(basis)):
                tr += mat[i, i]
            result[ct] = tr
        return result

    # ----------------------------------------------------------
    # Irrep decomposition via character inner product
    # ----------------------------------------------------------

    def irrep_decomposition(self, n: int, weight: Optional[int] = None
                            ) -> Dict[Tuple[int, ...], int]:
        """Decompose V^{tensor n} (weight-h part) into S_n irreps.

        Returns {partition: multiplicity}.

        Formula:
            m_lam = (1/|S_n|) sum_{sigma} chi_lam(sigma) * chi_V(sigma)
                  = (1/n!) sum_{classes C} |C| * chi_lam(C) * chi_V(C)

        Uses the cached character tables for n <= 5.
        """
        if n == 0:
            return {(): 1}
        char = self.character(n, weight)
        table = CHARACTER_TABLES[n]
        all_parts = partitions(n)
        order = factorial(n)
        result: Dict[Tuple[int, ...], int] = {}
        for lam in all_parts:
            mult = Fraction(0)
            for cls in all_parts:
                cls_size = partition_class_size(cls, n)
                chi_lam = Fraction(table[(lam, cls)])
                chi_v = char.get(cls, Fraction(0))
                mult += Fraction(cls_size) * chi_lam * chi_v
            mult /= Fraction(order)
            assert mult.denominator == 1, (
                f"Non-integer multiplicity {mult} for irrep {lam} at n={n}, "
                f"weight={weight}"
            )
            m_int = int(mult)
            if m_int != 0:
                result[lam] = m_int
        return result

    # ----------------------------------------------------------
    # Trivial and sign parts
    # ----------------------------------------------------------

    def trivial_part_dim(self, n: int, weight: Optional[int] = None) -> int:
        """dim of trivial isotypic = dim Sym^n(V)_weight (for deg-0 generators)
        or dim Lambda^n(V)_weight (for deg-1 generators).

        For degree-0 generators: the trivial irrep is the SYMMETRIC tensors.
        For degree-1 generators: the trivial irrep is obtained by accounting
        for Koszul signs — the "trivial" character tr_id = dim, tr_transp = +dim
        corresponds to the sign isotypic of the ungraded action.
        """
        decomp = self.irrep_decomposition(n, weight)
        # Irrep = (n,) has character constantly 1 on every class
        return decomp.get((n,), 0) * irrep_dimension((n,))

    def sign_part_dim(self, n: int, weight: Optional[int] = None) -> int:
        """dim of sign isotypic = dim Lambda^n(V) (for deg-0) or Sym^n(V) (deg-1)."""
        decomp = self.irrep_decomposition(n, weight)
        sign_part = tuple([1] * n)
        return decomp.get(sign_part, 0) * irrep_dimension(sign_part)

    def isotypic_dim(self, n: int, lam: Tuple[int, ...],
                     weight: Optional[int] = None) -> int:
        """Dimension of the lam-isotypic component.

        dim(lam-isotypic) = multiplicity * dim(irrep).
        """
        decomp = self.irrep_decomposition(n, weight)
        return decomp.get(lam, 0) * irrep_dimension(lam)

    # ----------------------------------------------------------
    # Sanity-check identities
    # ----------------------------------------------------------

    def verify_decomposition_dimension(self, n: int,
                                        weight: Optional[int] = None) -> bool:
        """Check that sum_lam mult(lam) * dim(V_lam) = dim V^{tensor n}.

        This is the decomposition axiom for a finite group rep.
        """
        decomp = self.irrep_decomposition(n, weight)
        total = sum(m * irrep_dimension(lam) for lam, m in decomp.items())
        return total == self.tensor_dim(n, weight)

    def schur_weyl_consistency(self, n: int) -> bool:
        """For degree-0 generators (sl_2, sl_3), verify Schur-Weyl duality:

            mult(lam) in V^{tensor n} == dim S^lam(V)

        where dim S^lam(V) is computed via the Weyl/SSYT formula.
        """
        if self.desuspended_degree != 0:
            return True  # Schur-Weyl needs ungraded action
        decomp = self.irrep_decomposition(n)  # no weight filter
        for lam in partitions(n):
            sw = schur_functor_dim(lam, self.dim_v)
            got = decomp.get(lam, 0)
            if sw != got:
                return False
        return True

    # ----------------------------------------------------------
    # Cyclic subgroup decomposition
    # ----------------------------------------------------------

    def cyclic_character(self, n: int,
                         weight: Optional[int] = None) -> List[Fraction]:
        """Trace of c_n^k for k = 0, 1, ..., n-1 on V^{tensor n}_weight.

        Here c_n is the standard n-cycle (1 2 ... n), i.e. 0->1->2->...->n-1->0
        as a 0-indexed permutation.

        The character at c_n^k has a closed form: fix generators i_1,...,i_n
        with i_{j+k mod n} = i_j for all j. This forces the sequence to be
        periodic with period d = gcd(n, k), giving (when weight is not
        restricted) dim V^{gcd(n,k)} fixed points. With weight restriction,
        we count periodic-weight sequences.
        """
        traces: List[Fraction] = []
        for k in range(n):
            sigma = tuple((i + k) % n for i in range(n))
            mat = self.sn_action_matrix(n, sigma, weight)
            tr = Fraction(0)
            for i in range(len(mat)):
                tr += mat[i, i]
            traces.append(tr)
        return traces

    def cyclic_invariant_dim(self, n: int,
                             weight: Optional[int] = None) -> Fraction:
        """Dimension of the C_n-invariant subspace.

        By Burnside: (1/n) * sum_{k=0}^{n-1} tr(c_n^k).
        """
        traces = self.cyclic_character(n, weight)
        return sum(traces, Fraction(0)) / Fraction(n)

    def cyclic_eigenspace_dims(self, n: int,
                                weight: Optional[int] = None
                                ) -> Dict[int, Fraction]:
        """Dimensions of the eigenspaces of c_n.

        The k-th eigenspace has eigenvalue zeta_n^k where zeta_n = exp(2 pi i / n).
        Dimension formula (Fourier on C_n):

            dim E_k = (1/n) sum_{j=0}^{n-1} zeta_n^{-jk} tr(c_n^j).

        Since characters of cyclic groups are roots of unity and the traces
        are integers, the eigenspace dims are non-negative rationals. We
        return Fractions — but for reality, they should all be integers.

        We compute using a symbolic root-of-unity expansion and return
        Fraction values. When n divides the phase j*k, zeta contributions
        are real.
        """
        traces = self.cyclic_character(n, weight)

        # For signed Fourier: multiply traces by zeta^{-jk} and sum.
        # Since the result is a non-negative integer, we compute symbolically
        # using Chebyshev-like identities: we collect coefficients as
        # sum_j tr_j * zeta^{-jk}, then simplify.
        # Since traces are rational and we want integer answers, we use
        # numpy complex arithmetic and round.
        result: Dict[int, Fraction] = {}
        tr_float = [float(t) for t in traces]
        for k in range(n):
            val = 0.0 + 0.0j
            for j in range(n):
                phase = -2j * np.pi * j * k / n
                val += tr_float[j] * np.exp(phase)
            val /= n
            # Should be a real non-negative integer
            assert abs(val.imag) < 1e-8, f"Non-real cyclic eigenspace dim: {val}"
            int_val = round(val.real)
            assert abs(val.real - int_val) < 1e-8, (
                f"Non-integer cyclic eigenspace dim: {val.real}"
            )
            result[k] = Fraction(int_val)
        return result

    # ----------------------------------------------------------
    # Frobenius characteristic expansion
    # ----------------------------------------------------------

    def frobenius_characteristic(self, n: int,
                                  weight: Optional[int] = None
                                  ) -> Dict[Tuple[int, ...], int]:
        """Frobenius characteristic of V^{tensor n}_weight as a Sym-function.

        ch(V^{tensor n}) = sum_lam mult(lam) * s_lam

        where mult(lam) is the S_n-irrep multiplicity and s_lam the Schur
        function. We return the multiplicity dict — the correspondence
        lam <-> s_lam is canonical.
        """
        return self.irrep_decomposition(n, weight)

    def schur_coefficients(self, max_n: int, weight: Optional[int] = None
                           ) -> Dict[int, Dict[Tuple[int, ...], int]]:
        """Full Schur expansion of the generating series

            Sum_n ch(V^{tensor n}) q^n = Sum_n Sum_lam mult_n(lam) s_lam q^n

        Returns dict n -> {partition: coefficient}.
        """
        out: Dict[int, Dict[Tuple[int, ...], int]] = {}
        for n in range(max_n + 1):
            if n == 0:
                out[0] = {(): 1}
                continue
            if n > 5:
                break
            out[n] = self.irrep_decomposition(n, weight)
        return out

    # ----------------------------------------------------------
    # Ordered-vs-unordered ratio as a Schur consequence
    # ----------------------------------------------------------

    def ordered_unordered_identity(self, n: int) -> Dict[str, int]:
        """Check the 'ordered vs unordered' combinatorial identity for V^{tensor n}.

        For a vector space V of dimension d:
          - dim V^{tensor n}   = d^n           (ordered)
          - dim Sym^n V        = C(d+n-1, n)   (unordered/symmetric)
          - dim Lambda^n V     = C(d, n)       (unordered/antisymmetric)
          - Orbit counting:    d^n = sum_{lam} (dim V_lam) * (dim S^lam V)
                                     [Schur-Weyl]

        Returns dict with each component.
        """
        if self.desuspended_degree != 0:
            return {}  # this identity is for ungraded
        full = self.dim_v ** n
        sym = comb(self.dim_v + n - 1, n)
        ext = comb(self.dim_v, n)
        schur_weyl = sum(irrep_dimension(lam) * schur_functor_dim(lam, self.dim_v)
                         for lam in partitions(n)
                         if len(lam) <= self.dim_v)
        return {
            'tensor_dim': full,
            'sym_dim': sym,
            'ext_dim': ext,
            'schur_weyl_sum': schur_weyl,
        }

    # ----------------------------------------------------------
    # Differential equivariance check
    # ----------------------------------------------------------

    def sn_equivariance_defect(self, n: int, weight: int
                                ) -> Dict[str, Fraction]:
        """Measure the defect of S_n equivariance of the bar differential.

        We don't construct the differential here, but we expose the symbolic
        content: the bar differential is a sum of local contractions
        d_i: V^{tensor n} -> V^{tensor n-1} at positions i = 1, ..., n-1.
        The full differential d = sum_i (-1)^{i-1} d_i is S_n-equivariant only
        up to the sign representation — precisely because d_i involves the
        bracket m_2(v_i, v_{i+1}) which is skew.

        Concretely: for a transposition tau_j = (j, j+1), tau_j o d_i = d_i'
        o tau_j where d_i' is the SAME contraction but with an additional
        sign coming from m_2(tau_j(v_i), tau_j(v_{i+1})) = -m_2(v_i, v_{i+1}).

        We return the theoretical equivariance characterization:
        - 'number_of_contractions': n - 1
        - 'relative_sign_per_transposition': -1 (from skew bracket)
        - 'induced_cohomology_action': 'symmetric' if desusp_deg = 1 else 'alternating'
        """
        return {
            'n': n,
            'n_contractions': n - 1,
            'skew_sign': -1,
            'desuspension_degree': self.desuspended_degree,
            'induced_action_on_cohomology': (
                'sign-twisted' if self.desuspended_degree == 0
                else 'trivial-twisted'
            ),
        }


