def schur_weyl_sl2(n: int) -> Dict[Tuple[int, ...], int]:
    """Shortcut: S_n decomposition of V_sl2^{tensor n}, V_sl2 = span{e,f,h}."""
    eng = SnBarIrrepEngine('sl2')
    return eng.irrep_decomposition(n)


def virasoro_arity_4_decomposition(max_weight: int = 10
                                    ) -> Dict[int, Dict[Tuple[int, ...], int]]:
    """Decompose V_vir_modes^{tensor 4} at each total weight <= max_weight.

    V_vir_modes has basis {L_{-2}, L_{-3}, ..., L_{-max_weight}}, each with
    desuspended degree 1. The S_4 action includes Koszul signs from the
    graded permutation action.
    """
    eng = SnBarIrrepEngine('virasoro_modes', max_weight=max_weight)
    out: Dict[int, Dict[Tuple[int, ...], int]] = {}
    for h in range(8, max_weight + 1):  # minimum 2*4 = 8
        decomp = eng.irrep_decomposition(4, weight=h)
        if decomp:
            out[h] = decomp
    return out


def cyclic_decomposition_sl2(n: int) -> Dict[int, Fraction]:
    """Cyclic subgroup C_n eigenspace decomposition for V_sl2^{tensor n}."""
    eng = SnBarIrrepEngine('sl2')
    return eng.cyclic_eigenspace_dims(n)


