# Each table is a dict: {(irrep_partition, class_partition) -> character_value}
# Rows = irreps indexed by partition of n
# Cols = conjugacy classes indexed by partition of n (cycle type)

CHARACTER_TABLES: Dict[int, Dict[Tuple[Tuple[int, ...], Tuple[int, ...]], int]] = {}


def _register_character_table(n: int, rows: List[Tuple[int, ...]],
                               cols: List[Tuple[int, ...]],
                               table: List[List[int]]):
    entry: Dict[Tuple[Tuple[int, ...], Tuple[int, ...]], int] = {}
    for i, row in enumerate(rows):
        for j, col in enumerate(cols):
            entry[(row, col)] = table[i][j]
    CHARACTER_TABLES[n] = entry


