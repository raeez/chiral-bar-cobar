r"""S_n action on the bar complex B^n(A) = V^{\otimes n} and irreducible decomposition.

MATHEMATICAL FRAMEWORK
======================

The bar complex in degree n carries a natural S_n action permuting tensor
factors:

    sigma . (v_1 \otimes ... \otimes v_n) = \epsilon(sigma; v_1,...,v_n) *
                                              v_{sigma^{-1}(1)} \otimes ... \otimes v_{sigma^{-1}(n)}

where the Koszul sign depends on the desuspended degrees |s^{-1}v_i| = |v_i| - 1
(AP45 convention). For weight-1 generators (all generators of sl_2, affine Kac-
Moody loop algebras at conformal weight 1), the desuspended degree is 0, so
the action is the STRICT permutation action with no Koszul signs. For
weight-2 generators (Virasoro T, Vir-like primary fields of weight 2) the
desuspended degree is 1 and transpositions carry an extra (-1) sign.

EQUIVARIANCE OF THE DIFFERENTIAL
================================

The bar differential d_bar is NOT S_n-equivariant in general: writing a
transposition \tau_{i,i+1} at position i, the differential contracts adjacent
pairs via the bracket

    d_{i} (v_1 | ... | v_n) = \pm v_1 | ... | m_2(v_i, v_{i+1}) | ... | v_n,

and the bracket m_2 of a non-commutative (dg Lie) structure is NOT symmetric
in its inputs: m_2(a, b) = -(-1)^{|a||b|} m_2(b, a). The differential thus
intertwines the S_n action only up to the sign representation — precisely
the source of the Orlik-Solomon relations on the bar complex.

The Arnold relations on the cohomology H^*(B(A)) assemble the bar cohomology
into a NATURAL S_n representation: the induced action factors through the
antisymmetrization (on desuspended-degree-0 generators) or through the
symmetrization (on desuspended-degree-1 generators) of the full tensor
power.

FROBENIUS CHARACTERISTIC
========================

For each n, the graded dimension of the free V-generated tensor power
admits a Frobenius characteristic:

    ch(V^{\otimes n}) = \sum_{\lambda \vdash n} m_\lambda(V) s_\lambda

where m_\lambda(V) is the multiplicity of the Schur functor S^\lambda(V)
and s_\lambda is the Schur function. By Schur-Weyl duality, for V an
ordinary vector space of dimension d:

    dim S^\lambda(V) = s_\lambda(1, 1, ..., 1)  [d ones]

so for d=3 (sl_2 regular, weight 1), the decomposition of V^{\otimes n} into
irreps of S_n is given by the Schur-Weyl duality:

    V^{\otimes n} = \bigoplus_{\lambda \vdash n, \ell(\lambda) \le d} S^\lambda(V) \otimes V_\lambda

with V_\lambda the irreducible S_n-representation indexed by \lambda.
The multiplicity of V_\lambda is dim S^\lambda(V) = the number of semi-standard
Young tableaux of shape \lambda with entries in {1,...,d}.

CYCLIC SUBGROUP
===============

The cyclic group Z/n \subset S_n (generated by the n-cycle c_n =
(1 2 3 ... n)) acts on V^{\otimes n}. The eigenspaces of c_n correspond
to characters chi_k(c_n) = zeta_n^k for k = 0, 1, ..., n-1 where
zeta_n = exp(2 pi i / n).

The dimension of the k-eigenspace is:

    dim (V^{\otimes n})^{c_n = zeta_n^k} = (1/n) \sum_{d | n} mu(d) \cdot zeta_n^{-kd} \cdot d^{n/d}_{...}

More usefully, by Burnside/cycle counting the total dimension splits as:

    dim V^{\otimes n} = \sum_{k=0}^{n-1} dim (k-eigenspace)

and the trivial (k=0) eigenspace is the C_n-invariant subspace, which
(by Burnside's lemma averaged over C_n) has dimension

    (1/n) \sum_{d | n} phi(n/d) d^{gcd stuff}...

We compute the cyclic decomposition directly as kernels of (c_n - zeta_n^k I)
using exact rational arithmetic for rational eigenvalues (k such that
zeta_n^k is a root of unity in Q, i.e., k=0 when n is odd and k=0, n/2 when
n is even), and return complex-dimensional answers for the remaining
eigenvalues via character theory.

SYMMETRIC VS ANTISYMMETRIC
==========================

The two extremal isotypic components of V^{\otimes n} under S_n are:

    Sym^n(V) = trivial isotypic       (V_{(n)}, dim C(d+n-1, n))
    Lambda^n(V) = sign isotypic        (V_{(1,1,...,1)}, dim C(d, n))

where d = dim V. The other isotypics are intermediate Schur functors
S^\lambda(V) with dim = number of SSYT of shape \lambda in d letters.

For the desuspended bar complex with degree-1 generators (Virasoro), the
KOSZUL SIGN flips the role of trivial and sign: the "naive symmetric" is
Lambda^n(V) (antisymmetric tensors) and the "naive antisymmetric" is
Sym^n(V) (symmetric tensors).

ORDERED VS UNORDERED RATIO
==========================

For DISTINCT generators, the ordered bar (all permutations allowed) has
dimension n! times the unordered bar (subsets only). But the full tensor
power V^{\otimes n} (with repetition) is STRICTLY LARGER than n! times
anything — it includes the diagonal v \otimes v \otimes ... \otimes v which
is fixed by all of S_n. Schur-Weyl gives the exact decomposition:

    dim V^{\otimes n} = \sum_\lambda (dim V_\lambda) * (dim S^\lambda(V))
                      = d^n                            (for dim V = d)

REFERENCES
==========
- Fulton-Harris, "Representation Theory", Appendix A.
- Macdonald, "Symmetric Functions and Hall Polynomials", Ch I.
- AP45: desuspension lowers degree: |s^{-1}v| = |v| - 1.
- AP37: ordered (tensor/Hochschild) != unordered (exterior/Chevalley-Eilenberg).
- bar_complex_ordered_unordered_engine.py: the chain-level comparison.
"""

