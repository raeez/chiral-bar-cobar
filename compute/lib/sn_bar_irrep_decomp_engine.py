def _frac(x) -> Fraction:
    if isinstance(x, Fraction):
        return x
    if isinstance(x, (int, np.integer)):
        return Fraction(int(x))
    return Fraction(x)


def _frac_array(shape) -> np.ndarray:
    arr = np.empty(shape, dtype=object)
    arr.fill(Fraction(0))
    return arr


# ============================================================
# Partitions and S_n combinatorics
# ============================================================

def partitions(n: int) -> List[Tuple[int, ...]]:
    """All partitions of n, sorted in lex-dominance (largest first).

    Returned as tuples (lambda_1 >= lambda_2 >= ...) summing to n.
    """
    if n == 0:
        return [()]
    out: List[Tuple[int, ...]] = []

    def _gen(remaining: int, max_part: int, acc: List[int]):
        if remaining == 0:
            out.append(tuple(acc))
            return
        for k in range(min(remaining, max_part), 0, -1):
            acc.append(k)
            _gen(remaining - k, k, acc)
            acc.pop()

    _gen(n, n, [])
    return out


def partition_class_size(lam: Tuple[int, ...], n: int) -> int:
    """Size of the S_n conjugacy class with cycle type lam.

    |C_lam| = n! / (product over parts k of (k^{m_k} * m_k!))
    where m_k is the multiplicity of k in lam.
    """
    from collections import Counter
    assert sum(lam) == n
    counts = Counter(lam)
    denom = 1
    for k, m in counts.items():
        denom *= (k ** m) * factorial(m)
    return factorial(n) // denom


def cycle_type(perm: Sequence[int]) -> Tuple[int, ...]:
    """Cycle type of a 0-indexed permutation given as a list."""
    n = len(perm)
    seen = [False] * n
    cycles: List[int] = []
    for i in range(n):
        if seen[i]:
            continue
        length = 0
        j = i
        while not seen[j]:
            seen[j] = True
            j = perm[j]
            length += 1
        cycles.append(length)
    return tuple(sorted(cycles, reverse=True))


