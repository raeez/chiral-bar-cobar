def verify_fm():
    results = {}

    # Poincare polynomial
    results["P(Conf_2) = [1,1]"] = poincare_polynomial(2) == [1, 1]
    results["P(Conf_3) = [1,3,2]"] = poincare_polynomial(3) == [1, 3, 2]
    results["P(Conf_4) = [1,6,11,6]"] = poincare_polynomial(4) == [1, 6, 11, 6]

    # Top degrees
    results["top dim(2) = 1"] = os_top_dim(2) == 1
    results["top dim(3) = 2"] = os_top_dim(3) == 2
    results["top dim(4) = 6"] = os_top_dim(4) == 6
    results["top dim(5) = 24"] = os_top_dim(5) == 24

    # Arnold
    results["Arnold rels(3) = 1"] = arnold_relation_count(3) == 1
    results["Arnold rels(4) = 4"] = arnold_relation_count(4) == 4
    results["Arnold rels(5) = 10"] = arnold_relation_count(5) == 10

    # FM boundary strata
    results["FM_3 strata = 4"] = fm_boundary_strata_count(3) == 4
    results["FM_4 strata = 11"] = fm_boundary_strata_count(4) == 11

    # Euler char
    results["chi(Conf_1) = 1"] = euler_char(1) == 1
    results["chi(Conf_2) = 0"] = euler_char(2) == 0
    results["chi(Conf_3) = 0"] = euler_char(3) == 0

    # M-bar_{0,5}
    m05 = mbar_05_data()
    results["M05 is del Pezzo deg 5"] = m05["del_pezzo_degree"] == 5
    results["M05 dim H^2 = 5"] = m05["dim_H2"] == 5

    return results


