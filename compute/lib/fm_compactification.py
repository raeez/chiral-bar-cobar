"""Fulton-MacPherson compactification and configuration space forms.

FM_n(X) is the compactification of Conf_n(X) used for bar complex propagators.

Ground truth from the manuscript (configuration_spaces.tex, bar_cobar_construction.tex):
  - FM_n(C) is built by iterated real blowups along diagonals
  - FM_n(X) = Bl_{diags}(X^n), NOT X^n \\ Delta
  - Boundary strata: nested trees, operad structure
  - Log forms on FM: propagator omega_{ij} = d log(z_i - z_j)
  - Arnold relation: omega_{ij}^omega_{jk} + omega_{jk}^omega_{ki} + omega_{ki}^omega_{ij} = 0
  - Orlik-Solomon algebra: H*(Conf_n(C)) generated by eta_{ij} with Arnold
  - Poincare polynomial: P_t(Conf_n(C)) = prod_{j=1}^{n-1}(1+jt)
  - Top degree: dim H^{n-1}(Conf_n(C)) = (n-1)!

  From CLAUDE.md:
  - M-bar_{0,5} = Bl_4(P^2) (del Pezzo degree 5), dim H^2 = 5 (NOT P^2)
  - Normal bundle N_{Delta_S/X^n} = direct-sum T_X (tangent, NOT cotangent)

CONVENTIONS:
- Cohomological grading
"""

from __future__ import annotations

from math import factorial, comb
from typing import Dict, List, Tuple


# ---------------------------------------------------------------------------
# Configuration space cohomology
# ---------------------------------------------------------------------------

def poincare_polynomial(n: int) -> List[int]:
    """Poincare polynomial of Conf_n(C).

    P_t = prod_{j=1}^{n-1}(1+jt).
    Returns [b_0, b_1, ..., b_{n-1}].
    """
    if n <= 0:
        return []
    poly = [1]
    for j in range(1, n):
        new_poly = [0] * (len(poly) + 1)
        for k, c in enumerate(poly):
            new_poly[k] += c
            new_poly[k + 1] += j * c
        poly = new_poly
    return poly


def os_dim(n: int, degree: int) -> int:
    """Dimension of H^degree(Conf_n(C)).

    From the Poincare polynomial.
    """
    poly = poincare_polynomial(n)
    if 0 <= degree < len(poly):
        return poly[degree]
    return 0


def os_top_dim(n: int) -> int:
    """Top-degree OS dimension = (n-1)!."""
    if n <= 0:
        return 0
    return factorial(n - 1)


def euler_char(n: int) -> int:
    """Euler characteristic of Conf_n(C).

    chi = P_{-1} = prod_{j=1}^{n-1}(1-j).
    For n >= 2: chi = 0 (odd Betti numbers equal even).
    Actually: chi = prod (1-j) for j=1..n-1.
    n=2: (1-1) = 0. n=3: (1-1)(1-2) = 0. Always 0 for n >= 2.
    n=1: empty product = 1.
    """
    if n <= 1:
        return 1
    result = 1
    for j in range(1, n):
        result *= (1 - j)
    return result


# ---------------------------------------------------------------------------
# Arnold relations
# ---------------------------------------------------------------------------

def arnold_relation_count(n: int) -> int:
    """Number of independent Arnold relations for n points.

    For each triple {i,j,k}, one Arnold relation:
    eta_{ij}^eta_{jk} + eta_{jk}^eta_{ki} + eta_{ki}^eta_{ij} = 0

    Total = C(n, 3).
    """
    return comb(n, 3)


def form_space_dim_after_arnold(n: int, degree: int) -> int:
    """Dimension of form space after Arnold relations.

    This equals H^degree(Conf_n(C)) = os_dim(n, degree).
    """
    return os_dim(n, degree)


# ---------------------------------------------------------------------------
# FM boundary strata
# ---------------------------------------------------------------------------

def fm_boundary_strata_count(n: int) -> int:
    """Number of codimension-1 boundary strata of FM_n(C).

    Each stratum corresponds to a subset S of {1,...,n} with |S| >= 2
    colliding to a point. The number of such subsets is
    sum_{k=2}^{n} C(n,k) = 2^n - n - 1.
    """
    return 2**n - n - 1


def fm_codim1_strata(n: int) -> List[Tuple[int, ...]]:
    """List codimension-1 boundary strata of FM_n(C).

    Each is a subset S with |S| >= 2 of {1,...,n} (points that collide).
    Returns subsets as sorted tuples.
    """
    from itertools import combinations
    strata = []
    for k in range(2, n + 1):
        for subset in combinations(range(1, n + 1), k):
            strata.append(subset)
    return strata


# ---------------------------------------------------------------------------
# Propagator forms
# ---------------------------------------------------------------------------

def propagator_form(i: int, j: int) -> str:
    """Log form omega_{ij} = d log(z_i - z_j).

    The propagator on FM_n(C) used for bar complex.
    """
    return f"d log(z_{i} - z_{j})"


def propagator_residue(i: int, j: int) -> str:
    """Residue of omega_{ij} along D_{ij} (collision divisor).

    Res_{D_{ij}} omega_{ij} = 1 (simple pole).
    This extracts OPE data via Res_{z_i=z_j} A(z_i)B(z_j).
    """
    return f"Res_{{D_{{{i}{j}}}}} omega_{{{i}{j}}} = 1"


