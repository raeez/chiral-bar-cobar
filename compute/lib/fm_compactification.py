def mbar_05_data() -> Dict[str, object]:
    """Data for M-bar_{0,5} = Bl_4(P^2) (del Pezzo degree 5).

    Ground truth: CLAUDE.md.
    """
    return {
        "blowup": "Bl_4(P^2)",
        "del_pezzo_degree": 5,
        "dim_H2": 5,  # NOT 1 (it's not P^2)
        "euler_char": 1 + 5 + 1,  # h^0 + h^2 + h^4 = 7? No, del Pezzo has h^2 = 5
        "note": "M-bar_{0,5} is a smooth surface (del Pezzo)",
    }


def normal_bundle_diagonal() -> str:
    """Normal bundle of diagonal in X^n.

    N_{Delta/X^n} = bigoplus T_X (TANGENT bundle, NOT cotangent).

    Ground truth: CLAUDE.md.
    """
    return "N_{Delta/X^n} = direct_sum T_X (tangent bundle)"


