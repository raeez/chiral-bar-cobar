def fl_central_charge_wn(N: int, k: Num) -> Fraction:
    r"""Fateev-Lukyanov central charge for W^k(sl_N) + gl(1).

    c(W_N, k) = (N-1) - N(N^2-1)(k+N-1)^2/(k+N)
    c(Y_{0,0,N}) = c(W_N, k) + 1     [add gl(1)]

    At N=2, k=1: c(W_2) = -7, c(Y) = -6.
    """
    k_f = _frac(k)
    kN = k_f + N
    if kN == 0:
        raise ValueError(f"Critical level k = -{N}")
    c_wn = Fraction(N - 1) - Fraction(N * (N**2 - 1)) * (kN - 1)**2 / kN
    return c_wn + 1  # add gl(1)


def fl_central_charge_wn_bare(N: int, k: Num) -> Fraction:
    r"""Fateev-Lukyanov central charge for W^k(sl_N) WITHOUT gl(1).

    c(W_N, k) = (N-1) - N(N^2-1)(k+N-1)^2/(k+N)
    """
    k_f = _frac(k)
    kN = k_f + N
    if kN == 0:
        raise ValueError(f"Critical level k = -{N}")
    return Fraction(N - 1) - Fraction(N * (N**2 - 1)) * (kN - 1)**2 / kN


def verify_wn_match(N: int, psi: Num) -> bool:
    r"""Verify Y_{0,0,N}[Psi] = W_N x gl(1) at k = Psi - N."""
    psi_f = _frac(psi)
    c_y = central_charge(0, 0, N, psi_f)
    k = psi_f - N
    c_fl = fl_central_charge_wn(N, k)
    return c_y == c_fl


def ff_complementarity_wn(N: int, psi: Num) -> Fraction:
    r"""Feigin-Frenkel complementarity for Y_{0,0,N}: c(Psi) + c(-Psi).

    For W_N: FF duality sends k -> -k - 2N, i.e. Psi -> -Psi.
    The complementarity sum c(W_N,k) + c(W_N,-k-2N) = 2(N-1) + 4N(N^2-1)
    (Freudenthal-de Vries).  For Y = W_N x gl(1), add 2 for the gl(1)
    contributions at levels k and -k.  Total: FdV(N) + 2.

    But actually c(gl(1)) = 1 always (independent of level), so the gl(1)
    contribution to the sum is 1 + 1 = 2.  YES.
    """
    psi_f = _frac(psi)
    c1 = central_charge(0, 0, N, psi_f)
    c2 = central_charge(0, 0, N, -psi_f)
    return c1 + c2


def fdv_constant_wn(N: int) -> Fraction:
    r"""Freudenthal-de Vries constant for W_N x gl(1).

    FdV(W_N) = 2(N-1) + 4N(N^2-1).
    For Y_{0,0,N} = W_N x gl(1): add 2 for gl(1) pair.
    """
    return Fraction(2 * (N - 1) + 4 * N * (N**2 - 1) + 2)


