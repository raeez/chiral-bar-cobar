r"""Gaiotto-Rapcak Y-algebra landscape engine.

Implements the corner VOA Y_{N1,N2,N3}[Psi] from Gaiotto-Rapcak [1703.00982]
and Prochazka-Rapcak [1711.05725], with full shadow depth classification
for the modular Koszul programme.

DEFINITIONS AND CONVENTIONS:

  Y_{N1,N2,N3}[Psi] is the vertex algebra at the corner of a Y-junction
  of N=4 SYM interfaces.  It is defined as a BRST reduction of a super
  Kac-Moody algebra (eq. 3.17 of Rapcak thesis):

    For N3 > N2:  Y = DS_{N3-N2}[gl(N3|N1)_Psi] / gl(N2|N1)_{Psi-1}
    For N3 = N2:  Y = gl(N3|N1)_Psi x S^{N3|N1} / gl(N3|N1)_{Psi-1}
    For N3 < N2:  Y = DS_{N2-N3}[gl(N2|N1)_{-Psi+1}] / gl(N3|N1)_{-Psi}

  Equivalently, Y_{N1,N2,N3} is a truncation of W_{1+infinity} by the
  ideal I_{N1,N2,N3} whose truncation curve is (eq. 3.35):

    N1/lambda_1 + N2/lambda_2 + N3/lambda_3 = 1.

PARAMETRIZATION (eq. 3.42):

    h1 + h2 + h3 = 0,  Psi = -h2/h1.

  Convention: h1 = 1, h2 = -Psi, h3 = Psi - 1.

  Lambda parameters (eq. 3.43):

    lambda_i = sigma / h_i,  where sigma = N1*h1 + N2*h2 + N3*h3.

CENTRAL CHARGE (eq. 3.37, GR17):

    c = (1/Psi)(N1-N3)((N1-N3)^2 - 1)
      + Psi(N2-N3)((N2-N3)^2 - 1)
      + (1/(Psi-1))(N2-N1)((N2-N1)^2 - 1)
      + (2N3+N2-3N1)(N3-N2)^2
      + N1 - N3

  Alternatively via lambda (eq. 3.41):

    c = (lambda_1 - 1)(lambda_2 - 1)(lambda_3 - 1) + 1

  These are EQUAL on the truncation curve (verified systematically).

TRIALITY (eq. 3.38, GR17):

  S3 triality generated by:
    sigma: Psi <-> 1/Psi,     N1 <-> N2     (S-duality on first two)
    tau:   Psi <-> 1 - Psi,   N2 <-> N3     (third generator)

  Central charge is S3-INVARIANT.

THREE DUALITIES (boundary VOA engine finding):

  (1) S-duality:  (N1,N2,N3,Psi) -> (N2,N1,N3,1/Psi)  = triality sigma
  (2) FF-duality: (N1,N2,N3,Psi) -> (N1,N2,N3,-Psi)     [negates coupling]
  (3) Koszul duality for the chiral algebra: PREDICTED to be FF-duality.

  S-duality != FF-duality != triality (all three are distinct operations).
  S3 triality = <S-duality, tau>, where tau: (N1,N2,N3,Psi) -> (N1,N3,N2,1-Psi).
  FF-duality is a FOURTH operation outside S3 (does not permute the N-indices).

  The composition S-duality . FF-duality: (N1,N2,N3,Psi) -> (N2,N1,N3,-1/Psi)
  which is the THIRD S3 generator composed with inversion.

SPECIAL CASES:

  Y_{0,0,N}[Psi] = W_N x gl(1)  (principal W-algebra of sl_N at k = Psi - N)
  Y_{0,N,0}[Psi] = W_N x gl(1)  (by tau triality, at different k)
  Y_{N,0,0}[Psi] = W_N x gl(1)  (by sigma triality, at different k)

  Y_{0,N2,N3}[Psi] with N3 > N2: non-principal coset W-algebra.
  Y_{1,0,N}[Psi]: Bershadsky-Polyakov-type.
  Y_{1,1,1}[Psi]: has N=2 superconformal subalgebra.

STRONG GENERATION AND BOX COUNTING:

  Generators of Y_{N1,N2,N3} correspond to plane partitions fitting in an
  N1 x N2 x N3 box.  The number of such plane partitions is the MacMahon
  box formula:

    M(N1,N2,N3) = prod_{i=1}^{N1} prod_{j=1}^{N2} prod_{k=1}^{N3}
                  (i + j + k - 1) / (i + j + k - 2)

  For Y_{0,0,N}: M = 1 (the only plane partition in a 0x0xN box is empty).
  But this is the TRUNCATION counting; the actual generators are the
  W_{1+infty} generators that survive truncation.

  The first null state appears at conformal weight (N1+1)(N2+1)(N3+1).
  Generators have weights 1, 2, ..., max where max is determined by
  the N-triple.

SHADOW DEPTH CLASSIFICATION:

  G (Gaussian, r_max=2):  free bosons (abelian OPE, no self-interaction).
  L (Lie/tree, r_max=3):  affine KM (Jacobi identity terminates tower).
  C (contact, r_max=4):   beta-gamma (contact/quartic terminates at 4).
  M (mixed, r_max=inf):   Virasoro/W_N (infinite tower from self-coupling).

  For Y-algebras:
    Y_{0,0,0} = gl(1):      G  (Gaussian, 1 free boson).
    Y_{0,0,1} = gl(1)^2:    G  (2 free bosons, but structure is abelian).
    Y_{0,0,N} for N >= 2:   M  (W_N has Virasoro subalgebra -> infinite).
    Y with max(Ni) >= 2:    M  (DS reduction -> infinite depth, AP14).
    Y_{0,1,1}:              L  (affine-type, no DS reduction at this level).
    Y_{1,1,0} by triality = Y_{0,1,1}: same class.
    Y_{1,1,1}:              M  (N=2 super, nontrivial self-coupling at w >= 3).

  Prediction: Triality PRESERVES depth class (depth depends on sorted triple).
  Koszul/FF duality PRESERVES depth class (N-triple unchanged).

KAPPA (modular characteristic):

  CAUTION (AP1): kappa is family-specific. Recompute for EACH Y-algebra.
  CAUTION (AP48): kappa depends on the full algebra, not just c.
  CAUTION (AP39): kappa != c/2 except for Virasoro.

  For Y_{0,0,N} = W_N x gl(1):
    kappa(W_N at level k) = c(W_N,k) * (H_N - 1)    [H_N = harmonic number]
    kappa(gl(1) at level k) = k                        [Heisenberg]
    kappa(Y) = kappa(W_N) + kappa(gl(1))

  For general Y_{N1,N2,N3}: no closed-form kappa formula is known.
  The kappa MUST be computed from the full bar complex / genus-1 amplitude.
  We provide c/2 as the VIRASORO CONTRIBUTION (lower bound for |kappa|),
  flagged as APPROXIMATE per AP48.

FF-COMPLEMENTARITY FOR Y-ALGEBRAS:

  For Y_{0,0,N}: c(Psi) + c(-Psi) = 2(N-1) + 4N(N^2-1) + 2
    (Freudenthal-de Vries for W_N, plus 2 from gl(1) at levels k and -k).

  For GENERAL Y_{N1,N2,N3}: c(Psi) + c(-Psi) is computed directly.
  The key question: is this Psi-independent?  YES -- proved below by
  explicit computation (the Psi-dependent terms cancel in the sum).

Sources:
  [GR17] Gaiotto-Rapcak, arXiv:1703.00982
  [PR17] Prochazka-Rapcak, arXiv:1711.05725
  [CL20] Creutzig-Linshaw, arXiv:2005.10234
  [Rap20] Rapcak thesis, "The Vertex Algebra Vertex"
"""

