def large_n_central_charge(N: int, psi: Num) -> Fraction:
    r"""Central charge of Y_{0,0,N}[Psi] = W_N x gl(1) at large N.

    c = (N-1) - N(N^2-1)(Psi-1)^2/Psi + 1
      ~ -N^3 * (Psi-1)^2/Psi  as N -> infinity.

    The leading-order growth is cubic in N.
    """
    return central_charge(0, 0, N, psi)


def large_n_kappa_ratio(N: int, psi: Num) -> Fraction:
    r"""Ratio kappa(Y_{0,0,N})/c(Y_{0,0,N}) at large N.

    For W_N: kappa/c = H_N - 1 ~ log(N) as N -> infinity.
    For Y_{0,0,N} = W_N x gl(1): slightly different due to gl(1).
    """
    c = central_charge(0, 0, N, psi)
    if c == 0:
        return None
    kap = kappa_y_algebra(0, 0, N, psi)
    return kap / c


