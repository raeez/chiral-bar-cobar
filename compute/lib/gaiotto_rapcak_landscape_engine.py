def macmahon_box(a: int, b: int, c_box: int) -> int:
    r"""Count plane partitions fitting in an a x b x c box.

    MacMahon's formula:
      M(a,b,c) = prod_{i=1}^{a} prod_{j=1}^{b} prod_{k=1}^{c}
                 (i + j + k - 1) / (i + j + k - 2)

    If any dimension is 0, the count is 1 (only the empty partition).
    """
    if a == 0 or b == 0 or c_box == 0:
        return 1
    # Compute as exact integer using the product formula
    num = 1
    den = 1
    for i in range(1, a + 1):
        for j in range(1, b + 1):
            for k in range(1, c_box + 1):
                num *= (i + j + k - 1)
                den *= (i + j + k - 2)
    assert num % den == 0, f"MacMahon formula not integer: {num}/{den}"
    return num // den


def generator_weights(N1: int, N2: int, N3: int) -> List[int]:
    r"""Conformal weights of strong generators of Y_{N1,N2,N3}.

    Y_{N1,N2,N3} is a truncation of W_{1+infinity}.  The generators
    are the W_{1+infinity} generators at weights 1, 2, 3, ... that
    survive the truncation.  The first null appears at weight
    (N1+1)(N2+1)(N3+1), so generators exist at weights
    1, 2, ..., (N1+1)(N2+1)(N3+1) - 1.

    However, for Y_{0,0,N}: the truncation eliminates generators at
    weight > N, giving generators at weights 1, ..., N.

    The precise generator content depends on the box counting:
    at weight s, the number of generators is the number of plane
    partitions of size s that fit in the N1 x N2 x N3 box.

    For simplicity, we list the integer generator weights.
    Y_{0,0,N}: weights 1, ..., N (N generators).
    Y_{0,L,M}: weights 1, ..., max(L,M) (approximately).
    General: weights 1, ..., max(N1,N2,N3) at minimum.
    """
    sorted_n = sorted([N1, N2, N3], reverse=True)
    M, L, K = sorted_n  # M >= L >= K

    if M == 0:
        # Y_{0,0,0}: single gl(1) current at weight 1
        return [1]

    if L == 0:
        # Y_{0,0,M}: W_M x gl(1), generators at 1, ..., M
        return list(range(1, M + 1))

    if K == 0:
        # Y_{0,L,M}: truncated W_{1+inf} with generators
        # at 1, ..., M (at least)
        return list(range(1, M + 1))

    # General Y_{K,L,M}: generators at 1, ..., M (at least)
    # The first null is at (K+1)(L+1)(M+1), so potentially more
    # generators up to weight (K+1)(L+1)(M+1) - 1
    max_gen_weight = M  # conservative: at least M generators
    return list(range(1, max_gen_weight + 1))


def num_generators(N1: int, N2: int, N3: int) -> int:
    r"""Number of strong generators of Y_{N1,N2,N3}.

    For Y_{0,0,N}: N generators (spins 1 through N).
    """
    return len(generator_weights(N1, N2, N3))


def first_null_weight(N1: int, N2: int, N3: int) -> int:
    r"""Weight of the first null state in Y_{N1,N2,N3}.

    The first null appears at weight (N1+1)(N2+1)(N3+1).
    """
    return (N1 + 1) * (N2 + 1) * (N3 + 1)


def num_generators_box(N1: int, N2: int, N3: int) -> int:
    r"""Total number of strong generators from MacMahon box counting.

    This counts ALL plane partitions in the N1 x N2 x N3 box,
    minus 1 (for the empty partition, which does not correspond to
    a generator).

    Actually: the MacMahon count M(N1,N2,N3) counts plane partitions
    INCLUDING the empty one.  The generators correspond to NONEMPTY
    plane partitions, so the count is M(N1,N2,N3) - 1.

    For Y_{0,0,N}: M(0,0,N) = 1, so 0 generators via this count.
    This is WRONG -- Y_{0,0,N} has N generators.

    The resolution: the correct generator counting for Y_{N1,N2,N3}
    as a W_{1+inf} truncation does NOT come from MacMahon box counting
    of plane partitions in the N1 x N2 x N3 box.  Rather, the box
    (N1+1) x (N2+1) x (N3+1) is the COMPLEMENTARY region, and the
    number of generators at weight s is the number of W_{1+inf}
    generators at weight s that DO NOT vanish under truncation.

    For Y_{0,0,N}: weights 1, ..., N survive -> N generators.
    For Y_{0,1,N}: weights 1, ..., N+1 survive -> N+1 generators.
    For Y_{1,1,N}: weights 1, ..., N+1 (and additional) -> N+1+ generators.

    SIMPLE FORMULA: num_generators = max(N1,N2,N3) for the
    W_N-type cases.  For general, this is a lower bound.
    """
    sorted_n = sorted([N1, N2, N3])
    if sorted_n[2] == 0:
        return 1  # Y_{0,0,0}
    return max(N1, N2, N3)


