# 2. Central charge
# ============================================================================

def central_charge_lambda(lam1: Fraction, lam2: Fraction,
                          lam3: Fraction) -> Fraction:
    r"""Central charge of W_{1+infinity} in lambda parametrization (eq. 3.41).

    c_{1+infty} = (lambda_1 - 1)(lambda_2 - 1)(lambda_3 - 1) + 1
    """
    return (lam1 - 1) * (lam2 - 1) * (lam3 - 1) + 1


def central_charge(N1: int, N2: int, N3: int, psi: Num) -> Fraction:
    r"""Central charge of Y_{N1,N2,N3}[Psi] via eq. 3.37 of GR17.

    c = (1/Psi)(N1-N3)((N1-N3)^2 - 1)
      + Psi(N2-N3)((N2-N3)^2 - 1)
      + (1/(Psi-1))(N2-N1)((N2-N1)^2 - 1)
      + (2*N3 + N2 - 3*N1)*(N3 - N2)^2
      + N1 - N3

    This equals c_{1+infty} evaluated on the truncation curve.
    """
    psi_f = _frac(psi)
    if psi_f == 0 or psi_f == 1:
        raise ValueError(f"Psi = {psi_f} is degenerate")

    a = N1 - N3
    b = N2 - N3
    d = N2 - N1

    term1 = Fraction(1, psi_f) * a * (a**2 - 1)
    term2 = psi_f * b * (b**2 - 1)
    term3 = Fraction(1, psi_f - 1) * d * (d**2 - 1)
    term4 = Fraction((2 * N3 + N2 - 3 * N1) * (N3 - N2)**2)
    term5 = Fraction(N1 - N3)

    return term1 + term2 + term3 + term4 + term5


def central_charge_via_lambda(N1: int, N2: int, N3: int,
                              psi: Num) -> Fraction:
    r"""Central charge via lambda parametrization (cross-check path).

    Computes lambdas from (N1, N2, N3, Psi), then evaluates
    c = (l1-1)(l2-1)(l3-1) + 1.
    """
    psi_f = _frac(psi)
    lam1, lam2, lam3 = lambda_params(N1, N2, N3, psi_f)
    return central_charge_lambda(lam1, lam2, lam3)


# ============================================================================
# 3. Triality
# ============================================================================

def triality_sigma(N1: int, N2: int, N3: int,
                   psi: Num) -> Tuple[int, int, int, Fraction]:
    r"""Apply sigma triality: Psi <-> 1/Psi, N1 <-> N2.

    This is S-duality in the Gaiotto-Witten sense.
    """
    psi_f = _frac(psi)
    if psi_f == 0:
        raise ValueError("Cannot apply sigma at Psi=0")
    return (N2, N1, N3, Fraction(1, psi_f))


def triality_tau(N1: int, N2: int, N3: int,
                 psi: Num) -> Tuple[int, int, int, Fraction]:
    r"""Apply tau triality: Psi <-> 1 - Psi, N2 <-> N3."""
    psi_f = _frac(psi)
    return (N1, N3, N2, 1 - psi_f)


def triality_orbit(N1: int, N2: int, N3: int,
                   psi: Num) -> List[Tuple[int, int, int, Fraction]]:
    r"""Compute the full S3 triality orbit of Y_{N1,N2,N3}[Psi].

    The orbit has at most 6 elements (the S3 group).
    """
    psi_f = _frac(psi)
    seen = set()
    orbit = []

    def _add(n1, n2, n3, p):
        key = (n1, n2, n3, p)
        if key not in seen:
            seen.add(key)
            orbit.append(key)

    # Identity
    _add(N1, N2, N3, psi_f)
    # sigma
    s = triality_sigma(N1, N2, N3, psi_f)
    _add(*s)
    # tau
    t = triality_tau(N1, N2, N3, psi_f)
    _add(*t)
    # sigma . tau
    st = triality_sigma(*t)
    _add(*st)
    # tau . sigma
    ts = triality_tau(*s)
    _add(*ts)
    # sigma . tau . sigma = tau . sigma . tau
    sts = triality_sigma(*ts)
    _add(*sts)

    return orbit


def verify_triality_invariance(N1: int, N2: int, N3: int,
                               psi: Num) -> bool:
    r"""Verify central charge invariance under all triality moves."""
    psi_f = _frac(psi)
    c_orig = central_charge(N1, N2, N3, psi_f)
    orbit = triality_orbit(N1, N2, N3, psi_f)
    for n1, n2, n3, p in orbit:
        if p == 0 or p == 1:
            continue  # skip degenerate
        c_dual = central_charge(n1, n2, n3, p)
        if c_dual != c_orig:
            return False
    return True


