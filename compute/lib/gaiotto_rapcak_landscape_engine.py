def s_duality(N1: int, N2: int, N3: int,
              psi: Num) -> Tuple[int, int, int, Fraction]:
    r"""S-duality = triality sigma: (N1,N2,N3,Psi) -> (N2,N1,N3,1/Psi)."""
    return triality_sigma(N1, N2, N3, psi)


def ff_duality(N1: int, N2: int, N3: int,
               psi: Num) -> Tuple[int, int, int, Fraction]:
    r"""Feigin-Frenkel duality: (N1,N2,N3,Psi) -> (N1,N2,N3,-Psi).

    For Y_{0,0,N}: this sends k = Psi-N to k' = -Psi-N = -(k+2N),
    which is the standard FF involution k -> -k-2h^v for sl_N.

    FF-duality does NOT permute the N-indices.
    """
    psi_f = _frac(psi)
    return (N1, N2, N3, -psi_f)


def koszul_dual_prediction(N1: int, N2: int, N3: int,
                           psi: Num) -> Tuple[int, int, int, Fraction]:
    r"""Predicted Koszul dual Y_{N1',N2',N3'}[Psi'].

    PREDICTION: Koszul duality = FF-duality for Y-algebras.

    Rationale: For Y_{0,0,N} = W_N x gl(1), the Koszul dual is
    W_N at FF-dual level x gl(1) at negative level. The FF involution
    k -> -k-2N sends Psi -> -Psi.

    For general Y: the BRST definition involves gl(N3|N1) at level Psi.
    The FF involution of the superalgebra negates the level while
    preserving the rank parameters.

    This is CONJECTURAL for general Y-algebras. For Y_{0,0,N} it is
    the proved Feigin-Frenkel duality.

    NOTE: This is DIFFERENT from S-duality (which swaps N1<->N2 and
    inverts Psi). S-duality != Koszul duality. See boundary_voa_koszul_engine.

    Returns (N1', N2', N3', Psi').
    """
    return ff_duality(N1, N2, N3, psi)


def koszul_complementarity_sum(N1: int, N2: int, N3: int,
                               psi: Num) -> Optional[Fraction]:
    r"""Compute c(A) + c(A^!) using FF-duality prediction.

    Returns None if the dual coupling is degenerate.
    """
    psi_f = _frac(psi)
    _, _, _, psi_d = koszul_dual_prediction(N1, N2, N3, psi_f)
    if psi_d == 0 or psi_d == 1:
        return None
    return central_charge(N1, N2, N3, psi_f) + central_charge(N1, N2, N3, psi_d)


def s_duality_complementarity_sum(N1: int, N2: int, N3: int,
                                  psi: Num) -> Optional[Fraction]:
    r"""Compute c(A) + c(S(A)) under S-duality.

    S(A) = Y_{N2,N1,N3}[1/Psi].  By triality invariance of c,
    this always equals 2*c(A).
    """
    psi_f = _frac(psi)
    c = central_charge(N1, N2, N3, psi_f)
    N1s, N2s, N3s, psi_s = s_duality(N1, N2, N3, psi_f)
    if psi_s == 0 or psi_s == 1:
        return None
    c_s = central_charge(N1s, N2s, N3s, psi_s)
    return c + c_s


