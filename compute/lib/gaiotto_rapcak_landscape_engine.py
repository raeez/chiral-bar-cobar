Num = Union[int, float, Fraction]


def _frac(x: Num) -> Fraction:
    """Coerce to Fraction for exact arithmetic."""
    if isinstance(x, Fraction):
        return x
    return Fraction(x)


# ============================================================================
# 1. Core parameter computations
# ============================================================================

def h_params_from_psi(psi: Num) -> Tuple[Fraction, Fraction, Fraction]:
    r"""Compute Omega-deformation parameters from Psi.

    Convention: h1 = 1, h2 = -Psi, h3 = Psi - 1.
    Then h1 + h2 + h3 = 0 and Psi = -h2/h1.

    Raises ValueError for degenerate Psi in {0, 1}.
    """
    psi_f = _frac(psi)
    if psi_f == 0:
        raise ValueError("Psi = 0 is degenerate (h2 = 0)")
    if psi_f == 1:
        raise ValueError("Psi = 1 is degenerate (h3 = 0)")
    h1 = Fraction(1)
    h2 = -psi_f
    h3 = psi_f - 1
    return (h1, h2, h3)


def lambda_params(N1: int, N2: int, N3: int,
                  psi: Num) -> Tuple[Fraction, Fraction, Fraction]:
    r"""Compute lambda parameters for Y_{N1,N2,N3}[Psi].

    lambda_i = sigma / h_i  where sigma = N1*h1 + N2*h2 + N3*h3  (eq. 3.43).
    """
    h1, h2, h3 = h_params_from_psi(psi)
    sigma = N1 * h1 + N2 * h2 + N3 * h3
    return (sigma / h1, sigma / h2, sigma / h3)


def verify_lambda_constraint(lam1: Fraction, lam2: Fraction,
                             lam3: Fraction) -> bool:
    r"""Verify 1/lambda_1 + 1/lambda_2 + 1/lambda_3 = 0 (eq. 3.33/3.40).

    This follows from h1 + h2 + h3 = 0 and lambda_i = sigma/h_i.
    """
    if lam1 == 0 or lam2 == 0 or lam3 == 0:
        return False
    return Fraction(1, lam1) + Fraction(1, lam2) + Fraction(1, lam3) == 0


def verify_truncation_curve(N1: int, N2: int, N3: int,
                            lam1: Fraction, lam2: Fraction,
                            lam3: Fraction) -> bool:
    r"""Verify N1/lambda_1 + N2/lambda_2 + N3/lambda_3 = 1 (eq. 3.35)."""
    if lam1 == 0 or lam2 == 0 or lam3 == 0:
        return False
    val = (Fraction(N1) / lam1 + Fraction(N2) / lam2
           + Fraction(N3) / lam3)
    return val == 1


