# 7. Shadow depth classification
# ============================================================================

def shadow_depth_class(N1: int, N2: int, N3: int) -> str:
    r"""Predict shadow depth class for Y_{N1,N2,N3}.

    G (Gaussian, r_max=2):  Y_{0,0,0}, Y_{0,0,1}  (free bosons).
    L (Lie/tree, r_max=3):  Y_{0,1,1}  (affine-type coset, no higher W).
    M (mixed, r_max=inf):   everything with max(Ni) >= 2, plus Y_{1,1,1}.

    NO class C appears in the Y-algebra landscape.  The reason: class C
    (contact, r_max=4) arises from beta-gamma systems, which are Y_{0,1,1}
    with specific coupling.  But Y_{0,1,1} is affine-type (class L) at
    generic coupling.  The contact structure only appears at special Psi
    values, and we classify at GENERIC Psi.

    Rationale for each classification:
      G: Y_{0,0,0} is a single boson; Y_{0,0,1} is two free bosons.
      L: Y_{0,1,1} = coset of gl(1|1) by gl(0|1) = affine gl(1) type.
         No DS reduction, no Virasoro subalgebra beyond trivial.
         At generic Psi, this is an affine-type algebra (Jacobi terminates).
      M: Y_{0,0,N>=2} contains Virasoro (from W_N), hence infinite tower.
         Y_{1,1,1} has generators up to weight 8, including spin-2
         (Virasoro), hence infinite tower.
         Y with max(Ni) >= 2: DS reduction produces W-type generators.
    """
    sorted_n = sorted([N1, N2, N3])

    # Y_{0,0,0}
    if sorted_n == [0, 0, 0]:
        return 'G'

    # Y_{0,0,1}
    if sorted_n == [0, 0, 1]:
        return 'G'

    # Y_{0,1,1}: affine-type
    if sorted_n == [0, 1, 1]:
        return 'L'

    # Y_{0,0,N} for N >= 2: W_N type
    if sorted_n[0] == 0 and sorted_n[1] == 0 and sorted_n[2] >= 2:
        return 'M'

    # Y_{1,1,1}: has N=2 superconformal subalgebra
    if sorted_n == [1, 1, 1]:
        return 'M'

    # All other cases with max >= 2: class M (DS reduction)
    return 'M'


def shadow_depth(N1: int, N2: int, N3: int) -> Optional[int]:
    r"""Shadow depth r_max for Y_{N1,N2,N3}.

    Returns the depth (2, 3, or None for infinity).
    """
    cls = shadow_depth_class(N1, N2, N3)
    depth_map = {'G': 2, 'L': 3, 'C': 4, 'M': None}
    return depth_map[cls]


