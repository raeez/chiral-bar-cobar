def ff_complementarity_sum(N1: int, N2: int, N3: int,
                           psi: Num) -> Fraction:
    r"""Compute c(Y_{N1,N2,N3}[Psi]) + c(Y_{N1,N2,N3}[-Psi]).

    This is the FF-complementarity sum.  For Y_{0,0,N} it equals
    FdV(N) + 2.  For general triples, we compute it directly.
    """
    psi_f = _frac(psi)
    c1 = central_charge(N1, N2, N3, psi_f)
    c2 = central_charge(N1, N2, N3, -psi_f)
    return c1 + c2


def ff_complementarity_constant(N1: int, N2: int, N3: int) -> Fraction:
    r"""Compute the Psi-INDEPENDENT FF complementarity constant.

    c(Psi) + c(-Psi) for Y_{N1,N2,N3}.  This should be independent of
    Psi (verified numerically, then proved algebraically).

    Algebraic proof:
      The central charge formula is c(Psi) = A/Psi + B*Psi + C/(Psi-1) + D
      where A, B, C, D depend only on the N-triple.
      Under Psi -> -Psi:  c(-Psi) = -A/Psi - B*Psi + C/(-Psi-1) + D.
      Sum: c + c' = 2D + C/(Psi-1) + C/(-Psi-1) = 2D - 2C/(Psi^2-1)?

    Actually, let me compute this algebraically from the formula.
    In the direct formula eq 3.37:
      a = N1-N3, b = N2-N3, d = N2-N1.
      c(Psi) = a(a^2-1)/Psi + b(b^2-1)*Psi + d(d^2-1)/(Psi-1) + E
      c(-Psi) = -a(a^2-1)/Psi - b(b^2-1)*Psi + d(d^2-1)/(-Psi-1) + E

    Sum: 2E + d(d^2-1)*(1/(Psi-1) + 1/(-Psi-1))
       = 2E + d(d^2-1)*(-2/(Psi^2-1))

    This is NOT Psi-independent in general!  The 1/(Psi-1) + 1/(-Psi-1)
    = ((-Psi-1) + (Psi-1))/((Psi-1)(-Psi-1)) = -2/(-(Psi^2-1)) = 2/(Psi^2-1).

    So: c(Psi)+c(-Psi) = 2E + 2*d*(d^2-1)/(Psi^2-1).

    This is Psi-independent ONLY when d*(d^2-1) = 0, i.e. d in {-1,0,1}.
    d = N2-N1.  So FF-complementarity is Psi-independent iff |N2-N1| <= 1.

    For Y_{0,0,N}: d = 0, so Psi-independent. CORRECT.
    For Y_{1,2,3}: d = 1, d^2-1 = 0, so Psi-independent. CORRECT.
    For Y_{0,2,3}: d = 2, d^2-1 = 3, so NOT Psi-independent.

    This is a KEY FINDING: FF-complementarity is NOT universally
    Psi-independent for Y-algebras.

    We compute at a large generic Psi as an approximation for the
    "constant" part.  For the truly Psi-independent cases, any Psi works.
    """
    # Use two test values; if they agree, the sum is constant
    psi_test_1 = Fraction(7)
    psi_test_2 = Fraction(11)
    s1 = ff_complementarity_sum(N1, N2, N3, psi_test_1)
    s2 = ff_complementarity_sum(N1, N2, N3, psi_test_2)
    if s1 == s2:
        return s1
    else:
        # Not Psi-independent; return None
        return None


def ff_complementarity_is_constant(N1: int, N2: int, N3: int) -> bool:
    r"""Check if c(Psi) + c(-Psi) is Psi-independent.

    Algebraically: independent iff (N2-N1)*((N2-N1)^2 - 1) = 0,
    i.e. |N2-N1| in {0, 1}.
    """
    d = N2 - N1
    return d * (d**2 - 1) == 0


