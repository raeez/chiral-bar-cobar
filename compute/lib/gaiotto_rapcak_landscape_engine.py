@dataclass
class YAlgebraData:
    """Complete data for a Y-algebra in the landscape."""
    N1: int
    N2: int
    N3: int
    psi: Fraction
    central_charge: Fraction
    central_charge_lambda: Optional[Fraction]
    lambda_params: Optional[Tuple[Fraction, Fraction, Fraction]]
    kappa: Fraction
    kappa_is_exact: bool
    depth_class: str
    depth: Optional[int]
    num_generators: int
    first_null: int
    generator_weights: List[int]
    triality_orbit_size: int
    koszul_dual: Tuple[int, int, int, Fraction]
    ff_complementarity: Optional[Fraction]
    ff_complementarity_is_constant: bool
    s_dual: Tuple[int, int, int, Fraction]
    is_wn_type: bool
    notes: str = ""


def analyze_y_algebra(N1: int, N2: int, N3: int,
                      psi: Num) -> YAlgebraData:
    r"""Complete analysis of Y_{N1,N2,N3}[Psi]."""
    psi_f = _frac(psi)

    c = central_charge(N1, N2, N3, psi_f)
    c_lam = central_charge_via_lambda(N1, N2, N3, psi_f)
    lams = lambda_params(N1, N2, N3, psi_f)
    kap = kappa_y_algebra(N1, N2, N3, psi_f)
    kap_exact = kappa_y_algebra_is_exact(N1, N2, N3)
    cls = shadow_depth_class(N1, N2, N3)
    dep = shadow_depth(N1, N2, N3)
    ngen = num_generators(N1, N2, N3)
    fnull = first_null_weight(N1, N2, N3)
    gweights = generator_weights(N1, N2, N3)
    orbit = triality_orbit(N1, N2, N3, psi_f)
    kd = koszul_dual_prediction(N1, N2, N3, psi_f)
    ff_comp = koszul_complementarity_sum(N1, N2, N3, psi_f)
    ff_const = ff_complementarity_is_constant(N1, N2, N3)
    sd = s_duality(N1, N2, N3, psi_f) if psi_f != 0 else (N1, N2, N3, psi_f)

    sorted_n = sorted([N1, N2, N3])
    is_wn = (sorted_n[0] == 0 and sorted_n[1] == 0)

    return YAlgebraData(
        N1=N1, N2=N2, N3=N3, psi=psi_f,
        central_charge=c,
        central_charge_lambda=c_lam,
        lambda_params=lams,
        kappa=kap,
        kappa_is_exact=kap_exact,
        depth_class=cls,
        depth=dep,
        num_generators=ngen,
        first_null=fnull,
        generator_weights=gweights,
        triality_orbit_size=len(orbit),
        koszul_dual=kd,
        ff_complementarity=ff_comp,
        ff_complementarity_is_constant=ff_const,
        s_dual=sd,
        is_wn_type=is_wn,
    )


def landscape_survey(max_N: int = 3,
                     psi: Num = Fraction(3, 1)) -> List[YAlgebraData]:
    r"""Survey all Y_{N1,N2,N3} with each N_i in [0, max_N]."""
    psi_f = _frac(psi)
    results = []
    for n1 in range(max_N + 1):
        for n2 in range(max_N + 1):
            for n3 in range(max_N + 1):
                data = analyze_y_algebra(n1, n2, n3, psi_f)
                results.append(data)
    return results


def landscape_summary(max_N: int = 3,
                      psi: Num = Fraction(3, 1)) -> Dict[str, int]:
    r"""Summary statistics of the Y-algebra landscape."""
    results = landscape_survey(max_N, psi)
    summary = {'G': 0, 'L': 0, 'C': 0, 'M': 0, 'total': len(results)}
    for data in results:
        summary[data.depth_class] += 1
    return summary


def landscape_depth_census(max_N: int = 3) -> Dict[str, List[Tuple[int, int, int]]]:
    r"""Census of depth classes with representative N-triples."""
    census: Dict[str, List[Tuple[int, int, int]]] = {
        'G': [], 'L': [], 'C': [], 'M': []
    }
    seen_sorted = set()
    for n1 in range(max_N + 1):
        for n2 in range(max_N + 1):
            for n3 in range(max_N + 1):
                key = tuple(sorted([n1, n2, n3]))
                if key in seen_sorted:
                    continue
                seen_sorted.add(key)
                cls = shadow_depth_class(n1, n2, n3)
                census[cls].append((n1, n2, n3))
    return census


