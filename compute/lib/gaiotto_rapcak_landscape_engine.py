# 10. Triality and Koszul duality interaction
# ============================================================================

def triality_preserves_depth_class(N1: int, N2: int, N3: int) -> bool:
    r"""Check if triality preserves shadow depth class.

    Since shadow_depth_class depends only on sorted(N1,N2,N3),
    and triality permutes the triple, depth class is ALWAYS preserved.
    """
    cls_orig = shadow_depth_class(N1, N2, N3)
    for perm in [(N1, N2, N3), (N1, N3, N2), (N2, N1, N3),
                 (N2, N3, N1), (N3, N1, N2), (N3, N2, N1)]:
        if shadow_depth_class(*perm) != cls_orig:
            return False
    return True


def koszul_preserves_depth_class(N1: int, N2: int, N3: int,
                                 psi: Num) -> bool:
    r"""Check if Koszul duality preserves shadow depth class.

    FF duality preserves the N-triple, so depth class is ALWAYS preserved.
    """
    cls_orig = shadow_depth_class(N1, N2, N3)
    N1d, N2d, N3d, _ = koszul_dual_prediction(N1, N2, N3, psi)
    return shadow_depth_class(N1d, N2d, N3d) == cls_orig


