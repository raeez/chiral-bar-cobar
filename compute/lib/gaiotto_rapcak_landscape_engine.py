def harmonic_number(n: int) -> Fraction:
    """H_n = 1 + 1/2 + ... + 1/n."""
    return sum(Fraction(1, j) for j in range(1, n + 1))


def kappa_wn_bare(N: int, k: Num) -> Fraction:
    r"""Kappa for bare W^k(sl_N): kappa = c(W_N, k) * (H_N - 1).

    At N=2: kappa = c(Vir)/2 = c/2 (since H_2 - 1 = 1/2).
    """
    H_N = harmonic_number(N)
    c = fl_central_charge_wn_bare(N, k)
    return c * (H_N - 1)


def kappa_heisenberg(k: Num) -> Fraction:
    r"""Kappa for Heisenberg at level k: kappa = k."""
    return _frac(k)


def kappa_y_algebra(N1: int, N2: int, N3: int, psi: Num) -> Fraction:
    r"""Modular characteristic kappa for Y_{N1,N2,N3}[Psi].

    CAUTION (AP48): kappa depends on the full algebra, not just c.

    For Y_{0,0,N} = W_N x gl(1):
      kappa = kappa(W_N at level k=Psi-N) + kappa(gl(1) at level k=Psi-N)
            = c(W_N,k) * (H_N - 1) + k

    For Y_{0,0,0} = gl(1): kappa = 0 (trivial, c=0 from formula).
    For Y_{0,0,1} ~ gl(1)^2: kappa = 1 (from c=1 formula).

    For GENERAL Y with triality-equivalent to Y_{0,0,N}: map to that frame.

    For Y with min(sorted) >= 1 (genuine non-WN type):
      No closed-form kappa formula is known.
      We use c/2 as the Virasoro contribution with APPROXIMATE flag.
      This is a LOWER BOUND for |kappa| when higher-spin generators
      contribute positively.

    Returns exact Fraction (or approximate for general case).
    """
    psi_f = _frac(psi)
    c = central_charge(N1, N2, N3, psi_f)

    sorted_n = sorted([N1, N2, N3])

    # Y_{0,0,0}: c = 0 from formula, kappa = 0
    if sorted_n == [0, 0, 0]:
        return Fraction(0)

    # Y_{0,0,N}: identify the correct triality frame and compute
    if sorted_n[0] == 0 and sorted_n[1] == 0:
        N = sorted_n[2]
        if N == 1:
            # Y_{0,0,1}: c = 1, this is gl(1)^2-like, kappa = c = 1
            return c

        # Y_{0,0,N}: W_N x gl(1)
        # Need to find the correct level k for the frame where N3 = N
        # In the original (N1,N2,N3) variables, we need the triality map
        # that puts the nonzero index in position 3
        if N3 == N and N1 == 0 and N2 == 0:
            k = psi_f - N
        elif N1 == N and N2 == 0 and N3 == 0:
            # triality sigma: (N,0,0,Psi) -> (0,N,0,1/Psi) -> (0,0,N,Psi-via-tau)
            # Actually, (N,0,0)[Psi] = (0,N,0)[1/Psi] via sigma
            # (0,N,0)[Psi'] = (0,0,N)[1-Psi'] via tau
            # Combined: (N,0,0)[Psi] = (0,0,N)[1 - 1/Psi] = (0,0,N)[(Psi-1)/Psi]
            psi_eff = (psi_f - 1) / psi_f
            k = psi_eff - N
        elif N2 == N and N1 == 0 and N3 == 0:
            # (0,N,0)[Psi] = (0,0,N)[1-Psi] via tau
            psi_eff = 1 - psi_f
            k = psi_eff - N
        elif N1 == 0 and N2 == N and N3 == 0:
            psi_eff = 1 - psi_f
            k = psi_eff - N
        elif N1 == 0 and N3 == N and N2 == 0:
            k = psi_f - N
        elif N2 == 0 and N3 == N and N1 == 0:
            k = psi_f - N
        else:
            # Fallback: use the Psi - max(N) formula
            k = psi_f - N

        H_N = harmonic_number(N)
        c_wn = fl_central_charge_wn_bare(N, k)
        kappa_w = c_wn * (H_N - 1)
        kappa_gl1 = k
        return kappa_w + kappa_gl1

    # General case: c/2 approximation (AP48 warning)
    return c / 2


def kappa_y_algebra_is_exact(N1: int, N2: int, N3: int) -> bool:
    r"""Returns True if kappa computation is exact (not approximate).

    Exact for Y_{0,0,N}-type (up to triality).  Approximate otherwise.
    """
    sorted_n = sorted([N1, N2, N3])
    return sorted_n[0] == 0 and sorted_n[1] == 0


