def galois_rank_wn(N: int) -> int:
    """Compute the F_2-rank of the Galois group Gal(K_N(c)/Q(c)).

    For N <= 3: rank = 0 (no higher-spin couplings, delta_F2 is rational).
    For N >= 4: rank = N - 2 (the main theorem).

    This function computes the rank from the discriminant matrix for N <= 7
    (where the factor data is known/reliable) and returns the proven value
    N - 2 for all N >= 4.
    """
    if N <= 3:
        return 0
    data = build_discriminant_matrix(N)
    return data['rank']


