def parity_allowed(i: int, j: int, k: int) -> bool:
    """Check if the triple (W_i, W_j, W_k) is Z_2-parity-allowed.

    In the W_N algebra, W_w has Z_2 parity (-1)^w. A trivalent vertex
    coupling C_{ijk} vanishes unless the number of odd-weight fields is even.
    """
    n_odd = sum(1 for w in (i, j, k) if w % 2 == 1)
    return n_odd % 2 == 0


def is_gravitational(i: int, j: int, k: int) -> bool:
    """True if the triple is a pure gravitational coupling.

    Gravitational couplings: (T,T,T) = (2,2,2), (T,W_j,W_j) = (2,j,j).
    These are rational in c (determined by conformal invariance alone).
    """
    triple = tuple(sorted([i, j, k]))
    if triple == (2, 2, 2):
        return True
    if triple[0] == 2 and triple[1] == triple[2]:
        return True
    return False


def higher_spin_couplings(N: int) -> List[Tuple[int, int, int]]:
    """All parity-allowed non-gravitational higher-spin couplings for W_N.

    Returns sorted list of triples (i,j,k) with i <= j <= k, all >= 2,
    at least one index >= 3, parity-allowed, and not gravitational.
    """
    weights = list(range(2, N + 1))
    couplings = []
    for i in weights:
        for j in weights:
            if j < i:
                continue
            for k in weights:
                if k < j:
                    continue
                if not parity_allowed(i, j, k):
                    continue
                if is_gravitational(i, j, k):
                    continue
                # Must involve at least one weight >= 3
                if max(i, j, k) < 3:
                    continue
                couplings.append((i, j, k))
    return sorted(couplings)


def lollipop_couplings(N: int) -> List[Tuple[int, int, int]]:
    r"""Couplings that appear with ODD multiplicity in the genus-2 graph sum.

    These are the couplings of the form g_{j,k,k} (two equal indices) that
    appear at the genus-0 trivalent vertex of the lollipop graph of M_bar_{2,0}.

    The lollipop graph has:
    - Vertex v1: genus 1, valence 1 (one half-edge to bridge edge e1)
    - Vertex v2: genus 0, valence 3 (one half-edge to e1, two to self-loop e2)
    At v2: coupling C_{j,k,k} where j = label(e1), k = label(e2).

    This coupling appears ONCE (odd multiplicity) in this graph.

    Parity at v2: (j,k,k) must be parity-allowed.
    Since k appears twice: n_odd = (1 if j odd else 0) + 2*(1 if k odd else 0).
    This is even iff j is even. So j must be EVEN.
    For j = 2: gravitational (rational). For j >= 4 even: higher-spin (irrational).

    Self-couplings g_{k,k,k}: appear at the figure-eight graph (genus 0, two
    self-loops). The figure-eight vertex has valence 4 (4 half-edges from 2 loops),
    which gives a 4-point function. The leading OPE channel decomposes this into
    products that can produce odd powers. For even k, g_{kkk} appears once at a
    lollipop vertex with j=k.

    Returns: sorted list of parity-allowed triples (i,j,k) with i <= j <= k
    that contribute odd powers of sqrt(g_{ijk}^2) to delta_F2.
    """
    weights = list(range(2, N + 1))
    result = []

    for j in weights:
        if j < 4:
            continue  # j=2 is gravitational; j=3 is odd -> parity-forbidden
        if j % 2 == 1:
            continue  # j must be even for parity
        for k in weights:
            if k < 3:
                continue  # k=2 gives gravitational coupling
            triple = tuple(sorted([j, k, k]))
            if not parity_allowed(*triple):
                continue
            if is_gravitational(*triple):
                continue
            if triple not in result:
                result.append(triple)

    # Also include self-couplings g_{k,k,k} for EVEN k >= 4
    for k in weights:
        if k < 4 or k % 2 == 1:
            continue
        triple = (k, k, k)
        if parity_allowed(*triple) and triple not in result:
            result.append(triple)

    return sorted(result)


def odd_power_couplings(N: int) -> List[Tuple[int, int, int]]:
    """Couplings contributing odd powers to delta_F2(W_N).

    This includes:
    1. Lollipop-type couplings g_{j,k,k} (j even >= 4, k >= 3)
    2. Products g_{a,b,c} * g_{d,e,f} from banana/barbell graphs where the
       product involves an odd power of a given square root

    For the F_2 Galois analysis, what matters is which DISCRIMINANTS appear
    with odd total multiplicity. A product g_{abc} * g_{def} contributes
    sqrt(D_{abc} * D_{def}), which is F_2-dependent on D_{abc} and D_{def}
    individually. So the relevant discriminants are exactly those from the
    lollipop-type couplings.

    Additional contributions from other graphs (banana, barbell) produce
    products of square roots, but these are F_2-linearly dependent on the
    individual square roots from lollipop contributions.

    Returns: the same list as lollipop_couplings (the basis of F_2-generators).
    """
    return lollipop_couplings(N)


