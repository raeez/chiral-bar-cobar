def f2_rank(matrix: List[List[int]]) -> int:
    """Compute the rank of a matrix over F_2 (list of lists of 0/1)."""
    if not matrix:
        return 0
    m = [row[:] for row in matrix]
    nrows = len(m)
    ncols = len(m[0]) if m else 0
    rank = 0
    for col in range(ncols):
        pivot = None
        for row in range(rank, nrows):
            if m[row][col] == 1:
                pivot = row
                break
        if pivot is None:
            continue
        m[rank], m[pivot] = m[pivot], m[rank]
        for row in range(nrows):
            if row != rank and m[row][col] == 1:
                m[row] = [(m[row][j] + m[rank][j]) % 2 for j in range(ncols)]
        rank += 1
    return rank


def f2_kernel(matrix: List[List[int]]) -> List[List[int]]:
    """Compute a basis for the kernel of a matrix over F_2.

    Returns a list of F_2-vectors (lists of 0/1) that span ker(M).
    """
    if not matrix:
        return []
    m = [row[:] for row in matrix]
    nrows = len(m)
    ncols = len(m[0]) if m else 0

    # Augment with identity for tracking
    aug = [m[i] + [1 if i == j else 0 for j in range(nrows)] for i in range(nrows)]

    # Row reduce
    rank = 0
    pivot_cols = []
    for col in range(ncols):
        pivot = None
        for row in range(rank, nrows):
            if aug[row][col] == 1:
                pivot = row
                break
        if pivot is None:
            continue
        aug[rank], aug[pivot] = aug[pivot], aug[rank]
        for row in range(nrows):
            if row != rank and aug[row][col] == 1:
                aug[row] = [(aug[row][j] + aug[rank][j]) % 2
                            for j in range(ncols + nrows)]
        pivot_cols.append(col)
        rank += 1

    # Kernel vectors = rows with all-zero left part
    kernel = []
    for i in range(nrows):
        if all(aug[i][j] == 0 for j in range(ncols)):
            kernel.append(aug[i][ncols:])
    return kernel


def f2_rank_of_squarefree_classes(classes: Sequence[int]) -> int:
    """F_2-rank of a set of squarefree integers in Q*/Q*^2.

    Each integer is treated as an F_2-vector indexed by its prime support.
    """
    if not classes:
        return 0
    # Collect all primes
    all_primes: List[int] = []
    factorizations = []
    for s in classes:
        if s in (0, 1, -1):
            factorizations.append([])
            continue
        ps = _factor_squarefree(abs(s))
        factorizations.append(ps)
        for p in ps:
            if p not in all_primes:
                all_primes.append(p)
    all_primes.sort()
    # Build F_2 matrix
    mat = []
    for ps in factorizations:
        row = [1 if p in ps else 0 for p in all_primes]
        mat.append(row)
    return f2_rank(mat)


