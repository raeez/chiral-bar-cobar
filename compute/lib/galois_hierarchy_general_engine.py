def numerical_galois_at(c0: Fraction, N: int) -> Dict[str, Any]:
    """Evaluate the Galois structure at a specific rational c for W_N.

    Computes the squarefree class of each contributing coupling's value at c0,
    then computes the F_2-rank of the resulting integer classes.

    Returns the actual Galois group at c0, which may be smaller than the generic
    group (due to accidental rationalization at special c values).
    """
    couplings = odd_power_couplings(N)
    if not couplings:
        return {
            'c': c0,
            'N': N,
            'classes': {},
            'rank': 0,
            'group': 'trivial',
        }

    classes = {}
    for triple in couplings:
        fn = _G_SQUARED_FUNCTIONS.get(tuple(sorted(triple)))
        if fn is None:
            classes[triple] = None
            continue
        try:
            val = fn(c0)
            if val == 0:
                classes[triple] = 0
            else:
                classes[triple] = squarefree_kernel_rational(val)
        except ZeroDivisionError:
            classes[triple] = None

    # Compute F_2-rank of the nontrivial classes
    nontrivial = [s for s in classes.values() if s is not None and s not in (0, 1)]
    rank = f2_rank_of_squarefree_classes(nontrivial)

    if rank == 0:
        group = 'trivial'
    elif rank == 1:
        group = 'Z/2'
    else:
        group = f'(Z/2)^{rank}'

    return {
        'c': c0,
        'N': N,
        'classes': classes,
        'rank': rank,
        'group': group,
        'generic_rank': max(0, N - 2) if N >= 4 else 0,
        'collapsed': rank < (N - 2 if N >= 4 else 0),
    }


def verify_at_special_points(N: int) -> List[Dict[str, Any]]:
    """Verify the Galois structure at all special c values for W_N."""
    return [numerical_galois_at(c0, N) for c0, _label in SPECIAL_C_VALUES]


