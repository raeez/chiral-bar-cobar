def g_squared(i: int, j: int, k: int, N: int) -> Optional[Fraction]:
    r"""Evaluate g_{ijk}^2 at generic c (as a function returning Fraction).

    Only implemented for N <= 5 with exact bootstrap formulas.
    Returns None if not implemented.
    """
    triple = tuple(sorted([i, j, k]))
    if triple in _G_SQUARED_EXACT:
        return _G_SQUARED_EXACT[triple]
    return None


def g_squared_at(i: int, j: int, k: int, c0: Fraction) -> Optional[Fraction]:
    """Evaluate g_{ijk}^2 at a specific rational c = c0.

    Returns the exact Fraction value, or None if the formula is not available.
    """
    triple = tuple(sorted([i, j, k]))
    fn = _G_SQUARED_FUNCTIONS.get(triple)
    if fn is None:
        return None
    return fn(c0)


# Exact OPE structure constants as functions of Fraction c.
def _g334_sq(c: Fraction) -> Fraction:
    """g_{334}^2 = 42 c^2 (5c+22) / [(c+24)(7c+68)(3c+46)]."""
    return (Fraction(42) * c * c * (5 * c + 22)
            / ((c + 24) * (7 * c + 68) * (3 * c + 46)))


def _g444_sq(c: Fraction) -> Fraction:
    """g_{444}^2 = 112 c^2 (2c-1)(3c+46) / [(c+24)(7c+68)(10c+197)(5c+3)]."""
    return (Fraction(112) * c * c * (2 * c - 1) * (3 * c + 46)
            / ((c + 24) * (7 * c + 68) * (10 * c + 197) * (5 * c + 3)))


def _g345_sq(c: Fraction) -> Fraction:
    """g_{345}^2 = 1680 c^2 (5c+22)(2c-1) / [(c+24)(7c+68)(3c+46)(10c+197)]."""
    return (Fraction(1680) * c * c * (5 * c + 22) * (2 * c - 1)
            / ((c + 24) * (7 * c + 68) * (3 * c + 46) * (10 * c + 197)))


def _g455_sq(c: Fraction) -> Fraction:
    """g_{455}^2 = 2240 c^2 (2c-1)(3c+46)(c+2) / [(c+24)(7c+68)(5c+3)(10c+197)(2c+37)]."""
    return (Fraction(2240) * c * c * (2 * c - 1) * (3 * c + 46) * (c + 2)
            / ((c + 24) * (7 * c + 68) * (5 * c + 3) * (10 * c + 197) * (2 * c + 37)))


