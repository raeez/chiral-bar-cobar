# 11. WEIGHT-BY-WEIGHT GALOIS GROWTH
# ============================================================================

def galois_growth_table(N_max: int = 9) -> List[Dict[str, Any]]:
    """Table of Galois group growth from W_2 to W_{N_max}.

    For each N, records:
    - The new weight introduced
    - The new primitive coupling
    - The new weight-specific factor
    - The cumulative Galois rank
    - The cumulative number of contributing couplings
    """
    table = []
    for N in range(2, N_max + 1):
        entry = {
            'N': N,
            'new_weight': N if N >= 3 else None,
            'predicted_rank': max(0, N - 2) if N >= 4 else 0,
        }

        if N >= 4:
            lb = proof_lower_bound(N)
            entry['primitive_coupling'] = lb['primitive_couplings'][-1]
            entry['new_factor'] = lb['unique_factors'][-1]
            entry['new_factor_desc'] = lb['factor_descriptions'][-1]
        else:
            entry['primitive_coupling'] = None
            entry['new_factor'] = None
            entry['new_factor_desc'] = None

        couplings = odd_power_couplings(N)
        entry['n_couplings'] = len(couplings)
        entry['couplings'] = couplings

        gdata = galois_group_wn(N)
        entry['computed_rank'] = gdata['rank']
        entry['verified'] = gdata['verified']

        table.append(entry)

    return table


# ============================================================================
# 12. FIELD EXTENSION AT RATIONAL C
# ============================================================================

def field_extension_at(c0: Fraction, N: int) -> Dict[str, Any]:
    """Full field extension data at a rational c value for W_N.

    Returns the squarefree classes, the field degree, the Galois group,
    and whether the Galois group is collapsed relative to the generic group.
    """
    data = numerical_galois_at(c0, N)

    nontrivial_classes = {k: v for k, v in data['classes'].items()
                          if v is not None and v not in (0, 1)}

    # Compute the field: Q(sqrt(s_1), sqrt(s_2), ..., sqrt(s_r))
    sqrts = list(set(nontrivial_classes.values()))
    field_generators = [f'sqrt({s})' for s in sqrts]

    return {
        'c': c0,
        'N': N,
        'classes': data['classes'],
        'nontrivial_classes': nontrivial_classes,
        'rank': data['rank'],
        'group': data['group'],
        'field_degree': 1 << data['rank'],
        'field_generators': field_generators,
        'generic_rank': data['generic_rank'],
        'collapsed': data['collapsed'],
    }


