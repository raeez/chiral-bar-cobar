# For N >= 6, the exact OPE formulas are not hardcoded, but the FACTOR STRUCTURE
# is determined by the W-algebra bootstrap. The key structural inputs:
#
# 1. UNIVERSAL FACTORS: (c+24) and (7c+68) appear in the denominator of EVERY
#    higher-spin coupling for N >= 4 (from the universal Virasoro Kac determinant).
#
# 2. WEIGHT-SPECIFIC FACTORS: each weight w >= 3 introduces at least one new
#    linear factor phi_w(c) = (a_w * c + b_w) that appears in the Kac determinant
#    at weight w but NOT at any lower weight.
#
# 3. SHARING PATTERN: the denominator of g_{ijk}^2 contains ALL factors from
#    the Kac determinants at weights i, j, k. The numerator contains factors
#    from the null-vector conditions.
#
# From the Miura transform (free-field realization of W_N), the Kac determinant
# at weight h in the W_N algebra is:
#
#   det_h^{W_N}(c) = prod_{subsets S of {1,...,N-1}} (c - c_S)
#
# where c_S are specific rational numbers. The LEADING new factor at weight w
# is the one indexed by the subset S = {w-1} (single element), giving:
#
#   c_{w-1} = (N-1) - N(N^2-1) / (w(w-1))
#
# This determines the weight-w-specific factor (a_w * c + b_w).

def weight_specific_factor(w: int, N: int) -> Tuple[int, int]:
    r"""The leading new factor (b, a) for (a*c + b) introduced at weight w.

    From the Miura transform, the critical central charge at weight w is:
        c_w = (N-1) - N(N^2 - 1) / (w(w-1))

    Writing c_w = -b_w / a_w in lowest terms gives the factor (a_w * c + b_w).

    For example:
        w=3, N=4: c_3 = 3 - 4*15/6 = 3 - 10 = -7 -> (7c + ...)
        Hmm, let me compute more carefully.

    Actually the precise formula depends on the normalization. Let me use
    the known values directly for small w and derive the pattern.
    """
    # The weight-specific factor comes from the condition that the Shapovalov
    # determinant at weight w has a new zero. From the free-field realization:
    #
    # For principal W_N: c = (N-1)(1 - N(N+1) t^2)
    # The weight-w Kac determinant zero at level 1 occurs at:
    #   t^2 = 1/(w(w-1))  (from the Miura eigenvalue condition)
    # giving c_w = (N-1)(1 - N(N+1)/(w(w-1)))
    #            = (N-1) - (N-1)*N*(N+1) / (w*(w-1))
    #
    # For the W_N-INDEPENDENT factor (the one that is new at weight w and
    # does not depend on N), we need the factor from the universal
    # Virasoro sub-determinant. From the Kac formula for the Virasoro algebra:
    #   c_{r,s} = 1 - 6(p-q)^2/(pq) at p/q = r/s
    # The factor at weight w with r*s = w is (p*q*c - p*q + 6(p-q)^2).

    return _WEIGHT_FACTORS.get(w, _compute_weight_factor(w))


# Known weight-specific factors (verified against the exact OPE formulas).
