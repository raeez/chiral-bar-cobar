def _is_perfect_square_int(n: int) -> bool:
    """Check if a nonnegative integer is a perfect square."""
    if n < 0:
        return False
    if n == 0:
        return True
    s = isqrt(n)
    return s * s == n


def is_rational_square(q: Fraction) -> bool:
    """True iff q >= 0 and q is a square in Q."""
    if q < 0:
        return False
    if q == 0:
        return True
    return _is_perfect_square_int(q.numerator) and _is_perfect_square_int(q.denominator)


def squarefree_kernel_int(n: int) -> int:
    """Squarefree part of |n|: product of primes appearing to odd power."""
    if n == 0:
        return 0
    n = abs(n)
    out = 1
    p = 2
    while p * p <= n:
        e = 0
        while n % p == 0:
            n //= p
            e += 1
        if e % 2 == 1:
            out *= p
        p += 1
    if n > 1:
        out *= n
    return out


def squarefree_kernel_rational(q: Fraction) -> int:
    """Squarefree integer s representing the class of q in Q*/Q*^2.

    For q = a/b in lowest terms: s = squarefree(a*b), with sign.
    """
    if q == 0:
        return 0
    sign = 1 if q > 0 else -1
    a = abs(q.numerator)
    b = abs(q.denominator)
    return sign * squarefree_kernel_int(a * b)


def _factor_squarefree(n: int) -> List[int]:
    """Prime factorization of a squarefree positive integer."""
    if n <= 1:
        return []
    n = abs(n)
    primes = []
    p = 2
    while p * p <= n:
        if n % p == 0:
            primes.append(p)
            n //= p
        else:
            p += 1
    if n > 1:
        primes.append(n)
    return primes


