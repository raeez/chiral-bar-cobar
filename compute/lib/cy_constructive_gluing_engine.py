def verify_k3_noether() -> bool:
    r"""Noether formula for K3: chi(O_S) = (c_1^2 + c_2) / 12.

    c_1(K3) = 0 (trivial canonical), c_2(K3) = chi_{top}(K3) = 24.
    chi(O_S) = (0 + 24) / 12 = 2.  Check: chi(O_S) = h^0 - h^1 + h^2 = 1 - 0 + 1 = 2.
    """
    return (0 + 24) // 12 == 2


def verify_k3_signature() -> bool:
    r"""Hirzebruch signature theorem for K3: sigma = (1/3)(2c_2 + c_1^2 - 4chi).

    Actually: sigma(K3) = (1/3) p_1(K3) where p_1 = c_1^2 - 2c_2.
    p_1 = 0 - 2*24 = -48.  sigma = -48/3 = -16.

    Cross-check: b_2^+ = 3, b_2^- = 19.  sigma = 3 - 19 = -16.  CONSISTENT.
    """
    p1 = 0 - 2 * 24
    sigma_formula = p1 // 3
    sigma_direct = 3 - 19
    return sigma_formula == sigma_direct == -16


def verify_kummer_euler() -> bool:
    r"""Euler characteristic of Kummer surface.

    chi(Kum) = 24 (it's a K3!).

    Via the blow-up formula:
    chi(Kum) = chi((ExE)/Z_2) + 16 * (chi(P^1) - chi(pt))
             = chi((ExE)/Z_2) + 16 * (2 - 1)
             = chi((ExE)/Z_2) + 16.

    chi(ExE) = chi(E)^2 = 0.
    chi((ExE)/Z_2) = (chi(ExE) + chi(ExE)^{Z/2}) / 2  (NOT directly;
    for orbifolds: chi((ExE)/Z_2) = chi^{orb} = (chi(ExE) + 16)/2 = 8,
    using the fact that Z/2 has 16 fixed points with chi(pt) = 1 each.)

    Actually: for the ORBIFOLD (not resolution):
    chi((ExE)/Z_2) = (0 + 16)/2 = 8.  (Generic orbifold formula.)
    After blowing up 16 points: chi(Kum) = 8 + 16 = 24.

    Cross-check: 24 = chi(K3).  CORRECT.
    """
    chi_orbifold = (0 + 16) // 2  # = 8
    chi_kummer = chi_orbifold + 16  # = 24
    return chi_kummer == K3_EULER_CHAR


def verify_product_euler() -> bool:
    r"""chi(K3 x E) = chi(K3) * chi(E) = 24 * 0 = 0.

    Cross-check via Betti numbers: sum (-1)^k b_k = 0.
    """
    product = K3_EULER_CHAR * 0
    betti_check = k3e_euler_from_betti()
    hodge_check = k3e_euler_from_hodge()
    return product == 0 and betti_check == 0 and hodge_check == 0


def verify_mukai_lattice_signature() -> bool:
    r"""Mukai lattice has signature (4, 20).

    U^4 has signature (4, 4).
    E_8(-1)^2 has signature (0, 16).
    Total: (4, 20).

    Cross-check: 4 + 20 = 24 = rank.  4 - 20 = -16 = sigma(K3).
    """
    sig_plus = 4
    sig_minus = 20
    return (sig_plus + sig_minus == K3_MUKAI_RANK and
            sig_plus - sig_minus == -16)
