def brauer_group_rank(picard_rank: int) -> int:
    r"""Rank of the torsion-free part of the Brauer group of K3.

    Br(K3) = H^2_{an}(S, O*_S)_{tors} is a torsion group.
    The Brauer group fits in the exponential sequence:
        H^2(S, Z) -> H^2(S, O_S) -> H^2(S, O*_S) -> H^3(S, Z) = 0

    So Br(S) = H^2(S, O_S) / im(H^2(S, Z)) up to torsion.
    Since H^2(S, O_S) = C (for K3) and H^2(S, Z) is a lattice,
    the analytic Brauer group Br'(S) = (Q/Z)^{b_2 - rho}
    where b_2 = 22 and rho = Picard rank.

    Actually: the transcendental lattice T(S) has rank 22 - rho,
    and Br(S) ~ Hom(T(S), Q/Z) ~ (Q/Z)^{22 - rho} (as abstract groups,
    ignoring the lattice structure).

    The rank of this group (as a Q/Z-module) is 22 - rho.
    """
    if picard_rank < 1 or picard_rank > 20:
        raise ValueError(f"Picard rank must be in [1, 20], got {picard_rank}")
    return 22 - picard_rank


def brauer_obstruction_trivial_for_untwisted() -> bool:
    r"""The Brauer obstruction is trivial for the UNTWISTED derived category.

    D^b(S) (untwisted sheaves) has trivial B-field, so the gluing of
    local charts encounters NO Brauer obstruction.

    Twisted categories D^b(S, alpha) for alpha in Br(S) DO have
    nontrivial obstruction.

    Returns True.
    """
    return True


def brauer_group_data(picard_rank: int) -> Dict[str, Any]:
    r"""Full Brauer group analysis for a K3 surface.

    Returns dict with obstruction data for the gluing problem.
    """
    br_rank = brauer_group_rank(picard_rank)
    return {
        'picard_rank': picard_rank,
        'transcendental_rank': 22 - picard_rank,
        'brauer_rank': br_rank,
        'brauer_group': f'(Q/Z)^{{{br_rank}}}',
        'untwisted_obstruction': 'trivial',
        'twisted_obstruction': 'nontrivial' if br_rank > 0 else 'trivial',
        'generic_br_rank': 21,  # for rho = 1
        'maximal_br_rank': 0 if picard_rank == 22 else 22 - picard_rank,
        # Note: picard_rank can be at most 20 for K3
    }


