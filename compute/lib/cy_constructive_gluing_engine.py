def k3_no_exceptional_collection() -> bool:
    r"""D^b(K3) admits no full exceptional collection.

    Proof (Bridgeland, Huybrechts):
    1. K3 has trivial canonical bundle, so Serre functor S = [2].
    2. Any admissible subcategory A of D^b(K3) inherits Serre functor [2].
    3. A full exceptional collection would decompose D^b(K3) into
       pieces with Serre = [2], but exceptional objects have
       trivial Serre action -- contradiction.

    More precisely: if D^b(K3) = <E_1, ..., E_m> were a full exceptional
    collection, then K_0(K3) would be free of rank m with the Euler form
    being UPPER TRIANGULAR with 1's on the diagonal.  But chi(E,E) = 2
    for any spherical object on K3 (not 1), and the Euler form on K3
    is SYMMETRIC (surface Serre duality), not upper triangular.

    Returns True (this is a theorem, not a computation).
    """
    return True


def k3_spherical_object_self_ext() -> Dict[int, int]:
    r"""Ext^*(E, E) for a spherical object E on K3.

    A spherical object on a K3 surface satisfies:
        Ext^k(E, E) = H^k(S^2) = C if k in {0, 2}, else 0.

    So: ext^0 = 1, ext^1 = 0, ext^2 = 1.
    Euler form: chi(E, E) = 1 - 0 + 1 = 2.

    Examples: O_S, O_C (structure sheaf of a rational curve), ideal sheaves
    of 0-cycles, etc.
    """
    return {0: 1, 1: 0, 2: 1}


def k3_spherical_euler_self() -> int:
    r"""chi(E, E) for a spherical object E on K3.

    chi = 1 - 0 + 1 = 2.
    """
    exts = k3_spherical_object_self_ext()
    return sum((-1)**k * v for k, v in exts.items())


def k3_min_k0_generators() -> int:
    r"""Minimum number of objects needed to generate K_0(K3) = Z^{24}.

    Since K_0(K3) = Z^{24}, we need at least 24 objects whose K-theory
    classes form a basis (or at least a set of rank-24 generators).

    This is the K-THEORETIC minimum.  CATEGORICAL generation may require
    fewer objects (since twist functors expand the generated subcategory).
    """
    return K3_MUKAI_RANK


def k3_spherical_twist_square_shift() -> int:
    r"""The Seidel-Thomas theorem: T_E^2 = [-2] on K3.

    For a spherical object E with Ext*(E,E) = H*(S^n), the spherical
    twist satisfies T_E^{n+1} = [-(n+1)] for n = dim(S^n)/2.
    On K3 (2-spherical): T_E^2 = [-2].

    Returns the shift: -2.
    """
    return -2


def k3_spherical_twist_k0_action(mukai_class: np.ndarray,
                                  object_class: np.ndarray,
                                  mukai_pairing: np.ndarray) -> np.ndarray:
    r"""Action of spherical twist T_E on K_0(K3) = Mukai lattice.

    T_E(v) = v + <v, [E]>_Mukai * [E]

    This is a REFLECTION in the Mukai lattice (a Picard-Lefschetz reflection).

    Parameters
    ----------
    mukai_class : np.ndarray  -- the Mukai vector v to be twisted (length 24)
    object_class : np.ndarray  -- the Mukai vector [E] of the spherical object
    mukai_pairing : np.ndarray  -- the 24x24 Mukai pairing matrix

    Returns
    -------
    np.ndarray : T_E(v) = v + <v, [E]> * [E]
    """
    inner = int(mukai_class @ mukai_pairing @ object_class)
    return mukai_class + inner * object_class


def k3_generation_by_spherical_objects(picard_rank: int = 1) -> Dict[str, Any]:
    r"""Analysis of categorical generation of D^b(K3) by spherical objects.

    For a K3 with Picard rank rho:
    - The Picard lattice Pic(S) has rank rho, embedded in H^{1,1}(S,Z)
    - The transcendental lattice T(S) has rank 22 - rho
    - The Mukai lattice has rank 24

    Spherical objects O_S, O_S(H), O_C (for rational curves C) generate
    under the action of Aut(D^b(K3)).

    The autoequivalence group acts on the Mukai lattice by oriented Hodge
    isometries.  The orbit of [O_S] = (1, 0, 1) generates:
    - At minimum: the sublattice spanned by reflections of (1, 0, 1)
    - This spans the FULL Mukai lattice for generic K3 (by Orlov + Mukai)

    Returns dict with generation data.
    """
    transcendental_rank = 22 - picard_rank
    algebraic_mukai_rank = 2 + picard_rank  # H^0 + Pic + H^4

    return {
        'picard_rank': picard_rank,
        'transcendental_rank': transcendental_rank,
        'mukai_rank': K3_MUKAI_RANK,
        'algebraic_mukai_rank': algebraic_mukai_rank,
        'min_spherical_generators': K3_MUKAI_RANK,
        'has_exceptional_collection': False,
        'generation_method': 'spherical_twists',
        'twist_order': k3_spherical_twist_square_shift(),
    }


