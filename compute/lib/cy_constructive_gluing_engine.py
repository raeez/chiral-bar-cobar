def transition_bimodule_euler_char(chi_matrix_1: np.ndarray,
                                    chi_matrix_2: np.ndarray,
                                    overlap_chi: np.ndarray) -> int:
    r"""Euler characteristic of the transition bimodule.

    For tilting generators T_1, T_2 on overlapping charts:
        chi(M) = chi(RHom(T_1|_U12, T_2|_U12))
               = trace of the overlap Euler form.

    In the simplified model: chi(M) = tr(overlap_chi).
    """
    return int(np.trace(overlap_chi))


def a1_flop_transition_dimension() -> Dict[str, int]:
    r"""Transition bimodule data for the A_1 flop.

    The A_1 flop: X -> X' related by flopping a single (-1,-1)-curve.
    D^b(X) ~ D^b(X') via the flop functor (Bondal-Orlov, Bridgeland).

    The flop functor is a Fourier-Mukai transform with kernel = structure
    sheaf of the fiber product, modified at the flopping curve.

    For the LOCAL model (conifold transition):
    - Chart 1: O_P^1(-1) -> C^3 (resolved)
    - Chart 2: O_P^1(-1) -> C^3 (flopped)
    - Transition: RHom(O(-1), O(-1)) over the common base

    The Ext groups for the A_1 (conifold) flop:
        Ext^0(O_C(-1), O_C(-1)) = C (identity)
        Ext^1(O_C(-1), O_C(-1)) = 0 (rigid curve)
        Ext^2(O_C(-1), O_C(-1)) = C (by Serre duality on CY3)

    Total: dim RHom = 1 + 0 + 1 = 2.
    Euler char: chi = 1 - 0 + 1 = 2 (but wait, on CY3 chi = 0 for
    self-Ext of any object -- so this must be 0 for a genuine CY3 object).

    CORRECTION: on a CY3, chi(E, E) = 0 for any E (by Serre duality:
    ext^k = ext^{3-k}, so chi = 0).

    For the (-1,-1)-curve C in a CY3:
        ext^0(O_C(-1), O_C(-1)) = 1
        ext^1(O_C(-1), O_C(-1)) = 0 (rigid)
        ext^2(O_C(-1), O_C(-1)) = 0 (rigid)
        ext^3(O_C(-1), O_C(-1)) = 1 (Serre dual of ext^0)

    chi = 1 - 0 + 0 - 1 = 0.  CORRECT for CY3.
    Total dim: 1 + 0 + 0 + 1 = 2.
    """
    return {
        'ext0': 1,
        'ext1': 0,
        'ext2': 0,
        'ext3': 1,
        'total_dim': 2,
        'euler_char': 0,  # CY3 constraint
    }


def conifold_transition_bimodule() -> Dict[str, Any]:
    r"""Full transition bimodule data for the conifold (A_1) transition.

    Two tilting generators T_+ (resolution) and T_- (flop) on the
    conifold X and its flop X'.

    T_+ = O_X (+) O_C(-1) on X (2 summands)
    T_- = O_{X'} (+) O_{C'}(-1) on X' (2 summands)

    The transition bimodule M = RHom(T_+|_U, T_-|_U) over the common
    smooth locus U = X \ C = X' \ C'.

    Over U: T_+|_U = O_U (+) O_U (both restrict to O_U since C is
    contracted), so M is a free (End(T_+|_U), End(T_-|_U))-bimodule
    of rank 1 in each component.

    The Euler form of the RESOLVED conifold (2 objects: O, O_C(-1)):
        chi(O, O) = 0       (on CY3)
        chi(O, O_C(-1)) = chi(O_C(-1)) = ... depends on resolution
        chi(O_C(-1), O) = -chi(O, O_C(-1))  (antisymmetry on CY3)
        chi(O_C(-1), O_C(-1)) = 0

    For the resolved conifold Tot(O(-1)+O(-1) -> P^1):
        chi(O_C(-1)) = h^0(O_C(-1)) = 0 (negative degree on P^1)
        chi(O, O_C(-1)) = 0 - 0 + 0 - 0 = 0 actually.

    Let me use the K-theory: K_0 = Z^2 with basis {[O], [O_C(-1)]}.
    Euler matrix on the resolved conifold (local CY3):
        [[ 0, -1],
         [ 1,  0]]

    (This is the standard antisymmetric form for the A_1 quiver on a CY3.)
    """
    euler_matrix = np.array([[0, -1], [1, 0]], dtype=int)
    return {
        'euler_matrix': euler_matrix,
        'k0_rank': 2,
        'is_antisymmetric': True,
        'flop_kernel_rank': 1,
        'description': 'A_1 conifold transition bimodule',
    }


