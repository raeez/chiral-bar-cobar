def pn_euler_form_upper_triangular(n: int) -> bool:
    r"""Check that the Euler form matrix is upper triangular on P^n.

    For a full exceptional collection <E_1, ..., E_m>:
      chi(E_i, E_j) = 0 for i > j  (orthogonality)
      chi(E_i, E_i) = 1  (exceptional => Hom = C, higher Ext = 0)

    So the matrix should be upper triangular with 1's on the diagonal.

    For the Beilinson collection on P^n with O(0), ..., O(n):
      chi(O(i), O(j)) = 0 for i > j (since H^*(O(j-i)) = 0 for j < i
      with j-i in {-1, ..., -n}, which is in the vanishing range for
      0 < |j-i| < n+1).

    Wait: H^n(O(m)) for m = -(n+1) is binom(n, n) = 1.
    So chi(O(n), O(0)) = (-1)^n * 1 for j-i = -n, i.e. chi(O(n), O(0)) = (-1)^n.
    This is NOT zero for n > 0!

    So the Beilinson collection on P^n does NOT give an upper triangular
    Euler form for n >= 2.  The issue: for non-exceptional collection
    (meaning chi(E_i, E_j) != 0 for some i > j), the Euler form matrix
    has more structure.

    Actually: the FULL STRONG exceptional collection has Ext^k(E_i, E_j) = 0
    for all k when i > j.  Let me recheck:
      For i > j: O(i), O(j) with i > j.
      Ext^k(O(i), O(j)) = H^k(O(j-i)) with j-i < 0.
      H^0(O(m)) = 0 for m < 0.  Check.
      H^k(O(m)) = 0 for 0 < k < n (Bott vanishing).  Check.
      H^n(O(m)) = H^0(O(-m-n-1))^* by Serre duality.
        For m = j-i with -n <= j-i <= -1:
        -m-n-1 = i-j-n-1.  For i-j in {1,...,n}: -m-n-1 in {-n, ..., -1}.
        Since -m-n-1 < 0: H^0(O(-m-n-1)) = 0.  So H^n(O(j-i)) = 0.  Check.

    So Ext^k(O(i), O(j)) = 0 for ALL k when i > j AND 1 <= i-j <= n.
    This confirms: the Euler form IS upper triangular for the Beilinson
    collection on P^n.
    """
    chi = pn_euler_form_matrix(n)
    # Check lower triangle is zero
    for i in range(n + 1):
        for j in range(i):
            if chi[i, j] != 0:
                return False
    # Check diagonal is 1
    for i in range(n + 1):
        if chi[i, i] != 1:
            return False
    return True


def pn_euler_form_determinant(n: int) -> int:
    r"""Determinant of the Euler form on P^n.

    Since the Euler form is upper triangular with 1's on diagonal,
    det = 1.
    """
    chi = pn_euler_form_matrix(n)
    return int(round(np.linalg.det(chi.astype(float))))


