r"""cy_constructive_gluing_engine.py -- Constructive gluing of D^b(K3 x E)
from finitely many quiver charts.

MATHEMATICAL CONTENT
====================

=== 1. THE CONSTRUCTIVE GLUING QUESTION ===

Given a Calabi-Yau 3-fold X = K3 x E, can D^b(X) be explicitly recovered
from finitely many quiver charts with explicit transition data?

The answer depends on the K3 surface S:

(a) For S with ADE singularities: each singular point provides a quiver chart
    via the McKay correspondence.  The resolved exceptional locus generates a
    subcategory D^b(mod-Pi(Q_Gamma)).

(b) For smooth S: no full exceptional collection exists (Bridgeland 1999),
    but D^b(S) is generated by spherical objects and their twist functors.

(c) For orbifold K3 (Kummer): BKR gives D^b(Kum(A)) ~ D^b_G(A) where
    A = E x E and G = Z/2.  This is the most explicit constructive case.

=== 2. WARM-UP: PROJECTIVE SPACE ===

For P^n, there is a FULL exceptional collection (Beilinson):
    D^b(P^n) = <O, O(1), ..., O(n)>

One chart suffices: End(O + O(1) + ... + O(n)) is the Beilinson quiver with
potential.  The tilting generator T = O + O(1) + ... + O(n) satisfies:
    dim Ext^k(T, T) computed by Bott vanishing.

For P^2: T = O + O(1) + O(2).
    Ext^*(O(i), O(j)) = H*(P^2, O(j-i)).
    By Bott vanishing on P^n: H^k(P^n, O(m)) = 0 for 0 < k < n, all m.
    H^0(P^2, O(m)) = binom(m+2, 2) for m >= 0, else 0.
    H^2(P^2, O(m)) = H^0(P^2, O(-m-3))^* = binom(-m-1, 2) for m <= -3, else 0.

=== 3. K3 SURFACE: NO EXCEPTIONAL COLLECTIONS ===

D^b(K3) admits NO full exceptional collection (Bridgeland, Huybrechts).
Proof sketch: Any admissible subcategory inherits Serre functor [2].
The unique indecomposable category with Serre = [2] is D^b(K3) itself.

However, K(K3) = Z^{24} (Mukai lattice), and D^b(K3) IS generated by
finitely many objects (any spanning class for K-theory + thick generation).

Minimum generating set: The minimum number of objects whose K-theory classes
span K_0(K3) = Z^{24} is 24 (= rank of Mukai lattice).  But categorical
generation requires more: the objects must generate D^b(K3) as a THICK
SUBCATEGORY.

For a K3 with Picard rank rho:
  - The algebraic lattice has rank 2 + rho
  - Spherical objects: O_S (one), O_S(nH) for each n
  - Point sheaves: O_p for p in S (continuous family)
  - The Mukai lattice Lambda = U^4 + E_8(-1)^2 of rank 24

CATEGORICAL GENERATION: A single spherical object O_S and its twists
generate D^b(K3) only if the orbit of [O_S] under Aut(D^b(K3)) spans
the Mukai lattice.  For generic K3, this requires composing line bundle
twists and spherical twist functors.

=== 4. TRANSITION BIMODULES ===

For two tilting generators T_1, T_2 on overlapping open sets U_1, U_2:
    The transition data is M = RHom(T_1|_{U_{12}}, T_2|_{U_{12}}).

M carries an (End(T_1), End(T_2))-bimodule structure (A-infinity).
The gluing condition: the triple composition on U_{123} must be associative
up to coherent homotopy.

For the A_1 flop (simplest case):
    Two charts related by the flop autoequivalence.
    The transition bimodule has dimension computable from Ext groups.

=== 5. K-THEORY VERIFICATION ===

If n charts with tilting generators T_1, ..., T_n glue to reconstruct
D^b(X), then:
    K_0(glued) = Z^{rk K_0(X)}

For X = K3 x E: K_0(X) = Z^{48}.

The Mayer-Vietoris sequence in K-theory:
    ... -> K_1(U_{ij}) -> K_0(X) -> bigoplus K_0(U_i) -> bigoplus K_0(U_{ij}) -> ...

=== 6. OBSTRUCTION ANALYSIS ===

The Brauer group Br(S) obstructs SHEAF-THEORETIC gluing when a nontrivial
B-field is present.  For a K3 surface:
    Br(K3) = (Q/Z)^{22 - rho}

where rho = Picard number.  For generic K3 (rho = 1): Br = (Q/Z)^{21}.

The Brauer obstruction lives in H^2(S, O*_S) -> H^2(S, G_m).
It measures the failure of a twisted sheaf to be an honest sheaf.

For the UNTWISTED derived category D^b(S), the Brauer class is trivial
and the gluing proceeds without obstruction.  Twisted derived categories
D^b(S, alpha) for alpha in Br(S) require alpha-twisted transition data.

=== 7. KUMMER SURFACE: EXPLICIT CONSTRUCTION ===

For S = Kum(E x E) (Kummer K3 = blow-up of (E x E)/(Z/2)):

The BKR equivalence: D^b(Kum) ~ D^b_{Z/2}(E x E).

The Z/2-equivariant category has:
  - BULK sector: equivariant sheaves pulled back from E x E
  - EXCEPTIONAL sector: 16 exceptional objects from the 16 fixed points
    (the 2-torsion points of E x E, which are the blown-up singularities)

K-theory: K_0(Kum) = Z^{24} splits as:
  - Invariant part from E x E: K_0(E x E)^{Z/2} has rank ...
  - Fixed-point contribution: Z^{16} from the 16 exceptional (-2)-curves

The K-theory decomposition:
  K_0(E) = Z^2 with basis {[O_E], [O_p]}
  K_0(E x E) = Z^4 with basis {[O_E] bx [O_E], [O_E] bx [O_p],
                                 [O_p] bx [O_E], [O_p] bx [O_p]}
  Under Z/2 involution sigma: (x,y) -> (-x,-y):
    sigma*[O_E bx O_E] = [O_E bx O_E]  (structure sheaf is invariant)
    sigma*[O_p bx O_q] = [O_{-p} bx O_{-q}] (point sheaves permuted)

  The INVARIANT part of K_0(E x E) under Z/2 depends on the specific
  action on K-theory.

  For the Kummer lattice:
    rk K_0(Kum) = 24  (it IS a K3)
    Picard lattice of Kum: rho >= 17 (16 exceptional + pullback of NS(ExE))
      For generic E: NS(E) = Z, so NS(ExE) has rank 2+1 = 3 (product + diagonal),
      giving rho(Kum) = 16 + 3 = 19.  (Or rho = 18 for generic; 20 for CM.)

The Kummer lattice K_{Kum} is the sublattice of H^2(Kum, Z) generated
by the 16 exceptional classes.  It is an EVEN lattice of rank 16 and
discriminant group (Z/2)^6.

MULTI-PATH VERIFICATION
=======================
Path 1: K-theory rank computation (lattice rank, Mukai)
Path 2: Euler form algebra (Kunneth, antisymmetry on CY3)
Path 3: Hodge theory (HKR, Kunneth product)
Path 4: McKay correspondence (ADE quiver K-theory)
Path 5: BKR equivalence (equivariant K-theory)
Path 6: Brauer group computation (cohomological)
Path 7: Exceptional collection theory (Serre functor constraints)
Path 8: Kummer lattice theory (discriminant, embedding)
Path 9: Spherical object analysis (twist composition)
Path 10: Localization sequence (Mayer-Vietoris in K-theory)

BEILINSON WARNINGS
==================
AP1:  Formulas are family-specific (P^n vs K3 vs Kummer).
AP3:  Never pattern-complete from P^n to K3.
AP5:  Propagate corrections across all related engines.
AP10: Cross-verify K-theory ranks by multiple methods.
AP35: D^b(K3) indecomposability is a THEOREM (Bridgeland), not obvious.
AP38: Mukai lattice conventions: Lambda_{4,20}, NOT Lambda_{3,19}.
      chi(O_S) = 2 for K3 (NOT 1).
AP42: BKR is proved for Kummer; the general K3 gluing is harder.
"""

