def spherical_twist_matrix(v: np.ndarray, pairing: np.ndarray) -> np.ndarray:
    r"""Matrix of the spherical twist T_v on the lattice.

    T_v(w) = w + <w, v> v.

    The matrix representation: (T_v)_{ij} = delta_{ij} + v_i * (P @ v)_j
    where P is the pairing matrix.

    Actually: <w, v> = w^T P v, so T_v(w) = w + (w^T P v) v = w + v (v^T P^T w).
    In matrix form: T_v = I + v (P^T v)^T ... no.

    T_v(w) = w + <w, v>_P * v where <w, v>_P = w^T P v.
    As a linear map: T_v = I + (outer product) v otimes (P^T ... )

    Let me just write it out:
    T_v(w)_i = w_i + (sum_j w_j P_{jk} v_k) * v_i
             = w_i + v_i * (sum_{j,k} P_{jk} v_k w_j)
             = (delta_{ij} + v_i * sum_k P_{jk} v_k) w_j

    So the matrix M_{ij} = delta_{ij} + v_i * (P v)_j.

    In matrix notation: M = I + v (Pv)^T = I + np.outer(v, P @ v).
    """
    Pv = pairing @ v
    n = len(v)
    M = np.eye(n, dtype=int) + np.outer(v, Pv).astype(int)
    return M


def orbit_rank(generators: List[np.ndarray], lattice_rank: int,
               max_products: int = 3) -> int:
    r"""Rank of the sublattice generated by vectors and their images under
    compositions of spherical twist matrices.

    This is a simplified computation: we apply twist matrices up to
    max_products compositions and track the rank of the span.

    Parameters
    ----------
    generators : list of np.ndarray  -- initial Mukai vectors
    lattice_rank : int  -- rank of the ambient lattice
    max_products : int  -- max depth of twist compositions

    Returns
    -------
    int : rank of the generated sublattice
    """
    if not generators:
        return 0

    # Collect all vectors we can produce
    vectors = list(generators)

    # For a simple estimate: check the rank of the span
    if len(vectors) == 0:
        return 0

    mat = np.array([v for v in vectors], dtype=int)
    return int(np.linalg.matrix_rank(mat.astype(float)))


