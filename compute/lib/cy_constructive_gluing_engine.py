def kummer_lattice_gram_matrix() -> np.ndarray:
    r"""Gram matrix of the Kummer lattice (rank 16, in the standard basis
    of exceptional classes).

    In the NAIVE basis {e_1, ..., e_{16}} of exceptional classes:
        e_i . e_j = -2 delta_{ij}.

    So the Gram matrix is -2 I_{16}.

    NOTE: This is the Gram matrix for the sublattice generated by the
    individual exceptional classes.  The FULL Kummer lattice includes
    half-sums, which extend this to an index-2^5 overlattice.
    """
    return -2 * np.eye(16, dtype=int)


def kummer_naive_lattice_det() -> int:
    r"""Determinant of the naive lattice (span of exceptional classes).

    det(-2 I_{16}) = (-2)^{16} = 2^{16} = 65536.
    """
    return (-2)**16


def kummer_overlattice_index() -> int:
    r"""Index of the naive lattice inside the full Kummer lattice.

    The Kummer lattice K has discriminant 2^6, while the naive lattice
    has det = 2^{16}.  Since |det(K)| = |det(naive)| / [K : naive]^2:
        2^6 = 2^{16} / [K : naive]^2
        [K : naive]^2 = 2^{10}
        [K : naive] = 2^5 = 32.
    """
    return 2**5


def kummer_half_sum_count() -> int:
    r"""Number of independent half-sums (e_i + e_j + e_k + e_l)/2.

    The 16 fixed points of Z/2 on E x E are labeled by elements of
    (Z/2)^4 (the group E[2] x E[2]).  The half-sums form the
    extended lattice.

    The number of independent half-sums that extend the naive lattice
    is 5 (giving index 2^5 = 32).

    These correspond to the 5 independent "doubly-even" subsets of
    size 4 in the labeling by (Z/2)^4, modulo the overall parity.
    """
    return 5


def kummer_transcendental_rank(picard_rank: int = 18) -> int:
    r"""Rank of the transcendental lattice of the Kummer surface.

    T(Kum) has rank 22 - rho(Kum).
    For generic Kummer: 22 - 18 = 4.
    """
    return 22 - picard_rank


