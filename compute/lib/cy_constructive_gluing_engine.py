def k3_hodge_diamond() -> Dict[Tuple[int, int], int]:
    r"""Hodge diamond of K3.

    h^{p,q}(K3):
              1
           0     0
        1    20    1
           0     0
              1
    """
    return {
        (0, 0): 1,
        (1, 0): 0, (0, 1): 0,
        (2, 0): 1, (1, 1): 20, (0, 2): 1,
        (2, 1): 0, (1, 2): 0,
        (2, 2): 1,
    }


def elliptic_hodge_diamond() -> Dict[Tuple[int, int], int]:
    r"""Hodge diamond of an elliptic curve.

    h^{p,q}(E):
        1
      1   1
        1
    """
    return {(0, 0): 1, (1, 0): 1, (0, 1): 1, (1, 1): 1}


def k3e_hodge_numbers() -> Dict[Tuple[int, int], int]:
    r"""Hodge numbers of K3 x E via Kunneth.

    h^{p,q}(S x E) = sum_{p1+p2=p, q1+q2=q} h^{p1,q1}(S) * h^{p2,q2}(E).
    """
    h_s = k3_hodge_diamond()
    h_e = elliptic_hodge_diamond()
    result = {}
    for p in range(4):
        for q in range(4):
            val = 0
            for p1 in range(3):   # K3 is dim 2
                p2 = p - p1
                if p2 < 0 or p2 > 1:  # E is dim 1
                    continue
                for q1 in range(3):
                    q2 = q - q1
                    if q2 < 0 or q2 > 1:
                        continue
                    val += h_s.get((p1, q1), 0) * h_e.get((p2, q2), 0)
            result[(p, q)] = val
    return result


def k3e_euler_from_hodge() -> int:
    r"""Euler characteristic of K3 x E from Hodge numbers.

    chi = sum (-1)^{p+q} h^{p,q}.
    For K3 x E: chi = 0 (since chi(K3) * chi(E) = 24 * 0 = 0).
    """
    h = k3e_hodge_numbers()
    return sum((-1)**(p + q) * v for (p, q), v in h.items())


def k3e_betti_numbers() -> List[int]:
    r"""Betti numbers of K3 x E.

    b_k(S x E) = sum_{i+j=k} b_i(S) * b_j(E).

    K3: [1, 0, 22, 0, 1]
    E:  [1, 2, 1]
    """
    b_s = [1, 0, 22, 0, 1]
    b_e = [1, 2, 1]
    b = [0] * 7
    for i in range(5):
        for j in range(3):
            b[i + j] += b_s[i] * b_e[j]
    return b


def k3e_euler_from_betti() -> int:
    r"""Euler characteristic from Betti numbers.

    chi = sum (-1)^k b_k.
    """
    b = k3e_betti_numbers()
    return sum((-1)**k * b[k] for k in range(len(b)))


