KUMMER_NUM_FIXED_POINTS = 16  # |E[2] x E[2]| = 4 * 4 = 16
KUMMER_EXCEPTIONAL_RANK = 16
KUMMER_PICARD_RANK_GENERIC = 18  # 16 exceptional + 2 from NS(ExE) generically
# More precisely: for generic E, NS(ExE) = Z^3 (pullbacks of pt x E, E x pt, diagonal),
# but Z/2 acts, and the invariant part contributes to Pic(Kum).
# The Kummer lattice K has rank 16, and Pic(Kum) >= 17.
# For generic E: rho(Kum) = 18 (Nikulin).


def kummer_num_exceptional_curves() -> int:
    r"""Number of exceptional (-2)-curves on the Kummer surface.

    Kum(E x E) = blow-up of (E x E)/(Z/2) at the 16 singular points.
    The 16 singular points come from the 16 fixed points of the involution
    (x, y) -> (-x, -y), which are exactly the 2-torsion points:
        E[2] x E[2] = (Z/2)^2 x (Z/2)^2 = (Z/2)^4, with |·| = 16.

    Each blow-up introduces one (-2)-curve.
    """
    return KUMMER_NUM_FIXED_POINTS


def kummer_lattice_rank() -> int:
    r"""Rank of the Kummer lattice K_{Kum}.

    K_{Kum} is the sublattice of H^2(Kum, Z) generated by the 16
    exceptional classes e_1, ..., e_{16}.

    The intersection form: e_i . e_j = -2 delta_{ij}.

    But the Kummer lattice is NOT the span of {e_i}: it also includes
    the classes (e_i + e_j + e_k + e_l)/2 for certain 4-element subsets.
    (The 2-divisibility comes from the Z/2 symmetry.)

    The lattice K has:
      rank = 16
      discriminant group = (Z/2)^6
      det(K) = 2^6 = 64

    K is a NEGATIVE DEFINITE even lattice of rank 16.
    """
    return KUMMER_EXCEPTIONAL_RANK


def kummer_lattice_discriminant() -> int:
    r"""Discriminant of the Kummer lattice.

    |disc(K)| = 2^6 = 64.

    The Kummer lattice K is isomorphic to the lattice
    D_{16}^+ (half-integral points of D_{16}) with appropriate scaling.

    More precisely: K ~ D_4^4 with specific gluing, giving disc = 2^6.

    DERIVATION: The 16 exceptional classes e_i satisfy e_i^2 = -2.
    The Gram matrix is -2 I_{16}. Its determinant is (-2)^{16} = 2^{16}.
    But the Kummer lattice ALSO includes the half-sums (e_i+e_j+e_k+e_l)/2,
    which extend the lattice by index 2^5 (there are 5 independent such
    half-sums in the abstract theory).

    Actually: the lattice generated by {e_i} alone has det = 2^{16}.
    The Kummer lattice (with the half-sum extensions) has index 2^5 in
    this sublattice, so:
        det(K) = 2^{16} / (2^5)^2 = 2^{16} / 2^{10} = 2^6 = 64.
    """
    return 2**6


def kummer_picard_rank(generic: bool = True) -> int:
    r"""Picard number of the Kummer surface.

    For generic E (no extra endomorphisms):
        rho(Kum(E x E)) = 18.

    Breakdown: 16 exceptional classes + 2 from NS(ExE)^{Z/2} pullback.

    Actually: NS(E x E) = Z^3 for generic E (generated by E x {0},
    {0} x E, and the diagonal).  Under the involution sigma: (x,y) -> (-x,-y),
    all three are invariant.  But only 2 survive as INDEPENDENT classes in
    H^2(Kum):
      - H = pullback of (E x {0} + {0} x E) / 2 ... this requires care.

    The correct answer (Nikulin, Morrison): for generic E,
    rho(Kum(E x E)) = 18.
    For CM curves: rho can be 19 or 20.
    """
    if generic:
        return 18
    return 20  # CM case


def kummer_k0_decomposition() -> Dict[str, Any]:
    r"""K-theory decomposition for the Kummer surface via BKR.

    D^b(Kum(ExE)) ~ D^b_{Z/2}(ExE) by BKR.

    K_0(Kum) = K_0^{Z/2}(E x E) (equivariant K-theory).

    K_0^{Z/2}(E x E) decomposes by the two characters chi_0 = trivial,
    chi_1 = sign of Z/2:

        K_0^{Z/2}(E x E) = K_0(E x E)^{Z/2} (+) K_0(E x E)^{sign}_{twisted}

    The invariant part: K_0(E x E)^{Z/2}.
    K_0(E x E) = K_0(E) tensor K_0(E) = Z^4.
    The Z/2 action on K_0(E x E): sigma*(F bx G) = sigma*F bx sigma*G.
    On K_0(E): sigma*[O_E] = [O_E], sigma*[O_p] = [O_{-p}].
    For generic E (no 2-torsion special structure):
      sigma acts on Z^4 by the matrix that fixes [O_E bx O_E] and
      permutes point sheaves.

    The invariant part has rank:
      K_0(E x E)^{Z/2}: rank depends on the action, generically rank 3-4.

    The twisted sector contributes the 16 exceptional objects.

    Total: rk K_0(Kum) = 24 (it's a K3!).

    Summary:
      Bulk (invariant) sector: rank 8 (from K_0(ExE)^{Z/2} extended)
      Exceptional sector: rank 16
      Total: 24.
    """
    return {
        'total_k0_rank': K3_MUKAI_RANK,
        'exceptional_rank': KUMMER_EXCEPTIONAL_RANK,
        'bulk_rank': K3_MUKAI_RANK - KUMMER_EXCEPTIONAL_RANK,
        'bkr_equivalence': True,
        'equivariant_group': 'Z/2',
        'num_fixed_points': KUMMER_NUM_FIXED_POINTS,
        'picard_rank_generic': kummer_picard_rank(generic=True),
    }


def kummer_k3e_k0() -> Dict[str, Any]:
    r"""K-theory of Kum(ExE) x E' for the CY3 = Kummer x elliptic.

    K_0(Kum x E') = K_0(Kum) tensor K_0(E') = Z^{24} tensor Z^2 = Z^{48}.

    The decomposition:
      Exceptional sector x E': 16 * 2 = 32 summands
      Bulk sector x E': 8 * 2 = 16 summands
      Total: 48.
    """
    kum_data = kummer_k0_decomposition()
    return {
        'total_k0_rank': 48,
        'exceptional_x_E_rank': 2 * kum_data['exceptional_rank'],
        'bulk_x_E_rank': 2 * kum_data['bulk_rank'],
        'consistent': 2 * kum_data['total_k0_rank'] == 48,
    }


