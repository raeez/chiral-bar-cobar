def pn_exceptional_collection_length(n: int) -> int:
    r"""Length of the Beilinson exceptional collection on P^n.

    D^b(P^n) = <O, O(1), ..., O(n)>.  Length = n+1.
    """
    return n + 1


def pn_ext_between_line_bundles(n: int, i: int, j: int, k: int) -> int:
    r"""dim Ext^k(O(i), O(j)) on P^n.

    Ext^k(O(i), O(j)) = H^k(P^n, O(j-i)).

    By Bott vanishing on P^n:
      H^0(O(m)) = binom(m+n, n) for m >= 0, else 0.
      H^n(O(m)) = binom(-m-1, n) for m <= -(n+1), else 0.
      H^k(O(m)) = 0 for 0 < k < n, all m.

    Parameters
    ----------
    n : int  -- dimension of P^n
    i, j : int  -- twist degrees of line bundles
    k : int  -- Ext degree

    Returns
    -------
    int : dimension of Ext^k(O(i), O(j))
    """
    m = j - i
    if k < 0 or k > n:
        return 0
    if k == 0:
        if m >= 0:
            return _binom(m + n, n)
        return 0
    if k == n:
        # Serre duality: H^n(O(m)) = H^0(O(-m - n - 1))^*
        dual_m = -m - n - 1
        if dual_m >= 0:
            return _binom(dual_m + n, n)
        return 0
    # Bott vanishing: H^k = 0 for 0 < k < n
    return 0


def pn_tilting_endomorphism_dim(n: int) -> int:
    r"""Total dimension of End(T) for T = O + O(1) + ... + O(n) on P^n.

    dim End(T) = sum_{i,j=0}^{n} dim Hom(O(i), O(j))
               = sum_{i,j=0}^{n} h^0(P^n, O(j-i)).

    For P^2: Hom(O(i), O(j)) = H^0(O(j-i)).
    j-i ranges from -n to n.  For j >= i: h^0(O(j-i)) = binom(j-i+n, n).
    For j < i: h^0(O(j-i)) = 0.

    Total = sum_{0 <= i <= j <= n} binom(j-i+n, n).
    """
    total = 0
    for i in range(n + 1):
        for j in range(n + 1):
            total += pn_ext_between_line_bundles(n, i, j, 0)
    return total


def pn_euler_form_matrix(n: int) -> np.ndarray:
    r"""Euler form matrix chi(O(i), O(j)) for the exceptional collection on P^n.

    chi(O(i), O(j)) = sum_k (-1)^k dim Ext^k(O(i), O(j)).

    Returns (n+1) x (n+1) matrix.
    """
    size = n + 1
    chi = np.zeros((size, size), dtype=int)
    for i in range(size):
        for j in range(size):
            val = 0
            for k in range(n + 1):
                val += ((-1) ** k) * pn_ext_between_line_bundles(n, i, j, k)
            chi[i, j] = val
    return chi


def pn_k0_rank(n: int) -> int:
    r"""Rank of K_0(P^n).

    K_0(P^n) = Z^{n+1}, generated by [O], [O(1)], ..., [O(n)].
    This follows from the full exceptional collection.
    """
    return n + 1


def pn_euler_char(n: int) -> int:
    r"""Topological Euler characteristic chi(P^n) = n+1.

    chi(P^n) = sum_{k=0}^{2n} (-1)^k b_k = sum_{k=0}^{n} 1 = n+1.
    (Betti: b_{2k} = 1 for k = 0,...,n; b_{2k+1} = 0.)
    """
    return n + 1


def pn_chi_structure_sheaf(n: int) -> int:
    r"""Holomorphic Euler characteristic chi(O_{P^n}) = 1.

    chi(O_{P^n}) = sum (-1)^k h^k(O) = 1 (since h^0 = 1, h^k = 0 for k > 0).
    """
    return 1


def pn_number_of_charts() -> int:
    r"""For P^n with the Beilinson collection, 1 chart suffices.

    The tilting generator T = O + O(1) + ... + O(n) is a single global generator.
    D^b(P^n) ~ D^b(mod-End(T)).
    """
    return 1


def pn_beilinson_quiver_arrows(n: int) -> int:
    r"""Number of arrows in the Beilinson quiver for P^n.

    The Beilinson quiver has n+1 vertices and arrows from vertex i to vertex i+1
    of multiplicity dim Ext^0(O(i), O(i+1)) - ... actually:

    For P^2: O -> O(1) -> O(2) with:
      Hom(O, O(1)) = H^0(O(1)) = C^3 (3 arrows)
      Hom(O(1), O(2)) = H^0(O(1)) = C^3 (3 arrows)
    Total: 6 arrows.

    General P^n: arrows from i to i+1 have multiplicity h^0(O(1)) = n+1.
    There are n steps, each with n+1 arrows.  Total = n(n+1).
    """
    return n * (n + 1)


