"""Koszul dual dimension computation from quadratic relation data.

For a quadratic algebra A = T(V)/(R) with R ⊆ V⊗V:
  A! = T(V*)/(R^⊥)  (Koszul dual)
  (A!)_n = degree-n component

The bar cohomology H^n(B(A)) = (A!)_n when A is Koszul.

We compute (A!)_n for:
  1. Classical U(g): R = antisymmetric relations, A! = Λ(g*)
  2. Chiral KM: R includes bracket + Killing data

Key formula:
  (A!)_n = V^{⊗n} / I_n  where I_n is the degree-n part of the ideal (R^⊥)

  I_2 = R^⊥
  I_3 = (R^⊥ ⊗ V) + (V ⊗ R^⊥)  [inside V^{⊗3}]
  I_4 = (R^⊥ ⊗ V ⊗ V) + (V ⊗ R^⊥ ⊗ V) + (V ⊗ V ⊗ R^⊥)  [inside V^{⊗4}]

  dim((A!)_n) = dim(V^{⊗n}) - dim(I_n)

Application: verify H^2 = 36, H^3 = 204, H^4 = 1352 for sl₃-hat
from the quadratic OPE data.
"""

from __future__ import annotations
import numpy as np
from typing import Dict, Optional, Tuple

from compute.lib.sl3_bar import DIM_G, sl3_structure_constants, sl3_killing_form


def build_relation_subspace(dim_g: int, structure_constants: Dict,
                            killing_form: Optional[Dict] = None,
                            include_killing: bool = False) -> np.ndarray:
    """Build the relation subspace R ⊆ V⊗V from quadratic OPE data.

    Classical (bracket only):
      R = span{e_a ⊗ e_b - e_b ⊗ e_a - f^{ab}_c e_c⊗? : a < b}

      Actually for the PBW/bar relations: the relation is
        e_a ⊗ e_b - e_b ⊗ e_a = [e_a, e_b] (in degree 1)

      In V⊗V (degree 2 of the free algebra), the relation subspace is:
        R = span{e_a ⊗ e_b - e_b ⊗ e_a : a,b ∈ {0,...,d-1}, a < b}
        = Λ²(V) embedded in V⊗V
        dim(R) = C(d,2)

    With Killing (chiral OPE):
      Additional relation from double pole: κ(e_a, e_b) = κ^{ab}
      This adds the Killing form as a symmetric element in V⊗V.
      R_chiral = Λ²(V) + span{Killing element}

    Returns: matrix whose ROWS are basis vectors of R in V⊗V (dim d²).
    """
    d = dim_g

    # Antisymmetric relations: e_a ⊗ e_b - e_b ⊗ e_a for a < b
    relations = []
    for a in range(d):
        for b in range(a + 1, d):
            rel = np.zeros(d * d)
            rel[a * d + b] = 1.0   # e_a ⊗ e_b
            rel[b * d + a] = -1.0  # -e_b ⊗ e_a
            relations.append(rel)

    if include_killing and killing_form is not None:
        # Killing form element: Σ κ^{ab} e_a ⊗ e_b (symmetric)
        kf_rel = np.zeros(d * d)
        for (a, b), val in killing_form.items():
            kf_rel[a * d + b] += float(val)
        if np.linalg.norm(kf_rel) > 1e-10:
            relations.append(kf_rel)

    if not relations:
        return np.zeros((0, d * d))

    R = np.array(relations)

    # Orthogonalize to get a clean basis
    _, s, Vt = np.linalg.svd(R, full_matrices=False)
    rank = int(np.sum(s > 1e-10))
    return Vt[:rank]


def compute_R_perp(R_basis: np.ndarray, total_dim: int) -> np.ndarray:
    """Compute R^⊥ = orthogonal complement of R in V⊗V.

    R_basis: rows are basis of R (shape: rank × d²)
    Returns: rows are basis of R^⊥ (shape: (d²-rank) × d²)
    """
    if R_basis.shape[0] == 0:
        return np.eye(total_dim)

    # R^⊥ = null space of R^T
    # Equivalently: complete to a basis and take the complement
    U, s, Vt = np.linalg.svd(R_basis, full_matrices=True)
    rank = int(np.sum(s > 1e-10))

    # The last (total_dim - rank) rows of Vt are the null space of R_basis
    R_perp = Vt[rank:]
    return R_perp


def ideal_degree_n(R_perp: np.ndarray, dim_v: int, n: int) -> np.ndarray:
    """Build the degree-n ideal I_n ⊆ V^{⊗n} generated by R^⊥ in V⊗V.

    I_n = Σ_{i=0}^{n-2} V^{⊗i} ⊗ R^⊥ ⊗ V^{⊗(n-2-i)}

    Returns: matrix whose rows span I_n in V^{⊗n} (dim d^n).
    """
    d = dim_v
    d_n = d ** n
    r_perp_dim = R_perp.shape[0]

    if r_perp_dim == 0 or n < 2:
        return np.zeros((0, d_n))

    all_rows = []

    for pos in range(n - 1):
        # V^{⊗pos} ⊗ R^⊥ ⊗ V^{⊗(n-2-pos)}
        # Each basis vector of R^⊥ gives d^{pos} × d^{n-2-pos} = d^{n-2} vectors

        d_left = d ** pos
        d_right = d ** (n - 2 - pos)

        for r_idx in range(r_perp_dim):
            r_vec = R_perp[r_idx]  # shape (d²,)
            r_mat = r_vec.reshape(d, d)

            for left_idx in range(d_left):
                for right_idx in range(d_right):
                    # Build the full tensor: left ⊗ R_perp_row ⊗ right
                    full_vec = np.zeros(d_n)

                    for a in range(d):
                        for b in range(d):
                            if abs(r_mat[a, b]) < 1e-15:
                                continue
                            # Position in V^{⊗n}:
                            # indices: [left_idx_digits..., a, b, right_idx_digits...]
                            idx = left_idx * (d * d * d_right) + a * (d * d_right) + b * d_right + right_idx
                            full_vec[idx] = r_mat[a, b]

                    all_rows.append(full_vec)

    if not all_rows:
        return np.zeros((0, d_n))

    I_n = np.array(all_rows)
    return I_n


def koszul_dual_dim(R_perp: np.ndarray, dim_v: int, n: int) -> int:
    """Compute dim((A!)_n) = dim(V^{⊗n}) - rank(I_n).

    Args:
        R_perp: basis of R^⊥ (rows), shape (r, d²)
        dim_v: dimension of V
        n: degree

    Returns: dim((A!)_n)
    """
    d = dim_v

    if n == 0:
        return 1
    if n == 1:
        return d

    I_n = ideal_degree_n(R_perp, d, n)

    if I_n.shape[0] == 0:
        return d ** n

    rank = int(np.linalg.matrix_rank(I_n, tol=1e-8))
    return d ** n - rank


def verify_classical_koszul_dual(dim_g: int = 3):
    """Verify: for U(g) with R = Λ²(V), A! = Λ(V*) with dims C(d,n).

    Tests with sl₂ (d=3) for speed.
    """
    from math import comb
    d = dim_g

    # Build R = antisymmetric part of V⊗V
    R_basis = []
    for a in range(d):
        for b in range(a + 1, d):
            rel = np.zeros(d * d)
            rel[a * d + b] = 1.0
            rel[b * d + a] = -1.0
            R_basis.append(rel)
    R_basis = np.array(R_basis)
    R_perp = compute_R_perp(R_basis, d * d)

    print(f"Classical Koszul dual for d={d}:")
    print(f"  dim(R) = {R_basis.shape[0]}, dim(R⊥) = {R_perp.shape[0]}")

    results = {}
    for n in range(5):
        dim_n = koszul_dual_dim(R_perp, d, n)
        expected = comb(d, n)
        ok = dim_n == expected
        results[n] = (dim_n, expected, ok)
        print(f"  (A!)_{n} = {dim_n}  (expected C({d},{n}) = {expected})  {'✓' if ok else '✗'}")

    return results


def koszul_dual_dims_from_relations(dim_g: int, R_basis: np.ndarray,
                                     max_degree: int = 4) -> dict:
    """Compute Koszul dual dimensions from explicit relation data.

    Args:
        dim_g: dimension of the generator space V
        R_basis: rows are basis vectors of R ⊆ V⊗V
        max_degree: compute up to this degree

    Returns: {n: dim((A!)_n)} for n = 0, ..., max_degree
    """
    d = dim_g
    R_perp = compute_R_perp(R_basis, d * d)

    results = {}
    for n in range(max_degree + 1):
        if d ** n > 500000:  # memory limit
            break
        results[n] = koszul_dual_dim(R_perp, d, n)

    return results


def scan_relation_dims(dim_g: int, structure_constants: Dict,
                       killing_form: Dict, max_degree: int = 3) -> dict:
    """Scan different R dimensions to find which gives the chiral bar dims.

    Try: R = Λ²(V) only, R = Λ²(V) + killing, R = Λ²(V) + all symmetric, etc.
    """
    d = dim_g
    sc = structure_constants
    kf = killing_form

    results = {}

    # Case 1: Classical — R = Λ²(V) (antisymmetric tensors)
    R1 = build_relation_subspace(d, sc, None, include_killing=False)
    dims1 = koszul_dual_dims_from_relations(d, R1, max_degree)
    results['classical'] = {'R_dim': R1.shape[0], 'dims': dims1}

    # Case 2: Classical + Killing element
    R2 = build_relation_subspace(d, sc, kf, include_killing=True)
    dims2 = koszul_dual_dims_from_relations(d, R2, max_degree)
    results['bracket+killing'] = {'R_dim': R2.shape[0], 'dims': dims2}

    # Case 3: R = ALL antisymmetric + ALL symmetric (= full V⊗V)
    R3_rows = []
    for a in range(d):
        for b in range(d):
            rel = np.zeros(d * d)
            rel[a * d + b] = 1.0
            R3_rows.append(rel)
    R3 = np.array(R3_rows)
    dims3 = koszul_dual_dims_from_relations(d, R3, max_degree)
    results['full_VxV'] = {'R_dim': int(np.linalg.matrix_rank(R3)), 'dims': dims3}

    # Case 4: R = Λ²(V) + diagonal (e_a ⊗ e_a for all a)
    R4_rows = []
    for a in range(d):
        for b in range(a + 1, d):
            rel = np.zeros(d * d)
            rel[a * d + b] = 1.0
            rel[b * d + a] = -1.0
            R4_rows.append(rel)
    for a in range(d):
        rel = np.zeros(d * d)
        rel[a * d + a] = 1.0
        R4_rows.append(rel)
    R4 = np.array(R4_rows)
    dims4 = koszul_dual_dims_from_relations(d, R4, max_degree)
    results['antisym+diagonal'] = {'R_dim': int(np.linalg.matrix_rank(R4)), 'dims': dims4}

    return results


