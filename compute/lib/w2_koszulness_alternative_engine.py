def _partition_count_ge(n: int, min_part: int) -> int:
    """Number of partitions of n with all parts >= min_part."""
    if n < 0:
        return 0
    if n == 0:
        return 1
    dp = [0] * (n + 1)
    dp[0] = 1
    for part in range(min_part, n + 1):
        for j in range(part, n + 1):
            dp[j] += dp[j - part]
    return dp[n]


