def _compositions(n: int, k: int, min_part: int = 1) -> List[Tuple[int, ...]]:
    """All ordered k-tuples of integers >= min_part summing to n."""
    if k == 0:
        return [()] if n == 0 else []
    if k == 1:
        return [(n,)] if n >= min_part else []
    result = []
    for first in range(min_part, n - (k - 1) * min_part + 1):
        for rest in _compositions(n - first, k - 1, min_part):
            result.append((first,) + rest)
    return result


def w2_weight_space_dim(h: int) -> int:
    """Dimension of weight-h space of W(2), assuming free strong generation.

    This is the PBW dimension: the number of normally ordered monomials
    of total weight h built from generators T (wt 2) and W^a (wt 3, x3).

    PBW character = prod_{n>=2} 1/(1-q^n) * [prod_{n>=3} 1/(1-q^n)]^3.
    """
    return _pbw_dims(h)


def _pbw_dims(max_h: int) -> List[int]:
    """PBW dimensions for W(2) at weights 0, 1, ..., max_h."""
    # Generators: (weight, multiplicity) = (2, 1), (3, 3)
    dims = [0] * (max_h + 1)
    dims[0] = 1
    # Factor 1: single generator at weight 2
    for part in range(2, max_h + 1):
        for w in range(part, max_h + 1):
            dims[w] += dims[w - part]
    # Factor 2: three generators at weight 3
    for _ in range(3):
        old = dims[:]
        dims = [0] * (max_h + 1)
        # Convolve with prod_{n>=3} 1/(1-q^n)
        gen3 = [0] * (max_h + 1)
        gen3[0] = 1
        for part in range(3, max_h + 1):
            for w in range(part, max_h + 1):
                gen3[w] += gen3[w - part]
        for i in range(max_h + 1):
            if old[i] == 0:
                continue
            for j in range(max_h + 1 - i):
                dims[i + j] += old[i] * gen3[j]
    return dims


def w2_pbw_character(max_weight: int = 15) -> List[int]:
    """PBW character for W(2) through given weight."""
    return _pbw_dims(max_weight)


def _w2_basis_labels(h: int) -> List[str]:
    """Basis labels for the weight-h space of W(2) in PBW basis.

    Each basis element is a normally ordered monomial in T, W^+, W^0, W^-.
    We label by the multi-index of mode numbers applied.

    For the bar complex, we need the POSITIVE weight part A_+ only.
    Generators: T at weight 2, W^a at weight 3.
    Descendants: d^k T at weight 2+k, d^k W^a at weight 3+k.

    At weight h, the basis consists of all normally ordered products
    of descendants of {T, W^+, W^0, W^-} with total weight h.

    For computational tractability, we represent each state as a tuple:
    ((gen_index, derivative_order), ...) sorted by position.

    Generator indices: 0=T, 1=W^+, 2=W^0, 3=W^-.
    """
    if h <= 0:
        return []
    if h == 1:
        return []  # no generators at weight 1

    labels = []
    # Single generator descendants
    # T at weight 2: d^{h-2} T if h >= 2
    if h >= 2:
        labels.append(f"d^{h-2}T")
    # W^a at weight 3: d^{h-3} W^a if h >= 3
    if h >= 3:
        for a in ['+', '0', '-']:
            labels.append(f"d^{h-3}W{a}")

    # Two-generator products: all pairs (g1, k1), (g2, k2)
    # with wt(g1)+k1 + wt(g2)+k2 = h
    for h1 in range(2, h - 1):
        h2 = h - h1
        if h2 < 2:
            continue
        labels1 = _single_gen_labels(h1)
        labels2 = _single_gen_labels(h2)
        for l1 in labels1:
            for l2 in labels2:
                if l1 <= l2:  # ordered to avoid double counting
                    labels.append(f":{l1}.{l2}:")

    # Three-generator products (only if h >= 6)
    if h >= 6:
        for h1 in range(2, h - 3):
            for h2 in range(2, h - h1 - 1):
                h3 = h - h1 - h2
                if h3 < 2:
                    continue
                labels1 = _single_gen_labels(h1)
                labels2 = _single_gen_labels(h2)
                labels3 = _single_gen_labels(h3)
                for l1 in labels1:
                    for l2 in labels2:
                        for l3 in labels3:
                            if l1 <= l2 <= l3:
                                labels.append(f":{l1}.{l2}.{l3}:")
    return labels


def _single_gen_labels(h: int) -> List[str]:
    """Labels for single-generator descendants at weight h."""
    labels = []
    if h >= 2:
        labels.append(f"d^{h-2}T")
    if h >= 3:
        for a in ['+', '0', '-']:
            labels.append(f"d^{h-3}W{a}")
    return labels


def w2_ope_tt(c: Rational = W2_CENTRAL_CHARGE) -> Dict[int, Any]:
    """T(z) T(w) OPE for W(2) at c = -2.

    T(z) T(w) ~ c/2 / (z-w)^4 + 2T(w)/(z-w)^2 + dT(w)/(z-w).

    Bar r-matrix (AP19: d log absorbs one pole):
      r(T, T) has poles at z^{-3}, z^{-1}.
      Mode coefficients: (c/2) at (z-w)^{-3} and 2T at (z-w)^{-1}.

    For the bar complex at fixed weight, the relevant part is the
    simple-pole residue: r(T,T) = dT (from the (z-w)^{-1} term
    after d log absorption).
    """
    return {
        'max_pole': 4,
        'bar_max_pole': 3,  # AP19
        'residues': {
            4: c / 2,       # central term
            2: Rational(2),  # coefficient of T(w)
            1: Rational(1),  # coefficient of dT(w)
        },
    }


def w2_ope_tw() -> Dict[int, Any]:
    """T(z) W^a(w) OPE: primary of weight 3.

    T(z) W^a(w) ~ 3 W^a(w)/(z-w)^2 + d W^a(w)/(z-w).

    Bar r-matrix: simple-pole residue = dW^a.
    """
    return {
        'max_pole': 2,
        'bar_max_pole': 1,  # AP19
        'residues': {
            2: Rational(3),  # h_W * W^a
            1: Rational(1),  # dW^a
        },
    }


def w2_ope_ww() -> Dict[int, Any]:
    """W^a(z) W^b(w) OPE structure for W(2).

    The W-W OPE has max pole order 2*h_W = 2*3 = 6.

    From Kausch (1991) and Gaberdiel-Kausch (1996), the sl_2
    structure of the W-W OPE for W(2) (c=-2, h_W=3) is:

    W^+(z) W^-(w) ~ -1/(z-w)^6 + T(w)/(z-w)^4
                    + dT(w)/(2(z-w)^3) + ...
    W^0(z) W^0(w) ~ 2/3 * 1/(z-w)^6 + ...
    W^+(z) W^+(w) ~ 0 (by sl_2 weight)

    The bar r-matrix (AP19) has max pole 5.

    Key: the sl_2 structure gives the decomposition of :W^a W^b:
    at weight 6 = 2*3. The symmetric product Sym^2(3) = 5 + 1.
    The singlet (spin-0) component is :W^+ W^-: + :W^- W^+: - 2:W^0 W^0:.
    For p=2, this singlet is NOT null -- it is proportional to
    a T-descendant (specifically, a specific linear combination of
    d^4 T, :d^2 T . T:, :dT . dT:, :T.T.T:).

    This is the key fact: at p=2, the singlet IS expressible as a
    T-descendant, but this does NOT make it null -- it means the
    composite is a KNOWN state, not a NEW generator or a zero.
    The PBW count includes it as a composite.
    """
    return {
        'max_pole': 6,
        'bar_max_pole': 5,  # AP19
        'sl2_decomposition': '5 + 1',  # Sym^2(3)
        'singlet_null_at_p2': False,
        'notes': (
            'For p=2, the sl_2 singlet in :W^a W^b: is NOT null. '
            'It equals a specific T-descendant combination at weight 6.'
        ),
    }


def bar_chain_dims_w2(weight: int, max_bar_degree: int = 4) -> Dict[int, int]:
    """Dimensions of bar chain groups B^k(W(2)) at given conformal weight.

    B^k_h = tensor products of k elements from A_+ with weights summing to h.
    Each element comes from the weight space of W(2), with dim given by PBW.

    The bar complex has B^k_h = 0 for k > h/2 (since min generator weight = 2).
    """
    pbw = w2_pbw_character(weight)
    result = {}
    for k in range(1, max_bar_degree + 1):
        if k * 2 > weight:
            result[k] = 0
            continue
        # Count all ordered k-tuples (h_1, ..., h_k) with sum = h, each >= 2
        comps = _compositions(weight, k, 2)
        total = 0
        for comp in comps:
            prod = 1
            for hi in comp:
                if hi < len(pbw):
                    prod *= pbw[hi]
                else:
                    prod = 0
                    break
            total += prod
        result[k] = total
    return result


def _build_w2_r_matrix_numerical(max_weight: int = 8) -> Dict:
    """Build the numerical r-matrix for W(2) bar complex.

    The r-matrix r(a, b) for the bar complex extracts the collision
    residue from the OPE via d log(z-w) (AP19).

    For computational purposes, we work with the SIMPLIFIED model:
    the bar differential at bar degree k merges adjacent pairs via
    the r-matrix.  The r-matrix output is a linear combination of
    states at weight wt(a) + wt(b) - 1 (simple pole after d log).

    Higher poles contribute to the bar differential at different
    weight offsets: a pole of order n in the OPE becomes a pole of
    order n-1 in the bar r-matrix (AP19), and contributes to weight
    wt(a) + wt(b) - n + 1 in the output.

    For the FULL bar complex, we need:
      r_n(a, b) = coefficient at (z-w)^{-(n-1)} in the bar kernel,
                  contributing to output weight wt(a) + wt(b) - n.

    For the bar COHOMOLOGY computation (checking Koszulness), the
    key fact is that the bar differential at fixed conformal weight
    is a finite-dimensional linear map.

    We build this map numerically for small weights.
    """
    # For the simplified computation, we use the following model:
    # At each conformal weight h, the bar complex has finite-dimensional
    # chain groups, and the differential is determined by the OPE.
    #
    # The output of r(a, b) lives at various weights depending on
    # the pole structure.  For the bar differential restricted to
    # a fixed total weight, only specific pole orders contribute.
    return {
        'max_weight': max_weight,
        'TT_poles': [4, 2, 1],  # OPE pole orders in T-T
        'TW_poles': [2, 1],     # OPE pole orders in T-W
        'WW_poles': [6, 4, 3, 2, 1],  # OPE pole orders in W-W
    }


def bar_complex_euler_char_w2(weight: int) -> int:
    """Euler characteristic of the bar complex at given conformal weight.

    chi(B_h) = sum_k (-1)^k dim(B^k_h).

    For a Koszul algebra, chi(B_h) = (-1)^1 * dim(A!_h) = -dim(A!_h).
    This gives a necessary (but not sufficient) condition for Koszulness.
    """
    chain_dims = bar_chain_dims_w2(weight, max_bar_degree=weight // 2)
    chi = 0
    for k, d in chain_dims.items():
        chi += (-1)**k * d
    return chi


def bar_cohomology_check_w2(max_weight: int = 10) -> Dict:
    """Check bar cohomology concentration for W(2).

    For Koszulness, we need H^k(B(W(2))) = 0 for all k >= 2.
    Equivalently, all cohomology is concentrated in bar degree 1.

    This function computes the bar chain group dimensions and
    Euler characteristics as evidence for/against concentration.

    Route 1 evidence: if the Euler characteristics match the
    expected pattern for a Koszul algebra (chi_h = -dim(A!_h)),
    this supports Koszulness.
    """
    results = {}
    pbw = w2_pbw_character(max_weight)

    for h in range(2, max_weight + 1):
        chain_dims = bar_chain_dims_w2(h)
        chi = bar_complex_euler_char_w2(h)

        # For a Koszul algebra, H^1_h = A!_h = generators at weight h
        # and H^k_h = 0 for k >= 2.
        # So chi = -dim(H^1) = -(dim(B^1) - dim(im d_2)).
        # More precisely, chi = sum (-1)^k dim(H^k) = -dim(H^1) if concentrated.

        results[h] = {
            'chain_dims': chain_dims,
            'euler_char': chi,
            'bar_degree_1_dim': chain_dims.get(1, 0),
        }

    return {
        'max_weight': max_weight,
        'pbw_character': pbw,
        'weight_data': results,
        'route': 'bar_complex_euler_char',
    }


