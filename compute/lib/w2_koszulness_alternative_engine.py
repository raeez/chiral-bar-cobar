def kac_table_c_minus2(max_rs: int = 20) -> List[Dict]:
    """Kac table entries h_{r,s} for c = -2.

    c = -2 corresponds to the (2,1) model: p=2, p'=1.
    h_{r,s} = ((2r - s)^2 - 1) / 8.

    Since p'=1, only the column s=1 is "physical" in the minimal model.
    But for the TRIPLET algebra, all s values contribute.

    Entries with h_{r,s} = 0 (at the vacuum):
    (2r-s)^2 = 1 => s = 2r +/- 1.
    """
    entries = []
    for r in range(1, max_rs + 1):
        for s in range(1, max_rs + 1):
            if r * s > max_rs:
                continue
            h = Rational((2 * r - s)**2 - 1, 8)
            entries.append({
                'r': r,
                's': s,
                'h_rs': h,
                'level': r * s,
                'is_vacuum': h == 0,
            })
    return entries


