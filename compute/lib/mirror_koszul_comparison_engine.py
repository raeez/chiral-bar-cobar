# 12. The precise relationship: mirror = S . KD^{-1} . S (?)
# ============================================================================

def mirror_as_composition(N: int, psi: Num) -> Dict[str, Any]:
    r"""Attempt to express mirror symmetry as a composition of S and KD.

    If mirror = S . KD . S (or some other combination), then
    the kappa of the mirror dual should equal the kappa obtained
    by composing the operations.

    Compositions on the coupling Psi:
        S:       Psi -> 1/Psi
        KD:      Psi -> -Psi
        S.KD:    Psi -> -1/Psi
        KD.S:    Psi -> -1/Psi  (same!)
        S.KD.S:  Psi -> -(1/Psi) -> -Psi  ... wait:
            S: Psi -> 1/Psi
            KD: 1/Psi -> -1/Psi
            S: -1/Psi -> Psi/(-1) ... no, S just inverts:
               S(-1/Psi) = 1/(-1/Psi) = -Psi.
        So S.KD.S: Psi -> -Psi = KD(Psi).

    So S.KD.S = KD and KD.S.KD = S.  They generate a dihedral subgroup.

    The S_3 corner triality has a DIFFERENT action:
        sigma_12: Psi -> 1/Psi
        sigma_23: Psi -> 1 - Psi
        sigma_13: Psi -> Psi/(Psi-1)

    Neither KD (Psi -> -Psi) nor any of these S_3 elements give the
    mirror map directly.  Mirror symmetry acts on THEORIES (exchanging
    Coulomb and Higgs branches), not just on the coupling constant.

    CONCLUSION: Mirror symmetry CANNOT be expressed as a composition
    of S-duality and Koszul duality acting on the coupling constant alone.
    Mirror symmetry requires additional data (the exchange of M_C and M_H)
    that is not captured by Mobius transformations of Psi.
    """
    psi_f = _frac(psi)
    k = psi_f - N

    kap = kappa_affine_gl(N, k)

    # Compositions
    kap_S = kappa_affine_gl(N, s_dual_level(k, N))
    kap_KD = kappa_affine_gl(N, ff_dual_level(k, N))
    kap_SKD = kappa_affine_gl(N, ff_dual_level(s_dual_level(k, N), N))
    kap_KDS = kappa_affine_gl(N, s_dual_level(ff_dual_level(k, N), N))

    # S.KD.S
    step1 = s_dual_level(k, N)                         # 1/Psi - N
    step2 = ff_dual_level(step1, N)                     # -(1/Psi - N) - 2N
    step3_level = s_dual_level(step2, N)
    kap_SKDS = kappa_affine_gl(N, step3_level)

    return {
        'psi': psi_f,
        'kappa_original': kap,
        'kappa_S': kap_S,
        'kappa_KD': kap_KD,
        'kappa_S_then_KD': kap_SKD,
        'kappa_KD_then_S': kap_KDS,
        'kappa_S_KD_S': kap_SKDS,
        'S_KD_commute': (kap_SKD == kap_KDS),
        'S_KD_S_equals_KD': (kap_SKDS == kap_KD),
        'conclusion': (
            "Mirror symmetry cannot be expressed as a composition of "
            "S-duality and Koszul duality on the coupling constant. "
            "Mirror symmetry exchanges BRANCHES (Coulomb <-> Higgs), "
            "which is geometrical data beyond the coupling constant."
        ),
    }


