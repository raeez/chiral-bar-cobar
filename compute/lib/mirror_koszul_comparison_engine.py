# 4. S_3 triality group
# ============================================================================

@dataclass(frozen=True)
class TrialityOrbit:
    """The S_3 orbit of a coupling constant Psi under S-duality and FF-duality.

    S_3 is generated by:
        s = (12): Psi -> 1/Psi
        t = (123): Psi -> 1 - 1/Psi  (order 3, but we decompose via
                   two transpositions)

    More precisely, on the coupling constant tau = Psi, the S_3 acts by
    Mobius transformations:

        id:    tau
        S:     1/tau
        K:     -tau           (FF / Koszul)
        SK:    -1/tau         (composition S . K)
        KS:    -tau           ... wait, let's be careful.

    Actually for the CORNER VOA, the S_3 triality acts as:
        e:     Psi
        (12):  1/Psi           [S-duality]
        (23):  1 - Psi          [the third transposition]
        (13):  Psi/(Psi - 1)    [another transposition]
        (123): 1/(1 - Psi)      [3-cycle]
        (132): 1 - 1/Psi        [3-cycle]

    The Feigin-Frenkel involution Psi -> -Psi does NOT belong to this S_3
    (it is not a Mobius transformation with {0, 1, infinity} fixed points).

    CORRECTION: FF duality Psi -> -Psi is a SEPARATE operation from the
    S_3 triality of the corner VOA.  The full symmetry group of the
    coupling space is:
        S_3 (corner triality) x Z_2 (FF involution) = S_3 x Z_2
    but this product is NOT direct -- there are further identifications.

    What we call "Koszul duality" at the level of kappa is the FF involution,
    which lives OUTSIDE the S_3 triality.
    """
    psi: Fraction
    s_dual: Fraction            # 1/Psi
    ff_dual: Fraction           # -Psi
    s_ff_composition: Fraction  # -1/Psi = S(K(Psi))
    triality_23: Fraction       # 1 - Psi
    triality_13: Fraction       # Psi/(Psi-1)
    triality_123: Optional[Fraction]  # 1/(1-Psi) if Psi != 1
    triality_132: Optional[Fraction]  # 1 - 1/Psi if Psi != 0

    @property
    def s_equals_ff(self) -> bool:
        """S-duality = FF-duality iff 1/Psi = -Psi iff Psi^2 = -1.
        This has no REAL solutions.  Over C: Psi = +/- i."""
        return self.s_dual == self.ff_dual

    @property
    def ff_in_s3_orbit(self) -> bool:
        """Is the FF dual -Psi in the S_3 orbit of Psi?

        The S_3 orbit consists of:
            {Psi, 1/Psi, 1-Psi, Psi/(Psi-1), 1/(1-Psi), 1-1/Psi}

        For -Psi to be in this orbit, we need -Psi to equal one of these.
        """
        orbit = {self.psi, self.s_dual, self.triality_23, self.triality_13}
        if self.triality_123 is not None:
            orbit.add(self.triality_123)
        if self.triality_132 is not None:
            orbit.add(self.triality_132)
        return self.ff_dual in orbit


def compute_triality_orbit(psi: Num) -> TrialityOrbit:
    """Compute the full S_3 triality orbit of coupling Psi."""
    psi_f = _frac(psi)

    # S-duality: Psi -> 1/Psi
    if psi_f == 0:
        raise ValueError("Psi = 0: S-dual undefined")
    s_dual = Fraction(1) / psi_f

    # FF / Koszul: Psi -> -Psi
    ff_dual = -psi_f

    # S . FF composition: Psi -> -1/Psi
    s_ff = -Fraction(1) / psi_f

    # (23) transposition: Psi -> 1 - Psi
    t23 = Fraction(1) - psi_f

    # (13) transposition: Psi -> Psi/(Psi-1)
    if psi_f == 1:
        t13 = None
    else:
        t13 = psi_f / (psi_f - 1)

    # (123) 3-cycle: Psi -> 1/(1-Psi)
    if psi_f == 1:
        t123 = None
    else:
        t123 = Fraction(1) / (1 - psi_f)

    # (132) 3-cycle: Psi -> 1 - 1/Psi
    t132 = 1 - Fraction(1) / psi_f

    return TrialityOrbit(
        psi=psi_f,
        s_dual=s_dual,
        ff_dual=ff_dual,
        s_ff_composition=s_ff,
        triality_23=t23,
        triality_13=t13 if t13 is not None else psi_f,  # degenerate
        triality_123=t123,
        triality_132=t132,
    )


