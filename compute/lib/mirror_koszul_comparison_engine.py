# 6. The S-vs-KD discrepancy formula (exact)
# ============================================================================

def s_kd_discrepancy_affine_gl(N: int, psi: Num) -> Fraction:
    r"""Exact formula for kappa(A^S) - kappa(A^!) for affine gl_N.

    kappa(gl_N, k) = (N^2-1)(k+N)/(2N) + k

    k^S = 1/Psi - N:
      kappa^S = (N^2-1)(1/Psi)/(2N) + (1/Psi - N)
              = (N^2-1)/(2N*Psi) + 1/Psi - N

    k^FF = -Psi - N:
      kappa^FF = (N^2-1)(-Psi)/(2N) + (-Psi - N)
               = -(N^2-1)*Psi/(2N) - Psi - N

    Discrepancy = kappa^S - kappa^FF
      = (N^2-1)/(2N*Psi) + 1/Psi - N - [-(N^2-1)*Psi/(2N) - Psi - N]
      = (N^2-1)/(2N*Psi) + 1/Psi + (N^2-1)*Psi/(2N) + Psi
      = (N^2-1)/(2N) * (1/Psi + Psi) + (1/Psi + Psi)
      = [(N^2-1)/(2N) + 1] * (1/Psi + Psi)
      = (N^2 + 2N - 1)/(2N) * (Psi + 1/Psi)
      = (N + 1 - 1/N) / 2 * (Psi + 1/Psi)   ... let's keep it in the clean form.

    Clean form: Delta = [(N^2 + 2N - 1) / (2N)] * (Psi + 1/Psi)

    For N=1: Delta = (1+2-1)/2 * (Psi + 1/Psi) = 1 * (Psi + 1/Psi)
    For N=2: Delta = (4+4-1)/4 * (Psi + 1/Psi) = 7/4 * (Psi + 1/Psi)

    NOTE: For real Psi > 0, Psi + 1/Psi >= 2 (AM-GM), so Delta >= 2 * coeff.
    The discrepancy NEVER vanishes for real positive Psi.
    It vanishes iff Psi + 1/Psi = 0, i.e., Psi^2 = -1, i.e., Psi = +/- i.
    """
    psi_f = _frac(psi)
    N_f = Fraction(N)
    coeff = (N_f ** 2 + 2 * N_f - 1) / (2 * N_f)
    psi_plus_inv = psi_f + Fraction(1) / psi_f
    return coeff * psi_plus_inv


def s_kd_discrepancy_closed_form(N: int) -> str:
    """Return the closed-form formula as a string.

    Delta(Psi, N) = [(N^2 + 2N - 1)/(2N)] * (Psi + 1/Psi)
    """
    return f"[(N^2 + 2N - 1)/(2N)] * (Psi + 1/Psi) with N={N}"


def s_kd_coincidence_locus() -> str:
    """The locus where S-duality = Koszul duality.

    Over C: Psi^2 = -1, i.e., Psi = +/- i.
    Over R: EMPTY (no real solutions).

    This is the imaginary self-dual coupling where the Langlands
    dual and the Feigin-Frenkel dual coincide.
    """
    return "Psi^2 = -1 (over C: Psi = +/- i; over R: EMPTY)"


