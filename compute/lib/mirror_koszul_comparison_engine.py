def genus1_obstruction_koszul(kappa: Num) -> Fraction:
    """Genus-1 obstruction from Theorem D: F_1 = kappa/24."""
    return _frac(kappa) / 24


def genus1_obstruction_mirror_sum(kappa_C: Num, kappa_H: Num) -> Fraction:
    """Genus-1 mirror sum: F_1(A_C) + F_1(A_H) = (kappa_C + kappa_H)/24."""
    return (_frac(kappa_C) + _frac(kappa_H)) / 24


def genus1_obstruction_koszul_sum(kappa: Num, kappa_dual: Num) -> Fraction:
    """Genus-1 Koszul sum: F_1(A) + F_1(A^!) = (kappa + kappa')/24."""
    return (_frac(kappa) + _frac(kappa_dual)) / 24


def genus2_obstruction_scalar(kappa: Num) -> Fraction:
    """Genus-2 scalar obstruction: F_2 = 7*kappa/(5760)."""
    return 7 * _frac(kappa) / 5760


def genus_g_obstruction_comparison(
    kappa_A: Num,
    kappa_A_dual: Num,
    kappa_mirror: Optional[Num] = None,
) -> Dict[str, Any]:
    """Compare genus-1 and genus-2 obstructions across dualities.

    The genus-g obstruction F_g(A) = kappa(A) * lambda_g^FP (for uniform-weight
    algebras on the scalar lane) gives:

    F_g(A) + F_g(A^!) = [kappa(A) + kappa(A^!)] * lambda_g^FP  [Koszul sum]
    F_g(A_C) + F_g(A_H) = [kappa_C + kappa_H] * lambda_g^FP    [Mirror sum]

    These are DIFFERENT because kappa + kappa' != kappa_C + kappa_H.
    """
    kA = _frac(kappa_A)
    kA_d = _frac(kappa_A_dual)
    kM = _frac(kappa_mirror) if kappa_mirror is not None else None

    result = {
        'F1_A': genus1_obstruction_koszul(kA),
        'F1_A_dual': genus1_obstruction_koszul(kA_d),
        'F1_koszul_sum': genus1_obstruction_koszul_sum(kA, kA_d),
        'F2_A': genus2_obstruction_scalar(kA),
        'F2_A_dual': genus2_obstruction_scalar(kA_d),
        'F2_koszul_sum': genus2_obstruction_scalar(kA) + genus2_obstruction_scalar(kA_d),
    }

    if kM is not None:
        result['F1_mirror'] = genus1_obstruction_koszul(kM)
        result['F1_mirror_sum'] = genus1_obstruction_mirror_sum(kA, kM)
        result['F2_mirror'] = genus2_obstruction_scalar(kM)
        result['F2_mirror_sum'] = genus2_obstruction_scalar(kA) + genus2_obstruction_scalar(kM)
        result['F1_sums_equal'] = (result['F1_koszul_sum'] == result['F1_mirror_sum'])
        result['F2_sums_equal'] = (result['F2_koszul_sum'] == result['F2_mirror_sum'])
    else:
        result['F1_mirror'] = None
        result['F1_mirror_sum'] = None

    return result


