@dataclass(frozen=True)
class CategoricalVsAlgebraicKD:
    """Compare BLPW categorical KD with our chiral algebraic KD.

    BLPW categorical:
        O(M_C) is Koszul dual to O(M_H)
        This exchanges MODULE CATEGORIES.
        The Koszul ring is Ext^*_O(L_i, L_j) where L_i are simples.

    Chiral algebraic (Theorem A):
        A^! = (H*(B(A)))^v
        This produces the DUAL ALGEBRA (not its module category).

    The relationship:
        If A is formal (class G or L), then Rep(A) ~ O(M_C) and
        Rep(A^!) ~ O(M_H), and the two KDs AGREE.

        If A is non-formal (class C or M), the chiral bar complex
        captures HIGHER operations (A_infinity structure) that the
        categorical Ext algebra does not see.

    PREDICTION: The discrepancy between categorical and algebraic KD
    is measured by the shadow depth:
        depth 2 (G): categorical = algebraic
        depth 3 (L): categorical = algebraic (tree-level formality)
        depth 4 (C): categorical APPROXIMATES algebraic (quartic correction)
        depth inf (M): categorical DIFFERS from algebraic
    """
    algebra_name: str
    shadow_class: str
    shadow_depth: object  # int or float('inf')
    categorical_kd_type: str
    algebraic_kd_type: str
    categorical_equals_algebraic: bool
    discrepancy_source: Optional[str]


def categorical_vs_algebraic_kd(
    shadow_class: str,
    algebra_name: str = "",
) -> CategoricalVsAlgebraicKD:
    """Determine whether categorical and algebraic KD agree.

    The agreement depends on the shadow depth / formality class.
    """
    depth_map = {'G': 2, 'L': 3, 'C': 4, 'M': float('inf')}
    depth = depth_map.get(shadow_class, float('inf'))

    if shadow_class in ('G', 'L'):
        # Formal: categorical = algebraic
        return CategoricalVsAlgebraicKD(
            algebra_name=algebra_name,
            shadow_class=shadow_class,
            shadow_depth=depth,
            categorical_kd_type="Ext algebra is Koszul, concentrated in degrees 0,1",
            algebraic_kd_type="Bar cohomology concentrated in bar degree 1",
            categorical_equals_algebraic=True,
            discrepancy_source=None,
        )
    elif shadow_class == 'C':
        # Quartic correction
        return CategoricalVsAlgebraicKD(
            algebra_name=algebra_name,
            shadow_class=shadow_class,
            shadow_depth=depth,
            categorical_kd_type="Ext algebra has quartic deviation from Koszulity",
            algebraic_kd_type="Bar complex has nonzero quartic shadow Q^contact",
            categorical_equals_algebraic=False,
            discrepancy_source="Quartic contact term Q^contact "
                               "(exits bar complex by rank-one rigidity)",
        )
    else:  # class M
        return CategoricalVsAlgebraicKD(
            algebra_name=algebra_name,
            shadow_class=shadow_class,
            shadow_depth=depth,
            categorical_kd_type="Ext algebra has infinite tower of deviations",
            algebraic_kd_type="Bar complex has infinite shadow obstruction tower",
            categorical_equals_algebraic=False,
            discrepancy_source="Infinite shadow tower: all arities contribute "
                               "(Virasoro, W_N with N >= 2)",
        )


