def argyres_douglas_h0_diagram() -> Dict[str, Any]:
    r"""The four-duality diagram for the Argyres-Douglas H_0 theory.

    The H_0 theory (pure SU(2)) has:
        Chiral algebra: Virasoro at c = -22/5 (Lee-Yang minimal model)
        Koszul dual: Virasoro at c = 152/5

    There is NO boundary VOA decomposition into Coulomb/Higgs here
    because the theory is non-Lagrangian.  However:
        kappa = -11/5
        kappa' = 76/5
        kappa + kappa' = 65/5 = 13  (AP24: Virasoro complementarity)

    This is a class M algebra (infinite shadow depth).
    The Argyres-Douglas theories provide examples where Koszul duality
    is well-defined but 3d mirror symmetry is not directly applicable
    (the theory is isolated, not part of a mirror pair in the usual sense).
    """
    c = Fraction(-22, 5)
    c_dual = Fraction(26) - c
    kap = c / 2
    kap_dual = c_dual / 2

    return {
        'theory': 'Argyres-Douglas H_0',
        'algebra': f'Vir_{{c={c}}} (Lee-Yang)',
        'kappa': kap,
        'kappa_dual': kap_dual,
        'complementarity_sum': kap + kap_dual,  # = 13
        'shadow_class': 'M',
        'has_mirror_dual': False,
        'koszul_duality_applies': True,
        'mirror_symmetry_applies': False,
        'categorical_kd_applies': False,
        'conclusion': (
            "Koszul duality is well-defined (Virasoro is a chiral algebra), "
            "but 3d mirror symmetry and categorical KD are not directly "
            "applicable to this non-Lagrangian theory."
        ),
    }


def sqed_nf2_full_diagram() -> Dict[str, Any]:
    r"""The complete four-duality diagram for SQED(2).

    SQED(2) = U(1) with 2 hypermultiplets.
    Coulomb: C^2/Z_2, boundary VOA = Heisenberg H_1.
    Higgs: T*P^1, boundary VOA = Heisenberg H_1 (rank 1).
    Mirror: U(1)^1 linear quiver (= itself for N_f=2!).

    This is a self-mirror theory at N_f = 2.

    kappa_C = 1, kappa_H = 1.
    Mirror sum: 1 + 1 = 2 = N_f.
    Koszul dual of H_1: kappa(H_1^!) = -1.
    Koszul sum: 1 + (-1) = 0.

    The four kappas:
        kappa(A_C) = 1
        kappa(A_C^!) = -1
        kappa(A_H) = 1
        kappa(A_H^!) = -1

    Diagram:
        A_C(T)=H_1 --mirror--> A_H(T~)=H_1
           |                        |
           | KD                     | KD
           v                        v
        H_1^!=H_{-1}           H_1^!=H_{-1}

    The diagram COMMUTES for this self-mirror case, but only because
    both boundary VOAs happen to be the same algebra (H_1).

    CAUTION: A_C^! = H_1^! != A_H = H_1. The Koszul dual has kappa = -1,
    not kappa = 1.  The diagram commutes TAUTOLOGICALLY (mirror acts trivially)
    but the Koszul dual is still DIFFERENT from the mirror dual.
    """
    kC = Fraction(1)
    kH = Fraction(1)
    kC_dual = Fraction(-1)
    kH_dual = Fraction(-1)

    return {
        'theory': 'SQED(2)',
        'is_self_mirror': True,
        'kappa_C': kC,
        'kappa_H': kH,
        'kappa_C_koszul': kC_dual,
        'kappa_H_koszul': kH_dual,
        'mirror_sum': kC + kH,      # = 2
        'koszul_sum_C': kC + kC_dual,  # = 0
        'koszul_sum_H': kH + kH_dual,  # = 0
        'diagram_commutes': True,
        'reason': "Self-mirror: A_C = A_H, so mirror acts trivially. "
                  "But A_C^! != A_H: Koszul dual has kappa = -1, mirror has kappa = 1.",
        'obstruction_kd_vs_mirror': kC_dual - kH,  # = -1 - 1 = -2
    }


