# ============================================================================
# Exact arithmetic
# ============================================================================

Num = Union[int, Fraction]


def _frac(x) -> Fraction:
    """Coerce to exact Fraction."""
    if isinstance(x, Fraction):
        return x
    return Fraction(x)


# ============================================================================
# 1. Core kappa formulas (recomputed from first principles, AP1)
# ============================================================================

def kappa_affine_sl(N: int, k: Num) -> Fraction:
    r"""kappa(sl_N, k) = (N^2-1)(k+N)/(2N).

    AP1, AP39: This is NOT c/2 for N > 2.
    For N=1: sl_1 = 0 (trivial), dim = 0, so kappa = 0 for all k.
    """
    k_f = _frac(k)
    N_f = Fraction(N)
    if N == 1:
        # sl_1 is trivial: dim = 0, kappa = 0 regardless of level
        return Fraction(0)
    if k_f + N_f == 0:
        raise ValueError(f"Critical level k = -{N}")
    return (N_f ** 2 - 1) * (k_f + N_f) / (2 * N_f)


def kappa_affine_gl(N: int, k: Num) -> Fraction:
    r"""kappa(gl_N, k) = kappa(sl_N, k) + k.

    The u(1) Heisenberg contributes kappa = k.
    """
    k_f = _frac(k)
    return kappa_affine_sl(N, k_f) + k_f


def kappa_virasoro(c: Num) -> Fraction:
    """kappa(Vir_c) = c/2."""
    return _frac(c) / 2


def kappa_wn(N: int, c: Num) -> Fraction:
    """kappa(W_N, c) = (H_N - 1) * c."""
    H_N = sum(Fraction(1, j) for j in range(1, N + 1))
    return (H_N - 1) * _frac(c)


def c_affine_sl(N: int, k: Num) -> Fraction:
    """Sugawara central charge c(sl_N, k) = k(N^2-1)/(k+N)."""
    k_f = _frac(k)
    N_f = Fraction(N)
    if N == 1:
        return Fraction(0)  # sl_1 is trivial
    if k_f + N_f == 0:
        raise ValueError(f"Critical level k = -{N}")
    return k_f * (N_f ** 2 - 1) / (k_f + N_f)


def c_affine_gl(N: int, k: Num) -> Fraction:
    """c(gl_N, k) = c(sl_N, k) + 1."""
    return c_affine_sl(N, k) + 1


def c_wn_principal(N: int, k: Num) -> Fraction:
    """Fateev-Lukyanov: c(W_N, k) = (N-1) - N(N^2-1)(k+N-1)^2/(k+N)."""
    k_f = _frac(k)
    N_f = Fraction(N)
    kN = k_f + N_f
    if kN == 0:
        raise ValueError(f"Critical level k = -{N}")
    return Fraction(N - 1) - N_f * (N_f ** 2 - 1) * (kN - 1) ** 2 / kN


# ============================================================================
# 2. Feigin-Frenkel duality (the algebraic core of chiral Koszul duality)
# ============================================================================

def ff_dual_level(k: Num, h_dual: int) -> Fraction:
    """Feigin-Frenkel dual level: k' = -k - 2h^v."""
    return -_frac(k) - 2 * h_dual


def ff_dual_psi(psi: Num) -> Fraction:
    """FF duality on coupling: Psi -> -Psi.

    Since k = Psi - N and k' = -k - 2N = -Psi + N - 2N = -Psi - N,
    the dual coupling is Psi' = k' + N = -Psi.
    """
    return -_frac(psi)


# ============================================================================
# 3. S-duality (the physical Langlands duality)
# ============================================================================

def s_dual_psi(psi: Num) -> Fraction:
    """S-duality on coupling: Psi -> 1/Psi."""
    psi_f = _frac(psi)
    if psi_f == 0:
        raise ValueError("Psi = 0: S-dual coupling diverges")
    return Fraction(1) / psi_f


def s_dual_level(k: Num, N: int) -> Fraction:
    """S-dual level: k -> k^S = 1/(k+N) - N.

    From Psi = k + N -> 1/Psi = 1/(k+N), so k^S = 1/(k+N) - N.
    """
    k_f = _frac(k)
    psi = k_f + N
    if psi == 0:
        raise ValueError("Psi = 0: S-dual undefined")
    return Fraction(1) / psi - N


