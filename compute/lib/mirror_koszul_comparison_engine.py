# 15. Webster's categorical KD vs our algebraic KD: detailed comparison
# ============================================================================

@dataclass(frozen=True)
class WebsterComparison:
    """Detailed comparison of Webster categorical KD with chiral algebraic KD.

    Webster [arXiv:1611.06541] proves:
        O(M_C) and O(M_H) are Koszul dual categories
        for type-A quiver gauge theories.

    Our Theorem A proves:
        B(A) -> D_Ran(B(A)) = B(A^!) (Verdier intertwining)
        for chiral algebras on curves.

    The question: when does the categorical equivalence
        O(A_C) ~ O(A_H)^{Koszul}
    agree with the algebraic identity
        A_C^! = (H*(B(A_C)))^v?

    ANSWER: They agree when A_C is FORMAL (shadow class G or L).
    For non-formal algebras (class C, M), the chiral bar complex
    captures higher homotopy data that the Ext algebra misses.
    """
    gauge_theory: str
    coulomb_algebra: str
    higgs_algebra: str
    shadow_class_coulomb: str
    shadow_class_higgs: str
    webster_applies: bool       # Webster's theorem covers this case
    algebraic_kd_applies: bool  # Our Theorem A applies
    both_agree: bool            # categorical KD = algebraic KD
    reason: str


def webster_comparison_sqed(N_f: int) -> WebsterComparison:
    """Compare Webster and chiral KD for SQED(N_f)."""
    # SQED Coulomb: C^2/Z_{N_f}, quantized = deformation of Kleinian singularity
    # Higgs: T*CP^{N_f-1}
    # Both are smooth (resolved) conical symplectic resolutions for N_f >= 2
    sc_C = "G"  # Coulomb boundary VOA = Heisenberg, class G
    sc_H = "G"  # Higgs boundary VOA = free bosons, class G

    return WebsterComparison(
        gauge_theory=f"SQED({N_f})",
        coulomb_algebra=f"Heisenberg H_1 (rank 1)",
        higgs_algebra=f"Free bosons (rank {N_f - 1})",
        shadow_class_coulomb=sc_C,
        shadow_class_higgs=sc_H,
        webster_applies=(N_f >= 2),
        algebraic_kd_applies=True,
        both_agree=True,
        reason="Both class G (Gaussian): categorical and algebraic KD agree. "
               "But they produce DIFFERENT dual algebras: "
               f"Webster gives O(T*CP^{{{N_f-1}}}), chiral KD gives H_1^! = H_{{-1}}.",
    )


def webster_comparison_type_a_quiver(n: int) -> WebsterComparison:
    """Compare Webster and chiral KD for A_n quiver gauge theory."""
    # A_n quiver: gauge ranks (1, 2, ..., n), flavor at end = n+1
    # Coulomb: shifted Yangian quotient (class L for n >= 2)
    # Higgs: nilpotent cone closure in sl_{n+1}
    if n <= 1:
        sc_C = "G"
    elif n <= 3:
        sc_C = "L"
    else:
        sc_C = "C"

    return WebsterComparison(
        gauge_theory=f"A_{n} quiver",
        coulomb_algebra=f"Shifted Yangian Y_mu(sl_{n})",
        higgs_algebra=f"Quantized nilcone closure in sl_{n+1}",
        shadow_class_coulomb=sc_C,
        shadow_class_higgs="L" if n >= 2 else "G",
        webster_applies=True,
        algebraic_kd_applies=True,
        both_agree=(sc_C in ("G", "L")),
        reason=f"Class {sc_C}: "
               + ("categorical and algebraic KD agree (formal)."
                  if sc_C in ("G", "L")
                  else "algebraic KD captures higher operations missed by categorical KD."),
    )


def webster_comparison_nahm(N: int) -> WebsterComparison:
    """Compare for principal Nahm pole (W-algebra boundary VOA)."""
    return WebsterComparison(
        gauge_theory=f"GL({N}) with principal Nahm pole",
        coulomb_algebra=f"W^k(sl_{N}) at level k",
        higgs_algebra=f"Dual W^{{k'}}(sl_{N})",
        shadow_class_coulomb="M",
        shadow_class_higgs="M",
        webster_applies=False,  # Webster's theorem does not cover W-algebras directly
        algebraic_kd_applies=True,
        both_agree=False,
        reason="Class M (infinite shadow depth): algebraic KD captures the full "
               "infinite shadow obstruction tower. Categorical KD for W-algebra "
               "category O is expected but not yet proved in the BLPW framework.",
    )


