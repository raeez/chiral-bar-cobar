@dataclass(frozen=True)
class ComplementarityComparison:
    """Compare Theorem C complementarity with mirror complementarity.

    Theorem C (Koszul complementarity):
        kappa(A) + kappa(A^!) = const  (level-independent)
        For sl_N: sum = 0
        For gl_N: sum = -2N
        For Virasoro: sum = 13

    Mirror complementarity:
        kappa_C(T) + kappa_H(T) = ?  (depends on the theory T)
        For SQED(N_f): sum = N_f

    These are DIFFERENT sums of DIFFERENT pairs.
    """
    algebra_name: str
    koszul_sum: Fraction         # kappa(A) + kappa(A^!)
    mirror_sum: Optional[Fraction]  # kappa_C + kappa_H
    s_duality_sum: Fraction      # kappa(A) + kappa(A^S)
    koszul_sum_independent_of_level: bool
    mirror_sum_independent_of_level: Optional[bool]
    sums_coincide: Optional[bool]  # koszul_sum == mirror_sum?


def complementarity_comparison_gl(N: int, psi: Num) -> ComplementarityComparison:
    """Compare Koszul and mirror complementarity for gl_N.

    Koszul sum: kappa(gl_N, k) + kappa(gl_N, -k-2N) = -2N (constant!).
    S-duality sum: kappa(gl_N, k) + kappa(gl_N, 1/Psi - N) = level-dependent.
    """
    psi_f = _frac(psi)
    k = psi_f - N

    kap = kappa_affine_gl(N, k)
    kap_ff = kappa_affine_gl(N, ff_dual_level(k, N))
    kap_s = kappa_affine_gl(N, s_dual_level(k, N))

    koszul_sum = kap + kap_ff
    s_sum = kap + kap_s

    return ComplementarityComparison(
        algebra_name=f"gl_{N}",
        koszul_sum=koszul_sum,
        mirror_sum=None,  # depends on specific gauge theory, not just the algebra
        s_duality_sum=s_sum,
        koszul_sum_independent_of_level=True,
        mirror_sum_independent_of_level=None,
        sums_coincide=None,
    )


def complementarity_comparison_sqed(N_f: int) -> ComplementarityComparison:
    """Compare Koszul and mirror complementarity for SQED(N_f).

    Coulomb boundary VOA: Heisenberg H_1, kappa_C = 1.
    Higgs boundary VOA: free bosons of rank N_f - 1, kappa_H = N_f - 1.
    Mirror sum: kappa_C + kappa_H = N_f.

    Koszul dual of H_1: kappa(H_1^!) = -1.
    Koszul sum: kappa_C + kappa(VOA_C^!) = 1 + (-1) = 0.
    """
    kC = Fraction(1)             # Heisenberg at level 1
    kH = Fraction(N_f - 1)      # free bosons of rank N_f - 1
    kC_dual = -kC                # Koszul dual: kappa -> -kappa (KM type)

    koszul_sum = kC + kC_dual  # = 0
    mirror_sum = kC + kH       # = N_f

    return ComplementarityComparison(
        algebra_name=f"SQED({N_f})",
        koszul_sum=koszul_sum,
        mirror_sum=mirror_sum,
        s_duality_sum=koszul_sum,  # for Heisenberg, S = FF at leading order
        koszul_sum_independent_of_level=True,
        mirror_sum_independent_of_level=True,
        sums_coincide=(koszul_sum == mirror_sum),
    )


