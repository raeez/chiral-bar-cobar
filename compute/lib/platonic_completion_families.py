# 4. Completion filtration decay rate
# ============================================================

def completion_filtration_decay(family, max_weight=20, **kwargs):
    """
    For the positive part A⁺, weight-n components decay as the completion
    filtration increases. The decay rate measures how fast the bar complex
    concentrates on finite-dimensional data.

    Returns array of (weight, dim_A_weight, dim_bar1_weight) triples.
    """
    if family == 'heisenberg':
        dims = heisenberg_weight_dims(max_weight)
    elif family == 'affine_sl2':
        k = kwargs.get('k', 1)
        dims = affine_sl2_weight_dims(k, max_weight)
    elif family == 'virasoro':
        dims = virasoro_weight_dims(max_weight)
    elif family == 'w3':
        dims = w3_weight_dims(max_weight)
    elif family == 'betagamma':
        dims = betagamma_weight_dims(max_weight)
    else:
        raise ValueError(f"Unknown family: {family}")

    # Bar^1 at weight n: for the reduced bar complex, bar^1 = A/C·1,
    # so dim(bar^1_n) = dim(A_n) for n >= 1, and 0 for n = 0
    result = []
    for n in range(max_weight + 1):
        d_A = dims[n]
        d_bar1 = dims[n] if n >= 1 else 0
        result.append((n, d_A, d_bar1))

    return result


