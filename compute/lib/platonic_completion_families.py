def heisenberg_weight_dims(max_weight):
    """
    Weight-space dimensions for the Heisenberg VOA at central charge 1.
    dim V_n = p(n) (number of partitions).
    Weight-0 subspace is 1-dimensional (the vacuum).
    """
    # Compute partition numbers via dynamic programming
    dims = _partitions_up_to(max_weight)
    return dims


@lru_cache(maxsize=64)
def _partitions_up_to(n):
    """Return list [p(0), p(1), ..., p(n)]."""
    p = [0] * (n + 1)
    p[0] = 1
    for k in range(1, n + 1):
        for j in range(k, n + 1):
            p[j] += p[j - k]
    return p


def affine_sl2_weight_dims(k, max_weight):
    """
    Weight-space dimensions for V_k(sl_2) at level k (non-critical: k != -2).
    At weight n, dim = number of ways to partition n using generators J^a_{-m} (m>=1),
    with 3 generators (a = +, -, 0). So dim V_n = p_3(n) (3-colored partitions).
    """
    if abs(k + 2) < 1e-12:
        raise ValueError("Critical level k = -2 not allowed")
    dims = _colored_partitions(3, max_weight)
    return dims


@lru_cache(maxsize=64)
def _colored_partitions(colors, n):
    """p_c(n): partitions of n with c colors of each part size."""
    p = [0] * (n + 1)
    p[0] = 1
    for size in range(1, n + 1):
        # c colors of this size
        for _ in range(colors):
            for j in range(size, n + 1):
                p[j] += p[j - size]
    return p


def virasoro_weight_dims(max_weight):
    """
    Weight-space dimensions for the Virasoro VOA (universal, generic c).
    Freely generated by L_{-n} (n >= 2). dim V_n = p(n) - p(n-1) for n >= 2,
    but more precisely: dim V_n = number of partitions of n into parts >= 2.
    """
    # Partitions into parts >= 2
    dims = [0] * (max_weight + 1)
    dims[0] = 1  # vacuum
    for size in range(2, max_weight + 1):
        for j in range(size, max_weight + 1):
            dims[j] += dims[j - size]
    # But the actual Verma module is freely generated, so dims[0] = 1
    return dims


def w3_weight_dims(max_weight):
    """
    Weight-space dimensions for the W_3 algebra (generic c).
    Generators: L (weight 2), W (weight 3). Free generation by L_{-n} (n>=2), W_{-m} (m>=3).
    Partitions of n into parts from {2,3,4,...} union {3,4,5,...} (two series).
    Equivalently: 1-colored parts >= 2 plus 1-colored parts >= 3.
    """
    dims = [0] * (max_weight + 1)
    dims[0] = 1
    # Parts from L: sizes >= 2, 1 color
    for size in range(2, max_weight + 1):
        for j in range(size, max_weight + 1):
            dims[j] += dims[j - size]
    # Parts from W: sizes >= 3, 1 color
    save = list(dims)
    for size in range(3, max_weight + 1):
        for j in range(size, max_weight + 1):
            dims[j] += dims[j - size]
    # Actually we need the product of the two generating series. Redo properly.
    dims_L = [0] * (max_weight + 1)
    dims_L[0] = 1
    for size in range(2, max_weight + 1):
        for j in range(size, max_weight + 1):
            dims_L[j] += dims_L[j - size]
    dims_W = [0] * (max_weight + 1)
    dims_W[0] = 1
    for size in range(3, max_weight + 1):
        for j in range(size, max_weight + 1):
            dims_W[j] += dims_W[j - size]
    # Convolve
    result = [0] * (max_weight + 1)
    for i in range(max_weight + 1):
        for j in range(max_weight + 1 - i):
            result[i + j] += dims_L[i] * dims_W[j]
    return result


def betagamma_weight_dims(max_weight):
    """
    Weight-space dimensions for the βγ system (bc ghost at λ=0).
    Generators: β (weight 1), γ (weight 1), so β_{-n}, γ_{-n} for n >= 1.
    Since these are fermionic-like in the bc system but bosonic in βγ:
    βγ is bosonic with 2 generators at each weight >= 1.
    dim V_n = p_2(n) (2-colored partitions).
    """
    dims = _colored_partitions(2, max_weight)
    return dims


