# 3. Resonance rank computation
# ============================================================

def resonance_rank(family, max_stage=10, **kwargs):
    """
    Compute the resonance rank ρ(A) by finding the stable dimension of
    the weight-0 persistent subspace of the bar complex.

    ρ(A) = lim_{n→∞} dim(weight-0 persistent classes in B^{≤n})

    We compute at stages 1, 2, ..., max_stage and check for stabilization.

    Returns (rho, stabilized_at_stage).
    """
    dims = []
    for s in range(1, max_stage + 1):
        d = bar_complex_weight0_dim(family, s, **kwargs)
        dims.append(d)

    # Check stabilization: find first stage where dim stabilizes
    if len(dims) == 0:
        return 0, 0

    rho = dims[-1]
    stab_stage = len(dims)
    for i in range(len(dims)):
        if all(dims[j] == dims[i] for j in range(i, len(dims))):
            rho = dims[i]
            stab_stage = i + 1
            break

    return rho, stab_stage


