def shadow_depth(family, **kwargs):
    """
    The shadow depth r_max(A) from the shadow archetype classification:
      G (Gaussian): r_max = 2 (Heisenberg)
      L (Lie/tree): r_max = 3 (affine)
      C (contact):  r_max = 4 (βγ)
      M (mixed):    r_max = ∞ (Virasoro, W_N)

    Shadow depth classifies COMPLEXITY, not Koszulness or resonance.
    """
    depths = {
        'heisenberg': 2,
        'affine_sl2': 3,
        'betagamma': 4,
        'virasoro': float('inf'),
        'w3': float('inf'),
    }
    return depths.get(family, None)


def shadow_archetype(family, **kwargs):
    """Return the shadow archetype class letter."""
    archetypes = {
        'heisenberg': 'G',
        'affine_sl2': 'L',
        'betagamma': 'C',
        'virasoro': 'M',
        'w3': 'M',
    }
    return archetypes.get(family, None)


