def virasoro_bpz_singular_vectors(c, max_level=10):
    """
    Count BPZ singular vectors at h=0 for the Virasoro algebra at central charge c.

    At h=0, singular vectors occur at levels given by the Kac determinant vanishing.
    The Kac determinant at level N for h=0 is:

      det M_N(c, 0) = Π_{1<=r<=s, rs<=N} (h - h_{r,s}(c))^{p(N-rs)}

    At h=0, this vanishes when h_{r,s}(c) = 0 for some (r,s) with rs <= N.

    h_{r,s}(c) = ((m+1)r - ms)^2 - 1) / (4m(m+1))
    where c = 1 - 6/m(m+1).

    For GENERIC c, h_{r,s} = 0 only at (r,s) = (1,1). The level-1 singular vector
    L_{-1}|0⟩ = 0 is the translation invariance, which is always present.

    The resonance direction comes NOT from singular vectors at h=0 per se,
    but from the duality structure Vir_c ↔ Vir_{26-c}.

    Returns list of (level, multiplicity) of singular vectors at h=0.
    """
    # For generic c, only the trivial singular vector at level 1
    # (L_{-1}|0⟩ = 0, translation invariance)
    singular_vectors = [(1, 1)]  # level 1, multiplicity 1

    # Check if c is on a minimal model locus c = 1 - 6/m(m+1)
    # At minimal models, additional singular vectors appear
    # We check a few known cases
    for m_val in range(2, 50):
        c_mm = 1 - 6.0 / (m_val * (m_val + 1))
        if abs(c - c_mm) < 1e-10:
            # Minimal model: additional singular vectors
            for r in range(1, m_val):
                for s in range(1, m_val + 1):
                    if r * s <= max_level and r * s > 1:
                        # h_{r,s} = ((m+1)r - ms)^2 - 1) / (4m(m+1))
                        h_rs = ((m_val + 1) * r - m_val * s) ** 2 - 1
                        h_rs /= (4 * m_val * (m_val + 1))
                        if abs(h_rs) < 1e-10:
                            singular_vectors.append((r * s, 1))
            break

    return singular_vectors


def virasoro_resonance_dimension(c):
    """
    The resonance dimension for Vir_c.

    For generic c: ρ = 1 (the single Vir_{26-c} direction).
    The Koszul dual Vir_c^! = Vir_{26-c} lives at a different central charge
    (unless c = 13, the self-dual point). This single duality direction
    constitutes the entire resonance piece R_A.

    For c = 0: Vir_0^! = Vir_26. Still ρ = 1.
    For c = 13: self-dual, Vir_13^! = Vir_13. Still ρ = 1 (identity resonance).
    For c = 26: Vir_26^! = Vir_0. Still ρ = 1.
    """
    return 1


