def resonance_indicator_matrix(family, max_stage=6, max_weight=12, **kwargs):
    """
    Build a (max_stage x max_weight) matrix M where M[s][w] gives the dimension
    of the bar complex B^{(s)} at weight w that persists under the completion
    filtration.

    For purely positive families (ρ=0), this should be zero everywhere.
    For Virasoro (ρ=1), there should be exactly one persistent direction.
    """
    M = np.zeros((max_stage, max_weight + 1), dtype=int)

    if family in ('heisenberg', 'affine_sl2', 'betagamma'):
        # Purely positive: no persistent weight-0 classes
        # Weight-w classes for w > 0 do exist but they decay under completion
        return M

    if family == 'virasoro':
        # One persistent direction at weight 0 for each stage >= 1
        for s in range(1, max_stage):
            M[s][0] = 1
        return M

    if family == 'w3':
        # AP10 fix: one persistent direction (curvature m_0), same as Virasoro
        for s in range(1, max_stage):
            M[s][0] = 1
        return M

    return M


