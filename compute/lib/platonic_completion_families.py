def platonic_completion_evidence(max_stage=10):
    """
    Compute resonance ranks for all standard families and verify finiteness.

    Returns dict: family -> {rho, stabilized_at, shadow_depth, archetype, finite}.
    """
    results = {}
    for fam in ALL_FAMILIES:
        kwargs = {}
        if fam == 'affine_sl2':
            kwargs['k'] = 1
        rho, stab = resonance_rank(fam, max_stage=max_stage, **kwargs)
        sd = shadow_depth(fam)
        arch = shadow_archetype(fam)
        results[fam] = {
            'rho': rho,
            'stabilized_at': stab,
            'shadow_depth': sd,
            'archetype': arch,
            'finite': rho < float('inf'),
        }
    return results


def verify_platonic_conjecture(results=None):
    """
    Check conj:platonic-completion: ρ(A) < ∞ for all families.
    Returns (all_finite, failures).
    """
    if results is None:
        results = platonic_completion_evidence()
    failures = []
    for fam, data in results.items():
        if not data['finite']:
            failures.append(fam)
    return len(failures) == 0, failures


