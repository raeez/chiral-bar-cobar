    match = (k_koszul == s_dual_level) if s_dual_level is not None else False

    return {
        "k": k,
        "h_v": h_v,
        "psi": psi,
        "koszul_dual_level": k_koszul,
        "s_dual_level": s_dual_level,
        "levels_match": match,
        "kappa": kap,
        "kappa_koszul_dual": kap_koszul,
        "kappa_s_dual": s_dual_kappa,
        "kappa_sum_koszul": kap + kap_koszul,  # should be 0 for KM
        "explanation": (
            "Koszul dual: k -> -k-4 (FF involution). "
            f"S-dual: Psi={psi} -> -1/Psi -> k'={s_dual_level}. "
            f"Match: {match}."
        ),
    }


