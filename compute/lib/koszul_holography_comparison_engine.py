def _frac(x) -> Fraction:
    if isinstance(x, Fraction):
        return x
    return Fraction(x)


@lru_cache(maxsize=64)
def _bernoulli(n: int) -> Fraction:
    if n == 0:
        return Fraction(1)
    if n == 1:
        return Fraction(-1, 2)
    if n % 2 == 1 and n >= 3:
        return Fraction(0)
    result = Fraction(0)
    for k in range(n):
        result += comb(n + 1, k) * _bernoulli(k)
    return -result / (n + 1)


@lru_cache(maxsize=64)
def _lambda_fp(g: int) -> Fraction:
    """Faber-Pandharipande lambda_g^FP = (2^{2g-1}-1)|B_{2g}| / (2^{2g-1}(2g)!)."""
    if g < 1:
        raise ValueError(f"g must be >= 1, got {g}")
    B2g = abs(_bernoulli(2 * g))
    num = (2 ** (2 * g - 1) - 1) * B2g
    den = Fraction(2 ** (2 * g - 1)) * Fraction(factorial(2 * g))
    return num / den


# ===========================================================================
# Kappa formulas (canonical, single implementation per AP-AAP3)
# ===========================================================================

def kappa_km(dim_g: int, h_v: int, k: Fraction) -> Fraction:
    """kappa(g, k) = dim(g) * (k + h^v) / (2 * h^v).  AP39: != c/2 in general."""
    k = _frac(k)
    if k + h_v == 0:
        raise ValueError(f"Critical level k = -{h_v}: Sugawara undefined")
    return Fraction(dim_g) * (k + Fraction(h_v)) / (2 * Fraction(h_v))


def kappa_slN(N: int, k: Fraction) -> Fraction:
    return kappa_km(N * N - 1, N, _frac(k))


def kappa_glN(N: int, k: Fraction) -> Fraction:
    return kappa_slN(N, k) + _frac(k)


def kappa_virasoro(c: Fraction) -> Fraction:
    return _frac(c) / 2


def kappa_heisenberg(k: Fraction) -> Fraction:
    return _frac(k)


def kappa_betagamma(lam: Fraction) -> Fraction:
    lam = _frac(lam)
    return 6 * lam ** 2 - 6 * lam + 1


def kappa_bc(lam: Fraction) -> Fraction:
    return -kappa_betagamma(lam)


def kappa_symplectic_boson() -> Fraction:
    return kappa_betagamma(Fraction(1, 2))


