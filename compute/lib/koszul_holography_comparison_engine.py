def koszul_vs_s_duality_sl2(k: Fraction) -> Dict[str, Any]:
    """Quantitative comparison: Koszul dual level vs S-dual level for sl_2.

    Koszul: k -> -k-4 (Feigin-Frenkel involution, h^v(sl_2) = 2).
    S-dual: k -> -4/k (Langlands dual coupling).

    These coincide iff -k-4 = -4/k, i.e., k^2 + 4k - 4 = 0,
    which gives k = -2 +/- 2*sqrt(2).

    Wait: the standard S-duality for sl_2 sends the complexified coupling
    Psi = k + h^v = k + 2 to -1/Psi. So the dual level is:
    Psi' = -1/Psi => k' + 2 = -1/(k+2) => k' = -1/(k+2) - 2 = -(2k+5)/(k+2).

    Our Koszul dual: k' = -k-4.

    These coincide when -k-4 = -(2k+5)/(k+2), i.e.,
    (-k-4)(k+2) = -(2k+5), i.e., -k^2-6k-8 = -2k-5, i.e.,
    k^2+4k+3 = 0, i.e., (k+1)(k+3) = 0. So k=-1 or k=-3.

    At k = -1: Koszul dual level = -(-1)-4 = -3. S-dual level = -(2(-1)+5)/(-1+2) = -3/1 = -3. Match.
    At k = -3: Koszul dual level = -(-3)-4 = -1. S-dual level = -(2(-3)+5)/(-3+2) = 1/1 = 1. NO MATCH.

    Wait, recompute S-dual at k=-3: Psi = -3+2 = -1. S-dual: -1/(-1) = 1. So k' = 1-2 = -1. Match!

    So they coincide at k=-1 and k=-3, which are the Psi = 1 and Psi = -1 points.
    The self-duality under both operations: Psi = +/-1, i.e., k = -1 or k = -3.
    """
    k = _frac(k)
