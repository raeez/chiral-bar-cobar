@dataclass
class CDRLocalData:
    r"""Local data of the chiral de Rham complex on a d-dimensional manifold.

    The local model is the semi-infinite Weil algebra W^{ch}_d:
      - d beta-gamma pairs (a^i, b_i) with weights (0, 1): c_{bg} = +2 each
      - d bc pairs (phi^i, psi_i) with weights (0, 1): c_{bc} = -2 each
      - Total c = d*(+2) + d*(-2) = 0

    The OPEs are:
      a^i(z) b_j(w) ~ delta^i_j / (z-w)     [beta-gamma]
      phi^i(z) psi_j(w) ~ delta^i_j / (z-w)  [bc, fermionic]

    Conformal vector:
      T = -sum_i :b_i partial(a^i): - sum_i :psi_i partial(phi^i):
    """
    complex_dim: int
    c_betagamma: int = 0  # set in post_init
    c_bc: int = 0
    c_total: int = 0
    n_bosonic_pairs: int = 0
    n_fermionic_pairs: int = 0

    def __post_init__(self):
        d = self.complex_dim
        self.n_bosonic_pairs = d
        self.n_fermionic_pairs = d
        # AP137: c_bg(lambda=1) = +2, c_bc(lambda=1) = -2
        self.c_betagamma = 2 * d
        self.c_bc = -2 * d
        self.c_total = self.c_betagamma + self.c_bc  # = 0

    def local_character(self, nmax: int = 20) -> List[int]:
        r"""Character of the local CDR = W^{ch}_d.

        As a graded vector space (by L_0 eigenvalue):
          ch(W^{ch}_d) = prod_{n>=1} (1 + q^n)^d / (1 - q^n)^d
                       * prod_{n>=0} (1 + q^n)^d / (1 - q^n)^d
        ... actually this is more subtle. Each beta-gamma pair contributes
        prod_{n>=0} 1/(1-q^n) for beta modes and prod_{n>=1} 1/(1-q^n) for gamma modes.
        Each bc pair contributes prod_{n>=0} (1+q^n) for phi and prod_{n>=1} (1+q^n) for psi.

        For the full character with y-grading (y tracks fermion number):
          ch = prod_{n>=1} ((1+y*q^n)(1+y^{-1}*q^{n-1}) / ((1-q^n)^2))^d
        at y=1 this becomes:
          ch|_{y=1} = prod_{n>=1} ((1+q^n)^2 / (1-q^n)^2)^d  [... with zero-mode corrections]

        We compute the q-expansion of the CHARACTER without y-grading (y=1),
        which counts total states at each energy level.

        For the CHIRAL character (relevant for CDR sheaf cohomology), the
        formula is (Borisov-Libgober, MSV):
          The local contribution per coordinate is:
          prod_{n>=1} 1/(1-q^n)^2    (beta-gamma) x
          prod_{n>=1} (1+q^n)^2      (bc, but NOT including zero modes)
          ... modulo zero-mode subtleties.

        In practice, for d=2 (K3), the LOCAL CDR character is:
          ch(W^{ch}_2; q) = prod_{n>=1} ((1-q^n)^{-2} * (1+q^n)^2)^2
                          = prod_{n>=1} ((1+q^n)/(1-q^n))^4
        """
        d = self.complex_dim
        # Compute prod_{n>=1} ((1+q^n)/(1-q^n))^{2d}
        # = prod (1+q^n)^{2d} / (1-q^n)^{2d}
        # Numerator: (1+q^n)^{2d} = eta(2tau)^{2d} * 2^{2d} / eta(tau)^{2d} * ...
        # We compute directly via q-expansion.

        # (1+q^n) coefficients
        plus = [0] * nmax
        plus[0] = 1
        for n in range(1, nmax):
            # Multiply by (1 + q^n)
            new = list(plus)
            for j in range(nmax - n):
                new[j + n] += plus[j]
            plus = new

        # (1-q^n)^{-1} coefficients = partition-type
        minus_inv = _eta_inverse_coeffs(nmax)

        # Combine: ((1+q^n)/(1-q^n))^{2d}
        # = (prod (1+q^n))^{2d} * (prod 1/(1-q^n))^{2d}
        result = [0] * nmax
        result[0] = 1

        # (prod (1+q^n))^{2d}
        num = [0] * nmax
        num[0] = 1
        for _ in range(2 * d):
            num = _convolve(num, plus, nmax)

        # (prod 1/(1-q^n))^{2d}
        denom = [0] * nmax
        denom[0] = 1
        for _ in range(2 * d):
            denom = _convolve(denom, minus_inv, nmax)

        result = _convolve(num, denom, nmax)
        return result

    def betagamma_character(self, nmax: int = 20) -> List[int]:
        r"""Character of d beta-gamma pairs = prod_{n>=1} 1/(1-q^n)^{2d}.

        Each pair contributes prod_{n>=1} 1/(1-q^n)^2 (one from beta modes
        at weight n >= 1, one from gamma modes at weight n >= 1).
        The zero modes are treated separately.
        """
        d = self.complex_dim
        eta_inv = _eta_inverse_coeffs(nmax)
        result = [0] * nmax
        result[0] = 1
        for _ in range(2 * d):
            result = _convolve(result, eta_inv, nmax)
        return result

    def bc_character(self, nmax: int = 20) -> List[int]:
        r"""Character of d bc pairs (fermion number summed) at weights >= 1.

        Each pair contributes prod_{n>=1} (1+q^n)^2.
        """
        d = self.complex_dim
        plus = [0] * nmax
        plus[0] = 1
        for n in range(1, nmax):
            new = list(plus)
            for j in range(nmax - n):
                new[j + n] += plus[j]
            plus = new

        result = [0] * nmax
        result[0] = 1
        for _ in range(2 * d):
            result = _convolve(result, plus, nmax)
        return result


def cdr_local_data(dim: int) -> CDRLocalData:
    """Construct CDR local data for complex dimension dim."""
    return CDRLocalData(complex_dim=dim)


