# =====================================================================

def k3_elliptic_genus_fourier(nmax: int = 15) -> Dict[Tuple[int, int], int]:
    r"""Fourier coefficients of the K3 elliptic genus = 2 * phi_{0,1}.

    Returns {(n, l): 2*c(n,l)} where phi_{0,1} = sum c(n,l) q^n y^l.

    MULTI-PATH VERIFICATION:
      Path 1: Direct from discriminant-indexed coefficients (this function)
      Path 2: Constancy check (sum_l c(n,l) = 0 for n >= 1; = 24 for n = 0)
      Path 3: Euler characteristic (sum_l c(0,l) = 24 = chi(K3))
    """
    phi01_disc = _phi01_by_discriminant(4 * nmax + 10)
    result = {}
    for n in range(nmax):
        for l in range(-2 * n - 2, 2 * n + 3):
            D = 4 * n - l * l
            if D < -1:
                continue
            c_val = phi01_disc.get(D, 0)
            if c_val != 0:
                result[(n, l)] = 2 * c_val
    return result


def k3_elliptic_genus_y0(nmax: int = 15) -> List[int]:
    r"""Coefficients of the K3 elliptic genus at z=0 (y=1).

    Ell(K3; tau, 0) = 24 (constant).

    Returns [24, 0, 0, ...].
    """
    result = [0] * nmax
    coeffs = k3_elliptic_genus_fourier(nmax)
    for (n, l), c in coeffs.items():
        if 0 <= n < nmax:
            result[n] += c
    return result


def verify_elliptic_genus_constancy(nmax: int = 15) -> Tuple[bool, List[int]]:
    r"""Verify that Ell(K3; tau, 0) = 24 (constant in q).

    This is a non-trivial check: at each power of q, the sum over all
    y-charges must cancel to give 0 (for n >= 1) or 24 (for n = 0).
    """
    y0_coeffs = k3_elliptic_genus_y0(nmax)
    expected = [24] + [0] * (nmax - 1)
    is_constant = (y0_coeffs == expected)
    return is_constant, y0_coeffs


