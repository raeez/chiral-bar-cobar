def sigma_k(n: int, k: int) -> int:
    """Divisor sum sigma_k(n) = sum_{d|n} d^k."""
    if n <= 0:
        return 0
    return sum(d ** k for d in range(1, n + 1) if n % d == 0)


@lru_cache(maxsize=4096)
def partition_count(n: int) -> int:
    """Number of integer partitions of n, by pentagonal recurrence."""
    if n < 0:
        return 0
    if n == 0:
        return 1
    s = 0
    for k in range(1, n + 1):
        p1 = k * (3 * k - 1) // 2
        p2 = k * (3 * k + 1) // 2
        if p1 > n:
            break
        sign = (-1) ** (k + 1)
        s += sign * partition_count(n - p1)
        if p2 <= n:
            s += sign * partition_count(n - p2)
    return s


def bernoulli_number(n: int) -> Fraction:
    """Exact Bernoulli number B_n."""
    if n < 0:
        return Fraction(0)
    if n == 1:
        return Fraction(-1, 2)
    if n % 2 == 1 and n > 1:
        return Fraction(0)
    B = [Fraction(0)] * (n + 1)
    B[0] = Fraction(1)
    for m in range(1, n + 1):
        B[m] = Fraction(0)
        for kk in range(m):
            B[m] -= Fraction(math.comb(m, kk), m - kk + 1) * B[kk]
    return B[n]


def _convolve(a: List, b: List, nmax: int) -> List:
    """Cauchy product (convolution) truncated to nmax terms."""
    result = [0] * nmax
    la, lb = len(a), len(b)
    for i in range(min(la, nmax)):
        if a[i] == 0:
            continue
        for j in range(min(lb, nmax - i)):
            result[i + j] += a[i] * b[j]
    return result


def _convolve_frac(a: List, b: List, nmax: int) -> List:
    """Cauchy product with Fraction arithmetic."""
    result = [Fraction(0)] * nmax
    la, lb = len(a), len(b)
    for i in range(min(la, nmax)):
        if a[i] == 0:
            continue
        for j in range(min(lb, nmax - i)):
            result[i + j] += Fraction(a[i]) * Fraction(b[j])
    return result


