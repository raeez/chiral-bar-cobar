# Spectral sequence decomposition
# ---------------------------------------------------------------------------

def genus3_spectral_sequence_e1() -> Dict[int, List[StableGraph]]:
    """Genus spectral sequence E_1 page at total genus 3.

    Separates the 42 graphs by loop number h^1 = p. The E_1 page is:
      E_1^{p,q} with p + q = 3 - 1 = 2... actually, the filtration by
      loop genus gives:
        p = 0: trees (h^1 = 0), 4 graphs
        p = 1: one-loop (h^1 = 1), 9 graphs
        p = 2: two-loop (h^1 = 2), 14 graphs
        p = 3: three-loop (h^1 = 3), 15 graphs

    The differentials d_r: E_r^{p,q} -> E_r^{p+r, q-r+1} are the
    obstruction maps Ob_g from the genus spectral sequence
    (const:vol1-genus-spectral-sequence).

    Returns {h1: [list of graphs]}.
    """
    graphs = genus3_stable_graphs_n0()
    result: Dict[int, List[StableGraph]] = {}
    for g in graphs:
        h1 = g.first_betti
        result.setdefault(h1, []).append(g)
    return result


def genus3_spectral_sequence_counts() -> Dict[int, int]:
    """Count of graphs at each loop level.

    Returns {h1: count}:
      0 -> 4, 1 -> 9, 2 -> 14, 3 -> 15
    """
    pages = genus3_spectral_sequence_e1()
    return {h1: len(graphs) for h1, graphs in pages.items()}


