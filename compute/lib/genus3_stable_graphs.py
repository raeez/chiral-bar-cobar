def genus3_boundary_strata() -> Dict[int, List[StableGraph]]:
    """Classify boundary components of M_bar_{3,0} by codimension.

    The boundary Delta = M_bar_{3,0} - M_3 consists of all graphs with
    at least one edge. The codimension of a stratum is the number of
    edges (= number of nodes of the curve).

    dim(M_bar_{3,0}) = 3(3) - 3 = 6, so:
      codim 0:  1 graph  (interior = smooth genus-3 curve)
      codim 1:  2 graphs (irreducible node g=2, separating g=2+1)
      codim 2:  5 graphs
      codim 3:  9 graphs
      codim 4: 12 graphs
      codim 5:  8 graphs
      codim 6:  5 graphs (maximally degenerate, |E| = dim(M) = 6)

    The boundary divisors at codimension 1 are:
      Delta_0 (irreducible): genus-2 curve with a non-separating node
      Delta_1 (separating):  genus-2 + genus-1 curves joined at a point

    Returns {codimension: [list of graphs]}.
    """
    graphs = genus3_stable_graphs_n0()
    result: Dict[int, List[StableGraph]] = {}
    for g in graphs:
        codim = g.num_edges
        result.setdefault(codim, []).append(g)
    return result


def genus3_boundary_divisor_types() -> Dict[str, List[StableGraph]]:
    """Classify the codimension-1 boundary divisors of M_bar_{3,0}.

    Returns:
      "Delta_irr": the irreducible boundary (1 vertex g=2, 1 self-loop)
      "Delta_1": the separating node (vertices g=2 and g=1, 1 edge)

    These are the two boundary divisors of M_bar_{3,0} at codimension 1.
    """
    graphs = genus3_stable_graphs_n0()
    codim1 = [g for g in graphs if g.num_edges == 1]

    result: Dict[str, List[StableGraph]] = {"Delta_irr": [], "Delta_1": []}
    for g in codim1:
        if g.num_vertices == 1:
            # Self-loop on a single vertex: irreducible boundary
            result["Delta_irr"].append(g)
        else:
            # Two vertices joined by an edge: separating node
            result["Delta_1"].append(g)
    return result


# ---------------------------------------------------------------------------
# Graph profiles by shadow depth class
# ---------------------------------------------------------------------------

def genus3_graph_profiles(family: str = "G") -> Dict[str, object]:
    """Which graphs are active for each shadow depth class.

    At the scalar level (class G = Gaussian, Heisenberg), ALL graphs
    contribute via kappa^|E| / |Aut|.

    For shadow-depth class L (Lie/tree, r_max = 3), the cubic correction
    activates at graphs with at least one vertex of valence >= 3.

    For class C (contact, r_max = 4), the quartic correction activates
    at graphs with at least one vertex of valence >= 4.

    For class M (mixed, r_max = inf), all arity corrections are active.

    Returns a dict with keys:
      "active_count": number of graphs receiving corrections beyond scalar
      "scalar_only_count": number of graphs at pure scalar level
      "active_graphs": list of graph indices receiving higher corrections
      "scalar_sum": the pure scalar sum for this family
    """
    graphs = genus3_stable_graphs_n0()

    if family == "G":
        # Gaussian: shadow terminates at arity 2, everything is scalar
        min_val_for_correction = 999  # never triggers
    elif family == "L":
        # Lie/tree: cubic correction at valence >= 3
        min_val_for_correction = 3
    elif family == "C":
        # Contact: quartic correction at valence >= 4
        min_val_for_correction = 4
    elif family == "M":
        # Mixed: corrections at all arities >= 3
        min_val_for_correction = 3
    else:
        raise ValueError(f"Unknown family: {family}. Use 'G', 'L', 'C', 'M'.")

    active = []
    scalar_only = []
    for i, g in enumerate(graphs):
        max_val = max(g.valence)
        if max_val >= min_val_for_correction:
            active.append(i)
        else:
            scalar_only.append(i)

    # Scalar sum with kappa = 1 for normalization
    scalar_sum = graph_sum_scalar(graphs, kappa=Fraction(1))

    return {
        "active_count": len(active),
        "scalar_only_count": len(scalar_only),
        "active_graphs": active,
        "scalar_only_graphs": scalar_only,
        "scalar_sum_kappa1": scalar_sum,
    }


