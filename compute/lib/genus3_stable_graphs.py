# Scalar graph sum (polynomial in kappa)
# ---------------------------------------------------------------------------

def genus3_scalar_sum_polynomial() -> Dict[int, Fraction]:
    """Scalar graph sum as a polynomial in kappa.

    sum_Gamma kappa^|E(Gamma)| / |Aut(Gamma)| = sum_{e=0}^{6} c_e * kappa^e

    where c_e = sum_{Gamma: |E|=e} 1/|Aut(Gamma)|.

    Returns {edge_count: coefficient}:
      0 -> 1
      1 -> 3/2
      2 -> 15/8
      3 -> 107/48
      4 -> 97/48
      5 -> 55/48
      6 -> 5/16
    """
    graphs = genus3_stable_graphs_n0()
    coeffs: Dict[int, Fraction] = {}
    for g in graphs:
        ne = g.num_edges
        coeffs[ne] = coeffs.get(ne, Fraction(0)) + Fraction(1, g.automorphism_order())
    return coeffs


def genus3_scalar_sum_at(kappa: Fraction) -> Fraction:
    """Evaluate the scalar graph sum at a specific kappa value.

    This is sum_Gamma kappa^|E(Gamma)| / |Aut(Gamma)|.
    """
    return graph_sum_scalar(genus3_stable_graphs_n0(), kappa=kappa)


