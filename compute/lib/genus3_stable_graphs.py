def genus3_amplitudes_heisenberg(k: Fraction = Fraction(1),
                                 d: int = 1) -> Fraction:
    """Genus-3 free energy for Heisenberg of rank d at level k.

    F_3(H_k^d) = kappa(H_k^d) * lambda_3^FP = k * d * 31/967680

    Heisenberg is shadow-depth class G (Gaussian, r_max = 2): the shadow
    tower terminates at arity 2, so F_g is purely determined by kappa.
    No cubic or higher corrections.
    """
    kappa = k * d
    return kappa * lambda3_fp()


def genus3_amplitudes_affine(k: Fraction, g_type: str = "sl2") -> Fraction:
    """Genus-3 free energy for affine Kac-Moody V_k(g).

    F_3(V_k(g)) = kappa(V_k(g)) * lambda_3^FP + (cubic shadow corrections)

    At the SCALAR level (shadow depth class L, r_max = 3), the cubic
    shadow C_3 contributes a correction proportional to the structure
    constants. For the purpose of this module, we compute the scalar
    (kappa-only) contribution:

      kappa(V_k(g)) = dim(g) * (k + h^v) / 2

    The cubic correction is genus-dependent and involves integration
    over M_{g(v), val(v)} at vertices with val >= 3. At genus 3 the
    correction involves all graphs with at least one vertex of valence >= 3.

    Supported types: "sl2" (dim=3, h^v=2), "sl3" (dim=8, h^v=3),
    "slN" with N given, or general (dim, h_vee) pair.

    Returns the scalar-level F_3 (kappa contribution only).
    """
    if g_type == "sl2":
        dim_g, h_vee = 3, 2
    elif g_type == "sl3":
        dim_g, h_vee = 8, 3
    elif g_type.startswith("sl"):
        N = int(g_type[2:])
        dim_g = N * N - 1
        h_vee = N
    else:
        raise ValueError(f"Unknown type: {g_type}. Use 'sl2', 'sl3', 'slN'.")

    kappa = Fraction(dim_g) * (k + h_vee) / Fraction(2)
    return kappa * lambda3_fp()


def genus3_amplitudes_affine_general(k: Fraction, dim_g: int,
                                     h_vee: int) -> Fraction:
    """Genus-3 scalar free energy for V_k(g) with explicit dim and h^v.

    kappa(V_k(g)) = dim(g) * (k + h^v) / 2
    F_3 = kappa * lambda_3^FP   (scalar level)
    """
    kappa = Fraction(dim_g) * (k + h_vee) / Fraction(2)
    return kappa * lambda3_fp()


def genus3_amplitudes_virasoro(c: Fraction) -> Fraction:
    """Genus-3 free energy for Virasoro at central charge c.

    F_3(Vir_c) = kappa(Vir_c) * lambda_3^FP + (higher shadow corrections)

    kappa(Vir_c) = c/2. Virasoro is shadow-depth class M (mixed, r_max = inf),
    so the full answer involves the infinite shadow tower. This function
    returns the scalar-level (kappa-only) contribution.

    The cubic correction is proportional to c and involves only graphs with
    at least one vertex of valence >= 3 carrying a genus >= 1 vertex.
    The quartic contact invariant Q^contact_Vir = 10/[c(5c+22)] is nonzero,
    confirming that the quartic correction is present at genus 3.
    """
    kappa = c / Fraction(2)
    return kappa * lambda3_fp()


def genus3_amplitudes_betagamma() -> Fraction:
    """Genus-3 scalar free energy for beta-gamma (bc ghost system).

    kappa(beta-gamma) = -2 (the negative value reflects the fermionic ghost
    number anomaly). Shadow-depth class C (contact, r_max = 4).

    At the scalar level: F_3 = -2 * lambda_3^FP = -2 * 31/967680
                              = -31/483840
    """
    kappa = Fraction(-2)
    return kappa * lambda3_fp()


