def genus3_named_graphs() -> Dict[str, StableGraph]:
    """Identify the most important named graphs at genus 3.

    Returns a dictionary of descriptive names to StableGraph objects
    for the key graph types that appear in the modular Koszul theory:

    Single-vertex (4):
      "smooth":       g=3, no edges. The interior of M_3.
      "irr_node":     g=2, 1 self-loop. Delta_irr restricted to genus 3.
      "double_loop":  g=1, 2 self-loops. The banana at genus 1.
      "triple_loop":  g=0, 3 self-loops. Deepest single-vertex graph.

    Two-vertex, notable (4):
      "separating_21":  g=(2,1), 1 edge. Separating boundary Delta_1.
      "parallel_11":    g=(1,1), 2 parallel edges. One-loop cylinder.
      "theta_10":       g=(1,0), 3 parallel edges. Theta with genus shift.
      "quadruple_00":   g=(0,0), 4 parallel edges. h^1 = 3 banana-4.

    Tree (4):
      "tree_3":         Smooth genus-3. (= smooth above)
      "tree_21":        g=(2,1), 1 edge. (= separating_21 above)
      "tree_111_chain": g=(1,1,1), 2-edge chain.
      "tree_1110_star": g=(1,1,1,0), 3-edge star with central g=0.

    Deepest stratum (1):
      "K4":  Complete graph K_4 on 4 genus-0 vertices. 6 edges. |Aut| = 24.
    """
    graphs = genus3_stable_graphs_n0()
    named = {}

    for g in graphs:
        nv = g.num_vertices
        ne = g.num_edges
        vg = g.vertex_genera

        # Single-vertex graphs
        if nv == 1:
            loops = g.self_loop_count(0)
            if loops == 0:
                named["smooth"] = g
            elif loops == 1:
                named["irr_node"] = g
            elif loops == 2:
                named["double_loop"] = g
            elif loops == 3:
                named["triple_loop"] = g

        # Two-vertex graphs: pick notable ones
        elif nv == 2:
            multi = g.edge_multiplicity(0, 1)
            l0, l1 = g.self_loop_count(0), g.self_loop_count(1)
            sorted_genera = tuple(sorted(vg))

            if sorted_genera == (1, 2) and ne == 1:
                named["separating_21"] = g
            elif sorted_genera == (1, 1) and multi == 2 and l0 == 0 and l1 == 0:
                named["parallel_11"] = g
            elif sorted_genera == (0, 1) and multi == 3:
                named["theta_10"] = g
            elif sorted_genera == (0, 0) and multi == 4:
                named["quadruple_00"] = g

        # Three-vertex trees
        elif nv == 3 and g.first_betti == 0:
            named["tree_111_chain"] = g

        # Four-vertex special
        elif nv == 4:
            if g.first_betti == 0:
                named["tree_1110_star"] = g
            elif (ne == 6 and all(gv == 0 for gv in vg)
                  and g.automorphism_order() == 24):
                # K_4: complete graph, every pair connected by exactly one edge
                # Check: 6 edges on 4 vertices, all distinct pairs
                edge_set = set(g.edges)
                if len(edge_set) == 6:
                    named["K4"] = g

    return named


# ---------------------------------------------------------------------------
# Consistency with genus 2
# ---------------------------------------------------------------------------

def genus3_genus2_consistency_check() -> bool:
    """Verify structural consistency between genus-2 and genus-3 enumerations.

    Check 1: The number of genus-2 graphs (6) is less than genus-3 (42).
    Check 2: lambda_2 and lambda_3 satisfy the correct ratio:
      lambda_3 / lambda_2 = (31/967680) / (7/5760) = 31*5760 / (7*967680)
                          = 178560 / 6773760 = 31 / 1176 = 31/(168*7)
    Check 3: The orbifold Euler characteristics are computed from the same formula.
    """
    from compute.lib.stable_graph_enumeration import genus2_stable_graphs_n0
    g2 = genus2_stable_graphs_n0()
    g3 = genus3_stable_graphs_n0()

    # Check 1: count growth
    if not (len(g3) > len(g2)):
        return False

    # Check 2: lambda ratio
    l2 = _lambda_fp_exact(2)
    l3 = _lambda_fp_exact(3)
    ratio = l3 / l2
    expected_ratio = Fraction(31 * 5760, 7 * 967680)
    if ratio != expected_ratio:
        return False

    # Check 3: Euler characteristics from same engine
    chi2 = orbifold_euler_characteristic(g2)
    chi3 = orbifold_euler_characteristic(g3)
    if chi2 != Fraction(-181, 1440):
        return False
    if chi3 != Fraction(-12419, 90720):
        return False

    return True


