# ---------------------------------------------------------------------------
# Orbifold Euler characteristic
# ---------------------------------------------------------------------------

def genus3_euler_check() -> Tuple[Fraction, Fraction, bool]:
    """Verify the orbifold Euler characteristic of M_bar_{3,0}.

    The vertex-product formula gives:
      chi^orb(M_bar_{3,0}) = sum_Gamma prod_v chi^orb(M_{g(v),val(v)}) / |Aut(Gamma)|

    The known value from the graph sum is -12419/90720.

    Returns (computed_chi, expected_chi, match).
    """
    graphs = genus3_stable_graphs_n0()
    computed = orbifold_euler_characteristic(graphs)
    expected = Fraction(-12419, 90720)
    return (computed, expected, computed == expected)


def genus3_euler_decomposition() -> List[Tuple[int, Fraction]]:
    """Per-graph orbifold Euler characteristic contributions.

    Returns a list of (graph_index, chi_contribution) pairs.
    The sum of all contributions equals chi^orb(M_bar_{3,0}).
    """
    graphs = genus3_stable_graphs_n0()
    result = []
    for i, gamma in enumerate(graphs):
        val = gamma.valence
        vertex_product = Fraction(1)
        for v in range(gamma.num_vertices):
            vertex_product *= _chi_orb_open(gamma.vertex_genera[v], val[v])
        contrib = vertex_product / Fraction(gamma.automorphism_order())
        result.append((i, contrib))
    return result


# ---------------------------------------------------------------------------
# Lambda number and Faber-Pandharipande
# ---------------------------------------------------------------------------

def lambda3_fp() -> Fraction:
    """Faber-Pandharipande intersection number at genus 3.

    lambda_3^FP = (2^5 - 1) |B_6| / (2^5 * 6!)
               = 31 * (1/42) / (32 * 720)
               = 31 / 967680

    This is the tautological integral:
      int_{M_bar_{3,1}} psi^4 lambda_3 = 31/967680
    """
    return _lambda_fp_exact(3)


