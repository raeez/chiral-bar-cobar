# =========================================================================
# PBW character computation
# =========================================================================

def pbw_character(generators: List[Tuple[int, int]], max_weight: int) -> List[int]:
    """PBW character for given generators.

    generators: list of (conformal_weight, multiplicity) pairs.
    Returns: list of dimensions [dim_0, dim_1, ..., dim_{max_weight}].

    The PBW character is:
      prod over (h, m) of [prod_{n>=h} 1/(1-q^n)]^m

    This counts normally ordered monomials assuming FREE generation
    (no null vectors / relations).
    """
    # Compute each factor separately, then convolve.
    # For a single bosonic generator at weight h:
    # GF = prod_{n>=h} 1/(1-q^n) = sum p(k; parts >= h) q^k

    def single_generator_gf(h: int, mw: int) -> List[int]:
        """Partition generating function with parts >= h."""
        c = [0] * (mw + 1)
        c[0] = 1
        for part in range(h, mw + 1):
            for w in range(part, mw + 1):
                c[w] += c[w - part]
        return c

    def convolve(a: List[int], b: List[int], mw: int) -> List[int]:
        """Convolve two generating functions."""
        result = [0] * (mw + 1)
        for i in range(mw + 1):
            for j in range(mw + 1 - i):
                result[i + j] += a[i] * b[j]
        return result

    # Start with 1
    total = [0] * (max_weight + 1)
    total[0] = 1

    for h, m in generators:
        single = single_generator_gf(h, max_weight)
        for _ in range(m):
            total = convolve(total, single, max_weight)

    return total


def triplet_pbw_character(p: int, max_weight: int = 20) -> List[int]:
    """PBW character for W(p) with 4 generators.

    Generators: T (wt 2, mult 1), W^a (wt 2p-1, mult 3).
    """
    h_W = 2 * p - 1
    return pbw_character([(2, 1), (h_W, 3)], max_weight)


def virasoro_pbw_character(max_weight: int = 20) -> List[int]:
    """PBW character for the Virasoro sub-VOA (1 generator at wt 2)."""
    return pbw_character([(2, 1)], max_weight)


