# =========================================================================
# Central charge (verified, 3 paths)
# =========================================================================

def triplet_central_charge(p: int) -> Rational:
    """Central charge c(p) = 1 - 6(p-1)^2/p.

    Three verification paths:
      Path 1: Direct formula.
      Path 2: Feigin-Fuchs c = 1 - 3Q^2 with Q = sqrt(2p) - sqrt(2/p).
      Path 3: Known values from published tables.
    """
    if p < 2:
        raise ValueError(f"p must be >= 2, got {p}")
    return Rational(1) - Rational(6 * (p - 1)**2, p)


def triplet_kappa(p: int) -> Rational:
    """Modular characteristic kappa(W(p)) = c(p)/2.

    The genus-1 obstruction is controlled by the Virasoro sector
    (AP48: kappa depends on the full algebra, but for W(p) the
    Virasoro sub-VOA determines kappa at genus 1).
    """
    return triplet_central_charge(p) / 2


