def ope_data(p: int) -> Dict:
    """OPE structure for W(p).

    T-T OPE: standard Virasoro, max pole order 4.
    T-W OPE: primary field, max pole order 2.
    W-W OPE: max pole order 2 * (2p-1) (two fields of weight 2p-1).

    AP19: bar r-matrix pole = OPE pole - 1 (d log absorption).
    """
    h_W = 2 * p - 1
    ww_pole = 2 * h_W
    return {
        'p': p,
        'TT_max_pole': 4,
        'TW_max_pole': 2,
        'WW_max_pole': ww_pole,
        'overall_max_pole': ww_pole,
        'bar_r_matrix_max_pole': ww_pole - 1,
        'is_quadratic': False,  # WW pole > 4 for all p >= 2
    }


