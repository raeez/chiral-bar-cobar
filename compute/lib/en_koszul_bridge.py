def propagator_degree(n: int) -> int:
    """Degree of the propagator form on FM_k(R^n).

    The propagator is a closed (n-1)-form.
    At n=1: degree 0 (function/distribution)
    At n=2: degree 1 (logarithmic 1-form d log(z_i-z_j))
    At n=3: degree 2 (Chern-Simons propagator)
    """
    return n - 1


def propagator_form(n: int) -> str:
    """Description of the propagator form at manifold dimension n.

    Returns a human-readable description.
    """
    if n == 1:
        return "theta(x_i - x_j): step function (degree 0)"
    elif n == 2:
        return "eta_{ij} = d log(z_i - z_j): logarithmic 1-form (degree 1)"
    elif n == 3:
        return "Omega_{ij} = d(angle form on S^2): closed 2-form (degree 2)"
    else:
        return f"Closed (n-1)-form on S^{{n-1}} linking sphere (degree {n-1})"


def collision_relation_type(n: int) -> str:
    """Type of collision relations at manifold dimension n.

    n=2: Arnold relations (partial fractions for logarithmic forms)
    n>=3: Totaro relations (generalized Arnold for higher codimension)
    n=1: no relations (ordered configuration space is contractible)
    """
    if n == 1:
        return "none (ordered configuration contractible)"
    elif n == 2:
        return "Arnold relations"
    else:
        return "Totaro relations"


