def en_koszul_dual_operad(n: int) -> str:
    """Koszul dual of the E_n operad.

    E_1^! = E_1 (associative is self-dual, up to suspension)
    E_2^! = E_2 (little 2-disks is self-dual, Kontsevich)
    E_n^! = E_n for all n (E_n is self-dual)
    E_inf^! = E_inf (commutative is self-dual... WAIT)

    CRITICAL CORRECTION: Com^! = Lie, NOT Com.
    The correct statement:
      E_n^! = E_n{n} (shift by n)
    In the limit:
      E_inf^! = Lie{inf} which is Lie (shifted)

    So the E_n operad is Koszul self-dual up to a shift.
    """
    if n == 1:
        return "E_1{1} = Ass{1} (associative, shifted)"
    elif n == 2:
        return "E_2{2} (self-dual up to 2-shift)"
    else:
        return f"E_{n}{{{n}}} (self-dual up to {n}-shift)"


def en_koszul_algebras(n: int) -> Dict[str, str]:
    """Koszul duality for algebras at operad level n.

    E_1: Ass-algebras (A_inf). Koszul dual: A -> A^! (Ext-algebra).
         Bar-cobar: classical bar construction B(A).
    E_2: Braided algebras. Koszul dual: involves R-matrix.
         Bar-cobar: chiral bar complex B^{ch}(A).
    E_inf: Commutative algebras. Koszul dual: Com -> Lie.
           Com^! = Lie (NOT coLie).
    """
    if n == 1:
        return {
            'algebra_type': 'associative (A_inf)',
            'bar_complex': 'classical bar B(A)',
            'koszul_dual': 'Ext-algebra A^!',
            'key_structure': 'Stasheff associahedra K_n',
        }
    elif n == 2:
        return {
            'algebra_type': 'braided/chiral',
            'bar_complex': 'chiral bar B^{ch}(A)',
            'koszul_dual': 'chiral Koszul dual A^!',
            'key_structure': 'FM_n(C) with Arnold relations',
        }
    else:
        return {
            'algebra_type': f'E_{n}-algebra',
            'bar_complex': f'E_{n}-bar complex',
            'koszul_dual': f'E_{n}-Koszul dual',
            'key_structure': f'FM_n(R^{n}) with Totaro relations',
        }


