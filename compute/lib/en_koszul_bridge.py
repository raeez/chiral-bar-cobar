def fm_betti_table(max_n: int = 6) -> Dict[int, Dict[str, Any]]:
    """Table of FM_n(C) Betti numbers for n = 1, ..., max_n."""
    table = {}
    for n in range(1, max_n + 1):
        betti = arnold_betti_numbers(n)
        table[n] = {
            'betti': betti,
            'total': sum(betti),
            'euler': arnold_euler_characteristic(n),
            'num_generators': arnold_num_generators(n),
            'num_relations': arnold_num_relations(n),
            'top_degree': len(betti) - 1 if betti else 0,
        }
    return table
