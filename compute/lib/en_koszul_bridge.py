def totaro_arnold_dictionary_n2() -> Dict[str, Any]:
    """Totaro-Arnold dictionary at n=2.

    For FM_n(R^d) with d >= 2 (including d=2, i.e., C):
    The cohomology H*(Conf_n(R^d)) is generated by classes x_{ij}
    of degree d-1, subject to relations generalizing Arnold.

    At n=2, d=2: x_{12} is a degree-1 class.
    Under the identification FM_2(C) = C*, x_{12} = omega_{12}.
    """
    return {
        'n': 2,
        'd': 2,
        'generator_degree': 1,  # d - 1 = 2 - 1 = 1
        'x_{12}': 'omega_{12}',
        'H_star': 'C[x_{12}]/(x_{12}^2) = exterior algebra',
        'match': True,
    }


def totaro_betti_rd(n: int, d: int) -> List[int]:
    """Betti numbers of Conf_n(R^d) for d >= 2.

    For d even: H*(Conf_n(R^d)) is the graded-commutative algebra
    generated by x_{ij} of degree d-1 (odd) with:
      x_{ij}^2 = 0 (exterior)
      Arnold-type relations in degree 2(d-1)

    For d = 2 (complex plane): recovers Arnold algebra.

    The Poincare polynomial for d=2 is prod_{j=0}^{n-1}(1+jt).

    For general d >= 2, the Poincare polynomial involves higher
    Stirling numbers. Here we implement the d=2 case fully and
    provide the formula for general d.
    """
    if d == 2:
        return arnold_betti_numbers(n)

    # For d >= 3 and d odd: generators of even degree d-1
    # The algebra is polynomial (not exterior) truncated by relations
    # For d >= 3 and d even: generators of odd degree d-1
    # Totaro's result: same Poincare polynomial as d=2 but with
    # t replaced by t^{d-1}
    poly_d2 = arnold_poincare_polynomial(n)
    betti = {}
    for deg, val in poly_d2.items():
        betti[deg * (d - 1)] = val

    max_deg = max(betti.keys()) if betti else 0
    return [betti.get(k, 0) for k in range(max_deg + 1)]


