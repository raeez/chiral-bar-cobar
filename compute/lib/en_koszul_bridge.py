def e1_betti_numbers(n: int) -> List[int]:
    """Betti numbers of the E_1 configuration space Conf_n^{ord}(R).

    The ordered configuration space Conf_n^{ord}(R) = {x_1 < ... < x_n}
    is a single open simplex (connected, contractible).

    Hence: b_0 = 1, b_k = 0 for k >= 1.

    Total: sum b_k = 1.
    """
    if n < 1:
        return [1]
    return [1]  # contractible


def e2_betti_numbers(n: int) -> List[int]:
    """Betti numbers of the E_2 configuration space FM_n(C).

    These are the Arnold algebra Betti numbers.
    """
    return arnold_betti_numbers(n)


def e1_e2_homology_comparison(n: int) -> Dict[str, Any]:
    """Compare H*(FM_n^{E_1}(R)) vs H*(FM_n^{E_2}(C)).

    The inclusion R -> C induces a map on configuration spaces:
      Conf_n^{ord}(R) -> Conf_n(C)

    On cohomology, the induced map H*(Conf_n(C)) -> H*(Conf_n^{ord}(R))
    sends ALL positive-degree classes to zero (since Conf_n^{ord}(R)
    is contractible).

    Returns comparison data.
    """
    b_e1 = e1_betti_numbers(n)
    b_e2 = e2_betti_numbers(n)

    return {
        'n': n,
        'e1_betti': b_e1,
        'e2_betti': b_e2,
        'e1_total': sum(b_e1),
        'e2_total': sum(b_e2),
        'e1_contractible': True,
        'e2_euler': arnold_euler_characteristic(n),
        'kernel_dim': sum(b_e2) - 1,  # everything in positive degree maps to 0
        'map_description': 'omega_{ij} -> 0 for all i,j (target contractible)',
    }


def e1_e2_inclusion_on_generators(n: int) -> Dict[Tuple[int, int], int]:
    """The induced map i*: H^1(FM_n(C)) -> H^1(FM_n^{ord}(R)) on generators.

    Since Conf_n^{ord}(R) is contractible, H^1 = 0 and the map is zero
    on all generators.

    Returns dict {(i,j): 0} for all generators.
    """
    return {gen: 0 for gen in arnold_generators(n)}


