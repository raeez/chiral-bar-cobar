# 2. Arnold algebra representation: product structure
# =========================================================================

class ArnoldAlgebra:
    """Computational representation of the Arnold algebra H*(FM_n(C)).

    Elements are represented as linear combinations of monomials
    omega_{i1,j1} * omega_{i2,j2} * ... in the exterior algebra
    (graded commutative with omega^2 = 0), modulo Arnold relations.

    A monomial is stored as a frozenset of pairs (i,j) with i < j.
    The sign is tracked separately.
    """

    def __init__(self, n: int):
        self.n = n
        self._generators = arnold_generators(n)
        self._relations = arnold_relations(n)

    def generators(self) -> List[Tuple[int, int]]:
        return self._generators

    def relations(self) -> List[Tuple[Tuple[int, int], Tuple[int, int], Tuple[int, int]]]:
        return self._relations

    def is_basis_monomial(self, pairs: FrozenSet[Tuple[int, int]]) -> bool:
        """Check if a monomial (given as a set of generator pairs) is
        nonzero in the Arnold algebra.

        A monomial omega_{i1j1} ... omega_{ikjk} is zero if:
        1. Any pair is repeated (exterior algebra)
        2. Any Arnold relation forces it to zero

        For a monomial of degree k, it is nonzero iff the pairs form
        an "admissible" set: no two pairs share an endpoint in a way
        that creates a forbidden pattern.

        In practice, the Orlik-Solomon deletion-restriction theorem
        gives a basis: a subset S of generators is dependent (hence
        the monomial is zero or reducible) iff the matroid of the
        braid arrangement declares it dependent.  For practical small n,
        we use the dimension count to verify.
        """
        # Check no repeated pairs
        pairs_list = list(pairs)
        if len(pairs_list) != len(set(pairs_list)):
            return False
        # Check degree is at most n-1
        if len(pairs_list) > self.n - 1:
            return False
        return True  # Simplified check; full check requires matroid

    def dimension(self, degree: int) -> int:
        """Dimension of degree-k part of H*(FM_n(C)).

        This is the k-th Betti number b_k(FM_n(C)).
        """
        betti = arnold_betti_numbers(self.n)
        if degree < 0 or degree >= len(betti):
            return 0
        return betti[degree]

    def poincare_polynomial(self) -> Dict[int, int]:
        return arnold_poincare_polynomial(self.n)

    def total_dimension(self) -> int:
        return arnold_total_betti(self.n)


