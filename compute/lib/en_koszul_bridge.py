# 4. Specialization map E_2-bar -> E_1-bar (chiral)
# =========================================================================

def specialization_omega_to_dlog(i: int, j: int) -> str:
    """The specialization map sends omega_{ij} to the logarithmic 1-form.

    omega_{ij} -> eta_{ij} = d log(z_i - z_j) = (dz_i - dz_j)/(z_i - z_j)

    This is the holomorphic propagator on the curve.
    """
    return f"d_log(z_{i} - z_{j})"


def verify_specialization_preserves_arnold(i: int, j: int, k: int) -> Dict[str, Any]:
    """Verify that the specialization omega -> d log preserves Arnold relations.

    Under omega_{ij} -> eta_{ij} = d log(z_i - z_j), the Arnold relation
      omega_{ij}*omega_{jk} + omega_{jk}*omega_{ki} + omega_{ki}*omega_{ij} = 0
    maps to
      eta_{ij} ^ eta_{jk} + eta_{jk} ^ eta_{ki} + eta_{ki} ^ eta_{ij} = 0

    which is the classical Arnold relation for logarithmic forms.
    This holds because (z_i-z_j) + (z_j-z_k) + (z_k-z_i) = 0.
    """
    z = [Symbol(f'z_{m}') for m in range(max(i, j, k) + 1)]

    z_ij = z[i] - z[j]
    z_jk = z[j] - z[k]
    z_ki = z[k] - z[i]

    # Cyclic identity
    cyclic_sum = expand(z_ij + z_jk + z_ki)

    # The partial fraction identity:
    # 1/(z_ij * z_jk) + 1/(z_jk * z_ki) + 1/(z_ki * z_ij)
    # = (z_ki + z_ij + z_jk) / (z_ij * z_jk * z_ki)
    # = 0 / (z_ij * z_jk * z_ki) = 0
    #
    # We verify by computing the numerator when brought to common denominator.
    numerator = expand(z_ki + z_ij + z_jk)

    return {
        'triple': (i, j, k),
        'cyclic_identity_holds': cyclic_sum == 0,
        'partial_fraction_numerator': numerator,
        'arnold_preserved': numerator == 0,
        'mechanism': 'cyclic identity z_{ij} + z_{jk} + z_{ki} = 0',
    }


def specialization_matrix_n2() -> Dict[str, Any]:
    """Specialization at n=2: the single generator case.

    FM_2(C) = C* has H^*(FM_2(C)) = C[omega_{12}]/(omega_{12}^2)
    (exterior algebra on one generator).

    omega_{12} -> eta_{12} = d log(z_1 - z_2).

    This is the simplest instance of the Totaro-Arnold dictionary:
    the single degree-1 class on FM_2(C) maps to the propagator.
    """
    return {
        'n': 2,
        'generators': [('omega_{12}', 'eta_{12} = d log(z_1 - z_2)')],
        'H0': 1,
        'H1': 1,
        'poincare': '1 + t',
        'algebra': 'exterior on omega_{12}',
        'specialization': 'omega_{12} -> d log(z_1 - z_2)',
    }


def specialization_matrix_n3() -> Dict[str, Any]:
    """Specialization at n=3.

    FM_3(C) has H* with Betti numbers (1, 3, 2).
    Three generators: omega_{12}, omega_{13}, omega_{23}.
    One Arnold relation: omega_{12}*omega_{23} + omega_{23}*omega_{31} + omega_{31}*omega_{12} = 0.

    In degree 2: the 3 products omega_{ij}*omega_{kl} with {i,j} disjoint {k,l}...
    Actually all three products share endpoints so they are related by Arnold.
    The 3 possible degree-2 monomials are:
      omega_{12}*omega_{13}, omega_{12}*omega_{23}, omega_{13}*omega_{23}
    and the Arnold relation makes one redundant, leaving dim H^2 = 2.
    """
    return {
        'n': 3,
        'generators': [
            ('omega_{12}', 'd log(z_1 - z_2)'),
            ('omega_{13}', 'd log(z_1 - z_3)'),
            ('omega_{23}', 'd log(z_2 - z_3)'),
        ],
        'betti': [1, 3, 2],
        'poincare': '1 + 3t + 2t^2',
        'num_relations': 1,
        'relation': 'omega_{12}*omega_{23} + omega_{23}*omega_{13} + omega_{13}*omega_{12} = 0',
        'arnold_preserved': True,
    }


def specialization_matrix_n4() -> Dict[str, Any]:
    """Specialization at n=4.

    FM_4(C) has Betti numbers (1, 6, 11, 6).
    6 generators: omega_{12}, omega_{13}, omega_{14}, omega_{23}, omega_{24}, omega_{34}.
    4 Arnold relations (one per triple from C(4,3)).
    """
    betti = arnold_betti_numbers(4)
    return {
        'n': 4,
        'num_generators': 6,
        'betti': betti,
        'poincare': '1 + 6t + 11t^2 + 6t^3',
        'num_relations': 4,
        'total_dim': sum(betti),
        'arnold_preserved': True,
    }


