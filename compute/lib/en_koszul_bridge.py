"""E_n Koszul bridge: Arnold algebra, E_1 vs E_2 comparison, specialization maps.

Implements the bridge between E_n bar-cobar duality at different values
of n, and the connection to the chiral bar complex of Vol I.

The key mathematical facts:

1. **Arnold algebra**: H*(FM_n(C)) = H*(Conf_n(C)) is the graded-commutative
   algebra generated by degree-1 classes omega_{ij} (1 <= i < j <= n) subject
   to the Arnold relations:
       omega_{ij} * omega_{jk} + omega_{jk} * omega_{ki} + omega_{ki} * omega_{ij} = 0
   for all distinct i, j, k.  Poincare polynomial:
       P(FM_n(C), t) = prod_{j=0}^{n-1} (1 + jt)

2. **E_1 vs E_2**: The E_1 (associative/ordered) bar complex uses
   Conf_n(R) = {x_1 < ... < x_n}, which is contractible (a simplex).
   The E_2 (holomorphic) bar complex uses Conf_n(C) = FM_n(C), whose
   cohomology is the Arnold algebra.  The inclusion R -> C induces
   FM_n^{E_1}(R) -> FM_n^{E_2}(C), and the induced map on cohomology
   sends omega_{ij} -> 0 for all i, j (since Conf_n(R) is contractible
   for the ordered case).

3. **Specialization map E_2-bar -> E_1-bar**: omega_{ij} -> d log(z_i - z_j).
   This is the holomorphic propagator.  The Arnold relations are preserved
   because they follow from the cyclic identity z_{ij} + z_{jk} + z_{ki} = 0
   (see appendices/arnold_relations.tex).

4. **Totaro-Arnold dictionary**: At n=2, the single generator x_{12} in
   degree 1 corresponds to omega_{12} = d log(z_1 - z_2).  At higher n,
   the Totaro presentation (for FM_n(R^d), d >= 2) generalizes Arnold.

MATHEMATICAL CONVENTIONS:
- Cohomological grading (|d| = +1)
- omega_{ij} = omega_{ji} (antisymmetric by convention: omega_{ij} = -omega_{ji}
  as differential forms, but the CLASSES are equal since eta_{ij} = d log(z_i-z_j)
  and the Arnold algebra uses unordered pairs)
- For computational clarity we work with i < j throughout
- Betti numbers: b_k(FM_n(C)) = |s(n,k)| where s(n,k) are unsigned Stirling
  numbers of the first kind

References:
  - appendices/arnold_relations.tex (Theorem thm:arnold-relations-appendix)
  - chapters/theory/en_koszul_duality.tex (Theorem thm:en-chiral-bridge)
  - chapters/theory/configuration_spaces.tex
  - Arnold (1969): cohomology of braid groups
  - Totaro (1996): configuration spaces of algebraic varieties
  - Kontsevich (1999): formality of the little 2-disks operad
"""

