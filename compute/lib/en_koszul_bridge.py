def arnold_poincare_polynomial(n: int) -> Dict[int, int]:
    """Poincare polynomial P(FM_n(C), t) = prod_{j=0}^{n-1} (1 + jt).

    Returns dict {degree: Betti number}.

    H^k(FM_n(C)) has dimension |s(n, n-k)| where s(n,k) are the
    (signed) Stirling numbers of the first kind, and |s(n,k)| are
    unsigned.

    The Poincare polynomial is:
      P_n(t) = 1 * (1+t) * (1+2t) * ... * (1+(n-1)t)

    Examples:
      n=1: P = 1                    (point)
      n=2: P = 1 + t               (circle)
      n=3: P = 1 + 3t + 2t^2       (complement of braid arrangement)
      n=4: P = 1 + 6t + 11t^2 + 6t^3
      n=5: P = 1 + 10t + 35t^2 + 50t^3 + 24t^4

    Note: P_n(1) = n! (Euler characteristic via inclusion-exclusion).
    """
    if n < 1:
        return {0: 1}

    # Multiply out (1+0t)(1+1t)(1+2t)...(1+(n-1)t) via convolution
    coeffs = {0: 1}
    for j in range(1, n):
        new_coeffs = {}
        for deg, val in coeffs.items():
            # Multiply by (1 + j*t)
            new_coeffs[deg] = new_coeffs.get(deg, 0) + val
            new_coeffs[deg + 1] = new_coeffs.get(deg + 1, 0) + j * val
        coeffs = new_coeffs

    return coeffs


def arnold_betti_numbers(n: int) -> List[int]:
    """Betti numbers b_0, b_1, ..., b_{n-1} of FM_n(C).

    b_k = |s(n, n-k)| = unsigned Stirling number of the first kind.

    The maximum degree is n-1 (top cohomology).
    Total: sum_k b_k = n! = Euler characteristic (with sign).
    """
    poly = arnold_poincare_polynomial(n)
    max_deg = max(poly.keys()) if poly else 0
    return [poly.get(k, 0) for k in range(max_deg + 1)]


def arnold_euler_characteristic(n: int) -> int:
    """Euler characteristic chi(FM_n(C)) = P_n(-1).

    chi = prod_{j=0}^{n-1} (1 - j) = 0 for n >= 3 (since factor (1-1) = 0 at j=1
    contributes... WAIT: that factor is at j=1 giving (1-1)=0, so chi=0 for n>=2.

    Actually: chi(FM_n(C)) = prod_{j=0}^{n-1}(1-j).
    j=0: 1, j=1: 0. So chi = 0 for n >= 2.
    n=1: chi = 1 (FM_1 = point).

    The TOTAL Betti sum is P_n(1) = n!, which is the Poincare polynomial at t=1.
    """
    if n <= 1:
        return 1
    # Product of (1-j) for j=0..n-1: includes factor (1-1)=0 for j=1 when n>=2
    return 0


def arnold_total_betti(n: int) -> int:
    """Total Betti number sum_k b_k = P_n(1) = n!."""
    return factorial(n)


def arnold_generators(n: int) -> List[Tuple[int, int]]:
    """List of Arnold generators omega_{ij} for FM_n(C), i < j.

    There are C(n,2) = n(n-1)/2 generators, each in degree 1.
    """
    return [(i, j) for i in range(1, n + 1) for j in range(i + 1, n + 1)]


def arnold_num_generators(n: int) -> int:
    """Number of Arnold generators: C(n,2) = n(n-1)/2."""
    return n * (n - 1) // 2


def arnold_relations(n: int) -> List[Tuple[Tuple[int, int], Tuple[int, int], Tuple[int, int]]]:
    """Arnold relations for FM_n(C): for each triple {i,j,k},
    omega_{ij} * omega_{jk} + omega_{jk} * omega_{ki} + omega_{ki} * omega_{ij} = 0.

    Returns list of triples ((i,j), (j,k), (k,i)) with i < j < k,
    representing the relation omega_{ij}*omega_{jk} + omega_{jk}*omega_{ki} + omega_{ki}*omega_{ij} = 0.

    Number of relations: C(n,3) = n(n-1)(n-2)/6.
    """
    rels = []
    for i in range(1, n + 1):
        for j in range(i + 1, n + 1):
            for k_idx in range(j + 1, n + 1):
                # Triple (i, j, k) with i < j < k
                # Arnold relation: eta_{ij} eta_{jk} + eta_{jk} eta_{ki} + eta_{ki} eta_{ij} = 0
                # Pairs: (i,j), (j,k), (k,i) -> normalize to i<j ordering
                # (i,j) already i<j
                # (j,k) already j<k
                # (k,i) -> (i,k) since i<k
                rels.append(((i, j), (j, k_idx), (i, k_idx)))
    return rels


def arnold_num_relations(n: int) -> int:
    """Number of Arnold relations: C(n,3) = n(n-1)(n-2)/6."""
    if n < 3:
        return 0
    return n * (n - 1) * (n - 2) // 6


def verify_arnold_relation_algebraic(i: int, j: int, k: int) -> bool:
    """Verify a single Arnold relation via the partial fractions identity.

    The Arnold relation
      eta_{ij} eta_{jk} + eta_{jk} eta_{ki} + eta_{ki} eta_{ij} = 0
    follows from the cyclic identity (z_i - z_j) + (z_j - z_k) + (z_k - z_i) = 0.

    Bringing all three wedge products to common denominator z_{ij}*z_{jk}*z_{ki},
    the numerators are all equal (= dz_{ij} ^ dz_{jk}) and the sum of the
    denominator factors is z_{ki} + z_{ij} + z_{jk} = 0.

    Returns True (always -- the identity is a tautology).
    """
    z = [Symbol(f'z_{m}') for m in range(max(i, j, k) + 1)]

    z_ij = z[i] - z[j]
    z_jk = z[j] - z[k]
    z_ki = z[k] - z[i]

    # The cyclic identity
    cyclic = simplify(z_ij + z_jk + z_ki)

    # The partial fraction sum (denominators when brought to common denominator)
    # Term 1: 1/(z_ij * z_jk) -> z_ki / (z_ij * z_jk * z_ki)
    # Term 2: 1/(z_jk * z_ki) -> z_ij / (z_ij * z_jk * z_ki)
    # Term 3: 1/(z_ki * z_ij) -> z_jk / (z_ij * z_jk * z_ki)
    # Sum of numerators = z_ki + z_ij + z_jk = 0

    return cyclic == 0


