# 6. E_n formality hierarchy
# =========================================================================

def en_formality_status(n: int) -> Dict[str, Any]:
    """Formality status of the E_n operad.

    n = infinity: E_inf is formal (tautologically, it is the commutative operad)
    n = 2: E_2 is formal (Kontsevich 1999, Tamarkin)
    n = 1: E_1 is NOT formal in general (obstructed by A_inf Massey products)
    n >= 2: E_n is formal for all n >= 2 (Kontsevich, Lambrechts-Volic)

    The chiral bar-cobar adjunction on curves inherits E_2 formality
    when the underlying chiral algebra is E_inf-chiral (symmetric OPE).
    For E_1-chiral algebras, the A_inf structure is non-trivial.
    """
    if n == 1:
        return {
            'n': 1,
            'formal': False,
            'obstruction': 'A_inf Massey products',
            'source': 'classical (Stasheff)',
            'relevance': 'E_1-chiral algebras have non-trivial A_inf',
        }
    elif n == 2:
        return {
            'n': 2,
            'formal': True,
            'obstruction': None,
            'source': 'Kontsevich (1999), Tamarkin',
            'relevance': 'Chiral bar-cobar for E_inf-chiral algebras',
        }
    elif n >= 3:
        return {
            'n': n,
            'formal': True,
            'obstruction': None,
            'source': 'Kontsevich, Lambrechts-Volic',
            'relevance': f'E_{n} algebras; relevant for dim-{n} TFT',
        }
    else:
        return {
            'n': n,
            'formal': True,
            'obstruction': None,
            'source': 'E_inf = commutative operad',
            'relevance': 'tautological',
        }


