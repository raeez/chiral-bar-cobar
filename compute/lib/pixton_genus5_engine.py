def cross_genus_lambda_pattern() -> Dict[str, Any]:
    r"""Verify cross-genus patterns for lambda_g^FP.

    1. lambda_{g+1}/lambda_g -> 1/(4pi^2) as g -> inf
    2. The A-hat generating function reproduces all lambda_g
    3. The (2pi)^{-2} decay controls the shadow growth rate
    """
    lambdas = {}
    for g in range(1, 7):
        lambdas[g] = lambda_fp_exact(g)

    # Ratio test
    ratios = {}
    limit = 1.0 / (4.0 * pi ** 2)
    for g in range(1, 6):
        r = lambdas[g + 1] / lambdas[g]
        ratios[g] = {
            'ratio': r,
            'ratio_float': float(r),
            'limit': limit,
            'relative_error': abs(float(r) - limit) / limit,
        }

    # Verify monotone convergence of relative errors
    errors = [ratios[g]['relative_error'] for g in range(1, 6)]
    converging = all(errors[i] > errors[i + 1] for i in range(len(errors) - 1))

    return {
        'lambdas': lambdas,
        'ratios': ratios,
        'errors_converging': converging,
        'limit': limit,
    }


def cross_genus_pf_pattern() -> Dict[str, Any]:
    r"""Compare planted-forest corrections across genera 2-5.

    At each genus g, the PF correction involves shadow coefficients
    S_3,...,S_{2g-1} (by shadow visibility).

    The pattern:
      g=2: S_3 enters (genus-2 PF correction)
      g=3: S_4, S_5 enter
      g=4: S_6, S_7 enter
      g=5: S_8, S_9 enter

    The number of new shadow coefficients at each genus = 2.
    """
    visibility = {}
    for g in range(2, 6):
        new_shadows = []
        for r in range(3, 2 * g):
            g_min = r // 2 + 1
            if g_min == g:
                new_shadows.append(r)
        visibility[g] = {
            'new_shadows': new_shadows,
            'max_visible_r': 2 * g - 1,
            'num_new': len(new_shadows),
        }

    return {
        'visibility_pattern': visibility,
        'two_new_per_genus_ge3': all(
            visibility[g]['num_new'] == 2 for g in range(3, 6)
        ),
        'g2_adds_one': visibility[2]['num_new'] == 1,
    }


