def ell_genus1(val: int, shadow: ShadowData) -> Any:
    r"""MC-determined genus-1 vertex weight.

    ell_1^{(1)} = kappa
    ell_2^{(1)} = S_3*kappa/24 - S_3^2  (exact from MC at (1,2))
    ell_k^{(1)} ~ kappa  (approximate for k >= 3)
    """
    if val == 1:
        return shadow.kappa
    elif val == 2:
        return shadow.S3 * shadow.kappa / 24 - shadow.S3 ** 2
    else:
        return shadow.kappa


def ell_genus2(val: int, shadow: ShadowData) -> Any:
    r"""MC-determined genus-2 vertex weight.

    ell_0^{(2)} = kappa * lambda_2^FP = 7*kappa/5760  (scalar approx)
    ell_k^{(2)} ~ kappa  for k >= 1 (approximate)
    """
    if val == 0:
        return shadow.kappa * Rational(7, 5760)
    else:
        return shadow.kappa


def vertex_weight(graph: StableGraph, shadow: ShadowData) -> Any:
    r"""Product of vertex weights for a stable graph.

    Genus-0, valence k >= 2: S_k (shadow coefficient)
    Genus-1, valence v: ell_v^{(1)} via ell_genus1
    Genus-2, valence v: ell_v^{(2)} via ell_genus2
    Genus >= 3, valence 0: 1 (smooth component)
    Genus >= 3, valence v >= 1: kappa (approximate)
    """
    weight = Integer(1)
    val = graph.valence
    for v in range(graph.num_vertices):
        gv = graph.vertex_genera[v]
        vv = val[v]
        if gv == 0:
            if vv >= 2:
                weight *= shadow.S(vv)
            else:
                weight *= Integer(1)
        elif gv == 1:
            weight *= ell_genus1(vv, shadow)
        elif gv == 2:
            weight *= ell_genus2(vv, shadow)
        else:
            if vv == 0:
                weight *= Integer(1)
            else:
                weight *= shadow.kappa
    return weight


def is_planted_forest(graph: StableGraph) -> bool:
    """Check if graph has at least one genus-0 vertex with valence >= 3."""
    val = graph.valence
    for v in range(graph.num_vertices):
        if graph.vertex_genera[v] == 0 and val[v] >= 3:
            return True
    return False


