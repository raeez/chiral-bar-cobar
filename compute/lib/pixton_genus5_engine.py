def pixton_ideal_dimension_g5() -> Dict[str, Any]:
    r"""Estimate the dimension of the Pixton ideal I_5^d at each codimension.

    I_5^d = dim(free tautological ring in degree d) - dim R^d(M-bar_5)

    The free tautological ring is generated by kappa, lambda, and boundary classes.
    The Pixton ideal trims it to the actual tautological ring.

    At codimension d = 6 (the first nontrivial Pixton relation):
      free generators >= dim(kappa monomials of degree 6) + lambda corrections
      Pixton ideal dimension = free - actual

    The conjecture: class-M shadow relations generate I_5 completely.
    """
    dims_taut = taut_ring_dimensions_g5()

    # Number of kappa monomials = number of partitions p(d)
    # p(0)=1, p(1)=1, p(2)=2, p(3)=3, p(4)=5, p(5)=7, p(6)=11,
    # p(7)=15, p(8)=22, p(9)=30, p(10)=42, p(11)=56, p(12)=77
    kappa_partitions = {
        0: 1, 1: 1, 2: 2, 3: 3, 4: 5, 5: 7, 6: 11,
        7: 15, 8: 22, 9: 30, 10: 42, 11: 56, 12: 77,
    }

    # The boundary classes add considerably more:
    # At genus 5, boundary strata at codimension d are counted by
    # stable graph decompositions with d edges.

    estimates = {}
    for d in range(13):
        kappa_dim = kappa_partitions.get(d, 0)
        taut_dim = dims_taut.get(d, 0)
        estimates[d] = {
            'kappa_partitions': kappa_dim,
            'taut_ring_dim': taut_dim,
            'pixton_ideal_lower_bound': max(0, kappa_dim - taut_dim),
        }

    return {
        'estimates': estimates,
        'first_nontrivial_codim': 6,
        'description': (
            "The Pixton ideal I_5 at genus 5 is nontrivial starting "
            "at codimension 6. Class-M shadow relations should generate it."
        ),
    }


