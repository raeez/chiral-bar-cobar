# Section 18: Depth filtration analysis
# ============================================================================

def depth_filtration_g5() -> Dict[str, Any]:
    r"""Analyze the depth filtration of the PF correction at genus 5.

    The PF correction decomposes by SHADOW DEPTH: the maximum valence
    of genus-0 vertices in each graph.

    At genus 5, the depth filtration is:
      depth 3: graphs where max genus-0 valence = 3 (S_3 only)
      depth 4: max genus-0 valence = 4 (S_4 appears)
      ...
      depth 9: max genus-0 valence = 9 (S_9 appears)
    """
    pf_amps = genus5_pf_amplitudes()

    by_depth = {}
    for amp in pf_amps:
        if amp.hodge_integral == Fraction(0):
            continue
        max_g0_val = 0
        for v in range(amp.graph.num_vertices):
            if amp.graph.vertex_genera[v] == 0:
                max_g0_val = max(max_g0_val, amp.graph.valence[v])
        if max_g0_val >= 3:
            by_depth.setdefault(max_g0_val, []).append(amp)

    summary = {}
    for depth, amps in sorted(by_depth.items()):
        summary[depth] = {
            'count': len(amps),
            'shadow_coeff': f'S_{depth}',
        }

    return {
        'by_depth': summary,
        'max_depth': max(by_depth.keys()) if by_depth else 0,
        'num_depths': len(by_depth),
    }


