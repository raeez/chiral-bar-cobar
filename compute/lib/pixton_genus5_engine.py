def genus5_pf_polynomial() -> Dict[str, Any]:
    r"""Extract delta_pf^{(5,0)} as a polynomial in shadow variables.

    Variables: kappa, S_3, S_4, S_5, S_6, S_7, S_8, S_9.
    """
    kappa_sym = Symbol('kappa')
    S3 = Symbol('S_3')
    S4 = Symbol('S_4')
    S5 = Symbol('S_5')
    S6 = Symbol('S_6')
    S7 = Symbol('S_7')
    S8 = Symbol('S_8')
    S9 = Symbol('S_9')

    shadow = ShadowData(
        'genus5_symbolic', kappa_sym, S3, S4,
        shadows={5: S5, 6: S6, 7: S7, 8: S8, 9: S9},
        depth_class='M',
    )

    pf_amps = genus5_pf_amplitudes()
    total = Integer(0)
    nonzero_count = 0

    for amp in pf_amps:
        contrib = amp.weighted_amplitude(shadow)
        if simplify(contrib) != 0:
            nonzero_count += 1
        total += contrib

    poly = cancel(expand(total))

    # Extract monomial structure
    monomials = _extract_monomials_g5(
        poly, kappa_sym, S3, S4, S5, S6, S7, S8, S9)

    return {
        'polynomial': poly,
        'monomials': monomials,
        'num_terms': sum(1 for v in monomials.values() if v != 0),
        'num_pf_graphs': len(pf_amps),
        'num_nonzero_pf_graphs': nonzero_count,
    }


def _extract_monomials_g5(
    expr: Any,
    kappa: Symbol, S3: Symbol, S4: Symbol,
    S5: Symbol, S6: Symbol, S7: Symbol,
    S8: Symbol, S9: Symbol,
) -> Dict[str, Any]:
    """Extract monomial coefficients from a polynomial in 8 variables."""
    expr_expanded = expand(expr)
    result = {}
    try:
        p = Poly(expr_expanded, kappa, S3, S4, S5, S6, S7, S8, S9, domain='QQ')
        for monom, coeff in p.as_dict().items():
            a, b, c, d, e, f, g, h = monom
            key = _monomial_key_g5(a, b, c, d, e, f, g, h)
            result[key] = Rational(coeff)
    except Exception:
        result['full_expression'] = expr_expanded
    return result


def _monomial_key_g5(a, b, c, d, e, f, g, h) -> str:
    """Human-readable key for a monomial kappa^a * S_3^b * ... * S_9^h."""
    names = [('kappa', a), ('S_3', b), ('S_4', c), ('S_5', d),
             ('S_6', e), ('S_7', f), ('S_8', g), ('S_9', h)]
    parts = []
    for name, exp in names:
        if exp > 0:
            parts.append(f"{name}^{exp}" if exp > 1 else name)
    return " * ".join(parts) if parts else "1"


