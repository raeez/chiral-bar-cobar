def verify_sym2_lambda2_from_casimir() -> Dict[str, object]:
    """Verify Sym^2/Lambda^2 splitting via numerical Casimir projector.

    Construct the 64x64 Casimir tensor Omega on adj tensor adj,
    the 64x64 permutation P, and the symmetrizer S = (I+P)/2.
    Project Omega onto Sym^2 and Lambda^2 subspaces and read off
    the eigenvalue multiplicities.

    This is Path 2: Casimir eigenvalue separation.
    """
    from compute.lib.theorem_sl3_yangian_r_matrix_engine import (
        casimir_tensor_adj,
        adjoint_rep_matrices,
    )

    Omega = casimir_tensor_adj()
    d = 8  # dim(adj)

    # Build permutation operator P on C^8 tensor C^8
    P = np.zeros((d * d, d * d))
    for i in range(d):
        for j in range(d):
            P[i * d + j, j * d + i] = 1.0

    # Symmetrizer and antisymmetrizer
    I64 = np.eye(d * d)
    S = (I64 + P) / 2.0   # projects onto Sym^2
    A = (I64 - P) / 2.0   # projects onto Lambda^2

    # Project Omega onto symmetric and antisymmetric subspaces
    Omega_sym = S @ Omega @ S
    Omega_anti = A @ Omega @ A

    # Eigenvalues of Omega restricted to Sym^2
    # (S has rank 36, A has rank 28)
    evals_sym_raw = np.linalg.eigvals(Omega_sym).real
    evals_anti_raw = np.linalg.eigvals(Omega_anti).real

    # Filter out zero eigenvalues from the kernel of the projector
    tol = 0.1
    # Sym^2 eigenvalues (non-trivial: from rank-36 subspace)
    evals_sym = sorted([v for v in evals_sym_raw if abs(v) > tol or
                        sum(1 for x in evals_sym_raw if abs(x) < tol) <= 28],
                       reverse=True)
    evals_anti = sorted([v for v in evals_anti_raw if abs(v) > tol or
                         sum(1 for x in evals_anti_raw if abs(x) < tol) <= 36],
                        reverse=True)

    # Better approach: diagonalize Omega within each subspace directly.
    # Use the fact that S is idempotent: restrict to image(S).
    # Find an orthonormal basis for image(S).

    U_sym, s_sym, _ = np.linalg.svd(S)
    rank_sym = int(np.sum(s_sym > 0.5))  # should be 36
    basis_sym = U_sym[:, :rank_sym]
    Omega_sym_restricted = basis_sym.T @ Omega @ basis_sym
    evals_sym_clean = sorted(np.linalg.eigvals(Omega_sym_restricted).real,
                             reverse=True)

    U_anti, s_anti, _ = np.linalg.svd(A)
    rank_anti = int(np.sum(s_anti > 0.5))  # should be 28
    basis_anti = U_anti[:, :rank_anti]
    Omega_anti_restricted = basis_anti.T @ Omega @ basis_anti
    evals_anti_clean = sorted(np.linalg.eigvals(Omega_anti_restricted).real,
                              reverse=True)

    # Cluster eigenvalues
    def cluster(vals: List[float], tol: float = 0.3) -> Dict[float, int]:
        clusters: Dict[float, int] = {}
        for v in vals:
            found = False
            for k in list(clusters.keys()):
                if abs(v - k) < tol:
                    clusters[k] += 1
                    found = True
                    break
            if not found:
                clusters[round(v, 1)] = 1
        return clusters

    sym_clusters = cluster(evals_sym_clean)
    anti_clusters = cluster(evals_anti_clean)

    # Expected Casimir eigenvalues on irreps inside adj tensor adj:
    # Omega eigenvalue on W inside V tensor V = (C_2(W) - 2*C_2(V)) / 2
    # C_2(adj) = C_2(1,1) = 6.
    # (2,2): C_2 = (2*4+2*4+2*4+12+12)/3 = 48/3 = 16 -> omega = (16-12)/2 = 2
    # (3,0): C_2 = (2*9+0+0+18+0)/3 = 36/3 = 12 -> omega = (12-12)/2 = 0
    # (0,3): C_2 = (0+0+2*9+0+18)/3 = 36/3 = 12 -> omega = 0
    # (1,1): C_2 = 6 -> omega = (6-12)/2 = -3
    # (0,0): C_2 = 0 -> omega = (0-12)/2 = -6

    expected_sym = {2.0: 27, 0.0: 8, -6.0: 1}   # eigenvalues on Sym^2 irreps
    expected_anti = {0.0: 20, -3.0: 8}            # wait, need to recheck

    # Actually the Casimir tensor Omega = sum g^{ab} rho(T_a) x rho(T_b)
    # acts on V x V with the eigenvalue formula:
    #   Omega|_{W} = (C_2(W) - C_2(V_1) - C_2(V_2)) / 2 * Id_W
    # For V_1 = V_2 = adj, C_2(V_1) + C_2(V_2) = 12.
    # So omega(W) = (C_2(W) - 12) / 2.

    # Recompute:
    # (2,2): C_2 = 16 -> omega = 2
    # (3,0): C_2 = 12 -> omega = 0
    # (0,3): C_2 = 12 -> omega = 0
    # (1,1): C_2 = 6 -> omega = -3
    # (0,0): C_2 = 0 -> omega = -6

    # In adj x adj = (2,2) + (3,0) + (0,3) + 2*(1,1) + (0,0):
    #   omega = 2: mult 27 (from (2,2))
    #   omega = 0: mult 10+10 = 20 (from (3,0) + (0,3))
    #   omega = -3: mult 8+8 = 16 (from 2*(1,1))
    #   omega = -6: mult 1 (from (0,0))

    # In Sym^2(adj) = (2,2) + (1,1) + (0,0):
    #   omega = 2: mult 27
    #   omega = -3: mult 8
    #   omega = -6: mult 1

    # In Lambda^2(adj) = (3,0) + (0,3) + (1,1):
    #   omega = 0: mult 10+10 = 20
    #   omega = -3: mult 8

    expected_sym_corrected = {2.0: 27, -3.0: 8, -6.0: 1}
    expected_anti_corrected = {0.0: 20, -3.0: 8}

    return {
        "rank_sym": rank_sym,
        "rank_anti": rank_anti,
        "sym_eigenvalue_clusters": sym_clusters,
        "anti_eigenvalue_clusters": anti_clusters,
        "expected_sym": expected_sym_corrected,
        "expected_anti": expected_anti_corrected,
        "sym_dims_match": rank_sym == 36,
        "anti_dims_match": rank_anti == 28,
    }


