def _weight_multiplicities(p: int, q: int) -> Dict[Tuple[int, int], int]:
    """Weight multiplicities of V_{(p,q)} via Freudenthal recursion.

    Returns {(m1, m2): multiplicity} in the omega-basis.
    """
    if p < 0 or q < 0:
        return {}

    def inner(x: Tuple[float, float], y: Tuple[float, float]) -> float:
        return (2 * x[0] * y[0] + x[0] * y[1]
                + x[1] * y[0] + 2 * x[1] * y[1]) / 3.0

    rho = (1, 1)
    lam = (p, q)

    def rho_shifted_norm(mu: Tuple[int, int]) -> float:
        s = (mu[0] + rho[0], mu[1] + rho[1])
        return inner(s, s)

    lam_rho_norm = rho_shifted_norm(lam)
    pos_roots = [(2, -1), (-1, 2), (1, 1)]
    simple_roots = [(2, -1), (-1, 2)]
    target_dim = sl3_dim(p, q)

    weights: Dict[Tuple[int, int], int] = {lam: 1}
    queue = [lam]
    visited = {lam}
    current_dim = 1

    while queue and current_dim < target_dim:
        mu = queue.pop(0)
        for alpha in simple_roots:
            nu = (mu[0] - alpha[0], mu[1] - alpha[1])
            if nu in visited:
                continue
            denom = lam_rho_norm - rho_shifted_norm(nu)
            if abs(denom) < 1e-10:
                continue

            numer = 0.0
            for alpha_p in pos_roots:
                k_val = 1
                while True:
                    shifted = (nu[0] + k_val * alpha_p[0],
                               nu[1] + k_val * alpha_p[1])
                    if shifted not in weights:
                        break
                    numer += 2.0 * inner(shifted, alpha_p) * weights[shifted]
                    k_val += 1

            mult = numer / denom
            mult_int = int(round(mult))
            if mult_int > 0:
                weights[nu] = mult_int
                current_dim += mult_int
                visited.add(nu)
                queue.append(nu)

    return weights


def tensor_product_decomposition(
    a1: int, a2: int, b1: int, b2: int
) -> Dict[Tuple[int, int], int]:
    """Decompose V_{(a1,a2)} tensor V_{(b1,b2)} into sl_3 irreps.

    Returns {(c1, c2): multiplicity}.
    Uses character multiplication + highest-weight peeling.
    """
    char_a = _weight_multiplicities(a1, a2)
    char_b = _weight_multiplicities(b1, b2)

    product: Dict[Tuple[int, int], int] = {}
    for (m1, m2), ma in char_a.items():
        for (n1, n2), mb in char_b.items():
            key = (m1 + n1, m2 + n2)
            product[key] = product.get(key, 0) + ma * mb

    decomposition: Dict[Tuple[int, int], int] = {}
    remaining = dict(product)
    for _ in range(300):
        if not remaining:
            break
        dominant = [(wt, m) for wt, m in remaining.items()
                    if wt[0] >= 0 and wt[1] >= 0 and m > 0]
        if not dominant:
            break
        dominant.sort(key=lambda x: (x[0][0] + x[0][1], x[0][0]),
                      reverse=True)
        hw = dominant[0][0]
        decomposition[hw] = decomposition.get(hw, 0) + 1
        ch = _weight_multiplicities(hw[0], hw[1])
        for wt, mult in ch.items():
            if wt in remaining:
                remaining[wt] -= mult
                if remaining[wt] == 0:
                    del remaining[wt]

    return decomposition


