def _generate_test_insertions(genus: int, n_vir: int,
                               max_n: int = 4) -> List[Tuple[int, ...]]:
    r"""Generate valid test insertion tuples for L_{n_vir} at genus g.

    For L_{n_vir}, we need insertions S = (d_1, ..., d_n) such that:
    - After inserting tau_{n_vir+1}, the dimension constraint is satisfied:
      (n_vir + 1) + sum(S) = 3g - 3 + n + 1
    - Stability: 2g - 2 + n + 1 > 0

    Returns list of valid tuples.
    """
    d_extra = max(n_vir + 1, 0)  # degree of the inserted tau

    results = []

    for n in range(0, max_n + 1):
        # After insertion: n+1 points total
        # dim constraint: d_extra + sum(S) = 3g - 3 + (n+1)
        target_sum = 3 * genus - 3 + n + 1 - d_extra
        if target_sum < 0:
            continue
        if 2 * genus - 2 + n + 1 <= 0:
            continue
        # Also need 2g - 2 + n > 0 for the INNER correlator to be stable
        # (not always required, but needed for nontrivial check)

        # Generate sorted tuples of n non-negative integers summing to target_sum
        for ins in _partitions_nonneg(target_sum, n):
            results.append(ins)

    return results


def _partitions_nonneg(total: int, n: int,
                        min_val: int = 0) -> List[Tuple[int, ...]]:
    """Sorted tuples of n non-negative integers summing to total."""
    if n == 0:
        return [()] if total == 0 else []
    if n == 1:
        return [(total,)] if total >= min_val else []
    out = []
    for first in range(min_val, total + 1):
        for rest in _partitions_nonneg(total - first, n - 1, first):
            out.append((first,) + rest)
    return out


