    # and int psi_1^0 pi^* lambda_g = lambda_g^FP (when 2g-2 = 0, i.e., g=1 it's 1/24)
    # Actually: int_{M-bar_{g,1}} pi^* lambda_g is NOT lambda_g^FP.
    # pi^* lambda_g has dimension g in a space of dimension 3g-2,
    # so we need 2g-2 more psi classes to pair.

    # The correct dilaton equation for free energies:
    # sum_n (2g-2+n) F_{g,n} with appropriate weighting.

    # Let's verify via WK directly: F_g = kappa * lambda_g^FP.
