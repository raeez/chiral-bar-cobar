@lru_cache(maxsize=64)
def lambda_fp(g: int) -> Fraction:
    r"""Faber-Pandharipande intersection number lambda_g^FP.

    lambda_g^FP = (2^{2g-1} - 1) / 2^{2g-1} * |B_{2g}| / (2g)!

    This is int_{M-bar_{g,1}} psi_1^{2g-2} lambda_g (FP identity).

    Verified values:
        g=1: 1/24
        g=2: 7/5760
        g=3: 31/967680
        g=4: 127/154828800
        g=5: 73/3503554560
    """
    if g < 1:
        raise ValueError(f"Genus must be >= 1, got {g}")
    power = 2 ** (2 * g - 1)
    B_2g = bernoulli(2 * g)
    abs_B_2g = Rational(abs(int(B_2g.p)), int(B_2g.q))
    result = Rational(power - 1, power) * abs_B_2g / factorial(2 * g)
    return Fraction(int(result.p), int(result.q))


@lru_cache(maxsize=64)
def lambda_g_integral(g: int) -> Fraction:
    r"""Bernoulli Hodge coefficient |B_{2g}| / (2g * (2g)!).

    This is the DENOMINATOR in the Hodge-psi identity:

        lambda_g^FP = (2^{2g-1} - 1) * 2g / 2^{2g-1}  *  |B_{2g}| / (2g * (2g)!)

    So: lambda_g^FP / lambda_g_integral = (2^{2g-1} - 1) * 2g / 2^{2g-1}.

    Computed values:
        g=1: |B_2|/(2*2!) = (1/6)/4 = 1/24
        g=2: |B_4|/(4*24) = (1/30)/96 = 1/2880
        g=3: |B_6|/(6*720) = (1/42)/4320 = 1/181440

    NOTE: This is NOT the integral of lambda_g over M-bar_g (which vanishes
    for g >= 2 by dimensional reasons: deg(lambda_g) = g < dim(M-bar_g) = 3g-3).
    Rather, it is the Bernoulli factor that appears as the natural base
    in the Hodge-psi identity.  The Faber integrals
    int_{M-bar_2} lambda_2 lambda_1 = 1/240 and
    int_{M-bar_3} lambda_3 lambda_2 lambda_1 = 1/6048
    involve PRODUCTS of Hodge classes and are computed elsewhere.
    """
    if g < 1:
        raise ValueError(f"Genus must be >= 1, got {g}")
    B_2g_val = bernoulli(2 * g)
    abs_B = Rational(abs(int(B_2g_val.p)), int(B_2g_val.q))
    result = abs_B / (2 * g * factorial(2 * g))
    return Fraction(int(result.p), int(result.q))


