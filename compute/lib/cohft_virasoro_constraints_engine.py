def hodge_psi_identity_check(max_g: int = 4) -> Dict[str, Any]:
    r"""Verify the Hodge-psi identity (Faber-Pandharipande formula).

    The FP identity:
        lambda_g^{FP} = int_{M-bar_{g,1}} psi_1^{2g-2} lambda_g

    This DEFINES lambda_g^{FP} as a mixed Hodge-psi integral.

    The pure lambda integral is DIFFERENT:
        int_{M-bar_g} lambda_g = |B_{2g}| / (2g * (2g)!)   (Faber)

    The RATIO:
        lambda_g^{FP} / int lambda_g = (2^{2g-1} - 1) * 2g / 2^{2g-1}

    This ratio grows as 2g (linearly), reflecting the psi insertion.

    The Mumford formula connects them via:
        ch(E) = g + sum_{k>=1} B_{2k}/(2k)! * kappa_{2k-1}

    where E is the Hodge bundle. The Chern character gives:
        lambda_g = c_g(E) = polynomial in ch_1, ..., ch_g via Newton's identities.

    For the shadow CohFT: the obstruction class is obs_g = kappa * lambda_g
    (in H^{2g}(M-bar_g)), and the free energy is its integral against the
    appropriate psi-class monomials from the Givental reconstruction.
    """
    results = {
        'max_genus': max_g,
        'checks': {},
        'all_pass': True,
    }

    for g in range(1, max_g + 1):
        fp = lambda_fp(g)
        pure = lambda_g_integral(g)
        ratio = fp / pure if pure != Fraction(0) else None

        # Expected ratio: (2^{2g-1} - 1) * 2g / 2^{2g-1}
        power = 2 ** (2 * g - 1)
        expected_ratio = Fraction((power - 1) * 2 * g, power)

        match = (ratio == expected_ratio) if ratio is not None else False

        results['checks'][g] = {
            'lambda_fp': fp,
            'lambda_integral': pure,
            'ratio': ratio,
            'expected_ratio': expected_ratio,
            'ratio_matches': match,
        }
        if not match:
            results['all_pass'] = False

    return results


