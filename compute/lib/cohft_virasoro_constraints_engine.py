# Section 12: Virasoro operators on the Heisenberg shadow CohFT
# =========================================================================

def heisenberg_virasoro_operators(kappa_val: Fraction,
                                   max_n: int = 5) -> Dict[str, Any]:
    r"""Virasoro operators L_n for the Heisenberg shadow CohFT.

    For the Heisenberg algebra H_k with kappa = k, the shadow CohFT is
    rank-1 with R = 1 (class G, trivially S_3 = 0).

    The Virasoro operators (eq:virasoro-operators-heisenberg):
        L_n = -(n+1)! d/dt_n + sum_k (2k+2n+1)!!/(2k-1)!! t_k d/dt_{k+n+1}
              + (kappa/2) delta_{n,0}

    These are the SAME Virasoro operators as in the Witten-Kontsevich theorem,
    rescaled by kappa. The constraint L_n(Z^sh) = 0 is equivalent to
    L_n^{WK}(Z^{WK}) = 0 after substituting Z^sh = kappa * Z^{WK}.

    For Heisenberg (S_3 = 0): the CohFT is trivial at genus 0 (no
    cubic vertex). All amplitudes come from the genus-1 seed kappa/24.
    The Virasoro constraints are trivially satisfied since the only
    nonzero correlators are <tau_1>_1 = kappa/24 and its descendants.
    """
    results = {
        'kappa': kappa_val,
        'family': 'Heisenberg',
        'shadow_class': 'G',
        'R_matrix': 'trivial (R = Id)',
        'operators': {},
    }

    # The Virasoro operator coefficients
    for n in range(-1, max_n + 1):
        op = {
            'index': n,
            'derivative_coeff': -int(factorial(n + 1)) if n >= 0 else 1,
        }

        # Coefficient (2k+2n+1)!!/(2k-1)!! for each k
        merge_coeffs = {}
        for kk in range(0, 8):
            num = _double_factorial_odd(kk + n)
            den = _double_factorial_odd(kk - 1) if kk >= 1 else 1
            if den != 0:
                merge_coeffs[kk] = Fraction(num, den)
        op['merge_coefficients'] = merge_coeffs

        if n == 0:
            op['constant_term'] = kappa_val / Fraction(2)
        else:
            op['constant_term'] = Fraction(0)

        results['operators'][n] = op

    return results


