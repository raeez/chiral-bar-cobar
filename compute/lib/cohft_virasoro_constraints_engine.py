# Section 11: Shadow CohFT string equation at specific genera
# =========================================================================

def string_equation_genus_explicit(g: int, kappa_val: Fraction) -> Dict[str, Any]:
    r"""Explicit string equation verification at genus g for the shadow CohFT.

    At genus g, the string equation says:
        <tau_0 tau_{d_1} ... tau_{d_n}>_g = sum_{i: d_i>0} <... tau_{d_i-1} ...>_g

    For the shadow free energy (n=0): the string equation constrains the
    descendant potential, not the free energies directly. At (g,0) there are
    no marked points, so there is no tau_0 to insert.

    At (g,1): we can verify
        <tau_0 tau_d>_g = 0  when d = 3g-2 (dimension counting)
    since inserting tau_0 into a (g,1) correlator requires stability.

    At (g,2): meaningful checks exist.
        <tau_0 tau_{d_1} tau_{d_2}>_g where d_1+d_2+0 = 3g-3+3 = 3g.
        So d_1+d_2 = 3g, and the string equation gives:
        <tau_0 tau_{d_1} tau_{d_2}>_g = <tau_{d_1-1} tau_{d_2}>_g + <tau_{d_1} tau_{d_2-1}>_g

    Returns explicit checks at the given genus.
    """
    results = {
        'genus': g,
        'kappa': kappa_val,
        'checks': {},
        'all_pass': True,
    }

    # Generate all valid (g,2) correlators and check string equation
    target = 3 * g  # d_1 + d_2 = 3g for the correlator with tau_0 inserted
    for d1 in range(0, target + 1):
        d2 = target - d1
        if d1 > d2:
            continue  # avoid duplicates
        if d1 == 0:
            # <tau_0 tau_0 tau_{3g}>_g -- string eqn: sum
            # RHS: <tau_0 tau_{3g-1}>_g (only d2 can decrease if > 0)
            #   + <tau_0 tau_{3g}>_g if d1=0 decreases... but d1=0 can't decrease.
            # Wait: string equation removes the tau_0 and decreases each remaining d_i.
            # <tau_0 tau_0 tau_{3g}>_g = <tau_{-1} tau_{3g}>_g + <tau_0 tau_{3g-1}>_g
            # = 0 + <tau_0 tau_{3g-1}>_g   (tau_{-1} = 0)
            pass

        lhs = wk_intersection(g, (0, d1, d2))
        rhs = Fraction(0)
        for idx, d_val in enumerate([d1, d2]):
            if d_val > 0:
                new = [d1, d2]
                new[idx] -= 1
                rhs += wk_intersection(g, tuple(sorted(new)))

        match = (lhs == rhs)
        key = f'tau_0_tau_{d1}_tau_{d2}'
        results['checks'][key] = {
            'lhs': lhs,
            'rhs': rhs,
            'defect': lhs - rhs,
            'passes': match,
        }
        if not match:
            results['all_pass'] = False

    return results


