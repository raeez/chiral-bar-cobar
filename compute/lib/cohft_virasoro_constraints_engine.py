def shadow_free_energy(g: int, kappa_val: Fraction) -> Fraction:
    r"""Shadow free energy at genus g.

    F_g^{sh}(A) = kappa(A) * lambda_g^{FP}

    where lambda_g^{FP} = (2^{2g-1}-1)|B_{2g}|/(2^{2g-1}(2g)!).
    """
    if g < 1:
        raise ValueError(f"Genus must be >= 1, got {g}")
    return kappa_val * lambda_fp(g)


def shadow_partition_function_coefficients(
    kappa_val: Fraction, max_g: int = 6
) -> Dict[int, Fraction]:
    r"""Coefficients of the shadow partition function Z^sh = sum_g F_g hbar^{2g}.

    Returns {g: F_g} for 1 <= g <= max_g.
    """
    return {g: shadow_free_energy(g, kappa_val) for g in range(1, max_g + 1)}


def shadow_partition_function_closed_form_check(
    kappa_val: Fraction, max_g: int = 6
) -> Dict[str, Any]:
    r"""Verify that F_g matches the A-hat genus generating function.

    Z^sh = kappa * (hbar/2 / sin(hbar/2) - 1) = sum_{g>=1} kappa * lambda_g^FP * hbar^{2g}

    The generating function identity:
        hbar/2 / sin(hbar/2) = sum_{g>=0} (2^{2g-1}-1)|B_{2g}|/(2^{2g-1}) * hbar^{2g}/(2g)!
                              + 1  (the g=0 term gives 1)

    So hbar/2/sin(hbar/2) - 1 = sum_{g>=1} lambda_g^{FP} * hbar^{2g}.

    We verify by expanding sin(x) = sum (-1)^k x^{2k+1}/(2k+1)! and computing
    the ratio as a formal power series.
    """
    # Method 1: Direct FP numbers
    direct = {g: lambda_fp(g) for g in range(1, max_g + 1)}

    # Method 2: Power series expansion of x/sin(x) - 1 where x = hbar/2
    # x/sin(x) = sum_{g>=0} (2^{2g} - 2) |B_{2g}| / (2g)! * x^{2g}  (NO, recheck)
    # Actually: x/sin(x) = sum_{g>=0} (-1)^g (2 - 2^{2g}) B_{2g} / (2g)! * x^{2g}
