    #   hbar/2 / sin(hbar/2) = sum_{g>=0} (-1)^g (2 - 2^{2g}) B_{2g} / (2g)! * (hbar/2)^{2g}

    # Let's compute from the Bernoulli expansion directly.
    # x/sin(x) = sum_{n>=0} (-1)^{n+1} (2-2^{2n}) B_{2n} x^{2n} / (2n)!
    # Reference: DLMF 4.19.5, corrected sign convention.
    #
    # Actually: x/sin(x) = sum_{n>=0} (-1)^{n+1} 2(1 - 2^{2n-1}) B_{2n} x^{2n} / (2n)!
    #
    # Let me just compute by inverting the series.
    # sin(x)/x = 1 - x^2/6 + x^4/120 - x^6/5040 + ...
    # We want x/sin(x) = 1/(1 - x^2/6 + x^4/120 - ...)
    # = 1 + x^2/6 + 7x^4/360 + 31x^6/15120 + ...

    # Compute via series inversion
    N = max_g + 1
    # sin(x)/x = sum_{k>=0} (-1)^k x^{2k} / (2k+1)!
    sin_over_x = [Fraction(0)] * (2 * N + 1)
