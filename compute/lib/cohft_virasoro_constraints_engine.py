    # <tau_1>_g = (2g-2) <empty>_g
    # where <tau_1>_g = int_{M-bar_{g,1}} psi_1 * Omega_{g,1}
    # and <empty>_g = int_{M-bar_g} Omega_{g,0} = F_g

