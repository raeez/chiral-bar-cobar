"""Yangian bar complex and E_1-chiral structure.

The Yangian Y(g) is an E_1-chiral algebra (noncommutative, nonlocal vertex algebra).
It is the Koszul dual to the quantum group U_q(g) in the E_1 framework.

Ground truth from the manuscript (yangians.tex):
  - Y(g) is generated by J^a_n for a in g, n >= 0
  - RTT presentation: R_{12}(u-v) T_1(u) T_2(v) = T_2(v) T_1(u) R_{12}(u-v)
  - E_1 Yangian bar complex: B_{E_1}(Y(g)) encodes the RTT relations

  From koszul_pair_structure.tex:
  - E_1 duality theorem (thm:e1-duality-main): Y(g) and U_q(g) are E_1-Koszul dual
  - Koszulness of Y(sl_2): dim B^n_h grows polynomially (conjectured)

  From yangians.tex:
  - Y(sl_2) generators: J^a_n for a in {e, h, f}, n >= 0
  - Y(sl_3) generators: J^a_n for a in {e1,e2,e12,h1,h2,f1,f2,f12}, n >= 0
  - Coulomb branch: Y(g) = quantized Coulomb branch of 3d N=4

CONVENTIONS:
- E_1-chiral framework (not commutative chiral)
- Spectral parameter u for RTT
"""

from __future__ import annotations

from typing import Dict, List

from sympy import Rational, Symbol


# ---------------------------------------------------------------------------
# Yangian data
# ---------------------------------------------------------------------------

YANGIAN_DATA = {
    "sl2": {
        "g_dim": 3,
        "g_rank": 2,
        "basis": ["e", "h", "f"],
        "generators_per_level": 3,
        "e1_koszul_dual": "U_q(sl_2)",
    },
    "sl3": {
        "g_dim": 8,
        "g_rank": 2,
        "basis": ["e1", "e2", "e12", "h1", "h2", "f1", "f2", "f12"],
        "generators_per_level": 8,
        "e1_koszul_dual": "U_q(sl_3)",
    },
}


def yangian_generator_count(g: str, max_level: int) -> int:
    """Number of Yangian generators through level n.

    Y(g) has dim(g) generators at each level n >= 0.
    Total through level N: (N+1) * dim(g).
    """
    data = YANGIAN_DATA[g]
    return (max_level + 1) * data["g_dim"]


def yangian_rtt_relation_count(g: str) -> int:
    """Number of independent RTT relations.

    The RTT equation R T_1 T_2 = T_2 T_1 R has dim(g)^2 matrix entries,
    each giving a relation. After symmetry, there are dim(g)^2 relations
    (some redundant).
    """
    data = YANGIAN_DATA[g]
    return data["g_dim"] ** 2


# ---------------------------------------------------------------------------
# E_1 bar complex structure
# ---------------------------------------------------------------------------

def e1_bar_structure(g: str) -> Dict[str, object]:
    """E_1 bar complex structure for Y(g).

    The E_1 bar complex differs from the chiral bar complex:
    - Uses E_1 operad (little 1-discs) instead of chiral
    - Configuration space: Conf_n(R) (not Conf_n(C))
    - Orlik-Solomon replaced by: H*(Conf_n(R)) (simpler)

    Key difference: E_1 forms have dim H^0(Conf_n(R)) = n! (all orderings).
    """
    data = YANGIAN_DATA[g]
    return {
        "operad": "E_1 (little 1-discs)",
        "config_space": "Conf_n(R)",
        "generators_per_level": data["g_dim"],
        "koszul_dual": data["e1_koszul_dual"],
        "framework": "E_1-chiral (noncommutative)",
    }


def e1_bar_deg2_dim(g: str) -> int:
    """Dimension of E_1 bar complex in degree 2 (level 0 generators only).

    Level-0 generators: J^a_0 for a in basis(g).
    B^2 = g^{otimes 2} otimes H^1(Conf_2(R)).
    H^1(Conf_2(R)) = 0 (Conf_2(R) is contractible minus diagonal = R^2 \\ R).
    Actually Conf_2(R) has two components (z_1 < z_2 and z_1 > z_2), so
    H^0(Conf_2(R)) = Z^2. The form space is 1-dimensional per ordering.

    For E_1: the relevant cochains use the ordered config space.
    B^2_{E_1} = g^{otimes 2} (one ordering contributes).
    """
    data = YANGIAN_DATA[g]
    return data["g_dim"] ** 2


# ---------------------------------------------------------------------------
# Koszulness (conjectural)
# ---------------------------------------------------------------------------

def yangian_koszulness_status(g: str) -> Dict[str, object]:
    """Status of Koszulness conjecture for Y(g).

    Ground truth: yangians.tex.
    Y(sl_2) Koszulness: conjectured, polynomial growth evidence.
    Y(sl_3) Koszulness: conjectured, less evidence.
    """
    return {
        "sl2": {
            "status": "conjectured",
            "evidence": "polynomial growth of bar cohomology",
            "reference": "yangians.tex",
        },
        "sl3": {
            "status": "conjectured",
            "evidence": "expected from Coulomb branch geometry",
            "reference": "yangians.tex",
        },
    }.get(g, {"status": "unknown"})


# ---------------------------------------------------------------------------
# Coulomb branch connection
# ---------------------------------------------------------------------------

def coulomb_branch_data(g: str) -> Dict[str, str]:
    """Coulomb branch interpretation of Y(g).

    Y(g) = quantized Coulomb branch of 3d N=4 gauge theory.
    """
    return {
        "algebra": f"Y({g})",
        "geometry": f"Coulomb branch of 3d N=4 with gauge group G({g})",
        "quantization": "Y(g) is a deformation quantization of the Coulomb branch",
        "koszul_dual": f"U_q({g}) = quantized Higgs branch (symplectic dual)",
    }


# ---------------------------------------------------------------------------
# Verification
# ---------------------------------------------------------------------------

def verify_yangian():
    results = {}

    # Generator counts
    results["Y(sl2) level 0: 3 gens"] = yangian_generator_count("sl2", 0) == 3
    results["Y(sl2) level 1: 6 gens"] = yangian_generator_count("sl2", 1) == 6
    results["Y(sl3) level 0: 8 gens"] = yangian_generator_count("sl3", 0) == 8

    # RTT
    results["sl2 RTT: 9 rels"] = yangian_rtt_relation_count("sl2") == 9
    results["sl3 RTT: 64 rels"] = yangian_rtt_relation_count("sl3") == 64

    # E_1 structure
    e1_sl2 = e1_bar_structure("sl2")
    results["E_1 operad"] = e1_sl2["operad"] == "E_1 (little 1-discs)"
    results["sl2 dual = U_q(sl_2)"] = e1_sl2["koszul_dual"] == "U_q(sl_2)"

    # Bar deg 2
    results["B^2_{E1}(sl2) = 9"] = e1_bar_deg2_dim("sl2") == 9
    results["B^2_{E1}(sl3) = 64"] = e1_bar_deg2_dim("sl3") == 64

    return results


