def kappa_factor(family: str, n: int, k: int = 1) -> Rational:
    r"""Modular characteristic of V_k(g) for simple g of type family_n.

    kappa(g, k) = dim(g) * (k + h^v) / (2 * h^v)

    At k = 1:
        kappa(A_n, 1) = n(n+2)(n+2) / (2(n+1)) = n(n+2)/2
        kappa(D_n, 1) = n(2n-1)(2n-1) / (2*2(n-1)) = n(2n-1)/(4(n-1)) * (2n-1)
          ... simpler: dim * (1+h) / (2h) for simply-laced
        kappa(E_8, 1) = 248 * 31 / 60 = 7688/60 = 1922/15

    CAUTION (AP1): these are NOT c/2.  kappa != c/2 in general.
    """
    d = dim_simple(family, n)
    hv = dual_coxeter_number(family, n)
    return Rational(d * (k + hv), 2 * hv)


def central_charge_factor(family: str, n: int, k: int = 1) -> Rational:
    r"""Central charge of V_k(g) for simple g.

    c(g, k) = k * dim(g) / (k + h^v)

    At k = 1 for simply-laced:
        c(A_n, 1) = n(n+2)/(n+2) = n
        c(D_n, 1) = n(2n-1)/(2n-1) = n
        c(E_6, 1) = 78/13 = 6
        c(E_7, 1) = 133/19 = 7
        c(E_8, 1) = 248/31 = 8

    So c(g, 1) = rank(g) for all simply-laced simple g.  This is because
    c = k * dim / (k + h) and dim = rank + |R| = rank + rank * h
    (since |R| = rank * h for simply-laced types... wait, that's not right.
    Actually: |R(A_n)| = n(n+1) = rank * h. |R(D_n)| = 2n(n-1) = rank * h.
    |R(E_6)| = 72 = 6*12 = rank * h.  |R(E_7)| = 126 = 7*18 = rank * h.
    |R(E_8)| = 240 = 8*30 = rank * h.  YES: |R| = rank * h for simply-laced.
    So dim = rank + rank*h = rank*(1+h), hence c = dim/(1+h) = rank.)
    """
    d = dim_simple(family, n)
    hv = dual_coxeter_number(family, n)
    return Rational(d * k, k + hv)


def cubic_shadow_factor(family: str, n: int, k: int = 1) -> Rational:
    r"""Cubic shadow S_3 for V_k(g) on the current (Lie algebra) line.

    For affine KM on the current line: alpha = 1 (universal), S_3 = alpha/3 = 1/3.
    This is INDEPENDENT of the simple Lie algebra type and of the level k
    (as long as the algebra is affine KM on the current line).

    The S_3 = 1/3 comes from the Lie bracket [,] contributing a cubic
    term to the shadow tower through the A-infinity transfer.  The Jacobi
    identity ensures this terminates: S_4 = 0 on the current line.
    """
    return Rational(1, 3)


def quartic_shadow_factor(family: str, n: int, k: int = 1) -> Rational:
    """S_4 = 0 for affine KM on the current line (Jacobi identity)."""
    return Rational(0)


