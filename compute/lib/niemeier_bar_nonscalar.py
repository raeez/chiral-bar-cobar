# Kappa decomposition analysis
# =========================================================================

def kappa_decomposition(label: str) -> Dict[str, Any]:
    r"""Decompose kappa = 24 into Heisenberg + KM contributions.

    For a lattice VOA V_Lambda:
        kappa(V_Lambda) = rank = 24.

    The Heisenberg (Cartan) sector contributes:
        kappa_Heis = rank = 24 (one per boson, each at level 1).

    But the affine KM factor V_1(g) has kappa(g) = dim(g)*(1+h^v)/(2*h^v),
    which is MORE than just rank(g) = c(g,1).  The difference
        delta_kappa(g) = kappa(g) - rank(g)
    accounts for the root-system-enhanced curvature beyond the Cartan sector.

    IMPORTANT: The two decompositions are different.
    (A) As a tensor product of affine VOAs: kappa = sum kappa(g_i, 1).
    (B) As Cartan Heisenberg + root vertex operators: kappa = 24 (pure Cartan).

    Decomposition (A) is the multi-channel shadow decomposition.
    Decomposition (B) is the cocycle-curvature orthogonality theorem.
    They give the SAME total (24) because kappa is additive and
    the tensor product of affine VOAs = the lattice VOA.

    Wait -- these MUST agree.  Let me verify:
    sum_i kappa(g_i, 1) = 24 for all Niemeier lattices (verified below).
    rank(Lambda) = 24.
    So sum_i kappa(g_i, 1) = rank(Lambda).

    This is a nontrivial identity! It says:
        sum_i dim(g_i)*(1+h_i)/(2*h_i) = 24
    for every Niemeier root system whose components have uniform Coxeter number h.

    Proof: For a uniform-h Niemeier lattice with components g_1, ..., g_s
    all having Coxeter number h, each with rank r_i:
        dim(g_i) = r_i * (1 + h)  [since |R(g_i)| = r_i * h for simply-laced]
    So: kappa(g_i,1) = r_i*(1+h)*(1+h)/(2h) = r_i*(1+h)^2/(2h)
    Sum: sum kappa_i = (1+h)^2/(2h) * sum r_i = (1+h)^2/(2h) * 24.

    Hmm, that gives (1+h)^2/(2h) * 24, not 24.  So for h=2 (24A_1):
    (3)^2/4 * 24 = 9/4 * 24 = 54 != 24.

    WAIT.  Let me recompute.  At k=1:
    kappa(A_1, 1) = dim(sl_2) * (1 + h^v) / (2 h^v) = 3 * 3 / 4 = 9/4.
    For 24 copies: total = 24 * 9/4 = 54.  This is NOT 24.

    So the multi-channel kappa sum is NOT 24 in general!  The total kappa
    of V_Lambda = 24 (rank), but the sum of per-factor kappas can be different.

    RESOLUTION: V_Lambda is NOT literally the tensor product of V_1(g_i)
    in the category of vertex algebras with separate actions.  It is a
    SIMPLE CURRENT EXTENSION (by the glue code) of the tensor product.
    The glue vectors modify the curvature.

    The correct per-factor kappa decomposition uses the LATTICE VOA kappa = rank,
    not the affine VOA kappa.  The per-factor contribution to kappa is
    kappa_i = rank(g_i) = c(g_i, 1), NOT kappa(g_i, 1).

    Let me redo: the Cartan sector of factor g_i contributes kappa = rank(g_i)
    (Heisenberg level-1 bosons).  The root sector contributes 0 (curvature-
    braiding orthogonality).  So the per-factor contribution is rank(g_i).

    But then every factor just contributes its rank, and we need a finer
    invariant.  The ROOT SYSTEM TYPE (not just rank) is what distinguishes
    the lattices.  The bar complex knows the root system through the
    weight-1 OPE structure (the Lie bracket structure constants).
    """
    dec = factor_decomposition(label)
    components = dec['components']

    per_factor = []
    for i, (fam, n) in enumerate(components):
        d = dim_simple(fam, n)
        h = coxeter_number(fam, n)
        rank = n
        kap = kappa_factor(fam, n, k=1)
        c = central_charge_factor(fam, n, k=1)

        per_factor.append({
            'type': f'{fam}_{n}',
            'family': fam,
            'rank': rank,
            'dim': d,
            'h': h,
            'kappa_affine': kap,
            'c_affine': c,
            'kappa_lattice': Rational(rank),  # contribution to lattice kappa
        })

    # The sum of affine kappas -- NOT equal to 24 in general
    affine_kappa_sum = sum(pf['kappa_affine'] for pf in per_factor)

    # The sum of lattice kappas = rank = 24
    lattice_kappa_sum = sum(pf['kappa_lattice'] for pf in per_factor)

    return {
        'label': label,
        'per_factor': per_factor,
        'affine_kappa_sum': affine_kappa_sum if per_factor else Rational(24),
        'lattice_kappa_sum': lattice_kappa_sum if per_factor else Rational(24),
        'affine_kappa_equals_24': affine_kappa_sum == Rational(24) if per_factor else True,
    }


