def verify_dim_rank_roots() -> bool:
    """Verify dim = rank + |R| for all ADE types used."""
    for fam in ['A', 'D', 'E']:
        if fam == 'A':
            for n in range(1, 25):
                if not _verify_dim_equals_rank_plus_roots(fam, n):
                    return False
        elif fam == 'D':
            for n in range(3, 25):
                if not _verify_dim_equals_rank_plus_roots(fam, n):
                    return False
        elif fam == 'E':
            for n in [6, 7, 8]:
                if not _verify_dim_equals_rank_plus_roots(fam, n):
                    return False
    return True


def verify_c_equals_rank_at_k1() -> bool:
    """Verify c(g, 1) = rank(g) for all simply-laced types used in Niemeier."""
    for label in ALL_NIEMEIER_LABELS:
        components = NIEMEIER_REGISTRY[label]['components']
        for fam, n in components:
            c = central_charge_factor(fam, n, k=1)
            if c != Rational(n):
                return False
    return True


def verify_total_c_24() -> bool:
    """Verify sum c_i = 24 for all non-Leech Niemeier lattices."""
    for label in ALL_NIEMEIER_LABELS:
        if label == 'Leech':
            continue
        dec = factor_decomposition(label)
        if dec['total_c'] != Rational(24):
            return False
    return True


def verify_niemeier_uniform_h() -> bool:
    """Verify all components of each Niemeier lattice have the same Coxeter number."""
    for label in ALL_NIEMEIER_LABELS:
        if label == 'Leech':
            continue
        components = NIEMEIER_REGISTRY[label]['components']
        hs = [coxeter_number(f, n) for f, n in components]
        if not all(h == hs[0] for h in hs):
            return False
    return True


