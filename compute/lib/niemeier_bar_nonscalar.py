# Discrimination analysis
# =========================================================================

def minimal_distinguishing_invariant() -> Dict[str, Any]:
    r"""Find the minimal set of bar-complex invariants that distinguishes all 24.

    Tests progressively richer invariants until all 24 are distinguished.

    Level 0: scalar kappa. Value = 24 for all. Distinguishes 1/24.
    Level 1: |R| (number of roots = weight-1 dim - 24). 17 distinct values.
    Level 2: sorted Coxeter number vector. 24 distinct? Yes -- Niemeier's thm
             says the root system (hence Coxeter numbers) determines the lattice.
    Level 3: sorted affine kappa vector. Also 24 distinct (encodes root system).
    Level 4: factor type multiset. 24 distinct (IS the root system).
    """
    results = {}

    # Level 0: scalar kappa
    by_kappa: Dict[Any, List[str]] = {}
    for label in ALL_NIEMEIER_LABELS:
        val = Rational(24)
        if val not in by_kappa:
            by_kappa[val] = []
        by_kappa[val].append(label)
    results['level_0_scalar_kappa'] = {
        'distinct_values': len(by_kappa),
        'collisions': {str(k): v for k, v in by_kappa.items() if len(v) > 1},
    }

    # Level 1: number of roots
    by_roots: Dict[int, List[str]] = {}
    for label in ALL_NIEMEIER_LABELS:
        nr = NIEMEIER_REGISTRY[label]['num_roots']
        if nr not in by_roots:
            by_roots[nr] = []
        by_roots[nr].append(label)
    results['level_1_num_roots'] = {
        'distinct_values': len(by_roots),
        'collisions': {str(k): v for k, v in by_roots.items() if len(v) > 1},
    }

    # Level 2: sorted h-vector (Coxeter numbers)
    by_hvec: Dict[Tuple, List[str]] = {}
    for label in ALL_NIEMEIER_LABELS:
        hv = h_vector(label)
        if hv not in by_hvec:
            by_hvec[hv] = []
        by_hvec[hv].append(label)
    results['level_2_h_vector'] = {
        'distinct_values': len(by_hvec),
        'collisions': {str(k): v for k, v in by_hvec.items() if len(v) > 1},
    }

    # Level 3: sorted affine kappa vector
    by_kv: Dict[Tuple, List[str]] = {}
    for label in ALL_NIEMEIER_LABELS:
        kv = affine_kappa_vector(label)
        if kv not in by_kv:
            by_kv[kv] = []
        by_kv[kv].append(label)
    results['level_3_kappa_vector'] = {
        'distinct_values': len(by_kv),
        'collisions': {str(k): v for k, v in by_kv.items() if len(v) > 1},
    }

    # Level 4: factor type multiset (the root system itself)
    by_ftm: Dict[Tuple, List[str]] = {}
    for label in ALL_NIEMEIER_LABELS:
        ftm = factor_type_multiset(label)
        if ftm not in by_ftm:
            by_ftm[ftm] = []
        by_ftm[ftm].append(label)
    results['level_4_factor_multiset'] = {
        'distinct_values': len(by_ftm),
        'collisions': {str(k): v for k, v in by_ftm.items() if len(v) > 1},
    }

    # Find minimal level
    for level in range(5):
        key = f'level_{level}_' + ['scalar_kappa', 'num_roots', 'h_vector',
                                    'kappa_vector', 'factor_multiset'][level]
        if results[key]['distinct_values'] == 24:
            results['minimal_complete_level'] = level
            results['minimal_complete_invariant'] = key
            break
    else:
        results['minimal_complete_level'] = None

    return results


def collision_analysis() -> Dict[str, Any]:
    r"""Detailed analysis of which lattice pairs collide at each level.

    This is the key result: how many invariants do you need to tell
    all 24 apart?
    """
    # Pairs that share the same |R|
    from compute.lib.niemeier_shadow_atlas import root_count_collisions
    rc = root_count_collisions()

    # For each colliding pair at level 1, check if level 2 (h-vector) resolves
    resolved_by_h = {}
    for nr, labels in rc.items():
        hvecs = {lab: h_vector(lab) for lab in labels}
        distinct = len(set(hvecs.values())) == len(labels)
        resolved_by_h[nr] = {
            'labels': labels,
            'h_vectors': {lab: list(hv) for lab, hv in hvecs.items()},
            'resolved': distinct,
        }

    return {
        'root_count_collisions': {str(k): v for k, v in rc.items()},
        'num_collision_groups': len(rc),
        'resolved_by_h_vector': resolved_by_h,
    }


