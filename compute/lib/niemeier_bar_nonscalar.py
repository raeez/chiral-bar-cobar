# Casimir eigenvalue (another per-factor invariant)
# =========================================================================

def casimir_eigenvalue(family: str, n: int, k: int = 1) -> Rational:
    r"""Eigenvalue of the quadratic Casimir on the adjoint representation,
    normalized so that the Killing form is 2*h^v times the standard form.

    For the affine VOA V_k(g), the Sugawara construction gives:
        L_0 = C_2 / (2*(k + h^v))
    where C_2 is the quadratic Casimir eigenvalue on each weight space.

    On the adjoint representation at level k:
        C_2(adj) = 2 * h^v  (with standard normalization (theta,theta)=2).

    The conformal weight of the adjoint (current) representation is:
        h_adj = C_2(adj) / (2*(k+h^v)) = h^v / (k+h^v) = h^v / (1+h^v) at k=1.

    But for the CURRENTS j^a(z) at weight 1, C_2(adj) / (2*(k+h^v)) = 1
    only if we use the right normalization.  Actually j^a has weight 1
    by definition (it's a weight-1 primary field), so this is a consistency check.
    """
    hv = dual_coxeter_number(family, n)
    return Rational(2 * hv)


