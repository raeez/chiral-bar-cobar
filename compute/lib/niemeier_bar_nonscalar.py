def affine_kappa_vector(label: str) -> Tuple[Rational, ...]:
    r"""Sorted vector of per-factor affine kappa values.

    kappa_i = dim(g_i) * (1 + h_i) / (2 * h_i) for each simple factor g_i.

    This vector is a COMPLETE invariant distinguishing all 24 Niemeier lattices.
    The proof relies on:
    (1) The map g -> kappa(g, 1) = dim(g)*(1+h)/(2h) is injective on ADE types.
    (2) The sorted multiset of kappa values determines the root system multiset.
    (3) The root system determines the Niemeier lattice uniquely.

    For (1): dim(g)*(1+h)/(2h) = (rank*(1+h))*(1+h)/(2h) = rank*(1+h)^2/(2h).
    We need: if (r_1, h_1) and (r_2, h_2) give the same kappa, then g_1 = g_2.
    The function r*(1+h)^2/(2h) on ADE types (parametrized by family, rank):
        A_n: n*(n+2)^2/(2(n+1))
        D_n: n*(2n-1)^2/(4(n-1))
        E_6: 6*13^2/24 = 6*169/24 = 1014/24 = 169/4
        E_7: 7*19^2/36 = 7*361/36 = 2527/36
        E_8: 8*31^2/60 = 8*961/60 = 7688/60 = 1922/15

    We verify injectivity computationally below.
    """
    if label not in NIEMEIER_REGISTRY:
        raise ValueError(f"Unknown Niemeier lattice: {label}")

    components = NIEMEIER_REGISTRY[label]['components']
    if not components:
        return ()  # Leech

    kappas = [kappa_factor(fam, n, k=1) for fam, n in components]
    return tuple(sorted(kappas))


def kappa_vector_is_injective() -> Tuple[bool, Dict[Tuple, List[str]]]:
    r"""Verify that the sorted affine kappa vector distinguishes all 24 lattices.

    Returns (is_injective, collision_dict).
    """
    by_kappa: Dict[Tuple, List[str]] = {}
    for label in ALL_NIEMEIER_LABELS:
        kv = affine_kappa_vector(label)
        if kv not in by_kappa:
            by_kappa[kv] = []
        by_kappa[kv].append(label)

    collisions = {k: v for k, v in by_kappa.items() if len(v) > 1}
    return (len(collisions) == 0, collisions)


