def weight_spectrum(label: str, max_weight: int = 4) -> Dict[int, int]:
    r"""Weight spectrum of the bar complex B(V_Lambda) at low weights.

    Returns {weight: dimension} for the bar-relevant part.

    Weight 0: just the vacuum. dim = 1.
    Weight 1: Cartan generators + root vectors. dim = 24 + |R|.
    Weight 2: symmetric products of weight-1, Sugawara, etc.
              For lattice VOA of rank r with root system R:
              dim V_2 = r(r+1)/2 + |R| + |{alpha+beta : alpha,beta in R, ht 2}|
              ... this requires more detailed root system analysis.

    For now, we compute weight 1 exactly and weight 2 as a lower bound.
    """
    if label not in NIEMEIER_REGISTRY:
        raise ValueError(f"Unknown Niemeier lattice: {label}")

    data = NIEMEIER_REGISTRY[label]
    n_roots = data['num_roots']
    components = data['components']

    spectrum = {0: 1}

    # Weight 1: currents j^a(z) for Cartan + root vectors e^alpha(z)
    # Cartan: rank = 24 for non-Leech, 24 for Leech (Heisenberg)
    # Roots: |R(Lambda)| for non-Leech, 0 for Leech
    spectrum[1] = 24 + n_roots

    # Weight 2: more complex
    # Normal-ordered products :j^a j^b: give rank*(rank+1)/2 = 24*25/2 = 300
    # The derivative partial j^a gives another rank = 24
    # Plus vertex operators for norm-4 vectors (which are NOT roots)
    # For roots alpha with |alpha|^2 = 2: e^alpha has weight 1
    # For vectors v with |v|^2 = 4: e^v has weight 2
    # Also: :e^alpha j^a: for each root alpha, but these are derivs
    # of e^alpha, hence already counted.
    #
    # Actually, for a holomorphic VOA of central charge 24:
    # The weight-2 space has dimension = 24*25/2 + 24 + n_4
    # where n_4 = number of lattice vectors of norm 4.
    # But n_4 is hard to compute without the full lattice.
    #
    # We can compute it from the theta series: r(2) = n_4 (including 0-vector
    # which has norm 0, and roots which have norm 2).
    # Actually theta(q) = sum_{v in Lambda} q^{|v|^2/2}.
    # So coefficient of q^1 = |R| (norm 2 vectors with |v|^2/2 = 1).
    # Coefficient of q^2 = number of norm-4 vectors.
    #
    # For the lattice VOA, dim V_h = theta coefficient of q^h (approximately;
    # the exact dim V_h involves the Heisenberg partition function).
    # Precisely: the graded dimension of V_Lambda is
    #   Z(q) = Theta_Lambda(q) / eta(q)^{24}
    # where eta(q)^{24} = q * prod (1-q^n)^{24}.
    # So: dim V_h = coefficient of q^h in Theta_Lambda(q) / (q * prod (1-q^n)^{24}).
    #
    # This is well beyond the scope of simple formulas for h >= 2.
    # We record only weight-1 data, which is already discriminating.

    return spectrum


def weight_1_dimension(label: str) -> int:
    """Dimension of the weight-1 subspace: rank + |R|."""
