def dim_simple(family: str, n: int) -> int:
    r"""Dimension of a simple Lie algebra of ADE type.

    A_n: sl_{n+1} has dimension n(n+2).
    D_n: so_{2n} has dimension n(2n-1).
    E_6: 78.  E_7: 133.  E_8: 248.

    Verification: dim = rank + |R|, where rank is the Cartan rank
    and |R| is the number of roots.  For A_n: rank = n, |R| = n(n+1),
    so dim = n + n(n+1) = n(n+2).
    """
    if family == 'A':
        return n * (n + 2)
    elif family == 'D':
        if n < 3:
            raise ValueError(f"D_n requires n >= 3, got {n}")
        return n * (2 * n - 1)
    elif family == 'E':
        return {6: 78, 7: 133, 8: 248}[n]
    else:
        raise ValueError(f"Unknown ADE family: {family}")


def _verify_dim_equals_rank_plus_roots(family: str, n: int) -> bool:
    """Verify dim(g) = rank(g) + |R(g)| (basic consistency)."""
    d = dim_simple(family, n)
    r = root_count(family, n)
    rank = n  # Cartan rank = n for all ADE types
    return d == rank + r


