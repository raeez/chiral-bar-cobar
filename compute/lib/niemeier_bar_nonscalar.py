def inner_product_distribution(label: str) -> Dict[int, int]:
    r"""Distribution of inner products (alpha, beta) for alpha, beta in R(Lambda).

    For ADE root systems with roots of norm 2 (|alpha|^2 = 2):
    possible inner products are -2, -1, 0, 1, 2.

    Returns {ip_value: count} where count = #{(alpha, beta) : (alpha,beta) = ip_value}.

    (alpha, alpha) = 2 for all roots: contributes |R| pairs with ip = 2.
    (alpha, -alpha) = -2: contributes |R| pairs with ip = -2.
    (alpha, beta) = 0: orthogonal roots.
    (alpha, beta) = 1: alpha - beta is a root (Weyl group orbit).
    (alpha, beta) = -1: alpha + beta is a root.

    For a simple component g of rank r and root system R(g):
        #{(alpha, beta) : ip = 2} = |R(g)| (alpha = beta)
        #{(alpha, beta) : ip = -2} = |R(g)| (alpha = -beta)
        #{(alpha, beta) : ip = 0 and alpha != +-beta} = computed per type
        #{(alpha, beta) : ip = 1} = computed per type
        #{(alpha, beta) : ip = -1} = computed per type

    Total pairs: |R|^2 = sum of all counts.
    """
    if label == 'Leech' or label not in NIEMEIER_REGISTRY:
        if label == 'Leech':
            return {0: 0}  # no roots
        raise ValueError(f"Unknown Niemeier lattice: {label}")

    data = NIEMEIER_REGISTRY[label]
    components = data['components']
    n_total = data['num_roots']

    # For roots in the same simple component:
    #   ip = 2:  |R| pairs (alpha = beta)
    #   ip = -2: |R| pairs (alpha = -beta)
    #   ip = 1:  each root alpha has #{beta : (alpha,beta)=1} adjacent roots
    #   ip = -1: each root alpha has #{beta : (alpha,beta)=-1} adjacent roots
    #   ip = 0:  remaining

    # For roots in DIFFERENT components: always ip = 0.

    ip_2 = n_total  # alpha = beta
    ip_minus2 = n_total  # alpha = -beta

    # Count within-component ip = 1 and ip = -1
    ip_1_total = 0
    ip_minus1_total = 0
    ip_0_within = 0

    for fam, n in components:
        nr = root_count(fam, n)
        # For each root alpha in a simply-laced simple root system:
        # #{beta : (alpha,beta) = 1} = number of roots beta such that
        # alpha - beta is a root (i.e., beta = alpha - gamma for a root gamma
        # with gamma != alpha).
        #
        # For ADE: the number of ordered pairs with ip = 1 is |R| * valence
        # where valence = #{beta in R : (alpha,beta) = 1} is uniform over roots
        # (by Weyl group transitivity).
        #
        # Valence for simple ADE types:
        # If alpha is a root, #{beta root : (alpha,beta)=1} = 2*(h-1)  ???
        # Let me think more carefully.
        #
        # For a simple root system R:
        # Total ordered pairs (alpha,beta) with various ip values:
        #   ip=2: |R| (diagonal)
        #   ip=-2: |R| (negatives)
        #   ip=1: |R| * n_plus where n_plus = #{beta : (alpha,beta)=1} per alpha
        #   ip=-1: |R| * n_minus where n_minus = #{beta : (alpha,beta)=-1} per alpha
        #   ip=0: |R| * n_orth - 2*|R| ... hmm, n_orth includes alpha != +-beta
        #
        # By symmetry beta -> -beta: n_plus = n_minus.
        # So: |R|^2 = |R| + |R| + 2*|R|*n_plus + |R|*n_orth
        # i.e., |R| - 2 = 2*n_plus + n_orth
        # where n_orth = #{beta : (alpha,beta) = 0, beta != +-alpha}
        #
        # For A_n: |R| = n(n+1).
        #   n_plus = n_minus = 2(n-1) for n >= 2 (each root is connected
        #   to 2(n-1) roots by ip = +-1... let me compute for small cases).
        #
        # Actually, for type A_n, let's use the standard representation.
        # Roots are e_i - e_j for 1 <= i != j <= n+1.
        # (e_i-e_j, e_k-e_l) = delta_{ik} - delta_{il} - delta_{jk} + delta_{jl}.
        # This equals 1 iff exactly one of: i=k, j=l (but not both) minus
        # one of: i=l, j=k.
        # ip = 1: i=k, j!=l, i!=l, j!=k: i.e., alpha and beta share first
        #   index but differ in second, and no other coincidence.
        #   Or: j=l, i!=k, i!=l, j!=k.
        #   Count: fixing alpha = e_1-e_2:
        #     ip=1: e_1-e_j (j!=1,2): n-1 choices.
        #           e_i-e_2 (i!=1,2): n-1 choices.
        #     Total n_plus = 2(n-1).
        #   Similarly n_minus = 2(n-1) (by beta -> -beta).
        #   n_orth = n(n+1) - 2 - 2*2*(n-1) = n^2+n-2-4n+4 = n^2-3n+2 = (n-1)(n-2).
        #   Check: |R|-2 = n(n+1)-2 = n^2+n-2. 2*n_plus + n_orth = 4(n-1)+(n-1)(n-2) = (n-1)(4+n-2) = (n-1)(n+2) = n^2+n-2. Correct.

        n_plus = _adjacent_roots_per_root(fam, n)
        n_orth = _orthogonal_roots_within_component(fam, n)

        ip_1_total += nr * n_plus
        ip_minus1_total += nr * n_plus  # n_minus = n_plus by symmetry
        ip_0_within += nr * n_orth

    # Cross-component pairs: all have ip = 0
    # Number of cross-component ordered pairs:
    cross_pairs = n_total ** 2 - sum(root_count(f, n) ** 2 for f, n in components)

    ip_0_total = ip_0_within + cross_pairs

    return {
        2: ip_2,
        1: ip_1_total,
        0: ip_0_total,
        -1: ip_minus1_total,
        -2: ip_minus2,
    }


