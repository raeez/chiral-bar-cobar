# Factor type multiset (canonical form)
# =========================================================================

def factor_type_multiset(label: str) -> Tuple[Tuple[str, int, int], ...]:
    r"""Canonical multiset of factor types for a Niemeier lattice.

    Returns sorted tuple of (family, rank, multiplicity) triples.
    E.g., for 3E_8: (('E', 8, 3),).
    For D_16 + E_8: (('D', 16, 1), ('E', 8, 1)).
    """
    if label not in NIEMEIER_REGISTRY:
        raise ValueError(f"Unknown Niemeier lattice: {label}")

    components = NIEMEIER_REGISTRY[label]['components']
    count: Dict[Tuple[str, int], int] = {}
    for fam, n in components:
        key = (fam, n)
        count[key] = count.get(key, 0) + 1

    result = tuple(sorted(
        (fam, n, mult) for (fam, n), mult in count.items()
    ))
    return result


