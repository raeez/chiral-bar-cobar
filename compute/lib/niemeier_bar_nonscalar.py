def factor_decomposition(label: str) -> Dict[str, Any]:
    r"""Complete per-factor bar complex data for a Niemeier lattice.

    Returns a dict with:
      - components: list of (family, rank) tuples
      - kappa_vector: list of kappa values for each factor
      - c_vector: list of central charges for each factor
      - h_vector: list of Coxeter numbers
      - dim_vector: list of dim(g_i)
      - S3_vector: list of S_3 values (all 1/3 for non-Leech)
      - S4_vector: list of S_4 values (all 0)
      - num_factors: number of simple factors
      - total_kappa: sum (should be 24)
      - total_c: sum (should be 24)
      - total_dim: sum of dimensions
      - weight_1_bar_dim: 24 + |R| (weight-1 sector of bar complex)
      - kappa_sorted: sorted kappa vector (canonical invariant)
    """
    if label not in NIEMEIER_REGISTRY:
        raise ValueError(f"Unknown Niemeier lattice: {label}")

    data = NIEMEIER_REGISTRY[label]
    components = data['components']

    kappa_vec = []
    c_vec = []
    h_vec = []
    dim_vec = []
    S3_vec = []
    S4_vec = []

    for fam, n in components:
        kappa_vec.append(kappa_factor(fam, n, k=1))
        c_vec.append(central_charge_factor(fam, n, k=1))
        h_vec.append(Rational(coxeter_number(fam, n)))
        dim_vec.append(dim_simple(fam, n))
        S3_vec.append(cubic_shadow_factor(fam, n, k=1))
        S4_vec.append(quartic_shadow_factor(fam, n, k=1))

    total_kappa = sum(kappa_vec) if kappa_vec else Rational(24)
    total_c = sum(c_vec) if c_vec else Rational(24)
    total_dim = sum(dim_vec) if dim_vec else 0

    kappa_sorted = tuple(sorted(kappa_vec))

    return {
        'label': label,
        'components': components,
        'num_factors': len(components),
        'kappa_vector': kappa_vec,
        'c_vector': c_vec,
        'h_vector': h_vec,
        'dim_vector': dim_vec,
        'S3_vector': S3_vec,
        'S4_vector': S4_vec,
        'total_kappa': total_kappa,
        'total_c': total_c,
        'total_dim': total_dim,
        'weight_1_bar_dim': 24 + data['num_roots'],
        'kappa_sorted': kappa_sorted,
    }


