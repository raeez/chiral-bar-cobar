def collision_residue_pole_orders(ope_pole_orders):
    """Given OPE pole orders, compute collision residue pole orders.

    The bar kernel is d log(z-w), which absorbs one power of (z-w).
    So OPE pole of order n becomes collision residue pole of order n-1.

    AP19: Virasoro OPE has poles at z^{-4}, z^{-2}, z^{-1}.
    Collision residue (r-matrix) has poles at z^{-3}, z^{-1}.
    (No z^{-2} pole in the r-matrix for Virasoro!)

    Args:
        ope_pole_orders: list of pole orders in the OPE (e.g. [4, 2, 1] for Virasoro)

    Returns:
        list of pole orders in the collision residue
    """
    return [p - 1 for p in ope_pole_orders if p - 1 > 0]


