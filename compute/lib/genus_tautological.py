# ============================================================================
# Exact Bernoulli numbers (self-contained, no external dependency)
# ============================================================================

@lru_cache(maxsize=64)
def _bernoulli_exact(n: int) -> Fraction:
    """Exact Bernoulli number B_n via the Akiyama-Tanigawa algorithm.

    Convention: B_1 = -1/2 (first Bernoulli numbers).
    B_0 = 1, B_2 = 1/6, B_4 = -1/30, B_6 = 1/42.
    """
    if n < 0:
        return Fraction(0)
    a = [Fraction(1, m + 1) for m in range(n + 1)]
    for j in range(1, n + 1):
        for m in range(n, j - 1, -1):
            a[m] = (m - j + 1) * (a[m] - a[m - 1])
    return a[n]


# ============================================================================
# Faber-Pandharipande intersection numbers
# ============================================================================

@lru_cache(maxsize=32)
def lambda_fp(g: int) -> Fraction:
    r"""Faber-Pandharipande number lambda_g^FP.

    lambda_g^FP = int_{M-bar_{g,1}} lambda_g * psi_1^{2g-2}
                = (2^{2g-1} - 1) / 2^{2g-1} * |B_{2g}| / (2g)!

    Independently verified values:
      g=1: 1/24
      g=2: 7/5760
      g=3: 31/967680
      g=4: 127/154828800
      g=5: 73/3503554560
    """
    if g < 1:
        raise ValueError(f"lambda_g^FP requires g >= 1, got {g}")
    B_2g = _bernoulli_exact(2 * g)
    abs_B_2g = abs(B_2g)
    power = 2 ** (2 * g - 1)
    return Fraction(power - 1, power) * abs_B_2g / Fraction(math_factorial(2 * g))


