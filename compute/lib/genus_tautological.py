class Genus3TautologicalFreeEnergy:
    r"""Compute F_3(A) via tautological intersection theory on M-bar_3.

    The genus-3 free energy is:
      F_3(A) = kappa(A) * lambda_3^FP + delta_pf^{(3,0)}(A)

    where lambda_3^FP = 31/967680 and delta_pf^{(3,0)} is the planted-
    forest correction involving S_3, S_4, S_5.

    PLANTED-FOREST CORRECTION at genus 3 (eq:delta-pf-genus3-explicit):

      delta_pf^{(3,0)} is an 11-term polynomial in kappa, S_3, S_4, S_5:

        7/8   S_3 S_5
      + 3/512 S_3^3 kappa
      - 5/128 S_3^4
      - 167/96 S_3^2 S_4
      + 83/1152 S_3 S_4 kappa
      - 343/2304 S_3 kappa
      - 1/4608 S_3^2 kappa^2
      - 1/82944 S_3 kappa^3
      - 7/12 S_4^2
      + 1/1152 S_4 kappa^2
      - 1/96 S_5 kappa

    NOTE: The genus-1+ vertex weights in this formula are approximate
    (the exact coefficients require the full R-matrix computation).
    The S_4/S_5 coefficients are exact.
    """

    def __init__(self, kappa_val, S_3=Fraction(0), S_4=Fraction(0),
                 S_5=Fraction(0), name='generic'):
        self.kappa = kappa_val
        self.S_3 = S_3
        self.S_4 = S_4
        self.S_5 = S_5
        self.name = name

    @classmethod
    def heisenberg(cls, k=1):
        """Heisenberg at level k. Class G: all shadow coefficients zero."""
        return cls(kappa_val=Fraction(k), name=f'Heisenberg_k{k}')

    @classmethod
    def virasoro(cls, c):
        """Virasoro at central charge c. Class M."""
        c = Fraction(c)
        kappa = c / 2
        S_3 = Fraction(2)
        S_4 = Fraction(10) / (c * (5 * c + 22))
        S_5 = Fraction(-48) / (c ** 2 * (5 * c + 22))
        return cls(kappa_val=kappa, S_3=S_3, S_4=S_4, S_5=S_5,
                   name=f'Virasoro_c{c}')

    @classmethod
    def affine_sl2(cls, k=1):
        """Affine sl_2 at level k. Class L."""
        k = Fraction(k)
        kappa = Fraction(3) * (k + 2) / 4
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'affine_sl2_k{k}')

    @classmethod
    def affine_sl3(cls, k=1):
        """Affine sl_3 at level k."""
        k = Fraction(k)
        kappa = Fraction(4) * (k + 3) / 3
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'affine_sl3_k{k}')

    @classmethod
    def w3(cls, c):
        """W_3 at central charge c."""
        c = Fraction(c)
        kappa = Fraction(5) * c / 6
        return cls(kappa_val=kappa, name=f'W3_c{c}')

    @classmethod
    def betagamma(cls, lam=1):
        """Beta-gamma at conformal weight lambda."""
        lam = Fraction(lam)
        kappa = 6 * lam ** 2 - 6 * lam + 1
        return cls(kappa_val=kappa, name=f'betagamma_lam{lam}')

    @classmethod
    def lattice(cls, rank=8):
        """Lattice VOA."""
        return cls(kappa_val=Fraction(rank), name=f'lattice_rank{rank}')

    @classmethod
    def w_N(cls, N, c):
        """W_N algebra at central charge c.

        kappa(W_N) = c * (H_N - 1) where H_N = 1 + 1/2 + ... + 1/N.
        """
        c = Fraction(c)
        H_N = sum(Fraction(1, j) for j in range(1, N + 1))
        kappa = c * (H_N - 1)
        return cls(kappa_val=kappa, name=f'W{N}_c{c}')

    @classmethod
    def g2_affine(cls, k=1):
        """Affine G_2 at level k."""
        k = Fraction(k)
        kappa = Fraction(7) * (k + 4) / 4
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'G2_k{k}')

    @classmethod
    def f4_affine(cls, k=1):
        """Affine F_4 at level k."""
        k = Fraction(k)
        kappa = Fraction(52) * (k + 9) / 18
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'F4_k{k}')

    @classmethod
    def e6_affine(cls, k=1):
        """Affine E_6 at level k."""
        k = Fraction(k)
        kappa = Fraction(78) * (k + 12) / 24
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'E6_k{k}')

    @classmethod
    def e7_affine(cls, k=1):
        """Affine E_7 at level k."""
        k = Fraction(k)
        kappa = Fraction(133) * (k + 18) / 36
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'E7_k{k}')

    @classmethod
    def e8_affine(cls, k=1):
        """Affine E_8 at level k."""
        k = Fraction(k)
        kappa = Fraction(248) * (k + 30) / 60
        return cls(kappa_val=kappa, S_3=Fraction(2),
                   name=f'E8_k{k}')

    def F3_scalar(self) -> Fraction:
        """F_3^{scal} = kappa * lambda_3^FP = 31 * kappa / 967680."""
        return self.kappa * lambda_fp(3)

    def delta_pf_genus3(self) -> Fraction:
        r"""Planted-forest correction at genus 3.

        delta_pf^{(3,0)} = 11-term polynomial in kappa, S_3, S_4, S_5.

        From eq:delta-pf-genus3-explicit.
        """
        k = self.kappa
        s3 = self.S_3
        s4 = self.S_4
        s5 = self.S_5

        # Each term:
        t1 = Fraction(7, 8) * s3 * s5
        t2 = Fraction(3, 512) * s3 ** 3 * k
        t3 = Fraction(-5, 128) * s3 ** 4
        t4 = Fraction(-167, 96) * s3 ** 2 * s4
        t5 = Fraction(83, 1152) * s3 * s4 * k
        t6 = Fraction(-343, 2304) * s3 * k
        t7 = Fraction(-1, 4608) * s3 ** 2 * k ** 2
        t8 = Fraction(-1, 82944) * s3 * k ** 3
        t9 = Fraction(-7, 12) * s4 ** 2
        t10 = Fraction(1, 1152) * s4 * k ** 2
        t11 = Fraction(-1, 96) * s5 * k

        return t1 + t2 + t3 + t4 + t5 + t6 + t7 + t8 + t9 + t10 + t11

    def F3_total(self) -> Fraction:
        """F_3 = F_3^{scal} + delta_pf^{(3,0)}."""
        return self.F3_scalar() + self.delta_pf_genus3()

    def F3_report(self) -> Dict[str, Any]:
        """Complete genus-3 report."""
        return {
            'family': self.name,
            'kappa': self.kappa,
            'S_3': self.S_3,
            'S_4': self.S_4,
            'S_5': self.S_5,
            'F3_scalar': self.F3_scalar(),
            'delta_pf': self.delta_pf_genus3(),
            'F3_total': self.F3_total(),
            'lambda_3_FP': lambda_fp(3),
        }


