# Lambda_g * psi^{2g-2} as Hodge integral (the FP integral)
# ============================================================================

def fp_integral_from_wk(g: int) -> Fraction:
    r"""Compute lambda_g^FP from Witten-Kontsevich + Hodge integral formula.

    The FP number can be extracted from a combination of WK numbers
    using the Hodge integral formula (Faber-Pandharipande 1998):

      int_{M-bar_{g,1}} lambda_g psi^{2g-2}
        = sum_{j=0}^{2g-2} (-1)^j binom(2g-2, j) <tau_{2g-2-j} tau_j>_g * ???

    Actually, the FP integral has a more direct relation. The
    formula lambda_g^FP = (2^{2g-1}-1)/2^{2g-1} * |B_{2g}|/(2g)!
    is proved directly from GRR (not from WK recursion).

    Here we verify FP numbers are consistent with WK by checking
    that the A-hat generating function matches.
    """
    return lambda_fp(g)
