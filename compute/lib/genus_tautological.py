class MumfordRelations:
    """Mumford's relations from Grothendieck-Riemann-Roch on the universal curve.

    The GRR formula for the Hodge bundle E on M-bar_g gives:

      ch(E) = g/2 + sum_{k>=0} B_{2k}/(2k)! * kappa_{2k-1}

    (with the convention that kappa_{-1} = 2g - 2 + n for M-bar_{g,n}).

    The Chern character determines all Chern classes, and hence
    gives relations among tautological classes.

    At genus 2:
      ch(E) = 1 + lambda_1 + (lambda_1^2 - 2 lambda_2)/2
      ch_0 = 2  (rank of E)
      ch_1 = lambda_1 = c_1(E)
      ch_2 = (lambda_1^2 - 2 lambda_2)/2

    Mumford's formula gives ch_k in terms of kappa classes.
    Comparing gives relations between lambda and kappa classes.
    """

    @staticmethod
    def grr_ch_1_genus2() -> Dict[str, Any]:
        """ch_1(E) = kappa_1/12 on M-bar_2 (from GRR).

        Actually: ch_1(E) = lambda_1 (by definition of Chern character).
        And from the Noether formula: kappa_1 = 12 lambda_1 - delta_irr - delta_1.
        So lambda_1 = (kappa_1 + delta_irr + delta_1) / 12.
        """
        return {
            'relation': 'lambda_1 = (kappa_1 + delta_irr + delta_1) / 12',
            'source': 'Noether formula on M-bar_2',
        }

    @staticmethod
    def grr_ch_2_genus2() -> Dict[str, Any]:
        """ch_2(E) from GRR at genus 2.

        ch_2(E) = (lambda_1^2 - 2 lambda_2) / 2

        From GRR: ch_2(E) = B_2/2! * kappa_1 + correction
                          = (1/6)/2 * kappa_1 + ...
                          = kappa_1 / 12 + boundary corrections.
        """
        return {
            'ch_2': '(lambda_1^2 - 2 * lambda_2) / 2',
            'grr_expression': 'kappa_1 / 12 + boundary',
        }


# ============================================================================
# Witten-Kontsevich intersection numbers (self-contained implementation)
# ============================================================================

@lru_cache(maxsize=8192)
def wk_intersection(g: int, d_tuple: Tuple[int, ...]) -> Fraction:
    r"""Witten-Kontsevich intersection number <tau_{d_1} ... tau_{d_n}>_g.

    Uses string equation, dilaton equation, and DVV recursion.

    Parameters
    ----------
    g : int
        Genus.
    d_tuple : tuple of int
        Psi-class exponents (d_1, ..., d_n).

    Returns
    -------
    Fraction : exact intersection number.
    """
    d_list = sorted(d_tuple, reverse=True)
    d_tuple = tuple(d_list)
    n = len(d_tuple)

    if n == 0:
        return Fraction(0)
    if any(d < 0 for d in d_tuple):
        return Fraction(0)

    # Stability: 2g - 2 + n > 0
    if 2 * g - 2 + n <= 0:
        return Fraction(0)

    # Dimension constraint: sum d_i = 3g - 3 + n
    if sum(d_tuple) != 3 * g - 3 + n:
        return Fraction(0)

    # Base cases
    if g == 0 and d_tuple == (0, 0, 0):
        return Fraction(1)
    if g == 1 and d_tuple == (1,):
        return Fraction(1, 24)

    # String equation: if any d_i = 0
    if 0 in d_tuple:
        rest = list(d_tuple)
        rest.remove(0)
        result = Fraction(0)
        for j in range(len(rest)):
            if rest[j] >= 1:
                new_d = rest[:j] + [rest[j] - 1] + rest[j + 1:]
                result += wk_intersection(g, tuple(sorted(new_d, reverse=True)))
        return result

    # Dilaton equation: if any d_i = 1 and n >= 2
    if 1 in d_tuple and n >= 2:
        rest = list(d_tuple)
        rest.remove(1)
        prefactor = Fraction(2 * g - 2 + n - 1)
        return prefactor * wk_intersection(g, tuple(sorted(rest, reverse=True)))

    # DVV recursion: all d_i >= 2
    d1 = d_list[0]
    rest = list(d_list[1:])

    def _odd_double_fact(m):
        if m <= 0:
            return 1
        result = 1
        for k in range(1, m + 1, 2):
            result *= k
        return result

    lhs_coeff = _odd_double_fact(2 * d1 + 1)
    rhs = Fraction(0)

    # Term 1: sum over j in rest
    for j_idx in range(len(rest)):
        dj = rest[j_idx]
        new_d = d1 + dj - 1
        remaining = rest[:j_idx] + rest[j_idx + 1:]
        new_tuple = tuple(sorted([new_d] + remaining, reverse=True))
        num = _odd_double_fact(2 * d1 + 2 * dj - 1)
        den = _odd_double_fact(2 * dj - 1)
        coeff = Fraction(num, den)
        rhs += coeff * wk_intersection(g, new_tuple)

    # Term 2: sum over a + b = d1 - 2
    import itertools
    if d1 >= 2:
        for a in range(d1 - 1):
            b = d1 - 2 - a
            dfa = _odd_double_fact(2 * a + 1)
            dfb = _odd_double_fact(2 * b + 1)
            coeff2 = Fraction(dfa * dfb, 2)

            # Non-separating: <tau_a tau_b tau_{rest}>_{g-1}
            if g >= 1:
                nsep_tuple = tuple(sorted([a, b] + rest, reverse=True))
                rhs += coeff2 * wk_intersection(g - 1, nsep_tuple)

            # Separating: sum over partitions
            for size_I in range(len(rest) + 1):
                for I_indices in itertools.combinations(range(len(rest)), size_I):
                    J_indices = [k for k in range(len(rest)) if k not in I_indices]
                    I_d = [rest[k] for k in I_indices]
                    J_d = [rest[k] for k in J_indices]
                    for g1 in range(g + 1):
                        g2 = g - g1
                        tuple_I = tuple(sorted([a] + I_d, reverse=True))
                        tuple_J = tuple(sorted([b] + J_d, reverse=True))
                        val = (wk_intersection(g1, tuple_I)
                               * wk_intersection(g2, tuple_J))
                        if val != 0:
                            rhs += coeff2 * val

    return Fraction(rhs, lhs_coeff)


# ============================================================================
# Hodge integral / lambda_g relation to FP numbers via Faber's formula
# ============================================================================

def faber_hodge_integral(g: int, lambda_indices: Tuple[int, ...],
                         psi_exponents: Tuple[int, ...] = ()) -> Optional[Fraction]:
    r"""Compute Hodge integrals int_{M-bar_{g,n}} prod lambda_{a_i} prod psi_j^{d_j}.

    Currently implemented for specific cases at g = 1, 2, 3.

    For the Faber-Pandharipande number:
      int_{M-bar_{g,1}} lambda_g psi^{2g-2} = lambda_g^FP

    Parameters
    ----------
    g : int
        Genus.
    lambda_indices : tuple of int
        Hodge class indices (a_1, ..., a_k) for prod lambda_{a_i}.
    psi_exponents : tuple of int
        Psi-class exponents (d_1, ..., d_n) for prod psi_j^{d_j}.

    Returns
    -------
    Fraction or None if not implemented.
    """
    lambda_deg = sum(lambda_indices)
    psi_deg = sum(psi_exponents)
    n = len(psi_exponents)
    total_deg = lambda_deg + psi_deg
    dim = 3 * g - 3 + n

    # Dimension check
    if total_deg != dim:
        return Fraction(0)

    # Stability check
    if 2 * g - 2 + n <= 0:
        return Fraction(0)

    # FP number: lambda_g, psi^{2g-2}
    if lambda_indices == (g,) and psi_exponents == (2 * g - 2,):
        return lambda_fp(g)

    # int_{M-bar_{g,1}} lambda_g (alone, n=1, dim = 3g-2, deg lambda_g = g)
    # Need g = 3g - 2, so g = 1 only. For g=1: int lambda_1 = 1/24.
    if lambda_indices == (g,) and psi_exponents == () and n == 0:
        # This is on M-bar_{g,0}. dim = 3g-3. deg lambda_g = g.
        # Need g = 3g-3, so g = 3/2 (not integer) for g >= 2.
        # Only works for g=1: dim M-bar_{1,1} = 1. Actually M-bar_{1,0}
        # is not stable. So this doesn't work as stated.
        return None

    # Genus 2 specific integrals
    if g == 2:
        # int_{M-bar_{2,1}} lambda_2 psi^2 = 7/5760
        if lambda_indices == (2,) and psi_exponents == (2,):
            return Fraction(7, 5760)
        # int_{M-bar_{2,1}} lambda_1^2 psi^2 = 1/1152
        if sorted(lambda_indices) == [1, 1] and psi_exponents == (2,):
            return Fraction(1, 1152)
        # int_{M-bar_{2,1}} lambda_1 psi^3 (dim = 4, deg = 1+3 = 4)
        if lambda_indices == (1,) and psi_exponents == (3,):
            return Fraction(29, 5760)
        # int_{M-bar_{2,1}} psi^4 = <tau_4>_2 = 1/1152
        if lambda_indices == () and psi_exponents == (4,):
            return wk_intersection(2, (4,))

    # Genus 3 specific integrals
    if g == 3:
        # int_{M-bar_{3,1}} lambda_3 psi^4 = 31/967680
        if lambda_indices == (3,) and psi_exponents == (4,):
            return Fraction(31, 967680)

    return None


# ============================================================================
# The A-hat generating function verification
# ============================================================================

def ahat_generating_function_check(max_genus: int = 6) -> Dict[int, Dict[str, Any]]:
    r"""Verify the A-hat generating function for FP numbers.

    The universal generating function (thm:universal-generating-function):

      sum_{g>=1} lambda_g^FP * x^{2g} = (x/2)/sin(x/2) - 1

    The function (x/2)/sin(x/2) = t/sin(t) evaluated at t = x/2.
    We compute the coefficients a_{2g} of t^{2g} in t/sin(t) via
    power series inversion: if sin(t)/t = sum c_n t^{2n}, then
    t/sin(t) = sum a_n t^{2n} with a_0 = 1 and
    a_n = -(1/c_0) * sum_{j=0}^{n-1} a_j c_{n-j}.

    Then lambda_g^FP = a_g / 2^{2g} (substituting t = x/2).

    Verified values:
      a_1 = 1/6,     lambda_1^FP = 1/24
      a_2 = 7/360,   lambda_2^FP = 7/5760
      a_3 = 31/15120, lambda_3^FP = 31/967680
    """
    # Compute coefficients of t/sin(t) by power series inversion
    N = max_genus + 2
    # c_k = (-1)^k / (2k+1)!  (coefficient of t^{2k} in sin(t)/t)
    c = [Fraction((-1) ** k, math_factorial(2 * k + 1)) for k in range(N)]

    # a_n via recurrence: a_0 = 1, a_n = -(1/c_0) sum_{j<n} a_j c_{n-j}
    a = [Fraction(0)] * N
    a[0] = Fraction(1)
    for n in range(1, N):
        s = Fraction(0)
        for j in range(n):
            s += a[j] * c[n - j]
        a[n] = -s / c[0]

    results = {}
    for g in range(1, max_genus + 1):
        fp = lambda_fp(g)
        # lambda_g^FP = a_g / 2^{2g}
        ahat_coeff = a[g] / Fraction(2 ** (2 * g))

        results[g] = {
            'lambda_fp': fp,
            'ahat_coefficient': ahat_coeff,
            'match': fp == ahat_coeff,
        }

    return results


# ============================================================================
# Orbifold Euler characteristic of M-bar_g (independent from graph sums)
# ============================================================================

@lru_cache(maxsize=32)
def chi_orb_mbar(g: int) -> Fraction:
    r"""Orbifold Euler characteristic chi^{orb}(M-bar_g).

    From Harer-Zagier:
      chi(M_g) = zeta(1-2g) = B_{2g}/(2g)  (for g >= 1)

    And chi^{orb}(M-bar_g) includes boundary corrections.

    Known values:
      g=1: chi^{orb}(M-bar_{1,1}) = -1/12
           (chi(M_{1,1}) = -1/12 from B_2/2 = (1/6)/2 = 1/12,
            but with sign: zeta(-1) = -B_2/2 = -1/12)
      g=2: chi^{orb}(M-bar_2) = ... computed from graph sum

    We compute using the Harer-Zagier formula for the open moduli space
    and then add boundary corrections via the graph-sum decomposition.
    """
    if g < 1:
        raise ValueError(f"Genus must be >= 1, got {g}")

    # zeta(1-2g) = -B_{2g}/(2g)
    # chi(M_g) = zeta(1-2g) = -B_{2g}/(2g)
    B_2g = _bernoulli_exact(2 * g)
    chi_open = -B_2g / Fraction(2 * g)

    # For g=1: chi(M_{1,1}) = -B_2/2 = -(1/6)/2 = -1/12
    # For g=2: chi(M_2) = -B_4/4 = -(-1/30)/4 = 1/120
    # For g=3: chi(M_3) = -B_6/6 = -(1/42)/6 = -1/252

    return chi_open


