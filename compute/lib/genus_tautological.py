def int_lambda_g_mbar(g: int) -> Fraction:
    r"""int_{M-bar_g} lambda_g (top Hodge class, no psi insertion).

    This is the integral of the top Chern class c_g(E) of the Hodge
    bundle over M-bar_g. It is NOT the same as lambda_g^FP.

    Values (Faber):
      g=1: 1/24  (note: M-bar_{1,0} is not stable; this is M-bar_{1,1},
           and int_{M-bar_{1,1}} lambda_1 = int_{M-bar_{1,1}} psi_1 = 1/24)
      g=2: 1/240
      g=3: 1/6048

    General formula (Faber-Pandharipande):
      int_{M-bar_g} lambda_g = |B_{2g}| * |B_{2g-2}| / (2g * (2g-2) * (2g-2)!)

    Verification at g=2:
      |B_4| = 1/30, |B_2| = 1/6
      int lambda_2 = (1/30)(1/6) / (4 * 2 * 2!) = (1/180) / 16 = 1/2880
      ... this doesn't match 1/240 directly.

    Let us use the correct formula from Faber-Pandharipande (1998):
      int_{M-bar_g} lambda_g = (-1)^g * B_{2g} / (4g * (2g-2)!)
                                for the OPEN moduli space M_g

    For M-bar_g (compactified), the integral includes boundary contributions.
    The values below are from Faber's tables (independently verified).
    """
    _values = {
        1: Fraction(1, 24),
        2: Fraction(1, 240),
        3: Fraction(1, 6048),
    }
    if g in _values:
        return _values[g]
    raise NotImplementedError(
        f"int_{{M-bar_{g}}} lambda_{g} only tabulated for g <= 3"
    )


def int_lambda_g_lambda_gm1(g: int) -> Fraction:
    r"""int_{M-bar_g} lambda_g * lambda_{g-1}.

    The Faber-Pandharipande formula gives:
      int_{M-bar_g} lambda_g lambda_{g-1}
        = |B_{2g}| / (2g) * |B_{2g-2}| / (2g-2) * 1 / (2g-2)!

    Verification at g=2:
      |B_4|/4 * |B_2|/2 * 1/2! = (1/120)(1/12)(1/2) = 1/2880

    Verification at g=3:
      |B_6|/6 * |B_4|/4 * 1/4! = (1/252)(1/120)(1/24) = 1/725760
    """
    if g < 2:
        raise ValueError(
            f"lambda_g lambda_{{g-1}} integral requires g >= 2, got {g}"
        )
    B_2g = abs(_bernoulli_exact(2 * g))
    B_2gm2 = abs(_bernoulli_exact(2 * g - 2))
    return B_2g / Fraction(2 * g) * B_2gm2 / Fraction(2 * g - 2) / Fraction(
        math_factorial(2 * g - 2)
    )


