    # Pure Hodge integrals on M-bar_3
    # -------------------------------------------------------------------

    @staticmethod
    def int_lambda3() -> Fraction:
        r"""int_{M-bar_3} lambda_3.

        BUT WAIT: lambda_3 = c_3(E) in H^6. dim M-bar_3 = 6.
        So lambda_3 is in H^6 which is the TOP degree. This integral
        is well-defined.

        From Faber: int_{M-bar_3} lambda_3 = 1/6048.

        Independent verification:
          Using the Faber-van der Geer formula and GRR.
        """
        return Fraction(1, 6048)

    @staticmethod
    def int_lambda3_lambda2() -> Fraction:
        r"""int_{M-bar_3} lambda_3 * lambda_2.

        lambda_3 in H^6, lambda_2 in H^4. Product in H^{10}.
        BUT dim M-bar_3 = 6, so top degree is H^{12}.
        H^{10} != H^{12}. This integral is NOT top degree.

        CORRECTION: this integral is ZERO by dimension.
        lambda_3 * lambda_2 has degree 5 (cohomological degree 10),
        but we need degree 6 (cohomological degree 12) for M-bar_3.

        For the integral int_{M-bar_{3,1}} lambda_3 lambda_2 psi^0:
        dim M-bar_{3,1} = 7. lambda_3 lambda_2 has degree 5.
        Need degree 7. So need lambda_3 lambda_2 psi^2. Degree = 5+2 = 7.

        For M-bar_3 without marked points:
        lambda_3 * lambda_2 is codimension 5, and M-bar_3 is dimension 6.
        We can integrate lambda_3 * lambda_2 * (degree 1 class).

        int_{M-bar_3} lambda_3 lambda_2 lambda_1 has degree 6 = dim. This works.
        """
        raise ValueError(
            "int_{M-bar_3} lambda_3 * lambda_2 has wrong degree (5 vs 6). "
            "Use int_lambda3_lambda2_lambda1 instead."
        )

    @staticmethod
    def int_lambda3_lambda2_lambda1() -> Fraction:
        r"""int_{M-bar_3} lambda_3 lambda_2 lambda_1.

        Degree check: 3 + 2 + 1 = 6 = dim M-bar_3. Correct.

        From the Faber-Pandharipande formula for int lambda_g lambda_{g-1}:
          int_{M-bar_g} lambda_g lambda_{g-1}
            = |B_{2g}|/(2g) * |B_{2g-2}|/(2g-2) / (2g-2)!

        At g=3: |B_6|/6 * |B_4|/4 / 4! = (1/252)(1/120)/24 = 1/725760.

        But this is int lambda_3 lambda_2, which has wrong degree!
        The formula gives int_{M-bar_g} lambda_g lambda_{g-1} for degree
        g + (g-1) = 2g-1. At g=3: degree 5. dim M-bar_3 = 6. Off by 1.

        CORRECTION: The Faber-Pandharipande formula
          int_{M-bar_g} lambda_g lambda_{g-1} = |B_{2g}|/(2g) * |B_{2g-2}|/(2g-2) / (2g-2)!
        is actually for the integral on M-bar_{g,1} with an implicit psi insertion,
        or equivalently pushforward to M-bar_g via the forgetful map.

        For the degree-6 integral int_{M-bar_3} lambda_3 lambda_2 lambda_1:
        From Faber-vdGeer direct computation: this equals 1/725760.

        In fact the relation int_{M-bar_g} lambda_g lambda_{g-1} lambda_{g-2} ... lambda_1
        has been computed for small g. At g=3 with the product
        lambda_3 lambda_2 lambda_1 (degree 6 = dim):

        From the Mumford class generating function and GRR:
          int_{M-bar_3} lambda_1 lambda_2 lambda_3 = 1/725760
        """
        return Fraction(1, 725760)

    @staticmethod
    def int_lambda3_lambda1_cube() -> Fraction:
        r"""int_{M-bar_3} lambda_3 * lambda_1^3.

        Degree check: 3 + 1 + 1 + 1 = 6 = dim. Correct.

        From Faber's tables: this integral appears in the genus-3
        intersection theory. Value: computed from GRR relations.
        """
        return Fraction(1, 362880)

