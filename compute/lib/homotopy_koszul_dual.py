def compute_truncated_poly(n: int, max_bar_degree: int = 6,
                           verbose: bool = False) -> Dict:
    """Compute bar cohomology and A-infinity structure for k[x]/(x^n).

    Returns dict with:
    - 'cohomology': {p: dim H_p(B(A))}
    - 'd_squared': {p: max|d^2|}
    - 'm3': description of m_3 operations
    - 'm4': description of m_4 operations
    """
    A = TruncatedPolynomialAlgebra(n)
    bar = BarComplex(A)

    # d^2 = 0 check
    d2_check = {}
    for p in range(2, max_bar_degree + 1):
        d2_check[p] = bar.verify_d_squared(p)

    # Cohomology
    coh = bar.cohomology_all(max_bar_degree)

    if verbose:
        print(f"\n=== k[x]/(x^{n}) ===")
        print(f"Aug ideal dim = {A.aug_ideal_dim}")
        for p in range(max_bar_degree + 1):
            dim_Bp = bar.basis_size(p)
            print(f"  B_{p}: dim = {dim_Bp}, H_{p} = {coh[p]}")
        print(f"  d^2 = 0 checks: {d2_check}")

    # A-infinity transfer (only if small enough)
    m3_data = {}
    m4_data = {}
    if n <= 5 and max_bar_degree >= 3:
        try:
            transfer = AInfinityTransfer(bar, min(max_bar_degree, 8))

            if verbose:
                print(f"  Cohomology dims from transfer: {transfer.H_dims}")

            # m3 on degree-1 classes
            if transfer.H_dims.get(1, 0) > 0:
                m3_111 = transfer.m3_tree_level(1, 1, 1)
                m3_data['(1,1,1)'] = {
                    'matrix': m3_111,
                    'nonzero': np.any(np.abs(m3_111) > 1e-10),
                    'target_deg': 2,
                }
                if verbose:
                    print(f"  m_3(H_1, H_1, H_1) -> H_2:")
                    print(f"    nonzero = {m3_data['(1,1,1)']['nonzero']}")
                    if m3_data['(1,1,1)']['nonzero']:
                        print(f"    matrix =\n{m3_111}")

                # m4 on degree-1 classes
                m4_1111 = transfer.m4_tree_level(1, 1, 1, 1)
                m4_data['(1,1,1,1)'] = {
                    'matrix': m4_1111,
                    'nonzero': np.any(np.abs(m4_1111) > 1e-10),
                    'target_deg': 2,
                }
                if verbose:
                    print(f"  m_4(H_1, H_1, H_1, H_1) -> H_2:")
                    print(f"    nonzero = {m4_data['(1,1,1,1)']['nonzero']}")
                    if m4_data['(1,1,1,1)']['nonzero']:
                        print(f"    matrix =\n{m4_1111}")

        except Exception as e:
            if verbose:
                print(f"  A-infinity transfer failed: {e}")

    return {
        'algebra': f'k[x]/(x^{n})',
        'n': n,
        'cohomology': coh,
        'd_squared': d2_check,
        'm3': m3_data,
        'm4': m4_data,
    }


def dual_numbers_verification():
    """Verify k[x]/(x^2) = k[epsilon] is Koszul.

    Bar cohomology should be k in each degree: H_p = 1 for all p.
    A! = k[y] (polynomial ring). All m_k = 0 for k >= 3.
    """
    return compute_truncated_poly(2, max_bar_degree=6, verbose=False)


def cubic_truncation():
    """Compute A-infinity structure for k[x]/(x^3).

    This is the simplest non-Koszul example. The algebra has basis
    {1, x, x^2} with x^3 = 0. The augmentation ideal I = span{x, x^2}
    has dim 2, so bar degree p has dim 2^p.

    Expected:
    - H_1 = 2 (generated by [x] and [x^2])
    - H_2: should have dim > 0 from [x|x] class (x*x = x^2 is nonzero,
      so [x|x] is NOT a cycle; but [x^2|x] - [x|x^2] might be)
    - Nontrivial m_3 should appear (Massey product)
    """
    return compute_truncated_poly(3, max_bar_degree=6, verbose=False)


