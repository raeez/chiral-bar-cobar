def admissible_sl2_analysis(verbose: bool = False) -> Dict:
    """Analyze L_{-1/2}(sl_2) at the admissible level k = -1/2.

    V_k(sl_2) is chirally Koszul for all k (PBW/universality).
    L_k(sl_2) = V_k(sl_2) / (null ideal) at admissible levels.

    For k = -1/2 (the simplest admissible level for sl_2):
    - h^v(sl_2) = 2, so k = -1/2 is admissible (k + h^v = 3/2)
    - The null vector appears at level 2: (e_{-1})^2 |0>
    - After quotienting, the simple module L_{-1/2}(sl_2) is a
      rational VOA (BPZ/WZW model)

    The question: is L_{-1/2}(sl_2) chirally Koszul?

    Key insight: the bar complex of L_k(sl_2) differs from that of
    V_k(sl_2) precisely by the ideal generated by null vectors.
    The bar cohomology of V_k(sl_2) is Koszul (concentrated) for
    all k. But the quotient may destroy this concentration.

    ANSWER (from the manuscript, prop:pbw-universality):
    V_k(g) is chirally Koszul for ALL k. The simple quotient L_k(g)
    may fail Koszulness at admissible levels due to vacuum null vectors.
    For L_{-1/2}(sl_2): the null vector at level 2 creates a new
    generator in bar degree 2, potentially breaking concentration.

    However, L_{-1/2}(sl_2) = Vir_{c=-1} (the Ising model Virasoro
    at c = 1/2... no, that's not right). Actually:
    L_{-1/2}(sl_2) is a rational VOA with 2 simple modules.

    The precise Koszulness status of L_k(g) at admissible levels
    is OPEN in general (see manuscript remarks).
    """
    result = {
        'algebra': 'L_{-1/2}(sl_2)',
        'level': -0.5,
        'h_dual': 2,
        'admissible': True,
        'universal_koszul': True,  # V_k(sl_2) is Koszul for all k
        'simple_quotient_koszul': 'OPEN',
        'null_vector_level': 2,
        'null_vector': '(e_{-1})^2 |0>',
        'analysis': (
            'V_{-1/2}(sl_2) is chirally Koszul (prop:pbw-universality). '
            'The simple quotient L_{-1/2}(sl_2) has a null vector at level 2, '
            'which introduces a new relation in the bar complex. Whether this '
            'breaks bar cohomology concentration is an open question for this '
            'specific admissible level. The PBW criterion applies only to the '
            'universal algebra V_k(g), not to simple quotients.'
        ),
    }

    if verbose:
        print(f"\n=== Admissible sl_2 analysis ===")
        for key, val in result.items():
            print(f"  {key}: {val}")

    return result


