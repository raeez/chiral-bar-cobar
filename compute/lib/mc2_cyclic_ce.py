"""Cyclic Chevalley-Eilenberg cohomology for semisimple Lie algebras.

Implements the CE cochain complex C*(g, g) with adjoint coefficients,
its cyclic subcomplex C*_cyc(g, g), and cohomology computation.

This is MC2 Step 1 infrastructure: the cyclic deformation complex
of a semisimple Lie algebra g is the prototype for the cyclic
coderivation complex of the chiral bar construction.

Key results:
    H^n_cyc(g, g) = C  if n = 2,   generated by Killing 3-cocycle
                   = 0  otherwise
    for any simple Lie algebra g.

The Killing 3-cocycle phi(a,b,c) = kap([a,b], c) is the UNIQUE cyclic
infinitesimal deformation, corresponding to the l_3 bracket in the
cyclic L-infinity model of mc2_cyclic_linf.py.

Mathematical identification: H^n_cyc(g, g) = H^{n+1}(g) (ordinary CE
cohomology with trivial coefficients, degree-shifted by 1), via the
isomorphism C^n_cyc(g, g) = Lambda^{n+1}(g*) induced by the invariant form.
"""

from fractions import Fraction
from functools import lru_cache
from typing import Dict, List, Optional, Tuple

import numpy as np


# ---------- exact linear algebra helpers ----------

def _exact_rank(M: np.ndarray) -> int:
    """Rank of a rational matrix via row echelon form."""
    M = np.array(M, dtype=object).copy()
    rows, cols = M.shape
    pivot_row = 0
    for col in range(cols):
        found = False
        for row in range(pivot_row, rows):
            if M[row, col] != 0:
                M[[pivot_row, row]] = M[[row, pivot_row]]
                found = True
                break
        if not found:
            continue
        pv = M[pivot_row, col]
        for c2 in range(cols):
            M[pivot_row, c2] = Fraction(M[pivot_row, c2], pv)
        for row in range(rows):
            if row != pivot_row and M[row, col] != 0:
                factor = M[row, col]
                for c2 in range(cols):
                    M[row, c2] -= factor * M[pivot_row, c2]
        pivot_row += 1
    return pivot_row


def _modular_rank(M: np.ndarray, p: int) -> int:
    """Rank over F_p for an integer matrix."""
    rows, cols = M.shape
    A = [[int(M[i, j]) % p for j in range(cols)] for i in range(rows)]
    pivot_row = 0
    for col in range(cols):
        pivot = None
        for row in range(pivot_row, rows):
            if A[row][col] % p:
                pivot = row
                break
        if pivot is None:
            continue
        A[pivot_row], A[pivot] = A[pivot], A[pivot_row]
        inv = pow(A[pivot_row][col], -1, p)
        for c2 in range(col, cols):
            A[pivot_row][c2] = (A[pivot_row][c2] * inv) % p
        for row in range(rows):
            if row == pivot_row:
                continue
            factor = A[row][col] % p
            if factor == 0:
                continue
            for c2 in range(col, cols):
                A[row][c2] = (A[row][c2] - factor * A[pivot_row][c2]) % p
        pivot_row += 1
        if pivot_row == rows:
            break
    return pivot_row


def _integer_rank_via_primes(
    M: np.ndarray,
    primes: Tuple[int, ...] = (101, 103, 107, 109, 113),
) -> int:
    """Rank over Q for an integral matrix, estimated deterministically via primes.

    For integer matrices, the rank over Q agrees with the rank over F_p for all
    but finitely many primes. We use several fixed primes and take the maximum;
    if the entries are not integral, we fall back to exact rational elimination.
    """
    rows, cols = M.shape
    if rows == 0 or cols == 0:
        return 0
    for i in range(rows):
        for j in range(cols):
            value = M[i, j]
            if isinstance(value, Fraction):
                if value.denominator != 1:
                    return _exact_rank(M)
            elif int(value) != value:
                return _exact_rank(M)
    return max(_modular_rank(M, p) for p in primes)


def _null_space(M: np.ndarray) -> List[np.ndarray]:
    """Null space basis of a rational matrix."""
    M = np.array(M, dtype=object).copy()
    rows, cols = M.shape
    pivot_cols: List[int] = []
    pivot_row = 0
    for col in range(cols):
        found = False
        for row in range(pivot_row, rows):
            if M[row, col] != 0:
                M[[pivot_row, row]] = M[[row, pivot_row]]
                found = True
                break
        if not found:
            continue
        pivot_cols.append(col)
        pv = M[pivot_row, col]
        for c2 in range(cols):
            M[pivot_row, c2] = Fraction(M[pivot_row, c2], pv)
        for row in range(rows):
            if row != pivot_row and M[row, col] != 0:
                factor = M[row, col]
                for c2 in range(cols):
                    M[row, c2] -= factor * M[pivot_row, c2]
        pivot_row += 1
    free_cols = [j for j in range(cols) if j not in pivot_cols]
    null_vecs = []
    for fc in free_cols:
        v = np.zeros(cols, dtype=object)
        for i in range(cols):
            v[i] = Fraction(0)
        v[fc] = Fraction(1)
        for i, pc in enumerate(pivot_cols):
            v[pc] = -M[i, fc]
        null_vecs.append(v)
    return null_vecs


def _solve_in_basis(
    basis_mat: np.ndarray,
    target: np.ndarray,
    ambient_dim: int,
    basis_dim: int,
) -> np.ndarray:
    """Express target in given basis via row reduction of augmented matrix."""
    aug = np.zeros((ambient_dim, basis_dim + 1), dtype=object)
    for r in range(ambient_dim):
        for cc in range(basis_dim):
            aug[r, cc] = basis_mat[r, cc]
        aug[r, basis_dim] = target[r]
    pr = 0
    for col in range(basis_dim):
        found = False
        for row in range(pr, ambient_dim):
            if aug[row, col] != 0:
                aug[[pr, row]] = aug[[row, pr]]
                found = True
                break
        if not found:
            continue
        pv = aug[pr, col]
        for c2 in range(basis_dim + 1):
            aug[pr, c2] = Fraction(aug[pr, c2], pv)
        for row in range(ambient_dim):
            if row != pr and aug[row, col] != 0:
                fac = aug[row, col]
                for c2 in range(basis_dim + 1):
                    aug[row, c2] -= fac * aug[pr, c2]
        pr += 1
    coords = np.zeros(basis_dim, dtype=object)
    for j in range(basis_dim):
        coords[j] = aug[j, basis_dim]
    return coords


