    _set(0, 8, 7, -2)
    # [e1, f22] = -f12
    _set(0, 9, 8, -1)
    # [e2, h1] = 2*e2
    _set(1, 4, 1, 2)
    # [e2, h2] = -2*e2
    _set(1, 5, 1, -2)
    # [e2, f2] = h2
    _set(1, 7, 5, 1)
    # [e2, f12] = f1
    _set(1, 8, 6, 1)
    # [e12, h2] = -e12
    _set(2, 5, 2, -1)
