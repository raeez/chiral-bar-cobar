def enumerate_dominant_weights(rank: int, max_total: int) -> List[Tuple[int, ...]]:
    """All dominant weights (a_1,...,a_rank) with sum(a_i) <= max_total, excluding (0,...,0)."""
    result: List[Tuple[int, ...]] = []
    _enum_rec(rank, max_total, [], result)
    return result


def _enum_rec(rank: int, remaining: int,
              partial: List[int], result: List[Tuple[int, ...]]) -> None:
    """Recursive enumeration."""
    if len(partial) == rank:
        if any(c > 0 for c in partial):
            result.append(tuple(partial))
        return
    for c in range(remaining + 1):
        _enum_rec(rank, remaining - c, partial + [c], result)


def count_dominant_weights(rank: int, max_total: int) -> int:
    """Count dominant weights with sum <= max_total, excluding trivial."""
    # C(max_total + rank, rank) - 1  (stars and bars minus the zero weight)
    return _binomial(max_total + rank, rank) - 1


def _binomial(n: int, k: int) -> int:
    """Binomial coefficient."""
    if k < 0 or k > n:
        return 0
    return math.comb(n, k)


# =========================================================================
# 3. Categorical zeta: zeta^{DK}_{sl_N}(s) = sum dim(V_lambda)^{-s}
# =========================================================================

def categorical_zeta_slN(N: int, s: complex,
                         max_total: int = 20,
                         include_trivial: bool = True) -> complex:
    r"""Categorical zeta for sl_N.

    zeta^{DK}_{sl_N}(s) = sum_{lambda nontrivial dominant} dim(V_lambda)^{-s}

    If include_trivial, adds 1^{-s} = 1 for the trivial rep.

    For sl_2 with include_trivial=True: this equals zeta(s).
    """
    rank = N - 1
    if rank <= 0:
        return complex(1) if include_trivial else complex(0)

    result = complex(1) if include_trivial else complex(0)
    weights = enumerate_dominant_weights(rank, max_total)
    for hw in weights:
        d = weyl_dim_slN(N, hw)
        if d > 0:
            result += d ** (-s)
    return result


def categorical_zeta_sl2(s: complex, N_terms: int = 1000) -> complex:
    r"""Categorical zeta for sl_2 = sum_{n>=1} n^{-s} = zeta(s).

    Uses direct summation.  Includes trivial (dim=1) automatically
    since dim(V_0) = 1.
    """
    return sum(n ** (-s) for n in range(1, N_terms + 1))


def categorical_zeta_sl3(s: complex, max_total: int = 30) -> complex:
    r"""Categorical zeta for sl_3 using the closed-form dimension.

    zeta^{DK}_{sl_3}(s) = 1 + sum_{(a,b)!=(0,0)} [(a+1)(b+1)(a+b+2)/2]^{-s}

    Includes trivial.
    """
    result = complex(1)  # trivial
    for total in range(1, max_total + 1):
        for a in range(total + 1):
            b = total - a
            d = weyl_dim_sl3(a, b)
            result += d ** (-s)
    return result


def categorical_zeta_sl4(s: complex, max_total: int = 15) -> complex:
    r"""Categorical zeta for sl_4 using closed-form dimension."""
    result = complex(1)  # trivial
    for total in range(1, max_total + 1):
        for a in range(total + 1):
            for b in range(total - a + 1):
                c = total - a - b
                d = weyl_dim_sl4(a, b, c)
                result += d ** (-s)
    return result


