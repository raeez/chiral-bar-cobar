# 15. Critical line evaluation (for Riemann zeta comparison on sl_2)
# =========================================================================

def critical_line_sl2(t_values: Optional[List[float]] = None,
                      N_terms: int = 5000) -> List[Dict[str, Any]]:
    r"""Evaluate zeta^{DK}_{sl_2}(1/2 + it) along the critical line.

    Since zeta^{DK}_{sl_2} = zeta, this is the Riemann zeta on Re(s) = 1/2.
    The Riemann Hypothesis asserts all nontrivial zeros lie here.

    We compute partial sums (which do NOT converge for Re(s) = 1/2, but
    the oscillatory behaviour is informative).
    """
    if t_values is None:
        t_values = [0.0, 5.0, 10.0, 14.13, 14.14, 20.0, 21.02, 25.01, 30.0]

    results = []
    for t in t_values:
        s = complex(0.5, t)
        # Partial sum (NOT convergent, but shows oscillatory structure)
        z_partial = sum(n ** (-s) for n in range(1, N_terms + 1))
        results.append({
            't': t,
            's': s,
            'partial_sum': z_partial,
            'abs_partial': abs(z_partial),
        })
    return results


