# 1. Weyl dimension formula for sl_N (direct product formula)
# =========================================================================

@lru_cache(maxsize=2048)
def weyl_dim_slN(N: int, hw: Tuple[int, ...]) -> int:
    r"""Dimension of V_lambda for sl_N via the direct product formula.

    For sl_N, the positive roots are e_i - e_j for 1 <= i < j <= N.
    The Weyl dimension formula becomes:

        dim V_lambda = prod_{1 <= i < j <= N} (mu_i - mu_j) / (j - i)

    where mu = lambda + rho with rho = (N-1, N-2, ..., 1, 0) and
    lambda is in partition form: lambda_i = sum_{k=i}^{N-1} a_k
    for fundamental weight coordinates (a_1, ..., a_{N-1}).

    Args:
        N: the N in sl_N (N >= 2)
        hw: highest weight in fundamental weight coordinates (length N-1)

    Returns:
        Dimension as a positive integer.
    """
    if len(hw) != N - 1:
        raise ValueError(f"sl_{N} requires {N-1} weight coords, got {len(hw)}")
    if any(c < 0 for c in hw):
        return 0

    # Convert fundamental weight coords to partition form
    # lambda_i = sum_{k=i}^{N-1} a_k  for i = 1, ..., N-1; lambda_N = 0
    lam = [0] * N
    for i in range(N - 1):
        lam[i] = sum(hw[k] for k in range(i, N - 1))

    # rho = (N-1, N-2, ..., 1, 0)
    # mu = lambda + rho
    mu = [lam[i] + (N - 1 - i) for i in range(N)]

    # dim = prod_{i<j} (mu_i - mu_j) / (j - i)
    dim = Fraction(1)
    for i in range(N):
        for j in range(i + 1, N):
            dim *= Fraction(mu[i] - mu[j], j - i)

    result = int(dim)
    assert result > 0, f"Dimension must be positive, got {result} for sl_{N}, hw={hw}"
    return result


def weyl_dim_sl2(n: int) -> int:
    """dim V_n = n + 1 for sl_2."""
    return n + 1


def weyl_dim_sl3(a: int, b: int) -> int:
    """dim V(a,b) = (a+1)(b+1)(a+b+2)/2 for sl_3."""
    if a < 0 or b < 0:
        return 0
    return (a + 1) * (b + 1) * (a + b + 2) // 2


def weyl_dim_sl4(a: int, b: int, c: int) -> int:
    r"""dim V(a,b,c) for sl_4 via closed-form product.

    The 6 positive roots of A_3 give:

    dim V(a,b,c) = (a+1)(b+1)(c+1)(a+b+2)(b+c+2)(a+b+c+3) / 12

    Verification: 6 positive roots, so the product has 6 factors.
    """
    if a < 0 or b < 0 or c < 0:
        return 0
    num = ((a + 1) * (b + 1) * (c + 1)
           * (a + b + 2) * (b + c + 2)
           * (a + b + c + 3))
    # Denominator = prod_{1<=i<j<=4} (j-i) = 1*2*3*1*2*1 = 12
    return num // 12


def weyl_dim_sl5(a: int, b: int, c: int, d: int) -> int:
    r"""dim V(a,b,c,d) for sl_5 via closed-form product.

    The 10 positive roots of A_4 give a product of 10 terms in the numerator.
    Denominator = prod_{1<=i<j<=5} (j-i) = 1^4 * 2^3 * 3^2 * 4 = 1*8*9*4 = 288.

    Actually: prod_{1<=i<j<=5} (j-i) = product of (j-i) for each pair:
    (1,2):1, (1,3):2, (1,4):3, (1,5):4,
    (2,3):1, (2,4):2, (2,5):3,
    (3,4):1, (3,5):2,
    (4,5):1
    = 1*2*3*4*1*2*3*1*2*1 = 288
    """
    if a < 0 or b < 0 or c < 0 or d < 0:
        return 0
    # mu_i = lam_i + rho_i where rho = (4,3,2,1,0)
    # lam in partition: lam_1 = a+b+c+d, lam_2 = b+c+d, lam_3 = c+d, lam_4 = d, lam_5 = 0
    mu = [a + b + c + d + 4, b + c + d + 3, c + d + 2, d + 1, 0]
    num = 1
    for i in range(5):
        for j in range(i + 1, 5):
            num *= (mu[i] - mu[j])
    # Denominator
    den = 1
    for i in range(5):
        for j in range(i + 1, 5):
            den *= (j - i)
    return num // den


