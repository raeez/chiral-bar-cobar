def multipath_weyl_dim_sl3(a: int, b: int) -> Dict[str, Any]:
    r"""3-path verification of sl_3 Weyl dimension.

    Path 1: Closed formula (a+1)(b+1)(a+b+2)/2
    Path 2: General product formula via weyl_dim_slN
    Path 3: Character at q=1 limit (hook-content formula)
    """
    path1 = weyl_dim_sl3(a, b)
    path2 = weyl_dim_slN(3, (a, b))

    # Path 3: direct product formula
    # mu = (a+b+2, b+1, 0), denominators (j-i): (1,2,1)
    mu = [a + b + 2, b + 1, 0]
    path3 = 1
    for i in range(3):
        for j in range(i + 1, 3):
            path3 *= (mu[i] - mu[j])
    path3 //= (1 * 2 * 1)

    return {
        'a': a, 'b': b,
        'path1_formula': path1,
        'path2_general': path2,
        'path3_direct_product': path3,
        'all_agree': path1 == path2 == path3,
    }


def multipath_weyl_dim_sl4(a: int, b: int, c: int) -> Dict[str, Any]:
    r"""3-path verification of sl_4 Weyl dimension.

    Path 1: Closed formula
    Path 2: General product formula via weyl_dim_slN
    Path 3: Direct mu-product computation
    """
    path1 = weyl_dim_sl4(a, b, c)
    path2 = weyl_dim_slN(4, (a, b, c))

    # Path 3: direct
    mu = [a + b + c + 3, b + c + 2, c + 1, 0]
    num = 1
    den = 1
    for i in range(4):
        for j in range(i + 1, 4):
            num *= (mu[i] - mu[j])
            den *= (j - i)
    path3 = num // den

    return {
        'a': a, 'b': b, 'c': c,
        'path1_formula': path1,
        'path2_general': path2,
        'path3_direct_product': path3,
        'all_agree': path1 == path2 == path3,
    }


def multipath_categorical_zeta_sl2(s: float) -> Dict[str, Any]:
    r"""5-path verification for sl_2 categorical zeta.

    Path 1: Direct rep counting via weyl_dim_slN
    Path 2: sum n^{-s} (explicit integer sum)
    Path 3: Euler-Maclaurin
    Path 4: Euler product
    Path 5: Known values (s=2 -> pi^2/6, s=4 -> pi^4/90)
    """
    N_terms = 5000

    path1 = categorical_zeta_sl2(s, N_terms)
    path2 = sum(n ** (-s) for n in range(1, N_terms + 1))
    path3 = riemann_zeta_em(s)
    path4 = _euler_product_partial(s, 200)

    path5_exact = None
    if s == 2.0:
        path5_exact = PI_SQ / 6
    elif s == 4.0:
        path5_exact = PI ** 4 / 90
    elif s == 6.0:
        path5_exact = PI ** 6 / 945
    elif s == 8.0:
        path5_exact = PI ** 8 / 9450
    elif s == 10.0:
        path5_exact = PI ** 10 / 93555

    return {
        's': s,
        'path1_rep_counting': path1,
        'path2_integer_sum': path2,
        'path3_euler_maclaurin': path3.real,
        'path4_euler_product': path4.real,
        'path5_exact': path5_exact,
        'max_diff_12': abs(path1 - path2),
        'max_diff_13': abs(path1 - path3.real),
    }


def _euler_product_partial(s: complex, n_primes: int) -> complex:
    """Partial Euler product of the Riemann zeta."""
    primes = _sieve(n_primes)
    result = complex(1)
    for p in primes:
        result *= 1.0 / (1.0 - p ** (-s))
    return result


def _sieve(count: int) -> List[int]:
    """First `count` primes."""
    if count <= 0:
        return []
    if count < 6:
        limit = 15
    else:
        limit = int(count * (math.log(count) + math.log(math.log(count)) + 2)) + 10
    sieve = [True] * (limit + 1)
    sieve[0] = sieve[1] = False
    for i in range(2, int(limit ** 0.5) + 1):
        if sieve[i]:
            for j in range(i * i, limit + 1, i):
                sieve[j] = False
    return [i for i in range(2, limit + 1) if sieve[i]][:count]


