# 18. Representation density and Weyl law
# =========================================================================

def representation_density_slN(N: int, D_max: int,
                               max_total: int = 40) -> Dict[str, Any]:
    r"""Count irreps with dimension <= D_max and estimate the growth rate.

    The Weyl law predicts: #{irreps with dim <= D} ~ C * D^{r/|Phi^+|}
    where r = N-1 (rank) and |Phi^+| = N(N-1)/2.
    Exponent = r/|Phi^+| = (N-1)/(N(N-1)/2) = 2/N.

    For sl_2: exponent = 1 (linear growth, matching D positive integers <= D).
    For sl_3: exponent = 2/3.
    For sl_4: exponent = 1/2.
    """
    rank = N - 1
    n_pos_roots = num_positive_roots(N)
    predicted_exponent = rank / n_pos_roots  # = 2/N

    # Count by enumeration
    count = 0
    for total in range(0, max_total + 1):
        weights: List[Tuple[int, ...]] = []
        _enum_exact(rank, total, [], weights)
        for hw in weights:
            d = weyl_dim_slN(N, hw)
            if d <= D_max:
                count += 1
        # Check if all dims at this total exceed D_max
        if total > 1:
            all_big = all(
                weyl_dim_slN(N, tuple(total if i == j else 0 for i in range(rank))) > D_max
                for j in range(rank)
            )
            if all_big:
                break

    count += 1  # include trivial

    # Estimate exponent from count ~ C * D_max^alpha
    alpha_est = math.log(count) / math.log(D_max) if D_max > 1 and count > 1 else None

    return {
        'N': N,
        'D_max': D_max,
        'count': count,
        'predicted_exponent': predicted_exponent,
        'estimated_exponent': alpha_est,
    }


