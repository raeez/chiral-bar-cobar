# 6. Euler product analysis
# =========================================================================

def has_euler_product(N: int, s: float, max_total: int = 15,
                     max_prime: int = 50) -> Dict[str, Any]:
    r"""Test whether zeta^{DK}_{sl_N}(s) admits an Euler product.

    For sl_2: YES (= Riemann zeta).
    For sl_N, N >= 3: NO in the classical sense (Weyl dimensions are
    not multiplicative under tensor product).

    Test: compute zeta^{DK}(s) as a Dirichlet series sum_d a_d * d^{-s}
    and check if a_d is a MULTIPLICATIVE function (a_{mn} = a_m * a_n
    for gcd(m,n) = 1).
    """
    max_product = max_prime * max_prime
    coeffs = dirichlet_coefficients_slN(N, max_product, max_total=max_total)

    multiplicative_failures = []
    for d1 in range(2, max_prime):
        for d2 in range(d1, max_prime):
            if math.gcd(d1, d2) != 1:
                continue
            prod = d1 * d2
            a_d1 = coeffs.get(d1, 0)
            a_d2 = coeffs.get(d2, 0)
            a_prod = coeffs.get(prod, 0)
            if a_d1 * a_d2 != a_prod:
                multiplicative_failures.append({
                    'd1': d1, 'd2': d2, 'product': prod,
                    'a_d1': a_d1, 'a_d2': a_d2, 'a_prod': a_prod,
                    'expected': a_d1 * a_d2,
                })

    return {
        'N': N,
        'is_multiplicative': len(multiplicative_failures) == 0,
        'n_failures': len(multiplicative_failures),
        'first_failures': multiplicative_failures[:10],
    }


