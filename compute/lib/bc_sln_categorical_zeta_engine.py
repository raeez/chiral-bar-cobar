# 19. sl_N dimension spectrum analysis
# =========================================================================

def dimension_gaps_slN(N: int, max_dim: int = 100,
                       max_total: int = 0) -> Dict[str, Any]:
    r"""Find gaps in the dimension spectrum of sl_N.

    For sl_2: NO gaps (every positive integer is a dimension).
    For sl_N, N >= 3: there are integers that are NOT dimensions of any
    irreducible representation.
    """
    if max_total <= 0:
        # For sl_N, the fundamental rep has dim N at total weight 1.
        # The max dim at total weight k grows as ~k^{N(N-1)/2}.
        # So to reach max_dim, we need roughly total ~ max_dim^{2/N(N-1)}.
        # For sl_2: total ~ max_dim (linear). Use max_dim itself.
        max_total = max(50, max_dim)
    coeffs = dirichlet_coefficients_slN(N, max_dim, max_total=max_total)
    dims_present = set(coeffs.keys())
    gaps = [d for d in range(1, max_dim + 1) if d not in dims_present]

    return {
        'N': N,
        'max_dim': max_dim,
        'n_dims_present': len(dims_present),
        'n_gaps': len(gaps),
        'gap_density': len(gaps) / max_dim,
        'first_gaps': gaps[:20],
        'has_gaps': len(gaps) > 0,
    }


def max_multiplicity_slN(N: int, max_dim: int = 200) -> Dict[str, Any]:
    r"""Find the maximum Dirichlet coefficient a_d for sl_N.

    For sl_2: max a_d = 1 (every dimension occurs exactly once).
    For sl_N, N >= 3: multiplicities grow.
    """
    coeffs = dirichlet_coefficients_slN(N, max_dim)
    if not coeffs:
        return {'N': N, 'max_mult': 0, 'argmax': None}

    max_d = max(coeffs, key=coeffs.get)
    return {
        'N': N,
        'max_multiplicity': coeffs[max_d],
        'argmax_dim': max_d,
        'sample': {d: coeffs[d] for d in sorted(coeffs)[:30]},
    }


