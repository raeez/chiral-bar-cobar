# 13. Witten zeta special values
# =========================================================================

def witten_volume(N: int, genus: int,
                  max_total: int = 20) -> complex:
    r"""Witten zeta at s = 2g - 2 (the "volume" of moduli of flat bundles).

    Z_W(sl_N, 2g-2) = sum_{lambda nontrivial} dim(V_lambda)^{-(2g-2)}

    For g >= 2, this converges provided 2g-2 > sigma_c = 2/N, i.e., g > 1/N + 1.
    For all N >= 2 and g >= 2, this holds.

    The volume of the moduli space of flat SU(N) connections on Sigma_g
    is proportional to Z_W(sl_N, 2g-2).  Exact computation from Witten's
    formula (1991).
    """
    s = 2 * genus - 2
    return witten_zeta_slN(N, s, max_total=max_total)


def witten_volume_sl2_exact(genus: int) -> float:
    r"""Exact Witten volume for sl_2.

    Vol(M_{flat}(Sigma_g, SU(2))) = 2 * (2*pi^2)^{g-1} * zeta(2g-2)/(2g-2)!
    ... actually the formula involves Bernoulli numbers.

    Z_W(sl_2, 2g-2) = zeta(2g-2) - 1 (excluding the trivial rep).
    = sum_{n>=2} n^{-(2g-2)}

    Using zeta(2k) = (-1)^{k+1} B_{2k} (2pi)^{2k} / (2*(2k)!) for k >= 1:
    zeta(2) = pi^2/6, zeta(4) = pi^4/90, zeta(6) = pi^6/945, ...
    """
    s = 2 * genus - 2
    if s <= 0:
        return float('inf')
    # Use Euler-Maclaurin for high accuracy
    z = riemann_zeta_em(float(s))
    return z.real - 1.0  # exclude trivial


