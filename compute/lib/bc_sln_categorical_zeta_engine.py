# 4. Abscissa of convergence
# =========================================================================

def abscissa_of_convergence(N: int) -> float:
    r"""Theoretical abscissa of convergence sigma_c for zeta^{DK}_{sl_N}(s).

    sigma_c = 2/N.

    Derivation: rank r = N-1. Number of positive roots |Phi^+| = N(N-1)/2.
    Number of dominant weights with total <= k is C(k+r-1,r-1) ~ k^{r-1}/(r-1)!.
    Typical dim at total k is ~ k^{|Phi^+|}.
    Series ~ sum_k k^{r-1} * k^{-|Phi^+|*s}, converges for
    r-1 - |Phi^+|*s < -1, i.e., s > r/|Phi^+| = (N-1)/(N(N-1)/2) = 2/N.
    """
    return 2.0 / N


def numerical_abscissa_estimate(N: int, max_total: int = 20,
                                s_values: Optional[List[float]] = None) -> Dict[str, Any]:
    r"""Numerical estimate of sigma_c by checking partial sum growth.

    For s > sigma_c the partial sums converge; for s < sigma_c they diverge.
    We check a grid of s values and find the transition.
    """
    if s_values is None:
        s_values = [0.1 * k for k in range(1, 31)]  # 0.1 to 3.0

    sigma_theory = abscissa_of_convergence(N)

    results = {}
    for s in s_values:
        z1 = categorical_zeta_slN(N, s, max_total=max_total // 2, include_trivial=False)
        z2 = categorical_zeta_slN(N, s, max_total=max_total, include_trivial=False)
        ratio = abs(z2 / z1) if abs(z1) > 1e-30 else float('inf')
        results[s] = {
            'z_half': z1,
            'z_full': z2,
            'ratio': ratio,
            'converging': ratio < 1.5,
        }

    return {
        'sigma_theory': sigma_theory,
        'N': N,
        'results': results,
    }


# =========================================================================
# 5. Dirichlet coefficients: a_d = #{irreps of dimension d}
# =========================================================================

def dirichlet_coefficients_slN(N: int, max_dim: int,
                               max_total: int = 50) -> Dict[int, int]:
    r"""Compute a_d = number of irreps of sl_N with dimension d.

    For sl_2: a_d = 1 for all d >= 1 (each dim occurs exactly once).
    For sl_3: a_d >= 1 can occur (e.g., a_3 = 2 from fund + antifund).
    """
    coeffs: Dict[int, int] = {}
    # Trivial
    coeffs[1] = 1

    rank = N - 1
    for total in range(1, max_total + 1):
        weights_at_total: List[Tuple[int, ...]] = []
        # Enumerate weights with sum == total
        _enum_exact(rank, total, [], weights_at_total)
        all_big = True
        for hw in weights_at_total:
            d = weyl_dim_slN(N, hw)
            if d <= max_dim:
                all_big = False
                coeffs[d] = coeffs.get(d, 0) + 1
        if total > 2 and all_big and len(weights_at_total) > 0:
            break

    return coeffs


def _enum_exact(rank: int, total: int,
                partial: List[int], result: List[Tuple[int, ...]]) -> None:
    """Enumerate weights with sum == total (exact)."""
    if len(partial) == rank - 1:
        c = total - sum(partial)
        if c >= 0:
            result.append(tuple(partial + [c]))
        return
    for c in range(total - sum(partial) + 1):
        _enum_exact(rank, total, partial + [c], result)


def dimension_spectrum_slN(N: int, max_dim: int) -> List[Tuple[int, int]]:
    """Sorted list of (dim, multiplicity) for sl_N irreps."""
    coeffs = dirichlet_coefficients_slN(N, max_dim)
    return sorted(coeffs.items())


def sl2_dirichlet_coefficient(d: int) -> int:
    """For sl_2, a_d = 1 for all d >= 1."""
    return 1 if d >= 1 else 0


