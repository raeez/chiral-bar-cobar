# 9. Large-N limit ('t Hooft expansion)
# =========================================================================

def large_N_normalized_zeta(N: int, s: float,
                            max_total: int = 10) -> Dict[str, Any]:
    r"""Normalized categorical zeta in the large-N limit.

    As N -> infinity:
    - The number of representations grows super-exponentially.
    - The abscissa sigma_c = 2/N -> 0.
    - The relevant scaling is s ~ alpha/N for fixed alpha.

    We compute:
        Z_N(alpha) = zeta^{DK}_{sl_N}(alpha/N) * N^{-beta}

    for various normalization exponents beta, seeking a finite limit.

    The 't Hooft scaling: in gauge theory, the relevant parameter is
    lambda = g^2 * N (the 't Hooft coupling).  The dimension of the
    adjoint is N^2 - 1 ~ N^2, and the number of representations of
    weight <= k is ~ k^{N-1}.  So the "natural" normalization involves
    exponential growth in N.
    """
    alpha = s  # Interpret s as the scaled parameter alpha = N*s_physical
    s_physical = alpha / N
    sigma_c = abscissa_of_convergence(N)

    if s_physical <= sigma_c + 0.01:
        # Below or near abscissa: partial sum unreliable
        return {
            'N': N, 'alpha': alpha, 's_physical': s_physical,
            'sigma_c': sigma_c, 'status': 'below_abscissa',
            'zeta_value': None,
        }

    z = categorical_zeta_slN(N, s_physical, max_total=max_total, include_trivial=True)

    # Log-normalization
    log_z = cmath.log(z) if abs(z) > 1e-50 else complex(float('-inf'))

    return {
        'N': N, 'alpha': alpha, 's_physical': s_physical,
        'sigma_c': sigma_c, 'status': 'computed',
        'zeta_value': z,
        'log_zeta': log_z,
        'log_zeta_per_N_sq': log_z / N ** 2 if abs(z) > 1e-50 else None,
    }


# =========================================================================
# 10. Zeta regularization at negative s
# =========================================================================

def regularized_zeta_sl2_negative(s_neg: int) -> Fraction:
    r"""Riemann zeta at negative integers (exact).

    zeta(-n) = -B_{n+1}/(n+1) for n >= 0
    where B_k is the k-th Bernoulli number.

    zeta(0) = -1/2, zeta(-1) = -1/12, zeta(-2) = 0,
    zeta(-3) = 1/120, zeta(-4) = 0, zeta(-5) = -1/252, ...
    """
    n = -s_neg
    if n < 0:
        raise ValueError(f"Expected non-positive s, got {s_neg}")
    return (-1) ** n * _bernoulli(n + 1) / (n + 1)


@lru_cache(maxsize=128)
def _bernoulli(n: int) -> Fraction:
    """n-th Bernoulli number B_n (B_1 = -1/2 convention)."""
    B = [Fraction(0)] * (n + 1)
    B[0] = Fraction(1)
    for m in range(1, n + 1):
        B[m] = Fraction(0)
        for k in range(m):
            B[m] -= math.comb(m + 1, k) * B[k]
        B[m] /= (m + 1)
    return B[n]


def regularized_negative_slN(N: int, s_neg: int,
                             max_total: int = 25) -> Dict[str, Any]:
    r"""Compute zeta^{DK}_{sl_N}(s) at negative integer s via truncated sum.

    For sl_2: compare with exact Bernoulli formula.
    For sl_N >= 3: the sum is finite (only finitely many terms with
    dim^{-s} = dim^{|s|} that don't overflow), so the partial sum is an
    approximation to a regularized value.

    The "regularized value" for a general Dirichlet series sum a_n n^{-s}
    at negative s is obtained via analytic continuation, which generally
    requires the Dirichlet series to have a meromorphic continuation.
    For sl_N >= 3, whether zeta^{DK}_{sl_N} has meromorphic continuation
    is a deep open question.

    Here we compute the TRUNCATED sum and flag it as such.
    """
    s = complex(s_neg)
    z_trunc = categorical_zeta_slN(N, s, max_total=max_total, include_trivial=True)

    result: Dict[str, Any] = {
        'N': N,
        's': s_neg,
        'truncated_sum': z_trunc,
        'max_total': max_total,
    }

    if N == 2:
        exact = regularized_zeta_sl2_negative(s_neg)
        result['exact_bernoulli'] = float(exact)
        result['exact_fraction'] = str(exact)

    return result


# =========================================================================
# 11. Embedding sl_{N-1} -> sl_N
# =========================================================================

def embedding_test(N: int, s: float,
                   max_total: int = 12) -> Dict[str, Any]:
    r"""Test the embedding: every sl_{N-1} irrep restricts to sl_N irreps.

    More precisely: sl_{N-1} embeds in sl_N via the block embedding
    (acting on the first N-1 coordinates).  Under restriction, an sl_N
    irrep V_lambda decomposes into sl_{N-1} irreps.  This gives an
    INCLUSION of dimension spectra, but NOT a factorization of the zeta.

    What we test: is zeta^{DK}_{sl_N}(s) >= zeta^{DK}_{sl_{N-1}}(s) ?
    This should hold because sl_N has MORE representations (in the sense
    of having a larger dimension spectrum).

    Actually, this is NOT obviously true because the sl_N categorical
    zeta counts sl_N irreps by their sl_N dimension, not their
    restriction to sl_{N-1}.  But empirically it should hold because
    sl_N fundamental has dim N > N-1 = dim of sl_{N-1} fundamental,
    so the smaller dimensions contribute more to the zeta.
    """
    if N <= 2:
        return {'N': N, 'status': 'N too small for embedding test'}

    z_N = categorical_zeta_slN(N, s, max_total=max_total, include_trivial=True)
    z_Nm1 = categorical_zeta_slN(N - 1, s, max_total=max_total, include_trivial=True)

    return {
        'N': N,
        's': s,
        'zeta_slN': z_N,
        'zeta_slNm1': z_Nm1,
        'slN_geq_slNm1': z_N.real >= z_Nm1.real,
        'ratio': z_N / z_Nm1 if abs(z_Nm1) > 1e-30 else None,
    }


# =========================================================================
# 12. Asymptotic analysis
# =========================================================================

def asymptotic_leading_term(N: int, s: float,
                            max_total: int = 20) -> Dict[str, Any]:
    r"""Leading term of zeta^{DK}_{sl_N}(s) near the abscissa sigma_c.

    As s -> sigma_c^+ = (2/N)^+:

    zeta^{DK}(s) ~ C_N / (s - 2/N)^{alpha_N}

    where alpha_N and C_N are determined by the Weyl law for the
    growth of representation dimensions.

    For sl_2: sigma_c = 1, alpha = 1, C = 1 (residue of Riemann zeta at 1).

    We estimate alpha_N and C_N numerically from partial sums.
    """
    sigma_c = abscissa_of_convergence(N)

    # Compute at several s close to sigma_c
    epsilons = [0.5, 0.3, 0.2, 0.15, 0.1, 0.08, 0.05]
    data_points = []
    for eps in epsilons:
        s_val = sigma_c + eps
        z = categorical_zeta_slN(N, s_val, max_total=max_total, include_trivial=False)
        if abs(z) > 1e-30:
            data_points.append((eps, abs(z)))

    # Estimate alpha from log-log fit: log|zeta| ~ -alpha * log(eps) + log(C)
    if len(data_points) >= 3:
        log_eps = [math.log(dp[0]) for dp in data_points]
        log_z = [math.log(dp[1]) for dp in data_points]
        # Linear regression
        n_pts = len(log_eps)
        mean_x = sum(log_eps) / n_pts
        mean_y = sum(log_z) / n_pts
        ss_xy = sum((log_eps[i] - mean_x) * (log_z[i] - mean_y) for i in range(n_pts))
        ss_xx = sum((log_eps[i] - mean_x) ** 2 for i in range(n_pts))
        if abs(ss_xx) > 1e-30:
            slope = ss_xy / ss_xx
            alpha_est = -slope
            intercept = mean_y - slope * mean_x
            C_est = math.exp(intercept)
        else:
            alpha_est = None
            C_est = None
    else:
        alpha_est = None
        C_est = None

    return {
        'N': N,
        'sigma_c': sigma_c,
        'alpha_estimated': alpha_est,
        'C_estimated': C_est,
        'data_points': data_points,
    }


