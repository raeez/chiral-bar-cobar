# 7. Product formula: zeta^{DK}_{sl_N}(s) vs products of Riemann zetas
# =========================================================================

def riemann_zeta_em(s: complex, N_em: int = 200) -> complex:
    r"""Riemann zeta via Euler-Maclaurin summation."""
    if abs(s - 1.0) < 1e-12:
        return complex(float('inf'))

    partial = sum(n ** (-s) for n in range(1, N_em + 1))
    tail = N_em ** (1 - s) / (s - 1) + 0.5 * N_em ** (-s)

    bernoulli = [
        Fraction(1, 6), Fraction(-1, 30), Fraction(1, 42),
        Fraction(-1, 30), Fraction(5, 66), Fraction(-691, 2730),
    ]
    correction = complex(0)
    for k_idx, bk in enumerate(bernoulli):
        k = k_idx + 1
        if k == 1:
            fact = float(bk) / 2.0 * s * N_em ** (-(s + 1))
        else:
            num_prod = complex(1)
            for j in range(2 * k - 1):
                num_prod *= (s + j)
            denom = math.factorial(2 * k)
            fact = float(bk) * num_prod / denom * N_em ** (-(s + 2 * k - 1))
        correction += fact

    return partial + tail + correction


def product_formula_test(N: int, s: float,
                         max_total: int = 20) -> Dict[str, Any]:
    r"""Test the product formula conjecture:

    zeta^{DK}_{sl_N}(s) ?= prod_{1<=i<j<=N} zeta((j-i)*s) * (correction)

    For sl_2: prod = zeta(s). Exact.
    For sl_3: prod = zeta(s)*zeta(2s). Test ratio.
    For sl_4: prod = zeta(s)*zeta(2s)*zeta(3s)*zeta(s)*zeta(2s)*zeta(s)
            = zeta(s)^3 * zeta(2s)^2 * zeta(3s). Test ratio.
    """
    cat_z = categorical_zeta_slN(N, s, max_total=max_total, include_trivial=True)

    # Product of zeta((j-i)*s) for 1 <= i < j <= N
    product_z = complex(1)
    for i in range(1, N + 1):
        for j in range(i + 1, N + 1):
            product_z *= riemann_zeta_em((j - i) * s)

    ratio = cat_z / product_z if abs(product_z) > 1e-30 else complex(float('inf'))

    return {
        'N': N,
        's': s,
        'categorical_zeta': cat_z,
        'product_of_zetas': product_z,
        'ratio': ratio,
        'ratio_abs': abs(ratio),
    }


def witten_zeta_slN(N: int, s: float, max_total: int = 20) -> complex:
    r"""The Witten zeta function for sl_N.

    Z_W(sl_N, s) = sum_{lambda nontrivial} dim(V_lambda)^{-s}

    Same as categorical_zeta without the trivial rep.
    Named after Witten's formula for volumes of moduli of flat connections:
    Vol(M_{flat}(Sigma_g, G)) proportional to sum dim(V)^{2-2g}.
    """
    return categorical_zeta_slN(N, s, max_total=max_total, include_trivial=False)


# =========================================================================
# 8. Functional equation search
# =========================================================================

def functional_equation_test(N: int, s: float,
                             centers: Optional[List[float]] = None,
                             max_total: int = 15) -> Dict[str, Any]:
    r"""Search for a functional equation zeta^{DK}(s) ~ zeta^{DK}(c-s).

    For sl_2 (= Riemann zeta): the functional equation relates zeta(s) to
    zeta(1-s) via gamma factors, so the "center" is c = 1.

    For higher rank: search over candidate centers.
    """
    if centers is None:
        centers = [0.5, 1.0, 1.5, 2.0 / N, 1.0 / N]

    z_s = categorical_zeta_slN(N, s, max_total=max_total, include_trivial=True)

    results = {}
    for c in centers:
        s_reflected = c - s
        if s_reflected > 0:
            z_reflected = categorical_zeta_slN(N, s_reflected, max_total=max_total,
                                              include_trivial=True)
            ratio = z_s / z_reflected if abs(z_reflected) > 1e-30 else complex(float('inf'))
        else:
            z_reflected = None
            ratio = None
        results[c] = {
            'z_s': z_s,
            'z_reflected': z_reflected,
            'ratio': ratio,
        }

    return {'N': N, 's': s, 'results': results}


