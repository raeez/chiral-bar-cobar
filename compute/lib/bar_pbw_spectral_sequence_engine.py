DIM_SL2 = 3

# Basis: e=0, h=1, f=2
# Full bracket table with BOTH orderings (needed for loop algebra construction)
SL2_BRACKET = {
    (0, 1): {0: Fraction(-2)},   # [e, h] = -2e
    (0, 2): {1: Fraction(1)},    # [e, f] = h
    (1, 0): {0: Fraction(2)},    # [h, e] = 2e
    (1, 2): {2: Fraction(-2)},   # [h, f] = -2f
    (2, 0): {1: Fraction(-1)},   # [f, e] = -h
    (2, 1): {2: Fraction(2)},    # [f, h] = 2f
}


def build_sl2_negative_mode_algebra(max_weight: int = 8) -> NegativeModeAlgebra:
    """Build the negative-mode Lie algebra g_- = sl_2 tensor t^{-1}C[t^{-1}].

    Generators: (a, n) for a in {e,h,f}, n = 1,...,max_weight.
    Flat index: a + 3*(n-1).
    Weight of (a,n) = n.
    Bracket: [(a,m), (b,n)] = [a,b] tensor t^{-(m+n)}, NO central extension.
    """
    gen_weights = []
    gen_names = []
    name_map = {0: 'e', 1: 'h', 2: 'f'}
    for n in range(1, max_weight + 1):
        for a in range(DIM_SL2):
            gen_weights.append(n)
            gen_names.append(f"{name_map[a]}_{n}")

    n_gens = len(gen_weights)
    bracket_table: Dict[Tuple[int, int], Dict[int, Fraction]] = {}

    for n1 in range(1, max_weight + 1):
        for a in range(DIM_SL2):
            i = a + DIM_SL2 * (n1 - 1)
            for n2 in range(n1, max_weight + 1):
                for b in range(DIM_SL2):
                    j = b + DIM_SL2 * (n2 - 1)
                    if i >= j:
                        continue
                    m_sum = n1 + n2
                    if m_sum > max_weight:
                        continue
                    br = SL2_BRACKET.get((a, b))
                    if br:
                        result = {}
                        for c_lie, coeff in br.items():
                            flat_c = c_lie + DIM_SL2 * (m_sum - 1)
                            if flat_c < n_gens:
                                result[flat_c] = _frac(coeff)
                        if result:
                            bracket_table[(i, j)] = result

    return NegativeModeAlgebra(gen_weights, bracket_table, gen_names)


def compute_sl2_e1_page(max_weight: int = 8,
                         max_degree: int = 6) -> SpectralSequencePage:
    """Compute E_1 page for sl_2 affine: dimensions of Lambda^p(g_-^*)_h.

    E_1^{p,h} = dim Lambda^p(g_-^*)_h.
    """
    alg = build_sl2_negative_mode_algebra(max_weight)
    page = SpectralSequencePage(1, "sl_2")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = alg.chain_dim(p, h)
            page.set_dim(p, h, dim)

    return page


def compute_sl2_e2_page(max_weight: int = 8,
                         max_degree: int = 6) -> SpectralSequencePage:
    """Compute E_2 page for sl_2 affine: H^p_CE(g_-, C) at each weight.

    E_2^{p,h} = dim H^p(CE(g_-))_h.
    By Koszulness, E_2 = E_infinity.
    """
    alg = build_sl2_negative_mode_algebra(max_weight)
    page = SpectralSequencePage(2, "sl_2")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = alg.cohomology_dim(p, h)
            page.set_dim(p, h, dim)

    return page


def verify_sl2_collapse(max_weight: int = 8,
                         max_degree: int = 5) -> Dict[str, object]:
    """Verify E_2 = E_infinity for sl_2 by comparing with known bar cohomology.

    Known: H^n(B(V_k(sl_2))) is concentrated at weight n(n+1)/2 with dim 2n+1.
    """
    e2 = compute_sl2_e2_page(max_weight, max_degree)

    results = {"collapsed": True, "mismatches": []}

    for n in range(1, max_degree + 1):
        expected_weight = n * (n + 1) // 2
        expected_dim = 2 * n + 1

        # Check that E_2 at bar degree n is concentrated at the expected weight
        for h in range(0, max_weight + 1):
            dim = e2.get_dim(n, h)
            if h == expected_weight:
                if dim != expected_dim:
                    results["collapsed"] = False
                    results["mismatches"].append(
                        f"E_2^{{{n},{h}}} = {dim}, expected {expected_dim}")
            else:
                if dim != 0:
                    results["collapsed"] = False
                    results["mismatches"].append(
                        f"E_2^{{{n},{h}}} = {dim}, expected 0")

    return results


