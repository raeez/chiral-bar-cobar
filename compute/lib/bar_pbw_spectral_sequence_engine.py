class SpectralSequencePage:
    """Data for a page E_r of the PBW spectral sequence.

    E_r^{p,q} is stored as a dict (p, q) -> dim, along with
    the family name and metadata.
    """

    def __init__(self, page_number: int, family: str):
        self.page = page_number
        self.family = family
        self.dims: Dict[Tuple[int, int], int] = {}

    def set_dim(self, p: int, q: int, dim: int):
        if dim > 0:
            self.dims[(p, q)] = dim

    def get_dim(self, p: int, q: int) -> int:
        return self.dims.get((p, q), 0)

    def total_dim_at_degree(self, n: int) -> int:
        """Total dimension at total degree n = p + q."""
        return sum(d for (p, q), d in self.dims.items() if p + q == n)

    def total_dim_at_bar_degree(self, p: int) -> int:
        """Total dimension at bar degree p (summing over all q)."""
        return sum(d for (pp, q), d in self.dims.items() if pp == p)

    def total_dim_at_weight(self, w: int) -> int:
        """Total dimension at conformal weight w (stored in q)."""
        return sum(d for (p, q), d in self.dims.items() if q == w)

    def nonzero_entries(self) -> Dict[Tuple[int, int], int]:
        return {k: v for k, v in self.dims.items() if v > 0}

    def euler_char_at_weight(self, w: int) -> int:
        """Euler characteristic at weight w: sum (-1)^p dim E^{p,w}."""
        chi = 0
        for (p, q), d in self.dims.items():
            if q == w:
                chi += ((-1) ** p) * d
        return chi


