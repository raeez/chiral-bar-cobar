# 4. PBW filtration: explicit description
# ============================================================================

class PBWFiltration:
    """Explicit PBW filtration F^0 superset F^1 superset ... on the bar complex.

    The PBW filtration on B^n(A) = A_bar^{tensor n} is:
      F^p B^n = span{ [v_1|...|v_n] : sum of PBW grades >= p }

    For the bar complex of a vertex algebra A with generators {a_i} of
    conformal weights {h_i}, the PBW grade of a_i applied n times with
    modes m_1,...,m_n is sum(m_j) (total mode number).

    The associated graded gr^p_F B^n has d_1 = d_{leading pole} (the
    bracket term), and d_r for r >= 2 from sub-leading OPE poles.

    For QUADRATIC algebras (affine KM): max pole order = 2, so d_2 is
    the last differential.  But d_2 = 0 by Koszulness, giving E_2 = E_infty.

    For W_N: max pole order = 2N (from W_{(2N-1)}W), so d_r could be
    nonzero up to r = 2N-1.  Koszulness forces all d_r = 0 for r >= 2.
    """

    def __init__(self, family: str, max_pole_order: int):
        self.family = family
        self.max_pole_order = max_pole_order
        # The SS has d_r: E_r^{p,q} -> E_r^{p+r,q-r+1}
        # d_r can be nonzero only for r <= max_pole_order - 1
        self.max_differential_page = max_pole_order - 1

    def filtration_level(self, mode_numbers: List[int]) -> int:
        """PBW filtration level = sum of mode numbers."""
        return sum(mode_numbers)

    def last_possible_differential(self) -> int:
        """Last page r where d_r could be nonzero."""
        return self.max_differential_page

    def collapse_page_koszul(self) -> int:
        """For a Koszul algebra, the SS collapses at E_2."""
        return 2

    def collapse_page_generic(self) -> int:
        """For a generic (possibly non-Koszul) algebra, latest collapse page."""
        return self.max_differential_page + 1


def pbw_filtration_sl2() -> PBWFiltration:
    """PBW filtration for sl_2-hat.  Max pole order = 2 (quadratic OPE)."""
    return PBWFiltration("sl_2", max_pole_order=2)


def pbw_filtration_virasoro() -> PBWFiltration:
    """PBW filtration for Virasoro.  Max pole order = 4 (T_{(3)}T = c/2)."""
    return PBWFiltration("Virasoro", max_pole_order=4)


def pbw_filtration_w3() -> PBWFiltration:
    """PBW filtration for W_3.  Max pole order = 6 (W_{(5)}W = c/3)."""
    return PBWFiltration("W_3", max_pole_order=6)


def pbw_filtration_wN(N: int) -> PBWFiltration:
    """PBW filtration for W_N.  Max pole order = 2N (W_{(2N-1)}W)."""
    return PBWFiltration(f"W_{N}", max_pole_order=2 * N)


