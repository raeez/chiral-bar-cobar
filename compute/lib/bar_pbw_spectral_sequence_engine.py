# 8. Non-Koszul example: minimal model quotient
# ============================================================================

def minimal_model_e1_dims(max_weight: int = 10, max_degree: int = 3) -> SpectralSequencePage:
    """E_1 page for the Virasoro UNIVERSAL algebra (same as Virasoro E_1).

    For the minimal model at c = 1/2 (Ising), the SIMPLE QUOTIENT L_{1/2}(Vir)
    has additional null vectors that modify the bar complex.  The universal
    algebra V_c(Vir) is always Koszul; the simple quotient may not be.

    The null vector at weight 2 in the c=1/2 Verma module is:
      (L_{-1}^2 - (4/3) L_{-2}) |h_{1,2}> = 0 at h = 1/2.

    For the VACUUM module (h=0) at c=1/2: the null vector is at weight 2:
      (L_{-2} + (2/3) L_{-1}^2) |0> = 0  (Kac det vanishes at level 2).

    This means L_{-2}|0> is NOT independent in L_{1/2}(Vir), which
    removes one generator from the bar complex.  The remaining generators
    are {L_{-n} : n >= 3}, and the E_1 page changes.

    We compute E_1 for the QUOTIENT by removing the weight-2 generator.
    """
    # Quotient Vir_- by the relation identifying L_{-2} with descendants.
    # Generators: {L_{-n} : n >= 3} only.
    n_gens = max_weight - 2  # n from 3 to max_weight
    if n_gens <= 0:
        return SpectralSequencePage(1, "MinModel_c=1/2")

    gen_weights = list(range(3, max_weight + 1))
    gen_names = [f"L_{-(i+3)}" for i in range(n_gens)]

    # Bracket: same as Virasoro but restricted to n >= 3
    bracket_table: Dict[Tuple[int, int], Dict[int, Fraction]] = {}
    for a in range(n_gens):
        for b in range(a + 1, n_gens):
            wa = a + 3  # weight of generator a
            wb = b + 3  # weight of generator b
            w_out = wa + wb
            # Output generator index: w_out - 3
            c_idx = w_out - 3
            coeff = wb - wa  # = (b+3) - (a+3) = b - a
            if c_idx >= 0 and c_idx < n_gens and coeff != 0:
                bracket_table[(a, b)] = {c_idx: Fraction(coeff)}

    alg = NegativeModeAlgebra(gen_weights, bracket_table, gen_names)
    page = SpectralSequencePage(1, "MinModel_c=1/2")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = alg.chain_dim(p, h)
            page.set_dim(p, h, dim)

    return page


def minimal_model_e2_dims(max_weight: int = 10, max_degree: int = 3) -> SpectralSequencePage:
    """E_2 page for the minimal model quotient.

    Uses the reduced generator set {L_{-n} : n >= 3}.
    If the SS does NOT collapse at E_2, there will be discrepancies
    with the true bar cohomology.
    """
    n_gens = max_weight - 2
    if n_gens <= 0:
        return SpectralSequencePage(2, "MinModel_c=1/2")

    gen_weights = list(range(3, max_weight + 1))
    gen_names = [f"L_{-(i+3)}" for i in range(n_gens)]

    bracket_table: Dict[Tuple[int, int], Dict[int, Fraction]] = {}
    for a in range(n_gens):
        for b in range(a + 1, n_gens):
            wa = a + 3
            wb = b + 3
            w_out = wa + wb
            c_idx = w_out - 3
            coeff = wb - wa
            if c_idx >= 0 and c_idx < n_gens and coeff != 0:
                bracket_table[(a, b)] = {c_idx: Fraction(coeff)}

    alg = NegativeModeAlgebra(gen_weights, bracket_table, gen_names)
    page = SpectralSequencePage(2, "MinModel_c=1/2")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = alg.cohomology_dim(p, h)
            page.set_dim(p, h, dim)

    return page


