# 10. Concentration criterion
# ============================================================================

def is_concentrated_in_degree(page: SpectralSequencePage,
                               target_degree: int,
                               max_weight: int = 20) -> bool:
    """Check if all nonzero entries (excluding p=0) are at a single CE degree.

    NOTE: For Koszul algebras, E_2 is generally NOT concentrated in
    CE degree 1.  The Koszul dual A^! = bigoplus H^p(B(A))^v has
    generators (p=1), relations (p=2), syzygies (p=3), etc.  All can
    be nonzero.  This function is useful for detecting special structure,
    e.g. sl_2 at weight n(n+1)/2 has only H^n nonzero.
    """
    for (p, h), d in page.dims.items():
        if p != target_degree and p != 0 and d > 0:
            return False
    return True


