# 9. Euler characteristic cross-checks
# ============================================================================

def euler_char_e1(page: SpectralSequencePage, weight: int) -> int:
    """Euler characteristic of the E_1 page at given weight.

    chi(E_1)_h = sum_p (-1)^p dim E_1^{p,h}.
    This equals the Euler characteristic of the full bar complex at weight h,
    which is preserved by all differentials.
    """
    return page.euler_char_at_weight(weight)


def euler_char_e2(page: SpectralSequencePage, weight: int) -> int:
    """Euler characteristic of the E_2 page at given weight.

    Must equal chi(E_1)_h = chi(E_infinity)_h.
    """
    return page.euler_char_at_weight(weight)


def verify_euler_char_preservation(
    e1: SpectralSequencePage,
    e2: SpectralSequencePage,
    max_weight: int = 10,
) -> Dict[int, bool]:
    """Verify chi(E_1)_h = chi(E_2)_h for all weights.

    The Euler characteristic is invariant under the differential.
    """
    results = {}
    for h in range(0, max_weight + 1):
        chi1 = euler_char_e1(e1, h)
        chi2 = euler_char_e2(e2, h)
        results[h] = (chi1 == chi2)
    return results


