# 3. W_3 PBW spectral sequence
# ============================================================================

def build_w3_generator_data(max_weight: int = 14) -> Dict[str, object]:
    """Data for the W_3 bar complex generators and OPE structure.

    Generators: T_{-n} (n >= 2, weight n) and W_{-m} (m >= 3, weight m).

    IMPORTANT (AP37): The W_3 negative modes do NOT form a Lie algebra.
    The full [W_m, W_n] commutator involves the COMPOSITE field Lambda
    (= :TT: - 3/10 d^2T), which is NOT a generator.  The Jacobi identity
    holds only in the full W-algebra, not in the leading-pole truncation.

    Therefore, we CANNOT build a NegativeModeAlgebra for W_3 and compute
    CE cohomology as d_1 of the PBW spectral sequence.  Instead:

    1. The E_1 page dimensions (chain groups) can be computed: they are
       exterior powers of the generator space {T_{-n}, W_{-m}}.
    2. The E_2 page CANNOT be computed by CE cohomology alone; it requires
       the full W-algebra structure including composites.
    3. Koszulness means E_2 = E_infinity, and the E_2 = known bar cohomology
       (from the recurrence a_n = 4a_{n-1} - 2a_{n-2} - a_{n-3}).

    Returns dict with generator data for E_1 chain group computation.
    """
    gen_weights = []
    gen_names = []
    gen_type = []

    for n in range(2, max_weight + 1):
        gen_weights.append(n)
        gen_names.append(f"T_{-n}")
        gen_type.append('T')

    for m in range(3, max_weight + 1):
        gen_weights.append(m)
        gen_names.append(f"W_{-m}")
        gen_type.append('W')

    return {
        "gen_weights": gen_weights,
        "gen_names": gen_names,
        "gen_type": gen_type,
        "n_gens": len(gen_weights),
        "n_T": max_weight - 1,   # T generators
        "n_W": max_weight - 2,   # W generators
    }


def w3_chain_dim(degree: int, weight: int, max_weight: int = 20) -> int:
    """Dimension of Lambda^degree at conformal weight h for W_3 generators.

    Counts degree-element subsets of {T_{-n}: n>=2} union {W_{-m}: m>=3}
    with total weight = h.
    """
    data = build_w3_generator_data(max_weight)
    weights = data["gen_weights"]
    n_gens = data["n_gens"]

    # Count subsets of size `degree` from generators with weights summing to `weight`
    count = 0

    def _count(remaining_deg, remaining_wt, start):
        nonlocal count
        if remaining_deg == 0:
            if remaining_wt == 0:
                count += 1
            return
        for i in range(start, n_gens):
            w = weights[i]
            if w > remaining_wt:
                continue
            if n_gens - i - 1 < remaining_deg - 1:
                break
            _count(remaining_deg - 1, remaining_wt - w, i + 1)

    _count(degree, weight, 0)
    return count


def compute_w3_e1_page(max_weight: int = 12,
                        max_degree: int = 3) -> SpectralSequencePage:
    """Compute E_1 page for W_3: chain group dimensions.

    E_1^{p,h} = dim Lambda^p(generators)_h where generators are
    {T_{-n} : n >= 2} union {W_{-m} : m >= 3}.

    NOTE: The E_1 differential for W_3 is NOT simply the CE differential
    of a Lie algebra (AP37).  The W_3 modes include composites (Lambda),
    so the d_1 involves the full OPE structure.  We can still compute
    the chain group dimensions.
    """
    page = SpectralSequencePage(1, "W_3")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = w3_chain_dim(p, h, max_weight)
            page.set_dim(p, h, dim)

    return page


def compute_w3_e2_from_known(max_bar_degree: int = 8) -> SpectralSequencePage:
    """E_2 = E_infinity for W_3 from known bar cohomology.

    Since W_3 is Koszul (cor:universal-koszul), E_2 = E_infinity.
    The bar cohomology dimensions are known from the recurrence:
      a_n = 4*a_{n-1} - 2*a_{n-2} - a_{n-3} (n >= 4)
    with a_1 = 2, a_2 = 5, a_3 = 16.

    NOTE: These are TOTAL dimensions by bar degree, not decomposed
    by conformal weight.  The conformal-weight decomposition requires
    the full W_3 bar complex computation.
    """
    page = SpectralSequencePage(2, "W_3")

    for n in range(1, max_bar_degree + 1):
        dim = w3_bar_dim_recurrence(n)
        # Store with h = n as a placeholder (actual weight decomposition is complex)
        page.set_dim(n, n, dim)

    return page


# W_3 bar cohomology dimensions (from landscape_census.tex)
W3_BAR_COH = {1: 2, 2: 5, 3: 16, 4: 52, 5: 171, 6: 564, 7: 1862, 8: 6149}


def w3_bar_dim_recurrence(n: int) -> int:
    """a_n = 4*a_{n-1} - 2*a_{n-2} - a_{n-3} for n >= 4."""
    if n <= 0:
        return 0
    if n == 1:
        return 2
    if n == 2:
        return 5
    if n == 3:
        return 16
    return 4 * w3_bar_dim_recurrence(n - 1) - 2 * w3_bar_dim_recurrence(n - 2) - w3_bar_dim_recurrence(n - 3)


