def build_virasoro_negative_mode_algebra(max_weight: int = 16) -> NegativeModeAlgebra:
    """Build Vir_- = span{L_{-n} : n >= 2}.

    Generator indexed i corresponds to L_{-(i+2)}, weight = i+2.
    Bracket: [L_{-(a+2)}, L_{-(b+2)}] = (b-a) L_{-(a+b+4)},
    i.e. output index = a+b+2, coefficient = b-a.
    NO central extension (m+n >= 4 for m,n >= 2).
    """
    n_gens = max_weight - 1  # indices 0, ..., max_weight-2
    gen_weights = list(range(2, max_weight + 1))
    gen_names = [f"L_{-(i+2)}" for i in range(n_gens)]

    bracket_table: Dict[Tuple[int, int], Dict[int, Fraction]] = {}
    for a in range(n_gens):
        for b in range(a + 1, n_gens):
            c_idx = a + b + 2  # output generator index
            coeff = b - a      # (b+2) - (a+2) = b - a ... NO
            # [L_{-(a+2)}, L_{-(b+2)}] = ((b+2) - (a+2)) L_{-(a+2+b+2)}
            #                           = (b - a) L_{-(a+b+4)}
            # Output generator index for L_{-(a+b+4)} is (a+b+4) - 2 = a+b+2
            if c_idx < n_gens and coeff != 0:
                bracket_table[(a, b)] = {c_idx: Fraction(coeff)}

    return NegativeModeAlgebra(gen_weights, bracket_table, gen_names)


def compute_virasoro_e1_page(max_weight: int = 16,
                              max_degree: int = 4) -> SpectralSequencePage:
    """Compute E_1 page for Virasoro: dim Lambda^p(Vir_-^*)_h.

    E_1^{p,h} = number of p-element subsets of {L_{-n} : n >= 2}
    with total weight h.  Equivalently: partitions of h into p
    distinct parts, each >= 2.
    """
    alg = build_virasoro_negative_mode_algebra(max_weight)
    page = SpectralSequencePage(1, "Virasoro")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = alg.chain_dim(p, h)
            page.set_dim(p, h, dim)

    return page


def compute_virasoro_e2_page(max_weight: int = 16,
                              max_degree: int = 4) -> SpectralSequencePage:
    """Compute E_2 page for Virasoro: H^p_CE(Vir_-)_h.

    By Koszulness, E_2 = E_infinity = H*(B(Vir_c)).
    Bar cohomology is concentrated in bar degree 1 (Koszul dual generators).
    """
    alg = build_virasoro_negative_mode_algebra(max_weight)
    page = SpectralSequencePage(2, "Virasoro")

    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            dim = alg.cohomology_dim(p, h)
            page.set_dim(p, h, dim)

    return page


