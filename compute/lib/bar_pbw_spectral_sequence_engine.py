def verify_d_squared_zero(algebra: NegativeModeAlgebra,
                          max_weight: int = 10,
                          max_degree: int = 4) -> Dict[Tuple[int, int], bool]:
    """Verify d^2 = 0 at all (degree, weight) pairs.

    Returns dict mapping (degree, weight) -> True/False.
    """
    results = {}
    for p in range(0, max_degree + 1):
        for h in range(0, max_weight + 1):
            results[(p, h)] = algebra.verify_d_squared(p, h)
    return results


