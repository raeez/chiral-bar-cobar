class NegativeModeAlgebra:
    """Abstract negative-mode Lie algebra for the PBW spectral sequence.

    Generators indexed 0, 1, ..., n_gens - 1, each with a conformal weight.
    Bracket table: dict mapping (i, j) -> {k: coeff} for i < j.
    """

    def __init__(self, gen_weights: List[int],
                 bracket_table: Dict[Tuple[int, int], Dict[int, Fraction]],
                 gen_names: Optional[List[str]] = None):
        self.n_gens = len(gen_weights)
        self.gen_weights = gen_weights
        self.bracket_table = bracket_table
        self.gen_names = gen_names or [f"g_{i}" for i in range(self.n_gens)]

        # Cache
        self._basis_cache: Dict[Tuple[int, int], List[Tuple[int, ...]]] = {}
        self._diff_cache: Dict[Tuple[int, int], np.ndarray] = {}

    def weight_basis(self, degree: int, weight: int) -> List[Tuple[int, ...]]:
        """Basis of Lambda^degree at conformal weight h.

        Returns sorted list of degree-element subsets of generator indices
        whose weights sum to the given weight.
        """
        key = (degree, weight)
        if key in self._basis_cache:
            return self._basis_cache[key]
        result = list(self._enum_subsets(degree, weight, 0))
        self._basis_cache[key] = result
        return result

    def _enum_subsets(self, degree: int, weight: int, start: int):
        """Generate degree-subsets with exact total weight."""
        if degree == 0:
            if weight == 0:
                yield ()
            return
        if degree < 0 or weight <= 0:
            return
        for i in range(start, self.n_gens - degree + 1):
            w = self.gen_weights[i]
            if w > weight:
                continue
            remaining = weight - w
            # At least (degree-1) more generators needed, each with weight >= 1
            if remaining < degree - 1:
                continue
            for rest in self._enum_subsets(degree - 1, remaining, i + 1):
                yield (i,) + rest

    def chain_dim(self, degree: int, weight: int) -> int:
        """Dimension of Lambda^degree at conformal weight h."""
        return len(self.weight_basis(degree, weight))

    def ce_differential(self, degree: int, weight: int) -> np.ndarray:
        """CE differential d: Lambda^degree_weight -> Lambda^{degree+1}_weight.

        The CE differential on Lambda^p(g^*) is dual to the Lie bracket:
        (d omega)(v_0,...,v_p) = sum_{i<j} (-1)^{i+j} omega([v_i,v_j],...)

        Returns matrix with Fraction entries.
        """
        key = (degree, weight)
        if key in self._diff_cache:
            return self._diff_cache[key]

        source = self.weight_basis(degree, weight)
        target = self.weight_basis(degree + 1, weight)
        n_src, n_tgt = len(source), len(target)

        if n_src == 0 or n_tgt == 0:
            mat = _frac_array((max(n_tgt, 0), max(n_src, 0)))
            self._diff_cache[key] = mat
            return mat

        target_idx = {t: i for i, t in enumerate(target)}
        mat = _frac_array((n_tgt, n_src))

        for col, alpha in enumerate(source):
            alpha_set = set(alpha)
            alpha_list = list(alpha)

            for (beta, gamma), br in self.bracket_table.items():
                for c_idx, coeff in br.items():
                    if c_idx not in alpha_set:
                        continue
                    new_set = (alpha_set - {c_idx}) | {beta, gamma}
                    if len(new_set) != degree + 1:
                        continue
                    new_tuple = tuple(sorted(new_set))
                    row = target_idx.get(new_tuple)
                    if row is None:
                        continue
                    # Sign computation
                    pos_c = alpha_list.index(c_idx)
                    sorted_new = list(new_tuple)
                    pos_beta = sorted_new.index(beta)
                    remaining_after_beta = sorted(new_set - {beta})
                    pos_gamma = remaining_after_beta.index(gamma)
                    sign = Fraction((-1) ** (pos_c + pos_beta + pos_gamma))
                    mat[row, col] += sign * _frac(coeff)

        self._diff_cache[key] = mat
        return mat

    def differential_rank(self, degree: int, weight: int) -> int:
        """Rank of d: Lambda^degree -> Lambda^{degree+1} at given weight."""
        mat = self.ce_differential(degree, weight)
        if mat.size == 0:
            return 0
        return _exact_rank(mat)

    def cohomology_dim(self, degree: int, weight: int) -> int:
        """dim H^degree_weight = dim ker(d^degree) - dim im(d^{degree-1})."""
        dim_p = self.chain_dim(degree, weight)
        if dim_p == 0:
            return 0

        # ker(d^degree)
        d_curr = self.ce_differential(degree, weight)
        rank_curr = _exact_rank(d_curr) if d_curr.size > 0 else 0
        ker_dim = dim_p - rank_curr

        # im(d^{degree-1})
        if degree > 0:
            d_prev = self.ce_differential(degree - 1, weight)
            im_dim = _exact_rank(d_prev) if d_prev.size > 0 else 0
        else:
            im_dim = 0

        return ker_dim - im_dim

    def verify_d_squared(self, degree: int, weight: int) -> bool:
        """Verify d^2 = 0 at (degree, weight)."""
        d0 = self.ce_differential(degree, weight)
        d1 = self.ce_differential(degree + 1, weight)

        if d0.size == 0 or d1.size == 0:
            return True

        # Check d1 . d0 = 0
        rows1, cols1 = d1.shape
        rows0, cols0 = d0.shape
        if cols1 != rows0:
            # Dimension mismatch means one of them is zero
            return True

        prod = _frac_array((rows1, cols0))
        for i in range(rows1):
            for j in range(cols0):
                s = Fraction(0)
                for k in range(cols1):
                    s += d1[i, k] * d0[k, j]
                prod[i, j] = s

        return all(prod[i, j] == Fraction(0)
                   for i in range(rows1) for j in range(cols0))


