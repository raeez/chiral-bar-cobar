def wN_collapse_page_upper_bound(N: int) -> int:
    """Upper bound on collapse page for W_N spectral sequence.

    The W_N self-OPE W_{(2N-1)}W has pole order 2N.
    This drives d_{2N-1} differentials from scalar contact terms.
    For N >= 3, the last potentially nonzero differential is at page 2N-1.
    But Koszulness forces collapse at E_2.

    For generic (non-Koszul) W_N quotients: collapse at E_{2N}.

    Returns: 2N (the page AFTER the last possible differential).
    """
    return 2 * N


def wN_collapse_page_koszul() -> int:
    """For Koszul W_N (universal algebra), collapse at E_2."""
    return 2


