FR = Fraction


def _frac(x) -> Fraction:
    """Coerce to Fraction."""
    if isinstance(x, Fraction):
        return x
    if isinstance(x, (int, np.integer)):
        return Fraction(int(x))
    return Fraction(x)


def _frac_array(shape) -> np.ndarray:
    """Create a zero array of Fraction objects."""
    arr = np.empty(shape, dtype=object)
    arr.fill(Fraction(0))
    return arr


def _exact_rank(M: np.ndarray) -> int:
    """Compute rank of a Fraction-object matrix via Gaussian elimination."""
    if M.size == 0:
        return 0
    rows, cols = M.shape
    if rows == 0 or cols == 0:
        return 0
    A = np.array([[_frac(M[i, j]) for j in range(cols)]
                   for i in range(rows)], dtype=object)
    r = 0
    for c in range(cols):
        pivot = None
        for i in range(r, rows):
            if A[i, c] != Fraction(0):
                pivot = i
                break
        if pivot is None:
            continue
        A[[r, pivot]] = A[[pivot, r]]
        scale = A[r, c]
        for j in range(cols):
            A[r, j] = A[r, j] / scale
        for i in range(rows):
            if i == r:
                continue
            factor = A[i, c]
            if factor != Fraction(0):
                for j in range(cols):
                    A[i, j] = A[i, j] - factor * A[r, j]
        r += 1
    return r


def _exact_kernel_dim(M: np.ndarray) -> int:
    """Dimension of kernel of M."""
    if M.size == 0:
        rows, cols = M.shape
        return cols
    return M.shape[1] - _exact_rank(M)


