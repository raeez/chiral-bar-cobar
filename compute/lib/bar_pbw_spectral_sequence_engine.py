# 6. E_2 vs E_infinity comparison
# ============================================================================

def compare_e2_with_bar_cohomology(
    e2_page: SpectralSequencePage,
    known_bar_coh: Dict[int, int],
    max_degree: int = 6,
) -> Dict[str, object]:
    """Compare E_2 page total dimensions with known bar cohomology.

    If E_2 = E_infinity (Koszulness), then:
      sum_h E_2^{p,h} = dim H^p(B(A))
    and the total H^p dimensions should match known values.

    known_bar_coh: dict mapping bar degree p -> dim H^p.
    """
    results = {"match": True, "details": {}}

    for p in range(1, max_degree + 1):
        e2_total = sum(
            e2_page.get_dim(p, h)
            for h in range(0, 100)  # generous upper bound
        )
        known = known_bar_coh.get(p, 0)

        match = (known == 0 and e2_total >= 0) if known == 0 else (e2_total == known)
        results["details"][p] = {
            "e2_total": e2_total,
            "known": known,
            "match": match,
        }
        if not match and known > 0:
            results["match"] = False

    return results


