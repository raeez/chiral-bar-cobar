@lru_cache(maxsize=1024)
def _partitions_distinct(n: int, min_part: int, max_part: int) -> Tuple[Tuple[int, ...], ...]:
    """Partitions of n into distinct parts in [min_part, max_part]."""
    if n == 0:
        return ((),)
    if n < min_part or min_part > max_part:
        return ()
    results = []
    for part in range(min(n, max_part), min_part - 1, -1):
        for rest in _partitions_distinct(n - part, min_part, part - 1):
            results.append((part,) + rest)
    return tuple(results)


