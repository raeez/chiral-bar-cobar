@dataclass(frozen=True)
class KoszulnessData:
    r"""Koszulness data for a W-algebra at a nilpotent orbit.

    PBW-Slodowy collapse (thm:pbw-slodowy-collapse): the arc space
    J_infty(S_f) of the Slodowy slice S_f is affine, so the PBW
    filtration on W^k(sl_N, f) converges at generic level.  This gives
    Koszulness for ALL type-A W-algebras at generic level.

    Butson's strict chiral quantization provides an independent geometric
    proof that the W-algebra structure is compatible with the Slodowy
    slice geometry at every orbit.
    """
    partition: Partition
    n: int
    orbit_class: str
    # PBW-Slodowy data
    slodowy_slice_dimension: int  # dim S_f = dim g^f
    arc_space_affine: bool        # J_infty(S_f) is always affine for type A
    pbw_collapse: bool            # PBW-Slodowy collapse holds at generic level
    # Koszulness certificate
    koszul_at_generic_level: bool
    koszulness_mechanism: str     # "PBW-Slodowy" or "hook-transport" etc.
    # Shadow depth class
    shadow_class: str  # G, L, C, M
    # Bar cohomology data
    n_generators: int   # dim H^1(B(W))
    h2_upper_bound: int  # upper bound on dim H^2(B(W))


def koszulness_certificate(partition: Partition) -> KoszulnessData:
    r"""Compute koszulness data for W^k(sl_N, f_lambda) at generic level.

    At generic level, ALL type-A W-algebras are chirally Koszul:
      - The PBW-Slodowy collapse applies because the Slodowy slice is
        an affine variety (Slodowy, 1980).
      - The arc space J_infty(S_f) inherits the affine structure.
      - Butson's strict chiral quantization provides the vertex algebra
        structure directly on the arc space.

    The only non-Koszul type-A W-algebras are SIMPLE QUOTIENTS at
    admissible levels (where vacuum null vectors break the PBW property).
    """
    lam = normalize_partition(partition)
    n = partition_size(lam)
    orbit_cls = type_a_orbit_class(lam)

    gen_data = w_algebra_generator_data(lam)
    n_gen = gen_data.f_centralizer_dimension
    slodowy_dim = n_gen  # dim S_f = dim g^f

    # Upper bound on H^2
    dim_g = n * n - 1
    h2_upper = dim_g - n_gen

    # Shadow depth class: principal is M (infinite depth) for N >= 2,
    # trivial is L (affine), all non-trivial non-principal are M.
    if lam == (1,) * n:
        shadow_cls = "L"
    elif lam == (n,) and n == 1:
        shadow_cls = "G"
    else:
        shadow_cls = "M"

    return KoszulnessData(
        partition=lam,
        n=n,
        orbit_class=orbit_cls,
        slodowy_slice_dimension=slodowy_dim,
        arc_space_affine=True,  # Always true for type A
        pbw_collapse=True,      # At generic level
        koszul_at_generic_level=True,
        koszulness_mechanism="PBW-Slodowy",
        shadow_class=shadow_cls,
        n_generators=n_gen,
        h2_upper_bound=h2_upper,
    )


