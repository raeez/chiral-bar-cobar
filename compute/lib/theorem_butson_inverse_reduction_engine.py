def _central_charge_for_partition(
    partition: Partition, level=Symbol('k')
) -> object:
    """Central charge with the corrected BP branch applied locally."""
    lam = normalize_partition(partition)
    k = sympify(level)
    if partition_size(lam) == 3 and lam == (2, 1):
        return bp_central_charge(k)
    return krw_central_charge(lam, k)


def _kappa_for_partition(
    partition: Partition, level=Symbol('k')
) -> object:
    """Kappa with the corrected BP branch applied locally."""
    lam = normalize_partition(partition)
    k = sympify(level)
    if partition_size(lam) == 3 and lam == (2, 1):
        return simplify(anomaly_ratio_from_partition(lam) * bp_central_charge(k))
    return ds_kappa_from_affine(lam, k)

@dataclass(frozen=True)
class KappaEdgeData:
    r"""Kappa data along one inverse reduction edge."""
    edge: InverseReductionEdge
    source_kappa: object     # kappa(W^k(f_source))
    target_kappa: object     # kappa(W^k(f_target))
    kappa_deficit: object    # source_kappa - target_kappa (ghost contribution)
    kappa_deficit_simplified: object


def kappa_along_edge(
    edge: InverseReductionEdge, level=Symbol('k')
) -> KappaEdgeData:
    r"""Compute kappa data along an inverse reduction edge.

    The kappa deficit kappa(W_source) - kappa(W_target) measures the
    ghost contribution of the reduction step.  For inverse reduction
    to be compatible with bar-cobar, this deficit must be expressible
    in terms of the ghost system data.
    """
    k = sympify(level)
    src_kappa = _kappa_for_partition(edge.source, k)
    tgt_kappa = _kappa_for_partition(edge.target, k)
    deficit = simplify(src_kappa - tgt_kappa)
    return KappaEdgeData(
        edge=edge,
        source_kappa=src_kappa,
        target_kappa=tgt_kappa,
        kappa_deficit=src_kappa - tgt_kappa,
        kappa_deficit_simplified=deficit,
    )


