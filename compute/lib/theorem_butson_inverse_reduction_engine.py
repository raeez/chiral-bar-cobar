def anomaly_ratio_catalog(max_n: int = 6) -> Dict[Partition, Rational]:
    r"""Compute anomaly ratios for all partitions up to sl_{max_n}.

    The anomaly ratio rho_lambda = sum_i (+- 1/h_i) over strong generators
    is a k-independent rational number determined purely by the partition.
    """
    results = {}
    for n in range(2, max_n + 1):
        for lam in _partitions_of_n(n):
            lam = normalize_partition(lam)
            # Skip trivial partition (affine algebra, not a W-algebra)
            if lam == (1,) * n:
                continue
            results[lam] = anomaly_ratio_from_partition(lam)
    return results


def anomaly_ratio_transpose_relation(
    partition: Partition,
) -> Dict[str, object]:
    r"""Check the relation between anomaly ratios of lambda and lambda^t.

    For the transport-to-transpose conjecture to hold at the kappa level,
    we need rho_lambda * c(k, lambda) + rho_{lambda^t} * c(k^v, lambda^t) = 0.
    This is a nontrivial constraint relating rho and the KRW formula.
    """
    lam = normalize_partition(partition)
    lam_t = transpose_partition(lam)
    rho_src = anomaly_ratio_from_partition(lam)
    # Skip trivial
    if lam_t == (1,) * partition_size(lam):
        return {
            "partition": lam,
            "transpose": lam_t,
            "source_rho": rho_src,
            "dual_rho": None,
            "note": "trivial partition (affine algebra)",
        }
    rho_dual = anomaly_ratio_from_partition(lam_t)
    return {
        "partition": lam,
        "transpose": lam_t,
        "source_rho": rho_src,
        "dual_rho": rho_dual,
        "rho_sum": rho_src + rho_dual,
        "rho_ratio": rho_src / rho_dual if rho_dual != 0 else None,
    }


