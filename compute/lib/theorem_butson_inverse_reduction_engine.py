@dataclass(frozen=True)
class TransposeVerificationData:
    r"""Complete transport-to-transpose verification for one partition."""
    partition: Partition
    transpose: Partition
    n: int
    orbit_class: str
    is_self_transpose: bool
    # Generator data
    source_generators: WAlgebraGeneratorData
    dual_generators: WAlgebraGeneratorData
    source_n_generators: int
    dual_n_generators: int
    generators_match: bool  # whether generator counts agree
    # Central charge data
    source_c: object        # c(k, lambda)
    dual_c: object          # c(k^v, lambda^t)
    c_sum: object           # c + c' (should be k-independent)
    c_sum_is_constant: bool
    # Kappa data
    source_kappa: object
    dual_kappa: object
    kappa_sum: object       # kappa(lambda,k) + kappa(lambda^t,k^v)
    kappa_sum_simplified: object
    kappa_sum_is_constant: bool  # whether kappa_sum is k-independent
    # NOTE (AP24): kappa + kappa' = 0 holds only for KM/free fields.
    # For W-algebras, the sum is rho*K (Theorem D), generally nonzero.
    # For self-transpose partitions, the sum IS k-independent.
    # For non-self-transpose pairs, the sum is a k-dependent rational function.
    # The transport-to-transpose conjecture is about the DUALITY ITSELF,
    # not about kappa anti-symmetry.
    # Anomaly ratio
    source_anomaly_ratio: object
    dual_anomaly_ratio: object


def verify_transport_to_transpose(
    partition: Partition, level=Symbol('k')
) -> TransposeVerificationData:
    r"""Verify the transport-to-transpose conjecture for one partition.

    For conj:type-a-transport-to-transpose:
      (W^k(sl_N, f_lambda))^! = W^{k^v}(sl_N, f_{lambda^t})

    This checks:
      1. kappa(W_k(f_lambda)) + kappa(W_{k^v}(f_{lambda^t})) = 0
      2. c(k, lambda) + c(k^v, lambda^t) is k-independent
      3. Generator spectrum comparison
    """
    lam = normalize_partition(partition)
    lam_t = transpose_partition(lam)
    n = partition_size(lam)
    k = sympify(level)
    kv = hook_dual_level_sl_n(n, k)

    orbit_cls = type_a_orbit_class(lam)
    is_self_tr = (lam == lam_t)

    src_gen = w_algebra_generator_data(lam)
    dual_gen = w_algebra_generator_data(lam_t)

    src_c = _central_charge_for_partition(lam, k)
    dual_c = _central_charge_for_partition(lam_t, kv)
    c_sum_raw = src_c + dual_c
    c_sum_simplified = simplify(c_sum_raw)
    # Check if c_sum is k-independent
    c_sum_is_const = (simplify(c_sum_simplified.diff(k)) == 0
                      if hasattr(c_sum_simplified, 'diff') else True)

    # Kappa computation: only meaningful when BOTH sides are genuine W-algebras.
    # The trivial partition (1^n) is the affine algebra; the anomaly ratio
    # formula does NOT apply.  Flag this case.
    involves_affine = (lam == (1,) * n) or (lam_t == (1,) * n)

    if involves_affine:
        # Cannot compute kappa via anomaly ratio for the affine side
        src_kappa = None
        dual_kappa = None
        kappa_sum_raw = None
        kappa_sum_simplified = None
        kappa_sum_is_const = None
        src_rho = None
        dual_rho = None
    else:
        src_kappa = _kappa_for_partition(lam, k)
        dual_kappa = _kappa_for_partition(lam_t, kv)
        kappa_sum_raw = src_kappa + dual_kappa
        kappa_sum_simplified = simplify(kappa_sum_raw)
        kappa_sum_is_const = (simplify(kappa_sum_simplified.diff(k)) == 0
                              if hasattr(kappa_sum_simplified, 'diff') else True)
        src_rho = anomaly_ratio_from_partition(lam)
        dual_rho = anomaly_ratio_from_partition(lam_t)

    return TransposeVerificationData(
        partition=lam,
        transpose=lam_t,
        n=n,
        orbit_class=orbit_cls,
        is_self_transpose=is_self_tr,
        source_generators=src_gen,
        dual_generators=dual_gen,
        source_n_generators=src_gen.f_centralizer_dimension,
        dual_n_generators=dual_gen.f_centralizer_dimension,
        generators_match=(src_gen.f_centralizer_dimension
                          == dual_gen.f_centralizer_dimension),
        source_c=src_c,
        dual_c=dual_c,
        c_sum=c_sum_simplified,
        c_sum_is_constant=c_sum_is_const,
        source_kappa=src_kappa,
        dual_kappa=dual_kappa,
        kappa_sum=kappa_sum_raw,
        kappa_sum_simplified=kappa_sum_simplified,
        kappa_sum_is_constant=kappa_sum_is_const,
        source_anomaly_ratio=src_rho,
        dual_anomaly_ratio=dual_rho,
    )


def verify_all_partitions_transport(
    n: int, level=Symbol('k')
) -> Dict[Partition, TransposeVerificationData]:
    r"""Verify transport-to-transpose for all partitions of n."""
    results = {}
    for lam in _partitions_of_n(n):
        lam = normalize_partition(lam)
        # Skip trivial (1^n) and principal (n) which are known
        results[lam] = verify_transport_to_transpose(lam, level)
    return results


