@dataclass
class TransportGraph:
    r"""Complete transport graph for sl_n nilpotent orbits.

    After Butson-Nair, every covering relation in the orbit closure
    ordering is a bidirectional transport edge:
      - Forward: DS reduction (Kac-Roan-Wakimoto)
      - Inverse: Butson-Nair embedding

    The graph is connected (the orbit poset is connected), so transport
    from any partition to any other is possible via a sequence of
    reduction and inverse-reduction steps.
    """
    n: int
    partitions: List[Partition]
    edges: List[InverseReductionEdge]
    hasse_diagram: Dict[Partition, List[Partition]]
    # Reachability: can we reach every partition from hooks?
    hook_partitions: List[Partition]
    reachable_from_hooks: Set[Partition]
    full_reachability: bool  # True if every partition is reachable


def build_transport_graph(n: int) -> TransportGraph:
    r"""Build the complete transport graph for sl_n.

    This is the key computation that shows Butson-Nair extends
    the hook-type corridor to ALL orbits.
    """
    all_parts = list(_partitions_of_n(n))
    hasse = orbit_hasse_diagram(n)
    edges = all_inverse_reduction_edges(n)

    hooks = [p for p in all_parts if is_hook_partition(p)]

    # BFS from hooks through the full Hasse diagram
    reachable: Set[Partition] = set()
    queue = list(hooks)
    reachable.update(hooks)

    while queue:
        current = queue.pop(0)
        # Go up (inverse reduction) and down (forward reduction)
        for lam in all_parts:
            if lam in reachable:
                continue
            # Check adjacency in either direction
            if dominance_order(lam, current) or dominance_order(current, lam):
                if is_covering_relation(lam, current) or is_covering_relation(current, lam):
                    reachable.add(lam)
                    queue.append(lam)

    return TransportGraph(
        n=n,
        partitions=all_parts,
        edges=edges,
        hasse_diagram=hasse,
        hook_partitions=hooks,
        reachable_from_hooks=reachable,
        full_reachability=(reachable == set(all_parts)),
    )


