# Orbit closure ordering (dominance order in type A)
# =====================================================================

def dominance_order(lam: Partition, mu: Partition) -> bool:
    r"""Return True if lam >= mu in the dominance (closure) order.

    In type A, the closure ordering on nilpotent orbits coincides with
    the dominance partial order on partitions:
      lam >= mu  iff  sum_{i=1}^j lam_i >= sum_{i=1}^j mu_i  for all j.
    """
    lam = normalize_partition(lam)
    mu = normalize_partition(mu)
    if partition_size(lam) != partition_size(mu):
        raise ValueError("partitions must have the same size")
    max_len = max(len(lam), len(mu))
    lam_ext = lam + (0,) * (max_len - len(lam))
    mu_ext = mu + (0,) * (max_len - len(mu))
    partial_sum_lam = 0
    partial_sum_mu = 0
    for j in range(max_len):
        partial_sum_lam += lam_ext[j]
        partial_sum_mu += mu_ext[j]
        if partial_sum_lam < partial_sum_mu:
            return False
    return True


def is_covering_relation(lam: Partition, mu: Partition) -> bool:
    r"""Return True if lam covers mu in the dominance order.

    lam covers mu means: lam > mu and there is no nu with lam > nu > mu.
    """
    lam = normalize_partition(lam)
    mu = normalize_partition(mu)
    if not dominance_order(lam, mu) or lam == mu:
        return False
    n = partition_size(lam)
    for nu in _partitions_of_n(n):
        nu = normalize_partition(nu)
        if nu != lam and nu != mu:
            if dominance_order(lam, nu) and dominance_order(nu, mu):
                return False
    return True


def orbit_hasse_diagram(n: int) -> Dict[Partition, List[Partition]]:
    r"""Compute the Hasse diagram of the orbit closure ordering for sl_n.

    Returns a dict mapping each partition to its list of covers (partitions
    immediately below it in the dominance order).
    """
    all_parts = list(_partitions_of_n(n))
    covers: Dict[Partition, List[Partition]] = {p: [] for p in all_parts}
    for lam in all_parts:
        for mu in all_parts:
            if lam != mu and is_covering_relation(lam, mu):
                covers[lam].append(mu)
    return covers


def orbit_hasse_edges(n: int) -> List[Tuple[Partition, Partition]]:
    r"""All covering relation edges lam > mu in the orbit Hasse diagram."""
    hasse = orbit_hasse_diagram(n)
    edges = []
    for lam, covers in hasse.items():
        for mu in covers:
            edges.append((lam, mu))
    return edges


