def central_charge_conductor(partition: Partition, level=Symbol('k')) -> object:
    r"""Compute c(k, lambda) + c(k^v, lambda^t) for the partition.

    Under the transport-to-transpose conjecture, this should be
    k-independent.  For hook partitions this is proved; for general
    partitions it provides evidence.
    """
    lam = normalize_partition(partition)
    lam_t = transpose_partition(lam)
    n = partition_size(lam)
    k = sympify(level)
    kv = hook_dual_level_sl_n(n, k)
    return simplify(
        _central_charge_for_partition(lam, k)
        + _central_charge_for_partition(lam_t, kv)
    )


def central_charge_conductor_catalog(
    max_n: int = 6, level=Symbol('k')
) -> Dict[Partition, object]:
    r"""Compute c + c' for all partitions up to sl_{max_n}."""
    results = {}
    for n in range(3, max_n + 1):
        for lam in _partitions_of_n(n):
            lam = normalize_partition(lam)
            results[lam] = central_charge_conductor(lam, level)
    return results


