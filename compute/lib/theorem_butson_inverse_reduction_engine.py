@dataclass(frozen=True)
class InverseReductionEdge:
    r"""Data for one inverse reduction edge mu -> lambda.

    Butson-Nair prove: W^k(sl_N, f_mu) embeds into
    W^k(sl_N, f_lambda) tensor D_aux, where D_aux is an algebra of
    differential operators whose rank equals the difference
    dim(g^{f_mu}) - dim(g^{f_lambda}).
    """
    n: int
    source: Partition        # mu (smaller orbit)
    target: Partition        # lambda (larger orbit)
    source_centralizer_dim: int
    target_centralizer_dim: int
    auxiliary_rank: int      # dim(g^{f_mu}) - dim(g^{f_lambda})
    source_generators: WAlgebraGeneratorData
    target_generators: WAlgebraGeneratorData
    # Ghost system data for the reduction step
    ghost_dimension: int     # number of ghost pairs in the BRST complex
    is_hook_edge: bool       # whether both source and target are hooks


def _centralizer_dim(partition: Partition) -> int:
    """Dimension of f-centralizer g^f for the nilpotent f_lambda in sl_N."""
    gen_data = w_algebra_generator_data(partition)
    return gen_data.f_centralizer_dimension


def inverse_reduction_edge(
    n: int, source: Partition, target: Partition
) -> InverseReductionEdge:
    r"""Compute the inverse reduction edge data for mu -> lambda.

    The source partition mu corresponds to a SMALLER orbit (more generators),
    and the target lambda to a LARGER orbit (fewer generators).
    In the Hasse diagram, target covers source: target > source.

    Butson-Nair's inverse reduction embeds W(f_source) into
    W(f_target) tensor D_aux.  The auxiliary rank is the difference
    in centralizer dimensions.
    """
    source = normalize_partition(source)
    target = normalize_partition(target)
    if partition_size(source) != n or partition_size(target) != n:
        raise ValueError(f"partitions must be of {n}")
    if not dominance_order(target, source):
        raise ValueError("target must dominate source in closure ordering")

    src_gen = w_algebra_generator_data(source)
    tgt_gen = w_algebra_generator_data(target)
    src_cdim = src_gen.f_centralizer_dimension
    tgt_cdim = tgt_gen.f_centralizer_dimension

    # Auxiliary algebra rank: source has more generators than target
    aux_rank = src_cdim - tgt_cdim

    # Ghost dimension: for a single covering step, the ghost pairs
    # correspond to the positive-grade part of the quotient m+/m'+
    # This equals half the orbit dimension difference.
    orbit_dim_source = n * n - 1 - src_cdim  # rough; use exact
    orbit_dim_target = n * n - 1 - tgt_cdim
    ghost_dim = (orbit_dim_target - orbit_dim_source) // 2

    is_hook = is_hook_partition(source) and is_hook_partition(target)

    return InverseReductionEdge(
        n=n,
        source=source,
        target=target,
        source_centralizer_dim=src_cdim,
        target_centralizer_dim=tgt_cdim,
        auxiliary_rank=aux_rank,
        source_generators=src_gen,
        target_generators=tgt_gen,
        ghost_dimension=ghost_dim,
        is_hook_edge=is_hook,
    )


def all_inverse_reduction_edges(n: int) -> List[InverseReductionEdge]:
    r"""Compute all inverse reduction edges for sl_n.

    Each covering relation lambda > mu gives a bidirectional transport edge:
      - Forward (DS reduction): W(f_mu) -> W(f_lambda) via BRST
      - Inverse (Butson-Nair): W(f_mu) hookrightarrow W(f_lambda) tensor D_aux
    """
    edges = orbit_hasse_edges(n)
    result = []
    for (lam, mu) in edges:
        result.append(inverse_reduction_edge(n, mu, lam))
    return result


