@dataclass
class ButsonAnalysisSummary:
    r"""Summary of what Butson's results mean for Direction 4."""
    n: int
    total_partitions: int
    total_edges: int
    hook_edges: int
    non_hook_edges: int
    full_reachability: bool
    kappa_sum_well_defined_all: bool
    kappa_sum_constant_self_transpose: bool
    c_conductor_constant_self_transpose: bool
    all_koszul_at_generic: bool
    # Specific findings
    failures: List[str]
    findings: List[str]


def butson_analysis(n: int, level=Symbol('k')) -> ButsonAnalysisSummary:
    r"""Run the full Butson analysis for sl_n.

    This is the main entry point: computes the transport graph,
    verifies transport-to-transpose for all partitions, and checks
    Koszulness preservation.
    """
    graph = build_transport_graph(n)
    transport_data = verify_all_partitions_transport(n, level)

    total_parts = len(graph.partitions)
    total_edges = len(graph.edges)
    hook_edges = sum(1 for e in graph.edges if e.is_hook_edge)
    non_hook_edges = total_edges - hook_edges

    # Check kappa and c data for all partitions
    kappa_failures = []
    kappa_self_transpose_failures = []
    c_self_transpose_failures = []
    for lam, data in transport_data.items():
        # Skip trivial partition
        if lam == (1,) * n:
            continue
        # Skip partitions involving the affine algebra (trivial transpose)
        lam_t = data.transpose
        involves_affine = (lam == (1,) * n) or (lam_t == (1,) * n)
        if involves_affine:
            continue
        if data.kappa_sum_simplified is None:
            kappa_failures.append(f"{lam}: kappa computation failed")
        # For self-transpose: kappa sum and c sum should be k-independent
        if data.is_self_transpose:
            if not data.kappa_sum_is_constant:
                kappa_self_transpose_failures.append(
                    f"{lam}: kappa_sum = {data.kappa_sum_simplified}")
            if not data.c_sum_is_constant:
                c_self_transpose_failures.append(
                    f"{lam}: c_sum = {data.c_sum}")

    # Check Koszulness for all
    koszul_data = {lam: koszulness_certificate(lam) for lam in graph.partitions}
    all_koszul = all(kd.koszul_at_generic_level for kd in koszul_data.values())

    failures = (kappa_failures + kappa_self_transpose_failures
                + c_self_transpose_failures)
    findings = []

    if graph.full_reachability:
        findings.append(
            f"ALL {total_parts} partitions of {n} are reachable from hooks "
            f"via the Butson-Nair transport graph"
        )
    findings.append(
        f"Hasse diagram has {total_edges} edges "
        f"({hook_edges} hook, {non_hook_edges} non-hook)"
    )
    if not kappa_failures:
        findings.append(
            "kappa sum well-defined for all non-affine transpose pairs"
        )
    if not kappa_self_transpose_failures:
        findings.append(
            "kappa sum is k-independent for all self-transpose partitions"
        )
    if not c_self_transpose_failures:
        findings.append(
            "c + c' is k-independent for all self-transpose partitions"
        )
    findings.append(
        "NOTE (AP24): kappa + kappa' != 0 for non-self-transpose W-algebra "
        "pairs. The sum is a k-dependent rational function. This is the "
        "expected Theorem D behavior, not a failure."
    )
    if all_koszul:
        findings.append(
            "ALL type-A W-algebras are chirally Koszul at generic level "
            "(PBW-Slodowy)"
        )

    return ButsonAnalysisSummary(
        n=n,
        total_partitions=total_parts,
        total_edges=total_edges,
        hook_edges=hook_edges,
        non_hook_edges=non_hook_edges,
        full_reachability=graph.full_reachability,
        kappa_sum_well_defined_all=(len(kappa_failures) == 0),
        kappa_sum_constant_self_transpose=(len(kappa_self_transpose_failures) == 0),
        c_conductor_constant_self_transpose=(len(c_self_transpose_failures) == 0),
        all_koszul_at_generic=all_koszul,
        failures=failures,
        findings=findings,
    )
