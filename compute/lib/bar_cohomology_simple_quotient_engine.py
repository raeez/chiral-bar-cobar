@dataclass(frozen=True)
class PBWState:
    """A PBW monomial state in V_k(sl_2).

    Represented as a tuple of (lie_idx, mode) pairs in decreasing mode order,
    then decreasing lie_idx within the same mode. This is the PBW ordering
    convention: J^a_{-n_1} ... J^b_{-n_r} |0> with n_1 >= n_2 >= ... >= n_r >= 1.

    The conformal weight is sum of modes.
    """
    modes: Tuple[Tuple[int, int], ...]  # ((lie_idx, mode), ...)
    weight: int  # = sum of modes

    def __repr__(self):
        if not self.modes:
            return '|0>'
        parts = [f'{SL2_NAMES[a]}_{{{-m}}}' for a, m in self.modes]
        return ' '.join(parts) + ' |0>'


def enumerate_pbw_states(weight: int) -> List[PBWState]:
    """Enumerate all PBW basis states of V_k(sl_2) at conformal weight h.

    States are monomials J^{a_1}_{-n_1} ... J^{a_r}_{-n_r} |0> with
    n_1 >= n_2 >= ... >= n_r >= 1, sum(n_i) = weight, and within each
    mode level, generators are in order e >= h >= f (descending lie_idx).

    The number of states equals p_3(weight), the 3-colored partition of weight.
    """
    if weight == 0:
        return [PBWState(modes=(), weight=0)]

    states = []
    _enum_pbw_recursive(weight, [], 1, 0, states)
    return states


def _enum_pbw_recursive(remaining: int, current: List[Tuple[int, int]],
                        min_mode: int, min_lie_at_mode: int,
                        results: List[PBWState]):
    """Recursively build PBW states by adding generators.

    PBW ordering: modes are non-decreasing, and within the same mode,
    lie indices are non-decreasing. This gives a canonical enumeration
    that produces exactly p_{dim_g}(weight) states.

    Args:
        remaining: remaining weight to distribute
        current: list of (lie_idx, mode) pairs built so far
        min_mode: minimum mode for the next generator
        min_lie_at_mode: minimum lie_idx when at the current min_mode
        results: accumulator for completed states
    """
    if remaining == 0:
        # Build PBW state in standard PBW order:
        # modes descending, within same mode lie_idx ASCENDING (e < h < f).
        # The tuple order represents the LEFT-TO-RIGHT operator product in the ket:
        # first element = leftmost operator = acts LAST on vacuum.
        # E.g., ((0,1),(2,1)) = J^e_{-1} J^f_{-1} |0> meaning J^f acts first.
        results.append(PBWState(
            modes=tuple(sorted(current, key=lambda x: (-x[1], x[0]))),
            weight=sum(m for _, m in current)
        ))
        return
    if remaining < min_mode:
        return

    for mode in range(min_mode, remaining + 1):
        # Within the same mode, enforce lie_idx >= min_lie_at_mode
        # For a new (higher) mode, allow any lie_idx from 0
        start_lie = min_lie_at_mode if mode == min_mode else 0
        for lie_idx in range(start_lie, DIM_SL2):
            current.append((lie_idx, mode))
            _enum_pbw_recursive(remaining - mode, current,
                                mode, lie_idx, results)
            current.pop()


def colored_partition_count(h: int, n_colors: int = 3) -> int:
    """Number of n_colors-colored partitions of h.

    Coefficient of q^h in prod_{n>=1} (1-q^n)^{-n_colors}.
    For sl_2: n_colors = 3.
    """
    if h < 0:
        return 0
    if h == 0:
        return 1
    coeffs = [0] * (h + 1)
    coeffs[0] = 1
    for n in range(1, h + 1):
        for _ in range(n_colors):
            for j in range(n, h + 1):
                coeffs[j] += coeffs[j - n]
    return coeffs[h]


