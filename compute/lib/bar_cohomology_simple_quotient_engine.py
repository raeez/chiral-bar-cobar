def kac_wakimoto_character_sl2(p: int, q: int, max_weight: int) -> Dict[int, int]:
    """Kac-Wakimoto character of L_k(sl_2) at admissible level.

    For integrable levels (q=1), this is the standard character:
    ch(L_k) = sum_h dim(L_k[h]) q^h.

    The character is computed from the Weyl-Kac formula for admissible levels.
    For integrable sl_2 at level k:
    ch(L_k) = (theta_{k+1,k+2} - theta_{-k-1,k+2}) / (theta_{1,2} - theta_{-1,2})
    where theta_{m,k}(q) = sum_{n in Z} q^{k*n^2 + m*n}.

    For practical purposes, we compute from the Shapovalov analysis.
    """
    engine = SimpleQuotientBarEngine(p, q, max_weight=max_weight)
    return {h: engine.quotient_dim(h) for h in range(max_weight + 1)}


def bar_ext_vs_ordinary_ext(p: int, q: int) -> Dict:
    """Compare bar-Ext and ordinary-Ext for L_k(sl_2).

    bar-Ext^n(L_k) = H^n(B(L_k)): intrinsic to the chiral algebra.
    ordinary-Ext^n(L_0, L_0): derived Hom in the module category.

    For L_k Koszul: these are related by the Koszul functor.
    For L_k non-Koszul: these diverge.

    For integrable levels: the ordinary module category is semisimple,
    so Ext^n = 0 for n >= 1. But bar-Ext^1 = sl_2 (dim 3) is NONZERO.
    This is NOT a contradiction: bar-Ext lives in the chiral algebra
    (factorization) category, not the ordinary module category.

    The bar-Ext captures the structure of the CHIRAL ALGEBRA itself
    (its OPE structure), while ordinary-Ext captures the structure of
    the MODULE CATEGORY. For a Koszul algebra, the Koszul functor
    provides a precise relationship between them.
    """
    engine = SimpleQuotientBarEngine(p, q, max_weight=8)
    result = engine.compute_bar_cohomology()

    is_integrable = (q == 1)
    k = Fraction(p, q) - 2
    n_modules = (p - 1) * q if not is_integrable else p - 1

    # Bar-Ext dimensions
    bar_ext = result.total_bar_cohom

    # Ordinary-Ext: for integrable levels, semisimple => Ext^n = 0
    # For admissible non-integrable: non-semisimple, Ext^1 may be nonzero
    ordinary_ext_1 = 0 if is_integrable else None  # unknown for non-integrable
    ordinary_ext_2 = 0 if is_integrable else None

    return {
        'p': p, 'q': q, 'k': str(k),
        'is_integrable': is_integrable,
        'n_modules': n_modules,
        'bar_ext': bar_ext,
        'ordinary_ext_1': ordinary_ext_1,
        'ordinary_ext_2': ordinary_ext_2,
        'bar_koszul': result.is_koszul,
        'discrepancy': (
            'bar-Ext^1 = 3 (sl_2 generators) while ordinary-Ext^1 = 0 (semisimple). '
            'No contradiction: bar-Ext is in the chiral category, ordinary-Ext in modules.'
        ) if is_integrable else (
            'bar-Ext^1 = 3, ordinary-Ext^1 unknown for non-integrable admissible.'
        ),
    }


def sweep_admissible_levels(max_p: int = 10, max_q: int = 5,
                            max_weight: int = 6) -> List[Dict]:
    """Sweep over admissible levels and check Koszulness."""
    results = []
    for p in range(2, max_p + 1):
        for q_val in range(1, max_q + 1):
            if gcd(p, q_val) != 1:
                continue
            k = Fraction(p, q_val) - 2
            h_null = (p - 1) * q_val
            engine = SimpleQuotientBarEngine(p, q_val,
                                             max_weight=min(max_weight, h_null + 2),
                                             max_arity=min(4, h_null + 1))
            result = engine.compute_bar_cohomology()
            structural = engine.koszulness_structural_analysis()

            results.append({
                'p': p, 'q': q_val,
                'k': str(k),
                'c': str(engine.c),
                'kappa': str(engine.kappa),
                'h_null': h_null,
                'is_koszul_computed': result.is_koszul,
                'is_koszul_structural': structural['is_koszul'],
                'bar_H1': result.total_bar_cohom.get(1, 0),
                'bar_H2': result.total_bar_cohom.get(2, 0),
                'mechanism': structural['mechanism'][:80],
            })

    return results
