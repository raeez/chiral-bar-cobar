def null_vector_weight_sl2(p: int, q: int) -> int:
    """First null vector weight in vacuum Verma of hat{sl}_2 at k = p/q - 2.

    From the Kac-Kazhdan determinant: h_null = (p-1)*q.

    This is the conformal weight of the FIRST singular vector in the
    vacuum Verma module M(k*Lambda_0).
    """
    return (p - 1) * q


def shapovalov_determinant_sl2(k: Fraction, h: int) -> Fraction:
    """Shapovalov determinant at weight h for vacuum Verma of hat{sl}_2.

    The Shapovalov determinant det(S_h) is a polynomial in k whose zeros
    determine where singular vectors occur.

    For hat{sl}_2 at level k, the Shapovalov matrix at weight h is the
    Gram matrix <v_i | v_j> where {v_i} is a PBW basis of M(k*Lambda_0)[h].

    The Kac-Kazhdan formula gives:
    det(S_h) = prod_{(alpha, n)} ((k+2)*n - <rho, alpha^v>)^{p(h - n*ht(alpha))}

    For sl_2, the product runs over:
    - alpha = delta - alpha_1, n >= 1: factor = (k+2)*n - 1 = (k+1) at n=1
    - alpha = delta + alpha_1, n >= 1: factor = (k+2)*n + 1 = (k+3) at n=1
    - alpha = delta, n >= 1: factor = (k+2)*n = (k+2) at n=1

    We compute the NUMERICAL value for the given k.
    """
    # For a practical computation, we build the Shapovalov matrix directly
    # using the PBW basis and the commutation relations with the invariant
    # form. This is exact for Fraction-valued k.
    states = enumerate_pbw_states(h)
    n = len(states)
    if n == 0:
        return Fraction(1)

    # Build the Gram matrix
    gram = _build_shapovalov_matrix(k, h, states)
    # Compute determinant (exact)
    return _exact_determinant(gram)


def _build_shapovalov_matrix(k: Fraction, h: int,
                             states: List[PBWState]) -> np.ndarray:
    """Build the Shapovalov (Gram) matrix for vacuum Verma at weight h.

    <v | w> is computed using the commutation relations of hat{sl}_2:
    [J^a_m, J^b_n] = [a,b]_{m+n} + m * k * (a,b) * delta_{m+n,0}

    The Gram matrix entry <v_i | v_j> = <0 | (v_i)^dagger * v_j | 0>
    where dagger sends J^a_{-m} to J^a_m (anti-involution).

    So <J^a_{-m} |0> | J^b_{-n} |0>> = <0 | J^a_m J^b_{-n} |0>
    = delta_{m,n} * (m * k * (a,b)) + <0 | J^b_{-n} J^a_m |0> (normal ordering)
    = delta_{m,n} * m * k * (a,b) (second term vanishes since J^a_m |0> = 0 for m > 0)

    For higher-weight states, we recursively commute creation operators past
    annihilation operators using the Lie bracket + central term.
    """
    n = len(states)
    gram = _frac_array((n, n))

    # The Shapovalov form uses the Chevalley anti-involution (sigma(e)=f,
    # sigma(f)=e, sigma(h)=h), implemented in _shapovalov_inner_product.
    # With this anti-involution, the form IS symmetric: <v|w> = <w|v>.
    for i in range(n):
        for j in range(i, n):
            val = _shapovalov_inner_product(k, states[i], states[j])
            gram[i, j] = val
            gram[j, i] = val

    return gram


def _shapovalov_inner_product(k: Fraction, left: PBWState,
                              right: PBWState) -> Fraction:
    """Compute the Shapovalov inner product <left | right> in the vacuum Verma.

    The Shapovalov form uses the CHEVALLEY anti-involution sigma:
        sigma(J^e_n) = J^f_{-n}
        sigma(J^f_n) = J^e_{-n}
        sigma(J^h_n) = J^h_{-n}

    So for a state v = J^{a_1}_{-n_1} ... J^{a_r}_{-n_r} |0>:
        <v| = <0| sigma(J^{a_r}_{-n_r}) ... sigma(J^{a_1}_{-n_1})
            = <0| sigma_lie(a_r)_{n_r} ... sigma_lie(a_1)_{n_1}

    where sigma_lie swaps e<->f and fixes h.

    The operator string for <left|right> is then:
        <0| sigma(left_r)_{n_r} ... sigma(left_1)_{n_1} right_1_{-m_1} ... right_s_{-m_s} |0>
    """
    ops: List[Tuple[int, int]] = []
    # Bra: apply Chevalley anti-involution and reverse order
    for a, m in reversed(left.modes):
        ops.append((_chevalley_sigma(a), m))  # sigma swaps e<->f, positive mode
    # Ket: modes with negative sign (creators)
    for a, m in right.modes:
        ops.append((a, -m))
    return _normal_order_vev(k, tuple(ops))


def _chevalley_sigma(lie_idx: int) -> int:
    """Chevalley anti-involution on sl_2 Lie algebra indices.

    sigma(e) = f, sigma(f) = e, sigma(h) = h.
    In our basis: 0=e, 1=h, 2=f.
    sigma(0) = 2, sigma(2) = 0, sigma(1) = 1.
    """
    if lie_idx == 0:
        return 2
    elif lie_idx == 2:
        return 0
    else:
        return 1


# Memoization cache for normal ordering VEV
_vev_cache: Dict = {}


def _normal_order_vev(k: Fraction, ops: Tuple[Tuple[int, int], ...]) -> Fraction:
    """Compute <0| O_1 O_2 ... O_n |0> by recursive normal ordering.

    ops: tuple of (lie_idx, mode) where mode > 0 is annihilator,
    mode < 0 is creator, mode = 0 is zero-mode.

    Recursion: find the rightmost adjacent pair (O_i, O_{i+1}) where
    O_i has mode >= 0 and O_{i+1} has mode < 0. Apply:
        O_i O_{i+1} = [O_i, O_{i+1}] + O_{i+1} O_i
    The swap term (O_{i+1} O_i) moves toward normal order.
    The commutator term replaces two operators by one (or a scalar).

    Base cases:
    - Empty: <0|0> = 1
    - Starts with creator (mode < 0): <0| J^a_{-m} ... = 0
    - Ends with annihilator/zero (mode >= 0): ... J^a_m |0> = 0
    """
    cache_key = (k, ops)
    if cache_key in _vev_cache:
        return _vev_cache[cache_key]

    if not ops:
        return Fraction(1)

    # Quick termination: leftmost creator or rightmost annihilator/zero
    if ops[0][1] < 0:
        _vev_cache[cache_key] = Fraction(0)
        return Fraction(0)
    if ops[-1][1] >= 0:
        _vev_cache[cache_key] = Fraction(0)
        return Fraction(0)

    # Find the RIGHTMOST adjacent non-normal pair
    # (rightmost = bubble sort from right, most efficient for nearly sorted)
    for i in range(len(ops) - 2, -1, -1):
        a_idx, a_mode = ops[i]
        b_idx, b_mode = ops[i + 1]
        if a_mode >= 0 and b_mode < 0:
            result = Fraction(0)

            # Term 1: swap (bubble sort step toward normal order)
            swapped = ops[:i] + (ops[i + 1], ops[i]) + ops[i + 2:]
            result += _normal_order_vev(k, swapped)

            # Term 2: commutator [J^a_{a_mode}, J^b_{b_mode}]
            # = [a,b]_{a_mode + b_mode} + a_mode * k * (a,b) * delta_{a_mode + b_mode, 0}
            sum_mode = a_mode + b_mode  # b_mode is negative

            # Central term: nonzero only when a_mode + b_mode = 0 AND a_mode != 0
            if sum_mode == 0 and a_mode != 0:
                killing = SL2_KILLING.get((a_idx, b_idx), Fraction(0))
                if killing != Fraction(0):
                    scalar = a_mode * k * killing
                    reduced = ops[:i] + ops[i + 2:]
                    result += scalar * _normal_order_vev(k, reduced)

            # Bracket term: [a, b] at mode sum_mode
            bracket_data = SL2_BRACKET.get((a_idx, b_idx))
            if bracket_data:
                for c_lie, coeff in bracket_data:
                    if sum_mode != 0 or True:  # always include
                        new_op = (c_lie, sum_mode)
                        reduced = ops[:i] + (new_op,) + ops[i + 2:]
                        result += coeff * _normal_order_vev(k, reduced)

            _vev_cache[cache_key] = result
            return result

    # Fully normal ordered but didn't hit base cases above
    # This means all creators are left of all annihilators/zeros
    # which gives 0 (creators on bra vacuum = 0).
    _vev_cache[cache_key] = Fraction(0)
    return Fraction(0)


def _wick_contract(k: Fraction,
                   annihilators: List[Tuple[int, int]],
                   creators: List[Tuple[int, int]]) -> Fraction:
    """Legacy wrapper: converts to operator string and calls _normal_order_vev."""
    ops: List[Tuple[int, int]] = []
    for a, m in annihilators:
        ops.append((a, m))
    for a, m in creators:
        ops.append((a, -m))
    return _normal_order_vev(k, tuple(ops))


def _exact_determinant(M: np.ndarray) -> Fraction:
    """Exact determinant of a Fraction matrix via Gaussian elimination."""
    n = M.shape[0]
    if n == 0:
        return Fraction(1)
    A = np.array([[_frac(M[i, j]) for j in range(n)] for i in range(n)], dtype=object)
    det = Fraction(1)
    for c in range(n):
        pivot = None
        for i in range(c, n):
            if A[i, c] != Fraction(0):
                pivot = i
                break
        if pivot is None:
            return Fraction(0)
        if pivot != c:
            A[[c, pivot]] = A[[pivot, c]]
            det = -det
        det *= A[c, c]
        scale = A[c, c]
        for j in range(c, n):
            A[c, j] = A[c, j] / scale
        for i in range(c + 1, n):
            factor = A[i, c]
            if factor != Fraction(0):
                for j in range(c, n):
                    A[i, j] = A[i, j] - factor * A[c, j]
    return det


