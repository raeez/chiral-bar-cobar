    def _quotient_bar_cohom(self, arity: int, weight: int) -> int:
        """Bar cohomology of L_k at weight >= h_null.

        At these weights, the null truncation may create new cohomology.
        We compute by building the bar differential matrix and computing
        kernel/image dimensions.

        However, for the KEY STRUCTURAL THEOREM, note:
        The bar differential for weight-1 generators encodes the Lie bracket.
        The null vector at weight h_null generates an IDEAL in the vertex algebra.
        The bar complex of the quotient L_k = V_k/N_k is NOT simply the
        quotient of bar complexes B(V_k)/B(N_k), because the bar complex
        involves tensor products and the differential involves the OPE.

        For weight-1 generators of sl_2, the bar arity-n complex at total
        weight n consists entirely of weight-1 tensor products. The null
        at weight h_null >= 2 does NOT affect the arity-n complex at weight n.
        It CAN affect higher-weight bar chains.

        The structural prediction: for sl_2 at integrable level k, the
        null at weight k+1 truncates bar chains at weight >= k+1. But
        the bar cohomology of V_k is already concentrated at weight n
        in arity n (for the CE complex identification), and the PBW
        collapse gives H^n = 0 for n >= 2. The quotient cannot create
        NEW cohomology classes unless the null vector introduces relations
        that change the image/kernel structure of the bar differential.
        """
        # For a rigorous computation: build the differential matrix
        # and compute rank. This is expensive but exact.
        chain_dim = self.bar_chain_dim(arity, weight)
        if chain_dim == 0:
            return 0

        # Kernel of outgoing differential
        d_out = self.bar_differential_matrix(arity, weight)
        if d_out.size == 0:
            ker_dim = chain_dim
        else:
            ker_dim = chain_dim - _exact_rank(d_out)

        # Image of incoming differential
        d_in = self.bar_differential_matrix(arity + 1, weight)
        if d_in.size == 0:
            im_dim = 0
        else:
            im_dim = _exact_rank(d_in)

        return ker_dim - im_dim

    # ============================================================
    # Main computation: full bar cohomology table
    # ============================================================

    def compute_bar_cohomology(self) -> SimpleQuotientBarResult:
        """Compute the full bar cohomology of L_k(sl_2)."""
        # Weight space dimensions
        verma_dims = {}
        null_dims_dict = {}
        quotient_dims = {}

        for h in range(self.max_weight + 1):
            v = self.verma_dim(h)
            q_dim = self.quotient_dim(h)
            verma_dims[h] = v
            null_dims_dict[h] = v - q_dim
            quotient_dims[h] = q_dim

        # Bar chain dimensions
        bar_chain = {}
        for n in range(self.max_arity + 1):
            for h in range(self.max_weight + 1):
                bar_chain[(n, h)] = self.bar_chain_dim(n, h)

        # Bar cohomology
        bar_cohom = {}
        for n in range(self.max_arity + 1):
            for h in range(self.max_weight + 1):
                bar_cohom[(n, h)] = self.bar_cohom_dim(n, h)

        # Totals
        total = {}
        for n in range(self.max_arity + 1):
            total[n] = sum(bar_cohom[(n, h)] for h in range(self.max_weight + 1))

        # Koszulness check
        is_koszul = all(bar_cohom[(n, h)] == 0
                        for n in range(2, self.max_arity + 1)
                        for h in range(self.max_weight + 1))

        # Off-diagonal classes
        off_diag = []
        for n in range(2, self.max_arity + 1):
            for h in range(self.max_weight + 1):
                d = bar_cohom[(n, h)]
                if d > 0:
                    off_diag.append((n, h, d))

        # Character comparison
        pbw_char = {}
        kw_char = {}
        char_diff = {}
        for h in range(self.max_weight + 1):
            pbw_char[h] = self.verma_dim(h)
            kw_char[h] = self.quotient_dim(h)
            char_diff[h] = pbw_char[h] - kw_char[h]

        return SimpleQuotientBarResult(
            p=self.p, q=self.q, k=self.k, c=self.c,
            kappa=self.kappa, h_null=self.h_null,
            max_weight=self.max_weight, max_arity=self.max_arity,
            verma_dims=verma_dims, null_dims=null_dims_dict,
            quotient_dims=quotient_dims,
            bar_chain_dims=bar_chain,
            bar_cohom_dims=bar_cohom,
            total_bar_cohom=total,
            is_koszul=is_koszul,
            off_diagonal=off_diag,
            pbw_character=pbw_char,
            kw_character=kw_char,
            character_difference=char_diff,
        )

