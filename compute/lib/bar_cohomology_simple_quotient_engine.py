def _compute_quotient_projector(gram: np.ndarray, n: int, rank: int) -> np.ndarray:
    """Compute projector onto the non-null subspace.

    Given the Shapovalov Gram matrix, the null space is its kernel.
    The quotient L_k[h] is identified with any complement of the kernel.
    We use the image of the Gram matrix as a canonical complement.

    Returns an (n x rank) matrix whose columns form a basis for L_k[h]
    expressed in the V_k[h] PBW basis.
    """
    if rank == n:
        # No nulls: identity projector
        proj = np.eye(n, dtype=object)
        for i in range(n):
            for j in range(n):
                proj[i, j] = Fraction(1) if i == j else Fraction(0)
        return proj

    if rank == 0:
        return _frac_array((n, 0))

    # Row reduce gram to find pivot columns (non-null states)
    A = np.array([[_frac(gram[i, j]) for j in range(n)] for i in range(n)], dtype=object)
    pivot_cols = []
    r = 0
    for c in range(n):
        p = None
        for i in range(r, n):
            if A[i, c] != Fraction(0):
                p = i
                break
        if p is None:
            continue
        A[[r, p]] = A[[p, r]]
        pivot_cols.append(c)
        scale = A[r, c]
        for j in range(n):
            A[r, j] = A[r, j] / scale
        for i in range(n):
            if i == r:
                continue
            factor = A[i, c]
            if factor != Fraction(0):
                for j in range(n):
                    A[i, j] = A[i, j] - factor * A[r, j]
        r += 1
        if r >= rank:
            break

    # Projector: columns are the standard basis vectors for pivot columns
    proj = _frac_array((n, rank))
    for j, pc in enumerate(pivot_cols[:rank]):
        proj[pc, j] = Fraction(1)

    return proj


