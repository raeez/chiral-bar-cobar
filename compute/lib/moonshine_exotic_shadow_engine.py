def virasoro_shadow_coefficient(r: int, c: Optional[Rational] = None) -> Rational:
    r"""Virasoro shadow coefficient S_r at central charge c.

    S_2 = c/2
    S_3 = 2 (universal for all Virasoro algebras)
    S_4 = 10/[c(5c+22)] (quartic contact)
    S_r for r >= 5: recursion from the shadow master equation.

    Default: c = 24 (for V^natural).
    """
    if c is None:
        c = Rational(24)
    if r < 2:
        return Rational(0)
    if r == 2:
        return c / 2
    if r == 3:
        return Rational(2)
    if r == 4:
        return Rational(10) / (c * (5 * c + 22))

    # Recursion for r >= 5
    obstruction = Rational(0)
    for j in range(3, (r + 2) // 2 + 1):
        k = r + 2 - j
        if k < 3:
            continue
        Sj = virasoro_shadow_coefficient(j, c)
        Sk = virasoro_shadow_coefficient(k, c)
        if j < k:
            obstruction += 2 * j * k * Sj * Sk / c
        else:  # j == k
            obstruction += j * k * Sj * Sk / c

    return Rational(-obstruction, 2 * r)


