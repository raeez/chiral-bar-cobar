def colored_jones_sl2(n: int, q: complex) -> complex:
    r"""n-colored Jones polynomial of the trefoil at quantum parameter q.

    The n-colored Jones polynomial J_n(K; q) uses the n-dimensional irrep
    V_{n-1} of U_q(sl_2).  For the trefoil (positive braid sigma_1^3 on 2 strands):

    J_n(3_1; q) = sum_{k=0}^{n-1} q^{k(k+3)/2} * prod_{j=1}^{k} (1 - q^{n+j}) / (1 - q^j)

    (Habiro's formula, normalized so J_1 = 1).
    Actually, we use the formula from the quantum sl_2 representation theory:

    For the trefoil (closure of sigma_1^3 on 2 strands), the colored Jones
    polynomial in color V_n (dimension n) is computed from the R-matrix eigenvalue.

    On V_{n-1} tensor V_{n-1}, the R-matrix has eigenvalues
        q^{j(j+1)/2 - n(n-1)/4} for j = 0, 1, ..., n-1
    (from the Clebsch-Gordan decomposition).

    For small n, we use direct computation via quantum 6j symbols or
    the skein relation.  Here we give an efficient implementation for
    small colors.
    """
    # Use the cabled Jones polynomial formula
    # For trefoil = closure of sigma_1^3 on 2 strands:
    # The quantum trace of R^3 in the n-dim rep gives J_n.
    #
    # R-matrix eigenvalues on V_{n-1} tensor V_{n-1}:
    # The decomposition is V_{n-1} tensor V_{n-1} = V_0 + V_2 + ... + V_{2n-2}
    # On the V_{2j} summand, the eigenvalue of check_R is
    # (-1)^j q^{j(2n-1-j)/2}   ... no, let me use the standard formula.
    #
    # For U_q(sl_2), the Casimir eigenvalue on V_m is
    # c_m = [m/2]_q [m/2 + 1]_q  where [x]_q = (q^x - q^{-x})/(q - q^{-1})
    #
    # The R-matrix eigenvalue on V_j inside V_a tensor V_b is
    # q^{(c_j - c_a - c_b)/2}
    #
    # For the trefoil (braid word sigma_1^3), the quantum trace is
    # sum_j dim_q(V_j) * (R-eigenvalue on V_j)^3 / dim_q(V_a tensor V_b)

    # Simpler approach: use the Rosso-Jones formula
    # J_n(trefoil; q) = sum_{j} q^{3*c_j/2} * [j+1]_q / [1]_q
    # where the sum is over j in the decomposition of V_{n-1}^{tensor 2}

    if abs(q - 1.0) < 1e-12:
        return complex(n)  # classical limit

    def q_number(m: int) -> complex:
        """[m]_q = (q^m - q^{-m}) / (q - q^{-1})."""
        if abs(q - 1.0 / q) < 1e-14:
            return complex(m)
        return (q ** m - q ** (-m)) / (q - q ** (-1))

    def q_dim(m: int) -> complex:
        """Quantum dimension of V_m = [m+1]_q."""
        return q_number(m + 1)

    def casimir_eigenvalue(m: int) -> complex:
        """Eigenvalue of the quadratic Casimir on V_m.

        c_m = m(m+2)/4 in the convention where R eigenvalue = q^{c_j - c_a - c_b}.
        """
        return m * (m + 2) / 4.0

    # Decomposition: V_{n-1} tensor V_{n-1} = V_0 + V_2 + ... + V_{2(n-1)}
    a = n - 1  # highest weight of the input
    c_a = casimir_eigenvalue(a)

    # Braid word sigma_1^3: the invariant is the quantum trace of R^3
    # divided by the quantum dimension of the input.
    # Quantum Markov trace:
    # J_n = sum_{j in decomp} q^{3 * (c_j - 2*c_a)} * q_dim(j) * q^{writhe_correction}
    #
    # More precisely, for the closure of sigma_1^{2m+1} on 2 strands:
    # Jones_n = (-1)^{n-1} * q^{(2m+1)(n^2-1)/4} * sum_{j=0}^{n-1}
    #           (-1)^j * q^{-(2m+1)*j(j+1)/2} * [2j+1]_q / [n]_q

    # Use the well-known Rosso-Jones formula (see Ohtsuki, Theorem 10.3.2):
    # J_n(K; q) for K = trefoil with framing 0 and braid index 3
    # is given by the quantum group invariant.
    #
    # For the trefoil as closure of sigma_1^3:
    # J_n = sum_{j=0}^{n-1} (-1)^j q^{3j(j+1)/2} [2j+1]_q /
    #        ( prod_{k=1}^j [n-k]_q [n+k]_q / [k]_q^2 * ... )
    #
    # Simpler: use Habiro's cyclotomic expansion
    # J_n(3_1; q) = sum_{k=0}^{n-1} q^{k(k+3)/2}
    #               * prod_{j=1}^{k} (1 - q^{n+j})*(1 - q^{-n+j}) / ((1-q^j)*(1-q^{-j}))
    #
    # Wait -- Habiro's formula is for the colored Jones of the trefoil with
    # a specific normalization.  Let me use a cleaner formula.
    #
    # Standard formula (Morton-Lukac / Garoufalidis-Le):
    # For the trefoil 3_1 (right-handed, closure of sigma_1^3):
    # J_n(q) = q^{(n^2-1)/2} * sum_{k=0}^{n-1}
    #          prod_{j=1}^k (q^{(n-j)/2} - q^{-(n-j)/2}) * (q^{(n+j)/2} - q^{-(n+j)/2})
    #          / (q^{j/2} - q^{-j/2})^2
    #          * q^{-k(k+3)/2}
    #
    # This is getting complicated.  Let me use the simplest correct formula.

    # The simplest route: the quantum 2-strand braid.
    # V_{n-1} tensor V_{n-1} = direct sum V_{2j} for j = 0, 1, ..., n-1
    # R-eigenvalue on V_{2j} component: (-1)^{(n-1-j)} * q^{(j(2j+2) - (n-1)(n+1))/2}
    #   = eps(j) * q^{j(j+1) - (n-1)(n+1)/2}
    #
    # Actually, for sl_2, the braiding eigenvalue on V_m inside V_a tensor V_a is
    #   lambda_m = (-1)^{a - m/2} * q^{(m(m+2) - 2a(a+2))/4}
    # where m runs over {0, 2, 4, ..., 2a} in steps of 2.
    # Setting a = n-1, m = 2j (j = 0, ..., n-1):
    #   lambda_{2j} = (-1)^{n-1-j} * q^{(2j(2j+2) - 2(n-1)(n+1))/4}
    #               = (-1)^{n-1-j} * q^{(j(j+1) - (n^2-1)/2)}

    result = complex(0)
    for j in range(n):
        m = 2 * j
        sign = (-1) ** (n - 1 - j)
        casimir_diff = (j * (j + 1) - (n * n - 1) / 2.0)
        lam = sign * q ** casimir_diff
        qdim = q_dim(m)
        # The quantum trace of R^3:
        # sum_j qdim(V_{2j}) * lambda_{2j}^3
        result += qdim * lam ** 3

    # Normalize: J_n = result / qdim(V_{n-1})
    overall_qdim = q_dim(n - 1)
    if abs(overall_qdim) < 1e-50:
        return complex(0)

    return result / overall_qdim


def colored_jones_figure_eight(n: int, q: complex) -> complex:
    r"""n-colored Jones polynomial of the figure-eight knot at parameter q.

    The figure-eight knot 4_1 = closure of sigma_1 sigma_2^{-1} sigma_1 sigma_2^{-1}
    on 3 strands.

    Uses the Habiro-Le formula (cyclotomic expansion):
    J_n(4_1; q) = sum_{k=0}^{n-1} prod_{j=1}^{k} |1 - q^{n+j}|^2 / |1 - q^j|^2

    More precisely (Masbaum formula):
    J_n(4_1; q) = sum_{k=0}^{n-1} prod_{j=1}^{k}
                  (q^{(n-j)/2} - q^{-(n-j)/2})(q^{(n+j)/2} - q^{-(n+j)/2})
                  / (q^{j/2} - q^{-j/2})^2

    where we use q^{1/2} as the square root of q.
    """
    if abs(q - 1.0) < 1e-12:
        return complex(n)

    q_half = q ** 0.5

    def q_bracket(m: int) -> complex:
        """(q^{m/2} - q^{-m/2})."""
        return q_half ** m - q_half ** (-m)

    result = complex(1)  # k=0 term
    running_product = complex(1)
    for k in range(1, n):
        factor_num = q_bracket(n - k) * q_bracket(n + k)
        factor_den = q_bracket(k) ** 2
        if abs(factor_den) < 1e-50:
            break
        running_product *= factor_num / factor_den
        result += running_product

    return result


def knot_invariant_zeta(knot: str, s: complex, q: complex,
                        n_max: int = 30) -> complex:
    r"""Knot invariant zeta function.

    zeta^K(s) = sum_{n=2}^{n_max} |J_n(K; q)|^{-s}

    where J_n is the n-colored Jones polynomial.  We start from n=2
    since J_1 = 1 for all knots (contributes 1 for all s).

    Args:
        knot: 'trefoil' or 'figure_eight'
        s: complex parameter
        q: quantum parameter (e.g., e^{2*pi*i/(k+2)})
        n_max: maximum color
    """
    result = complex(0)
    for n in range(2, n_max + 1):
        if knot == 'trefoil':
            jn = colored_jones_sl2(n, q)
        elif knot == 'figure_eight':
            jn = colored_jones_figure_eight(n, q)
        else:
            raise ValueError(f"Unknown knot: {knot}")
        abs_jn = abs(jn)
        if abs_jn > 1e-50:
            result += abs_jn ** (-s)
    return result


