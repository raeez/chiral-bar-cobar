# 5. Evaluation module zeta
# =========================================================================

def evaluation_module_zeta_sl2(s: complex, a: complex,
                               N_terms: int = 100) -> complex:
    r"""Evaluation module zeta for sl_2.

    zeta^{eval}_{sl_2}(s, a) = sum_{n >= 1} dim(V_n)^{-s} * chi_n(a)

    where chi_n(a) = trace of the evaluation morphism on V_n at parameter a.
    For sl_2, V_n is the (n+1)-dimensional irrep with highest weight n.
    The character evaluated at a diagonal element diag(a, a^{-1}) is:

        chi_n(a) = sum_{k=0}^{n} a^{n-2k} = (a^{n+1} - a^{-(n+1)}) / (a - a^{-1})

    (the Weyl character formula for sl_2).

    For the Yangian evaluation representation, the evaluation parameter u
    enters as a shift: V(u) has the same dimension as V but the Yangian
    generators act with a u-dependent twist.  At the level of dimensions,
    dim V(u) = dim V is independent of u.  The trace of the transfer matrix
    T(u) = tr_{aux} R_{aux,phys}(u) at the evaluation parameter u gives the
    Baxter T-Q eigenvalue, which for sl_2 at a Bethe root encodes the
    integrability condition.

    Here we use the simpler character-weighted version.
    """
    if abs(a - 1.0 / a) < 1e-15:
        # a = +-1: degenerate, use L'Hopital
        # chi_n(1) = n + 1
        return sum((n + 1) ** (-s) * (n + 1) for n in range(N_terms))

    result = complex(0)
    for n in range(N_terms):
        dim_n = n + 1
        # Character of V_n at diag(a, a^{-1})
        if abs(a - 1.0 / a) < 1e-10:
            chi = complex(dim_n)
        else:
            chi = (a ** (n + 1) - a ** (-(n + 1))) / (a - 1.0 / a)
        result += dim_n ** (-s) * chi
    return result


