# 8. MC3 thick generation function
# =========================================================================

def sl2_generation_function(n_max: int) -> List[int]:
    r"""Generation function g(n) for sl_2 DK category.

    g(n) = minimum number of evaluation modules V(a_i) needed to thick-generate
    all irreps V_k with k <= n.

    For sl_2, the evaluation modules V(a) at different spectral parameters a
    are ALL isomorphic as sl_2-modules to the fundamental V_1 = C^2.
    Thick generation means: the thick subcategory generated by {V(a_i)} contains
    all finite-dimensional representations.

    By the Clebsch-Gordan decomposition, V_1^{tensor n} contains V_n as a
    summand.  So a SINGLE evaluation module V(a) already thick-generates
    the entire category (since V_n is a direct summand of V(a)^{tensor(n+1)},
    and thick subcategories are closed under direct summands and finite limits).

    More precisely: g(n) = 1 for all n >= 0.  ONE evaluation module suffices.

    This is a consequence of MC3: the evaluation-generated core = full category.

    However, the tensor power needed grows: to reach V_n, we need tensor
    power n + 1 (or n in some conventions).  So the "generation complexity"
    is better measured by the tensor power, not the number of generators.
    """
    return [1] * (n_max + 1)


def sl2_tensor_generation_depth(n: int) -> int:
    r"""Minimum tensor power of V_1 needed to generate V_n.

    V_1^{tensor k} = V_k + V_{k-2} + V_{k-4} + ...
    So V_n first appears in V_1^{tensor n}.
    Generation depth = n.
    """
    return n


def slN_generation_function(N: int, max_weight: int) -> List[int]:
    r"""Generation function for sl_N DK category.

    For sl_N, the fundamental representation V = C^N is an evaluation module.
    The exterior powers Lambda^k(V) (k = 1, ..., N-1) give the fundamental
    representations.  Tensor products of these generate all irreps.

    g(n) counts the minimum number of distinct evaluation modules needed
    to generate all irreps with highest weight of total <= n.

    By MC3 (all simple types): a single evaluation module V(a) at a generic
    point thick-generates the entire category, since tensor powers of V(a)
    produce all fundamental representations (via Schur-Weyl duality), and
    these generate everything.

    Returns list g(0), g(1), ..., g(max_weight).
    """
    # For all types, MC3 gives g(n) = 1 for n >= 1
    return [0] + [1] * max_weight


def generation_zeta(s: complex, g_func: List[int]) -> complex:
    r"""Generation zeta: zeta^{gen}(s) = sum_{n >= 1} g(n)^{-s}.

    When g(n) = 1 for all n >= 1 (as in sl_2), this is
    sum_{n >= 1} 1 = infinity (diverges for all s).

    When g(n) grows, the series can converge.  For the tensor-power
    generation depth g(n) = n, this gives:
    sum_{n >= 1} n^{-s} = zeta(s).
    """
    result = complex(0)
    for n in range(1, len(g_func)):
        gn = g_func[n]
        if gn > 0:
            result += gn ** (-s)
    return result


def tensor_generation_zeta_sl2(s: complex, N_terms: int = 1000) -> complex:
    r"""Tensor generation zeta for sl_2.

    Using tensor generation depth g(n) = n:
    zeta^{gen}(s) = sum_{n >= 1} n^{-s} = zeta(s).

    This gives ANOTHER route from the DK category to the Riemann zeta:
    the representation counting zeta and the generation depth zeta
    BOTH equal zeta(s) for sl_2.
    """
    return sum(n ** (-s) for n in range(1, N_terms + 1))


