def dominant_weights_slN(N: int, max_total: int) -> List[Tuple[int, ...]]:
    """All dominant weights lambda = (a_1, ..., a_{N-1}) with sum(a_i) <= max_total.

    Returns weights in lexicographic order, EXCLUDING the zero weight (trivial rep).
    """
    rank = N - 1
    if rank == 0:
        return []
    result = []
    _enumerate_dominant(rank, max_total, [], result)
    return result


def _enumerate_dominant(rank: int, remaining: int,
                        partial: List[int], result: List[Tuple[int, ...]]) -> None:
    """Recursive enumeration of nonneg integer tuples of given length with bounded sum."""
    if len(partial) == rank:
        if any(c > 0 for c in partial):
            result.append(tuple(partial))
        return
    for c in range(remaining + 1):
        _enumerate_dominant(rank, remaining - c, partial + [c], result)


def dominant_weights_bounded_dim(typ: str, n: int,
                                 max_dim: int) -> List[Tuple[Tuple[int, ...], int]]:
    """All dominant weights whose irrep dimension is <= max_dim.

    Returns list of (hw, dim) pairs, sorted by dimension.
    Uses iterative deepening on the sum of fundamental weight coordinates.
    """
    results = []
    seen_dims = set()
    for total in range(1, max_dim + 1):
        weights = []
        _enumerate_dominant(n, total, [], weights)
        for hw in weights:
            d = weyl_dimension(typ, n, hw)
            if d <= max_dim and (hw, d) not in seen_dims:
                results.append((hw, d))
                seen_dims.add((hw, d))
        # Heuristic early exit: if all new weights produce dims > max_dim, stop
        if total > 2 and all(
            weyl_dimension(typ, n, tuple(
                total if i == j else 0 for i in range(n)
            )) > max_dim for j in range(n)
        ):
            break
    results.sort(key=lambda x: (x[1], x[0]))
    return results


# =========================================================================
# 2. Object counting zeta: zeta^{DK}_g(s)
# =========================================================================

def object_counting_zeta(typ: str, n: int, s: complex,
                         max_total_weight: int = 30,
                         include_trivial: bool = False) -> complex:
    r"""Categorical zeta function of the DK category.

    zeta^{DK}_g(s) = sum_{lambda dominant, nontrivial} dim(V_lambda)^{-s}

    For sl_2 (A_1): this equals sum_{n >= 1} (n+1)^{-s} = zeta(s) - 1
    where zeta(s) is the Riemann zeta function.  Or equivalently,
    sum_{d >= 2} d^{-s} = zeta(s) - 1.

    Note: the sl_2 nontrivial representations have dimensions 2, 3, 4, ...
    so the sum is zeta(s) - 1 (missing the d=1 term from the Riemann zeta).

    BUT: there are ALSO representations of dimension 1 (the trivial rep).
    If include_trivial=True: the sum is sum_{d >= 1} d^{-s} = zeta(s).
    Actually, for sl_2, dim(V_n) = n + 1 for n = 0, 1, 2, ...
    so the dimensions are {1, 2, 3, 4, ...} = all positive integers.
    Therefore INCLUDING the trivial rep, zeta^{DK}_{sl_2}(s) = zeta(s).

    CORRECTION: For higher rank, different irreps can have the SAME dimension.
    E.g., sl_3 has V(1,0) and V(0,1) both of dimension 3.  In the Dirichlet
    series, these contribute 2 * 3^{-s}.  The Dirichlet coefficients a_d count
    the NUMBER of irreps of dimension d:

        zeta^{DK}_g(s) = sum_{d >= 1} a_d * d^{-s}

    For sl_2: a_d = 1 for all d >= 1 (each dimension occurs exactly once).
    This gives zeta(s) exactly.

    Args:
        typ: Lie type ('A', 'B', 'C', 'D')
        n: rank
        s: complex parameter
        max_total_weight: cutoff for weight enumeration
        include_trivial: whether to include the trivial representation (dim=1)

    Returns:
        Partial sum of the Dirichlet series.
    """
    result = complex(0)
    if include_trivial:
        result += 1.0  # trivial rep contributes 1^{-s} = 1

    # Enumerate nontrivial dominant weights
    weights = []
    _enumerate_dominant(n, max_total_weight, [], weights)
    for hw in weights:
        d = weyl_dimension(typ, n, hw)
        if d > 0:
            result += d ** (-s)

    return result


def object_counting_zeta_sl2(s: complex, N_terms: int = 1000,
                             include_trivial: bool = True) -> complex:
    r"""Categorical zeta for sl_2 = sum_{n >= start} n^{-s}.

    If include_trivial: sum_{n >= 1} n^{-s} = zeta(s).
    If not include_trivial: sum_{n >= 2} n^{-s} = zeta(s) - 1.

    Uses direct partial sum (no analytic continuation).
    For Re(s) > 1, converges to the Riemann zeta.
    """
    start = 1 if include_trivial else 2
    result = complex(0)
    for n in range(start, start + N_terms):
        result += n ** (-s)
    return result


def riemann_zeta_partial(s: complex, N_terms: int = 10000) -> complex:
    """Partial sum of the Riemann zeta function: sum_{n=1}^{N} n^{-s}.

    For Re(s) > 1, converges to zeta(s).
    """
    return sum(n ** (-s) for n in range(1, N_terms + 1))


def riemann_zeta_euler_maclaurin(s: complex, N: int = 100) -> complex:
    """Riemann zeta via Euler-Maclaurin summation (better convergence).

    zeta(s) = sum_{n=1}^{N} n^{-s} + N^{1-s}/(s-1) + N^{-s}/2
              + sum_k B_{2k}/(2k)! * prod_{j=0}^{2k-2}(s+j) * N^{-(s+2k-1)}

    Uses first few Bernoulli correction terms for improved accuracy.
    """
    if abs(s - 1.0) < 1e-12:
        return complex(float('inf'))

    # Partial sum
    partial = sum(n ** (-s) for n in range(1, N + 1))

    # Euler-Maclaurin tail correction
    tail = N ** (1 - s) / (s - 1) + 0.5 * N ** (-s)

    # Bernoulli number corrections
    bernoulli = [
        Fraction(1, 6),       # B_2
        Fraction(-1, 30),     # B_4
        Fraction(1, 42),      # B_6
        Fraction(-1, 30),     # B_8
        Fraction(5, 66),      # B_10
        Fraction(-691, 2730), # B_12
    ]

    correction = complex(0)
    s_prod = s  # running product s(s+1)...(s+2k-2) / (2k)!
    for k_idx, bk in enumerate(bernoulli):
        k = k_idx + 1  # k = 1, 2, 3, ...
        # Factor = B_{2k} / (2k)! * s(s+1)...(s+2k-2) * N^{-(s+2k-1)}
        if k == 1:
            fact = float(bk) / 2.0 * s * N ** (-(s + 1))
        else:
            # s(s+1)...(s+2k-2) / (2k)!
            numerator_prod = complex(1)
            for j in range(2 * k - 1):
                numerator_prod *= (s + j)
            denom = math.factorial(2 * k)
            fact = float(bk) * numerator_prod / denom * N ** (-(s + 2 * k - 1))
        correction += fact

    return partial + tail + correction


# =========================================================================
# 3. Dirichlet coefficients: a_d = #{irreps of dimension d}
# =========================================================================

def dirichlet_coefficients(typ: str, n: int,
                           max_dim: int) -> Dict[int, int]:
    r"""Compute a_d = number of irreducible representations of dimension d.

    For sl_2: a_d = 1 for all d >= 1 (dimensions are 1, 2, 3, 4, ...).
    For sl_3: a_d can be > 1 (e.g., a_3 = 2 from V(1,0) and V(0,1)).

    Returns dict {d: a_d} for 1 <= d <= max_dim.
    """
    coeffs: Dict[int, int] = {}
    # Include trivial
    coeffs[1] = 1

    # Enumerate nontrivial dominant weights, iterating over total weight sum.
    # For each total, we enumerate weights with sum EXACTLY equal to total
    # (not <= total) to avoid double-counting.
    for total in range(1, max_dim + 1):
        weights_le: List[Tuple[int, ...]] = []
        _enumerate_dominant(n, total, [], weights_le)
        # Filter to weights with sum == total (exact, not <=)
        weights_exact = [w for w in weights_le if sum(w) == total]
        all_big = True
        for hw in weights_exact:
            d = weyl_dimension(typ, n, hw)
            if d <= max_dim:
                all_big = False
                coeffs[d] = coeffs.get(d, 0) + 1
        # Early exit: if all weights at this total have dim > max_dim,
        # all higher totals will too (dimensions grow monotonically)
        if total > 2 and all_big and len(weights_exact) > 0:
            break

    return coeffs


def dimension_spectrum(typ: str, n: int,
                       max_dim: int) -> List[Tuple[int, int]]:
    """Sorted list of (dim, multiplicity) pairs.

    The multiplicity is a_d = #{irreps of dimension d}.
    """
    coeffs = dirichlet_coefficients(typ, n, max_dim)
    return sorted(coeffs.items())


