def _cartan_matrix(typ: str, n: int) -> List[List[int]]:
    """Cartan matrix for simple Lie algebra of type (typ, n).

    Supports: A_n (sl_{n+1}), B_n (so_{2n+1}), C_n (sp_{2n}), D_n (so_{2n}).
    """
    A = [[0] * n for _ in range(n)]
    for i in range(n):
        A[i][i] = 2
    if typ == 'A':
        for i in range(n - 1):
            A[i][i + 1] = -1
            A[i + 1][i] = -1
    elif typ == 'B':
        for i in range(n - 1):
            A[i][i + 1] = -1
            A[i + 1][i] = -1
        if n >= 2:
            A[n - 2][n - 1] = -2  # short root
    elif typ == 'C':
        for i in range(n - 1):
            A[i][i + 1] = -1
            A[i + 1][i] = -1
        if n >= 2:
            A[n - 1][n - 2] = -2  # long root
    elif typ == 'D':
        for i in range(n - 2):
            A[i][i + 1] = -1
            A[i + 1][i] = -1
        if n >= 3:
            A[n - 3][n - 1] = -1
            A[n - 1][n - 3] = -1
    else:
        raise ValueError(f"Unsupported type: {typ}")
    return A


def _positive_roots(typ: str, n: int) -> List[Tuple[int, ...]]:
    """Positive roots in simple root coordinates for (typ, n)."""
    A = _cartan_matrix(typ, n)
    # Build positive roots by reflection algorithm
    roots = set()
    # Start with simple roots
    for i in range(n):
        r = [0] * n
        r[i] = 1
        roots.add(tuple(r))

    changed = True
    max_iter = 200
    it = 0
    while changed and it < max_iter:
        changed = False
        it += 1
        new_roots = set()
        for r in list(roots):
            for i in range(n):
                # Reflect root r in simple root alpha_i
                # s_i(r) = r - <r, alpha_i^vee> * alpha_i
                # <r, alpha_i^vee> = sum_j r_j * A[j][i]
                inner = sum(r[j] * A[j][i] for j in range(n))
                new_r = list(r)
                new_r[i] -= inner
                new_r_t = tuple(new_r)
                if all(c >= 0 for c in new_r) and new_r_t not in roots:
                    new_roots.add(new_r_t)
                    changed = True
        roots.update(new_roots)

    return sorted(roots)


def _positive_coroots(typ: str, n: int) -> List[Tuple[int, ...]]:
    """Positive coroots in simple coroot coordinates.

    For the pairing <lambda, beta^vee> with lambda in fund wt coords,
    the coroots of A^T are the positive roots of the TRANSPOSE Cartan matrix.
    """
    A = _cartan_matrix(typ, n)
    AT = [[A[j][i] for j in range(n)] for i in range(n)]
    # Positive roots of AT
    roots = set()
    for i in range(n):
        r = [0] * n
        r[i] = 1
        roots.add(tuple(r))

    changed = True
    max_iter = 200
    it = 0
    while changed and it < max_iter:
        changed = False
        it += 1
        new_roots = set()
        for r in list(roots):
            for i in range(n):
                inner = sum(r[j] * AT[j][i] for j in range(n))
                new_r = list(r)
                new_r[i] -= inner
                new_r_t = tuple(new_r)
                if all(c >= 0 for c in new_r) and new_r_t not in roots:
                    new_roots.add(new_r_t)
                    changed = True
        roots.update(new_roots)

    return sorted(roots)


def weyl_dimension(typ: str, n: int, hw: Tuple[int, ...]) -> int:
    """Dimension of V_lambda via the Weyl dimension formula.

    hw = (lambda_1, ..., lambda_n) in fundamental weight coordinates.

    dim V_lambda = prod_{beta^vee > 0} <lambda + rho, beta^vee> / <rho, beta^vee>

    where rho = (1, 1, ..., 1) in fundamental weight coordinates.
    """
    if any(c < 0 for c in hw):
        return 0

    coroots = _positive_coroots(typ, n)
    rho = tuple(1 for _ in range(n))
    hw_rho = tuple(hw[i] + rho[i] for i in range(n))

    dim = Fraction(1)
    for cv in coroots:
        num = sum(hw_rho[j] * cv[j] for j in range(n))
        den = sum(rho[j] * cv[j] for j in range(n))
        if den == 0:
            continue
        dim *= Fraction(num, den)

    return int(dim)


def weyl_dimension_slN(N: int, hw: Tuple[int, ...]) -> int:
    """Weyl dimension for sl_N = A_{N-1}.

    hw in fundamental weight coordinates of sl_N (length N-1).
    """
    return weyl_dimension('A', N - 1, hw)


def weyl_dimension_sl2(n: int) -> int:
    """Dimension of the irreducible sl_2 representation V_n.

    V_n has highest weight n (= n * omega_1).  dim V_n = n + 1.
    """
    return n + 1


def weyl_dimension_sl3(a: int, b: int) -> int:
    """Dimension of the irreducible sl_3 representation V(a, b).

    V(a*omega_1 + b*omega_2) has dim = (a+1)(b+1)(a+b+2)/2.
    """
    if a < 0 or b < 0:
        return 0
    return (a + 1) * (b + 1) * (a + b + 2) // 2


