def slN_zeta_abscissa(N: int, max_total: int = 20) -> float:
    r"""Estimate the abscissa of convergence of zeta^{DK}_{sl_N}(s).

    The abscissa sigma_c is the infimum of Re(s) such that the Dirichlet
    series converges.

    For sl_2: sigma_c = 1 (same as Riemann zeta).

    For sl_N with N >= 3: the dominant weights grow like a lattice cone
    in R^{N-1}, and the dimensions grow polynomially.  The Weyl dimension
    formula gives dim V_lambda ~ C * prod_{alpha > 0} <lambda, alpha> for
    large lambda.  The number of irreps with dim <= D grows like D^alpha
    for some alpha depending on the rank.  The abscissa is determined by
    this growth rate.

    For sl_3: dimensions grow like ~ (total_weight)^3 / 2, and the number
    of irreps with total weight = k is ~ k (linear).  So the Dirichlet
    series behaves like sum_k k * (k^3)^{-s} = sum_k k^{1-3s}, converging
    for Re(s) > 2/3.

    General sl_N: the number of dominant weights with total <= k is
    C(k+r-1, r-1) ~ k^{r-1}/(r-1)! where r = rank = N-1.  The typical
    dimension at total k is ~ k^{|Phi^+|} where |Phi^+| = N(N-1)/2.
    So the series behaves like sum_k k^{r-1} * k^{-|Phi^+| * s},
    converging for Re(s) > r / |Phi^+| = (N-1) / (N(N-1)/2) = 2/N.

    Returns numerical estimate.
    """
    # Theoretical prediction: sigma_c = 2/N for sl_N
    return 2.0 / N


def verify_sl2_is_riemann_zeta(s: float, N_terms: int = 5000) -> Dict[str, Any]:
    r"""Verify that zeta^{DK}_{sl_2}(s) = zeta(s) for real s > 1.

    Computes the categorical zeta via direct representation counting
    and compares with the Riemann zeta via Euler-Maclaurin.

    This is the KEY IDENTITY: for sl_2, every positive integer occurs
    exactly once as the dimension of an irreducible representation
    (dim V_n = n + 1), so the categorical zeta IS the Riemann zeta.
    """
    # Path 1: Direct representation counting
    cat_zeta = sum((n + 1) ** (-s) for n in range(N_terms))

    # Path 2: Riemann zeta partial sum
    riem_partial = sum(n ** (-s) for n in range(1, N_terms + 1))

    # Path 3: Euler-Maclaurin
    riem_em = riemann_zeta_euler_maclaurin(s, N=200)

    # Path 4: Euler product
    primes = _sieve_primes(200)
    euler = complex(1)
    for p in primes:
        euler *= 1.0 / (1.0 - p ** (-s))

    return {
        'categorical_zeta': cat_zeta,
        'riemann_partial_sum': riem_partial,
        'riemann_euler_maclaurin': riem_em,
        'euler_product': euler,
        'cat_vs_partial_diff': abs(cat_zeta - riem_partial),
        'cat_vs_em_diff': abs(cat_zeta - riem_em.real),
        'euler_vs_em_diff': abs(euler.real - riem_em.real),
        's': s,
        'N_terms': N_terms,
    }


