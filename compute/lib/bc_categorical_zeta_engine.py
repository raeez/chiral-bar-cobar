def zeta_rank_scaling(s: float, N_max: int = 6,
                      max_total: int = 12) -> List[Tuple[int, complex]]:
    r"""How the categorical zeta grows with the rank N of sl_N.

    For large rank, the number of representations grows rapidly, so
    the categorical zeta increases.  The abscissa of convergence
    decreases as 2/N.
    """
    results = []
    for N in range(2, N_max + 1):
        z = object_counting_zeta_slN(N, s, max_total)
        results.append((N, z))
    return results


