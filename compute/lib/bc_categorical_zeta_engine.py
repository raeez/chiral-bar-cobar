r"""Categorical zeta functions from the Drinfeld-Kohno category.

Mathematical foundation
-----------------------
The Drinfeld-Kohno (DK) category of type g has objects = finite-dimensional
representations V_lambda of the quantum group U_q(g) (or equivalently, of the
Yangian Y(g) via the DK bridge), and morphisms = intertwiners.  MC3
(thm:categorical-cg-all-types, cor:mc3-all-types) proves that the evaluation
modules {V(a_i)} thick-generate this category for ALL simple Lie types.

The categorical zeta function counts irreducible objects weighted by dimension:

    zeta^{DK}_g(s) = sum_{lambda dominant, V_lambda != 0} dim(V_lambda)^{-s}

This is the Dirichlet series generated by the dimensions of irreducible
representations of g.

KEY IDENTITY (sl_2):
    For g = sl_2, the irreducible representations are V_n with dim = n+1 for
    n = 0, 1, 2, ...  The nontrivial irreps have dimensions 1, 2, 3, 4, ...
    Therefore:
        zeta^{DK}_{sl_2}(s) = sum_{n >= 1} n^{-s} = zeta(s)
    where zeta(s) is the Riemann zeta function.

    More precisely: with trivial rep (dim=1) INCLUDED, the sum is zeta(s)
    (since 1^{-s} + 2^{-s} + 3^{-s} + ... = zeta(s)).  The function
    object_counting_zeta() defaults to include_trivial=False, returning
    zeta(s) - 1.  Set include_trivial=True to get the full zeta(s).

This is NOT a coincidence --- the sl_2 representation ring is Z[x] with
x = [V_1], and dim: Z[x] -> Z sends x -> 2, so the dimension zeta is the
Dirichlet series of Z_>0 = {dim V_n : n >= 0, V_n nontrivial}.

For higher rank, the representation dimensions are given by the Weyl dimension
formula and the zeta function has richer structure:
    sl_3: dims = 1, 3, 3*, 6, 8, 10, 15, 15*, ...
    sl_4: dims = 1, 4, 4*, 6, 10, 15, 20, ...

The Euler product structure reflects the "prime decomposition" of representations
under tensor product (i.e., the structure of the representation ring).

Verification paths
------------------
    Path 1: Direct summation of dim(V_lambda)^{-s} over dominant weights
    Path 2: Weyl dimension formula (independent computation of dim)
    Path 3: For sl_2: comparison with scipy.special.zeta (Riemann zeta)
    Path 4: Euler product from prime indecomposable decomposition
    Path 5: Functional equation / analytic continuation checks
    Path 6: Growth asymptotics (Weyl law for rep dimensions)

Connections to the monograph
----------------------------
    - MC3 (cor:mc3-all-types): thick generation by evaluation modules
    - DK bridge (thm:drinfeld-kohno-bridge): Y(g) <-> U_q(hat{g})
    - Yangian R-matrix: genus-0 binary shadow of Theta_A
    - kappa(A): the dimension zeta at s=0 relates to kappa via regularization
    - Transfer matrix T(a): eigenvalues at evaluation parameter a encode
      how the integrable system "sees" the zeta at that spectral parameter

Conventions
-----------
    - Highest weight lambda in fundamental weight coordinates (Bourbaki).
    - dim V_lambda via the Weyl dimension formula.
    - Dirichlet series convention: sum a_n n^{-s} with n = dim(V_lambda).
    - Quantum parameter q = exp(2*pi*i / (k+h^vee)) for level k.
    - Cohomological grading (|d| = +1).

References
----------
    Chari-Pressley, "A Guide to Quantum Groups", Cambridge 1994.
    Molev, "Yangians and classical Lie algebras", AMS 2007.
    thm:categorical-cg-all-types (yangians_drinfeld_kohno.tex)
    concordance.tex: MC3 status
"""

