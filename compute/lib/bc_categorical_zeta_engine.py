# 7. Grothendieck ring K_0 and its zeta
# =========================================================================

def sl2_representation_ring_zeta(s: complex, N_terms: int = 200) -> complex:
    r"""Zeta function of the sl_2 representation ring K_0(Rep(sl_2)).

    K_0(Rep(sl_2)) = Z[x] where x = [V_1] (the fundamental representation).
    Under the dimension homomorphism dim: K_0 -> Z, x -> 2.

    The tensor product structure:
        V_a tensor V_b = V_{|a-b|} + V_{|a-b|+2} + ... + V_{a+b}
    gives the Clebsch-Gordan multiplication in K_0.

    The "Hasse-Weil zeta of Spec(K_0)" is the zeta function of the ring Z[x].
    Since Z[x] is a polynomial ring over Z, Spec(Z[x]) = A^1_Z.
    The Hasse-Weil zeta of A^1_Z is:

        Z(A^1_Z, s) = prod_p zeta_{F_p[x]}(s)
                     = prod_p 1/((1 - p^{-s})(1 - p^{1-s}))
                     = zeta(s) * zeta(s-1)

    Partial sum approximation.
    """
    # zeta(s) * zeta(s-1) via partial sums
    z1 = riemann_zeta_euler_maclaurin(s, N=100)
    z2 = riemann_zeta_euler_maclaurin(s - 1, N=100)
    return z1 * z2


