# 4. Euler product structure
# =========================================================================

def sl2_euler_product(s: complex, N_primes: int = 100) -> complex:
    r"""Euler product for the sl_2 categorical zeta = Riemann zeta.

    zeta(s) = prod_{p prime} (1 - p^{-s})^{-1}

    For sl_2, every representation V_n is indecomposable (simple), and the
    tensor product V_a x V_b decomposes via the Clebsch-Gordan series:
        V_a x V_b = V_{|a-b|} + V_{|a-b|+2} + ... + V_{a+b}

    The "primes" in the representation ring are the V_n themselves (each is
    simple, hence indecomposable).  However, the tensor product is NOT
    analogous to integer multiplication --- the dimension is multiplicative
    (dim(V_a x V_b) = dim(V_a) * dim(V_b)), but the decomposition is
    additive in the Grothendieck group.

    The Euler product of zeta(s) uses the ORDINARY primes p, coming from
    the factorization of the integer d (the dimension).  This is because
    zeta^{DK}_{sl_2}(s) = sum_{d >= 1} d^{-s} = zeta(s), and the Euler
    product of zeta(s) is prod_p (1 - p^{-s})^{-1}.

    This is a DIMENSION-LEVEL Euler product, not a representation-level one.
    """
    primes = _sieve_primes(N_primes)
    result = complex(1)
    for p in primes:
        result *= 1.0 / (1.0 - p ** (-s))
    return result


def _sieve_primes(count: int) -> List[int]:
    """Return the first `count` primes via sieve of Eratosthenes."""
    if count <= 0:
        return []
    # Upper bound for the count-th prime (generous)
    if count < 6:
        limit = 15
    else:
        limit = int(count * (math.log(count) + math.log(math.log(count)) + 2)) + 10
    sieve = [True] * (limit + 1)
    sieve[0] = sieve[1] = False
    for i in range(2, int(limit ** 0.5) + 1):
        if sieve[i]:
            for j in range(i * i, limit + 1, i):
                sieve[j] = False
    primes = [i for i in range(2, limit + 1) if sieve[i]]
    return primes[:count]


