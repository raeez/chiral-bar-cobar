def multipath_sl2_zeta(s: float) -> Dict[str, Any]:
    r"""5-path verification that zeta^{DK}_{sl_2}(s) = zeta(s).

    Path 1: Direct sum over representations dim(V_n)^{-s}
    Path 2: Sum over positive integers n^{-s} (Weyl dimension gives bijection)
    Path 3: Euler-Maclaurin accelerated Riemann zeta
    Path 4: Euler product over primes
    Path 5: Dirichlet coefficient check (a_d = 1 for all d)
    """
    N = 5000

    # Path 1: sum dim(V_n)^{-s} for n = 0, 1, ...
    path1 = sum(weyl_dimension_sl2(n) ** (-s) for n in range(N))

    # Path 2: sum n^{-s} for n = 1, 2, ...
    path2 = sum(n ** (-s) for n in range(1, N + 1))

    # Path 3: Euler-Maclaurin
    path3 = riemann_zeta_euler_maclaurin(s, N=200)

    # Path 4: Euler product
    primes = _sieve_primes(300)
    path4 = complex(1)
    for p in primes:
        path4 *= 1.0 / (1.0 - p ** (-s))

    # Path 5: Dirichlet coefficients
    coeffs = dirichlet_coefficients('A', 1, 100)
    all_ones = all(v == 1 for v in coeffs.values())

    return {
        'path1_rep_counting': path1,
        'path2_integer_sum': path2,
        'path3_euler_maclaurin': path3.real,
        'path4_euler_product': path4.real,
        'path5_dirichlet_all_ones': all_ones,
        'max_pairwise_diff': max(
            abs(path1 - path2),
            abs(path1 - path3.real),
            abs(path1 - path4.real),
            abs(path2 - path3.real),
        ),
    }


def multipath_sl3_dimensions() -> Dict[str, Any]:
    r"""Verify sl_3 dimensions via two independent methods.

    Path 1: Weyl dimension formula dim V(a,b) = (a+1)(b+1)(a+b+2)/2
    Path 2: General Weyl dimension via positive coroots
    """
    results = {}
    mismatches = 0
    for a in range(8):
        for b in range(8):
            d1 = weyl_dimension_sl3(a, b)
            d2 = weyl_dimension('A', 2, (a, b))
            match = (d1 == d2)
            if not match:
                mismatches += 1
            results[(a, b)] = {'direct': d1, 'weyl': d2, 'match': match}

    return {
        'all_match': mismatches == 0,
        'n_checked': len(results),
        'n_mismatches': mismatches,
        'details': results,
    }
