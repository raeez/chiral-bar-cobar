def object_counting_zeta_sl3(s: complex, max_total: int = 20) -> complex:
    r"""Categorical zeta for sl_3.

    zeta^{DK}_{sl_3}(s) = sum_{(a,b) != (0,0)} dim(V(a,b))^{-s}

    Dimensions: dim V(a,b) = (a+1)(b+1)(a+b+2)/2.

    The first few: 3, 3, 6, 8, 10, 10, 15, 15, 15, 21, 24, 24, ...
    Note: V(1,0) and V(0,1) both have dim 3 (fundamental and antifundamental).
    """
    result = complex(0)
    for total in range(1, max_total + 1):
        for a in range(total + 1):
            b = total - a
            d = weyl_dimension_sl3(a, b)
            if d > 0:
                result += d ** (-s)
    return result


def object_counting_zeta_slN(N: int, s: complex, max_total: int = 15) -> complex:
    r"""Categorical zeta for sl_N = A_{N-1}.

    zeta^{DK}_{sl_N}(s) = sum_{lambda nontrivial dominant} dim(V_lambda)^{-s}
    """
    rank = N - 1
    if rank == 0:
        return complex(0)

    result = complex(0)
    weights = dominant_weights_slN(N, max_total)
    for hw in weights:
        d = weyl_dimension('A', rank, hw)
        if d > 0:
            result += d ** (-s)
    return result


