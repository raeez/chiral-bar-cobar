r"""Spectral curve engine for the quadratic shadow curve.

The shadow obstruction tower generates rational functions S_r(c) at arity r.
On a stationary rank-one primary line, the MC equation gives a Riccati
diagnostic for the weighted generating function
H(t) = t^2 sqrt(Q_L(t)).

The shadow spectral curve is the double cover defined by
    y^2 = Q_L(t) = q_0 + q_1 t + q_2 t^2
where Q_L is the shadow metric.  This finite curve records the stationary
Riccati data on the chosen primary line.  It is not, by itself, the full KdV,
Eynard-Orantin, Seiberg-Witten, JT, or Weil-Petersson spectral curve; those
require separately supplied Lax/recursion/sewing/descendant data.

This module implements:

1. **Shadow ODE to algebraic curve** (Section 1): Riccati structure and
   extraction of the shadow curve y^2 = Q(t) from primary-line data.

2. **Branch points** (Section 2): Zeros of Q_L(t) as functions of c,
   transition between complex conjugate and real branch points at
   the critical central charge c* ~ 6.125.

3. **Monodromy** (Section 3): Numerical path integration around branch
   points, verifying the square-root sign monodromy -1.

4. **Period integrals** (Section 4): elementary contour integrals of dt/y
   on the genus-zero shadow curve; no elliptic period lattice is asserted.

5. **Auxiliary theta functions** (Section 5): standard theta functions on
   an externally supplied lattice; the rank-one shadow curve itself has
   trivial Jacobian.

6. **Multi-channel generalization** (Section 6): W_3 two-channel
   spectral curve from the bivariate shadow metric.

7. **External integrable-system comparisons** (Section 7): finite
   genus/radius diagnostics against separately supplied Toda or KdV data.
   The engine does not construct a full dg-shifted Yangian, KdV hierarchy,
   or EO recursion from Q_L alone.

References:
    thm:riccati-algebraicity (higher_genus_modular_koszul.tex)
    thm:shadow-connection (higher_genus_modular_koszul.tex)
    def:shadow-metric (higher_genus_modular_koszul.tex)
    thm:single-line-dichotomy (higher_genus_modular_koszul.tex)
    thm:shadow-radius (higher_genus_modular_koszul.tex)
    shadow_radius.py -- shadow growth rate
    shadow_tower_ode.py -- shadow obstruction tower ODE recursion
    shadow_hamilton_jacobi.py -- 2D Hamilton-Jacobi structure
"""

