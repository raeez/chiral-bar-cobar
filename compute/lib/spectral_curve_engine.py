def verdier_dual_scalar_lane(c_val: float) -> dict:
    """Virasoro scalar complementarity under c -> 26-c.

    For Virasoro, the Verdier-dual scalar lane sends c to 26-c, hence
    Q_Vir(t; c) to Q_Vir(t; 26-c).  This compares shadow curves attached to
    two scalar parameters.  It is not an assertion that the curve itself
    constructs the Koszul dual algebra A^!.

    The duality acts on invariants:
    - kappa -> kappa' = (26-c)/2
    - Delta -> Delta' = 40/(152-5c)
    - rho -> rho' = sqrt((180(26-c)+872)/((5(26-c)+22)(26-c)^2))
    - branch points t_+/- transform

    At c = 13 (self-dual scalar parameter): the listed scalar invariants are
    fixed.
    """
    inv = spectral_invariants(c_val)
    c_dual = 26.0 - c_val
    inv_dual = spectral_invariants(c_dual)

    return {
        'c': c_val,
        'c_dual': c_dual,
        'rho': inv['rho'],
        'rho_dual': inv_dual['rho'],
        'Delta': inv['Delta'],
        'Delta_dual': inv_dual['Delta'],
        'complementarity': complementarity_of_discriminants(c_val),
        'self_dual': abs(c_val - 13.0) < 1e-10,
        'scope': 'Verdier-dual scalar lane; not curve-level Koszul duality',
    }


def koszul_duality_on_curve(c_val: float) -> dict:
    """Compatibility wrapper for the older API name.

    The returned data are the Virasoro Verdier-dual scalar lane.  The shadow
    curve does not construct A^!, and Omega(B(A)) = A is bar-cobar inversion,
    not Koszul duality.
    """
    return verdier_dual_scalar_lane(c_val)


