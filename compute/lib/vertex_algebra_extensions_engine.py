@dataclass
class SimpleCurrentExtensionData:
    """Data for a simple current extension.

    A simple current J is a simple module satisfying:
    J x M is simple for every simple module M (fusion is a permutation).

    The extension V_ext = V + J (as V-module) becomes a VOA when
    the conformal weight h_J is in Z (or Z + 1/2 for super case).

    Key example: lattice VOA V_{sqrt{2k} Z} extended by J = V_{sqrt{2k} Z + sqrt{k/2}}
    gives the level-k affine sl_2 VOA.
    """
    base_name: str
    current_name: str
    h_J: Fraction              # conformal weight of J
    c: Fraction                # same as base (extension preserves c)
    kappa_base: Fraction
    kappa_extended: Fraction   # may differ from kappa_base
    order: int                 # order of J in the fusion group (J^n = V)
    notes: str = ""


def lattice_extension_to_sl2(k: int) -> SimpleCurrentExtensionData:
    """Lattice VOA extension giving affine sl_2.

    V_{A_1} (A_1 root lattice at level k) is a simple current extension
    of the Heisenberg algebra H_k = V_{sqrt{2k}Z}.

    The A_1 root lattice is Z * alpha with <alpha, alpha> = 2.
    At level k: the relevant lattice is sqrt(k) * A_1, so
    V_{sqrt(k) A_1} has c = 1.

    For the sl_2 identification:
    - Base: Heisenberg H at level k (kappa = k)
    - Extension: add vertex operators e^{+-alpha}
    - Result: V_k(sl_2) (universal affine sl_2)
    - kappa(V_k(sl_2)) = 3(k+2)/4

    The kappa CHANGES under extension:
    kappa(Heisenberg_k) = k
    kappa(V_k(sl_2)) = 3(k+2)/4

    At k=1: kappa(H_1) = 1, kappa(V_1(sl_2)) = 9/4.
    The extension MORE than doubles kappa because the new generators
    (e, f at weight 1) contribute additional genus-1 data.
    """
    if k < 1:
        raise ValueError(f"Need k >= 1, got {k}")

    c = Fraction(1)  # single free boson, c = 1
    # Wait -- the full sl_2 has c = 3k/(k+2), but for k=1 that's 1.
    # For general k, the Heisenberg at level k has c = 1 (single boson).
    # The affine sl_2 at level k has c = 3k/(k+2).
    # But the sl_2 at level k is NOT a simple current extension of
    # a single Heisenberg at level k. Rather:
    # V_k(sl_2) contains H = Cartan subalgebra (level k Heisenberg, c=1)
    # plus root vertex operators, and the full c = 3k/(k+2).
    #
    # Actually for k=1, sl_2 at level 1 has c = 1, and indeed
    # V_1(sl_2) = V_{A_1} (the A_1 root lattice VOA). This is a
    # Z/2-simple-current extension of the Heisenberg.
    #
    # For general k: the relationship between sl_2 at level k
    # and Heisenberg is NOT a simple current extension.
    # The correct statement: V_{sqrt(2k) Z} (lattice VOA, c=1)
    # can be extended by simple currents, but the result is a
    # LATTICE VOA V_{Gamma} for a larger lattice, not necessarily sl_2.
    #
    # For the canonical example: k=1 only.
    # V_1(sl_2) = V_{A_1} = Heisenberg + {e^{+- alpha}} extension.

    c_sl2 = Fraction(3 * k, k + 2)
    kappa_heisenberg = Fraction(k)
    kappa_sl2 = Fraction(3 * (k + 2), 4)

    return SimpleCurrentExtensionData(
        base_name=f"H_k (k={k})",
        current_name=f"e^{{alpha}} (root vertex operators)",
        h_J=Fraction(1),   # e^alpha has weight alpha^2/2 = 1 for A_1
        c=c_sl2,
        kappa_base=kappa_heisenberg,
        kappa_extended=kappa_sl2,
        order=2,  # (e^alpha)^2 = identity in fusion group
        notes=f"V_{k}(sl_2) as extension of H_{k}; kappa changes by {kappa_sl2 - kappa_heisenberg}",
    )


def kappa_change_under_extension(ext: SimpleCurrentExtensionData) -> Fraction:
    """Compute the change in kappa under simple current extension.

    delta_kappa = kappa(extended) - kappa(base)

    This is generically nonzero: adding generators changes the
    genus-1 obstruction.
    """
    return ext.kappa_extended - ext.kappa_base


