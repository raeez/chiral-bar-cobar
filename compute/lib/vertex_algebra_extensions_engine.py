def associated_variety_dim(voa_name: str, **params) -> Dict[str, Any]:
    """Compute the dimension of the associated variety X_V = Spec(R_V).

    Key results (Arakawa):
    - X_{V_k(g)} = g* (affine space, dim = dim(g)) for universal
    - X_{L_k(g)} = closure of a nilpotent orbit (for admissible k)
    - X_{Virasoro} = C (dim = 1)
    - X_{W_N} = C^{N-1} (dim = N-1) for universal
    - X_{V_Lambda} depends on the lattice

    Associated variety controls the growth rate of dim V_h:
    dim V_h ~ h^{dim X_V - 1} (up to logarithmic factors).

    C_2-cofiniteness: X_V = {0} iff V is C_2-cofinite.
    """
    results = {
        "Heisenberg": {
            "dim_X": 1,
            "description": "X = C (affine line)",
            "C2_cofinite": False,
        },
        "Virasoro": {
            "dim_X": 1,
            "description": "X = C (T generates freely)",
            "C2_cofinite": False,
        },
    }

    if voa_name == "V_k(sl_2)":
        return {
            "dim_X": 3,
            "description": "X = sl_2* (affine 3-space)",
            "C2_cofinite": False,
        }

    if voa_name == "L_k(sl_2)":
        k = params.get("k", 1)
        if k == 0:
            return {
                "dim_X": 0,
                "description": "X = {0} (trivial VOA)",
                "C2_cofinite": True,
            }
        # k >= 1 integer: X = nilpotent cone of sl_2 (Arakawa)
        # The nilcone is 2-dimensional, so L_k(sl_2) is NOT C_2-cofinite
        # (also called "not lisse"). It IS quasi-lisse (associated variety
        # is a union of finitely many symplectic leaves = nilpotent orbits).
        return {
            "dim_X": 2,
            "description": "X = nilcone N(sl_2) = {ef + h^2/4 = 0} (dim 2)",
            "C2_cofinite": False,
            "note": "Arakawa: X = nilcone for integer level; C_2-cofinite iff X = {0}",
        }

    if voa_name == "V_Lambda":
        rank = params.get("rank", 1)
        return {
            "dim_X": rank,
            "description": f"X = C^{rank} (Heisenberg part)",
            "C2_cofinite": False,
        }

    if voa_name == "GKO_coset":
        k = params.get("k", 1)
        # Virasoro minimal model M(k+2, k+3)
        return {
            "dim_X": 0,
            "description": "X = {0} (rational, C_2-cofinite)",
            "C2_cofinite": True,
        }

    if voa_name in results:
        return results[voa_name]

    raise ValueError(f"Unknown VOA: {voa_name}")


