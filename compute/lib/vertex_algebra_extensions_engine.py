@dataclass(frozen=True)
class SimpleQuotientData:
    """Data for the simple quotient L_k(sl_2) at non-negative integer level k.

    At integer level k >= 0, the vacuum Verma module V_k(sl_2) has a
    singular vector at weight k+1 (in the sl_2 vacuum module).
    The simple quotient L_k(sl_2) = V_k(sl_2) / J_k where J_k is
    generated by this singular vector.

    The key consequence: L_k(sl_2) is NOT freely strongly generated.
    The bar complex changes because the null ideal introduces relations
    among the would-be-free generators.
    """
    k: int                  # non-negative integer level
    c: Fraction             # central charge c(sl_2, k) = 3k/(k+2)
    kappa: Fraction         # modular characteristic = 3(k+2)/4
    null_weight: int        # weight of singular vector = k+1
    n_integrable_reps: int  # number of integrable highest-weight reps = k+1
    is_rational: bool       # always True for non-negative integer k


def simple_quotient_sl2(k: int) -> SimpleQuotientData:
    """Construct data for L_k(sl_2) at non-negative integer level k.

    The singular vector in the vacuum module of V_k(sl_2) occurs at
    conformal weight k+1 (it is (e_{-1})^{k+1}|0> up to normalization).

    Central charge: c = 3k/(k+2).
    kappa = dim(sl_2)(k + h^vee)/(2 h^vee) = 3(k+2)/4.

    Note: kappa is computed from the UNIVERSAL algebra V_k(sl_2),
    not from the quotient L_k(sl_2). At integer level, the
    two share the same genus-1 obstruction because the null ideal
    contributes zero to the genus-1 trace (singular vectors are
    orthogonal to all states in the BPZ inner product).
    """
    if k < 0:
        raise ValueError(f"Need k >= 0, got {k}")
    c = Fraction(3 * k, k + 2)
    kappa = Fraction(3 * (k + 2), 4)
    return SimpleQuotientData(
        k=k, c=c, kappa=kappa,
        null_weight=k + 1,
        n_integrable_reps=k + 1,
        is_rational=True,
    )


def universal_vacuum_dims_sl2(k: int, max_weight: int) -> Dict[int, int]:
    """Weight-space dimensions of the UNIVERSAL vacuum module V_k(sl_2).

    The vacuum module of the universal affine VOA V_k(sl_2) is freely
    generated by e, h, f (conformal weight 1 each).
    dim V_h = coefficient of q^h in 1/prod_{n>=1}(1 - q^n)^3
    (the 3-colored partition function).

    Multi-path verification:
    Path 1: 3-colored partition convolution (implemented here).
    Path 2: direct PBW monomial count at low weights (tested).
    """
    p3 = _three_colored_partitions(max_weight)
    return {h: p3[h] for h in range(max_weight + 1)}


def _wk_numerator(k: int, max_weight: int) -> List[int]:
    """Weyl-Kac character numerator for hat{sl}_2 at level k, vacuum module.

    The character of L_k(sl_2) vacuum module is ch(q) = N_k(q) / eta(q)^3
    where the numerator, derived from the Kac-Weyl character formula
    (Kac, Infinite-dimensional Lie algebras, Ch. 12), is:

    N_k(q) = 1 + sum_{n >= 1} [-(2mn - 1) q^{mn^2 - n} + (2mn + 1) q^{mn^2 + n}]

    with m = k + 2 (the shifted level).

    Verified against:
    - k=0: gives ch = 1 (trivial VOA, all higher dims zero)
    - k=1: matches the A_1 root lattice VOA character (theta_3/eta)
    - k=2,3,4,5: all dims non-negative, agree with V_k below null weight k+1

    The formula arises from the two families of affine Weyl group elements:
    - j > 0 contributes exponent m*j^2 - j with coefficient -(2mj - 1)
    - j < 0 (equivalently n = |j| >= 1) contributes exponent m*n^2 + n
      with coefficient +(2mn + 1)
    """
    m = k + 2
    coeffs = [0] * (max_weight + 1)
    coeffs[0] = 1
    for n in range(1, max_weight + 1):
        ep = m * n * n - n      # exponent from positive Weyl direction
        em = m * n * n + n      # exponent from negative Weyl direction
        if ep > max_weight and em > max_weight:
            break
        if ep <= max_weight:
            coeffs[ep] += -(2 * m * n - 1)
        if em <= max_weight:
            coeffs[em] += (2 * m * n + 1)
    return coeffs


def quotient_vacuum_dims_sl2(k: int, max_weight: int) -> Dict[int, int]:
    """Weight-space dimensions of the SIMPLE quotient vacuum module L_k(sl_2).

    Uses the Kac-Weyl character formula for hat{sl}_2 at level k:
    ch_{L_k}(q) = N_k(q) / eta(q)^3
    where N_k is computed by _wk_numerator.

    Key properties (verified computationally):
    - k=0: L_0(sl_2) = trivial VOA (dim 1 at weight 0, 0 elsewhere)
    - k >= 1: agrees with universal V_k for h < k+1 (below null vector)
    - k=1: matches lattice VOA V_{A_1} character exactly
    - All dimensions are non-negative for all k >= 0
    """
    if k < 0:
        raise ValueError(f"Need k >= 0, got {k}")

    num = _wk_numerator(k, max_weight)
    p3 = _three_colored_partitions(max_weight)

    # ch = N / eta^3 = N * (1/eta^3) as power series convolution
    dims = {}
    for h in range(max_weight + 1):
        total = 0
        for j in range(h + 1):
            total += num[j] * p3[h - j]
        dims[h] = total
    return dims


@lru_cache(maxsize=32)
def _three_colored_partitions(max_h: int) -> Tuple[int, ...]:
    """Coefficients of 1/prod_{n>=1}(1-q^n)^3 up to q^max_h.

    This is the 3-colored partition function: the number of ways
    to write h as an ordered triple (a, b, c) of partitions with a+b+c = h.
    """
    from compute.lib.utils import partition_number
    p_cache = [partition_number(n) for n in range(max_h + 1)]

    result = []
    for h in range(max_h + 1):
        total = 0
        for a in range(h + 1):
            for b in range(h - a + 1):
                c = h - a - b
                total += p_cache[a] * p_cache[b] * p_cache[c]
        result.append(total)
    return tuple(result)


def bar_cohomology_change_sl2(k: int, max_bar_deg: int = 4,
                               max_weight: int = 20) -> Dict[str, Any]:
    """Analyze how bar cohomology changes from V_k to L_k for sl_2.

    The key question: what new bar cohomology classes appear when
    we pass from the freely generated V_k to the quotient L_k?

    The answer depends on the interplay between:
    - The null vector at weight k+1
    - The Arnold relations in the bar complex
    - The Koszul property of L_k (proved for all admissible sl_2)

    Returns a dictionary with:
    - 'universal_dims': vacuum module dims for V_k
    - 'quotient_dims': vacuum module dims for L_k
    - 'null_weight': weight of singular vector
    - 'dim_reduction': {h: universal - quotient} for each h
    - 'kappa': modular characteristic (same for V_k and L_k)
    """
    univ = universal_vacuum_dims_sl2(k, max_weight)
    quot = quotient_vacuum_dims_sl2(k, max_weight)

    reduction = {}
    for h in range(max_weight + 1):
        diff = univ.get(h, 0) - quot.get(h, 0)
        if diff != 0:
            reduction[h] = diff

    data = simple_quotient_sl2(k)

    return {
        'k': k,
        'universal_dims': univ,
        'quotient_dims': quot,
        'null_weight': data.null_weight,
        'dim_reduction': reduction,
        'kappa': data.kappa,
        'c': data.c,
        'n_reps': data.n_integrable_reps,
    }


