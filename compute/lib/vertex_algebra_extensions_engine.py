@dataclass
class FeiginTipuninData:
    """Data for the Feigin-Tipunin algebra W(p) (p >= 2).

    W(p) is the kernel of the screening operator on the lattice VOA
    V_{sqrt{2p}Z}. It has:
    - Central charge c = 1 - 6(p-1)^2/p = 13 - 6p - 6/p
    - Single strong generator of weight 2p - 1
    - C_2-cofinite but NOT rational (for p >= 2)
    - Logarithmic (non-semisimple module category)

    For p = 2: W(2) = triplet algebra with c = -2, generator at weight 3.
    For p = 3: c = -7, generator at weight 5.

    W(p) is FREELY generated by a single field W of weight 2p-1.
    So it IS in the scope of our PBW universality theorem.
    kappa = c/2 (single generator of weight > 1, Virasoro-type formula).
    Wait: this needs careful analysis. The generator has weight 2p-1,
    not weight 2. The kappa formula depends on the OPE structure.

    For a single strong generator W of weight h, the kappa formula is:
    kappa = sigma * c where sigma = sum_{j=1}^{h-1} 1/(j(j+1)) for W.
    But actually for a single weight-h generator (like Virasoro with h=2):
    sigma = 1/2 gives kappa = c/2.
    For weight h in general: sigma depends on the specific OPE.

    For W(p) with generator at weight 2p-1:
    The OPE W(z)W(w) has singular terms up to (z-w)^{-(4p-2)}.
    The kappa = coefficient of the most singular term, normalized.
    This requires the explicit OPE, which is known only case by case.

    For p=2 (triplet, c=-2, generator at weight 3):
    kappa = c/2 = -1 (using the Virasoro sub-formula, since the
    Virasoro T is a descendant combination involving W).

    NOTE: For logarithmic VOAs, the genus-1 partition function is NOT
    the usual trace; it involves pseudo-traces (Miyamoto).
    The relation to kappa requires careful treatment.
    """
    p: int
    c: Fraction
    generator_weight: int  # = 2p - 1
    kappa_estimate: Fraction  # = c/2 (from Virasoro subalgebra)
    is_C2_cofinite: bool
    is_rational: bool
    is_freely_generated: bool
    notes: str = ""


def feigin_tipunin(p: int) -> FeiginTipuninData:
    """Construct data for the Feigin-Tipunin algebra W(p).

    Central charge: c = 1 - 6(p-1)^2/p = (p - 6p^2 + 6p + p^2)/p
    Wait: c = 1 - 6(p-1)^2/p.
    = 1 - 6(p^2 - 2p + 1)/p
    = 1 - 6p + 12 - 6/p
    = 13 - 6p - 6/p.

    For p=2: c = 13 - 12 - 3 = -2. Correct (triplet algebra).
    For p=3: c = 13 - 18 - 2 = -7. Correct.
    """
    if p < 2:
        raise ValueError(f"Need p >= 2, got {p}")

    c = 13 - 6 * p - Fraction(6, p)
    # Simplify: c = (13p - 6p^2 - 6)/p
    c_check = Fraction(13 * p - 6 * p * p - 6, p)
    assert c == c_check, f"c mismatch: {c} != {c_check}"

    return FeiginTipuninData(
        p=p,
        c=c,
        generator_weight=2 * p - 1,
        kappa_estimate=c / 2,  # from Virasoro subalgebra (AP48 caveat)
        is_C2_cofinite=True,
        is_rational=False,  # logarithmic for p >= 2
        is_freely_generated=True,  # single generator at weight 2p-1
        notes=f"W({p}): triplet-type logarithmic VOA, c = {c}",
    )


