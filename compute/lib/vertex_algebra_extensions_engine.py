@dataclass
class ZhuC2Data:
    """Data for Zhu's C_2 quotient algebra R_V = V / C_2(V).

    C_2(V) = span{a_{(-2)}b : a, b in V}.
    R_V = V / C_2(V) is a Poisson algebra under:
    - Product: a * b = a_{(-1)}b  (mod C_2)
    - Bracket: {a, b} = a_{(0)}b  (mod C_2)

    R_V is the associated graded of Zhu's algebra A(V) in a precise sense.

    For freely generated VOAs:
    - R_{Heisenberg} = C[j] (polynomial ring in one variable)
    - R_{Virasoro} = C[T] (polynomial ring, Poisson bracket {T,T} = 0 mod C_2)
    - R_{sl_2_k} = C[e, h, f]/(ef - h^2/4 - kh/2) if k integer (quotient by Casimir)
      Wait: for the universal V_k(sl_2), R_V = C[e, h, f] (free, 3 generators).
      For the simple quotient L_k(sl_2), the null vector at weight k+1
      imposes a relation in R_V.

    For non-freely generated VOAs:
    - R_{L_k(sl_2)} has relations from the singular vector projection.
    """
    voa_name: str
    generators: List[Tuple[str, int]]   # (name, weight) pairs
    relations: List[str]                 # descriptions of relations
    dim_by_weight: Dict[int, int]       # graded dimensions of R_V
    is_polynomial: bool                  # True if R_V is a polynomial ring
    poisson_bracket_type: str            # e.g. "abelian", "Kirillov-Kostant"


def zhu_c2_heisenberg() -> ZhuC2Data:
    """Zhu's C_2 algebra for the Heisenberg VOA.

    R_H = C[j] where j has weight 1.
    Poisson bracket: {j, j} = j_{(0)}j = 0 (abelian).
    dim R_H at weight h = 1 for all h >= 0 (monomials j^h).
    """
    dims = {h: 1 for h in range(20)}
    return ZhuC2Data(
        voa_name="Heisenberg",
        generators=[("j", 1)],
        relations=[],
        dim_by_weight=dims,
        is_polynomial=True,
        poisson_bracket_type="abelian",
    )


def zhu_c2_virasoro() -> ZhuC2Data:
    """Zhu's C_2 algebra for the Virasoro VOA.

    R_Vir = C[T] where T has weight 2.
    Poisson bracket: {T, T} = T_{(0)}T = dT, which is 0 in R_V
    because dT = L_{-1}T and L_{-1}V subset C_2(V) (for weight >= 2).

    So the Poisson bracket on R_Vir is trivially zero.
    dim R_Vir at weight h = 1 if h is even >= 0, else 0
    (monomials T^{h/2}).

    Actually, R_Vir has dim = 1 at every even weight.
    """
    dims = {h: (1 if h % 2 == 0 else 0) for h in range(20)}
    return ZhuC2Data(
        voa_name="Virasoro",
        generators=[("T", 2)],
        relations=[],
        dim_by_weight=dims,
        is_polynomial=True,
        poisson_bracket_type="abelian (dT in C_2)",
    )


def zhu_c2_sl2_universal(k: int) -> ZhuC2Data:
    """Zhu's C_2 algebra for the universal affine VOA V_k(sl_2).

    R_{V_k} = C[e, h, f] (polynomial ring in 3 variables of weight 1).
    Poisson bracket = sl_2 Lie bracket:
    {e, f} = h, {h, e} = 2e, {h, f} = -2f.
    This is the Kirillov-Kostant-Souriau Poisson structure on sl_2*.

    dim at weight h = (h+1)(h+2)/2 (monomials of degree h in 3 variables).
    """
    dims = {}
    for h in range(20):
        dims[h] = (h + 1) * (h + 2) // 2
    return ZhuC2Data(
        voa_name=f"V_{k}(sl_2)",
        generators=[("e", 1), ("h", 1), ("f", 1)],
        relations=[],
        dim_by_weight=dims,
        is_polynomial=True,
        poisson_bracket_type="Kirillov-Kostant (sl_2*)",
    )


def zhu_c2_sl2_simple(k: int) -> ZhuC2Data:
    """Zhu's C_2 algebra for the simple quotient L_k(sl_2).

    For k non-negative integer:
    The singular vector at weight k+1 projects to a relation in R_V.
    The Casimir C = ef + fh + h^2/4 acts on integrable reps.

    R_{L_k} = C[e, h, f] / (relation from singular vector)
    The associated variety X_{L_k} = Spec(R_{L_k}) is:
    - For k = 0: a point (trivial VOA)
    - For k >= 1: the nilpotent cone N subset sl_2* (Arakawa's theorem)
      X_{L_k} = closure of the nilpotent orbit O_min in sl_2*
      = {(e,h,f) : ef + h^2/4 = 0} (quadric cone, dim = 2)

    So for k >= 1: R_{L_k} = C[e, h, f]/(ef + h^2/4)
    This is NOT polynomial (one relation).
    dim at weight h = (h+1)(h+2)/2 - max(0, h(h-1)/2) for k >= 1
    Actually: dim = 2h + 1 for h >= 1 (Hilbert function of the quadric cone).
    Wait: the quadric cone in P^2 has degree 2. The affine cone has
    Hilbert function dim_h = 2h + 1 for h >= 0? No.

    Let's compute directly:
    C[e,h,f]/(ef + h^2/4) where deg(e) = deg(h) = deg(f) = 1.
    The relation ef + h^2/4 = 0 is of degree 2.
    dim_0 = 1, dim_1 = 3.
    dim_2 = 6 - 1 = 5 (subtract the relation).
    General: dim_h = (h+1)(h+2)/2 - (h-1)h/2 = 2h + 1 for h >= 1.
    And dim_0 = 1.
    Check: dim_2 = 2*2 + 1 = 5. (h+1)(h+2)/2 = 6. 6 - 1 = 5. Correct.
    dim_3 = 2*3 + 1 = 7. (4*5/2) - (2*3/2) = 10 - 3 = 7. Correct.
    """
    if k < 0:
        raise ValueError(f"Need k >= 0, got {k}")

    if k == 0:
        # Trivial VOA: R = C, only weight 0.
        dims = {h: (1 if h == 0 else 0) for h in range(20)}
        return ZhuC2Data(
            voa_name="L_0(sl_2) = C",
            generators=[],
            relations=["all generators vanish"],
            dim_by_weight=dims,
            is_polynomial=False,
            poisson_bracket_type="trivial",
        )

    # k >= 1: associated variety is the nilpotent cone
    dims = {0: 1}
    for h in range(1, 20):
        dims[h] = 2 * h + 1

    return ZhuC2Data(
        voa_name=f"L_{k}(sl_2)",
        generators=[("e", 1), ("h", 1), ("f", 1)],
        relations=[f"ef + h^2/4 = 0 (nilpotent cone)"],
        dim_by_weight=dims,
        is_polynomial=False,
        poisson_bracket_type="Kirillov-Kostant restricted to nilcone",
    )


