@dataclass
class CosetData:
    """Data for a coset (commutant) algebra Com(V, W).

    Com(V, W) = {w in W : [v_{(n)}, w] = 0 for all v in V, n >= 0}

    The coset inherits conformal structure if V contains the Virasoro
    subalgebra of W (Sugawara construction).
    """
    parent_name: str
    subalgebra_name: str
    c_parent: Fraction
    c_sub: Fraction
    c_coset: Fraction       # = c_parent - c_sub
    kappa_parent: Fraction
    kappa_sub: Fraction
    kappa_coset: Optional[Fraction]  # may differ from kappa_parent - kappa_sub
    is_rational: bool
    freely_generated: bool
    notes: str = ""


def gko_coset_data(k: int) -> CosetData:
    """GKO coset: (su(2)_k x su(2)_1) / su(2)_{k+1}.

    This gives the Virasoro minimal model M(k+2, k+3) with
    central charge c = 1 - 6/((k+2)(k+3)).

    The three pieces:
    - Parent W = su(2)_k x su(2)_1 with c_W = 3k/(k+2) + 1
    - Subalgebra V = su(2)_{k+1} diag with c_V = 3(k+1)/(k+3)
    - Coset Com(V, W) = Virasoro with c = c_W - c_V

    kappa values:
    - kappa(su(2)_k) = 3(k+2)/4
    - kappa(su(2)_1) = 3*3/4 = 9/4
    - kappa(su(2)_{k+1}) = 3(k+3)/4
    - kappa(coset) = kappa(M(k+2,k+3)) = c_coset/2
      (Virasoro algebra, so kappa = c/2)

    The kappa NON-ADDITIVITY is the key point:
    kappa(parent) = kappa(su(2)_k) + kappa(su(2)_1) = 3(k+2)/4 + 9/4
    kappa(sub) = 3(k+3)/4
    kappa(parent) - kappa(sub) = 3(k+2)/4 + 9/4 - 3(k+3)/4
                                = (3k+6+9-3k-9)/4 = 6/4 = 3/2

    But kappa(coset) = c_coset/2 = (1 - 6/((k+2)(k+3)))/2

    So kappa is NOT additive for cosets in general!
    kappa(parent) - kappa(sub) = 3/2 (constant)
    kappa(coset) = (1 - 6/((k+2)(k+3)))/2 (depends on k)

    This is because kappa depends on the ALGEBRA STRUCTURE (AP48),
    not just the central charge decomposition.
    """
    if k < 0:
        raise ValueError(f"Need k >= 0, got {k}")

    c_parent = Fraction(3 * k, k + 2) + 1  # su(2)_k x su(2)_1
    c_sub = Fraction(3 * (k + 1), k + 3)    # su(2)_{k+1}
    c_coset = c_parent - c_sub               # should be 1 - 6/((k+2)(k+3))

    # Verify
    c_expected = 1 - Fraction(6, (k + 2) * (k + 3))
    assert c_coset == c_expected, (
        f"GKO central charge mismatch: {c_coset} != {c_expected}"
    )

    kappa_parent = Fraction(3 * (k + 2), 4) + Fraction(9, 4)
    kappa_sub = Fraction(3 * (k + 3), 4)
    kappa_coset = c_coset / 2  # Virasoro minimal model: kappa = c/2

    return CosetData(
        parent_name=f"su(2)_{k} x su(2)_1",
        subalgebra_name=f"su(2)_{k+1}",
        c_parent=c_parent,
        c_sub=c_sub,
        c_coset=c_coset,
        kappa_parent=kappa_parent,
        kappa_sub=kappa_sub,
        kappa_coset=kappa_coset,
        is_rational=True,
        freely_generated=True,  # minimal models are freely generated by T
        notes=f"GKO coset = Virasoro M({k+2},{k+3})",
    )


def kappa_additivity_defect(coset: CosetData) -> Fraction:
    """Compute the defect of kappa additivity for a coset.

    delta = kappa(coset) - (kappa(parent) - kappa(sub))

    If delta = 0, kappa is additive. If delta != 0, the coset
    has a kappa anomaly: the modular characteristic of the coset
    is NOT determined by those of the parent and subalgebra alone.

    For GKO: delta = c_coset/2 - 3/2, which is nonzero for all k >= 0.
    """
    if coset.kappa_coset is None:
        raise ValueError("Coset kappa not computed")
    naive = coset.kappa_parent - coset.kappa_sub
    return coset.kappa_coset - naive


def coset_bar_dimension_bound(
    parent_dims: Dict[int, int],
    sub_dims: Dict[int, int],
    max_weight: int,
) -> Dict[int, int]:
    """Upper bound on coset vacuum module dimensions.

    The coset Com(V, W) embeds into W, so dim Com(V,W)_h <= dim W_h.
    A tighter bound: dim Com(V,W)_h <= dim W_h - dim V_h + delta_{h,0}
    (subtracting the subalgebra contribution, with correction at weight 0).

    This is a BOUND, not the exact answer. The exact answer requires
    the full commutant computation.
    """
    bounds = {}
    for h in range(max_weight + 1):
        w_dim = parent_dims.get(h, 0)
        v_dim = sub_dims.get(h, 0)
        if h == 0:
            bounds[h] = 1  # vacuum is always 1-dimensional
        else:
            bounds[h] = max(0, w_dim - v_dim)
    return bounds


