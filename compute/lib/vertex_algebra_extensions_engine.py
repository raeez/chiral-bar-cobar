def lattice_vs_heisenberg_kappa(lattice_name: str) -> Dict[str, Any]:
    """Compare kappa for a lattice VOA vs its Heisenberg subalgebra.

    For V_Lambda:
    - kappa(V_Lambda) = rank(Lambda)   (the lattice VOA kappa)
    - kappa(H^rank) = rank(Lambda)     (the Heisenberg kappa, level = 1 per boson)
    - Heisenberg H at level 1 has kappa = 1 per boson

    So kappa(V_Lambda) = kappa(H^rank) = rank.
    This is a COINCIDENCE for rank-1 lattices and
    a genuine identity for even unimodular lattices:
    the lattice VOA extension does not change kappa when
    the lattice vectors add integrally.

    For E8: kappa(V_{E8}) = 8 = rank.
    For Leech: kappa(V_Leech) = 24 = rank.
    NOT kappa = c/2 (which would also be rank/2 for lattices at c = rank).
    Wait: c = rank, so c/2 = rank/2 != rank.
    So kappa(lattice VOA) != c/2 generically. This is AP48.
    """
    data = lattice_voa_data(lattice_name)
    heisenberg_kappa_per_boson = Fraction(1)  # H at level 1 per boson
    heisenberg_total = heisenberg_kappa_per_boson * data.rank
    virasoro_formula = data.c / 2  # what c/2 would give (WRONG for lattice)

    return {
        'lattice': lattice_name,
        'rank': data.rank,
        'c': data.c,
        'kappa_lattice': data.kappa,
        'kappa_heisenberg': heisenberg_total,
        'kappa_virasoro_formula': virasoro_formula,
        'kappa_matches_heisenberg': data.kappa == heisenberg_total,
        'kappa_matches_virasoro': data.kappa == virasoro_formula,
        'AP48_demonstration': data.kappa != virasoro_formula,
    }


