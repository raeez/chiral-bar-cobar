def extension_kappa_change_catalog() -> List[Dict[str, Any]]:
    """Catalog of how kappa changes under various VOA constructions.

    This is a critical test of AP48: kappa depends on the full algebra.

    Summary:
    - Orbifold V^G: kappa PRESERVED (same genus-1 data)
    - Simple current extension: kappa CHANGES (new generators)
    - Coset: kappa NOT additive (AP48)
    - Simple quotient: kappa PRESERVED (null ideal orthogonal in BPZ)
    - DS reduction: kappa changes by a computable shift
    """
    entries = []

    # 1. Orbifold preserves kappa
    entries.append({
        'construction': 'Orbifold V^G',
        'kappa_rule': 'preserved',
        'example': 'V_Lambda^{Z/2}: kappa = rank',
        'reason': 'G-invariant states carry same genus-1 trace',
    })

    # 2. Simple current extension changes kappa
    for k in [1, 2, 3]:
        ext = lattice_extension_to_sl2(k)
        entries.append({
            'construction': f'Simple current extension (k={k})',
            'kappa_rule': 'changes',
            'kappa_base': ext.kappa_base,
            'kappa_extended': ext.kappa_extended,
            'delta': kappa_change_under_extension(ext),
            'reason': 'New generators contribute to genus-1 obstruction',
        })

    # 3. Coset non-additivity
    for k in [1, 2, 5]:
        coset = gko_coset_data(k)
        delta = kappa_additivity_defect(coset)
        entries.append({
            'construction': f'GKO coset (k={k})',
            'kappa_rule': 'not additive',
            'delta': delta,
            'reason': 'kappa depends on algebra structure, not just c (AP48)',
        })

    # 4. Simple quotient preserves kappa
    for k in [1, 2, 3]:
        sq = simple_quotient_sl2(k)
        entries.append({
            'construction': f'Simple quotient L_{k}(sl_2)',
            'kappa_rule': 'preserved from universal',
            'kappa': sq.kappa,
            'reason': 'Null ideal contributes zero to genus-1 trace (BPZ orthogonality)',
        })

    return entries
