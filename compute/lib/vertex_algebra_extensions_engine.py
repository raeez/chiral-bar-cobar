def gko_kappa_verification(max_k: int = 10) -> List[Dict[str, Any]]:
    """Verify the GKO coset kappa relation for k = 0, ..., max_k.

    For each k, compute:
    - c_coset = 1 - 6/((k+2)(k+3))   (minimal model central charge)
    - kappa_coset = c_coset/2          (Virasoro formula)
    - kappa_parent = 3(k+2)/4 + 9/4   (sum of su(2)_k and su(2)_1)
    - kappa_sub = 3(k+3)/4            (diagonal su(2)_{k+1})
    - delta = kappa_coset - (kappa_parent - kappa_sub)  (additivity defect)

    The defect delta is generically nonzero, demonstrating that
    kappa is NOT additive for cosets.

    Cross-check: at k -> infinity,
    c_coset -> 1, kappa_coset -> 1/2,
    kappa_parent - kappa_sub -> 3/2 (constant),
    so delta -> 1/2 - 3/2 = -1 (approaches constant).
    """
    results = []
    for k in range(max_k + 1):
        coset = gko_coset_data(k)
        delta = kappa_additivity_defect(coset)

        # Cross-check: direct computation of c_coset
        c_direct = 1 - Fraction(6, (k + 2) * (k + 3))
        assert coset.c_coset == c_direct

        results.append({
            'k': k,
            'c_coset': coset.c_coset,
            'kappa_coset': coset.kappa_coset,
            'kappa_parent': coset.kappa_parent,
            'kappa_sub': coset.kappa_sub,
            'naive_difference': coset.kappa_parent - coset.kappa_sub,
            'delta': delta,
            'delta_nonzero': delta != 0,
        })
    return results


def gko_minimal_model_identification(k: int) -> Dict[str, Any]:
    """Verify that GKO coset at level k gives minimal model M(k+2, k+3).

    The GKO coset (su(2)_k x su(2)_1) / su(2)_{k+1} produces the
    Virasoro minimal model M(p, q) with p = k+2, q = k+3.

    Central charge: c(p,q) = 1 - 6(p-q)^2/(pq) = 1 - 6/(pq).
    Since p - q = -1: (p-q)^2 = 1.
    So c = 1 - 6/(pq) = 1 - 6/((k+2)(k+3)).
    """
    p, q = k + 2, k + 3
    c_mm = 1 - Fraction(6, p * q)
    coset = gko_coset_data(k)

    # Number of primary fields in M(p,q): (p-1)(q-1)/2
    n_primaries = (p - 1) * (q - 1) // 2

    # kappa for the minimal model (Virasoro formula)
    kappa_mm = c_mm / 2

    return {
        'k': k,
        'p': p,
        'q': q,
        'c_minimal_model': c_mm,
        'c_coset': coset.c_coset,
        'c_match': c_mm == coset.c_coset,
        'kappa': kappa_mm,
        'n_primaries': n_primaries,
        'model_name': f"M({p},{q})",
    }


