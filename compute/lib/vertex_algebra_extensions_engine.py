@dataclass
class OrbifoldData:
    """Data for an orbifold VOA V^G.

    V^G = {v in V : g.v = v for all g in G}
    where G acts on V by VOA automorphisms.

    The orbifold is typically NOT freely generated even if V is.
    New generators arise from invariant combinations of the original ones.
    """
    parent_name: str
    group_name: str
    group_order: int
    c: Fraction          # same as parent (orbifold preserves c)
    kappa_parent: Fraction
    kappa_orbifold: Fraction  # same as parent (orbifold preserves kappa)
    n_twisted_sectors: int    # number of conjugacy classes of G
    freely_generated: bool


def lattice_orbifold_z2(rank: int) -> OrbifoldData:
    """Z/2 orbifold of a rank-r lattice VOA.

    The Z/2 acts by negating the lattice: Lambda -> -Lambda.
    This is the theta-involution, acting as (-1)^{L_0} on twisted sectors.

    Central charge c = rank (for a rank-r even lattice).
    kappa = rank (for lattice VOA: AP48).

    The orbifold V_Lambda^{Z/2} has:
    - Untwisted sector: V_Lambda^{Z/2} itself
    - Twisted sector: contributes to the extended algebra

    For the rank-1 case (Z lattice at level 1):
    V_Z^{Z/2} is generated by j^2 (weight 2) and e^{i phi} + e^{-i phi}
    (weight 1/2 if phi is the free boson). NOT freely generated.
    """
    c = Fraction(rank)
    kappa = Fraction(rank)
    return OrbifoldData(
        parent_name=f"V_Lambda (rank {rank})",
        group_name="Z/2",
        group_order=2,
        c=c,
        kappa_parent=kappa,
        kappa_orbifold=kappa,  # kappa preserved under orbifold
        n_twisted_sectors=2,   # Z/2 has 2 conjugacy classes
        freely_generated=False,
    )


def orbifold_vacuum_dims_z2(parent_dims: Dict[int, int]) -> Dict[int, int]:
    """Vacuum module dimensions of V^{Z/2} (untwisted sector).

    Under Z/2 acting by (-1) on weight-1 generators and (+1) on weight-2:
    the invariant subspace at each weight h is roughly dim(V_h)/2 + correction.

    For the Heisenberg algebra with Z/2: j -> -j:
    - Weight h even: invariant states are even-degree monomials
    - Weight h odd: no correction needed
    - dim (H^{Z/2})_h = number of partitions of h into even parts
                       + number of partitions of h into distinct odd parts

    For a general VOA, we use the rough estimate
    dim(V^G)_h = (1/|G|) sum_{g in G} Tr(g | V_h).

    For Z/2 with the action g: V_h -> (-1)^{f(h)} V_h where f counts
    the number of weight-1 generators used, this is nontrivial.

    Here we compute for the Heisenberg case explicitly.
    """
    # For Heisenberg with j -> -j:
    # V_h has basis: normal-ordered monomials j_{-n_1} ... j_{-n_k} |0>
    # with n_1 >= n_2 >= ... >= n_k >= 1, sum n_i = h.
    # Under Z/2: this picks up factor (-1)^k.
    # Invariant subspace: monomials with even k.
    max_h = max(parent_dims.keys()) if parent_dims else 0
    dims = {}
    for h in range(max_h + 1):
        # Count partitions of h with even number of parts
        # vs odd number of parts
        even_parts, odd_parts = _count_partitions_by_parity(h)
        dims[h] = even_parts
    return dims


def _count_partitions_by_parity(n: int) -> Tuple[int, int]:
    """Count partitions of n by parity of number of parts.

    Returns (even_count, odd_count) where
    even_count = number of partitions of n with even number of parts
    odd_count = number of partitions of n with odd number of parts.

    Uses the generating function identity:
    sum p_even(n) q^n = (1/2)(1/phi(q) + 1/phi(-q))
    sum p_odd(n) q^n  = (1/2)(1/phi(q) - 1/phi(-q))
    where phi(q) = prod(1 - q^n).
    """
    if n < 0:
        return (0, 0)
    if n == 0:
        return (1, 0)  # empty partition has 0 (even) parts

    # DP approach: p(n, k) = partitions of n into exactly k parts
    # Then sum over even k vs odd k.
    # p(n, k) = p(n-1, k-1) + p(n-k, k)
    pk = [[0] * (n + 2) for _ in range(n + 1)]
    pk[0][0] = 1
    for nn in range(1, n + 1):
        for kk in range(1, nn + 1):
            pk[nn][kk] = pk[nn - 1][kk - 1] + (pk[nn - kk][kk] if nn >= kk else 0)

    even_count = sum(pk[n][kk] for kk in range(0, n + 1, 2))
    odd_count = sum(pk[n][kk] for kk in range(1, n + 1, 2))
    return (even_count, odd_count)


