def zeta_zero_to_c(n: int) -> complex:
    """Map the n-th zeta zero rho_n = 1/2 + i*gamma_n to central charge.

    c(rho) = 26 * rho = 13 + 26*i*gamma_n.
    The real part is 13 (self-dual point!), imaginary part scales with gamma_n.
    """
    if n < 1 or n > len(ZETA_ZEROS_GAMMA):
        raise ValueError(f"n={n} out of range [1, {len(ZETA_ZEROS_GAMMA)}]")
    gamma_n = ZETA_ZEROS_GAMMA[n - 1]
    return complex(13.0, 26.0 * gamma_n)


def nc_distance_consecutive_zeros(n: int, max_r: int = 6) -> float:
    """NC distance between consecutive zeta zeros in the shadow geometry.

    d(phi_{c(rho_n)}, phi_{c(rho_{n+1})})

    Since c(rho_n) = 13 + 26i*gamma_n, consecutive zeros have
    purely imaginary separation 26i*(gamma_{n+1} - gamma_n).

    In the kappa sector: d = |kappa(c_n) - kappa(c_{n+1})| = 13|gamma_{n+1} - gamma_n|.
    With shadow corrections from S_3, S_4, ... at complex c.
    """
    if n < 1 or n >= len(ZETA_ZEROS_GAMMA):
        raise ValueError(f"n={n} out of range [1, {len(ZETA_ZEROS_GAMMA) - 1}]")

    c_n = zeta_zero_to_c(n)
    c_n1 = zeta_zero_to_c(n + 1)

    # Kappa sector contribution
    d_kappa = abs(virasoro_kappa(c_n) - virasoro_kappa(c_n1))

    # Higher-arity corrections
    Sr_n = virasoro_shadow_coefficients(c_n, max_r)
    Sr_n1 = virasoro_shadow_coefficients(c_n1, max_r)

    corrections = 0.0
    for r in range(3, max_r + 1):
        if r in Sr_n and r in Sr_n1:
            w_r = 1.0 / r**2  # spectral weight
            delta = abs(Sr_n[r] - Sr_n1[r])
            corrections += w_r * delta**2

    return math.sqrt(d_kappa**2 + corrections)


def nc_distance_zero_to_self_dual(n: int, max_r: int = 6) -> float:
    """NC distance from n-th zeta zero to the self-dual point c=13.

    d(phi_{c(rho_n)}, phi_{13})

    Since c(rho_n) = 13 + 26i*gamma_n, the distance is purely imaginary
    in the c-plane: |c(rho_n) - 13| = 26*gamma_n.

    In the kappa sector: d = |kappa(c(rho_n)) - kappa(13)| = 13*gamma_n.
    """
    c_n = zeta_zero_to_c(n)

    # Kappa sector
    d_kappa = abs(virasoro_kappa(c_n) - virasoro_kappa(13.0))

    # Higher corrections at complex c
    Sr_n = virasoro_shadow_coefficients(c_n, max_r)
    Sr_13 = virasoro_shadow_coefficients(13.0, max_r)

    corrections = 0.0
    for r in range(3, max_r + 1):
        if r in Sr_n and r in Sr_13:
            w_r = 1.0 / r**2
            delta = abs(Sr_n[r] - Sr_13[r])
            corrections += w_r * delta**2

    return math.sqrt(d_kappa**2 + corrections)


def nc_distance_zero_to_reflected(n: int, max_r: int = 6) -> float:
    """NC distance between rho_n and its complex conjugate rho_n_bar.

    rho_n = 1/2 + i*gamma_n, rho_n_bar = 1/2 - i*gamma_n.
    c(rho_n) = 13 + 26i*gamma_n, c(rho_n_bar) = 13 - 26i*gamma_n.

    These are complex conjugates: the distance is 52*gamma_n in kappa sector.
    """
    c_n = zeta_zero_to_c(n)
    c_nbar = c_n.conjugate()

    # Kappa sector
    d_kappa = abs(virasoro_kappa(c_n) - virasoro_kappa(c_nbar))

    # Higher corrections
    Sr_n = virasoro_shadow_coefficients(c_n, max_r)
    Sr_nbar = virasoro_shadow_coefficients(c_nbar, max_r)

    corrections = 0.0
    for r in range(3, max_r + 1):
        if r in Sr_n and r in Sr_nbar:
            w_r = 1.0 / r**2
            delta = abs(Sr_n[r] - Sr_nbar[r])
            corrections += w_r * delta**2

    return math.sqrt(d_kappa**2 + corrections)


def consecutive_zero_spacings_nc(N: int = 20, max_r: int = 6) -> np.ndarray:
    """NC distance spacings between consecutive zeros: d_n = d(rho_n, rho_{n+1}).

    Returns array of N-1 spacings.
    """
    spacings = []
    for n in range(1, N):
        d = nc_distance_consecutive_zeros(n, max_r)
        spacings.append(d)
    return np.array(spacings)


def normalized_zero_spacings_nc(N: int = 20, max_r: int = 6) -> np.ndarray:
    """Normalized NC spacings: s_n = d_n / <d> where <d> is the mean spacing.

    GUE prediction: the distribution of normalized spacings follows
    the Wigner surmise p(s) = (32/pi^2) * s^2 * exp(-4s^2/pi).
    """
    spacings = consecutive_zero_spacings_nc(N, max_r)
    mean_spacing = np.mean(spacings)
    if mean_spacing < 1e-15:
        return spacings
    return spacings / mean_spacing


def gue_wigner_surmise(s: float) -> float:
    """GUE Wigner surmise: p(s) = (32/pi^2) * s^2 * exp(-4s^2/pi)."""
    return (32.0 / math.pi**2) * s**2 * math.exp(-4.0 * s**2 / math.pi)


def kolmogorov_smirnov_vs_gue(spacings_normalized: np.ndarray) -> float:
    """Kolmogorov-Smirnov statistic comparing normalized spacings to GUE.

    Returns the KS statistic D = max |F_empirical(s) - F_GUE(s)|.
    Lower is better (more GUE-like).
    """
    from scipy.integrate import quad

    sorted_s = np.sort(spacings_normalized)
    n = len(sorted_s)

    ks_stat = 0.0
    for i, si in enumerate(sorted_s):
        # Empirical CDF at si
        F_emp = (i + 1) / n
        # GUE CDF at si
        F_gue, _ = quad(gue_wigner_surmise, 0, si)
        ks_stat = max(ks_stat, abs(F_emp - F_gue))

    return ks_stat


