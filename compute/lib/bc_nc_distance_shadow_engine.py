def truncation_convergence(c1: float, c2: float,
                            max_r_values: Optional[List[int]] = None) -> Dict[int, float]:
    """Connes distance as function of truncation level max_r.

    Tests numerical convergence: d(c1, c2; max_r) should stabilize.
    Returns dict max_r -> distance.
    """
    if max_r_values is None:
        max_r_values = [2, 3, 4, 5, 6, 8, 10, 12, 15]

    result = {}
    for mr in max_r_values:
        d = connes_distance_truncated(c1, c2, mr, N_sample=100)
        result[mr] = d
    return result


def distance_triangle_inequality_check(c1: float, c2: float, c3: float,
                                         max_r: int = 8) -> Tuple[bool, float]:
    """Verify the triangle inequality: d(c1, c3) <= d(c1, c2) + d(c2, c3).

    Returns (satisfies, slack) where slack = d(c1,c2) + d(c2,c3) - d(c1,c3).
    """
    d12 = connes_distance_truncated(c1, c2, max_r)
    d23 = connes_distance_truncated(c2, c3, max_r)
    d13 = connes_distance_truncated(c1, c3, max_r)
    slack = d12 + d23 - d13
    return slack >= -1e-10, slack


def distance_symmetry_check(c1: float, c2: float, max_r: int = 8) -> float:
    """Check symmetry: |d(c1, c2) - d(c2, c1)|. Should be 0."""
    d12 = connes_distance_truncated(c1, c2, max_r)
    d21 = connes_distance_truncated(c2, c1, max_r)
    return abs(d12 - d21)


def koszul_symmetry_check(c_val: float, max_r: int = 8) -> float:
    """Check Koszul symmetry: d(c, 13) should equal d(26-c, 13).

    By the Koszul involution c -> 26-c, the distance from c to 13
    should equal the distance from 26-c to 13.
    """
    d1 = connes_distance_truncated(c_val, 13.0, max_r)
    d2 = connes_distance_truncated(26.0 - c_val, 13.0, max_r)
    return abs(d1 - d2)


def multi_path_distance_verification(c1: float, c2: float,
                                       max_r: int = 8) -> Dict[str, float]:
    """Five-path verification of the NC distance between c1 and c2.

    Path 1: Connes formula (geodesic integral)
    Path 2: Lipschitz supremum (direct definition)
    Path 3: Kappa sector (leading approximation)
    Path 4: Wasserstein W1
    Path 5: Truncation-extrapolated value

    For commutative A^sh, paths 1 and 2 should agree.
    """
    d_connes = connes_distance_truncated(c1, c2, max_r)
    d_lipschitz = connes_distance_via_lipschitz(c1, c2, max_r)
    d_kappa = connes_distance_kappa_sector(c1, c2)
    d_W1 = wasserstein_shadow(c1, c2, p=1, max_r=max_r)

    # Extrapolated value from truncation convergence
    conv = truncation_convergence(c1, c2, [4, 6, 8, 10, 12])
    values = list(conv.values())
    d_extrap = values[-1] if values else d_connes  # best available

    return {
        'connes_geodesic': d_connes,
        'lipschitz_sup': d_lipschitz,
        'kappa_sector': d_kappa,
        'wasserstein_W1': d_W1,
        'truncation_extrapolated': d_extrap,
    }


