def shadow_christoffel(c_val: float, max_r: int = 10,
                        weights: Optional[Dict[int, float]] = None,
                        dc: float = 1e-5) -> float:
    """Christoffel symbol Gamma^c_{cc} for the shadow metric.

    Gamma = (1/2) * g^{cc} * dg_{cc}/dc

    where g_{cc} is the shadow metric tensor.
    """
    g = shadow_metric_tensor(c_val, max_r, weights)
    g_plus = shadow_metric_tensor(c_val + dc, max_r, weights)
    g_minus = shadow_metric_tensor(c_val - dc, max_r, weights)
    dg = (g_plus - g_minus) / (2 * dc)

    if abs(g) < 1e-15:
        return 0.0
    return 0.5 * dg / g


def geodesic_solve(c_start: float, c_end: float, max_r: int = 10,
                    weights: Optional[Dict[int, float]] = None,
                    N_steps: int = 500) -> Tuple[np.ndarray, np.ndarray]:
    """Solve the geodesic equation from c_start to c_end.

    gamma''(t) + Gamma^c_{cc}(gamma(t)) * gamma'(t)^2 = 0

    with gamma(0) = c_start, gamma(1) = c_end (boundary conditions).

    Uses shooting method: guess gamma'(0) = v0, integrate, adjust v0
    until gamma(1) = c_end.

    Returns (t_array, gamma_array).
    """
    dt = 1.0 / N_steps

    def integrate_geodesic(v0: float) -> np.ndarray:
        """Integrate geodesic ODE with initial velocity v0."""
        gamma_arr = np.zeros(N_steps + 1)
        v_arr = np.zeros(N_steps + 1)
        gamma_arr[0] = c_start
        v_arr[0] = v0

        for i in range(N_steps):
            c_cur = gamma_arr[i]
            v_cur = v_arr[i]
            # Avoid singular points
            if abs(c_cur) < 0.01:
                c_cur = 0.01
            if abs(5 * c_cur + 22) < 0.01:
                c_cur += 0.5

            Gamma = shadow_christoffel(c_cur, max_r, weights)
            # RK2 step
            a1 = -Gamma * v_cur**2
            c_mid = c_cur + 0.5 * dt * v_cur
            v_mid = v_cur + 0.5 * dt * a1
            if abs(c_mid) < 0.01:
                c_mid = 0.01
            Gamma_mid = shadow_christoffel(c_mid, max_r, weights)
            a_mid = -Gamma_mid * v_mid**2

            gamma_arr[i + 1] = c_cur + dt * v_mid
            v_arr[i + 1] = v_cur + dt * a_mid

        return gamma_arr

    # Shooting method: find v0 such that gamma(1) = c_end
    # Initial guess: straight line
    v0_guess = c_end - c_start

    # Bisection / secant method
    v_lo, v_hi = v0_guess * 0.5, v0_guess * 2.0
    if v0_guess < 0:
        v_lo, v_hi = v_hi, v_lo

    # Secant method
    v_a = v_lo
    v_b = v_hi
    gamma_a = integrate_geodesic(v_a)
    gamma_b = integrate_geodesic(v_b)
    fa = gamma_a[-1] - c_end
    fb = gamma_b[-1] - c_end

    for _ in range(50):
        if abs(fb - fa) < 1e-15:
            break
        v_new = v_b - fb * (v_b - v_a) / (fb - fa)
        gamma_new = integrate_geodesic(v_new)
        f_new = gamma_new[-1] - c_end
        if abs(f_new) < 1e-10:
            t_arr = np.linspace(0, 1, N_steps + 1)
            return t_arr, gamma_new
        v_a, fa = v_b, fb
        v_b, fb = v_new, f_new

    # Return best result
    gamma_final = integrate_geodesic(v_b)
    t_arr = np.linspace(0, 1, N_steps + 1)
    return t_arr, gamma_final


def geodesic_passes_through(c_start: float, c_end: float, c_target: float,
                             max_r: int = 10, tol: float = 0.5,
                             N_steps: int = 300) -> Tuple[bool, float]:
    """Check if the geodesic from c_start to c_end passes near c_target.

    Returns (passes, min_distance) where passes = (min_distance < tol).
    """
    _, gamma = geodesic_solve(c_start, c_end, max_r, N_steps=N_steps)
    distances = np.abs(gamma - c_target)
    min_dist = float(np.min(distances))
    return min_dist < tol, min_dist


def nc_metric_curvature(c_val: float, max_r: int = 10,
                         weights: Optional[Dict[int, float]] = None,
                         dc: float = 1e-4) -> float:
    """Scalar curvature R_NC(c) of the shadow metric.

    For a 1D metric g_{cc}(c), the scalar curvature is:
    R = -(1/g) * d^2(sqrt(g))/dc^2 / sqrt(g)
      = -(1/2g) * [g'' - (g')^2/(2g)]

    where g = g_{cc}, g' = dg/dc, g'' = d^2g/dc^2.
    """
    g = shadow_metric_tensor(c_val, max_r, weights)
    g_plus = shadow_metric_tensor(c_val + dc, max_r, weights)
    g_minus = shadow_metric_tensor(c_val - dc, max_r, weights)
    g_prime = (g_plus - g_minus) / (2 * dc)
    g_double = (g_plus - 2 * g + g_minus) / dc**2

    if abs(g) < 1e-15:
        return 0.0

    # Gaussian curvature for 1D manifold in ambient embedding
    # K = -1/(2*g) * (g'' - g'^2 / (2*g))
    R = -(1.0 / (2 * g)) * (g_double - g_prime**2 / (2 * g))
    return R


