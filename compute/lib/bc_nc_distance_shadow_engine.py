def koszul_dual_distance(c_val: float, max_r: int = 10,
                          N_sample: int = 200) -> float:
    """Distance d(phi_c, phi_{26-c}) from a Virasoro algebra to its Koszul dual.

    By AP24: kappa(Vir_c) + kappa(Vir_{26-c}) = 13.
    This distance should vanish at c=13 (self-dual point, AP8).
    """
    c_dual = 26.0 - c_val
    return connes_distance_truncated(c_val, c_dual, max_r, N_sample=N_sample)


def koszul_dual_distance_kappa_sector(c_val: float) -> float:
    """Koszul dual distance in pure kappa sector.

    d_kappa(c, 26-c) = |kappa(c) - kappa(26-c)| = |c/2 - (26-c)/2| = |c - 13|.
    Vanishes at c=13.
    """
    return abs(c_val - 13.0)


def self_dual_vanishing_exponent(c_values: Optional[List[float]] = None,
                                  max_r: int = 8,
                                  N_sample: int = 100) -> Tuple[float, float]:
    """Compute the vanishing exponent alpha in d(c, 26-c) ~ |c-13|^alpha.

    Fits log(d) = alpha * log(|c-13|) + const near c=13.

    Returns (alpha, r_squared) for the fit quality.
    """
    if c_values is None:
        # Points near c=13 on both sides
        c_values = [13.0 + delta for delta in
                    [0.1, 0.2, 0.5, 1.0, 2.0, 3.0, 5.0, 8.0, 10.0, 12.0]]

    log_x = []
    log_d = []
    for cv in c_values:
        x = abs(cv - 13.0)
        if x < 1e-10:
            continue
        d = connes_distance_truncated(cv, 26.0 - cv, max_r, N_sample=N_sample)
        if d > 1e-15:
            log_x.append(math.log(x))
            log_d.append(math.log(d))

    if len(log_x) < 2:
        return 1.0, 0.0

    # Linear regression: log_d = alpha * log_x + beta
    lx = np.array(log_x)
    ld = np.array(log_d)
    A_mat = np.vstack([lx, np.ones(len(lx))]).T
    result = np.linalg.lstsq(A_mat, ld, rcond=None)
    alpha = result[0][0]

    # R^2
    ld_pred = A_mat @ result[0]
    ss_res = np.sum((ld - ld_pred)**2)
    ss_tot = np.sum((ld - np.mean(ld))**2)
    r_sq = 1.0 - ss_res / ss_tot if ss_tot > 1e-15 else 0.0

    return float(alpha), float(r_sq)


