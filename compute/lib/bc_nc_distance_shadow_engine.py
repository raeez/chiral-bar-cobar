def connes_distance_kappa_sector(c1: float, c2: float) -> float:
    """Connes distance in the pure kappa sector (arity 2 only).

    In the kappa sector, kappa(c) = c/2 is the only observable.
    The Lipschitz condition ||[D, f]|| <= 1 becomes |f'(c)| * |kappa'(c)| <= 1.
    Since kappa'(c) = 1/2: d(phi_{c1}, phi_{c2}) = |kappa(c1) - kappa(c2)|
    = |c1 - c2| / 2.

    This is the LEADING approximation. Higher arities correct this.
    """
    return abs(c1 - c2) / 2.0


def connes_distance_truncated(c1: float, c2: float, max_r: int = 10,
                                weights: Optional[Dict[int, float]] = None,
                                N_sample: int = 200) -> float:
    """Connes distance between phi_{c1} and phi_{c2} including arities up to max_r.

    Computed via the geodesic integral:
        d(c1, c2) = int_{c1}^{c2} sqrt(g_cc(c)) dc

    where g_cc is the shadow metric tensor. This equals the Connes distance
    when the algebra is commutative (which A^sh is, being polynomial).

    For commutative A^sh, the Connes distance reduces to the geodesic
    distance in the Riemannian metric induced by the shadow embedding.

    N_sample: number of quadrature points for the integral.
    """
    if abs(c1 - c2) < 1e-15:
        return 0.0

    a, b = min(c1, c2), max(c1, c2)

    # Trapezoidal quadrature
    cs = np.linspace(a, b, N_sample + 1)
    integrand = np.zeros(N_sample + 1)
    for i, cv in enumerate(cs):
        # Avoid c=0 and singular points
        if abs(cv) < 0.01:
            cv_safe = 0.01 if cv >= 0 else -0.01
        elif abs(5 * cv + 22) < 0.01:
            cv_safe = cv + 0.1
        else:
            cv_safe = cv
        g = shadow_metric_tensor(cv_safe, max_r, weights)
        integrand[i] = math.sqrt(max(g, 0.0))

    dc = (b - a) / N_sample
    distance = np.trapz(integrand, dx=dc)
    return float(distance)


def connes_distance_matrix(c_values: List[float], max_r: int = 10,
                            weights: Optional[Dict[int, float]] = None,
                            N_sample: int = 100) -> np.ndarray:
    """Distance matrix d(c_i, c_j) for a list of central charge values.

    Returns an n x n symmetric matrix where n = len(c_values).
    """
    n = len(c_values)
    D = np.zeros((n, n))
    for i in range(n):
        for j in range(i + 1, n):
            d_ij = connes_distance_truncated(
                c_values[i], c_values[j], max_r, weights, N_sample
            )
            D[i, j] = d_ij
            D[j, i] = d_ij
    return D


def connes_distance_via_lipschitz(c1: float, c2: float, max_r: int = 10,
                                    N_test: int = 500) -> float:
    """Connes distance via Lipschitz supremum (direct definition).

    d(phi_{c1}, phi_{c2}) = sup{ |f(c1) - f(c2)| : ||[D, f]|| <= 1 }

    We sample test functions f from the shadow algebra and compute the
    supremum. Test functions: f = S_r for each r, plus linear combinations.
    The Lipschitz constraint is ||[D, f]|| <= 1.

    For commutative algebras, this equals the geodesic distance.
    """
    Sr1 = virasoro_shadow_coefficients(c1, max_r)
    Sr2 = virasoro_shadow_coefficients(c2, max_r)

    # Method: for each shadow observable S_r, the Lipschitz constant is
    # L_r = sup_c |dS_r/dc|. The normalized observable f_r = S_r / L_r
    # satisfies ||[D, f_r]|| <= 1 (approximately). Then:
    # d >= |f_r(c1) - f_r(c2)| = |S_r(c1) - S_r(c2)| / L_r.
    # The Connes distance is the supremum over all such test functions.

    # Also use the geodesic integral as a tighter bound
    max_val = 0.0

    for r in range(2, max_r + 1):
        if r not in Sr1 or r not in Sr2:
            continue
        delta_Sr = abs(Sr1[r] - Sr2[r])

        # Estimate Lipschitz constant of S_r via derivative sampling
        c_min, c_max = min(c1, c2), max(c1, c2)
        # Also sample outside the interval to get the global Lip constant
        c_sample_min = max(0.5, c_min - 2)
        c_sample_max = c_max + 2
        test_cs = np.linspace(c_sample_min, c_sample_max, 50)
        lip_r = 0.0
        for tc in test_cs:
            deriv = virasoro_shadow_derivatives(tc, max_r=r)
            if r in deriv:
                lip_r = max(lip_r, abs(deriv[r]))

        if lip_r > 1e-15:
            val = delta_Sr / lip_r
            max_val = max(max_val, val)

    return max_val


