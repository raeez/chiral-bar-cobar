def full_distance_profile(c_val: float, c_range: Optional[List[float]] = None,
                           max_r: int = 8) -> Dict[str, Any]:
    """Full NC distance profile from c_val to all points in c_range.

    Returns dict with keys: 'c_values', 'connes', 'euclidean', 'kappa_sector',
    'metric_tensor', 'curvature'.
    """
    if c_range is None:
        c_range = [float(i) for i in range(1, 26)]

    result = {
        'c_center': c_val,
        'c_values': c_range,
        'connes': [],
        'euclidean': [],
        'kappa_sector': [],
        'metric_tensor': [],
        'curvature': [],
    }
    for cv in c_range:
        result['connes'].append(connes_distance_truncated(c_val, cv, max_r, N_sample=80))
        result['euclidean'].append(abs(c_val - cv))
        result['kappa_sector'].append(connes_distance_kappa_sector(c_val, cv))
        result['metric_tensor'].append(shadow_metric_tensor(cv, max_r))
        result['curvature'].append(nc_metric_curvature(cv, max_r))

    return result


def zero_distance_summary(N: int = 20, max_r: int = 6) -> Dict[str, Any]:
    """Summary of NC distances at zeta zeros.

    Returns dict with:
    - consecutive_spacings: d(rho_n, rho_{n+1}) for n=1..N-1
    - to_self_dual: d(rho_n, 13) for n=1..N
    - to_reflected: d(rho_n, rho_n_bar) for n=1..N
    - kappa_spacings: kappa-sector consecutive spacings
    - normalized_spacings: normalized for GUE comparison
    """
    N_avail = min(N, len(ZETA_ZEROS_GAMMA))

    consec = []
    kappa_consec = []
    for n in range(1, N_avail):
        consec.append(nc_distance_consecutive_zeros(n, max_r))
        gamma_n = ZETA_ZEROS_GAMMA[n - 1]
        gamma_n1 = ZETA_ZEROS_GAMMA[n]
        kappa_consec.append(13.0 * abs(gamma_n1 - gamma_n))

    to_sd = [nc_distance_zero_to_self_dual(n, max_r) for n in range(1, N_avail + 1)]
    to_ref = [nc_distance_zero_to_reflected(n, max_r) for n in range(1, N_avail + 1)]

    consec_arr = np.array(consec)
    mean_spacing = np.mean(consec_arr) if len(consec_arr) > 0 else 1.0
    normalized = consec_arr / mean_spacing if mean_spacing > 1e-15 else consec_arr

    return {
        'consecutive_spacings': consec,
        'kappa_spacings': kappa_consec,
        'to_self_dual': to_sd,
        'to_reflected': to_ref,
        'normalized_spacings': normalized.tolist(),
        'mean_spacing': float(mean_spacing),
    }
