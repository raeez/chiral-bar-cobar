# 6. Wasserstein distance from shadow spectral measures
# =========================================================================

def shadow_spectral_measure(c_val: float, max_r: int = 15) -> Tuple[np.ndarray, np.ndarray]:
    """Shadow spectral measure at central charge c.

    The spectral measure mu_c = sum_r delta(lambda - lambda_r(c)) * w_r
    where lambda_r = r * S_r(c) are the Dirac eigenvalues and w_r are weights.

    Returns (positions, weights) arrays.
    """
    Sr = virasoro_shadow_coefficients(c_val, max_r)
    positions = []
    weights_arr = []
    for r in range(2, max_r + 1):
        if r in Sr:
            lam = r * Sr[r]
            if isinstance(lam, complex):
                lam = lam.real if abs(lam.imag) < 1e-10 else abs(lam)
            positions.append(float(lam))
            weights_arr.append(1.0 / r**2)

    pos = np.array(positions)
    wt = np.array(weights_arr)
    # Normalize weights
    if np.sum(wt) > 0:
        wt = wt / np.sum(wt)
    return pos, wt


def wasserstein_1d(positions1: np.ndarray, weights1: np.ndarray,
                    positions2: np.ndarray, weights2: np.ndarray,
                    p: int = 1) -> float:
    """Wasserstein-p distance between two 1D discrete measures.

    Uses the closed-form for 1D distributions via quantile functions:
    W_p^p = int_0^1 |F_1^{-1}(t) - F_2^{-1}(t)|^p dt

    For discrete measures, this is computed via sorted atoms.
    """
    # Build sorted CDFs
    idx1 = np.argsort(positions1)
    idx2 = np.argsort(positions2)
    pos1 = positions1[idx1]
    w1 = weights1[idx1]
    pos2 = positions2[idx2]
    w2 = weights2[idx2]

    # Merge into common grid for quantile comparison
    # Use the standard 1D Wasserstein computation
    cdf1 = np.cumsum(w1)
    cdf2 = np.cumsum(w2)

    # All breakpoints
    all_breaks = np.sort(np.unique(np.concatenate([cdf1, cdf2, [0.0, 1.0]])))

    total = 0.0
    for i in range(len(all_breaks) - 1):
        t_lo = all_breaks[i]
        t_hi = all_breaks[i + 1]
        t_mid = (t_lo + t_hi) / 2

        # Quantile of measure 1 at t_mid
        idx_q1 = np.searchsorted(cdf1, t_mid, side='left')
        idx_q1 = min(idx_q1, len(pos1) - 1)
        q1 = pos1[idx_q1]

        # Quantile of measure 2 at t_mid
        idx_q2 = np.searchsorted(cdf2, t_mid, side='left')
        idx_q2 = min(idx_q2, len(pos2) - 1)
        q2 = pos2[idx_q2]

        dt = t_hi - t_lo
        total += abs(q1 - q2)**p * dt

    return total**(1.0 / p) if p > 0 else total


def wasserstein_shadow(c1: float, c2: float, p: int = 1,
                        max_r: int = 15) -> float:
    """Wasserstein-p distance between shadow spectral measures at c1 and c2."""
    pos1, w1 = shadow_spectral_measure(c1, max_r)
    pos2, w2 = shadow_spectral_measure(c2, max_r)

    if len(pos1) == 0 or len(pos2) == 0:
        return float('inf')

    return wasserstein_1d(pos1, w1, pos2, w2, p)


def wasserstein_vs_connes_comparison(c_pairs: List[Tuple[float, float]],
                                      max_r: int = 10) -> Dict[str, List[float]]:
    """Compare Wasserstein and Connes distances for multiple pairs.

    Returns dict with 'connes', 'W1', 'W2', 'euclidean', 'kappa_sector' lists.
    """
    result = {
        'connes': [], 'W1': [], 'W2': [],
        'euclidean': [], 'kappa_sector': [],
    }
    for c1, c2 in c_pairs:
        result['connes'].append(connes_distance_truncated(c1, c2, max_r))
        result['W1'].append(wasserstein_shadow(c1, c2, p=1, max_r=max_r))
        result['W2'].append(wasserstein_shadow(c1, c2, p=2, max_r=max_r))
        result['euclidean'].append(abs(c1 - c2))
        result['kappa_sector'].append(connes_distance_kappa_sector(c1, c2))
    return result


