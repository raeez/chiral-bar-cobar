def shadow_metric_tensor(c_val: float, max_r: int = 10,
                          weights: Optional[Dict[int, float]] = None) -> float:
    """Shadow metric g_cc(c) = sum_r w_r * (dS_r/dc)^2.

    The metric on the c-line induced by the shadow embedding
    c -> (S_2(c), S_3(c), S_4(c), ...). Each arity component contributes
    to the metric with weight w_r. Default weights: w_r = 1/r^2
    (spectral decay).

    Returns g_cc as a positive real number.
    """
    if weights is None:
        weights = {r: 1.0 / r**2 for r in range(2, max_r + 1)}

    derivs = virasoro_shadow_derivatives(c_val, max_r)
    g_cc = 0.0
    for r, dSr in derivs.items():
        w = weights.get(r, 0.0)
        g_cc += w * dSr**2
    return g_cc


def shadow_dirac_spectrum(c_val: float, N_trunc: int = 20) -> np.ndarray:
    """Spectrum of the shadow Dirac operator D_sh at central charge c.

    D_sh acts on the truncated shadow Hilbert space spanned by
    {|r>}_{r=2}^{N_trunc+1} with eigenvalues lambda_r = r * S_r(c).

    This is the spectral data of the NC geometry: the eigenvalues of D_sh
    encode the shadow obstruction tower with arity weighting.
    """
    Sr = virasoro_shadow_coefficients(c_val, N_trunc + 1)
    eigenvalues = []
    for r in range(2, N_trunc + 2):
        if r in Sr:
            lam = r * Sr[r]
            if isinstance(lam, complex):
                lam = lam.real if abs(lam.imag) < 1e-10 else lam
            eigenvalues.append(lam)
    return np.array(eigenvalues, dtype=complex)


def shadow_commutator_norm(f_vals: np.ndarray, dirac_eigenvalues: np.ndarray) -> float:
    """||[D_sh, f]|| for a diagonal operator f in the shadow eigenbasis.

    In the eigenbasis of D_sh, [D_sh, f] has matrix elements
    (lambda_i - lambda_j) * <i|f|j>. For a function on the shadow space
    (diagonal in eigenbasis), the commutator norm is
    max_{i != j} |lambda_i - lambda_j| * |f_{ij}|.

    For diagonal f: [D_sh, f] is off-diagonal with entries
    (lambda_i - lambda_j) * f_{ij}. If f is multiplication by a shadow
    observable, f_{ij} encodes the transition amplitudes.

    Simplified model: f acts by multiplication on the truncated space,
    so ||[D, f]|| = max_i |df/d(lambda_i)| * gap_i where gap_i are spectral gaps.
    We use the Lipschitz estimate: ||[D, f]|| ~ max|f'| * max spectral gap^{-1}.
    """
    n = len(dirac_eigenvalues)
    if n < 2:
        return 0.0

    # Spectral gaps
    sorted_eigs = np.sort(dirac_eigenvalues.real)
    gaps = np.diff(sorted_eigs)
    nonzero_gaps = gaps[np.abs(gaps) > 1e-15]
    if len(nonzero_gaps) == 0:
        return float('inf')

    # For diagonal f: norm of commutator is max spectral variation of f
    # divided by the minimum spectral gap
    f_diffs = np.abs(np.diff(np.sort(f_vals.real)))
    if len(f_diffs) == 0:
        return 0.0

    max_f_variation = np.max(f_diffs)
    min_gap = np.min(np.abs(nonzero_gaps))

    return max_f_variation / min_gap if min_gap > 1e-15 else float('inf')


