r"""Non-commutative distance function on shadow geometry and geodesics at zeros.

Mathematical foundation
-----------------------
Connes' spectral distance formula defines a metric on the state space of a
non-commutative algebra (A, H, D):

    d(phi, psi) = sup{ |phi(a) - psi(a)| : ||[D, a]|| <= 1 }

for states phi, psi on the NC algebra A. Applied to the shadow spectral triple
(A^sh, H, D_sh) of a chirally Koszul algebra, this produces a genuine metric
on the "shadow parameter space" whose geometry encodes arithmetic structure.

For the Virasoro family, the shadow algebra A^sh = C[kappa, S_3, S_4, ...]
is parametrized by the central charge c. States are evaluation functionals
phi_c(f) = f(c), and the Connes distance d(phi_c, phi_{c'}) gives a
non-commutative deformation of the Euclidean metric on the c-line.

The shadow Dirac operator D_sh acts on the shadow Hilbert space
H = L^2(A^sh, <,>) with the inner product inherited from the cyclic pairing.
On the primary (kappa) sector, D_sh = d/dc acts as differentiation.
Higher-arity sectors contribute correction terms from the shadow connection
nabla^sh = d - Q'/(2Q) dt (thm:shadow-connection).

Key objects
-----------
1. SHADOW SPECTRAL TRIPLE: (A^sh, H_sh, D_sh) where
   - A^sh = polynomial algebra generated by shadow projections {S_r}_{r>=2}
   - H_sh = l^2 completion of A^sh with inner product from cyclic pairing
   - D_sh = shadow Dirac operator with kappa-weighted spectrum

2. CONNES DISTANCE: d(phi_c, phi_{c'}) = sup{ |f(c) - f(c')| : ||[D_sh, f]|| <= 1 }
   On the kappa sector: the Lipschitz condition ||[D_sh, f]|| <= 1 becomes
   |f'(c)| * |d(kappa)/dc| <= 1 (since [D_sh, f] ~ f' * kappa'(c)).
   For Virasoro kappa(c) = c/2: d(phi_c, phi_{c'}) = |c - c'| / |kappa'| = |c - c'| / (1/2) ...
   BUT the higher-arity corrections from S_3, S_4, ... modify this.

3. SHADOW GEODESICS: Shortest paths in the NC metric. The geodesic equation
   is gamma''(t) + Gamma^c_{cc} gamma'(t)^2 = 0 where the Christoffel symbol
   is determined by the shadow metric g_cc = sum_r (dS_r/dc)^2 * w_r.

4. WASSERSTEIN DISTANCE: W_p(mu_A, mu_{A'}) between shadow spectral measures
   at different central charges, compared with Connes distance.

Verification paths
------------------
    Path 1: Direct Connes formula (Lipschitz supremum)
    Path 2: Geodesic equation integration
    Path 3: Lipschitz norm from spectral data
    Path 4: Wasserstein comparison from spectral measures
    Path 5: Numerical convergence in truncation level

Anti-patterns guarded
---------------------
    AP1:  kappa formulas computed from first principles per family
    AP8:  Virasoro self-dual at c=13, NOT c=26
    AP10: Expected values derived from multiple independent paths
    AP20: kappa(A) is intrinsic to A; kappa_eff is different
    AP24: kappa(Vir_c) + kappa(Vir_{26-c}) = 13, NOT 0
    AP39: kappa = c/2 for Virasoro ONLY; not for general VOAs
    AP48: kappa depends on full algebra, not just Virasoro subalgebra

Connections to monograph
------------------------
    thm:shadow-connection (higher_genus_modular_koszul.tex)
    def:shadow-metric (higher_genus_modular_koszul.tex)
    thm:riccati-algebraicity (higher_genus_modular_koszul.tex)
    thm:single-line-dichotomy (higher_genus_modular_koszul.tex)
    Connes, "Noncommutative Geometry", Academic Press 1994
    Connes-Marcolli, "Noncommutative Geometry, Quantum Fields
      and Motives", AMS 2008

Conventions
-----------
    - Virasoro: kappa(Vir_c) = c/2. Self-dual at c=13.
    - Koszul duality: c -> 26-c. kappa + kappa' = 13 (AP24).
    - Shadow tower: S_2 = c/2, S_3 = 2, S_4 = 10/[c(5c+22)].
    - Cohomological grading (|d| = +1).
    - Zeta zeros: rho_n = 1/2 + i*gamma_n, c(rho) = 26*rho.
"""

