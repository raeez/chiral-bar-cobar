def virasoro_kappa(c_val: complex) -> complex:
    """kappa(Vir_c) = c/2. AP39: this is Virasoro-specific."""
    return c_val / 2


def virasoro_shadow_coefficients(c_val: complex, max_r: int = 20) -> Dict[int, complex]:
    """Shadow tower coefficients S_r(c) for Virasoro at central charge c.

    S_2 = c/2, S_3 = 2, S_4 = 10/[c(5c+22)].
    Higher arities from the bracket recurrence.
    """
    if abs(c_val) < 1e-15:
        # c=0: S_2=0, S_3=2, S_4 diverges. Return what we can.
        Sr: Dict[int, complex] = {2: 0.0, 3: 2.0}
        return Sr

    P = 2.0 / c_val  # propagator
    Sr = {}
    Sr[2] = c_val / 2
    Sr[3] = 2.0
    denom4 = c_val * (5 * c_val + 22)
    if abs(denom4) < 1e-30:
        Sr[4] = float('inf')
        return Sr
    Sr[4] = 10.0 / denom4

    for r in range(5, max_r + 1):
        o_r = 0.0
        for j in range(2, r + 1):
            kk = r + 2 - j
            if kk < 2 or kk > r or kk not in Sr:
                continue
            if j > kk:
                continue
            bracket = j * Sr[j] * P * kk * Sr[kk]
            if j == kk:
                o_r += 0.5 * bracket
            else:
                o_r += bracket
        Sr[r] = -o_r / (2 * r)
    return Sr


def virasoro_shadow_derivatives(c_val: float, max_r: int = 15,
                                 dc: float = 1e-6) -> Dict[int, float]:
    """Numerical derivatives dS_r/dc at given c, via central differences.

    These encode how the shadow tower varies with the central charge.
    """
    Sr_plus = virasoro_shadow_coefficients(c_val + dc, max_r)
    Sr_minus = virasoro_shadow_coefficients(c_val - dc, max_r)
    derivs = {}
    for r in Sr_plus:
        if r in Sr_minus:
            val = (Sr_plus[r] - Sr_minus[r]) / (2 * dc)
            if isinstance(val, complex) and abs(val.imag) < 1e-10:
                val = val.real
            derivs[r] = val
    return derivs


