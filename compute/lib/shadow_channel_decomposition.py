# Multi-channel shadow algebra
# ========================================================================

class MultiChannelShadow:
    """Shadow algebra data for a multi-channel modular Koszul algebra.

    For dim H²_cyc = r, the shadow algebra A^sh has:
    - Arity 2: r generators κ_1, ..., κ_r (the modular characteristics)
    - Arity 3: cubic shadows C_{ij} = [η_i, η_j] (Gerstenhaber brackets)
    - Arity 4: quartic shadows Q_{ij,kl} = η_i · η_j · η_k · η_l
    - Higher arities: generated by lower arities via the master equation

    For abelian algebras: C_{ij} = 0 for all i,j (no brackets).
    For non-abelian one-channel: r = 1, so C = C_{11} (single cubic shadow).
    """

    def __init__(self, dim_h2: int, is_abelian: bool,
                 kappa_values: List[float] = None):
        """
        Args:
            dim_h2: dimension of H²_cyc
            is_abelian: whether the OPE is abelian
            kappa_values: the scalar traces κ_i = tr(η_i) for each channel
        """
        self.r = dim_h2
        self.is_abelian = is_abelian
        self.kappa = kappa_values or [1.0] * dim_h2

    @property
    def n_cubic_shadows(self) -> int:
        """Number of independent cubic shadow components C_{ij}."""
        if self.is_abelian:
            return 0  # all vanish
        return self.r * (self.r + 1) // 2

    @property
    def n_quartic_shadows(self) -> int:
        """Number of independent quartic shadow components."""
        if self.is_abelian:
            return 0
        # Symmetric tensor: dim S⁴(ℂ^r) for the quartic part
        r = self.r
        return r * (r + 1) * (r + 2) * (r + 3) // 24

    @property
    def n_gerstenhaber_brackets(self) -> int:
        """Number of independent Gerstenhaber brackets [η_i, η_j] in H³."""
        return self.r * (self.r - 1) // 2

    def cubic_shadow(self, i: int, j: int) -> float:
        """The cubic shadow C_{ij} = ⟨[η_i, η_j]⟩.

        For abelian algebras: C_{ij} = 0 for all i, j.
        For one-channel non-abelian: C_{11} = κ · C_0 (the standard cubic shadow).
        """
        if self.is_abelian:
            return 0.0
        if self.r == 1:
            # One-channel: C = κ · (Killing-type structure constant)
            return self.kappa[0]  # simplified; actual value depends on the algebra
        # Multi-channel non-abelian: not expected to occur (Level 2 kills it)
        return 0.0

    def quartic_contact(self, i: int, j: int) -> float:
        """The quartic contact invariant Q_{ij} = η_i · η_j in A^sh_{4,0}.

        For abelian: Q_{ij} = 0 (no contact structure).
        For one-channel non-abelian: Q_{11} = Q_contact (e.g., 10/[c(5c+22)] for Vir).
        """
        if self.is_abelian:
            return 0.0
        if self.r == 1:
            return 1.0  # normalized; actual value is Q_contact(c)
        return 0.0

    def total_kappa(self) -> float:
        """Total modular characteristic κ = Σ κ_i."""
        return sum(self.kappa)

    def genus_g_shadow(self, g: int) -> np.ndarray:
        """The genus-g shadow vector Θ^(g) = Σ_i κ_i · η_i^(g).

        Returns a vector of length r, representing the r-channel shadow at genus g.
        For abelian algebras: Θ^(g) = diag(κ_1, ..., κ_r) · λ_g.
        Each channel is independent.
        """
        return np.array(self.kappa) * 1.0  # * lambda_g (tautological class)

    def verify_channel_independence(self) -> bool:
        """Verify that channels evolve independently.

        This holds when:
        (a) All Gerstenhaber brackets [η_i, η_j] = 0 (abelian case), OR
        (b) r = 1 (one-channel)

        Returns True if channels are independent.
        """
        if self.r == 1:
            return True
        if self.is_abelian:
            return True
        # Multi-channel non-abelian: brackets might not vanish
        return False


# ========================================================================
# Rank-N Heisenberg: explicit multi-channel shadow computation
# ========================================================================

class HeisenbergShadow:
    """Multi-channel shadow algebra for the rank-N Heisenberg.

    The rank-N Heisenberg has:
    - dim H²_cyc = N(N+1)/2 (metric deformations)
    - Shadow tower terminates at arity 2 (abelian → no cubic/quartic shadows)
    - Each metric component g_{ij} contributes independently to the genus tower

    The shadow algebra: A^sh = ℂ[g_{11}, g_{12}, ..., g_{NN}] ⊗ ℂ[λ_1, λ_2, ...]
    """

    def __init__(self, N: int, metric: np.ndarray = None):
        """
        Args:
            N: rank
            metric: N×N symmetric positive-definite matrix (default: identity)
        """
        self.N = N
        self.metric = metric if metric is not None else np.eye(N)
        assert self.metric.shape == (N, N)
        assert np.allclose(self.metric, self.metric.T), "Metric must be symmetric"

    @property
    def n_channels(self) -> int:
        """Number of deformation channels = N(N+1)/2."""
        return self.N * (self.N + 1) // 2

    @property
    def total_kappa(self) -> float:
        """Total modular characteristic κ = tr(g)."""
        return np.trace(self.metric)

    @property
    def central_charge(self) -> int:
        """Central charge c = N (the rank)."""
        return self.N

    def channel_kappas(self) -> Dict[Tuple[int, int], float]:
        """Individual channel modular characteristics.

        κ_{ij} = g_{ij} for each pair (i,j) with i ≤ j.
        """
        kappas = {}
        for i in range(self.N):
            for j in range(i, self.N):
                kappas[(i, j)] = self.metric[i, j]
        return kappas

    def genus_g_shadow_matrix(self, g: int) -> np.ndarray:
        """The genus-g shadow as an N×N matrix.

        Θ^(g)_{ij} = g_{ij} · λ_g

        The matrix structure shows the channel decomposition:
        - Diagonal elements: g_{ii} · λ_g (self-channel contributions)
        - Off-diagonal elements: g_{ij} · λ_g (cross-channel contributions)

        Returns the N×N matrix g_{ij} (times the implicit λ_g).
        """
        return self.metric.copy()

    def verify_abelian_vanishing(self) -> Dict:
        """Verify that all non-scalar shadow components vanish.

        For abelian algebras:
        - Cubic shadow C = 0 (no Lie bracket)
        - Quartic contact Q = 0 (no contact structure)
        - Quintic obstruction o^(5) = 0
        - Shadow tower terminates at arity 2

        Returns verification dict.
        """
        return {
            'cubic_shadow_vanishes': True,  # [η_i, η_j] = 0 (abelian)
            'quartic_contact_vanishes': True,  # Q = 0
            'quintic_obstruction_vanishes': True,  # o^(5) = 0
            'termination_arity': 2,
            'reason': 'Abelian OPE: all brackets and contacts vanish',
        }

    def complementarity_genus_g(self, g: int) -> Dict:
        """Multi-channel complementarity at genus g.

        Q_g(A) + Q_g(A!) = H*(M̄_g, Z(A))

        For the rank-N Heisenberg:
        - Z(A) = center = ℂ^N (the abelian currents)
        - H*(M̄_g, Z(A)) decomposes channel by channel
        - Q_g(A) = Σ_{ij} g_{ij} · Q_g(η_{ij})
        - Q_g(A!) = Σ_{ij} g^{ij} · Q_g(η_{ij}) (dual metric)

        The complementarity constant per channel:
          κ_{ij} + κ^!_{ij} = 0 (anti-symmetry for abelian algebras)
        since A! = A^v (the contragredient) has metric -g.
        """
        kappas = self.channel_kappas()
        g_inv = np.linalg.inv(self.metric)

        return {
            'n_channels': self.n_channels,
            'kappa_total': self.total_kappa,
            'kappa_dual_total': -self.total_kappa,  # anti-symmetry
            'complementarity_sum': 0.0,  # κ + κ! = 0 for abelian
            'channel_decomposition': True,
            'per_channel_kappas': kappas,
        }

    def shadow_algebra_generators(self) -> Dict:
        """The generators of the shadow algebra A^sh.

        A^sh = ℂ[κ_{ij} : i ≤ j] (polynomial ring in N(N+1)/2 variables)
        graded by arity (all generators at arity 2).

        This is a free commutative algebra because:
        1. All cubic shadows vanish (abelian)
        2. All relations are trivial
        3. The shadow master equation ∇_H(Sh_r) + o^(r) = 0 is trivially
           satisfied for r ≥ 3 (both terms vanish)
        """
        generators = {}
        for i in range(self.N):
            for j in range(i, self.N):
                generators[f'κ_{i+1}{j+1}'] = {
                    'arity': 2,
                    'genus': 0,
                    'value': self.metric[i, j],
                    'channel': (i, j),
                }
        return {
            'n_generators': len(generators),
            'generators': generators,
            'is_free_commutative': True,
            'highest_arity_generator': 2,
            'shadow_tower_terminates': True,
            'termination_arity': 2,
        }


