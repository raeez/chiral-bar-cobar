# Genus-2 multi-channel clutching computation
# ========================================================================

def genus2_multichannel_clutching(kappa_matrix: np.ndarray) -> Dict:
    """Compute the genus-2 shadow via clutching from genus 1.

    Genus-2 boundary: δ_0 (non-separating) and δ_1 (separating, g=1+1).

    At δ_1 (separating): Θ^(2)|_{δ_1} = Θ^(1) ⋆ Θ^(1)
    = (Σ_ij K_ij η_ij λ_1) ⋆ (Σ_kl K_kl η_kl λ_1)
    = Σ_{ij,kl} K_ij K_kl (η_ij ⋆ η_kl) λ_1²

    For abelian: η_ij ⋆ η_kl = δ_{jk} η_il (propagator contraction)
    This gives: Θ^(2)|_{δ_1} = K² · (propagator) · λ_1²

    At δ_0 (non-separating): Θ^(2)|_{δ_0} = Δ_ns(Θ^(1))
    = Σ_ij K_ij Δ_ns(η_ij) = tr(K) · (ns-kernel) + (off-diagonal corrections)

    For abelian: Δ_ns(η_ij) = δ_ij · (ns-kernel), so only diagonal channels survive.
    Θ^(2)|_{δ_0} = tr(K) · (ns-kernel)

    Key result: at genus 2, the separating clutching sees K² (matrix product),
    while the non-separating clutching sees tr(K). The channel structure propagates
    through matrix multiplication at separating boundaries.
    """
    r = kappa_matrix.shape[0]
    total_kappa = np.trace(kappa_matrix)

    # Separating boundary: K² (matrix product)
    sep_genus2 = kappa_matrix @ kappa_matrix

    # Non-separating boundary: tr(K) only
    ns_genus2 = total_kappa

    # Total genus-2 shadow (combining both boundary contributions)
    # The exact combination involves the Picard-Lefschetz formula
    # for the boundary of M̄_2.

    return {
        'n_channels': r,
        'separating_shadow': sep_genus2,
        'nonseparating_shadow': ns_genus2,
        'kappa_squared': sep_genus2,
        'total_kappa': total_kappa,
        'total_kappa_squared': total_kappa ** 2,
        'trace_kappa_sq': np.trace(sep_genus2),
        # Key identity: tr(K²) ≤ tr(K)² with equality iff K is rank 1
        'trace_ineq_saturated': np.isclose(np.trace(sep_genus2), total_kappa ** 2),
        'is_rank_one': np.isclose(np.trace(sep_genus2), total_kappa ** 2),
    }


# ========================================================================
# The Cauchy-Schwarz shadow inequality (NEW)
# ========================================================================

def cauchy_schwarz_shadow(kappa_matrix: np.ndarray) -> Dict:
    """The Cauchy-Schwarz inequality for the shadow algebra.

    For a multi-channel algebra with κ-matrix K:
        tr(K²) ≤ tr(K)²

    with equality iff K is a rank-1 matrix (i.e., K = κ · vv^T for
    some vector v, which means the algebra is effectively one-channel
    up to a change of basis).

    This is a GENUINE invariant of the multi-channel structure:
    - One-channel: tr(K²) = tr(K)² = κ² (equality)
    - Multi-channel: tr(K²) < tr(K)² (strict inequality)

    The ratio ρ = tr(K²) / tr(K)² ∈ [1/r, 1] measures the
    "channel spread":
    - ρ = 1: effectively one-channel
    - ρ = 1/r: maximally spread (all channels equal)

    At genus 2, the separating clutching gives a shadow proportional to K².
    The trace tr(K²) appears in the genus-2 complementarity relation.
    The inequality tr(K²) ≤ κ² means the genus-2 complementarity
    "loses information" compared to genus 1 — the off-diagonal channels
    contribute less than the diagonal ones.

    This is the SHADOW CAUCHY-SCHWARZ INEQUALITY:
        Q_2(A) ≤ Q_1(A)²
    with equality iff A is one-channel.
    """
    r = kappa_matrix.shape[0]
    total_kappa = np.trace(kappa_matrix)
    trace_sq = np.trace(kappa_matrix @ kappa_matrix)

    if abs(total_kappa) < 1e-15:
        return {
            'total_kappa': 0.0,
            'trace_kappa_sq': trace_sq,
            'ratio': float('inf'),
            'is_one_channel': True,
        }

    ratio = trace_sq / (total_kappa ** 2)

    return {
        'r': r,
        'total_kappa': total_kappa,
        'trace_kappa_sq': trace_sq,
        'kappa_squared': total_kappa ** 2,
        'ratio': ratio,
        'cauchy_schwarz_holds': trace_sq <= total_kappa ** 2 + 1e-10,
        'is_one_channel': np.isclose(ratio, 1.0),
        'channel_spread': 1.0 / ratio if ratio > 0 else float('inf'),
        'min_ratio': 1.0 / r,
        'max_ratio': 1.0,
        'interpretation': (
            'One-channel (ρ = 1)' if np.isclose(ratio, 1.0)
            else f'Multi-channel (ρ = {ratio:.4f}, spread = {1.0/ratio:.4f})'
        ),
    }


# ========================================================================
# Master computation: shadow channel decomposition for all examples
# ========================================================================

def master_shadow_decomposition() -> List[Dict]:
    """Run the shadow channel decomposition for all standard examples."""
    results = []

    # Rank-1 Heisenberg (one-channel)
    hs1 = HeisenbergShadow(1, np.array([[1.0]]))
    r1 = {
        'name': 'Heisenberg rank 1',
        'n_channels': hs1.n_channels,
        'total_kappa': hs1.total_kappa,
        'terminates_at': 2,
        **cauchy_schwarz_shadow(hs1.metric),
        **hs1.verify_abelian_vanishing(),
    }
    results.append(r1)

    # Rank-2 Heisenberg with identity metric (multi-channel)
    hs2_id = HeisenbergShadow(2)
    g2_id = genus2_multichannel_clutching(hs2_id.metric)
    r2 = {
        'name': 'Heisenberg rank 2 (identity metric)',
        'n_channels': hs2_id.n_channels,
        'total_kappa': hs2_id.total_kappa,
        'terminates_at': 2,
        **cauchy_schwarz_shadow(hs2_id.metric),
        'genus2_sep': g2_id['separating_shadow'].tolist(),
    }
    results.append(r2)

    # Rank-2 Heisenberg with general metric
    g_gen = np.array([[3.0, 1.0], [1.0, 2.0]])
    hs2_gen = HeisenbergShadow(2, g_gen)
    r3 = {
        'name': 'Heisenberg rank 2 (general metric)',
        'n_channels': hs2_gen.n_channels,
        'total_kappa': hs2_gen.total_kappa,
        'terminates_at': 2,
        **cauchy_schwarz_shadow(g_gen),
    }
    results.append(r3)

    # Rank-3 Heisenberg (identity metric)
    hs3 = HeisenbergShadow(3)
    r4 = {
        'name': 'Heisenberg rank 3',
        'n_channels': hs3.n_channels,
        'total_kappa': hs3.total_kappa,
        'terminates_at': 2,
        **cauchy_schwarz_shadow(hs3.metric),
    }
    results.append(r4)

    # Non-abelian one-channel examples
    for name, kappa in [('Virasoro c=25', 25.0/2),
                        ('W₃ c=2', 2.0/3),
                        ('ŝl₂ k=1', 9.0/4)]:
        K = np.array([[kappa]])
        r = {
            'name': name,
            'n_channels': 1,
            'total_kappa': kappa,
            **cauchy_schwarz_shadow(K),
            'non_abelian': True,
        }
        results.append(r)

    return results
