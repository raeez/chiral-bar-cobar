def quantum_parameter_from_level(
    lie_type: str, rank: int, p: int, q: int
) -> QuantumGroupParameter:
    """Compute the quantum group parameter q from an admissible level.

    For hat{g} at level k = p/q - h^v (admissible), the quantum group
    parameter is q_param = exp(pi*i/(k + h^v)) = exp(pi*i*q/p).

    Convention: k + h^v = p/q with gcd(p,q) = 1, p >= h^v.
    q_param = exp(pi*i*q/p).

    The ORDER of q_param as a root of unity:
        q_param^n = 1 iff pi*i*q*n/p is an integer multiple of 2*pi*i
        iff q*n/p is an even integer
        iff n = 2*p/gcd(2*q, 2*p)

    For gcd(p,q) = 1:
        If p is odd: q_param has order 2*p (since q*n = 2*p*m, n = 2*p*m/q,
            and gcd(q,p)=1 so min n = 2*p).
        If p is even: q_param has order p (since q is odd, q*n = 2*p*m,
            n = 2*p*m/q, and gcd(q,2*p) = gcd(q,2)*gcd(q/gcd(q,2),p) ...).
            Actually: q*n must be divisible by 2*p. Since gcd(q,p)=1,
            n must be divisible by p. And q*p must be divisible by 2.
            If q is odd and p is even: n = p works (q*p/2p = q/2... no).

    Let me just compute the order directly.
    q_param = exp(2*pi*i * q/(2p)).
    Order = 2*p / gcd(q, 2*p).
    """
    if gcd(p, q) != 1:
        raise ValueError(f"gcd(p,q) must be 1, got p={p}, q={q}")

    # Dual Coxeter numbers
    h_dual_map = {'sl': rank + 1, 'so': 2 * rank - 2 if rank >= 3 else rank,
                  'sp': rank + 1}
    if lie_type == 'sl':
        h_dual = rank + 1
    elif lie_type.startswith('so'):
        h_dual = 2 * rank - 2 if rank >= 4 else (rank if rank == 3 else 2)
    elif lie_type.startswith('sp'):
        h_dual = rank + 1
    else:
        h_dual = rank + 1  # fallback

    k = Fraction(p, q) - h_dual
    k_plus_hv = Fraction(p, q)

    # q = exp(pi*i/(k+h^v)) = exp(pi*i*q/p)
    q_val = complex(cos(pi * q / p), sin(pi * q / p))

    # Order of q as root of unity
    # q = exp(2*pi*i * q/(2p)), so order = 2*p / gcd(q, 2*p)
    order = (2 * p) // gcd(q, 2 * p)

    return QuantumGroupParameter(
        lie_type=f"{lie_type}_{rank}",
        rank=rank,
        h_dual=h_dual,
        level_p=p,
        level_q=q,
        k=k,
        k_plus_hv=k_plus_hv,
        q_order=order,
        q_value=q_val,
        is_root_of_unity=True,  # always true at admissible levels
        root_of_unity_order=order,
    )


def sl2_admissible_q_parameter(p: int, q: int) -> QuantumGroupParameter:
    """Specialized: sl_2 at admissible level k = p/q - 2."""
    return quantum_parameter_from_level('sl', 1, p, q)


