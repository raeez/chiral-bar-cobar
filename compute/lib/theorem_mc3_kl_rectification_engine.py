    cp = Fraction(3) * kp / (kp + 2)
    c_sum = c + cp
    return {
        'k': k,
        'k_prime': kp,
        'c': c,
        'c_prime': cp,
        'c_sum': c_sum,
        'expected': 6,
        'match': (c_sum == 6),
    }


