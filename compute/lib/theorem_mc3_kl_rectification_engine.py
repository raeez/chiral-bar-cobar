def verify_q_parameter_sl2_k_minus_half() -> Dict[str, Any]:
    """Multi-path verification of q = exp(2*pi*i/3) at k = -1/2.

    Path 1: Direct computation from k + h^v = 3/2.
        q = exp(pi*i/(3/2)) = exp(2*pi*i/3).

    Path 2: From the admissible parametrization k = p/q - 2 with p=3, q=2.
        q_param = exp(pi*i*q_denom/p) = exp(pi*i*2/3) = exp(2*pi*i/3).

    Path 3: From the root of unity order.
        q = exp(2*pi*i/3) has order 3 (since (exp(2*pi*i/3))^3 = exp(2*pi*i) = 1).
        This corresponds to the truncation level of the quantum group.
    """
    # Path 1
    k = Fraction(-1, 2)
    k_plus_hv = k + 2
    q_path1 = complex(cos(pi / float(k_plus_hv)), sin(pi / float(k_plus_hv)))

    # Path 2
    qp = sl2_admissible_q_parameter(3, 2)
    q_path2 = qp.q_value

    # Path 3: verify order
    q = q_path1
    q3 = q ** 3
    is_order_3 = abs(q3 - 1) < 1e-10

    return {
        'q_path1': q_path1,
        'q_path2': q_path2,
        'paths_agree': abs(q_path1 - q_path2) < 1e-10,
        'order': 3 if is_order_3 else qp.root_of_unity_order,
        'is_primitive_3rd_root': is_order_3,
        'q_exact': 'exp(2*pi*i/3)',
    }


