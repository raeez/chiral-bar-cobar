def kappa_sl2_at_level(k: Fraction) -> Fraction:
    """Kappa for hat{sl}_2 at level k.

    kappa(hat{sl}_2) = dim(sl_2) * (k + h^v) / (2 * h^v) = 3(k+2)/4.
    """
    return Fraction(3, 4) * (k + 2)


def kappa_vir_from_c(c: Fraction) -> Fraction:
    """Kappa for Virasoro at central charge c.

    kappa(Vir_c) = c/2.
    """
    return c / 2


def central_charge_sl2(k: Fraction) -> Fraction:
    """Central charge of hat{sl}_2 at level k: c = 3k/(k+2)."""
    return Fraction(3) * k / (k + 2)


def kl_kappa_analysis_sl2(p: int, q: int) -> Dict[str, Any]:
    """Analyze kappa preservation under KL equivalence for sl_2.

    At admissible level k = p/q - 2 for hat{sl}_2:
        c(V_k(sl_2)) = 3k/(k+2) = 3(p/q - 2)/(p/q) = 3(p - 2q)/p
        kappa(V_k(sl_2)) = 3(k+2)/4 = 3p/(4q)

    The Virasoro subalgebra has central charge c and kappa = c/2.
    But kappa(V_k(sl_2)) = 3(k+2)/4 != c/2 = 3k/(2(k+2)).
    These are DIFFERENT: kappa for the affine is not c/2 (AP39).

    Under DS reduction: V_k(sl_2) -> W^k(sl_2, f_prin) = Vir_c.
        c stays the same (by definition of DS central charge).
        kappa changes: kappa(V_k) = 3(k+2)/4, kappa(Vir_c) = c/2 = 3k/(2(k+2)).
        These are DIFFERENT functions of k.

    So the KL equivalence KL(V_k) ~ KL(Vir_c) does NOT preserve kappa.
    It preserves the MODULAR DATA (S-matrix, fusion rules) but kappa is
    an invariant of the ALGEBRA, not just its module category.
    """
    k = Fraction(p, q) - 2
    c = central_charge_sl2(k)
    kappa_affine = kappa_sl2_at_level(k)
    kappa_vir = kappa_vir_from_c(c)

    return {
        'level': k,
        'central_charge': c,
        'kappa_affine': kappa_affine,
        'kappa_virasoro': kappa_vir,
        'kappa_preserved': (kappa_affine == kappa_vir),
        'kappa_ratio': kappa_affine / kappa_vir if kappa_vir != 0 else None,
        'explanation': (
            "kappa(V_k(sl_2)) = 3(k+2)/4 = 3p/(4q). "
            "kappa(Vir_c) = c/2 = 3k/(2(k+2)) = 3(p-2q)/(2p). "
            "These differ because kappa is an ALGEBRA invariant, not a "
            "MODULE CATEGORY invariant. The KL braided tensor equivalence "
            "preserves fusion and modular data but NOT kappa. "
            "(AP39: kappa != c/2 for non-Virasoro families.)"
        ),
    }


