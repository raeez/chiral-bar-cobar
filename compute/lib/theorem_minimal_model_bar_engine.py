def ising_null_vector_structure() -> Dict[str, object]:
    """Detailed structure of the Ising vacuum null vector.

    For M(4,3), c=1/2:
    - w_0 = (4-1)(3-1) = 6
    - The singular vector at weight 6 in M(1/2, 0) is a specific
      linear combination of L_{-n} descendants of |0> at weight 6.
    - The vacuum module at weight 6 has basis:
      L_{-6}|0>, L_{-4}L_{-2}|0>, L_{-3}^2|0>, L_{-2}^3|0>
      (p_{>=2}(6) = 4 states).
    - The singular vector is one linear combination of these 4 states.
    - L(1/2, 0) at weight 6: dim = 4 - 1 = 3.

    The singular vector chi_6 in M(1/2, 0):
    For h=0, c=1/2, the singular vector at level 6 is:
    chi_6 = (a_1 L_{-6} + a_2 L_{-4}L_{-2} + a_3 L_{-3}^2 + a_4 L_{-2}^3)|0>
    where the coefficients are determined by the condition L_n chi_6 = 0 for n >= 1.

    The explicit form involves c-dependent coefficients. At c=1/2:
    The Kac determinant at level 6 for h=0 is:
    det_6(c=1/2, h=0) = product over (r,s) with rs <= 6 of h_{r,s}(c)^{p(6-rs)}
    which vanishes because h_{2,3}(c=1/2) = 0 (this is the null at weight 2*3=6
    in the Kac table for M(4,3)).
    """
    c = Rational(1, 2)
    w0 = 6

    # Vacuum module dimensions
    dims_universal = {h: partitions_geq2(h) for h in range(8)}
    # p_{>=2}(0)=1, p_{>=2}(1)=0, p_{>=2}(2)=1, p_{>=2}(3)=1,
    # p_{>=2}(4)=2, p_{>=2}(5)=2, p_{>=2}(6)=4, p_{>=2}(7)=4

    # Simple quotient dimensions: remove one null state at weight 6
    dims_simple = dict(dims_universal)
    for h in range(w0, 8):
        excess = h - w0
        null_dim = min(num_partitions(excess), dims_universal[h])
        dims_simple[h] = dims_universal[h] - null_dim

    return {
        'c': c,
        'null_weight': w0,
        'universal_dims': dims_universal,
        'simple_dims': dims_simple,
        'null_dim_at_w0': num_partitions(0),  # = 1
        'basis_at_w6': [
            'L_{-6}|0>',
            'L_{-4}L_{-2}|0>',
            'L_{-3}^2|0>',
            'L_{-2}^3|0>',
        ],
        'n_states_w6_universal': 4,
        'n_states_w6_simple': 3,
        'singular_vector_weight': 6,
    }


