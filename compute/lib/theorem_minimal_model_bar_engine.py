# ============================================================================
# 1. Partition arithmetic
# ============================================================================

@lru_cache(maxsize=1024)
def num_partitions(n: int) -> int:
    """Number of unrestricted partitions of n."""
    if n < 0:
        return 0
    if n == 0:
        return 1
    dp = [0] * (n + 1)
    dp[0] = 1
    for part in range(1, n + 1):
        for j in range(part, n + 1):
            dp[j] += dp[j - part]
    return dp[n]


@lru_cache(maxsize=1024)
def partitions_geq2(n: int) -> int:
    """Number of partitions of n into parts >= 2 (= Virasoro vacuum states)."""
    if n < 0:
        return 0
    if n <= 1:
        return 1 if n == 0 else 0
    dp = [0] * (n + 1)
    dp[0] = 1
    for part in range(2, n + 1):
        for j in range(part, n + 1):
            dp[j] += dp[j - part]
    return dp[n]


def partitions_geq2_list(n: int) -> List[Tuple[int, ...]]:
    """Enumerate all partitions of n into parts >= 2, sorted descending."""
    if n < 0:
        return []
    if n == 0:
        return [()]
    result = []

    def _recurse(remaining, max_part, current):
        if remaining == 0:
            result.append(tuple(current))
            return
        for p in range(min(max_part, remaining), 1, -1):
            _recurse(remaining - p, p, current + [p])

    _recurse(n, n, [])
    return result


# ============================================================================
# 2. Minimal model data
# ============================================================================

def minimal_model_c(p: int, q: int) -> Rational:
    """Central charge c_{p,q} = 1 - 6(p-q)^2/(pq)."""
    return Rational(1) - Rational(6 * (p - q) ** 2, p * q)


def vacuum_singular_weight(p: int, q: int) -> int:
    """First vacuum singular vector weight: w_0 = (p-1)(q-1)."""
    return (p - 1) * (q - 1)


def n_primaries(p: int, q: int) -> int:
    """Number of primary fields: (p-1)(q-1)/2."""
    return (p - 1) * (q - 1) // 2


def conformal_weight_kac(p: int, q: int, r: int, s: int) -> Rational:
    """Kac table conformal weight h_{r,s} for M(p,q)."""
    return Rational((p * r - q * s) ** 2 - (p - q) ** 2, 4 * p * q)


