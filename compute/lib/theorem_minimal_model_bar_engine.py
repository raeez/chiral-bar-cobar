r"""Minimal model bar complex: the first non-Koszul case.

MATHEMATICAL FRAMEWORK
======================

All standard families (Heisenberg, affine KM, beta-gamma, Virasoro, W_N)
are Koszul: H*(B(V)) is concentrated in bar degree 1 (cor:universal-koszul).
Minimal models M(p,q) are SIMPLE QUOTIENTS of the Virasoro algebra:
    L(c_{p,q}, 0) = V_{c_{p,q}} / J(c_{p,q})
where J is generated by the vacuum singular vector.  The null-vector quotient
can create non-trivial bar cohomology in higher degrees.

THIS ENGINE IS THE FIRST EXPLICIT COMPUTATION OF A NON-KOSZUL BAR COMPLEX
IN THE CHIRAL ALGEBRAIC FRAMEWORK.

(1) VACUUM SINGULAR VECTOR

For the minimal model M(p,q) (p > q >= 2, gcd(p,q)=1), the vacuum Verma
module M(c_{p,q}, 0) has its first singular vector at weight

    w_0 = (p-1)(q-1)

This is the (p-1, q-1) entry in the Kac table with h_{p-1,q-1} = 0.
The singular vector generates a sub-Verma module.

    M(4,3) = Ising, c=1/2:   w_0 = 3*2 = 6
    M(5,4) = TCI,   c=7/10:  w_0 = 4*3 = 12
    M(6,5) = Potts,  c=4/5:  w_0 = 5*4 = 20
    M(3,2) = trivial, c=0:   w_0 = 2*1 = 2

(2) EFFECT ON BAR COMPLEX

The bar complex at bar degree n involves tensor products of n copies of V_+
at total weight h >= 2n.  The quotient by J affects weights >= w_0.

Bar degree 1: affected at weight >= w_0 (states directly removed)
Bar degree 2: affected at weight >= max(4, w_0) (tensor factors affected)
Bar degree 3: affected at weight >= max(6, w_0) (triple tensors affected)

For Ising (w_0=6): bar degree 1 affected at weight >= 6, bar degree 2
at weight >= 6, bar degree 3 at weight >= 6.  The first non-trivial effect
is at total weight 6.

(3) MECHANISM OF NON-KOSZULNESS

The UNIVERSAL Virasoro V_c is Koszul: H^1 = C*T (one generator) with the
single quadratic relation (the Virasoro commutation relation makes B^2 -> B^1
surjective onto the relation space, with H^2 = C carrying this single
quadratic relation, and H^n = 0 for n >= 3 at generic c).

For the SIMPLE QUOTIENT L(c,0):
- The null descendant removal at weight >= w_0 CHANGES the differential
  matrices: some targets of the differential no longer exist.
- This can create new cycles in B^2 that are not boundaries from B^3,
  giving non-zero H^2 at weight >= w_0.
- The mechanism: if the null vector is in the IMAGE of the bar differential
  from B^2 -> B^1, removing it from B^1 creates a kernel element in B^2
  that was previously NOT a cycle (its image was the null state).

(4) ZHU ALGEBRA COMPARISON

For V_c (universal): A(V_c) = C[x], polynomial ring.
    Ext_{A(V_c)}(C, C): Ext^0 = C, Ext^1 = C, Ext^n = 0 for n >= 2.
    This matches bar cohomology: Koszul.

For L(c_{p,q}, 0) (simple): A(L(c,0)) is SEMISIMPLE of dimension n_irreps.
    Ext_{A(L)}(C, C): Ext^0 = C, Ext^n = 0 for n >= 1.
    This does NOT directly match bar cohomology because the chiral bar
    complex is NOT the same as the algebraic bar resolution of A(V).
    Zhu's algebra controls weight-0 bar cohomology; higher-weight bar
    cohomology involves the FULL OPE structure, not just the zero-mode.

(5) D-MODULE PURITY CONVERSE

The D-module purity conjecture (forward direction proved, converse open):
    Koszul (bar concentrated) <==> D-module purity on FM_n
For a non-Koszul simple quotient, the prediction is:
    Non-Koszul => non-pure => the bar D-module on FM_n has non-pure MHM
    extensions that obstruct PBW = Saito weight filtration.

The MECHANISM: the null vector quotient creates an EXTENSION in the bar
D-module's nearby cycles at the FM boundary stratum corresponding to the
collision level w_0.  This extension is non-split, violating purity.

ANTI-PATTERN GUARDS:
    AP1:  kappa(Vir_c) = c/2.  Computed from first principles.
    AP3:  Each computation independent.  No pattern-matching.
    AP9:  bar-Ext vs Zhu-Ext vs module-Ext: three distinct invariants.
    AP10: Multi-path verification: chain dims, Euler char, Zhu comparison,
          weight-by-weight analysis all cross-checked.
    AP14: Koszulness (bar concentration) vs Swiss-cheese formality (SC).
          These are different.  Non-Koszulness of L(c,0) says nothing about
          the Swiss-cheese structure of V_c.
    AP19: Bar differential uses d log residues (pole shift by 1).
    AP36: Converse of D-module purity is OPEN.  We provide evidence, not proof.
    AP45: Desuspension s^{-1} LOWERS degree by 1.

References:
    prop:pbw-universality (chiral_koszul_pairs.tex)
    rem:admissible-koszul-status (chiral_koszul_pairs.tex)
    thm:koszul-equivalences-meta item (xii) (chiral_koszul_pairs.tex)
    Frenkel-Zhu (1992): Zhu's algebra and vertex algebra modules
    BPZ84: Belavin-Polyakov-Zamolodchikov
"""

