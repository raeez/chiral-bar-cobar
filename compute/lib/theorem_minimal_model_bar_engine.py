def shadow_data(p: int, q: int) -> Dict[str, object]:
    """Shadow obstruction tower data for M(p,q).

    Minimal models share the Virasoro shadow data at c = c_{p,q}:
        kappa = c/2
        S_3 (alpha) = 2 (universal for Virasoro)
        S_4 = 10 / (c * (5c + 22))
        Delta = 8 * kappa * S_4 = 40 / (5c + 22)
        Shadow class: M (mixed, infinite tower) for c != 0 and 5c+22 != 0

    The shadow data is the SAME for V_c and L(c,0) because the shadow
    obstruction tower depends on the OPE structure (which is the same for the
    universal and simple quotient — the simple quotient just has fewer states,
    but the OPE STRUCTURE CONSTANTS are identical).
    """
    c = minimal_model_c(p, q)
    if c == 0:
        return {'kappa': Rational(0), 'class': 'trivial'}

    kappa = c / 2
    denom = 5 * c + 22
    if denom == 0:
        return {'kappa': kappa, 'class': 'singular', 'S4': None, 'Delta': None}

    S4 = Rational(10) / (c * denom)
    Delta = Rational(40) / denom

    return {
        'c': c,
        'kappa': kappa,
        'S3': 2,
        'S4': S4,
        'Delta': Delta,
        'class': 'M' if Delta != 0 else 'L',
        'shadow_depth': 'infinite' if Delta != 0 else 'finite',
    }


