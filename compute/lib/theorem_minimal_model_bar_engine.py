# ============================================================================
# 3. Vacuum module dimensions
# ============================================================================

def universal_vac_dim(h: int) -> int:
    """Dimension of V_c (universal Virasoro) augmentation ideal at weight h.

    V_+(h) has basis {L_{-lambda}|0>} for partitions lambda of h
    into parts >= 2.  dim = p_{>=2}(h).
    """
    if h < 2:
        return 0
    return partitions_geq2(h)


def null_submodule_dim(p: int, q: int, h: int) -> int:
    """Dimension of the null submodule N at weight h in M(c_{p,q}, 0).

    The singular vector at weight w_0 = (p-1)(q-1) generates a sub-Verma
    module.  At weight h >= w_0, the null subspace has dimension p(h - w_0)
    (unrestricted partitions of the excess), capped by the total dimension.

    For h < w_0: null_dim = 0.
    For h >= w_0: null_dim = min(p(h - w_0), p_{>=2}(h)).
    """
    w0 = vacuum_singular_weight(p, q)
    if h < w0:
        return 0
    return min(num_partitions(h - w0), partitions_geq2(h))


def simple_quotient_dim(p: int, q: int, h: int) -> int:
    """Dimension of L(c_{p,q}, 0) at weight h.

    dim L_h = dim V_h - dim N_h = p_{>=2}(h) - null_dim(h).
    """
    return max(0, universal_vac_dim(h) - null_submodule_dim(p, q, h))


def simple_quotient_dims_table(p: int, q: int, max_h: int) -> Dict[int, int]:
    """Table of L(c_{p,q}, 0) dimensions by weight."""
    return {h: simple_quotient_dim(p, q, h) for h in range(0, max_h + 1)}


# ============================================================================
# 4. Bar chain dimensions
# ============================================================================

def _tensor_dim_universal(n: int, h: int) -> int:
    """Dimension of (V_+)^{otimes n} at total weight h (universal)."""
    if n <= 0 or h < 2 * n:
        return 0
    if n == 1:
        return universal_vac_dim(h)
    total = 0
    for a in range(2, h - 2 * (n - 1) + 1):
        d = universal_vac_dim(a)
        if d == 0:
            continue
        total += d * _tensor_dim_universal(n - 1, h - a)
    return total


def _tensor_dim_simple(p: int, q: int, n: int, h: int) -> int:
    """Dimension of (L_+)^{otimes n} at total weight h (simple quotient)."""
    if n <= 0 or h < 2 * n:
        return 0
    if n == 1:
        return simple_quotient_dim(p, q, h)
    total = 0
    for a in range(2, h - 2 * (n - 1) + 1):
        d = simple_quotient_dim(p, q, a)
        if d == 0:
            continue
        total += d * _tensor_dim_simple(p, q, n - 1, h - a)
    return total


def bar_chain_dim_universal(n: int, h: int) -> int:
    """Dimension of B^n_h for the UNIVERSAL Virasoro.

    B^n_h = (V_+^{otimes n})_h  with Orlik-Solomon forms absorbed:
    the bar complex at arity n has form-degree (n-1), contributing
    no additional dimension factor in the ordered bar complex
    (each ordering gives one basis element).

    In the ORDERED bar complex: dim = number of n-tuples of states
    with weights summing to h.  The UNORDERED bar complex (relevant for
    bar cohomology) uses desuspended tensor products s^{-1}a_1 ... s^{-1}a_n
    without the (n-1)! factor.

    Here we use the ORDERED convention matching the existing codebase.
    """
    if n <= 0 or h < 2 * n:
        return 0
    return _tensor_dim_universal(n, h)


def bar_chain_dim_simple(p: int, q: int, n: int, h: int) -> int:
    """Dimension of B^n_h for the SIMPLE QUOTIENT L(c_{p,q}, 0)."""
    if n <= 0 or h < 2 * n:
        return 0
    return _tensor_dim_simple(p, q, n, h)


def bar_chain_table(p: int, q: int, max_n: int, max_h: int,
                    simple: bool = True) -> Dict[Tuple[int, int], int]:
    """Table of bar chain dimensions (n, h) -> dim B^n_h."""
    result = {}
    for n in range(1, max_n + 1):
        for h in range(2 * n, max_h + 1):
            if simple:
                d = bar_chain_dim_simple(p, q, n, h)
            else:
                d = bar_chain_dim_universal(n, h)
            result[(n, h)] = d
    return result


# ============================================================================
# 5. Euler characteristic
# ============================================================================

def euler_char_universal(h: int, max_n: int = None) -> int:
    """Euler characteristic sum_{n>=1} (-1)^n dim B^n_h (universal)."""
    if max_n is None:
        max_n = h // 2
    return sum((-1) ** n * bar_chain_dim_universal(n, h)
               for n in range(1, max_n + 1))


def euler_char_simple(p: int, q: int, h: int, max_n: int = None) -> int:
    """Euler characteristic sum_{n>=1} (-1)^n dim B^n_h (simple quotient)."""
    if max_n is None:
        max_n = h // 2
    return sum((-1) ** n * bar_chain_dim_simple(p, q, n, h)
               for n in range(1, max_n + 1))


# ============================================================================
# 6. Bar cohomology dimension estimates
# ============================================================================

def bar_cohomology_universal(max_h: int = 10) -> Dict[Tuple[int, int], int]:
    """Bar cohomology H^n_h for the UNIVERSAL Virasoro.

    The universal Virasoro V_c is Koszul (prop:pbw-universality):
        H^1_2 = 1 (the generator T at weight 2)
        H^n_h = 0 for (n,h) != (1,2) [and h > 0]

    The Euler characteristic at each weight must match:
        chi(h) = sum (-1)^n dim B^n_h = sum (-1)^n dim H^n_h

    For h=2: B^1_2 = 1 (just L_{-2}|0>), B^n_2 = 0 for n >= 2.
        chi = -1.  H^1_2 = 1, consistent.

    For h=4: B^1_4 = 1 (L_{-4}|0>, L_{-2}^2|0>: wait, p_{>=2}(4) = 2).
        Actually p_{>=2}(4) = 2: (4) and (2,2).
        B^2_4 = tensor dim of (V_+)^2 at weight 4: just 2*2 = 1^2 = 1
        (weight (2,2) only possible split).
        chi = -2 + 1 = -1.
        Koszul: H^1_4 = 1, H^2_4 = 0.  Check: chi = -1.  Consistent.

    We return the Koszul answer: H^1_h = p_{>=2}(h) - (h-1 corrections).
    Actually for Koszul V_c, H^1 = C*T (one-dimensional, generated by T
    at weight 2).  But this is the bar COHOMOLOGY of the full chiral algebra.
    The weight-h part of H^1 is:
        H^1_h = dim(relns at weight h coming from B^2) = ...

    For Koszul: H^*(B(V_c)) is generated in degree 1 by the single
    generator T, with the single quadratic relation [T, T] ~ c/2 * 1 + 2T.
    So as a graded vector space:
        H^1 = span{T} (dimension 1 at weight 2)
        H^2 = span{relation} (dimension 1 at weight 4: the Virasoro relation)
        H^n = 0 for n >= 3

    Wait, this is the bar cohomology of the OPERAD, not the bar complex.
    Let me reconsider.

    The chiral bar complex B(V) of a vertex algebra V is:
        B^n = (s^{-1} V_+)^{otimes n}
    with differential from the OPE.  H^*(B(V)) is the bar cohomology.

    For a QUADRATIC algebra (like V_c): H^1 = generators, H^2 = relations.
    Virasoro has one generator (T, weight 2) and one relation (the commutation
    relation, bilinear in T, weight 4).  For a Koszul algebra:
        H^n = 0 for n >= 3.

    So H^1_2 = 1, H^2_4 = 1, all others = 0.
    """
    result = {}
    # Koszul: H^1_2 = 1 (generator T), H^2_4 = 1 (Virasoro relation)
    for n in range(1, max_h // 2 + 1):
        for h in range(2 * n, max_h + 1):
            result[(n, h)] = 0
    result[(1, 2)] = 1
    result[(2, 4)] = 1
    return result


class MinimalModelBarAnalysis:
    """Complete bar complex analysis for a minimal model M(p,q).

    Compares the simple quotient L(c_{p,q}, 0) with the universal V_c.

    The key question: does H^n(B(L)) != 0 for n >= 3?
    If yes: L is NOT Koszul (bar cohomology not concentrated in degrees 1-2).
    """

    def __init__(self, p: int, q: int, max_weight: int = 12):
        from math import gcd
        if gcd(p, q) != 1 or q < 2 or p <= q:
            raise ValueError(f"Invalid minimal model M({p},{q})")
        self.p = p
        self.q = q
        self.c = minimal_model_c(p, q)
        self.w0 = vacuum_singular_weight(p, q)
        self.max_weight = max_weight
        self.n_irreps = n_primaries(p, q)

    def kappa(self) -> Rational:
        """Modular characteristic kappa = c/2 (Virasoro family: AP1)."""
        return self.c / 2

    def null_level(self) -> int:
        """Weight of the first vacuum singular vector."""
        return self.w0

    # ---- Dimension tables ----

    def universal_dims(self) -> Dict[int, int]:
        """Dimensions of V_{c}(h) = p_{>=2}(h)."""
        return {h: universal_vac_dim(h) for h in range(0, self.max_weight + 1)}

    def simple_dims(self) -> Dict[int, int]:
        """Dimensions of L(c,0)(h)."""
        return simple_quotient_dims_table(self.p, self.q, self.max_weight)

    def null_dims(self) -> Dict[int, int]:
        """Dimensions of the null subspace at each weight."""
        return {h: null_submodule_dim(self.p, self.q, h)
                for h in range(0, self.max_weight + 1)}

    def dimension_deficit(self) -> Dict[int, int]:
        """How many states the null quotient removes at each weight."""
        nd = self.null_dims()
        return {h: d for h, d in nd.items() if d > 0}

    # ---- Bar chain comparison ----

    def bar_chains_universal(self, max_n: int = 4) -> Dict[Tuple[int, int], int]:
        """Bar chain dimensions for V_c."""
        return bar_chain_table(self.p, self.q, max_n, self.max_weight,
                               simple=False)

    def bar_chains_simple(self, max_n: int = 4) -> Dict[Tuple[int, int], int]:
        """Bar chain dimensions for L(c,0)."""
        return bar_chain_table(self.p, self.q, max_n, self.max_weight,
                               simple=True)

    def chain_dimension_change(self, max_n: int = 4) -> Dict[Tuple[int, int], int]:
        """Change in bar chain dimensions: dim B^n_h(L) - dim B^n_h(V).

        Negative values = the simple quotient has fewer chains (expected).
        This is the raw data for detecting non-Koszulness.
        """
        univ = self.bar_chains_universal(max_n)
        simp = self.bar_chains_simple(max_n)
        result = {}
        for key in univ:
            delta = simp.get(key, 0) - univ.get(key, 0)
            if delta != 0:
                result[key] = delta
        return result

    # ---- Euler characteristics ----

    def euler_chars_comparison(self, max_h: int = None) -> Dict[int, Tuple[int, int]]:
        """Compare Euler characteristics: (chi_universal, chi_simple) by weight.

        For the universal Koszul algebra: chi_h is determined by H^1 and H^2.
        For the simple quotient: chi_h may differ, indicating changed cohomology.
        """
        if max_h is None:
            max_h = self.max_weight
        result = {}
        for h in range(2, max_h + 1):
            chi_u = euler_char_universal(h)
            chi_s = euler_char_simple(self.p, self.q, h)
            result[h] = (chi_u, chi_s)
        return result

    def euler_char_deficit(self, max_h: int = None) -> Dict[int, int]:
        """Weights where the Euler characteristic changes.

        chi_simple - chi_universal != 0 indicates that the bar cohomology
        has changed at that weight.  This is necessary (not sufficient)
        for non-Koszulness.
        """
        ec = self.euler_chars_comparison(max_h)
        return {h: (cs - cu) for h, (cu, cs) in ec.items() if cu != cs}

    # ---- Non-Koszulness detection ----

    def first_affected_weight(self) -> int:
        """First weight where the simple quotient differs from universal.

        This is w_0 = (p-1)(q-1), the null vector weight.
        Bar chains at (n, h) with h >= w_0 can be affected.
        """
        return self.w0

    def first_bar_degree_affected(self) -> int:
        """First bar degree where bar CHAINS differ.

        Bar degree n can be affected at weight h >= max(2n, w_0).
        So the minimum n is ceil(w_0 / 2) if w_0 > 2.
        But actually bar degree 1 is affected whenever h >= w_0,
        so bar degree 1 is affected at weight w_0.
        """
        return 1  # bar degree 1 is always first affected

    def affected_bar_degrees_at_null_weight(self) -> List[int]:
        """Which bar degrees are affected at exactly the null weight w_0.

        At weight w_0, bar degree n exists for n <= w_0 // 2.
        Bar degree 1 is affected (one state removed from B^1_{w_0}).
        Bar degree 2 is affected if w_0 >= 4 and the tensor product is
        affected (requires at least one factor at weight >= w_0 or the
        other factor to create the deficiency indirectly... actually
        bar degree 2 at weight w_0 uses pairs (a, w_0-a) with a >= 2.
        If a < w_0 and w_0-a < w_0, neither factor is affected.
        So bar degree 2 is NOT affected at weight w_0 when w_0 > 4.
        """
        result = [1]  # bar degree 1 always affected at w_0
        # Bar degree 2 at weight w_0: need one factor at weight >= w_0.
        # This means one factor at weight w_0 (with the other at weight 0,
        # but weight 0 has dim 0 in V_+).  So bar degree 2 at weight w_0
        # is NOT affected for w_0 > 2.
        # (For w_0 = 2 [trivial model], bar degree 1 at weight 2 is it.)
        return result

    def koszulness_obstruction_analysis(self, max_n: int = 4
                                         ) -> Dict[str, object]:
        """Full analysis of the Koszulness obstruction from the null quotient.

        Returns a dict with:
        - 'null_weight': the weight w_0 where the first difference occurs
        - 'euler_deficits': weights where chi changes
        - 'chain_changes': (n, h) -> change in chain dim
        - 'likely_non_koszul': whether the evidence suggests non-Koszulness
        - 'mechanism': description of the obstruction mechanism
        - 'new_cohomology_degree': predicted bar degree where new H^n != 0 appears
        """
        chain_changes = self.chain_dimension_change(max_n)
        euler_deficits = self.euler_char_deficit()

        # The key: at weight w_0, only B^1 is directly affected (loses states).
        # If the Euler char changes, that means the cohomology distribution changes.
        # Since B^n for n >= 2 is unchanged at weight w_0, any chi change at w_0
        # means H^1 loses dimension at weight w_0.  But H^1 for universal V_c
        # is zero at weight w_0 (Koszul: H^1 = {T at weight 2}, so H^1_{w_0} = 0
        # for w_0 > 2).  So removing states from B^1_{w_0} cannot change H^1_{w_0}
        # (it was already 0 in the quotient of B^1 by im(B^2 -> B^1)).
        #
        # The REAL effect happens at weights > w_0, where:
        # (a) B^1 has fewer states (null quotient)
        # (b) B^2 at weight > w_0 also has fewer chains (tensor products affected)
        # (c) The differential B^2 -> B^1 changes (fewer domain AND codomain elements)
        # (d) The change in ker/im can create non-zero H^2 at higher weights.

        # For the Ising model (w_0=6): at weight w_0=6, B^1 changes but B^2
        # at weight 6 uses pairs (2,4) and (3,3) and (4,2) -- all below w_0.
        # So B^2_6 is unchanged.  The differential B^2_6 -> B^1_6 changes
        # (codomain shrinks).  This could create new ker elements in B^1_6
        # (states that were in im(d) but whose target got removed), but these
        # would NOT create H^2 cycles.  Instead, B^1_6 shrinks -> cycles in B^1
        # that were zero in H^1 might now contribute to H^1.
        #
        # Wait: the differential goes B^{n+1} -> B^n (decreases bar degree).
        # So d: B^2 -> B^1.  If a state a in B^1 was d(b) for some b in B^2
        # (i.e., a is in the image, hence zero in H^1), and we REMOVE a from
        # B^1, then b is now in the KERNEL of d (since d(b) = a is gone from
        # the quotient), creating a non-trivial cycle in B^2.  If this cycle
        # is not in the image of d: B^3 -> B^2, then H^2 != 0.
        #
        # This is the mechanism! Removing the image of d from the codomain
        # creates new kernel elements in the domain.

        # At weight w_0: B^2 unchanged, B^1 shrinks.
        # New ker(d: B^2 -> B^1) elements = those b in B^2 with d(b) in the
        # null subspace of B^1.  These b are new H^2 candidates.
        # They are non-trivial in H^2 iff not in im(d: B^3 -> B^2).
        # Since B^3 is unchanged at weight w_0 (needs factors summing to w_0,
        # each >= 2, none >= w_0), the image of d: B^3 -> B^2 is also unchanged.

        # So the new H^2 elements at weight w_0 are:
        # dim(new ker) = dim(B^2 elements mapping to null subspace of B^1)
        # This is exactly the rank of the restriction of d to the null-subspace
        # target: d_null: B^2 -> N_{w_0} where N is the null subspace of B^1.

        # For Ising (w_0=6): N_6 has dimension 1 (one null state removed).
        # B^2_6 is the tensor product space at weight 6.  The rank of d
        # restricted to the null target gives dim(new H^2_6).

        # The UPPER BOUND on new H^2 at weight w_0 is dim(N_{w_0}) = 1 for Ising.

        null_dim_at_w0 = null_submodule_dim(self.p, self.q, self.w0)

        # Check: is B^2 unchanged at weight w_0?
        b2_univ = bar_chain_dim_universal(2, self.w0)
        b2_simp = bar_chain_dim_simple(self.p, self.q, 2, self.w0)
        b2_unchanged = (b2_univ == b2_simp)

        # Chain dims at the null weight for comparison
        b1_univ = bar_chain_dim_universal(1, self.w0)
        b1_simp = bar_chain_dim_simple(self.p, self.q, 1, self.w0)

