# Section 10: Shadow zeta / BTZ Selberg ratio
# =========================================================================

def shadow_zeta(s: complex, c: float, n_max: int = 5000) -> complex:
    r"""Shadow spectral zeta function for Virasoro at central charge c.

    zeta_A(s) = sum_{n >= 1} d(n) * n^{-s}

    where d(n) is the number of Virasoro descendants at level n.
    For large c, d(n) ~ p(n) (partition function).

    The generating function is:
        sum_{n >= 0} d(n) q^n = prod_{k >= 1} (1 - q^k)^{-1} = 1 / eta_prod(q)

    where eta_prod = prod_{k >= 1} (1 - q^k) (WITHOUT the q^{1/24} prefactor;
    see AP46).

    The shadow zeta is the Dirichlet series associated to this spectrum.
    """
    s = complex(s)
    c = float(c)

    # Compute partition numbers (descendants at each level)
    partitions = _partition_numbers(n_max)

    total = complex(0, 0)
    for n in range(1, min(n_max, len(partitions))):
        total += partitions[n] * (n ** (-s))
    return total


@lru_cache(maxsize=8)
def _partition_numbers(n_max: int) -> List[int]:
    """Integer partition numbers p(0), p(1), ..., p(n_max-1).

    p(n) = number of partitions of n.
    p(0) = 1, p(1) = 1, p(2) = 2, p(3) = 3, p(4) = 5, ...
    """
    p = [0] * n_max
    p[0] = 1
    for k in range(1, n_max):
        for n in range(k, n_max):
            p[n] += p[n - k]
    return p


def shadow_selberg_ratio(s: complex, c: float, q: float,
                         n_max_shadow: int = 2000,
                         n_max_selberg: int = 500) -> Dict[str, complex]:
    r"""Ratio zeta_A(s) / Z_BTZ(s).

    This ratio compares the shadow spectral zeta (from the Virasoro
    algebra) with the Selberg zeta (from the BTZ geometry).

    If the ratio simplifies to a known function, it would indicate
    a precise dictionary between algebraic (shadow) and geometric
    (Selberg) spectral data.
    """
    z_shadow = shadow_zeta(s, c, n_max_shadow)
    z_selberg = selberg_zeta_btz(s, q, n_max_selberg)

    ratio = complex(float('nan'))
    if abs(z_selberg) > 1e-30:
        ratio = z_shadow / z_selberg

    return {
        's': s,
        'shadow_zeta': z_shadow,
        'selberg_zeta': z_selberg,
        'ratio': ratio,
        'abs_ratio': abs(ratio) if not cmath.isnan(ratio) else float('nan'),
    }


