# Section 8: Selberg zeta evaluated at Riemann zero positions
# =========================================================================

def selberg_at_riemann_zeros(q: float, n_zeros: int = 20,
                             n_max: int = 500) -> List[Dict[str, Any]]:
    r"""Evaluate Z_BTZ(rho_n / 2) where rho_n are Riemann zeta zeros.

    If Z_BTZ vanishes at these points, it would imply a spectral-arithmetic
    connection between BTZ quasinormal modes and Riemann zeros.

    MATHEMATICAL EXPECTATION: Z_BTZ will NOT generically vanish at
    rho_n / 2.  The BTZ Selberg zeta zeros are at s = -n + 2 pi i k / log(1/q),
    which are spaced by the GEOMETRIC quantity log(1/q) = 2 pi r_+.
    The Riemann zeros are spaced by the ARITHMETIC quantity ~ 2 pi / log(gamma_n).
    These are incommensurate for generic r_+.

    However: the RATIO |Z_BTZ(rho_n/2)| may exhibit interesting structure
    as a function of n, reflecting partial resonances between the two
    spectra.
    """
    try:
        import mpmath
    except ImportError:
        return [{'error': 'mpmath required for Riemann zeros'}]

    results = []
    for k in range(1, n_zeros + 1):
        rho = complex(mpmath.zetazero(k))
        s_eval = rho / 2.0

        Z_val = selberg_zeta_btz(s_eval, q, n_max)
        log_Z = log_selberg_zeta(s_eval, q, n_max)

        results.append({
            'k': k,
            'rho': rho,
            'gamma': rho.imag,
            's_eval': s_eval,
            'Z_BTZ': Z_val,
            'abs_Z_BTZ': abs(Z_val),
            'log_Z_BTZ': log_Z,
            'is_zero': abs(Z_val) < 1e-8,
        })

    return results


def selberg_at_shifted_riemann_zeros(q: float, shift: float = 0.0,
                                     n_zeros: int = 20,
                                     n_max: int = 500) -> List[Dict[str, Any]]:
    r"""Evaluate Z_BTZ(shift + i gamma_n / 2) for Riemann zero heights gamma_n.

    We parametrize the evaluation point as s = shift + i gamma_n / 2.
    For shift = 1/4: the Riemann zero positions rho_n/2.
    For shift = 1/2: the critical line of the Riemann zeta.
    For shift = 0: the imaginary axis.
    """
    try:
        import mpmath
    except ImportError:
        return [{'error': 'mpmath required'}]

    results = []
    for k in range(1, n_zeros + 1):
        rho = complex(mpmath.zetazero(k))
        gamma_k = rho.imag
        s_eval = complex(shift, gamma_k / 2.0)

        Z_val = selberg_zeta_btz(s_eval, q, n_max)

        results.append({
            'k': k,
            'gamma': gamma_k,
            's_eval': s_eval,
            'abs_Z_BTZ': abs(Z_val),
        })

    return results


