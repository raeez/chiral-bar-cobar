def selberg_zeta_btz(s: complex, q: float, n_max: int = 500) -> complex:
    r"""Selberg zeta for BTZ: Z_BTZ(s) = prod_{n >= 0} (1 - q^{n+s}).

    This is the q-Pochhammer symbol (q^s; q)_infty.

    Parameters
    ----------
    s : complex
        The spectral parameter.
    q : float
        Thermal parameter q = e^{-2 pi r_+}, must satisfy 0 < q < 1.
    n_max : int
        Number of terms in the product.
    """
    s = complex(s)
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    product = complex(1, 0)
    for n in range(n_max):
        factor = 1.0 - q ** (n + s)
        product *= factor
        # Early termination: q^{n+Re(s)} is extremely small
        if abs(q ** (n + s.real)) < 1e-30:
            break
    return product


def selberg_zeta_from_mass(s: complex, M: float, J: float = 0.0,
                           n_max: int = 500) -> complex:
    r"""Selberg zeta Z_BTZ(s) for BTZ at mass M, angular momentum J."""
    q = thermal_parameter(M, J)
    if q <= 0 or q >= 1:
        return complex(float('nan'))
    return selberg_zeta_btz(s, q, n_max)


def log_selberg_zeta(s: complex, q: float, n_max: int = 500) -> complex:
    r"""log Z_BTZ(s) = sum_{n >= 0} log(1 - q^{n+s}).

    This avoids numerical underflow from the product representation
    when |Z_BTZ| is very small.

    For Re(s) > 0 and 0 < q < 1, all terms have |q^{n+s}| < 1,
    so log(1 - q^{n+s}) is well-defined on the principal branch.
    """
    s = complex(s)
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    total = complex(0, 0)
    for n in range(n_max):
        term = q ** (n + s)
        if abs(term) < 1e-30:
            break
        total += cmath.log(1.0 - term)
    return total


def selberg_zeta_derivative(s: complex, q: float, n_max: int = 500) -> complex:
    r"""Z'_BTZ(s) / Z_BTZ(s) = d/ds log Z_BTZ(s).

    d/ds log Z_BTZ(s) = sum_{n >= 0} -q^{n+s} log(q) / (1 - q^{n+s})

    The minus sign comes from d/ds log(1 - q^{n+s}) = -q^{n+s} log(q) / (1 - q^{n+s}).
    """
    s = complex(s)
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    log_q = math.log(q)
    total = complex(0, 0)
    for n in range(n_max):
        qns = q ** (n + s)
        if abs(qns) < 1e-30:
            break
        total += -qns * log_q / (1.0 - qns)
    return total


