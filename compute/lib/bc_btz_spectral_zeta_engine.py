# Section 6: Hawking temperature from spectral zeta
# =========================================================================

def hawking_temperature_from_spectral_zeta(h: float, r_plus: float,
                                           r_minus: float = 0.0) -> Dict[str, float]:
    r"""Extract Hawking temperature from the spectral zeta function.

    The zeta-function regularized free energy is

        F = -(1/2) zeta'_BTZ(0)

    which gives an effective temperature via the thermodynamic relation

        beta_H = d F / d E |_{E = E_0}

    For a direct extraction, consider the thermal partition function:

        Z(beta) = sum_n e^{-beta E_n}

    The spectral zeta encodes all moments of the spectrum.  The Hawking
    temperature can be identified from:

        T_H = r_+ / (2 pi)     [non-rotating]

    which is the horizon temperature.  We verify this against the spectral
    data.

    VERIFICATION: the Hawking temperature is a GEOMETRIC quantity
    (surface gravity / 2 pi).  The spectral zeta provides a consistency
    check, not an independent derivation.
    """
    # Geometric Hawking temperature
    T_H_geometric = (r_plus ** 2 - r_minus ** 2) / (TWO_PI * r_plus) if r_plus > 0 else 0.0

    # Spectral data
    zeta_0 = spectral_zeta_at_zero(h, r_plus, r_minus)
    zeta_prime_0 = spectral_zeta_derivative_at_zero(h, r_plus, r_minus)

    # The ratio -zeta'(0)/zeta(0) relates to the mean log-energy
    # <log E> = -zeta'(0)/zeta(0) when zeta(0) != 0
    mean_log_energy = float('nan')
    if abs(zeta_0) > 1e-15:
        mean_log_energy = -float(zeta_prime_0.real) / float(zeta_0)

    # The log of the functional determinant
    log_det = -zeta_prime_0

    return {
        'T_H_geometric': T_H_geometric,
        'beta_geometric': 1.0 / T_H_geometric if T_H_geometric > 0 else float('inf'),
        'r_plus': r_plus,
        'r_minus': r_minus,
        'zeta_BTZ_at_0': zeta_0,
        'zeta_prime_BTZ_at_0': complex(zeta_prime_0),
        'mean_log_energy': mean_log_energy,
        'log_det': complex(log_det),
    }


