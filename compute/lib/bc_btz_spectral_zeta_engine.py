# Section 15: Summary and cross-verification
# =========================================================================

def full_spectral_analysis(M: float, J: float = 0.0, h: float = 2.0,
                           c: float = 12.0,
                           n_zeros: int = 10) -> Dict[str, Any]:
    r"""Comprehensive spectral analysis of BTZ at given parameters.

    Computes:
    1. BTZ geometry (horizons, temperature)
    2. Quasinormal spectrum
    3. Spectral zeta at s = 2, 3, 4 (convergent values)
    4. Selberg zeta at s = 1/2, 1, 3/2
    5. Selberg zeta at Riemann zero positions
    6. Shadow zeta / Selberg ratio
    7. Modular crossing check
    8. Cardy consistency
    """
    horizons = btz_horizons(M, J)
    if not horizons['valid']:
        return {'error': 'invalid BTZ parameters'}

    r_plus = horizons['r_plus']
    r_minus = horizons['r_minus']
    q = thermal_parameter(M, J)
    T_H = hawking_temperature_btz(M, J)

    result = {
        'parameters': {'M': M, 'J': J, 'h': h, 'c': c},
        'geometry': horizons,
        'T_H': T_H,
        'beta': 1.0 / T_H if T_H > 0 else float('inf'),
        'q': q,
    }

    # Spectral zeta at integer points
    for s_val in [2, 3, 4]:
        z = spectral_zeta_btz(complex(s_val), h, r_plus, r_minus)
        result[f'zeta_BTZ({s_val})'] = z

    # Selberg zeta
    if 0 < q < 1:
        for s_val in [0.5, 1.0, 1.5]:
            Z = selberg_zeta_btz(complex(s_val), q)
            result[f'Z_BTZ({s_val})'] = Z

        # At Riemann zeros
        riemann_data = selberg_at_riemann_zeros(q, min(n_zeros, 5))
        result['riemann_zero_data'] = riemann_data

        # Modular crossing
        crossing = modular_crossing_selberg(q, complex(0.5))
        result['modular_crossing'] = crossing

    # Cardy consistency
    L0 = M / 2.0 + c / 24.0  # rough: non-rotating, single chirality
    cardy = bekenstein_hawking_from_shadow(c, L0)
    result['cardy'] = cardy

    return result
