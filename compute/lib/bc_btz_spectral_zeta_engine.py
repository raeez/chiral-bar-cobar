r"""BTZ spectral zeta and Selberg zeta from quasinormal modes.

MATHEMATICAL FRAMEWORK
======================

The BTZ black hole (Banados-Teitelboim-Zanelli 1992, hep-th/9204099) at
mass M and angular momentum J in 2+1 gravity with negative cosmological
constant has two horizons at

    r_+^2 = (Ml/2)(1 + sqrt(1 - (J/(Ml))^2))
    r_-^2 = (Ml/2)(1 - sqrt(1 - (J/(Ml))^2))

where l is the AdS radius.  The Hawking temperature is

    T_H = (r_+^2 - r_-^2) / (2 pi l^2 r_+)

In the AdS_3/CFT_2 dictionary with c = 3l/(2G_N):

    L_0 = (r_+ + r_-)^2 / (16 G_N l) = (c/24)(r_+/l + r_-/l)^2
    L_0bar = (r_+ - r_-)^2 / (16 G_N l) = (c/24)(r_+/l - r_-/l)^2

Throughout we set l = 1 (AdS units).

QUASINORMAL MODE SPECTRUM
=========================

For a scalar field of conformal weight h on the BTZ background, the
quasinormal mode frequencies are (Birmingham-Sachs-Solodukhin 2002,
hep-th/0112055):

    omega_{n,L} = -i(r_+ - r_-)(2n + h) / l^2      [left-movers]
    omega_{n,R} = -i(r_+ + r_-)(2n + h) / l^2      [right-movers]

for n = 0, 1, 2, ...  These are purely imaginary (overdamped in
Lorentzian signature).  The physical energies are E_n = |omega_n|.

BTZ SPECTRAL ZETA
==================

The spectral zeta function is

    zeta_BTZ(s) = sum_{n >= 0} |omega_{n,R}|^{-s} + |omega_{n,L}|^{-s}

For non-rotating BTZ (r_- = 0), this simplifies to

    zeta_BTZ(s) = 2 * (r_+)^{-s} * sum_{n >= 0} (2n + h)^{-s}
                = 2 * (r_+)^{-s} * 2^{-s} * sum_{n >= 0} (n + h/2)^{-s}
                = 2^{1-s} * (r_+)^{-s} * zeta_H(s, h/2)

where zeta_H(s, a) = sum_{n >= 0} (n + a)^{-s} is the Hurwitz zeta.

BTZ SELBERG ZETA
================

The Selberg zeta function for the BTZ quotient Gamma\H^3 (where Gamma is
the discrete group generated by the loxodromic element with translation
length 2 pi r_+) is (Perry-Williams 2003, Patterson-Perry 2001):

    Z_BTZ(s) = prod_{n >= 0} (1 - q^{n+s})

where q = e^{-2 pi r_+} is the thermal parameter.  For non-rotating BTZ:

    Z_BTZ(s) = prod_{k >= 0} (1 - q^{k+s})

This is a q-Pochhammer symbol: Z_BTZ(s) = (q^s; q)_infty.

The zeros of Z_BTZ lie at s_k = -k + 2 pi i n / log(1/q) for k = 0, 1, ...
and n in Z.  On the physical strip 0 <= Re(s) <= 1, the zeros are at the
points where q^s is a root of unity, i.e., s = (2 pi i n) / (2 pi r_+).

SELBERG ZETA AND RIEMANN ZETA
==============================

The connection to Riemann zeta zeros is INDIRECT, through the Selberg trace
formula.  For the BTZ geometry, the trace formula reads:

    sum_j h(r_j) = (Area/4pi) integral h(r) r tanh(pi r) dr
                  + sum_{gamma} sum_{k=1}^infty l_gamma / (2 sinh(k l_gamma/2)) g(k l_gamma)

where the sum is over primitive closed geodesics of length l_gamma.  For BTZ
there is a SINGLE primitive geodesic of length l_0 = 2 pi r_+ (the horizon).

The Selberg zeta for a QUOTIENT of H^3 is distinct from the Riemann zeta.
The zeros of Z_BTZ are determined by the spectrum of the Laplacian on
Gamma\H^3, NOT by the Riemann zeros.  Any coincidence would be accidental
or reflect a deeper structure we investigate here.

THETA FUNCTION REPRESENTATION
==============================

Z_BTZ(s) = (q^s; q)_infty can be related to theta functions via the
Jacobi triple product:

    sum_{n=-infty}^{infty} z^n q^{n^2/2}
        = prod_{n >= 1} (1 - q^n)(1 + z q^{n-1/2})(1 + z^{-1} q^{n-1/2})

Setting z = -q^{s-1/2} relates Z_BTZ to the Jacobi theta function theta_1.

SELF-DUAL TEMPERATURE
=====================

The high-T/low-T modular crossing sends beta -> 4 pi^2 / beta.
The self-dual temperature is beta_* = 2 pi, the Hawking-Page point.
At this point, the BTZ and thermal AdS saddles exchange dominance.

References:
    BTZ 1992: hep-th/9204099
    Birmingham-Sachs-Solodukhin 2002: hep-th/0112055
    Perry-Williams 2003: math-ph/0309034
    Patterson-Perry 2001: math-ph/0104015
    Bytsenko-Vanzo-Zerbini 1998: hep-th/9803158
"""

