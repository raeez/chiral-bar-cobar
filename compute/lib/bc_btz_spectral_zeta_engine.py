def quasinormal_frequency(n: int, h: float, r_plus: float,
                          r_minus: float = 0.0, chirality: str = 'R') -> complex:
    r"""Quasinormal frequency for a scalar of weight h on BTZ.

    omega_{n,R} = -i (r_+ + r_-)(2n + h)   [right-movers]
    omega_{n,L} = -i (r_+ - r_-)(2n + h)   [left-movers]

    These are purely imaginary (overdamped modes).
    """
    if chirality == 'R':
        return -1j * (r_plus + r_minus) * (2 * n + h)
    else:
        return -1j * (r_plus - r_minus) * (2 * n + h)


def quasinormal_energy(n: int, h: float, r_plus: float,
                       r_minus: float = 0.0, chirality: str = 'R') -> float:
    r"""Energy |omega_n| for the n-th quasinormal mode."""
    omega = quasinormal_frequency(n, h, r_plus, r_minus, chirality)
    return abs(omega)


def quasinormal_spectrum(h: float, r_plus: float, r_minus: float = 0.0,
                         n_max: int = 50) -> Dict[str, List[float]]:
    r"""Full quasinormal spectrum (both chiralities) up to mode number n_max.

    Returns lists of energies for left-movers and right-movers.
    """
    E_R = [quasinormal_energy(n, h, r_plus, r_minus, 'R')
           for n in range(n_max + 1)]
    E_L = [quasinormal_energy(n, h, r_plus, r_minus, 'L')
           for n in range(n_max + 1)]
    return {'right': E_R, 'left': E_L, 'h': h,
            'r_plus': r_plus, 'r_minus': r_minus}


