# Section 3: BTZ spectral zeta function
# =========================================================================

def _hurwitz_zeta(s: complex, a: float, n_max: int = 5000) -> complex:
    r"""Hurwitz zeta function zeta_H(s, a) = sum_{n=0}^{infty} (n + a)^{-s}.

    For Re(s) > 1: direct summation with Euler-Maclaurin correction.
    For Re(s) <= 1: use the reflection formula via mpmath if available,
    otherwise return NaN.

    We use direct summation plus an asymptotic tail correction:
        sum_{n > N} (n+a)^{-s} ~ integral_N^infty (x+a)^{-s} dx
                                = (N+a)^{1-s} / (s-1)
    """
    s = complex(s)
    a = float(a)
    if a <= 0:
        raise ValueError(f"Hurwitz zeta requires a > 0, got a = {a}")

    # Try mpmath for full analytic continuation
    try:
        import mpmath
        with mpmath.workdps(20):
            result = mpmath.hurwitz(s, a)
            return complex(result)
    except (ImportError, Exception):
        pass

    # Fallback: direct summation for Re(s) > 1
    re_s = s.real
    if re_s <= 1.0:
        return complex(float('nan'), float('nan'))

    total = complex(0, 0)
    for n in range(n_max):
        total += (n + a) ** (-s)
    # Euler-Maclaurin tail correction
    tail = (n_max + a) ** (1 - s) / (s - 1)
    return total + tail


def spectral_zeta_btz(s: complex, h: float, r_plus: float,
                      r_minus: float = 0.0, n_max: int = 5000) -> complex:
    r"""BTZ spectral zeta function.

    zeta_BTZ(s) = sum_{n >= 0} |omega_{n,R}|^{-s} + |omega_{n,L}|^{-s}

    For non-rotating BTZ (r_- = 0):
        zeta_BTZ(s) = 2 * r_+^{-s} * sum_{n >= 0} (2n + h)^{-s}
                    = 2^{1-s} * r_+^{-s} * zeta_H(s, h/2)

    For rotating BTZ:
        zeta_BTZ(s) = (r_+ + r_-)^{-s} * zeta_H(s, h/2)
                    + (r_+ - r_-)^{-s} * zeta_H(s, h/2)

    Parameters
    ----------
    s : complex
        The spectral parameter.
    h : float
        Conformal weight of the probe field.
    r_plus, r_minus : float
        Horizon radii (l = 1).
    n_max : int
        Truncation for direct summation fallback.
    """
    s = complex(s)
    a = h / 2.0
    if a <= 0:
        raise ValueError(f"Need h > 0 for spectral zeta, got h = {h}")

    hurwitz = _hurwitz_zeta(s, a, n_max)

    if r_minus == 0.0:
        # Non-rotating: both chiralities have scale r_+
        # E_n = r_+ * (2n + h) for both L and R
        return 2.0 * (r_plus ** (-s)) * (2.0 ** (-s)) * hurwitz
    else:
        # Rotating: E_{n,R} = (r_+ + r_-)(2n + h), E_{n,L} = (r_+ - r_-)(2n + h)
        scale_R = r_plus + r_minus
        scale_L = r_plus - r_minus
        if scale_L <= 0:
            # Extremal: left-movers have zero energy, zeta diverges
            return complex(float('inf'))
        return (scale_R ** (-s) + scale_L ** (-s)) * (2.0 ** (-s)) * hurwitz


def spectral_zeta_nonrotating(s: complex, h: float, r_plus: float) -> complex:
    r"""Simplified spectral zeta for non-rotating BTZ.

    zeta_BTZ(s) = 2^{1-s} r_+^{-s} zeta_H(s, h/2)

    This form makes the factorization into geometric (r_+) and
    spectral (Hurwitz zeta) parts manifest.
    """
    return spectral_zeta_btz(s, h, r_plus, 0.0)


def spectral_zeta_derivative_at_zero(h: float, r_plus: float,
                                     r_minus: float = 0.0) -> complex:
    r"""zeta'_BTZ(0) — needed for the functional determinant.

    For non-rotating BTZ:
        zeta'_BTZ(0) = 2 [log(2 r_+) zeta_H(0, h/2) - zeta_H'(0, h/2)]

    where zeta_H(0, a) = 1/2 - a  and
          zeta_H'(0, a) = log Gamma(a) - (1/2) log(2 pi).
    """
    a = h / 2.0
    if a <= 0:
        return complex(float('nan'))

    # zeta_H(0, a) = 1/2 - a
    hurwitz_at_0 = 0.5 - a

    # zeta_H'(0, a) = log Gamma(a) - (1/2) log(2 pi)
    hurwitz_prime_at_0 = math.lgamma(a) - 0.5 * LOG_TWO_PI

    if r_minus == 0.0:
        # Factor: 2 * (r_+)^{-s} * 2^{-s} * zeta_H(s, a)
        # d/ds at s = 0: 2 * [-log(2 r_+) * zeta_H(0, a) + zeta_H'(0, a)]
        # Note: d/ds [(r_+)^{-s} * 2^{-s}] |_{s=0} = -log(2 r_+)
        return 2.0 * (-math.log(2.0 * r_plus) * hurwitz_at_0 + hurwitz_prime_at_0)
    else:
        # General: (R^{-s} + L^{-s}) * 2^{-s} * zeta_H(s, a)
        # where R = r_+ + r_-, L = r_+ - r_-
        R = r_plus + r_minus
        L = r_plus - r_minus
        if L <= 0:
            return complex(float('nan'))

        # At s = 0: derivative of (R^{-s} + L^{-s}) * 2^{-s} * zeta_H(s, a)
        # = -(log(2R) + log(2L)) * zeta_H(0,a)/2  ... no, full product rule:
        # Let f(s) = (R^{-s} + L^{-s}), g(s) = 2^{-s}, h(s) = zeta_H(s, a)
        # (fgh)' = f'gh + fg'h + fgh'
        # f(0) = 2, f'(0) = -(log R + log L)
        # g(0) = 1, g'(0) = -log 2
        # h(0) = hurwitz_at_0, h'(0) = hurwitz_prime_at_0
        f0 = 2.0
        fp0 = -(math.log(R) + math.log(L))
        g0 = 1.0
        gp0 = -math.log(2.0)
        h0 = hurwitz_at_0
        hp0 = hurwitz_prime_at_0

        return fp0 * g0 * h0 + f0 * gp0 * h0 + f0 * g0 * hp0


def spectral_zeta_at_zero(h: float, r_plus: float,
                          r_minus: float = 0.0) -> float:
    r"""zeta_BTZ(0) — the spectral measure.

    For non-rotating BTZ:
        zeta_BTZ(0) = 2 * zeta_H(0, h/2) = 2 * (1/2 - h/2) = 1 - h
    """
    a = h / 2.0
    hurwitz_at_0 = 0.5 - a  # zeta_H(0, a) = 1/2 - a

    if r_minus == 0.0:
        return 2.0 * hurwitz_at_0
    else:
        # (R^{-0} + L^{-0}) * 2^{-0} * zeta_H(0, a) = 2 * hurwitz_at_0
        return 2.0 * hurwitz_at_0


