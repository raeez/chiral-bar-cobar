# =========================================================================

def theta_representation(s: complex, q: float, n_max: int = 200) -> Dict[str, complex]:
    r"""Relate Z_BTZ(s) to the Jacobi theta function.

    The q-Pochhammer symbol (q^s; q)_infty is related to theta_1 via
    the Jacobi triple product.  Specifically:

        (q; q)_infty = prod_{n >= 1} (1 - q^n)

    and the full Jacobi triple product is:

        sum_{n=-infty}^{infty} (-1)^n q^{n(n-1)/2} z^n
            = (q; q)_infty (z; q)_infty (q/z; q)_infty

    Setting z = q^s and dividing gives a relation between
    Z_BTZ(s) = (q^s; q)_infty and theta functions.

    Here we compute the theta series representation:

        theta_1(v | tau) = 2 sum_{n >= 0} (-1)^n q^{(n+1/2)^2/2} sin((2n+1) pi v)

    where q = e^{2 pi i tau} and v is related to s.

    For the BTZ: tau = i beta / (2 pi), so q_theta = e^{-beta}.
    """
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    # Z_BTZ(s) via product
    Z_product = selberg_zeta_btz(s, q, n_max)

    # (q; q)_infty = prod_{n >= 1} (1 - q^n)
    qq_inf = complex(1, 0)
    for n in range(1, n_max):
        qq_inf *= (1.0 - q ** n)
        if abs(q ** n) < 1e-30:
            break

    # Jacobi theta_3(v, q) = sum_{n=-N}^{N} q^{n^2} e^{2 pi i n v}
    # with q_theta = q^{1/2}
    # This is related but the exact identification requires care with
    # the nome vs thermal parameter conventions.
    # We compute the ratio Z_BTZ(s) / (q;q)_infty as a diagnostic.
    ratio = Z_product / qq_inf if abs(qq_inf) > 1e-30 else complex(float('nan'))

    return {
        's': s,
        'q': q,
        'Z_product': Z_product,
        'qq_infty': qq_inf,
        'ratio_Z_over_qq': ratio,
    }


