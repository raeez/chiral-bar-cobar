# Section 13: Cardy formula consistency at large L_0
# =========================================================================

def cardy_consistency(c: float, L0_values: Optional[List[float]] = None) -> List[Dict[str, float]]:
    r"""Verify Cardy formula consistency at large L_0.

    The Cardy formula S = 2 pi sqrt(c Delta / 6) (Delta = L_0 - c/24)
    is valid for Delta >> c.  We verify:

    1. S grows as sqrt(Delta) (correct scaling)
    2. The coefficient is 2 pi sqrt(c/6)
    3. The subleading correction is O(1/sqrt(Delta))

    At large L_0, the BTZ spectral zeta is dominated by the ground state:
        zeta_BTZ(s) ~ E_0^{-s} * zeta_H(s, h/2) [geometric optics limit]

    The Cardy entropy from the spectral zeta is:
        S = log(density of states at E) = beta E - log Z(beta)

    evaluated at the saddle point beta = pi sqrt(c/(6 Delta)).
    """
    if L0_values is None:
        L0_values = [10, 100, 1000, 10000, 100000]

    c = float(c)
    results = []

    for L0 in L0_values:
        Delta = L0 - c / 24.0
        if Delta <= 0:
            continue

        S_cardy = TWO_PI * math.sqrt(c * Delta / 6.0)
        beta_saddle = PI * math.sqrt(c / (6.0 * Delta))

        # Coefficient extraction: S / sqrt(Delta) should be 2 pi sqrt(c/6)
        coeff = S_cardy / math.sqrt(Delta)
        expected_coeff = TWO_PI * math.sqrt(c / 6.0)

        results.append({
            'L0': L0,
            'Delta': Delta,
            'S_cardy': S_cardy,
            'beta_saddle': beta_saddle,
            'coefficient': coeff,
            'expected_coefficient': expected_coeff,
            'relative_error': abs(coeff - expected_coeff) / expected_coeff,
        })

    return results


