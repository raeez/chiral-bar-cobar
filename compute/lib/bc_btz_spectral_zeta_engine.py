def btz_horizons(M: float, J: float = 0.0) -> Dict[str, float]:
    r"""Horizon radii for the BTZ black hole (l = 1).

    r_+^2 = (M/2)(1 + sqrt(1 - (J/M)^2))
    r_-^2 = (M/2)(1 - sqrt(1 - (J/M)^2))

    Extremal limit: |J| = M, r_+ = r_- = sqrt(M/2).
    Non-rotating: J = 0, r_+ = sqrt(M), r_- = 0.
    """
    M = float(M)
    J = float(J)
    if M <= 0:
        return {'r_plus': 0.0, 'r_minus': 0.0, 'valid': False}
    if abs(J) > M:
        return {'r_plus': 0.0, 'r_minus': 0.0, 'valid': False,
                'error': 'naked singularity: |J| > M'}
    disc = max(1.0 - (J / M) ** 2, 0.0)
    sq = math.sqrt(disc)
    r_plus_sq = (M / 2.0) * (1.0 + sq)
    r_minus_sq = (M / 2.0) * (1.0 - sq)
    return {
        'r_plus': math.sqrt(max(r_plus_sq, 0.0)),
        'r_minus': math.sqrt(max(r_minus_sq, 0.0)),
        'valid': True,
    }


def hawking_temperature_btz(M: float, J: float = 0.0) -> float:
    r"""Hawking temperature T_H = (r_+^2 - r_-^2) / (2 pi r_+).

    Non-rotating: T_H = r_+ / (2 pi) = sqrt(M) / (2 pi).
    """
    h = btz_horizons(M, J)
    if not h['valid'] or h['r_plus'] <= 0:
        return 0.0
    r_p = h['r_plus']
    r_m = h['r_minus']
    return (r_p ** 2 - r_m ** 2) / (TWO_PI * r_p)


def thermal_parameter(M: float, J: float = 0.0) -> float:
    r"""Thermal parameter q = e^{-2 pi r_+}.

    This is the multiplier of the loxodromic element generating the
    BTZ quotient.  For non-rotating BTZ: q = e^{-2 pi sqrt(M)}.
    """
    h = btz_horizons(M, J)
    if not h['valid'] or h['r_plus'] <= 0:
        return 0.0
    return math.exp(-TWO_PI * h['r_plus'])


def inverse_temperature_btz(M: float, J: float = 0.0) -> float:
    r"""Inverse Hawking temperature beta = 1/T_H = 2 pi r_+ / (r_+^2 - r_-^2).

    Non-rotating: beta = 2 pi / r_+ = 2 pi / sqrt(M).
    """
    T = hawking_temperature_btz(M, J)
    if T <= 0:
        return float('inf')
    return 1.0 / T


