# Section 5: Zeros of the Selberg zeta
# =========================================================================

def selberg_zeta_zeros_exact(q: float, n_max: int = 100) -> List[Dict[str, Any]]:
    r"""Exact zeros of Z_BTZ(s) = prod_{n >= 0} (1 - q^{n+s}).

    Z_BTZ(s) = 0 when q^{n+s} = 1 for some n >= 0, i.e.,

        (n + s) * log(q) = 2 pi i k    (k in Z)

    so  s = -n + 2 pi i k / log(1/q)   (since log q < 0).

    Re(s) = -n, Im(s) = 2 pi k / log(1/q).

    The zeros lie on vertical lines Re(s) = -n for n = 0, 1, 2, ...
    They are NOT on Re(s) = 1/2 (the Selberg zeta zeros for BTZ
    lie at integer real parts, not on a critical line).
    """
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    log_inv_q = math.log(1.0 / q)  # = 2 pi r_+
    spacing = TWO_PI / log_inv_q

    zeros = []
    for n in range(min(n_max, 10)):
        for k in range(-n_max, n_max + 1):
            s_zero = complex(-n, k * spacing)
            zeros.append({
                'n': n,
                'k': k,
                's': s_zero,
                'real_part': -n,
                'imag_part': k * spacing,
            })
    return zeros


def selberg_zeros_on_strip(q: float, real_min: float = -0.5,
                           real_max: float = 0.5,
                           k_max: int = 100) -> List[complex]:
    r"""Zeros of Z_BTZ in the strip real_min <= Re(s) <= real_max.

    For n = 0, Re(s) = 0 lies in any strip containing 0.
    For n >= 1, Re(s) = -n < 0.

    The only zeros with Re(s) = 0 come from n = 0:
        s = 2 pi i k / log(1/q),  k in Z.

    NOTE: The zeros do NOT cluster on Re(s) = 1/2.
    This is a mathematical FACT about the BTZ Selberg zeta,
    not a failure of the computation.
    """
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    log_inv_q = math.log(1.0 / q)
    spacing = TWO_PI / log_inv_q

    zeros = []
    for n in range(20):
        if -n < real_min or -n > real_max:
            continue
        for k in range(-k_max, k_max + 1):
            zeros.append(complex(-n, k * spacing))
    return zeros


def verify_selberg_zero(s: complex, q: float, n_max: int = 500,
                        tol: float = 1e-8) -> Dict[str, Any]:
    r"""Verify that s is a zero of Z_BTZ by evaluating the product."""
    Z = selberg_zeta_btz(s, q, n_max)
    return {
        's': s,
        'Z_BTZ': Z,
        'abs_Z': abs(Z),
        'is_zero': abs(Z) < tol,
    }


