# Section 9: Modular crossing (high-T / low-T duality)
# =========================================================================

def modular_crossing_selberg(q: float, s: complex,
                             n_max: int = 500) -> Dict[str, complex]:
    r"""Compare Z_BTZ at temperature beta and the dual 4 pi^2 / beta.

    The modular crossing sends beta -> 4 pi^2 / beta.  In terms of q:
        q = e^{-beta},  q_dual = e^{-4 pi^2 / beta}

    At the self-dual point beta_* = 2 pi:
        q_* = e^{-2 pi},  q_dual_* = e^{-2 pi} = q_*

    so the self-dual temperature is exactly the Hawking-Page point.
    """
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    beta = -math.log(q)
    beta_dual = 4.0 * PI ** 2 / beta
    q_dual = math.exp(-beta_dual)

    Z = selberg_zeta_btz(s, q, n_max)
    Z_dual = selberg_zeta_btz(s, q_dual, n_max) if 0 < q_dual < 1 else complex(float('nan'))

    return {
        'beta': beta,
        'beta_dual': beta_dual,
        'q': q,
        'q_dual': q_dual,
        'Z_BTZ': Z,
        'Z_BTZ_dual': Z_dual,
        'ratio': Z / Z_dual if abs(Z_dual) > 1e-30 else complex(float('nan')),
        'self_dual': abs(beta - TWO_PI) < 1e-10,
    }


def selberg_at_self_dual(s: complex, n_max: int = 500) -> complex:
    r"""Z_BTZ(s) at the self-dual temperature q_* = e^{-2 pi}.

    At this temperature, beta = 2 pi (the Hawking-Page point),
    and the modular crossing is trivially satisfied.
    """
    q_star = math.exp(-TWO_PI)
    return selberg_zeta_btz(s, q_star, n_max)


