# Section 12: WKB approximation of quasinormal modes
# =========================================================================

def wkb_quasinormal_frequencies(M: float, h: float,
                                n_max: int = 20) -> List[complex]:
    r"""WKB approximation for BTZ quasinormal frequencies.

    In the WKB approximation (valid for large n), the quasinormal
    frequencies of a scalar field on the BTZ background are:

        omega_n^{WKB} ~ -i r_+ (2n + h) * [1 + O(1/n)]

    The leading order matches the exact result (BTZ is special:
    the scalar wave equation is exactly solvable, so WKB is exact
    at leading order).

    The WKB approximation provides an independent verification path
    for the quasinormal spectrum.
    """
    r_plus = math.sqrt(M) if M > 0 else 0.0
    if r_plus <= 0:
        return []

    freqs = []
    for n in range(n_max):
        # Exact (non-rotating)
        omega_exact = -1j * r_plus * (2 * n + h)
        # WKB correction at order 1/n (vanishes for BTZ)
        omega_wkb = omega_exact  # BTZ: WKB is exact
        freqs.append(omega_wkb)
    return freqs


