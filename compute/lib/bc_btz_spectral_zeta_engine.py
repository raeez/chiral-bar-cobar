def bekenstein_hawking_from_shadow(c: float, L0: float) -> Dict[str, float]:
    r"""S_BH = 2 pi sqrt(c L_0 / 6) from Cardy / shadow data.

    In the shadow language:
        kappa(Vir_c) = c/2
        S_BH = 2 pi sqrt(c * Delta / 6)

    where Delta = L_0 - c/24 is the conformal dimension above vacuum.

    The genus-1 shadow free energy F_1 = kappa/24 = c/48 gives:
        c = 48 * F_1, so S_BH = 2 pi sqrt(48 * F_1 * Delta / 6) = 2 pi sqrt(8 * F_1 * Delta)

    This is the Cardy formula expressed through the shadow obstruction tower.
    """
    c = float(c)
    L0 = float(L0)
    Delta = L0 - c / 24.0
    kappa = c / 2.0

    S_BH = 0.0
    if c * Delta > 0:
        S_BH = TWO_PI * math.sqrt(c * Delta / 6.0)

    F1 = kappa / 24.0

    # Alternative: S_BH from F_1
    # c = 48 * F_1, so c * Delta / 6 = 48 * F_1 * Delta / 6 = 8 * F_1 * Delta
    S_BH_from_F1 = 0.0
    if F1 * Delta > 0:
        S_BH_from_F1 = TWO_PI * math.sqrt(8.0 * F1 * Delta)

    return {
        'c': c,
        'L0': L0,
        'Delta': Delta,
        'kappa': kappa,
        'F_1': F1,
        'S_BH': S_BH,
        'S_BH_from_F1': S_BH_from_F1,
        'match': abs(S_BH - S_BH_from_F1) < 1e-12 * max(abs(S_BH), 1.0),
    }


