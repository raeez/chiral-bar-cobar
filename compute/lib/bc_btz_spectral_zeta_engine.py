# Section 14: Phase structure and modular properties
# =========================================================================

def selberg_zeta_modular_check(q: float, s: complex,
                               n_max: int = 500) -> Dict[str, Any]:
    r"""Check modular properties of Z_BTZ under q -> q^{-1} (beta -> -beta).

    The q-Pochhammer symbol (q^s; q)_infty has a modular transformation
    property related to the Dedekind eta function:

        (q; q)_infty = e^{-pi i tau / 12} * eta(tau)    (wrong: see AP46)

    Actually: eta(tau) = q^{1/24} * (q; q)_infty where q = e^{2 pi i tau}.
    The q^{1/24} is NOT optional (AP46).

    Under tau -> -1/tau:
        eta(-1/tau) = sqrt(-i tau) * eta(tau)

    This gives a modular identity for (q; q)_infty that extends
    (with care) to (q^s; q)_infty.
    """
    if q <= 0 or q >= 1:
        raise ValueError(f"Need 0 < q < 1, got q = {q}")

    beta = -math.log(q)
    tau = 1j * beta / TWO_PI  # tau = i beta / (2 pi) on imaginary axis

    Z_s = selberg_zeta_btz(s, q, n_max)

    # (q; q)_infty = eta(tau) / q^{1/24}
    # eta(tau) under tau -> -1/tau transforms with sqrt(-i tau)
    qq_inf = complex(1, 0)
    for n in range(1, n_max):
        f = 1.0 - q ** n
        qq_inf *= f
        if abs(q ** n) < 1e-30:
            break

    # q^{1/24}
    q_24 = q ** (1.0 / 24.0)
    eta_val = q_24 * qq_inf  # eta(tau) = q^{1/24} (q; q)_infty

    return {
        'beta': beta,
        'tau': tau,
        's': s,
        'Z_BTZ': Z_s,
        'qq_infty': qq_inf,
        'eta': eta_val,
        'q_24': q_24,
    }


