def projective_cover_structure(p: int) -> Dict:
    """Structure of projective covers P(s) in the W(p) module category.

    Each simple module X_s has a projective cover P(s).
    The projective cover has a 4-step Loewy filtration:
      P(s): X_s -> (X_{s-1} + X_{s+1}) -> X_s
    (with appropriate boundary conditions at s=1 and s=p).

    The Loewy length is 3 for generic s, and 2 for s=1, p.

    The projective covers give the non-semisimple structure:
      Ext^1(X_s, X_{s'}) != 0 for |s - s'| = 1.
    """
    covers = {}
    for s in range(1, p + 1):
        neighbors = []
        if s > 1:
            neighbors.append(s - 1)
        if s < p:
            neighbors.append(s + 1)

        covers[s] = {
            'top': f'X_{s}^+',
            'middle': [f'X_{n}^+' for n in neighbors],
            'bottom': f'X_{s}^+',
            'loewy_length': 3 if neighbors else 2,
            'dim_ext1': len(neighbors),
        }

    return {
        'p': p,
        'projective_covers': covers,
        'total_loewy_sum': sum(
            c['loewy_length'] for c in covers.values()
        ),
    }


