def kappa_additivity_check(p: int) -> Dict:
    """Check kappa additivity for W(p) tensor products.

    For independent algebras A, B:
      kappa(A tensor B) = kappa(A) + kappa(B)

    For W(p1) tensor W(p2):
      kappa = c(p1)/2 + c(p2)/2

    This is a consistency check for the kappa computation.
    """
    results = []
    for p2 in range(2, p + 1):
        c1 = triplet_central_charge(p)
        c2 = triplet_central_charge(p2)
        kappa_tensor = c1 / 2 + c2 / 2
        kappa_direct = (c1 + c2) / 2
        results.append({
            'p1': p, 'p2': p2,
            'kappa_sum': kappa_tensor,
            'kappa_direct': kappa_direct,
            'agrees': (kappa_tensor == kappa_direct),
        })
    return {
        'p': p,
        'tensor_checks': results,
        'all_agree': all(r['agrees'] for r in results),
    }


def complementarity_consistency(max_p: int = 8) -> Dict:
    """Check complementarity sum kappa + kappa^! = 13 for all W(p).

    AP24: For Virasoro-type families, kappa + kappa^! = 13 (not 0).
    """
    results = []
    for p in range(2, max_p + 1):
        c = triplet_central_charge(p)
        kappa = c / 2
        kappa_dual = (26 - c) / 2
        comp_sum = kappa + kappa_dual
        results.append({
            'p': p,
            'kappa': kappa,
            'kappa_dual': kappa_dual,
            'sum': comp_sum,
            'equals_13': (comp_sum == 13),
        })
    return {
        'results': results,
        'all_equal_13': all(r['equals_13'] for r in results),
    }


def w2_bc_ghost_comparison() -> Dict:
    """Compare W(2) with the bc ghost system.

    W(2) and the bc ghost system both have c = -2.
    But they are DIFFERENT VOAs:
      W(2): BOSONIC generators T (wt 2), W (wt 3). Triplet algebra.
      bc: FERMIONIC generators b (wt 2), c (wt -1). Free field.

    In the bar-cobar framework:
      kappa(W(2)) = c/2 = -1
      kappa(bc) = c/2 = -1 (same!)
      But the bar complexes are DIFFERENT (different generators, OPE).

    W(2) is actually ISOMORPHIC to the even part of the symplectic
    fermion VOA (Kausch, Abe 2007). The full symplectic fermion VOA
    has a Z/2Z grading, and W(2) is the even part.

    For the bar-cobar framework, the key distinction is:
      bc system: class G or L (fermionic, quadratic-type OPE)
      W(2): class M (non-quadratic W-W OPE, pole order 6)
    """
    c = Rational(-2)
    return {
        'c': c,
        'W2_kappa': c / 2,
        'bc_kappa': c / 2,
        'kappas_equal': True,
        'algebras_isomorphic': False,
        'W2_class': 'M',
        'bc_class': 'G',  # bc ghost is Gaussian (free field, class G)
        'W2_generators': [('T', 2), ('W', 3)],
        'bc_generators': [('b', 2), ('c', -1)],
        'W2_WW_pole': 6,
        'bc_max_pole': 3,  # b(z)c(w) ~ 1/(z-w)
        'W2_c2_cofinite': True,
        'bc_c2_cofinite': False,
    }


