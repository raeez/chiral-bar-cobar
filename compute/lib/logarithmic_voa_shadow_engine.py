def zhu_algebra_structure(p: int) -> Dict:
    """Structure of the Zhu algebra A(W(p)).

    A(W(p)) has:
      - Total dimension: 2p
      - Semisimple quotient dimension: p (p simple modules up to
        projective covers / p irreducible representations)
      - Nilpotent radical dimension: p
      - Number of simple modules: 2p
      - NOT semisimple (key logarithmic feature)

    The 2p simple modules are:
      Lambda(s) for s = 1, ..., p: "standard" modules
      Pi(s) for s = 1, ..., p: "twisted" modules

    The conformal weights of simple modules (Adamovic-Milas):
      h(Lambda(s)) = ((2p - s)^2 - (p-1)^2) / (4p)
                   = (s^2 - 2ps + p^2 + 2p - 1) / (4p)
      Simplification: h(Lambda(s)) = (s - p)^2/(4p) + (2p - 1)/(4p)
      ... actually the formula is:
      h_{r,s} = ((pr - s)^2 - (p-1)^2) / (4p) for M(p,1) Kac table.

    For the TRIPLET modules:
      Lambda(s): h_s = (s^2 - 1)/(4p) - (p-1)^2/(4p)
      ... this needs care. Use the explicit formulas.

    Simple module conformal weights (Adamovic-Milas 2008, Thm 5.1):
      For W(p), the 2p simple modules have lowest conformal weights:
        Lambda(s): h_s = ((2s-1)^2 - 1) / (8p), s = 1, ..., p
        Pi(s):     h_s = (4s^2 - 1) / (8p),     s = 1, ..., p

    Wait, these are p + p = 2p modules. Let me use the standard
    notation from Adamovic-Milas.

    STANDARD LABELING (Adamovic-Milas 2008):
      2p simple modules: X_s^+ (s=1,...,p) and X_s^- (s=1,...,p)
      Conformal weights:
        h(X_s^+) = (s^2 - 1)/(4p) for s = 1, ..., p   (vacuum at s=1: h=0)
        h(X_s^-) = ((2p-s)^2 - 1)/(4p) for s = 1, ..., p

    Actually, the precise formula depends on convention. Let me use
    what is unambiguously known:
      - 2p simple modules
      - Vacuum module has h = 0
      - Nilpotent radical has dimension p in A(W(p))
    """
    n_simple = 2 * p

    # Conformal weights of simple modules
    # Using Kac table for M(p,1) type (Feigin-Fuchs):
    # h_{1,s} = (s^2 - 1) / (4p), s = 1, ..., 2p-1
    # But for W(p) the module structure is different.

    # Standard: W(p) has 2p simple modules with weights:
    conformal_weights_plus = {}
    conformal_weights_minus = {}
    for s in range(1, p + 1):
        # X_s^+: lowest weight in the "untwisted" sector
        h_plus = Rational((s**2 - 1), 4 * p)
        conformal_weights_plus[f'X_{s}^+'] = h_plus
        # X_s^-: lowest weight in the "twisted" sector
        h_minus = Rational(s**2, 4 * p)
        conformal_weights_minus[f'X_{s}^-'] = h_minus

    all_weights = {}
    all_weights.update(conformal_weights_plus)
    all_weights.update(conformal_weights_minus)

    return {
        'p': p,
        'dim_zhu': 2 * p,
        'n_simple_modules': n_simple,
        'is_semisimple': False,
        'nilpotent_radical_dim': p,
        'semisimple_quotient_dim': p,
        'conformal_weights': all_weights,
        'vacuum_weight': Rational(0),  # X_1^+ is the vacuum
    }


