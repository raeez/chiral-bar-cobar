def pseudotrace_genus1_structure(p: int) -> Dict:
    """Structure of the genus-1 pseudo-trace correction for W(p).

    The genus-1 partition function of W(p) is:
      Z_1(tau) = tr_{W(p)} q^{L_0 - c/24}  (standard trace)

    But the FULL modular-invariant genus-1 amplitude requires
    pseudo-trace contributions (Miyamoto 2004):
      Z_1^{full}(tau) = Z_1(tau) + sum_j alpha_j Z_1^{PT,j}(tau)

    The pseudo-traces Z_1^{PT,j} arise from the logarithmic modules:
    they involve terms like log(q) * (polynomial in characters).

    In the shadow obstruction tower:
      F_1 = kappa/24 (from the standard trace part)
      F_1^{PT} = additional logarithmic contribution (from pseudo-traces)

    The TOTAL genus-1 shadow is:
      F_1^{total}(W(p)) = kappa(W(p))/24 + F_1^{PT}(W(p))

    For the vacuum amplitude (no module insertion):
      F_1^{PT} = 0 (the vacuum module is NOT logarithmic)
    So the vacuum genus-1 shadow is simply F_1 = kappa/24.

    The pseudo-trace correction becomes visible only when we
    consider MODULE-LEVEL shadows (inserting a logarithmic module
    on the torus), which is beyond the vacuum-level shadow tower.
    """
    c = triplet_central_charge(p)
    kappa = c / 2

    return {
        'p': p,
        'c': c,
        'kappa': kappa,
        'F1_vacuum': kappa / 24,
        'F1_pseudotrace_vacuum': Rational(0),
        'F1_total_vacuum': kappa / 24,
        'pseudotrace_visible_at': 'module-level shadows (not vacuum)',
        'n_pseudotrace_functions': 2 * p - 1,
        'total_modular_functions': 3 * p - 1,
    }


