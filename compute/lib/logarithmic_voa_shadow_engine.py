# Discriminant and shadow metric
# =========================================================================

def shadow_metric_coefficients(p: int) -> Dict:
    """Shadow metric Q_L coefficients for W(p).

    The shadow metric on the primary line L (Virasoro sector):
      Q_L(t) = (2*kappa + 3*alpha*t)^2 + 2*Delta*t^2

    where alpha = S_3 (cubic shadow) and Delta = 8*kappa*S_4
    (critical discriminant).

    For W(p), these are the VIRASORO-SECTOR values because the
    vacuum shadow tower is controlled by the Virasoro sub-VOA.
    """
    c = triplet_central_charge(p)
    kappa = c / 2

    # Virasoro shadow coefficients (from the manuscript)
    if c == 0:
        return {'p': p, 'c': c, 'kappa': kappa, 'degenerate': True}

    S3 = Rational(-48) / (c**2 * (5*c + 22))
    S4 = Rational(10) / (c * (5*c + 22))  # This is Q^contact

    alpha = S3
    Delta = 8 * kappa * S4

    return {
        'p': p,
        'c': c,
        'kappa': kappa,
        'S3': S3,
        'S4': S4,
        'alpha': alpha,
        'Delta': Delta,
        'Delta_nonzero': (Delta != 0),
        'shadow_class_from_Delta': 'M' if Delta != 0 else 'G_or_L',
    }


