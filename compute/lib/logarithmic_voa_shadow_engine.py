def triplet_central_charge(p: int) -> Rational:
    """Central charge of the triplet algebra W(p).

    c(p) = 1 - 6(p-1)^2/p.

    This is the same central charge as the Virasoro minimal model M(p,1),
    but W(p) is NOT a minimal model: it is a larger algebra containing
    the Virasoro as a sub-VOA.

    Convention: p >= 2 integer.
    """
    if p < 2:
        raise ValueError(f"Triplet parameter p must be >= 2, got {p}")
    return Rational(1) - Rational(6 * (p - 1)**2, p)


def singlet_central_charge(p: int) -> Rational:
    """Central charge of the singlet algebra M(p).

    Same as W(p): c(p) = 1 - 6(p-1)^2/p.
    The singlet is a sub-VOA of the triplet.
    """
    return triplet_central_charge(p)


def triplet_central_charges_table(max_p: int = 10) -> Dict[int, Rational]:
    """Table of central charges for W(p), p = 2, ..., max_p."""
    return {p: triplet_central_charge(p) for p in range(2, max_p + 1)}


