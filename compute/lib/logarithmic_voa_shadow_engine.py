@dataclass
class TripletGeneratorData:
    """Generator data for the triplet algebra W(p).

    W(p) is strongly generated by:
      T: the Virasoro field, conformal weight 2
      W: the extra generator, conformal weight 2p-1

    For p=2: generators T (wt 2) and W (wt 3).
    For p=3: generators T (wt 2) and W (wt 5).
    """
    p: int
    T_weight: int = 2
    W_weight: int = 0  # set in __post_init__
    n_generators: int = 2
    c: Rational = Rational(0)

    def __post_init__(self):
        self.W_weight = 2 * self.p - 1
        self.c = triplet_central_charge(self.p)

    @property
    def is_uniform_weight(self) -> bool:
        """Whether generators have the same conformal weight.

        For p=2: T has weight 2, W has weight 3. NOT uniform.
        For all p >= 2: NOT uniform (2 != 2p-1 for p >= 2).
        """
        return self.T_weight == self.W_weight

    @property
    def max_ope_pole_order(self) -> int:
        """Maximum pole order in the OPE.

        T-T: pole order 4
        T-W: pole order 2 (primary field)
        W-W: pole order 2*(2p-1) (the key structure)
        """
        return 2 * self.W_weight

    @property
    def bar_r_matrix_max_pole(self) -> int:
        """Maximum pole order in the bar r-matrix.

        AP19: bar r-matrix pole = OPE pole - 1 (d log absorption).
        """
        return self.max_ope_pole_order - 1

    @property
    def generator_weights(self) -> List[int]:
        """List of generator conformal weights."""
        return [self.T_weight, self.W_weight]


def triplet_generators(p: int) -> TripletGeneratorData:
    """Generator data for W(p)."""
    return TripletGeneratorData(p=p)


