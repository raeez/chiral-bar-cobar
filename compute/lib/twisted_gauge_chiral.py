class C3HodgeData(CY3HodgeData):
    """C^3 = flat affine space (non-compact CY3).

    h^{0,*}(C^3): only H^{0,0} = C (constants). All higher vanish.
    """
    def __init__(self):
        super().__init__(name="C^3", h11=0, h21=0, is_compact=False)

    @property
    def dbar_cohomology_h0star(self) -> Dict[int, int]:
        return {0: 1, 1: 0, 2: 0, 3: 0}


class K3xEHodgeData(CY3HodgeData):
    """K3 x E (K3 surface times elliptic curve).

    By Kunneth:
      h^{1,1}(K3 x E) = h^{1,1}(K3) + h^{1,0}(E)*h^{0,1}(K3)
                        + h^{0,0}(E)*h^{1,1}(K3)...
    Actually: h^{p,q}(K3 x E) = sum_{a+c=p, b+d=q} h^{a,b}(K3) h^{c,d}(E).

    h^{1,1}(K3 x E) = h^{1,1}(K3)*h^{0,0}(E) + h^{1,0}(K3)*h^{0,1}(E)
                      + h^{0,0}(K3)*h^{1,1}(E) + h^{0,1}(K3)*h^{1,0}(E)
                      + h^{1,0}(E)*h^{0,1}(K3)
    Using K3: h^{1,0} = h^{0,1} = 0, h^{1,1} = 20, h^{0,0} = h^{2,2} = 1,
              h^{2,0} = h^{0,2} = 1.
    And E: h^{0,0} = h^{1,1} = 1, h^{1,0} = h^{0,1} = 1.

    h^{1,1} = 20*1 + 0 + 1*1 + 0 + 0 = 21.
    h^{2,1} = sum h^{a,b}(K3)*h^{c,d}(E) with a+c=2, b+d=1:
      (a,b)=(2,0),(c,d)=(0,1): h^{2,0}(K3)*h^{0,1}(E) = 1*1 = 1
      (a,b)=(2,1),(c,d)=(0,0): h^{2,1}(K3)*h^{0,0}(E) = 0*1 = 0
      (a,b)=(1,0),(c,d)=(1,1): h^{1,0}(K3)*h^{1,1}(E) = 0*1 = 0
      (a,b)=(1,1),(c,d)=(1,0): h^{1,1}(K3)*h^{1,0}(E) = 20*1 = 20
      (a,b)=(0,0),(c,d)=(2,1): 0 (E is dim 1)
      (a,b)=(0,1),(c,d)=(2,0): 0 (E is dim 1)
    h^{2,1} = 1 + 20 = 21.

    chi(K3 x E) = 2*(21 - 21) = 0.
    """
    def __init__(self):
        super().__init__(name="K3xE", h11=21, h21=21, is_compact=True)

    @property
    def dbar_cohomology_h0star(self) -> Dict[int, int]:
        """H^{0,*}(K3 x E) by Kunneth.

        h^{0,p}(K3 x E) = sum_{a+c=0, b+d=p} h^{a,b}(K3) h^{c,d}(E)
        Since a, c >= 0 and a + c = 0: a = c = 0.
        So h^{0,p}(K3 x E) = sum_{b+d=p} h^{0,b}(K3) h^{0,d}(E).

        K3: h^{0,0} = 1, h^{0,1} = 0, h^{0,2} = 1.
        E:  h^{0,0} = 1, h^{0,1} = 1.

        p=0: h^{0,0}(K3)*h^{0,0}(E) = 1.
        p=1: h^{0,0}(K3)*h^{0,1}(E) + h^{0,1}(K3)*h^{0,0}(E) = 1 + 0 = 1.
        p=2: h^{0,0}(K3)*h^{0,2}(E) + h^{0,1}(K3)*h^{0,1}(E)
             + h^{0,2}(K3)*h^{0,0}(E)
           = 0 + 0 + 1 = 1.  (E has no h^{0,2}, so first term is 0)
        p=3: h^{0,1}(K3)*h^{0,2}(E) + h^{0,2}(K3)*h^{0,1}(E)
           = 0 + 1*1 = 1.
        """
        return {0: 1, 1: 1, 2: 1, 3: 1}


