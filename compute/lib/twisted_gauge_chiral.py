# Section 11: Holomorphic twist field content
# ===========================================================================

def holomorphic_twist_field_content(cy3: CY3HodgeData,
                                    gauge_dim: int) -> Dict[str, Any]:
    """Field content of the holomorphic twist of 5d N=1 on CY3 x C.

    After the holomorphic twist, the 5d gauge field A in Omega^{0,*}(CY3 x C, g)
    decomposes by dbar-cohomology degree on CY3:

    For each p in {0, 1, 2, 3}:
      Fields in H^{0,p}(CY3) tensor g:
        - count: h^{0,p}(CY3) * dim(g) fields
        - conformal weight on C: 1 - p  (from the twist, Costello 2013)
          Actually the conformal weights depend on the specific twist.
          For the standard holomorphic twist:
            p=0: weight 1 (currents)
            p=1: weight 0 (zero modes)
            p=2: weight -1 (anti-fields)
            p=3: weight -2 (anti-field partners)

    Ghost sector: the BRST complex adds ghost fields.

    Returns: field content summary.
    """
    h0star = cy3.dbar_cohomology_h0star
    fields = {}
    total_fields = 0

    weight_assignment = {0: 1, 1: 0, 2: -1, 3: -2}

    for p in range(4):
        count = h0star.get(p, 0) * gauge_dim
        if count > 0:
            fields[f"A^(0,{p})"] = {
                'dbar_degree': p,
                'count': count,
                'conformal_weight': weight_assignment[p],
                'h0p': h0star.get(p, 0),
                'gauge_factor': gauge_dim,
            }
            total_fields += count

    return {
        'cy3': cy3.name,
        'gauge_dim': gauge_dim,
        'total_fields': total_fields,
        'fields': fields,
        'total_central_charge_contribution': sum(
            f['count'] * (1 - 6 * f['conformal_weight'] * (f['conformal_weight'] - 1))
            for f in fields.values()
        ),
    }


