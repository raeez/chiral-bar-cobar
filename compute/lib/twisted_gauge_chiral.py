def w_infinity_from_gl_n(N: int) -> Dict[str, Any]:
    r"""W_{1+infinity} algebra at c = N from GL(N) on C^3.

    At tree level: GL(N) gauge theory on C^3 x C gives affine gl_N at level 1.
    At the full quantum level (all loops): the boundary chiral algebra is
    W_{1+infinity} at c = N (Gaberdiel-Gopakumar, Costello-Li).

    W_{1+infinity} has generators W_s for s = 1, 2, 3, ...:
      W_1 = U(1) current (from u(1) factor)
      W_2 = stress tensor T (Virasoro, from sl_N Sugawara)
      W_3, W_4, ... = higher-spin currents (quantum corrections)

    The W_{1+infinity} algebra at c = N is the limit of W_N as N -> infinity,
    but it exists at each finite N as well.

    For the shadow obstruction tower:
      kappa(W_{1+inf}|_{c=N}) = kappa(gl_N at k=1)
    which involves the FULL gl_N kappa, not c/2 (AP39).
    """
    data = gl_n_data(N)
    kappa = data.kappa_total

    # Higher-spin generators: W_s for s = 1, ..., N
    # (for GL(N) at level 1, the algebra truncates at spin N)
    generators = {s: {'spin': s, 'conformal_weight': s} for s in range(1, N + 1)}

    return {
        'N': N,
        'c': N,  # c = N for W_{1+inf} at c = N
        'kappa': kappa,
        'generators': generators,
        'num_generators': N,
        'shadow_class': 'L' if N >= 2 else 'G',
        'F_1': data.F_g(1),
    }
