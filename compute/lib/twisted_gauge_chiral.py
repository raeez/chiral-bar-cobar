@dataclass(frozen=True)
class CY3HodgeData:
    """Hodge data for a Calabi-Yau 3-fold.

    A CY3 X has h^{3,0} = 1 (holomorphic 3-form Omega exists),
    h^{p,0} = 0 for 0 < p < 3 (strict CY, not just c_1 = 0).
    The independent Hodge numbers are h^{1,1} and h^{2,1}.
    """
    name: str
    h11: int          # h^{1,1}: Kahler moduli
    h21: int          # h^{2,1}: complex structure moduli
    is_compact: bool = True

    @property
    def euler_topological(self) -> int:
        """Topological Euler characteristic chi(X) = 2(h^{1,1} - h^{2,1})."""
        return 2 * (self.h11 - self.h21)

    @property
    def dbar_cohomology_h0star(self) -> Dict[int, int]:
        r"""Dimensions of H^{0,p}(X) for p = 0, 1, 2, 3.

        For a strict CY3 (simply connected, h^{1,0} = 0):
          h^{0,0} = 1, h^{0,1} = 0, h^{0,2} = 0, h^{0,3} = 1.

        For K3 x E (non-simply-connected):
          h^{0,0} = 1, h^{0,1} = 1, h^{0,2} = 1, h^{0,3} = 1.
        """
        # Default for strict CY3 (simply connected)
        return {0: 1, 1: 0, 2: 0, 3: 1}

    @property
    def holomorphic_euler(self) -> int:
        """Holomorphic Euler characteristic chi(O_X) = sum (-1)^p h^{0,p}."""
        h0s = self.dbar_cohomology_h0star
        return sum((-1)**p * h0s.get(p, 0) for p in range(4))

    @property
    def total_dbar_dim(self) -> int:
        """Total dimension of H^{0,*}(X)."""
        return sum(self.dbar_cohomology_h0star.values())


