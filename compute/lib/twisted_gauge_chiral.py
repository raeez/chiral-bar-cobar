def _frac(x) -> Fraction:
    """Coerce to Fraction for exact arithmetic."""
    if isinstance(x, Fraction):
        return x
    if isinstance(x, (int, str)):
        return Fraction(x)
    return Fraction(x)


@lru_cache(maxsize=64)
def _bernoulli_exact(n: int) -> Fraction:
    """Exact Bernoulli number B_n via the recurrence.

    B_0 = 1, B_1 = -1/2, B_n = 0 for odd n >= 3.
    Recurrence: sum_{k=0}^{n} C(n+1, k) B_k = 0 for n >= 1.
    """
    if n == 0:
        return Fraction(1)
    if n == 1:
        return Fraction(-1, 2)
    if n % 2 == 1 and n >= 3:
        return Fraction(0)
    result = Fraction(0)
    for k in range(n):
        result += comb(n + 1, k) * _bernoulli_exact(k)
    return -result / (n + 1)


@lru_cache(maxsize=64)
def _lambda_fp_exact(g: int) -> Fraction:
    r"""Faber-Pandharipande intersection number lambda_g^FP.

    lambda_g^FP = (2^{2g-1} - 1) |B_{2g}| / (2^{2g-1} (2g)!)

    g=1: 1/24.  g=2: 7/5760.  g=3: 31/967680.
    """
    if g < 1:
        raise ValueError(f"Genus must be >= 1, got {g}")
    B_2g = _bernoulli_exact(2 * g)
    abs_B = abs(B_2g)
    numerator = (2**(2*g - 1) - 1) * abs_B
    denominator = Fraction(2**(2*g - 1)) * Fraction(factorial(2 * g))
    return numerator / denominator


def _ahat_coefficient(g: int) -> Fraction:
    r"""Coefficient a_g of the A-hat generating function.

    A-hat(ix) = (x/2)/sin(x/2) = 1 + sum_{g>=1} a_g x^{2g}.
    a_g = |B_{2g}| / (2 * (2g)!) * 2^{2g} = |B_{2g}| * 2^{2g-1} / (2g)!

    Actually the standard formula gives:
    (x/2)/sin(x/2) = sum_{k>=0} |B_{2k}| (2^{2k} - 2) / (2k)! * x^{2k}  ... no.

    Let us use the direct relation: lambda_g^FP from the GF
    sum_{g>=1} lambda_g x^{2g} = (x/2)/sin(x/2) - 1.

    So a_g = lambda_g^FP.
    """
    return _lambda_fp_exact(g)


