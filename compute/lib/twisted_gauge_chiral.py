def cs_propagator_pole_order(cy3: CY3HodgeData) -> int:
    r"""Pole order of the CS propagator on CY3 x C.

    The CS propagator on CY3 x C decomposes as:
      P(x,z; y,w) = G_X(x,y) * 1/(z-w)

    The pole in the C-direction is always simple (order 1).
    The Green's function G_X has a singularity of order 2d-2
    along the diagonal in X (for complex dimension d).

    For CY3 (d=3): G_X ~ 1/|x-y|^4 (in real coordinates).
    The OPE pole order along C is 1.
    """
    return 1  # Simple pole from the Cauchy kernel on C


def tree_level_ope_structure_constants(data: TwistedGaugeChiralData
                                       ) -> Dict[str, Any]:
    """Tree-level OPE structure constants from the CS propagator.

    For GL(N) on C^3 x C: the tree-level OPE gives affine gl_N at level k.

    J^a(z) J^b(w) ~ k delta^{ab} / (z-w)^2 + f^{ab}_c J^c(w) / (z-w)

    The structure constants f^{ab}_c are the Lie algebra structure constants.
    The level k comes from the normalization of the CS action.

    Returns a dictionary describing the OPE structure.
    """
    N = data.gauge_rank
    k = data.level

    result: Dict[str, Any] = {
        'gauge_group': data.gauge_group,
        'cy3': data.cy3.name,
        'tree_level_algebra': data.tree_level_algebra_type,
        'ope_pole_order': cs_propagator_pole_order(data.cy3),
        'level': k,
    }

    if data.cy3.name == "C^3":
        # Flat space: pure affine algebra
        result['generators'] = {
            'J': {
                'conformal_weight': 1,
                'multiplicity': data.gauge_dim,
                'source': 'H^{0,0}(C^3) tensor g',
            },
        }
        result['ope'] = {
            'JJ_pole_2': k,                    # level (Killing form)
            'JJ_pole_1': 'f^{ab}_c J^c',       # structure constants
        }
        # Central charge of gl_N at level k
        # c(sl_N, k) = k(N^2-1)/(k+N)
        # c(u(1)) = 1
        if data.gauge_group.startswith("GL"):
            c_sl = k * (N*N - 1) / (k + N)
            c_total = c_sl + 1
        else:
            c_total = k * (N*N - 1) / (k + N)
        result['central_charge'] = c_total

    elif data.cy3.name == "K3xE":
        # K3 x E: four families of generators from H^{0,*}
        h0star = data.cy3.dbar_cohomology_h0star
        result['generators'] = {
            'J': {
                'conformal_weight': 1,
                'multiplicity': h0star[0] * data.gauge_dim,
                'source': 'H^{0,0}(K3xE) tensor g',
            },
            'psi': {
                'conformal_weight': Fraction(1, 2),
                'multiplicity': h0star[1] * data.gauge_dim,
                'source': 'H^{0,1}(K3xE) tensor g',
            },
            'phi': {
                'conformal_weight': 0,
                'multiplicity': h0star[2] * data.gauge_dim,
                'source': 'H^{0,2}(K3xE) tensor g',
            },
            'eta': {
                'conformal_weight': Fraction(-1, 2),
                'multiplicity': h0star[3] * data.gauge_dim,
                'source': 'H^{0,3}(K3xE) tensor g',
            },
        }
        # The OPE is more complex: involves the K3 Green's function
        # with h^{1,1} = 20 Kahler moduli
        result['ope'] = {
            'JJ_pole_2': k,
            'JJ_pole_1': 'f^{ab}_c J^c',
            'additional_opes': 'from K3 cohomology pairing',
            'k3_moduli_dependence': True,
            'num_kahler_moduli': data.cy3.h11,
        }

    return result


