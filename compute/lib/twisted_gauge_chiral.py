@dataclass(frozen=True)
class TwistedGaugeChiralData:
    """Data for the boundary chiral algebra from twisted gauge theory on CY3 x C.

    The 5d holomorphic CS theory on CY3 x C with gauge group G produces
    a chiral algebra A on C whose properties depend on the CY3 geometry
    and the gauge group.

    Parameters:
        cy3: the CY3 variety data
        gauge_group: name of the gauge group (e.g., "GL(N)" or "SU(N)")
        gauge_rank: rank N of the gauge group
        gauge_dim: dimension of the Lie algebra g
        gauge_dual_coxeter: dual Coxeter number h^v
        level: the CS level (default 1 for twisted N=4)
    """
    cy3: CY3HodgeData
    gauge_group: str
    gauge_rank: int
    gauge_dim: int
    gauge_dual_coxeter: int
    level: Fraction = Fraction(1)

    @property
    def num_boundary_fields(self) -> int:
        """Number of boundary field species = dim H^{0,*}(CY3) * dim g."""
        return self.cy3.total_dbar_dim * self.gauge_dim

    @property
    def tree_level_algebra_type(self) -> str:
        """The tree-level boundary algebra type.

        For CY3 = C^3: affine g at level k (the cs level).
        For CY3 compact with nontrivial H^{0,*}: larger algebra involving
        the H^{0,*} generators.
        """
        if self.cy3.name == "C^3":
            return f"affine_{self.gauge_group}_level_{self.level}"
        return f"twisted_{self.gauge_group}_on_{self.cy3.name}"

    @property
    def kappa_gauge(self) -> Fraction:
        """kappa contribution from the gauge sector (affine algebra part).

        For gl_N at level k:
          kappa(sl_N, k) = (N^2-1)(k+N)/(2N)
          kappa(u(1), k) = k
          kappa(gl_N, k) = kappa(sl_N) + kappa(u(1))

        For su_N at level k:
          kappa(su_N, k) = (N^2-1)(k+N)/(2N)

        IMPORTANT (AP1, AP39): This is the GAUGE SECTOR kappa, not the
        full theory kappa.  For CY3 != C^3, there are additional
        contributions from the CY3 geometry.
        """
        N = self.gauge_rank
        k = self.level

        if self.gauge_group.startswith("GL"):
            # gl_N = sl_N + u(1)
            kappa_sl = Fraction(N * N - 1) * (k + N) / (2 * N)
            kappa_u1 = k
            return kappa_sl + kappa_u1
        elif self.gauge_group.startswith("SU"):
            return Fraction(N * N - 1) * (self.level + N) / (2 * N)
        else:
            raise ValueError(f"Unsupported gauge group: {self.gauge_group}")

    @property
    def kappa_cy3(self) -> Fraction:
        """kappa contribution from the CY3 geometry.

        For CY3 = C^3: no geometric contribution (flat space).
        For CY3 compact: chi^CY(X) is the geometric kappa.

        When the gauge group is trivial (N=1 abelian), the full kappa
        is entirely from the CY3 geometry.
        """
        return chi_cy(self.cy3)

    @property
    def kappa_total(self) -> Fraction:
        """Total modular characteristic.

        For CY3 = C^3 with GL(N): kappa = kappa_gauge (the affine algebra).
        For compact CY3 with GL(1): kappa = chi^CY(CY3).
        For compact CY3 with GL(N), N > 1: kappa = N * chi^CY(CY3) + corrections
        (the full formula depends on the specific theory and is known
        only in special cases).

        For the abelian case (N=1), the total kappa is chi^CY.
        For the non-abelian case on C^3, the total kappa is the gauge kappa.
        For the non-abelian case on compact CY3, we use the product formula
        where it is known.
        """
        if self.cy3.name == "C^3":
            return self.kappa_gauge
        elif self.gauge_rank == 1:
            return self.kappa_cy3
        else:
            # For GL(N) on compact CY3: the leading term at large N is
            # N^2 * chi^CY(CY3) from the trace in the adjoint.
            # At N = 1: just chi^CY.
            # The exact formula for general N involves the 't Hooft coupling.
            # We give the tree-level (perturbative) answer.
            return Fraction(self.gauge_rank**2) * self.kappa_cy3

    @property
    def shadow_class(self) -> str:
        """Shadow depth class from the CY3 geometry.

        G (Gaussian, r_max=2): C^3 with abelian gauge, conifold
        L (Lie/tree, r_max=3): C^3 with non-abelian gauge (current algebra)
        C (contact, r_max=4): special non-compact CY3s
        M (mixed, r_max=inf): K3 x E, quintic, general compact CY3
        """
        if self.cy3.name == "C^3":
            if self.gauge_rank == 1:
                return "G"  # Heisenberg
            else:
                return "L"  # Affine KM
        elif self.cy3.name == "conifold":
            return "G"
        elif self.cy3.name == "T^6":
            return "G"  # Abelian holonomy -> Heisenberg-like
        else:
            return "M"  # K3 x E, quintic: infinite tower

    def F_g(self, g: int) -> Fraction:
        """Genus-g free energy F_g = kappa_total * lambda_g^FP.

        This is the scalar-lane formula (uniform-weight, AP32).
        """
        return self.kappa_total * _lambda_fp_exact(g)


