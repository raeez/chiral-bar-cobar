def kappa_vs_c_half(data: TwistedGaugeChiralData) -> Dict[str, Any]:
    """Check kappa vs c/2 (AP39: they differ for non-Virasoro families).

    For GL(N) on C^3:
      kappa(gl_N, k=1) = (N^2-1)(1+N)/(2N) + 1
      c(gl_N, k=1)/2 = ((N^2-1)/(1+N)*1 + 1) / 2 = (N-1+1)/2 = N/2

    These are equal ONLY for N=1 (Heisenberg: kappa = 1, c/2 = 1/2... no).

    Actually for N=1, GL(1) at level 1:
      kappa(sl_1) = 0 (sl_1 is trivial, dim=0)
      kappa(u(1)) = 1
      kappa(gl_1) = 1

      c(sl_1) = 0
      c(u(1)) = 1
      c(gl_1) = 1
      c/2 = 1/2

    So kappa = 1 != c/2 = 1/2 already at N=1.
    This is because kappa(Heisenberg at level k) = k, while c(Heis) = 1,
    so kappa = k != c/2 = 1/2.  (AP39 in full force.)
    """
    kappa = data.kappa_total
    # Compute central charge
    N = data.gauge_rank
    k = data.level
    if data.cy3.name == "C^3" and data.gauge_group.startswith("GL"):
        c_sl = k * (N*N - 1) / (k + N) if N > 1 else Fraction(0)
        c_total = c_sl + 1
    else:
        c_total = None

    return {
        'kappa': kappa,
        'c_total': c_total,
        'c_half': c_total / 2 if c_total is not None else None,
        'equal': kappa == c_total / 2 if c_total is not None else None,
    }


def kappa_duality_check(data: TwistedGaugeChiralData) -> Dict[str, Any]:
    """Check kappa + kappa' for the Koszul dual pair (AP24).

    For affine KM at level k:
      kappa(g, k) = dim(g)(k+h^v)/(2h^v)
      kappa(g, k') = dim(g)(k'+h^v)/(2h^v) where k' = -k - 2h^v
                   = dim(g)(-k-h^v)/(2h^v) = -dim(g)(k+h^v)/(2h^v)
                   = -kappa(g, k)

    So kappa + kappa' = 0 for KM (AP24: this is specific to KM, not universal).

    For GL(N) = SL(N) x U(1):
      kappa(sl_N, k) + kappa(sl_N, k') = 0  (KM duality)
      kappa(u(1), k) + kappa(u(1), -k) = k + (-k) = 0
    Total: kappa(gl_N, k) + kappa(gl_N, k') = 0.  Anti-symmetric.
    """
    if data.cy3.name != "C^3":
        # Duality for compact CY3 is different
        return {
            'applicable': False,
            'reason': 'KM duality applies to C^3 gauge theory only',
        }

    N = data.gauge_rank
    k = data.level
    h_v = data.gauge_dual_coxeter

    # Feigin-Frenkel dual level
    k_dual = -k - 2 * h_v if h_v > 0 else -k

    # Compute kappa at dual level
    if data.gauge_group.startswith("GL"):
        kappa_sl = Fraction(N*N - 1) * (k + N) / (2 * N)
        kappa_sl_dual = Fraction(N*N - 1) * (k_dual + N) / (2 * N)
        kappa_u1 = k
        kappa_u1_dual = k_dual if h_v > 0 else -k  # For U(1): k -> -k
        # Actually for the full GL(N) duality, the U(1) part goes k -> -k
        kappa_u1_dual = -k

        kappa_original = kappa_sl + kappa_u1
        kappa_dual = kappa_sl_dual + kappa_u1_dual
    else:
        kappa_original = Fraction(N*N - 1) * (k + N) / (2 * N)
        kappa_dual = Fraction(N*N - 1) * (k_dual + N) / (2 * N)

    return {
        'applicable': True,
        'k': k,
        'k_dual': k_dual,
        'kappa': kappa_original,
        'kappa_dual': kappa_dual,
        'sum': kappa_original + kappa_dual,
        'anti_symmetric': kappa_original + kappa_dual == Fraction(0),
    }


def genus_expansion_consistency(data: TwistedGaugeChiralData,
                                g_max: int = 5) -> Dict[str, Any]:
    """Verify genus expansion F_g = kappa * lambda_g^FP for g = 1, ..., g_max.

    Cross-checks:
    (1) F_g is positive for kappa > 0
    (2) F_g decays like 1/(2pi)^{2g} (Bernoulli asymptotics)
    (3) The A-hat generating function matches: sum F_g x^{2g} = kappa * (A-hat(ix) - 1)
    """
    kappa = data.kappa_total

    results: Dict[int, Fraction] = {}
    for g in range(1, g_max + 1):
        F = kappa * _lambda_fp_exact(g)
        results[g] = F

    # Check positivity
    positive = all(F >= 0 for F in results.values()) if kappa >= 0 else True

    # Check decay: |F_{g+1}/F_g| should decrease
    ratios = {}
    for g in range(1, g_max):
        if results[g] != 0:
            ratios[g] = abs(results[g + 1] / results[g])

    return {
        'kappa': kappa,
        'F_g': results,
        'positive_for_kappa_positive': positive,
        'decay_ratios': ratios,
    }


