# GAP 4: THE LAMBDA=0 UNCONDITIONAL TRICK
# ============================================================================
#
# The proof claims: Delta(E)*w_0 = 0*(a-b-1/2) = 0, "unconditionally."
# This is correct AS STATED but there's a subtlety: the formula
# Delta(E)*w_lambda = lambda*(a-b-(lambda+1)/2) is derived for
# FINITE-DIMENSIONAL highest weight modules (where w_lambda is
# the standard singular vector in V_1 tensor V_lambda).
#
# For infinite-dimensional modules like L^-, the Yangian coproduct
# formula needs to be VERIFIED, not assumed.


class TestGap4_Lambda0UnconditionalTrick:
    """Attack surface: Yangian coproduct on infinite-dim modules."""

    def test_coproduct_formula_derivation(self):
        """The coproduct Delta(E) = E tensor 1 + 1 tensor E + (1/2) h tensor e
        is valid for ANY tensor product, not just finite-dim ones.

        This is because the coproduct is a HOMOMORPHISM of algebras:
        it's defined on the Yangian Y(g), not on the modules.

        So Delta(E)*w_0 = (E tensor 1 + 1 tensor E + (1/2) h tensor e)*w_0
        is well-defined for w_0 in V_1(a) tensor L^-(b), regardless of
        L^-(b) being infinite-dimensional.
        """
        # The formula Delta(E)*w_lambda = lambda*(a-b-(lambda+1)/2)
        # is computed by:
        #   1. w_lambda = v_+ tensor f^lambda * v_0 (up to normalization)
        #   2. E tensor 1 acts on the V_1 factor
        #   3. 1 tensor E acts on the L^- factor
        #   4. h tensor e is the "correction" term

        # For lambda=0: w_0 = v_+ tensor v_0 - (some correction)
        # Actually, the singular vector in V_1 tensor M(lambda) is:
        #   w = lambda * (v_- tensor v_lambda) - (v_+ tensor f*v_lambda)
        # At lambda=0:
        #   w = 0 * (v_- tensor v_0) - (v_+ tensor f*v_0)
        #     = -(v_+ tensor f*v_0)

        # The e-annihilation: e*w = -(e*v_+) tensor f*v_0 - v_+ tensor (e*f*v_0)
        #                         = 0 - v_+ tensor h*v_0
        #                         = -v_+ tensor 0*v_0 = 0
        # (because h*v_0 = 0 for L^- which has hw = 0)

        # This is a VALID computation for L^- as well as M(0).
        # The key: h*v_0 = 0 requires the highest weight vector of L^-
        # to have weight 0, which is TRUE by definition.

        # Now for the YANGIAN generator E:
        # Delta(E)*w = (E tensor 1)*w + (1 tensor E)*w + correction terms
        # On V_1(a): E = a*e (evaluation). So E*v_+ = a*(e*v_+) = 0.
        # On L^-(b): E acts as b*e + ... (the Yangian action).
        # Specifically, E*v_0 = ... This depends on the Yangian structure of L^-.

        # The CRUCIAL POINT: the formula Delta(E)*w_lambda = lambda*(...)
        # is derived for VERMA modules M(lambda), where the Yangian
        # structure is completely determined by the evaluation map.
        # For L^-, the Yangian structure is different — L^- is NOT
        # an evaluation module.

        # HOWEVER: the computation only uses:
        #   (a) e*v_0 = 0 (highest weight)
        #   (b) h*v_0 = 0 (weight zero)
        #   (c) E*v_0 = ??? (this is where L^- differs from M(0))

        # For L^-(b): E*v_0 is NOT necessarily zero. The Yangian
        # generator E on the prefundamental module can have a
        # nontrivial action on the highest weight vector.

        # Wait — E is the Yangian raising generator. If v_0 is a
        # highest weight vector for both sl_2 AND the Yangian
        # (i.e., a highest ell-weight vector), then E*v_0 = 0.

        # For L^-: v_0 IS a highest ell-weight vector (by definition).
        # So E*v_0 = 0 in L^-.

        # Conclusion: the lambda=0 trick IS valid for L^-.
        # The gap is COSMETIC — the argument needs to be stated
        # more carefully (specifying that v_0 is highest ell-weight,
        # not just highest weight).

        # Verify: the computation gives 0 for any parameter values
        for a in [0, 1, 2.5, -3, 100]:
            for b in [0, 1, 2.5, -3, 100]:
                # lambda=0: result = 0 * (a - b - 1/2) = 0
                result = 0 * (a - b - 0.5)
                assert result == 0.0

    def test_yangian_coproduct_well_defined_on_tensor(self):
        """Verify that the Yangian coproduct makes sense on
        V_1(a) tensor L^-(b), even though L^-(b) is infinite-dim.

        The coproduct Delta: Y -> Y tensor Y is well-defined at the
        algebra level. Its action on V tensor W is:
          Delta(x) * (v tensor w) = sum x'(v) tensor x''(w)

        For each v in V_1 (finite-dim), this produces a FINITE sum.
        The resulting vector is in V_1 tensor L^-, which is well-defined
        (tensor product of a finite-dim with an infinite-dim module).
        """
        # The tensor product V_1 tensor L^- is well-defined as a module.
        # At each weight level, the multiplicity is finite.
        L_char = prefundamental_character_sl2(depth=30)
        V1 = eval_module_V1()
        tensor = tensor_product_characters(V1, L_char)

        # All multiplicities should be finite
        for w, m in tensor.items():
            assert m < float('inf')
            assert m >= 0

        # At weight 1: mult = p(0) = 1
        assert tensor.get(1, 0) == 1
        # At weight -1: mult = p(0) + p(1) = 2
        assert tensor.get(-1, 0) == 2


