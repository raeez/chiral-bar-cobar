class TestGap6_HigherExtObstructions:
    """Attack surface: higher Ext between evaluation and prefundamental."""

    def test_resolution_length_grows_without_bound(self):
        """The obstruction sequence delta(k) = p(k) - 1 grows without bound.

        For a finite resolution of M(0) by L^- modules:
          ... -> C_r -> ... -> C_1 -> C_0 -> M(0) -> 0
        the alternating character sum must cancel delta(k) at each weight.

        The resolution length r must satisfy: the r-th stage reduces
        the obstruction to zero. But the obstruction delta(k) grows
        as exp(pi*sqrt(2k/3)), which is SUB-exponential but SUPERPOLYNOMIAL.

        This means: any finite resolution has length that grows
        with the weight depth, and a FINITE-LENGTH resolution
        cannot work for ALL weights simultaneously.
        """
        obs = pk_obstruction_sequence(max_k=40)

        # The obstruction grows without bound
        deltas = [d["delta_k"] for d in obs]
        assert deltas[-1] > 100, (
            f"delta(40) = {deltas[-1]}, growing without bound"
        )

        # The growth is superpolynomial: delta(k) > k^2 for large k
        for k in range(10, 41):
            delta = partition_function(k) - 1
            assert delta > k, (
                f"delta({k}) = {delta} > {k}: superlinear growth"
            )

        # This means: a FINITE resolution of M(0) by L^- cannot
        # have a bounded number of terms independent of the weight depth.
        # The resolution must be INFINITE (a chain complex, not a
        # finite exact sequence).

        # In a triangulated category, thick generation allows
        # finite iterated cones. An infinite resolution produces
        # an object in the LOCALIZING subcategory (closure under
        # infinite coproducts), not the thick subcategory.

    def test_resolution_convergence_analysis(self):
        """Analyze whether the greedy resolution converges.

        At each stage of the resolution, we subtract shifted L^- modules
        to cancel the leading obstruction. Does the residual decrease?
        """
        result = resolution_obstruction_dimensions(lam=0, max_degree=8, depth=40)

        # The residual norm should eventually decrease
        residuals = [s.get("residual_norm", 0) for s in result["stages"]]

        # Check if the resolution is making progress
        # (the first nonzero position moves deeper at each stage)
        shifts = result["shifts_used"]
        if len(shifts) >= 3:
            # The shifts should be non-decreasing (resolution pushes deeper)
            assert result["converging"], (
                f"Resolution shifts: {shifts}. "
                "The first nonzero obstruction is NOT moving deeper — "
                "the resolution may NOT converge."
            )

    def test_ext_dimension_growth_between_generators(self):
        """Compute dimension bounds on Ext^i(L^-, V_n) and check finiteness.

        For thick generation to work, we need:
          projdim(L^-) < infinity in the category generated by {V_n, L^-}.

        If L^- has infinite projective dimension over the evaluation
        modules, thick generation fails (would need infinitely many cones).
        """
        depth = 40
        L_char = prefundamental_character_sl2(depth=depth)

        # For each V_n, compute the weight-space overlap (Hom bound)
        hom_bounds = {}
        for n in range(0, 21, 2):  # only even n have overlap
            Vn = eval_module_Vn(n)
            bound = 0
            for w in L_char:
                if w in Vn:
                    bound += min(L_char[w], Vn[w])
            hom_bounds[n] = bound

        # Hom(L^-, V_n) has bound n/2 + 1 for even n
        for n in range(0, 21, 2):
            expected = n // 2 + 1
            assert hom_bounds[n] == expected, (
                f"Hom bound for V_{n}: got {hom_bounds[n]}, expected {expected}"
            )

        # The Hom bound grows LINEARLY with n. This is FINITE for each n,
        # but the total Ext can still be infinite if higher Ext^i contribute.

        # KEY OBSERVATION: the p(k) obstruction means that to resolve
        # M(0) from L^-, we need sum_{k>=2} (p(k)-1) "cancellation units."
        # Each V_n can cancel at most n/2 + 1 units.
        # Total cancellation needed for first 40 weights:
        total_needed = sum(partition_function(k) - 1 for k in range(2, 41))
        # Total available from V_0, V_2, ..., V_40:
        total_available = sum(n // 2 + 1 for n in range(0, 42, 2))

        # Is total_available >= total_needed?
        assert total_available > 0
        # For the comparison:
        ratio = total_needed / total_available if total_available > 0 else float('inf')

        # If ratio > 1, we need more evaluation modules than available
        # at the given depth. This probes the resolution complexity.
        assert ratio > 1, (
            f"Resolution ratio: {total_needed}/{total_available} = {ratio:.2f}. "
            f"Need more cancellation units than available from first 21 even V_n. "
            f"This shows the resolution requires MANY evaluation modules."
        )


