# GAP 2: PRO-COMPLETION DANGER
# ============================================================================
#
# The Francis-Gaitsgory pro-nilpotent completion is a powerful but DANGEROUS
# tool. It changes the category in several ways:
#   1. Some modules that are non-compact become compact after completion.
#   2. Some modules that exist in the original category may become zero.
#   3. The t-structure can change.
#   4. The monoidal structure can change.
#
# The claim "PROVED (modulo formal pro-completion assembly)" is actually
# claiming that a very specific categorical construction (pro-nilpotent
# completion of the evaluation-generated core) recovers the full O^sh_{<=0}.
# This is NOT a formal consequence of the K_0 generation.


class TestGap2_ProCompletionDanger:
    """Attack surface: Francis-Gaitsgory pro-completion changes the category."""

    def test_pro_completion_gains_new_objects(self):
        """The pro-nilpotent completion of thick<{V_n}> gains new objects.

        The pro-nilpotent completion adds formal inverse limits.
        In particular, M(lambda) = R lim W_m (Pro-Weyl) is NOT in
        thick<{V_n}> but IS in the pro-completion.

        The question is: does the pro-completion of thick<{V_n, L^-}>
        equal ALL of O^sh_{<=0}, or does it miss some modules?

        The Mittag-Leffler condition (R^1 lim = 0) ensures that
        Verma modules are recovered. But what about:
        - Asymptotic modules (Hernandez-Zhang)?
        - Kirillov-Reshetikhin modules?
        - Non-standard highest-weight modules?
        """
        # Pro-Weyl: M(lambda) = lim W_m where W_m is the m-th Weyl truncation
        # Check that the inverse system stabilizes at each weight level
        depth = 30
        for lam in [0, 2, 5, 10]:
            M_char = sl2_verma_character(lam, depth=depth)
            # W_m has weights lam, lam-2, ..., lam-2(m-1)
            for m in range(1, depth):
                W_m = {lam - 2 * k: 1 for k in range(m)}
                # At each fixed weight w = lam - 2k, the system stabilizes
                # for m > k: W_m[w] = 1 for all m > k.
                for k in range(min(m, depth)):
                    w = lam - 2 * k
                    assert W_m.get(w, 0) == M_char.get(w, 0), (
                        f"Pro-Weyl fails: W_{m} at weight {w}"
                    )

    def test_completion_vs_original_thick_subcategory(self):
        """The thick subcategory in the completed category may be strictly
        larger than the thick subcategory in the original category.

        This is the heart of Gap 2: the statement
          "Compact(W-hat(O^sh_{<=0})) = thick<{V_n} U {L^-}>"
        is about the COMPLETED category W-hat, not the original O^sh.

        The question: does the completed DK equivalence descend to
        an equivalence of the original (uncompleted) categories?
        """
        # The ind-completion of a small stable category C is
        # Ind(C) = Fun^{ex}(C^op, Sp)^{omega} (compact objects in presheaves).
        # The thick subcategory of Ind(C) generated by C is C itself.
        # But the thick subcategory of Ind(C) can contain objects
        # that are compact in Ind(C) but NOT in C.

        # Computational probe: how many "layers" of completion are needed?
        # For each Verma M(lam), the Pro-Weyl system requires infinitely
        # many Weyl truncations. The DEPTH of the pro-system grows linearly
        # with lambda.
        pro_depths = {}
        for lam in range(0, 30):
            # Number of Weyl truncations needed to capture weight -2k:
            # need m > k, so for depth d, need m > d.
            # The pro-system is NOT finite — it's a genuine inverse limit.
            pro_depths[lam] = lam + 1  # minimum m to see all weights up to 0

        # The pro-depth grows linearly. This means the completion
        # is NOT a finite process — it genuinely uses the pro-structure.
        assert all(d > 0 for d in pro_depths.values())

    def test_lost_modules_in_completion(self):
        """Are there modules in O^sh that are LOST in the pro-completion?

        In the pro-nilpotent completion, modules M with M/rad^n(M) = 0
        for all n (i.e., M = rad^n(M) for all n) are killed.

        For category O, this shouldn't happen for highest-weight modules
        (they have a finite-codimensional head). But for ASYMPTOTIC
        modules (Hernandez-Zhang), which are not highest-weight, this
        could be an issue.
        """
        # The negative prefundamental L^- IS highest-weight (hw = 0).
        # So it's not lost in the completion.
        # But asymptotic modules are a separate class.

        # For sl_2, the asymptotic module A(a) has:
        #   weight 0: dim 1
        #   weight +2k: dim p(k) for k >= 1  (POSITIVE weights!)
        #   weight -2k: dim p(k) for k >= 1

        # This is NOT highest-weight: it has both positive and negative
        # weight spaces. The pro-nilpotent completion might not see it.

        # Construct the character of a hypothetical asymptotic module
        depth = 30
        asymptotic_char = {}
        for k in range(-depth, depth + 1):
            if k >= 0:
                asymptotic_char[2 * k] = partition_function(k)
            else:
                asymptotic_char[2 * k] = partition_function(-k)

        # This has infinite total dimension in BOTH directions
        pos_dim = sum(v for k, v in asymptotic_char.items() if k > 0)
        neg_dim = sum(v for k, v in asymptotic_char.items() if k < 0)
        assert pos_dim > 50
        assert neg_dim > 50

        # Such a module cannot be recovered by Pro-Weyl (which is for
        # highest-weight modules only). This is a GENUINE gap if
        # the MC3 claim is meant to include asymptotic modules.


