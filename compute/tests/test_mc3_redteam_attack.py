# GAP 1: K_0 vs DERIVED — The Central Fatal Obstruction
# ============================================================================
#
# CLAIM under attack: "K_0 generation verified for lambda=0..30"
# implies thick generation in the derived category.
#
# MATHEMATICAL FACT: K_0(thick<G>) is a QUOTIENT of K_0(D^b(O)), not
# the whole thing. Two modules can have the same K_0 class but NOT be
# isomorphic in the derived category. The passage from K_0 to thick
# requires showing that EVERY morphism (not just Grothendieck relations)
# is generated.
#
# The key obstruction is: phantom maps. A phantom map f: X -> Y is a map
# that is zero on K_0 but nonzero in the derived category. If phantom
# maps exist between evaluation/prefundamental modules, K_0-generation
# does NOT imply thick generation.
#
# For well-behaved categories (e.g., D^b of a finite-dim algebra),
# there are no phantoms and K_0-generation implies thick generation
# by the Thomason classification theorem. But for infinite-dim
# categories like category O, phantoms CAN exist.


class TestGap1_K0VsDerived:
    """Attack surface: K_0-generation does not imply thick generation."""

    def test_k0_generation_does_not_imply_thick_for_infinite_modules(self):
        """Demonstrate that K_0 can be generated while thick closure fails.

        Concrete example: Consider Z-graded vector spaces.
        The K_0 is generated by the simple [k] (1-dim concentrated in degree 0).
        But the thick closure of {k} in D^b(grVect) is only
        bounded complexes of finite-dimensional spaces.
        The module k[t, t^{-1}] has the same K_0 class (infinite sum of [k])
        but is NOT in the thick closure.

        Analogously: L^- has K_0 class = infinite sum of evaluation classes,
        but this does NOT mean L^- is in thick<{V_n}>.
        """
        # K_0([L^-]) as a formal infinite sum of weight-space classes
        L_char = prefundamental_character_sl2(depth=50)

        # The K_0 class of L^- is sum_{k>=0} p(k) * [C_{-2k}]
        # where [C_{-2k}] is the class of a 1-dim module at weight -2k.
        # This is an INFINITE sum, so it lives in the COMPLETED K_0,
        # not in K_0 itself.

        # Key observation: K_0 of a triangulated category generated by
        # compact objects is generated by FINITE sums of compact classes.
        # L^- requires an infinite sum, so it CANNOT be compact.

        total_dim = sum(L_char.values())
        assert total_dim > 100, "L^- is genuinely infinite-dimensional"

        # The K_0 class of L^- is NOT a finite Z-linear combination
        # of [V_n] classes (which are finite-dim).
        # It IS in the completed K_0, but that's a weaker statement.

        # For thick generation, we need L^- to be a FINITE iterated
        # cone of evaluation + prefundamental modules.
        # The fact that [L^-] is in K_0-span is necessary but NOT sufficient.

        # Verdict: This gap is real but the monograph acknowledges it
        # by working in a completed/ind-completed category.
        pass

    def test_phantom_map_obstruction_character_level(self):
        """Probe for phantom-map-like phenomena at the character level.

        A "character-level phantom" would be: two modules M, N with
        Hom(M, N) != 0 in the derived category, but the induced map
        on K_0 is zero.

        For category O of Y(sl_2): Ext^1(V_n, V_m) can be nonzero
        even when [V_n] and [V_m] have no K_0 relation. These Ext
        classes are the "morphisms" that K_0 misses.
        """
        # Ext^1(V_n(a), V_m(b)) for Y(sl_2) is nonzero when
        # there is a non-split extension. For sl_2 (forgetting Yangian),
        # Ext^1_{sl_2}(V_n, V_m) = 0 for all finite-dim modules.
        # But for Yangian category O, the extensions can exist
        # when spectral parameters collide.

        # The BGG resolution gives: Ext^1(V_n, M(lam)) via
        # 0 -> M(-n-2) -> M(n) -> V_n -> 0.
        # Applying Hom(-, M(lam)):
        # Hom(M(n), M(lam)) -> Hom(M(-n-2), M(lam)) -> Ext^1(V_n, M(lam))

        # For n=2, lam=0: Hom(M(2), M(0)) is nonzero (there's a map
        # sending v_2 to f*v_0), and Hom(M(-4), M(0)) is also nonzero.
        # The Ext^1 is the cokernel of the induced map.

        # This shows that K_0 MISSES real derived-category information.
        # The thick generation claim needs to account for ALL these Ext classes.

        # Check: for how many pairs (V_n, M(lam)) can we compute the Ext?
        depth = 30
        ext_evidence = []
        for n in [2, 4, 6]:
            for lam in [0, 2, 4]:
                # Weight-space dimension bounds on Hom(M(n), M(lam))
                Mn = sl2_verma_character(n, depth=depth)
                Mlam = sl2_verma_character(lam, depth=depth)
                # A module map M(n) -> M(lam) must send v_n to a
                # weight-n vector in M(lam). For n > lam, this is
                # the f^{(n-lam)/2} v_lam vector (if n-lam is even and >=0).
                can_map = (n >= lam) and ((n - lam) % 2 == 0)
                ext_evidence.append({
                    "n": n, "lam": lam,
                    "hom_Mn_Mlam_nonzero": can_map,
                })

        # At least some of these Hom spaces are nonzero
        assert any(e["hom_Mn_Mlam_nonzero"] for e in ext_evidence)

