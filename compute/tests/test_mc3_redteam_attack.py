        # At a = b: the coproduct formula Delta(E)*w_lam = lam*(a-b-(lam+1)/2)
        # gives at lam=0: Delta(E)*w_0 = 0*(a-b-1/2) = 0.
        # This is still zero. So the singular vector still exists.
        # BUT: the quotient V_1(a) tensor L^-(a) / Y*w_0 might NOT be
        # irreducible when a=b.

