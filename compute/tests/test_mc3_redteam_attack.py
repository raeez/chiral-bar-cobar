class TestGap5_OPolyDensity:
    """Attack surface: the passage from O_poly to full category O."""

    def test_opoly_is_strict_subcategory(self):
        """O_poly is a STRICT subcategory of O.

        O_poly contains modules whose composition factors are all
        evaluation modules V_n(a). This includes all finite-dim modules
        and their extensions.

        O_poly does NOT contain:
        - Prefundamental modules L^-(a) (their weight multiplicities
          are partition numbers, not 1).
        - Asymptotic modules.
        - Modules with infinite composition series.

        The thick closure of evaluation modules in D^b(O) is
        D^b(O_poly) by Lemma lem:fd-thick-closure. This CANNOT
        reach L^-(a).
        """
        # O_poly: generated by V_n(a) modules (all mults = 1).
        # Any module in O_poly has bounded weight multiplicities
        # (at each weight, the multiplicity is bounded by the
        # number of composition factors at that weight).

