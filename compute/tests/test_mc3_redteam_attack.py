# ============================================================================
#
# MC3 requires not just an equivalence of CATEGORIES but a BRAIDED
# MONOIDAL equivalence compatible with the Kazhdan-Lusztig (KL) functor.
#
# The KL functor: O(affine g) -> O(Yangian g) is a monoidal functor
# (respects tensor products). For the full DK/KL equivalence, this
# monoidal structure must be BRAIDED (respects the braiding/R-matrix).
#
# Even if the underlying categories match, the braided monoidal
# structure might NOT transfer. This is because:
# (a) The braiding involves the R-matrix, which depends on the
#     spectral parameter in a HOLOMORPHIC way.
# (b) The bar-cobar construction produces an A-infinity structure,
#     and the braided monoidal structure is an ADDITIONAL structure
#     on top of the A-infinity.
# (c) The transfer from the bar-cobar equivalence to a braided
#     monoidal equivalence requires COHERENCE DATA (associator,
#     braiding hexagons) that may not be produced by the bar-cobar.


class TestGap7_BraidedMonoidalCompatibility:
    """Attack surface: braided monoidal structure transfer."""

    def test_tensor_product_compatibility(self):
        """The tensor product structure on evaluation modules is
        determined by the R-matrix. The bar-cobar equivalence
        must intertwine tensor products.

        At the character level, this is automatic (tensor products
        of characters are just convolutions). But at the module level,
        the tensor product involves the coproduct Delta, which the
        bar-cobar equivalence must respect.
        """
        # Verify that the tensor product structure on eval modules
        # is consistent with the Baxter TQ relation.
        V1 = eval_module_V1()
        depth = 30

        # [V_1] * [V_1] = [V_2] + [V_0] (Clebsch-Gordan)
        V1V1 = tensor_product_characters(V1, V1)
        V2 = eval_module_Vn(2)
        V0 = eval_module_Vn(0)
        CG_rhs = sum_characters(V2, V0)
        assert formal_character_equal(V1V1, CG_rhs), "CG for V_1 x V_1"

        # [V_1] * [M(lam)] = [M(lam+1)] + [M(lam-1)] (Baxter TQ)
        for lam in range(0, 10):
            M_lam = sl2_verma_character(lam, depth=depth)
            tensor = tensor_product_characters(V1, M_lam)
            M_plus = sl2_verma_character(lam + 1, depth=depth)
            M_minus = sl2_verma_character(lam - 1, depth=depth)
            expected = sum_characters(M_plus, M_minus)
            assert formal_character_equal(tensor, expected), (
                f"Baxter TQ fails for M({lam})"
            )

        # The braided monoidal structure requires MORE than just K_0
        # compatibility. It requires:
        # 1. Associator: (A x B) x C -> A x (B x C) is a natural isomorphism
        # 2. Braiding: A x B -> B x A is a natural isomorphism
        # 3. Hexagon axioms: the braiding and associator satisfy coherence
        #
        # The bar-cobar equivalence produces (1) but NOT necessarily (2) and (3).
        # The braiding comes from the R-matrix, which is a SEPARATE structure.

    def test_r_matrix_holomorphy_obstruction(self):
        """The R-matrix R(u) for Y(sl_2) is a RATIONAL function of u.

        R(u) = u*I + P (for V_1 x V_1)

        The poles of R(u) are at u = 0 (P/u has a pole).
        The BRAIDING of the category uses R(u-v) where u, v are
        evaluation parameters.

        For the braided monoidal structure to transfer through the
        bar-cobar equivalence, the R-matrix must be compatible with
        the bar differential. This is a NONTRIVIAL constraint.
        """
        # R(u) = u*I + P has a pole-free region for Re(u) >> 0.
        # The bar-cobar equivalence works at the "formal" level
        # (A-infinity categories over the formal punctured disk).
        # The braiding requires ANALYTIC continuation to all u.

        # Check: the R-matrix eigenvalues
        # On Sym^2: eigenvalue u+1 (zero at u=-1)
        # On Alt^2: eigenvalue u-1 (zero at u=1)

        # These zeros correspond to the SIMPLE tensor products:
        # V_1(a) x V_1(a+1) has a non-split extension (V_2 or V_0).

        # The braided monoidal structure requires these R-matrix
        # zeros to be COMPATIBLE with the exact sequences produced
        # by the bar-cobar construction. This is the content of
        # the Drinfeld-Kohno theorem at the derived level.

        # For V_1 x V_1:
        for u in [0.5, 1.0, 2.0, 5.0]:
            # R-matrix eigenvalues
            sym_eigenvalue = u + 1
            alt_eigenvalue = u - 1
            # Neither is zero for generic u
            if u != 1.0:
                assert alt_eigenvalue != 0
            if u != -1.0:
                assert sym_eigenvalue != 0


