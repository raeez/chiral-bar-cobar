"""Tests for finite generation of the σ-invariant shadow ring.

Verifies the THEOREM: (A^sh)^σ for Virasoro is generated by three elements
(Δ^(2), Δ^(3), Δ^(4)). All higher entries are ring products.
"""

from sympy import Rational, simplify
import pytest

from compute.lib.sigma_ring_finite_generation import (
    generators,
    level_independence_classification,
    sigma_invariant_from_products,
    sigma_invariant_tower,
    verify_finite_generation,
    virasoro_shadow_coefficients,
)


class TestShadowCoefficients:
    def test_S2(self):
        from sympy import Symbol
        c = Symbol('c')
        S = virasoro_shadow_coefficients(4)
        assert simplify(S[2] - c / 2) == 0

    def test_S3(self):
        S = virasoro_shadow_coefficients(4)
        assert simplify(S[3] - 2) == 0

    def test_S4(self):
        from sympy import Symbol
        c = Symbol('c')
        S = virasoro_shadow_coefficients(4)
        expected = Rational(10) / (c * (5 * c + 22))
        assert simplify(S[4] - expected) == 0


class TestSigmaInvariantTower:
    def test_Delta2_is_13(self):
        D = sigma_invariant_tower(4)
        assert simplify(D[2] - 13) == 0

    def test_Delta3_is_4(self):
        D = sigma_invariant_tower(4)
        assert simplify(D[3] - 4) == 0

    def test_Delta4_level_dependent(self):
        from sympy import Symbol, diff
        c = Symbol('c')
        D = sigma_invariant_tower(4)
        assert simplify(diff(D[4], c)) != 0


class TestFiniteGeneration:
    def test_arity5(self):
        """Δ^(5) equals the ring product of Δ^(3) and Δ^(4)."""
        results = verify_finite_generation(5)
        assert results[5] is True

    def test_arity6(self):
        """Δ^(6) equals the ring product of lower generators."""
        results = verify_finite_generation(6)
        assert results[6] is True

    def test_arity7(self):
        """Δ^(7) equals the ring product of lower generators."""
        results = verify_finite_generation(7)
        assert results[7] is True

    def test_all_through_7(self):
        results = verify_finite_generation(7)
        assert all(results.values()), f"Failed at arities: {[r for r,v in results.items() if not v]}"


class TestGenerators:
    def test_three_generators(self):
        g = generators()
        assert len(g) == 3
        assert "Delta_2" in g
        assert "Delta_3" in g
        assert "Delta_4" in g

    def test_perturbative_generators_constant(self):
        g = generators()
        assert g["Delta_2"] == 13
        assert g["Delta_3"] == 4


