class TestR1EvaluationStability:
    """R1: V_n ‚äó L‚Åª = ‚äï L‚Åª(shifted) ‚Äî evaluation-stability consequences."""

    def test_n_components_equals_n_plus_1(self):
        """V_n ‚äó L‚Åª has exactly n+1 components."""
        for n in range(1, 9):
            r = prefundamental_clebsch_gordan(n)
            assert r["n_components"] == n + 1

    def test_highest_weights_arithmetic_progression(self):
        """Highest weights form {n, n-2, ..., -n}: arithmetic progression."""
        for n in range(1, 9):
            r = prefundamental_clebsch_gordan(n)
            expected = [n - 2 * j for j in range(n + 1)]
            assert r["highest_weights"] == expected

    def test_cg_deep_depth_60(self):
        """CG holds at depth 60 for n=1..5."""
        for n in range(1, 6):
            r = prefundamental_clebsch_gordan(n, depth=60)
            assert r["match"], f"CG failed at n={n}, depth=60"

    def test_k0_ring_closure(self):
        """K_0 ring generated by {[V_n], [L‚Åª]} is closed under tensor."""
        from compute.lib.sl2_baxter import eval_module_Vn, tensor_product_characters
        from compute.lib.hjz_prefundamental import prefundamental_character_sl2

        L = prefundamental_character_sl2(depth=30)
        # [V_1] * [V_1] = [V_2] + [V_0] (known)
        # [V_1] * [L‚Åª] = [L‚Åª(1)] + [L‚Åª(-1)] (R1 with n=1)
        r = prefundamental_clebsch_gordan(1, depth=30)
        assert r["match"], "K_0 closure via R1 failed"

        # [V_2] * [L‚Åª] = [L‚Åª(2)] + [L‚Åª(0)] + [L‚Åª(-2)] (R1 with n=2)
        r2 = prefundamental_clebsch_gordan(2, depth=30)
        assert r2["match"], "K_0 closure via R1 at n=2 failed"

    def test_containment_all_lambda_via_r1(self):
        """R1 implies containment for ALL Œª, not just tested range.

        Proof: V_Œª ‚äó L‚Åª = ‚äï_{j=0}^Œª L‚Åª(Œª-2j) by R1.
        Each L‚Åª(Œª-2j) has weight Œª-2j-2k with mult p(k).
        The j=0 term L‚Åª(Œª) has weight Œª-2k with mult p(k) ‚â• 1 = dim M(Œª)_{Œª-2k}.
        So ch(M(Œª)) ‚â§ ch(L‚Åª(Œª)) ‚â§ ch(V_Œª ‚äó L‚Åª).
        """
        for lam in [0, 5, 10, 20, 30]:
            r = verma_in_k0_span_of_prefundamental(lam, depth=40)
            assert r["containment"], f"Containment failed at Œª={lam}"


