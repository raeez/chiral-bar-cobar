class TestR3PartitionExtraction:
    """R3: K - L‚Åª(hw=-4) recovers partition numbers p(k)."""

    def test_r3_partition_recovery(self):
        """Subtracting L‚Åª(hw=-4) from K recovers p(k) at shifted weights."""
        from compute.lib.sl2_baxter import sl2_verma_character, subtract_characters
        from compute.lib.hjz_prefundamental import (
            partition_function,
            prefundamental_character_sl2,
        )

        depth = 50
        L = prefundamental_character_sl2(depth=depth)
        M0 = sl2_verma_character(0, depth=depth)
        K = subtract_characters(L, M0)

        # L‚Åª(hw=-4) has weight -4-2k with mult p(k)
        L_shifted = {-4 - 2 * k: partition_function(k) for k in range(depth)}
        R = subtract_characters(K, L_shifted)

        # R at weight -2m should be:
        # K(-2m) - L‚Åª(-4)(-2m) = (p(m)-1) - p(m-2)
        for m in range(2, depth - 2):
            expected = partition_function(m) - 1 - partition_function(m - 2)
            actual = R.get(-2 * m, 0)
            assert actual == expected, \
                f"R3 residual at weight {-2*m}: got {actual}, expected {expected}"

    def test_hardy_ramanujan_growth(self):
        """Partition numbers p(k) match Hardy-Ramanujan asymptotics."""
        import math
        from compute.lib.hjz_prefundamental import partition_function

        for k in [10, 20, 30, 40, 50]:
            pk = partition_function(k)
            hr = math.exp(math.pi * math.sqrt(2 * k / 3)) / (4 * k * math.sqrt(3))
            ratio = pk / hr
            # Ratio should approach 1 for large k
            assert 0.5 < ratio < 2.0, \
                f"HR ratio at k={k}: {ratio:.4f} (p(k)={pk}, HR={hr:.2f})"


