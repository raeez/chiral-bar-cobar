class TestR2KernelDecomposition:
    """R2: K = ch(L‚Åª) - ch(M(0)) has coefficients = cumulative Œ£ p‚ÇÇ(k)."""

    def test_kernel_coefficients_are_pk_minus_1(self):
        """K at weight -2m equals p(m) - 1 for all m."""
        from compute.lib.sl2_baxter import sl2_verma_character, subtract_characters
        from compute.lib.hjz_prefundamental import (
            partition_function,
            prefundamental_character_sl2,
        )

        depth = 100
        L = prefundamental_character_sl2(depth=depth)
        M0 = sl2_verma_character(0, depth=depth)
        K = subtract_characters(L, M0)

        for m in range(depth):
            w = -2 * m
            assert K.get(w, 0) == partition_function(m) - 1, \
                f"K at weight {w}: got {K.get(w, 0)}, expected {partition_function(m) - 1}"

    def test_kernel_deep_depth_100(self):
        """R2 kernel pattern verified to depth 100."""
        from compute.lib.sl2_baxter import sl2_verma_character, subtract_characters
        from compute.lib.hjz_prefundamental import (
            partition_function,
            prefundamental_character_sl2,
        )

        L = prefundamental_character_sl2(depth=100)
        M0 = sl2_verma_character(0, depth=100)
        K = subtract_characters(L, M0)
        for m in range(100):
            assert K.get(-2 * m, 0) == partition_function(m) - 1

    def test_kernel_vanishes_at_weight_0_and_neg2(self):
        """K at weight 0 = p(0)-1 = 0 and weight -2 = p(1)-1 = 0."""
        from compute.lib.sl2_baxter import sl2_verma_character, subtract_characters
        from compute.lib.hjz_prefundamental import prefundamental_character_sl2

        L = prefundamental_character_sl2(depth=20)
        M0 = sl2_verma_character(0, depth=20)
        K = subtract_characters(L, M0)
        assert K.get(0, 0) == 0
        assert K.get(-2, 0) == 0

    def test_kernel_first_nonzero_at_weight_neg4(self):
        """First nonzero kernel entry at weight -4: p(2)-1 = 1."""
        from compute.lib.sl2_baxter import sl2_verma_character, subtract_characters
