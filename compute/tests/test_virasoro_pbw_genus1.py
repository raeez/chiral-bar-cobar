    def test_weight4_one_new(self):
        """V-bar_4 has 1 new lowest weight vector (spin-3 rep starting here).

        dim V-bar_4 = 2, rank L_1 = 1 (onto V-bar_3 = dim 1), so ker = 1.
        """
        dims = lowest_weight_dimensions(5)
        assert dims[4] == 1

